		The valency effects of each type — and in particular the A-incorporation type, unusual
		cross-linguistically — are treated in <Xr ch="noun-incorporation-object" />,
		<Xr ch="noun-incorporation-subject-possessor-stranding" />, and
		<Xr ch="noun-incorporation-oblique-polysynthesis-debate" />.
	</p>
	<p>
		How productive and how polysynthetic Ainu incorporation is in formal terms is contested.
		Shibatani (1988) analysed Ainu incorporation as a productive type-I lexical compounding process
		without semantic bleaching of the incorporated nominal, placing Ainu at the strong end of the
		polysynthesis scale <Ref k="shibatani1988" />. Bugaeva's (2022) characterization as "open
		head-marking" identifies why: the inventory of what may be incorporated or stacked is
		unconstrained by a fixed paradigm, unlike the closed agreement paradigms of dependent-marking
		languages <Ref k="bugaeva2022" p="Ch.1 §4.7" /> ‹contested›. The applicative system
		(<i lang="ain-Latn">e-</i>, <i lang="ain-Latn">ko-</i>, <i lang="ain-Latn">o-</i>) further
		intersects with incorporation: an applicative argument can be simultaneously incorporated in
		ways that bear on the syntactic status of both operations
		<Ref k="kaiser1998" />; these interactions are treated in
		<Xr ch="applicative-system-overview" />.
	</p>
</S>

<S t="SOV order and the head-final clause" id="sov">
	<p>
		The basic constituent order of Hokkaido Ainu clauses is SOV: subject precedes object, the finite
		verb closes the clause, and the clause is strictly head-final throughout. Modifiers precede their
		nominal heads; subordinate clauses precede the matrix verb; postpositional phrases — built on
		relational nouns and postpositions — follow the head noun with the adpositional element in final
		position <Ref k="bugaeva2022" p="Ch.1 §4.3" />; <Ref k="sato2025a" p="361" />;
		<Ref k="nakagawa2024" p="§4" />. The relational-noun system and the postpositional inventory are
		treated in <Xr ch="relational-and-spatial-nouns" /> and
		<Xr ch="postpositions-local-case-and-motion-events" />.
	</p>
	<p>
		The SOV/head-final pattern sits in a typological tension with the head-marking type. GPR
		head-marking languages tend toward verb-initial order; Ainu's SOV gives it a surface resemblance
		to its dependent-marking neighbours (Japanese, Korean, Turkic) that does not extend to structural
		organization <Ref k="bugaeva2022" p="Ch.1 §4.3, §7" />. Bugaeva accounts for this via pragmatic
		right-dislocation of subject and object NPs: when arguments are shifted rightward for discourse
		reasons, the resulting surface order approaches verb-initial, aligning Ainu with the pragmatically
		common patterns of the GPR grouping <Ref k="bugaeva2022" p="Ch.1 §4.3" />. The full syntax of
		constituent order and argument realization is in <Xr ch="constituent-order-head-final" />.
	</p>
	<p>
		Community member @aomidori_cs_ (aynu-corpora Discord, 2024-01-29) has proposed that the SOV
		order is not original to Ainu but the result of contact with Northeast Asian SOV-dominant
		neighbours, citing the preverbal position of the negator
		<A w="somo" gl="not (clausal negator)" /> as a cross-linguistic diagnostic found in SVO
		languages <Ref k="aynucorporadiscord" /> ‹speculative›. No comparative reconstruction of proto-Ainu
		word order is currently available to evaluate the proposal.
	</p>
</S>

<S t="The alignment profile: mixed morphological type" id="alignment">
	<p>
		The person-affix paradigm of Hokkaido Ainu shows a typologically mixed alignment. The
		first-singular row is nominative–accusative: the same prefix <i lang="ain-Latn">ku=</i> marks
		both S (intransitive subject) and A (transitive subject), opposed to <i lang="ain-Latn">en=</i>
		for O. The second-person rows are neutral: <i lang="ain-Latn">e=</i> (2SG) and
		<i lang="ain-Latn">eci=</i> (2PL) cover S, A, and O alike. The third person is zero in all
		three roles. The first-plural exclusive and the fourth person are tripartite: S is marked by a
		<em>suffix</em> (<i lang="ain-Latn">=as</i>, <i lang="ain-Latn">=an</i>) while A is marked by
		a <em>prefix</em> (<i lang="ain-Latn">ci=</i>, <i lang="ain-Latn">a=</i>) and O by a
		distinct prefix (<i lang="ain-Latn">un=</i>, <i lang="ain-Latn">i=</i>)
		<Ref k="nakagawa2024" p="165–169" />; <Ref k="bugaeva2012" p="472" />. This side-encoding
		— prefix for transitive A, suffix for intransitive S — is the morphological basis on which
		Bugaeva characterizes the overall system as "mixed, with elements of tripartite alignment"
		<Ref k="bugaeva2022" p="Ch.1 §1" />.
	</p>
	<p>
		The detailed person paradigm, portmanteau exponents, and the full alignment debate — tripartite,
		hierarchical, inverse — are treated in <Xr ch="person-marking-architecture" /> and
		<Xr ch="alignment-hierarchical-and-the-debate" />. The split-intransitive question, which in
		Ainu surfaces in verb classes and valency morphology rather than in the agreement paradigm, is
		treated in <Xr ch="alignment-split-intransitive" />.
	</p>
	<p>
		The <em>fourth person</em> (also termed the indefinite person) is a feature of the Ainu person
		system unusual enough to warrant mention here. A single form-set covers first-person-plural
		inclusive ('we including you'), indefinite/impersonal agent, second-person honorific address, and
		logophoric (protagonist) reference in oral-narrative direct speech
		<Ref k="bugaeva2022" p="Ch.1 §4.1" />; <Ref k="nakagawa2024" p="163–164, 174" />;
		<Ref k="bugaeva2008" p="40–43" />. Terminology for the set is unsettled across sources:
		Nakagawa uses "fourth person", Tamura lists the same forms as separate sub-persons, and Bugaeva
		analyses the quotation use as logophoric
		<Ref k="tamura1972" p="18–20" />; <Ref k="bugaeva2012" p="474–476" /> ‹contested›. The full
		referential range is catalogued in <Xr ch="indefinite-fourth-person-paradigm" /> and
		<Xr ch="fourth-person-honorific" />.
	</p>
</S>

<S t="Typologically unusual features in the regional context" id="unusual-features">
	<S t="Locative noun class and possessive alienability" id="noun-classes">
		<p>
			Ainu nouns divide into two classes in the syntax of spatial phrases
			<Ref k="bugaeva2022" p="Ch.1 §4.4" />; <Ref k="nakagawa2022pos" />. <em>Ordinary nouns</em>
			require a <em>locative noun</em> as an intermediary head (<A w="or" gl="place" />,
			<A w="ka" gl="top" />, <A w="sam" gl="side" />, <A w="tum" gl="middle" />) to form a spatial
			phrase; locative nouns can head a spatial phrase directly, as in
			<i lang="ain-Latn">pet or ta</i> 'at the river' (river + place + LOC). The locative/ordinary
			distinction helps track grammatical relations when the third person is zero-marked: a locative
			noun heads a spatial phrase and is not parsed as a bare object NP. The full system is treated
			in <Xr ch="relational-and-spatial-nouns" />.
		</p>
		<p>
			Property concepts that would be expressed by adjectives in related languages function as
			stative verbs in Ainu, with no separate adjective class
			<Ref k="bugaeva2022" p="Ch.1 §4.4" />; this is treated in
			<Xr ch="property-concept-predication" />.
		</p>
	</S>

	<S t="Vigesimal numerals" id="vigesimal">
		<p>
			The numeral system operates on a base-20 (vigesimal) frame, typologically unusual in the
			region. Ochiai (2021) documents a historical shift in the reckoning strategy within that frame
			— from a subtractive to an additive counting method — on the basis of dialect comparison
			<Ref k="ochiai2021" />. Verbal number is separately marked by suppletive singular/plural stem
			pairs (<i lang="ain-Latn">ek</i> / <i lang="ain-Latn">arki</i> 'come',
			<i lang="ain-Latn">arpa</i> / <i lang="ain-Latn">paye</i> 'go') and by the pluractional
			suffix <i lang="ain-Latn">-pa</i> <Ref k="bugaeva2022" p="Ch.1 §4.2" />;
			<Ref k="nakagawa2024" p="141–159" />; the full treatment of numeral morphology is in
			<Xr ch="vigesimal-numeral-system" /> and
			<Xr ch="numeral-classifiers-ordinals-and-quantifier-syntax" />; verbal number in
			<Xr ch="suppletive-verbal-number" />.
		</p>
	</S>

	<S t="The four-term evidential system" id="evidential">
		<p>
			Hokkaido Ainu encodes the speaker's source of information periphrastically through a
			nominalization-plus-copula construction. Four formal (defective) nouns —
			<A w="ruwe" gl="track, trace (evidential)" />,
			<A w="siri" gl="visible appearance (evidential)" />,
			<A w="hawe" gl="voice (evidential)" />, and
			<A w="humi" gl="sound, feeling (evidential)" /> — combine with the copula
			<A w="ne" gl="be (copula)" /> to yield a four-term paradigm encoding a visual-trace /
			inferential evidential, a direct-visual / situational evidential, a reportative, and a
			non-visual sensory evidential respectively
			<Ref k="dalcorso2018" p="304" />; <Ref k="nakagawa2024" p="258, 392" />;
			<Ref k="takahashi2022" />. The system is built on the grammaticalization of sensory nouns
			into a nominalization slot, a structural type unusual in Northeast Asia. All four terms and
			the schema they instantiate are treated in <Xr ch="evidential-schema-overview" />.
		</p>
	</S>
</S>

