<S t="The genetic isolate and its typological placement" id="typological-place">
	<p>
		Hokkaido Ainu has no demonstrated genetic relationship with any other language family
		<Ref k="bugaeva2022" p="Ch.1 §1" />; <Ref k="shibatani1990" p="§1" />. Proposals connecting
		Ainu to Japanese, Korean, Altaic branches (Tungusic, Mongolic, Turkic), Austronesian, Nivkh, or
		Chukotko-Kamchatkan have each failed to produce the systematic sound correspondences and shared
		morphology that genetic relationship requires. The fuller treatment of the isolate status and the
		history of macro-comparison controversies belongs to <Xr ch="genetic-position-macro-comparison" />.
	</p>
	<p>
		The isolate argument has a structural dimension that goes beyond the absence of lexical
		correspondences. Bugaeva's analysis frames Ainu as "a head-marking language of the Pacific Rim"
		— after the framework of <Ref k="nichols1986" /> — and treats the structural mismatch between
		Ainu's head-marking profile and the dependent-marking profile of all its geographical neighbours
		as a positive argument against relatedness
		<Ref k="bugaeva2022" p="Ch.1 §2, §7" />. Head-marking is "well attested in the Americas and
		Melanesia but vanishingly rare elsewhere" <Ref k="nichols1986" /> and characterizes the
		<em>Greater Pacific Rim</em> (GPR) macroarea — a population of languages whose distribution
		Nichols traces back more than ten millennia <Ref k="bugaeva2022" p="Ch.1 §2" />. Northeast Asia
		is predominantly dependent-marking: Japanese, Korean, Tungusic, Mongolic, and Turkic all mark
		syntactic relations on the dependent NP (nominal case, adnominal genitive, uninflected
		postpositions governing case). The head/dependent-marking parameter is among the most
		diachronically stable typological features known <Ref k="nichols1986" />; its consistent
		divergence between Ainu and every language proposed as its relative accordingly constitutes
		evidence independent of the lexical record <Ref k="bugaeva2022" p="Ch.1 §7" />.
	</p>
	<p>
		In a single typological characterization: Hokkaido Ainu is SOV, agglutinating, polysynthetic,
		incorporating, and head-marking at both clause and phrase level
		<Ref k="bugaeva2022" p="Ch.1 §1" />; <Ref k="shibatani1990" p="§1" />. The sections that
		follow treat each dimension of that profile in turn, closing with a WALS-style feature summary.
	</p>
</S>

<S t="Head-marking, indexing, and the absence of case" id="head-marking">
	<p>
		Nichols' parameter classifies languages by the locus of the morphological signal of a syntactic
		relation: on the head of the phrase, on the dependent, or on both. Head-marking correlates —
		verbal cross-reference of arguments, noun incorporation, instrumental/directional affixes on
		verbs, inflected adpositions, pronominal affixes on nouns — differ systematically from
		dependent-marking correlates — case on nouns, adnominal genitive marking, agreement suffixes on
		attributives, uninflected adpositions governing case <Ref k="nichols1986" />. Hokkaido Ainu
		shows the head-marking correlates and lacks all the dependent-marking ones across both clause and
		noun phrase <Ref k="bugaeva2022" p="Ch.1 §7" />; <Ref k="sato2025a" p="361" />.
	</p>
	<p>
		The grammatical role of a noun phrase in the clause is signalled by person indexes on the finite
		verb, not by case suffixes on the NP itself. A transitive verb carries an A-index for the
		agent/transitive-subject and may carry an O-index for the patient; an intransitive verb carries
		an S-index. The NP argument is caseless. Third-person arguments are zero-indexed; first and
		second persons carry overt prefixal indexes for A/S and O roles
		<Ref k="bugaeva2022" p="Ch.1 §4.1" />; <Ref k="nakagawa2024" p="163–169" />;
		<Ref k="bugaeva2012" p="471" />.
	</p>

	<Ex
		m="topenpe hekattar ku=kore"
		g="sweets children 1SG.A=give"
		tr="I gave the children sweets."
		cite="nakagawa2024:127"
		dial="SAR"
		place="Nakagawa ex. 11a"
		note="Both non-subject participants surface as bare NPs with no case morphology; the verb ku=kore indexes only the 1SG agent. Recipient vs theme is recoverable from the semantics of kore 'give' and from constituent order, not from case marking."
	/>

	<p>
		The absence of morphological case in an SOV agglutinating language is cross-linguistically
		unusual. Japanese and Korean, Ainu's closest geographical neighbours, pair SOV order with
		elaborate nominal case systems; Ainu makes the same constituent order work without case, relying
		entirely on verbal person-indexing and semantics for argument tracking
		<Ref k="bugaeva2022" p="Ch.1 §4.1, §4.4" />; <Ref k="sato2025a" p="361" />. WALS-type
		surveys classify Hokkaido Ainu as "borderline" or "zero" in case inventory
		<Ref k="bugaeva2022" p="Ch.1 §4.4" />.
	</p>
	<p>
		The full architecture of the S/A/O paradigm — four formal persons, the side-encoding of
		intransitive S versus transitive A, portmanteau exponents, and the mixed alignment that results
		— is set out in <Xr ch="person-marking-architecture" />.
	</p>
</S>

<S t="Possession indexed on the head noun" id="possession">
	<p>
		In the noun phrase the same head-marking logic operates. A possessed nominal does not require a
		case-marked genitive possessor; instead the head noun itself carries two signals of the possessive
		relation — a personal prefix agreeing with the possessor, and an affiliative suffix
		marking the noun as standing in a possessed relation
		<Ref k="bugaeva2022" p="Ch.1 §4.4" />; <Ref k="bugaeva2021poss" />.
	</p>

	<Ex
		m="ku=yup-ihi"
		g="1SG.POSS=elder.brother-POSS"
		tr="my elder brother"
		constructed
		note="The possessor relation is doubly marked on the head noun: ku= is the first-singular possessor prefix and -ihi is the affiliative (possessed) suffix. An independent possessor NP, when it appears, is caseless and appositive, not genitive-marked."
	/>

	<p>
		Within the nominal domain Ainu distinguishes <em>obligatorily-possessed (inalienable)</em>
		nouns — body parts, kin terms, and spatial-relational nouns that must carry a possessor — from
		nouns that are non-possessible <Ref k="bugaeva2022" p="Ch.1 §5.1" />. These possessive classes
		are absent from the surrounding Northeast Asian languages
		<Ref k="bugaeva2022" p="Ch.1 §7" /> and align Ainu with GPR-type possessive-class systems.
		The full classification, the morphophonology of the affiliative suffix, and the appositive
		possessive construction are treated in <Xr ch="alienable-inalienable-split" />,
		<Xr ch="affiliative-suffix-morphophonology" />, and <Xr ch="concept-form-affiliative-form" />.
	</p>
</S>

<S t="Polysynthesis and noun incorporation" id="polysynthesis">
	<p>
		Hokkaido Ainu is genuinely polysynthetic in Nichols' sense of <em>open head-marking</em>: there
		is no fixed closed paradigm of what categories may accumulate on the verb
		<Ref k="bugaeva2022" p="Ch.1 §4.7" />. A single verb word can stack person indexes, applicative
		prefixes, reflexive or reciprocal prefixes, incorporated nominals, and a number suffix while
		remaining one phonological word. The density of such accumulation is most visible in Classical
		Ainu, particularly in the heroic epic genre <em>yukar</em>
		<Ref k="shibatani1990" p="§1" />; <Ref k="shibatani1988" />:
	</p>

	<Ex
		m="e-yay-ko-tuyma-si-ram-suy-pa"
		g="APPL-REFL-APPL-far-REFL.POSS-heart-turn.repeatedly-PL"
		tr="to ponder deeply"
		cite="shibatani1990"
		dial="HK"
		note="Nine syllables, eight bound morphemes in a single verb word: two applicative prefixes (e-, ko-), reflexive (yay-), incorporated distance noun (tuyma 'far'), reflexive-possessive (si-), incorporated body-part noun (ram 'heart, mind'), verbal root (suy 'revolve'), and pluractional suffix (-pa). After Shibatani (1990 §1) as an illustration of Classical Ainu polysynthetic capacity."
	/>

	<p>
		In ordinary Hokkaido speech, a comparable degree of stacking is less frequent; a transitive verb
		routinely carries two person indexes and one or two derivational prefixes, and a single
		incorporated nominal already constitutes a polymorphemic complex. The register difference between
		Classical Ainu and everyday speech — and what it implies for the grammar/lexicon boundary — is
		a live research question <Ref k="shibatani1990" p="5" /> ‹contested›.
	</p>
	<p>
