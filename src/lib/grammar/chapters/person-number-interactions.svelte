<S t="Two layers of number" id="overview">
	<p>
		Person and number meet in Hokkaido Ainu at two formally distinct layers. Within the
		personal-affix paradigm, number is built into specific person rows: the first plural exclusive
		series (<i lang="ain-Latn">ci=</i>/<i lang="ain-Latn">=as</i>/<i lang="ain-Latn">un=</i>),
		second plural <i lang="ain-Latn">eci=</i>, and the fourth-person series, whose number behavior
		is reference-dependent rather than form-fixed. Verbal number forms a second layer, realized
		through suppletive singular/plural stem alternations and through two homophonous formatives: a
		root-attaching plural suffix <i lang="ain-Latn">-pa</i> and an outer plural auxiliary or element
		<i lang="ain-Latn">pa</i> that follows the person-inflected verb <Ref
			k="nakagawa2024"
			p="140–162"
		/>; <Ref k="bugaeva2012" p="471" />; <Ref k="nakagawa2022" />. The person inventory is set out
		in <Xr ch="person-marking-architecture" />; the stem alternations in
		<Xr ch="suppletive-verbal-number" />; the exclusive row and clusivity in
		<Xr ch="first-person-plural-and-clusivity" />; and the full range of fourth-person functions in
		<Xr ch="indefinite-fourth-person-paradigm" />.
	</p>

