	<p>
		Corbett's distinction between the indexing of participant number and the marking of event or
		action number <Ref k="corbett2000" paren /> maps onto these two layers. Intransitive plural
		marking in Hokkaido is agreement-like: the number of S determines both the stem choice and the
		S-affix form. Transitive plural marking is oriented primarily to the object or to the action,
		bringing it closer to event-number marking in Corbett's terms <Ref k="nakagawa2024" p="142–145"
		/>.
	</p>
</S>

<S t="What controls the plural verb" id="plural-control">
	<p>
		With intransitive verbs the plural stem — whether suppletive (<i lang="ain-Latn">ek</i>/<i
			lang="ain-Latn">arki</i> 'come', <i lang="ain-Latn">arpa</i>/<i lang="ain-Latn">paye</i>
		'go', <i lang="ain-Latn">an</i>/<i lang="ain-Latn">oka(y)</i> 'exist') or suffixal (V/<i
			lang="ain-Latn">-pa</i> pairs such as <i lang="ain-Latn">rewe</i>/<i lang="ain-Latn"
			>rew-pa</i>) — agrees with S. The 1PL.EXCL suffix <i lang="ain-Latn">=as</i> and a plural
		fourth-person suffix <i lang="ain-Latn">=an</i> both obligatorily co-occur with a plural
		intransitive stem; a singular S affix selects the singular stem <Ref
			k="nakagawa2024"
			p="142–146"
		/>; <Ref k="bugaeva2012" p="471" />.
	</p>

	<Ex
		m="kuruma an wa ci=o tek arki=as"
		g="car exist CONJ 1PL.EXCL.A=board then come.PL=1PL.EXCL.S"
		tr="There was a car, and we boarded it and came."
		cite="takahashi2015:206"
		dial="TOK"
		place="Takahashi ex. 2"
		note="The intransitive arki is the plural suppletive of ek 'come'; it co-occurs with the 1PL.EXCL S suffix =as. The same group is indexed as A on the transitive o 'board' by the prefix ci=."
	/>

	<p>
		With transitive verbs the picture diverges. Plural marking is tied primarily to the plurality of
		O or of the action; a first- or fourth-person plural A prefix (<i lang="ain-Latn">ci=</i>, <i
			lang="ain-Latn">a=</i>) does not by itself reliably trigger the plural stem or outer <i
			lang="ain-Latn">pa</i>. Nakagawa characterises intransitive plural as agreement-like and
		transitive plural as action- or object-oriented and often optional <Ref
			k="nakagawa2024"
			p="142–146"
		/>; <Ref k="bugaeva2012" p="471" />. Ijäs's learner grammar states the same split: intransitive
		plural follows S, transitive plural ordinarily follows O or the action <Ref k="ijas2023" />. The
		corpus shows verb-specific and dialect-variable departures from any simple threshold, and
		Nakagawa's discussion of numerals and quantifiers treats the co-occurrence with plural morphology
		as complex and speaker-variable <Ref k="nakagawa2024" p="160–162" />.
	</p>

	<Ex
		m="apkas=as pa"
		g="walk=1PL.EXCL.S PL"
		tr="We walk."
		cite="ijas2023"
		constructed
		note="Constructed teaching form from Ijäs L9; illustrates outer pa following the 1PL.EXCL S affix on an intransitive verb in Saru-type dialects."
	/>
</S>

<S t="Stem-forming -pa and outer pa" id="pa">
	<p>
		Nakagawa distinguishes the two <i lang="ain-Latn">pa</i> elements by their position relative to
		the person-inflected form. Stem-forming <i lang="ain-Latn">-pa</i> builds the plural member of a
		V/<i lang="ain-Latn">-pa</i> pair and attaches to the root, inside any person marker or
		causative suffix. Outer <i lang="ain-Latn">pa</i> follows the fully person-inflected verb,
		outside any person affix, and marks participant or event plurality <Ref
			k="nakagawa2024"
			p="151–159"
		/>. Sakaguchi applies a formal positional criterion — a clitic (接語) attaches to the
		person-inflected form rather than to the root — and concludes that the Saru, Chitose, and
		Ishikari outer element is a clitic, distinct from the root-attaching suffix <Ref
			k="sakaguchi2024"
			p="12, 77–83"
		/>. The hypothesis that outer <i lang="ain-Latn">pa</i> is a later western Hokkaido development
		that displaced an earlier pan-Ainu outer <i lang="ain-Latn">(h)ci</i> is argued in <Ref
			k="nakagawa2022"
		/> ‹contested›.
	</p>

	<p>
		The minimal pair demonstrating the position contrast uses the fourth-person intransitive suffix
		<i lang="ain-Latn">=an</i>. When outer <i lang="ain-Latn">pa</i> is used, it follows <i
			lang="ain-Latn">=an</i>; when stem-forming <i lang="ain-Latn">-pa</i> is used, it precedes <i
			lang="ain-Latn">=an</i>.
	</p>

	<Ex
		m="nenpoka iki=an pa wa siknu=an wa"
		g="somehow do=4.S PL CONJ live=4.S CONJ"
		tr="Somehow we managed and survived, and…"
		cite="nakagawa2024:159"
		dial="SAR"
		place="Nakagawa ex. 87"
		note="Outer pa follows the fourth-person S suffix =an."
	/>

	<Ex
		m="irwak sinne sorekusu iki-pa=an"
		g="sibling like exactly do-PL=4.S"
		tr="We were like siblings."
		cite="nakagawa2024:159"
		dial="SAR"
		place="Nakagawa ex. 88"
		note="Stem-forming -pa precedes the fourth-person S suffix =an, confirming root-level attachment."
	/>

	<Ex
		m="rus patek a=mi wa hotke=an pa"
		g="fur only 4.A=wear CONJ sleep=4.S PL"
		tr="We wore only furs and slept."
		cite="ilcaa1976:aa-irc/005#157"
		dial="SAR"
		place="Kawakami Matsuko, ILCAA Ainu materials, uwepeker 7"
		note="Fourth-person transitive a=mi (wear) followed by intransitive hotke=an (sleep); outer pa follows the person-inflected form of the second predicate."
	/>

	<p>
		Outer <i lang="ain-Latn">pa</i> can repeat across conjoined predicates in a clause chain because
		its scope is over the person-inflected predicate, not over the root. The inner suffix <i
			lang="ain-Latn">-pa</i> belongs to the stem and contributes its number reading at the lexical
		level. The two may co-occur on the same verb — a V/<i lang="ain-Latn">-pa</i> stem combined with
		outer <i lang="ain-Latn">pa</i> — and both combine with causative suffixes while maintaining
		their respective attachment sites <Ref k="nakagawa2024" p="156–159" />.
	</p>
</S>

<S t="Fourth-person plurals and number mismatches" id="fourth-person-number">
	<p>
		The fourth-person series triggers plural verbal morphology in four of its five uses, regardless of
		the actual referent count. Nakagawa enumerates five functions at 2024: 174–179; the number
		behavior follows from the function <Ref k="nakagawa2024" p="160–161, 174–179" />; <Ref
			k="tamura1972"
			p="28, 34"
		/>; <Ref k="bugaeva2012" p="475–476" />.
	</p>

	<p>
		Inclusive first plural (use ①, 'you and I') always takes the plural verb stem, since the
		referent is inherently multiple <Ref k="nakagawa2024" p="174" />; <Ref
			k="takahashi2015"
			p="209"
		/>. In Tokachi, the inclusive series consistently co-occurs with plural verb forms even when
		referring to a pair, and this co-selection is obligatory <Ref k="takahashi2015" p="209" />.
	</p>

	<Ex
		m="eani hem paye=an ro"
		g="2SG also go.PL=4.S HORT"
		tr="Let's go together, you and I."
		cite="nakagawa2024:175"
		dial="CHI"
		place="Nakagawa ex. 138"
		note="The plural suppletive paye co-occurs with =an; the reference is inherently multiple (speaker + addressee)."
	/>

	<p>
		Honorific second-person address (use ②) requires the plural verb stem for a single addressee, as
		a form of social amplification <Ref k="nakagawa2024" p="175–176" />; <Ref
			k="tamura1972"
			p="28"
		/>. The mismatch between grammatically plural morphology and a singular referent is systematic
		and socially conditioned.
	</p>

	<Ex
		m="hunak wa arki=an ru-we an"
		g="where from come.PL=4.S track-POSS exist"
		tr="Where have you come from?"
		cite="nakagawa2024:176"
		dial="CHI"
		place="Nakagawa ex. 140, woman addressing an adult man"
		note="One person is addressed; the fourth-person S suffix =an appears on the plural suppletive stem arki."
	/>

	<p>
		Indefinite/impersonal use (use ③) — the equivalent of French <i lang="fr">on</i> or German <i
			lang="de">man</i> — also takes the plural verb stem, with an unspecified agent defocused from
		the event <Ref k="nakagawa2024" p="176–177" />; <Ref k="tamura1972" p="34" />; <Ref
			k="sato2008"
			p="203–205"
		/>.
	</p>

	<p>
		Narrative first-person use (use ④) is where dialectal variation is sharpest. In Saru and
		Chitose, a single protagonist may appear with the singular verb stem. Horobetsu also permits
		singular stems for a singular narrator, though the only available free pronoun is the formally
		plural <i lang="ain-Latn">aokay</i>. Shizunai shows the opposite configuration: the verb is
		consistently plural regardless of narrator number, while the pronouns distinguish <i
			lang="ain-Latn">anani</i> (singular) from <i lang="ain-Latn">anoka</i> (plural) <Ref
			k="nakagawa2024"
			p="174–179, 254–256"
		/>. The crossed mismatches — singular verb with plural pronoun in Horobetsu, plural verb with
		singular pronoun in Shizunai — confirm that the fourth-person form-set is not simply a plural
		of the first person, and they supply evidence for the historically composite character of the
		series ‹consensus›.
	</p>
</S>

<S t="Shizunai ci and Sakhalin =hci: position as the diagnostic" id="hci-ci">
	<p>
		Shizunai uses an outer <i lang="ain-Latn">ci</i> where Saru, Chitose, and Ishikari use outer <i
			lang="ain-Latn">pa</i>. The position is the same: Shizunai <i lang="ain-Latn">ci</i> follows
		the person-inflected form, outside <i lang="ain-Latn">=an</i>, in exactly the outer-plural slot
		<Ref k="nakagawa2024" p="151–155" />.
	</p>

	<Ex
		m="hoski no hotke=an ci akus"
		g="first ADV sleep=4.S PL then"
		tr="When we slept first, …"
		cite="nakagawa2024:155"
		dial="SHI"
		place="Nakagawa ex. 74, after Okuda 1999"
		note="Shizunai ci follows the fourth-person S suffix =an in the outer-plural slot, parallel to Saru outer pa."
	/>

	<p>
		Horobetsu presents a formally different element. Its <i lang="ain-Latn">-ci</i> suffix appears
		fossilized in items such as <i lang="ain-Latn">iki-ci</i> and <i lang="ain-Latn">kar-ici</i>,
		and the diagnostic ordering places it inside the person marker (<i lang="ain-Latn">iki-ci=as</i>),
		with the derivational layer rather than the outer plural slot. The Horobetsu form therefore
		behaves like a root-level suffix, not like the outer elements of Shizunai or Saru <Ref
			k="nakagawa2024"
			p="154–155"
		/>.
	</p>

	<p>
		The Sakhalin form <i lang="ain-Latn">=(a)hci</i> (Aniwa Bay <i lang="ain-Latn">=(a)hsi</i>) was
		described by Murasaki as an optional third-person plural marker <Ref k="murasaki1979" p="49–52"
		/>. Nakagawa disputes the classification: the element is non-obligatory and appears with subjects
		of all persons, placing it with the outer-auxiliary set rather than with person-agreement affixes
		<Ref k="nakagawa2024" p="151–155, 178" /> ‹contested›. Sakaguchi applies formal positional
		diagnostics and arrives at the same result: <i lang="ain-Latn">=hci</i> attaches to the
		person-inflected verb, can follow an auxiliary, and precedes a nominalizer — the signature of a
		clitic over the person-inflected form, not a root-level agreement suffix <Ref
			k="sakaguchi2024"
			p="77–83, 163"
		/>. Baek adds that Sakhalin <i lang="ain-Latn">-(a)hci</i> extends to the nominal domain, a
		trait shared with Uilta and Nivkh in the Sakhalin area and absent from Hokkaido <Ref
			k="baek2021"
			p="73"
		/>.
	</p>

	<Ex
		m="eciokaya-hcin naa eci=paye-hci-hi nee nanko"
		g="2PL-PL still 2PL.S=go.PL-PL-NMLZ COP probably"
		tr="You people must have gone too."
		cite="murasaki1979:52; nakagawa2024:155"
		dial="SA"
		place="Murasaki ex. 70 (Sakhalin contrast)"
		note="The Sakhalin plural element -hci/-hcin appears with a second-person plural subject, not a third-person one, undermining its analysis as third-person plural agreement. Hokkaido dialects lack productive =hci; the corresponding function is served by outer pa (Saru/Chitose/Ishikari) or outer ci (Shizunai)."
	/>

	<p>
		The result for Hokkaido is that no dialect has a productive third-person plural agreement suffix.
		Plural participant reference is encoded at the verbal-number layer — suppletive stems, inner <i
			lang="ain-Latn">-pa</i>, outer <i lang="ain-Latn">pa</i> or <i lang="ain-Latn">ci</i> — and
		through the number mismatches of the fourth-person row, not through an additional person-affix
		position <Ref k="nakagawa2024" p="151–155" />.
	</p>
</S>

<S t="Multiple plural marking" id="multiple-plural">
	<p>
		The plural exponents occupy different layers of the verb and can therefore accumulate within a
		single predicate. A V/<i lang="ain-Latn">-pa</i> stem encodes plural at the root level; outer <i
			lang="ain-Latn">pa</i> can follow the person-inflected form of that same stem to mark
		additional participant or event plurality at the outer layer. Similarly, a V/<i lang="ain-Latn"
			>-pa</i
		> stem combines with causative morphology before the person affix while outer <i lang="ain-Latn"
			>pa</i
		> appears outside. In clause chains, outer <i lang="ain-Latn">pa</i> may repeat on successive
		predicates because its scope is over the person-inflected predicate rather than the clause
		sequence as a unit <Ref k="nakagawa2024" p="156–159" />.
	</p>

	<p>
		Plural accumulation does not affect the cap on person affixes. A finite verb carries at most one
		subject index and one object index, and no third argument is indexed by person. In ditransitives
		such as <i lang="ain-Latn">kore</i> 'give', plural morphology can co-occur with the ordinary
		object-indexing pattern in which the object affix tracks the human recipient while the theme
		remains an unindexed bare noun phrase <Ref k="sato2008" p="147" />; <Ref
			k="sato2023b"
			p="39, 47"
		/>. The two-affix cap and the ditransitive pattern are treated in
		<Xr ch="object-indexing-monotransitive" /> and <Xr ch="personal-affix-template-and-ordering" />.
	</p>
</S>
