			transparently the suffix <i lang="ain-Latn">-hi</i> 'thing, trace' attached to the base:
			<i lang="ain-Latn">san+hi = sanihi</i>, <i lang="ain-Latn">makan+hi = makanihi</i>
			<Ref k="tangiku2022" p="§5" /> — a pathway corroborated in community analysis
			(nukopoli, aynu-corpora Discord 2024-12-16; <Ref k="aynucorporadiscord" />), and
			consistent with the general account of the affiliative's origin from the noun
			<i lang="ain-Latn">hi</i> 'thing, trace' argued by Janhunen (2020) via
			<Ref k="alonso2022" p="§2.1" />.
		</p>
	</S>
