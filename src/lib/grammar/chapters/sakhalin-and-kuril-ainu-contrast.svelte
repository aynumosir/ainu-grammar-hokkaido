	<p>
		Three competing reconstructions of Proto-Ainu prosody account for this correspondence.
		(a) Hattori (1967) and De Boer (2010) held that Proto-Ainu had vowel length alone and
		Hokkaido developed pitch accent through contact with Japanese. (b) <Ref k="vovin1993" />
		and <Ref k="itabashi2001" /> argued that Proto-Ainu had both length and pitch, with the
		Sakhalin branch losing accent under Amur-Sakhalin contact. (c) Satō (2015, 2016) proposed
		Proto-Ainu had pitch and no length, the Sakhalin vowel length being a phonetically
		conditioned open-syllable phenomenon. Alonso de la Fuente endorses view (c) in the body
		of his chapter but (a) in the conclusion, leaving the question unresolved
		<Ref k="alonso2022" p="§2.3–2.4" /> ‹contested›. Community discussion (nukopoli,
		aynu-corpora Discord 2023; <Ref k="aynucorporadiscord" />) arrives at the same impasse,
		recognising both the Hattori and the Satō positions as defensible.
	</p>
</S>

<S t="Final-consonant debuccalization in Sakhalin" id="debuccalization">
	<p>
		The phonological signature of the Sakhalin varieties relative to Hokkaido is systematic
		debuccalization of coda obstruents. Where Hokkaido has a word-final or syllable-final
		<i lang="ain-Latn">p</i>, <i lang="ain-Latn">t</i>, or <i lang="ain-Latn">k</i>, the
		core Sakhalin varieties produce <i lang="ain-Latn">h</i>; final
		<i lang="ain-Latn">m</i> becomes <i lang="ain-Latn">n</i>; and final
		<i lang="ain-Latn">r</i> receives a following vowel, keeping the syllable open
		<Ref k="tangiku2022" p="§2.3" />; <Ref k="alonso2022" p="§1" />. A secondary rule
		applies within Sakhalin: the <i lang="ain-Latn">h</i> produced by debuccalization becomes
		<i lang="ain-Latn">s</i> following <i lang="ain-Latn">i</i>
		(<i lang="ain-Latn">h &gt; s / i__</i>), yielding alternants such as
		<i lang="ain-Latn">sis</i> for HA <i lang="ain-Latn">sik</i> 'eye'. Alonso de la Fuente
