<S t="Ainuic: three varieties, two branches" id="taxonomy">
	<p>
		Ainu has no demonstrated genetic relatives and is treated as a language isolate
		<Ref k="janhunen2022" p="§2" />; the failed macro-comparisons are surveyed in
		<Xr ch="genetic-position-macro-comparison" />. Internally the language forms a small
		family — sometimes labelled Ainuic — of three entities: Hokkaido (HA), Sakhalin, and
		the Northern Kuril varieties <Ref k="janhunen2022" p="§2" />. The tripartition was
		formalised on geographic grounds in the early twentieth century and appeared to receive
		objective support from the lexicostatistic studies of <Ref k="hattorichiri1960" /> and
		<Ref k="asai1974" />. <Ref k="ono2020" /> demonstrates with spectral-clustering
		methods, however, that the three-way "Major Division" carries no statistically robust
		support: the cleaner two-way structure is Hokkaido (including the Kuril varieties) versus
		Sakhalin. The internal Hokkaido classification and the dialectometry underlying that
		finding are treated in <Xr ch="hokkaido-dialect-classification-and-dialectometry" />;
		the boundary-dialect methodology is refined further by <Ref k="fukazawaono2024" />.
	</p>
	<p>
		The branching order follows the archaeological record of the Satsumon culture's northward
		expansion from Honshu: the language reached Hokkaido from around the seventh century CE,
		southern Sakhalin around the thirteenth century, and the Northern Kuriles around the
		fifteenth to sixteenth century <Ref k="janhunen2022" p="§3a–3b" />. The Sakhalin branch
		diverged from Common Ainu approximately 800 years ago; the Kuril varieties diverged later
		and cluster more closely with northeast Hokkaido in the lexicostatistic data
		<Ref k="alonso2022" p="§4" />; <Ref k="janhunen2022" p="§3b" />. Both the Sakhalin and
		Kuril varieties ceased to be community languages during the twentieth century. The primary
		Sakhalin documentation consists of the grammar and texts of <Ref k="murasaki1979" /> and
		the folkloric corpus of <Ref k="pilsudski1912" />; the Kuril varieties are known only from
		questionnaire records assembled by eighteenth- and nineteenth-century travellers. Within
		Hokkaido, the dialect geographically and lexically closest to Sakhalin is Sōya, at
		the northern tip of the island <Ref k="hattorichiri1960" />.
	</p>
</S>

<S t="The prosodic correspondence: length and pitch" id="prosody">
	<p>
		The Sakhalin varieties have a vowel-length contrast on the five vowels
		<i lang="ain-Latn">a e i o u</i>, with long vowels occurring only in open stem syllables;
		Hokkaido has a pitch-accent system and no distinctive vowel length. Hattori (1967) first
		established the correspondence: a Sakhalin long vowel corresponds to a Hokkaido high
		pitch, and a Sakhalin short vowel in a closed-syllable stem likewise corresponds to
		Hokkaido high pitch in the same environments
		<Ref k="alonso2022" p="§2.3" />; <Ref k="tangiku2022" p="§2.1" />. The pitch-accent
		rules are described in <Xr ch="pitch-accent-placement-rule" />; the diachronic account of
		the length–pitch relationship is the subject of
		<Xr ch="final-h-history-and-sakhalin-length-reflex" />.
	</p>

