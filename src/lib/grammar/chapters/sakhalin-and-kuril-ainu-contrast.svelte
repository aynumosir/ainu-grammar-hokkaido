<S t="Word-initial consonants in the Nairo subdialect" id="nairo">
	<p>
		Within Sakhalin, the Nairo subdialect (eastern Sakhalin) shows word-initial
		<i lang="ain-Latn">t-</i> where Hokkaido and the remaining Sakhalin varieties have
		<i lang="ain-Latn">r-</i>: Nairo <i lang="ain-Latn">teera</i> 'wind' corresponds to
		HA <i lang="ain-Latn">rera</i> and non-Nairo SA <i lang="ain-Latn">reera</i>; Nairo
		<i lang="ain-Latn">taarak</i> 'smooth' corresponds to HA <i lang="ain-Latn">raarak</i>
		<Ref k="tangiku2022" p="§2.5" />. Alonso de la Fuente interprets this as retention of a
		Proto-Ainu lateral *<i lang="ain-Latn">l</i> that merged with
		<i lang="ain-Latn">r</i> in most dialects but with <i lang="ain-Latn">t</i> in Nairo
		— on this account Nairo preserves the most archaic reflex for the relevant set
		<Ref k="alonso2022" p="§2.2.1–2.2.2" /> ‹contested›. The PA consonant reconstruction,
		including the evidence from Ainu→Ghilyak loans showing Proto-Ghilyak *<i lang="ain-Latn">l-</i>
		opposite these forms, is treated in <Xr ch="proto-ainu-segmental-reconstruction" />.
	</p>
</S>

<S t="Morphological and lexical divergences" id="morph-lex">
	<S t="Numerals: vigesimal versus decimal" id="numerals">
		<p>
			Hokkaido Ainu counts in a vigesimal (base-20) system; Sakhalin diverges above ten.
			The Sakhalin varieties use a decimal structure for eleven and above, with
			<i lang="ain-Latn">kunkutu</i> '10' and <i lang="ain-Latn">tanku</i> '100' as the
			key items <Ref k="tangiku2022" p="§6.1" />. The word <i lang="ain-Latn">tanku</i>
			'100' is a Tungusic borrowing; <i lang="ain-Latn">kunkutu</i> '10' may derive from
			Itelmen (the language of the Kamchatka Peninsula), on the basis of a resemblance to
			forms recorded by Krasheninnikov in 1755 — this is a comparison, not a demonstrated
			etymology <Ref k="tangiku2022" p="§6.1" /> ‹speculative›. Community discussion
			(antitwilight, aynu-corpora Discord 2024-11-22; <Ref k="aynucorporadiscord" />) also
			notes the Itelmen resemblance for <i lang="ain-Latn">kunkutu</i>, attributing the
			decimal restructuring to Sakhalin trade contact ‹speculative›. The Hokkaido vigesimal
			system is described in <Xr ch="vigesimal-numeral-system" />.
		</p>
	</S>

	<S t="Person affixes and plural marking" id="person-plural">
		<p>
			The Sakhalin varieties lack the first-person-plural-exclusive forms that Hokkaido
			marks with <i lang="ain-Latn">ci=</i> / <i lang="ain-Latn">=as</i>
			<Ref k="tangiku2022" p="§7.4" />. In an "elders' style" of Sakhalin speech, the
			fourth-person forms <i lang="ain-Latn">an=</i> / <i lang="ain-Latn">=an</i> cover
			first-singular reference alongside the dedicated
			<i lang="ain-Latn">ku=</i> <Ref k="tangiku2022" p="§7.4" />. The Hokkaido
			first-person plural and the exclusive/inclusive distinction are described in
			<Xr ch="first-person-plural-and-clusivity" />.
		</p>
		<p>
			Plural marking across the three Ainuic entities shows a layered historical picture.
			Sakhalin uses the suffix <i lang="ain-Latn">-hci</i> / <i lang="ain-Latn">-hcin</i>;
			most Hokkaido dialects outside Saru–Chitose use <i lang="ain-Latn">ci</i>; Saru,
			Chitose, and Ishikari use <i lang="ain-Latn">pa</i>
			<Ref k="nakagawafukazawa2022" p="§4.2" />; <Ref k="tangiku2022" p="§7.5" />.
			Nakagawa establishes that Hokkaido <i lang="ain-Latn">ci</i> is the older Hokkaido
			form, and <i lang="ain-Latn">pa</i> is an innovation that spread through the Saru and
			Ishikari regions <Ref k="nakagawafukazawa2022" p="§4.2" />.
		</p>
	</S>

	<S t="The allative particle and a puzzle of shared isoglosses" id="allative-isogloss">
		<p>
			Sakhalin uses the allative particle <i lang="ain-Latn">(e)ne</i>, which coincides
			with the form found in northern and eastern Hokkaido (Sōya, Asahikawa, Bihoro,
			Kushiro, Shiranuka) rather than the southwestern HA forms
			<i lang="ain-Latn">un</i> (Horobetsu, Chitose, Yakumo) or
			<i lang="ain-Latn">en</i> (Tokachi) <Ref k="nakagawafukazawa2022" p="§4.3" />.
			Other shared features between Sakhalin and the geographically distant southern
			Hokkaido dialects (Saru–Chitose) are harder to explain: the interrogative form
			<i lang="ain-Latn">hemata</i> 'what' and the second-person-plural object prefix
			<i lang="ain-Latn">eci=</i> occur in both Saru–Chitose and Sakhalin, a distribution
			that Nakagawa and Fukazawa describe as "hard to believe independent"
			<Ref k="nakagawafukazawa2022" p="§5" /> ‹contested›. Whether these features reflect
			retention in peripheral varieties, a contact relic, or convergence is treated in
			<Xr ch="hokkaido-dialect-classification-and-dialectometry" />.
		</p>
	</S>
</S>

<S t="The Kuril varieties" id="kuril">
	<p>
		The Northern Kuril varieties are known only from questionnaire records compiled by
		eighteenth- and nineteenth-century travellers — abbreviated in Alonso de la Fuente's
		discussion as KD (Dybowski), KK (Krašeninnikov), KL (Klaproth), KT (Torii), and KV
		(Voznesenskij) <Ref k="alonso2022" p="§2.2" />. The lexicostatistic reanalysis of
		<Ref k="ono2020" /> finds the Kuril varieties clustering with the northeast Hokkaido
		group — Samani, Obihiro, Kushiro, Bihoro, Sōya — rather than with Sakhalin, supporting
		a two-way Hokkaido-plus-Kuril versus Sakhalin division over the classic three-way one.
		A separate analysis using nineteenth-century Kuril material by Satō and Bugaeva (2019)
		places the Kuril items closer to southern Hokkaido (Saru/Chitose); the two results rest
		on different data sets and are not directly comparable <Ref k="ono2020" /> ‹contested›.
	</p>
	<p>
		Phonologically the Kuril varieties largely preserve the coda stops
		<i lang="ain-Latn">p/t/k</i> that Sakhalin debuccalises, but they also show prothetic
		<i lang="ain-Latn">h-</i> in some forms alongside loss of original
		<i lang="ain-Latn">h-</i> in others — an independent innovation parallel to the
		prothetic <i lang="ain-Latn">h-</i> found in the Bihoro dialect of Hokkaido
		<Ref k="alonso2022" p="§2.2.5" />. The word for 'mouth' appears as
		<i lang="ain-Latn">car</i> in Kuril records (aligning with northeastern HA and Sakhalin
		against the southwestern HA form <i lang="ain-Latn">par</i>), an isogloss documented in
		place-name data by <Ref k="kirikae1994" /> (via <Ref k="nakagawafukazawa2022" p="§3.4" />)
		and diachronically accounted for as a palatalized-series relic *<i lang="ain-Latn">pʸaɾ</i>
		by <Ref k="alonso2022" p="§2.2.6" />. The diachrony of that split is treated in
		<Xr ch="proto-ainu-segmental-reconstruction" />. Hokkaido phonological microvariation,
		including the Bihoro and northeastern features that align partially with Kuril, is
		described in <Xr ch="hokkaido-phonological-microvariation" />. A Kuril contrast text
		appears in <Xr ch="sakhalin-contrast-text" />.
	</p>
</S>

