	<S t="The affiliative and the underlying base consonant" id="affiliative">
		<p>
			The <Xr ch="concept-form-affiliative-form">possessed (affiliative) form</Xr> of
			Sakhalin nouns reveals the base consonant that debuccalization masks in the independent
			form. The affiliative suffix <i lang="ain-Latn">-ihi / -ehe</i> attaches directly to
			the underlying stem: the noun whose independent form is
			<i lang="ain-Latn">sis</i> 'eye' (&lt; *<i lang="ain-Latn">sih</i>, with
			<i lang="ain-Latn">h &gt; s</i> before <i lang="ain-Latn">i</i>) surfaces in the
			possessed form as <i lang="ain-Latn">sik-ihi</i> — the base /k/ restored — not as the
			unattested *<i lang="ain-Latn">sis-ihi</i>. The underlying consonant is thus directly
			recoverable from morphological alternation even where phonology has eliminated it from
			the independent form <Ref k="tangiku2022" p="§2.3, §5" />; <Ref k="alonso2022" p="§1" />.
			The Hokkaido Saru counterpart is identical: <i lang="ain-Latn">sik-ihi</i>.
		</p>

