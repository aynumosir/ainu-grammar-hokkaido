<S t="Bugaeva, Nakagawa, and the corpus-computational turn" id="current-state">
	<p>
		The scholarship of the early 21st century has advanced on two fronts simultaneously: deeper
		domain-specific analysis of grammatical subsystems, and the development of digitally accessible
		annotated corpora. Article-length studies have addressed domains that the reference grammars
		treated only summarily, and the resulting findings have in several cases revised or substantially
		refined the standard analyses.
	</p>

	<p>
		Bugaeva Anna's research has reshaped understanding of several grammatical subsystems. Her work
		on Ainu applicatives <Ref k="bugaeva2006" /> <Ref k="bugaeva2010" /> provided a
		cross-linguistically situated analysis of the three applicative prefixes
		<i lang="ain-Latn">e-</i>, <i lang="ain-Latn">ko-</i>, and <i lang="ain-Latn">o-</i>; her
		analysis of the antipassive <Ref k="bugaeva2021antip" /> placed Ainu within the comparative
		typology of antipassive constructions; and her work on possession
		<Ref k="bugaeva2021poss" /> and causatives <Ref k="bugaeva2014" /> has refined the standard
		valency analysis. Her treatment of the diachrony of anticausatives <Ref k="bugaeva2025" />
		extends the historical dimension of the valency description. The Handbook of the Ainu language
		<Ref k="bugaeva2022" />, which she edited, assembles specialist treatments of every major
		domain: phonology <Ref k="shiraishi2022" />, verbal valency
		<Ref k="bugaevakobayashi2022" />, dialectology <Ref k="nakagawafukazawa2022" />, oral
		literature <Ref k="endo2022" /> <Ref k="okuda2022" />, historical and comparative linguistics
		<Ref k="alonso2022" />, and the revitalization movement <Ref k="ono2022" />. The Handbook
		constitutes the most comprehensive reference work on Ainu available in English.
	</p>

	<p>
		Nakagawa Hiroshi's <Ref k="nakagawa2024" /> — <i lang="ja">アイヌ語広文典</i> ('An extensive
		grammar of the Ainu language') — is the most recent single-author reference grammar of
		Hokkaido Ainu. It surveys the full grammatical range from phonology through complex
		clause-linkage, drawing primarily on Saru and Chitose data, and provides detailed paradigm
		tables with Japanese translation throughout. It is the primary reference grammar for this work,
		cited throughout by section. A critical review of its analyses and relation to earlier grammars
		is available in <Ref k="sato2025b" />.
	</p>

	<p>
		Domain-specific monographs and dissertations have contributed substantially alongside the
		reference grammars. Dal Corso's dissertation on Ainu evidentiality <Ref k="dalcorso2018" />
		provided a semantico-pragmatic account of the formal-noun evidential system and a
		morphosyntactic analysis of its construction and clause-final frames. Historical phonology has
		been examined by <Ref k="itabashi2001" />, who established the diachronic correspondence
		between Sakhalin Ainu vowel length and Hokkaido Ainu pitch accent. The Handbook chapter by
		<Ref k="alonso2022" /> critically reviewed earlier Proto-Ainu reconstruction proposals,
		including those of <Ref k="vovin1993" />, and advanced a revised segmental and suprasegmental
		inventory. Dialectological classification has been reconsidered by
		<Ref k="nakagawafukazawa2022" /> and in subsequent quantitative analyses.
	</p>

	<p>
		The documentary and corpus infrastructure of Ainu linguistics has grown substantially since the
		1970s. The annotated oral-literature text editions published by Nakagawa and others, the NINJAL
		Corpus of Ainu oral literature <Ref k="ninjal2003" />, and the Glossed Audio Corpus
		<Ref k="glossedcorpus2016" /> have made the oral-narrative tradition available in digitally
		accessible, aligned, and partially glossed form. Natural-language-processing-oriented work,
		including the philological study of Sino-Ainu materials by <Ref k="yasuoka2023" />, marks a
		further extension of the field's methodological range. The sources underpinning this grammar's
		corpus layer are described in <Xr ch="oral-literature-and-spoken-corpora" /> and
		<Xr ch="dialect-sample-and-corpus-method" />.
	</p>

	<p>
		The revitalization movement and the emergence of new-speaker varieties constitute a distinct
		strand of contemporary research. Its history is surveyed in <Ref k="ono2022" /> and treated
		in depth in <Xr ch="sociolinguistic-situation-revitalization" />. The typological profile of
		the language is mapped in <Xr ch="typological-profile" />, and the genetic-relationship
		question is treated in <Xr ch="genetic-position-macro-comparison" />.
	</p>
</S>
