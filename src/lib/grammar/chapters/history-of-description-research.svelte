<S t="Edo-period records and the philological tradition" id="edo-records">
	<p>
		The earliest systematic documentation of the Ainu language dates from the Edo period (17th–19th
		centuries), when Japanese interpreters and officials stationed at trading posts on Hokkaido
		compiled word-lists, phrase-books, and glossaries for practical administrative use. The most
		comprehensive of these, described by <Ref k="refsing1986" p="17" />, is the
		<i lang="ja">蝦夷方言藻汐草</i> (<i>Ezo hōgen moshiogusa</i>, 'An Ezo dialect miscellany'),
		compiled by the interpreter Uehara Kumajirō and the official Abe Chōzaburō and first issued in
		1792. It contains roughly three thousand lexical entries organized by semantic field, a list of
		common phrases, and short texts, and it served as a primary reference for subsequent Japanese
		scholarship. The late Meiji period added further lexical resources, among them the Saru-dialect
		topical conversation dictionary of <Ref k="kanazawa1898" />.
	</p>

	<p>
		The Edo-period manuscript tradition has received philological attention in recent scholarship.
		<Ref k="fukazawa2017" /> examines the Kaga-ke (<span lang="ja">加賀家</span>) documents — a
		set of administrative and linguistic records from the Matsumae domain — in a dissertation that
		subjects these materials to systematic critical scrutiny. The Sino-Ainu glosses embedded in the
		<i lang="ja">蝦夷島奇觀</i> (<i>Ezoshima kikan</i>) have been studied by
		<Ref k="yasuoka2023" />, contributing to a digitally oriented philological programme that
		bridges the manuscript tradition and modern corpus resources.
	</p>

	<p>
		These sources function primarily as practical aids for Japanese-Ainu communication rather than
		as linguistic analyses. They provide invaluable lexical witnesses, particularly for varieties
		spoken in regions where communities ceased to transmit the language before systematic recording
		was possible, but they contain no morphological analysis and their phonological transcriptions
		require careful critical interpretation. Their evidentiary status is assessed in
		<Xr ch="written-sources-grammars-dictionaries" />.
	</p>
</S>

<S t="Batchelor, Piłsudski, and early Western documentation" id="batchelor-pilsudski">
	<p>
		Basil Hall Chamberlain's 1887 monograph <Ref k="chamberlain1887" /> marshalled detailed
		linguistic arguments against relating Ainu to Japanese or any Altaic family, concluding that it
		must be treated as genealogically unclassifiable. His points were subsequently reviewed and
		extended by Kindaichi Kyōsuke, and the isolate status has remained the scholarly consensus; the
		question is examined in <Xr ch="genetic-position-macro-comparison" />.
	</p>

	<p>
		The most influential early description available in a European language came from John Batchelor,
		a British Anglican missionary who spent several decades working among the Hokkaido Ainu. His
		dictionary — held in this project's sources in the <Ref k="batchelor1905" /> edition and in
		its final revised form of <Ref k="batchelor1938" /> — combined Ainu-English-Japanese glosses
		with a grammatical sketch, and it remained outside Japan the most accessible major source
		material on the language for several decades <Ref k="refsing1986" p="18" />. Among Batchelor's
		contributions was a Roman-alphabet transcription system for Ainu, the most direct antecedent of
		the Latin-script orthographic tradition examined in <Xr ch="glossing-abbreviations-and-citation" />.
		The limitations of the dictionary, however, were noted by his Japanese successors: Refsing
		<Ref k="refsing1986" p="18–19" /> documents Chiri Mashiho's extended criticism cataloguing
		mistranslations, phantom entries, and unreliable forms, and concludes that the work requires a
		critical and cautious approach. The grammatical sketch shares the presupposition of European
		missionary-grammar categories and requires verification against the later native-speaker-based
		record.
	</p>

	<p>
		Bronisław Piłsudski's collection <Ref k="pilsudski1912" />, compiled from fieldwork on
		Sakhalin, supplies an early and substantial documentary record of the Sakhalin Ainu variety.
		The significance of that collection for Sakhalin phonetics, phonology, and morphosyntax has
		been reassessed in <Ref k="dalcorso2024" />, and the texts have been re-edited with
		translation and grammatical notes by <Ref k="dalcorso2021" />. The Sakhalin materials feature
		in this grammar as systematic external contrast, marked with the dialect tag
		<span class="sc">sa</span>; the historical and geographic setting shared between Hokkaido and
		Sakhalin Ainu is outlined in <Xr ch="ainu-people-homeland-history" />.
	</p>
</S>

<S t="Kindaichi and Chiri: the classical Japanese descriptive tradition" id="kindaichi-chiri">
	<p>
		Kindaichi Kyōsuke (1882–1971) established Ainu studies as a recognized academic discipline in
		Japan, bringing sustained scholarly productivity and the institutional standing of the Tokyo
		Imperial University to a field that had previously lacked both. His primary interest lay in the
		oral-literature tradition — particularly the heroic epics known as yukar — and his grammatical
		work was conceived as an instrument for that philological programme
		<Ref k="kindaichi1933" />. Among the textual materials he drew on were yukar notebooks
		compiled by the Ainu narrator Kannari Matsu, who wrote down her repertoire in romanized Ainu
		over several years in Kindaichi's home, eventually filling seventy notebooks
		<Ref k="refsing1986" p="19" />. The oral-literature tradition itself is surveyed in
		<Xr ch="oral-literature-and-spoken-corpora" />.
	</p>

	<p>
		Kindaichi's analytical contributions to Hokkaido Ainu grammar were substantial. Refsing
		identifies two advances in particular: he demonstrated that the language's person-indexing
		elements are personal affixes on the verb rather than the free pronouns that Batchelor's
		description had posited, and he distinguished the abstract nominal form from the personal-reference-bearing
		concrete (possessed) form — an opposition later refined by Tamura and central to the nominal
		morphology of this grammar <Ref k="refsing1986" p="21–22" />.
	</p>

	<p>
		Chiri Mashiho (1909–1961), an Ainu scholar, studied under Kindaichi at Tokyo Imperial University
		— the first Ainu student admitted to that institution — and his graduation thesis became the
		joint grammar <Ref k="kindaichi1936" />, <i lang="ja">アイヌ語法概説</i> ('An outline of Ainu
		grammar'), co-authored with Kindaichi. Published in 1936, it remained the standard Japanese
		reference work for decades. Chiri subsequently undertook fieldwork on Sakhalin and produced a
		grammar centred on the Sakhalin dialects <Ref k="chiri1942" />. His later scholarly career
		turned toward lexicography and etymological research; he began a projected ten-volume classified
		dictionary of Ainu, completing three volumes on plants, animals, and human beings respectively
		before his death in 1961 <Ref k="chiri1953" />.
	</p>

	<p>
		Chiri Yukie (1903–1922) compiled the first published collection of Ainu kamuy yukar (divine
		epics) with Japanese translations <Ref k="chiri1923" />, a text-publication project that
		established the model of romanized Ainu with facing-page translation and drew the oral-literary
		tradition into print for the first time.
	</p>

	<p>
		The descriptive framework of Kindaichi and Chiri organized grammar around traditional Japanese
		parts-of-speech categories — noun, pronoun, adjective, adverb, verb, particle — with
		grammatical relations treated semantically: the analyst begins from a meaning-type (nominative,
		locative, dative) and asks how the language expresses it. Refsing notes that this approach
		presupposes categories from the analyst's prior framework rather than deriving them from the
		distributional behavior of Ainu morphemes, and produces a coherent overview whose ordering
		reflects the analyst's starting-point as much as the structure of the language
		<Ref k="refsing1986" p="40" />. The contrast between this tradition and the formal-distributional
		analysis of Tamura is a recurring reference point in the history of Ainu grammaticography.
	</p>
</S>

<S t="The modern descriptive era: Tamura, Refsing, Shibatani, and Satō" id="modern-era">
	<p>
		Post-war descriptive work began with two large comparative projects by Hattori Shirō. The
		lexicostatistic study he undertook with Chiri Mashiho surveyed 19 Ainu localities — 13 on
		Hokkaido, 6 on Sakhalin — using a 200-item Swadesh list and established the first quantitative
		basis for internal dialect classification <Ref k="hattorichiri1960" />. The Ainu dialect
		dictionary he subsequently edited <Ref k="hattori1964" /> covers the same 19 localities and
		remained the foundational pan-dialectal lexical resource for decades; the current state of the
		dialect classification that builds on that data is described in
		<Xr ch="dialect-sample-and-corpus-method" />. The Sakhalin variety was described in the
		grammar of <Ref k="murasaki1979" />, later re-edited and annotated by
		<Ref k="dalcorso2021" />. Asai Tōru produced a grammar of the Ishikari dialect
		<Ref k="asai1969" />, extending the descriptive record to a variety north of the Saru basin.
	</p>

	<p>
		Tamura Suzuko — publishing through the 1950s and 1960s under her maiden name Fukuda — built
		the Saru dialect into the best-described variety of Hokkaido Ainu. Her early articles on Saru
		verbal morphology <Ref k="fukuda1956" />, auxiliary verbs <Ref k="tamura1960" />, and particle
		systems <Ref k="fukuda1961" /> applied distributional criteria to morpheme classification,
		departing from the semantic ordering of the Kindaichi-Chiri tradition. Refsing's comparison
		shows that Tamura was among the first to delimit morpheme classes by formal and positional
		behavior — for instance, restricting the possessed (belonging) form to a defined semantic class
		of nouns, where Kindaichi had treated the opposition as applicable to all nouns
		<Ref k="refsing1986" p="22, 42–47" />. Her Saru-dialect dictionary <Ref k="tamura1996" />
		remains the most comprehensive single-dialect Ainu dictionary and is used throughout this
		grammar as a primary lexical resource. The English-language grammar
		<Ref k="tamura2000" /> made the Saru description accessible to non-Japanese readership,
		though it was not available to this project for direct consultation.
	</p>

	<p>
		Refsing's 1986 monograph <Ref k="refsing1986" /> on the Shizunai dialect applied the
		distributional programme more explicitly, organizing morpheme classes by positional evidence
		alone and providing substantial coverage of a Hokkaido variety otherwise under-described in the
		literature. Its introduction — a critical survey of the history of research through the early
		post-war period — is the most thorough historiography of Ainu linguistics available in English
		prior to the Handbook, and large parts of the historical account in the present chapter draw on
		that survey.
	</p>

	<p>
		Shibatani's 1990 chapter on Ainu <Ref k="shibatani1990" />, in his typological survey of the
		languages of Japan, situated the language within the cross-linguistic typological literature at
		a scale that earlier descriptions had not attempted. He characterized Hokkaido Ainu as
		head-marking, polysynthetic, SOV, and organized for evidential distinctions through a
		nominalization-plus-copula construction — a characterization confirmed and refined by subsequent
		scholarship; see <Xr ch="typological-profile" />. His 1988 article <Ref k="shibatani1988" />
		had placed Ainu noun incorporation within the cross-linguistic typology of polysynthesis,
		establishing a framework for the incorporation analysis taken up in later work.
	</p>

	<p>
		Satō Tomomi's grammar <Ref k="sato2008" /> is the most comprehensive Japanese-language
		description since Tamura's, covering Hokkaido Ainu morphology, syntax, argument structure,
		valency operations, and information structure, with particular depth on the Chitose variety.
		Together with Tamura's Saru dictionary and grammar, it forms one of the two principal
		Japanese-language reference works against which this grammar triangulates its analyses.
	</p>

