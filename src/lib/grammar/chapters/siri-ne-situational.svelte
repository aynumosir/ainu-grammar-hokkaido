<S t="Place in the evidential paradigm" id="paradigm">
	<p>
		The formal noun <i lang="ain-Latn">siri</i> — the
		<Xr ch="concept-form-affiliative-form">possessed form</Xr> of <A
			w="sir"
			gl="visible appearance, land, weather"
		/> — is the direct visual evidential of Hokkaido Ainu. Combined with the copula
		<i lang="ain-Latn">ne</i> it yields the declarative frame
		<i lang="ain-Latn">siri ne</i>, glossed <span class="sc">dir.vis</span> by Dal Corso: a
		direct evidential whose information source is a situation accessible to sight
		<Ref k="dalcorso2018" p="305–306" />. The full four-term paradigm and its
		nominalization-plus-copula architecture are set out in
		<Xr ch="evidential-schema-overview" />; the present chapter treats
		<i lang="ain-Latn">siri ne</i> and its indirect siblings
		<i lang="ain-Latn">siri an</i> and <i lang="ain-Latn">siri ki</i>. The
		reasoning-based term <i lang="ain-Latn">ruwe ne</i> is treated in
		<Xr ch="ruwe-ne-inferential" />; the non-visual sensory term in
		<Xr ch="humi-ne-sensory" />.
	</p>

	<p>
		Within the reliability hierarchy, <i lang="ain-Latn">siri ne</i> occupies the second
		position, below <i lang="ain-Latn">ruwe ne</i> (knowledge/reasoning) and above
		<i lang="ain-Latn">humi ne</i> and <i lang="ain-Latn">hawe ne</i>
		<Ref k="dalcorso2018" p="337" />. In Dal Corso's Hokkaido reference texts it supplies 149
		tokens (6.68% of all evidential forms), against 1508 (67.56%) for
		<i lang="ain-Latn">ruwe ne</i>; its indirect counterparts
		<i lang="ain-Latn">siri an</i> (59 tokens, 2.64%) and
		<i lang="ain-Latn">siri ki</i> (42 tokens, 1.88%) are less frequent still
		<Ref k="dalcorso2018" p="72" />. Unlike <A w="ru" gl="track, path" />, whose evidential
		reflexes are confined to direct evidentiality in the southern dialects, <A
			w="sir"
			gl="visible appearance, land, weather"
		/> enters both direct and indirect evidentiality, yielding the three-member subfamily set
		out in the table below.
	</p>

