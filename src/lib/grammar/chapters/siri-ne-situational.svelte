<S t="Predication type: the Tokachi stage-level constraint" id="predication-type">
	<p>
		Takahashi's Tokachi study (consultant: Sawai Tomeno, Honbetsu) establishes that the
		visual nominalizer is restricted to stage-level (imperfective, transient) predication. A
		passing scene — someone in the act of fleeing — takes the Tokachi cognate
		<i lang="ain-Latn">sin</i> (concept-form allomorph of <i lang="ain-Latn">sir</i>):
		<i lang="ain-Latn">kira sin ne nankor</i> '(she) seems to be fleeing' (Takahashi 2013,
		ex. 5). An individual-level (permanent) property, even one visually accessible, takes
		<i lang="ain-Latn">ru</i> instead — the unmarked member that imposes no predication-type
		restriction <Ref k="takahashi2013" p="131–133" />. Among the four Tokachi nominalizers,
		only <i lang="ain-Latn">sir</i> carries this restriction; <i lang="ain-Latn">hum</i>,
		<i lang="ain-Latn">haw</i>, and <i lang="ain-Latn">ru</i> operate at both predication
		levels <Ref k="takahashi2013" p="133" /> ‹consensus›.
	</p>

	<p>
		Takahashi draws a parallel with the English perception-verb constraint on individual-level
		complements: a stage-/individual-level distinction that in English appears on the
		complement-type of the matrix verb, but in Ainu appears on the nominalizer itself
		<Ref k="takahashi2013" p="133" />. The constraint corroborates
		<i lang="ain-Latn">siri ne</i>'s characterization as inherently tied to observable,
		imperfective situations. Satō's Chitose evidence points the same direction: in
		interrogatives, <i lang="ain-Latn">siri</i> selects for processes ongoing before the
		eyes, not for stable properties <Ref k="sato2008" p="134" />. Whether the Tokachi
		restriction is a local grammaticalization feature or a pan-Hokkaido tendency described
		only piecemeal in the southern sources remains unresolved ‹speculative›. The
		complementary distribution with <i lang="ain-Latn">ru / ruwe</i> is examined in
		<Xr ch="ruwe-ne-inferential" s="predication-type" />.
	</p>
</S>

<S t="Double evidentiality and the siri an / ruwe ne pairing" id="double-evid">
	<p>
		Double evidentiality — two formal nouns stacked in the same clause — is more frequent in
		Hokkaido Ainu than in Sakhalin Ainu, and the siri-family forms participate in it in a
		diagnostically consistent pattern <Ref k="dalcorso2018" p="326–327, 331–333" />. When
		<i lang="ain-Latn">siri an</i> (indirect visual) occurs in a double-evidential
		construction, its reinforcing partner is invariably <i lang="ain-Latn">ruwe ne</i>, never
		<i lang="ain-Latn">siri ne</i> — as in the smoke-rising example
		(<i lang="ain-Latn">supuya at kor sir-an ruwe ne anan</i>, Nakagawa text, Dal Corso ex.
		268) <Ref k="dalcorso2018" p="326–327" />. The pattern is systematic:
		<i lang="ain-Latn">siri ki siri ne</i> is also attested (Dal Corso exx. 267, 274), while
		combinations such as <i lang="ain-Latn">*siri an siri ne</i> are not reported
		<Ref k="dalcorso2018" p="331–333" />.
	</p>

	<p>
		This distribution provides a reliability argument for the
		<i lang="ain-Latn">ruwe ne</i> / <i lang="ain-Latn">siri ne</i> split. If the two were
		merely co-equal visual channels, there would be no structural reason for the indirect
		visual form to seek out <i lang="ain-Latn">ruwe ne</i> specifically as its ratifying
		partner. The systematicity of the pairing suggests that <i lang="ain-Latn">ruwe ne</i>
		functions as a knowledge operator that can ratify the output of a lower-reliability
		evidential as established fact, while <i lang="ain-Latn">siri ne</i> — as a scene-level
		visual evidential — cannot fill that ratifying role ‹contested›. Dal Corso leaves the
		exact semantic driver open <Ref k="dalcorso2018" p="311–312, 335–336" />; the full
		argument is developed in <Xr ch="ruwe-ne-inferential" s="visual-puzzle" />.
	</p>
</S>

<S t="Diachrony, weather predicates, and dialect microvariation" id="diachrony">
	<p>
		The diachronic path parallels those of the other formal nouns (see
		<Xr ch="ruwe-ne-inferential" s="diachrony-dialect" />): the noun
		<A w="sir" gl="visible appearance, land, weather" /> yields the possessed formal noun
		<i lang="ain-Latn">sir-i</i> 'the appearance of —', which grammaticalizes as the
		clause-final evidential <i lang="ain-Latn">[clause] siri ne</i>
		<Ref k="hirosawa2026" p="65" />; <Ref k="takahashi2018" p="111" />. The
		grammaticalization is partial: the lexical source remains transparent in weather
		compounds, and the choice of post-nominal predicate (<i lang="ain-Latn">ne</i> vs
		<i lang="ain-Latn">an</i> vs <i lang="ain-Latn">ki</i>) continues to carry evidential
		weight <Ref k="dalcorso2018" p="24–25" />.
	</p>

	<p>
		The meteorological transparency is most visible in the compound predicates built on
		<A w="sir" />: <i lang="ain-Latn">sir-pirka</i> 'good weather',
		<i lang="ain-Latn">sir-kunne</i> 'be dark / nightfall',
		<i lang="ain-Latn">sir-peker</i> 'be bright / dawn'. These incorporate
		<i lang="ain-Latn">sir</i> as a weather-subject noun and are lexicalized verb compounds,
		not evidential constructions. The evidential and the weather-noun use are synchronic
		relatives on a shared stem, one grammaticalized into the clause-final paradigm, the other
		retained as a productive lexical pattern.
	</p>

	<p>
		Dialect microvariation runs parallel to what was observed for
		<i lang="ain-Latn">ruwe ne</i>:
	</p>

	<ul>
		<li>
			<strong>Tokachi</strong> uses the bare concept form <i lang="ain-Latn">sin</i>
			(allomorph of <i lang="ain-Latn">sir</i>) in clause-final position — not the possessed
			<i lang="ain-Latn">siri</i> — and applies the stage-level restriction discussed above
			<Ref k="nakagawa2024" p="110" />; <Ref k="takahashi2013" p="129–131" />.
		</li>
		<li>
			<strong>Horobetsu</strong> grammaticalizes <i lang="ain-Latn">siri</i> a step further,
			into adverbial-clause and conjunction-like markers — a 'versatile nominalization' use
			not reported for well-described Saru. The earliest attestation is Chiri's adversative
			construction (<i lang="ain-Latn">ci-sautar-i sinki siri suke ki wa …</i> 'my sisters,
			though tired, cooked…') <Ref k="takahashi2018" p="108, 111" />.
		</li>
		<li>
			<strong>Asahikawa and Shiranuka</strong> use <i lang="ain-Latn">ru an</i> in the
			inferential function that <i lang="ain-Latn">siri an</i> fills in the southern
			dialects — an apparent complementary distribution that complicates pan-Hokkaido
			generalizations <Ref k="dalcorso2018" p="326" />.
		</li>
	</ul>
</S>
