<S t="Morphology: siri is sir-i, 'the appearance of —'" id="morphology">
	<p>
		The source noun <A w="sir" /> covers a semantic cluster of visible and spatial referents:
		land, mountain, island, 'that area', and the observable appearance or state of something
		<Ref k="tamura1996" p="s.v. sir" />. The noun's polysemy — 'ground, surface, countenance,
		sheath' — noted by community analysts as unusually broad (nukopoli, aynu-corpora Discord
		2023) <Ref k="aynucorporadiscord" /> ‹corpus-confirmed› is consistent with Tamura's
		dictionary entry and with its distribution across compounds, place names, and weather
		predicates. The possessed form is <i lang="ain-Latn">sir-i</i>, 'the appearance of —',
		which serves as the evidential formal noun <Ref k="nakagawa2024" p="258, 391" />. Tamura
		notes explicitly that whether the concept form <i lang="ain-Latn">sir</i> and the
		possessed form <i lang="ain-Latn">siri</i> carry a usage or nuance distinction in the
		evidential function is unknown <Ref k="tamura1996" p="s.v. sir" /> ‹speculative›. The
		blocking of the extra syllable that suppresses <i lang="ain-Latn">*ruwehe</i> and
		<i lang="ain-Latn">*humihi</i> in evidential function does not apply here:
		<i lang="ain-Latn">siri</i> already coincides with the regular possessed form
		<Ref k="tamura1996" p="s.v. ruwe, humi" />.
	</p>

	<p>
		As a free noun, <A w="sir" /> has low currency in Saru and Chitose — it appears mainly
		word-internally in weather and light-condition compounds (<i lang="ain-Latn">sir-pirka</i>
		'good weather', <i lang="ain-Latn">sir-kunne</i> 'be dark', <i lang="ain-Latn"
			>sir-peker</i
		> 'be bright / dawn') and in place names such as <i lang="ain-Latn">Poro-sir</i>
		(Horoshiri, 'big mountain') <Ref k="nakagawa2024" p="391" />. The weather-compound use
		is the synchronic residue of the noun's meteorological-and-landscape origin and reinforces
		the evidential's visual-situational semantics. On the continuative and aspectual forms
		built on <A w="sir" /> — including <i lang="ain-Latn">siran</i> / <i lang="ain-Latn"
			>siri=an</i
		> 'pass (of time)' — see <Xr ch="continuative-siri-siran-kane-an" />.
	</p>
</S>

<S t="Meaning: directly visible situation" id="semantics">
	<p>
		The earliest grammarians classified <i lang="ain-Latn">siri ne</i> as the
		<span lang="ja">見説法</span>, the 'sight-report mood': it asserts what the speaker
		directly observes. Chiri glosses it as "saying 'such-and-such is / has just happened'
		about a fact before one's eyes"
		<Ref k="kindaichi1936" />; <Ref k="hirosawa2026" p="65–66" />. The modern evidential
		framing preserves the core: Nakagawa defines <i lang="ain-Latn">siri ne</i> as marking
		that the speaker's judgment rests on visual information
		<Ref k="nakagawa2024" p="392" />, and Tamura gives it as 'used for conditions that can
		be seen' <Ref k="hirosawa2026" p="67" />. Satō's question data refine the
		characterization: when the interrogative frame uses <i lang="ain-Latn">siri</i>, the
		content nominalized is a <em>process ongoing before the eyes</em> — not a completed event
		or stable property <Ref k="sato2008" p="134–135" />. Dal Corso's
		<span class="sc">dir.vis</span> gloss captures the same range: the copula
		<i lang="ain-Latn">ne</i> signals an internal, speaker-anchored perspective on a visually
		accessed event, distinguishing <i lang="ain-Latn">siri ne</i> from the
		external-perspective indirect forms <Ref k="dalcorso2018" p="305–306, 313–315" />.
	</p>

	<Ex
		m="a=kor ekasi a=imek-kar sir-i ne"
		g="4.A=have elder 4.A=offering-make appearance-POSS COP"
		tr="I am distributing (food) to my grandfather."
		cite="nakagawa2024:397"
		dial="SAR"
		note="The speaker presents her own ongoing action — the food distribution — as a directly visible situation under siri ne."
	/>

	<Ex
