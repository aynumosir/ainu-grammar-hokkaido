		g="now 4.A=APPL-be.old appearance-POSS COP CONJ thus"
		tr="And so I have aged along with it, as one can see —"
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker民話8(その3); ILCAA Ainu materials aa-irc/008#6"
		note="siri ne asserts the visible fact of the narrator's ageing concurrent with the passage of events; the wa-clause links the observation to the following narrative beat."
	/>

	<p>
		Two extensions of the basic visual reading arise in the corpus. Hirosawa cites an example
		in which <i lang="ain-Latn">siri ne</i> predicates a future event not yet observed — a
		punishment that will happen — showing that the form can range over a situationally
		entailed, not merely currently perceived, state of affairs
		<Ref k="hirosawa2026" p="70" /> ‹corpus-suggested›. And Dal Corso's
		<span class="sc">dir.vis</span> subsumes both ongoing direct perception and cases where
		visible evidence yields a near-certain situational inference; both are direct in the sense
		that the copula perspective is the speaker's own
		<Ref k="dalcorso2018" p="305–306" />.
	</p>

	<p>
		The contrast with <i lang="ain-Latn">ruwe ne</i> on the same visual base is the hardest
		boundary in the system. Dal Corso leaves it explicitly unresolved: a sense-channel
		hierarchy fails to predict the choice, and he suspects a driver beyond elicitation
		<Ref k="dalcorso2018" p="311–312, 335–336" />. Hirosawa's hypothesis — that
		<i lang="ain-Latn">siri</i> profiles the raw visible appearance while
		<i lang="ain-Latn">ruwe</i> profiles the knowledge extracted from it — is offered
		as a hypothesis (仮説), not a settled result
		<Ref k="hirosawa2026" p="81, 89" /> ‹contested›. The double-evidential evidence bearing
		on the split is set out in the section on double evidentiality below.
	</p>
</S>

<S t="The siri an polysemy" id="siri-an-polysemy">
	<p>
		<i lang="ain-Latn">siri an</i> is three-way ambiguous. The three readings are formally
		identical but contextually distinguished:
	</p>

	<ul>
		<li>
			<strong>Interrogative frame</strong>: <i lang="ain-Latn">siri</i> (formal noun) + the
			existential <i lang="ain-Latn">an</i> replacing the copula, forming a content question.
			The Kanazawa 1898 example
			<i lang="ain-Latn">Tokyo un paye=an siri an?</i> 'Did you go to Tokyo?' illustrates
			this use <Ref k="kanazawa1898" />; <Ref k="nakagawa2024" p="394" />. This is the
			interrogative-frame counterpart of declarative <i lang="ain-Latn">siri ne</i>, parallel
			to <i lang="ain-Latn">ruwe an</i> and <i lang="ain-Latn">hawe an</i> in the same
			paradigm; see <Xr ch="interrogative-strategies-question-particles" />.
		</li>
		<li>
			<strong>Indirect visual / reasoning evidential</strong>
			(<span class="sc">ind.rsn</span>): an evidential with external, non-copular perspective,
			reporting an event visually or situationally accessible but not directly witnessed by the
			current speaker. Dal Corso places this reading higher in reliability than
			<i lang="ain-Latn">siri ki</i> and notes that it systematically pairs with
			<i lang="ain-Latn">ruwe ne</i> in double-evidential constructions — discussed in the
			section on double evidentiality below
			<Ref k="dalcorso2018" p="306, 315–317, 325–327" />.
		</li>
		<li>
			<strong>Aspectual resultative / progressive</strong>: a periphrastic aspect form
			semantically close to <i lang="ain-Latn">wa an</i> / <i lang="ain-Latn">kor an</i>,
			expressing an unspecified or vaguely defined condition
			<Ref k="tamura2000" p="186" />; <Ref k="dalcorso2018" p="315–318" />. In this use
			<i lang="ain-Latn">siri an</i> functions as a lexical predicate ('be like, appear, pass
			of time') and is not an evidential. The aspectual and continuative uses, including
			<i lang="ain-Latn">siran</i>, are treated in
			<Xr ch="continuative-siri-siran-kane-an" />; the existential
			<i lang="ain-Latn">an</i> itself in
			<Xr ch="existential-aspect-an-oka">the existential and aspectual <i lang="ain-Latn"
					>an</i></Xr>.
		</li>
	</ul>

	<p>
		The three-way ambiguity is described functionally in the read sources but lacks a unified
		syntactic account; Satō 2013's analysis of <i lang="ain-Latn">siri an</i> as a
		polysemous aspectual-modal-evidential in Chitose is cited by Dal Corso but was not
		consulted directly <Ref k="dalcorso2018" p="316" /> ‹speculative›. Discourse context and
		the presence or absence of a question word are the primary practical diagnostics.
	</p>

	<Ex
		m="i=resu sir-i an sekor yaynu=an kor an=an a p"
		g="1SG.O=raise appearance-POSS exist QUOT 4.S=think while 4.S=exist PFV NMLZ"
		tr="I had been thinking that (my older sibling) was raising me, but (it turned out otherwise)…"
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker民話11; ILCAA Ainu materials aa-irc/013#348"
		note="The siri an here carries the aspectual / indirect value: 'it seemed that he was raising me'. The sekor yaynu=an frame embeds this under the narrator's own cognitive report; the nominalized tail a p carries an adversative nuance."
	/>

	<p>
		<i lang="ain-Latn">siri ki</i> (also contracted <i lang="ain-Latn">sirki</i>) is the
		unambiguous indirect-visual form. Dal Corso glosses it <span class="sc">ind.vis</span>:
		an event visible in a distanced way or from a source other than the current speaker, the
		verb <i lang="ain-Latn">ki</i> 'do' placing it in the external-perspective set alongside
		<i lang="ain-Latn">humi as</i> and <i lang="ain-Latn">hawe as</i>
		<Ref k="dalcorso2018" p="306–307, 313–314" />. A Kayano (Biratori) narrative instance
		has a character's skilled cooking assessed by a bystander under
		<i lang="ain-Latn">sirki</i>, expressing surprised indirect-visual perception
		<Ref k="dalcorso2018" p="307" />.
	</p>
</S>

