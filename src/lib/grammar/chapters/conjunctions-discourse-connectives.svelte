<S t="Word-class status and the connective–clause-linker boundary" id="word-class">
	<p>
		Hokkaido Ainu has no dedicated word class of conjunctions (接続詞). Nakagawa folds the
		clause-initial discourse connectives treated here into the adverb class (副詞) and concludes
		that no separate conjunction category is warranted
		<Ref k="nakagawa2024" p="516, §21.2.3" />. The structural ground for this classification is
		the independence of these forms: they need no host predicate, impose no morphological
		requirement on the following clause, and can occur sentence-initially after a pause or
		paragraph break. This distinguishes them from the bound post-predicate 接続助詞 (clause-linking
		particles) — <A w="wa" />, <A w="hine" />, <A w="kor" />, <A w="akusu" />, and their
		relatives — which attach directly to a verbal stem and cannot stand alone. A further
		diagnostic is stacking: the free connectives can follow a 接続助詞 without any intervening
		predicate, yielding attested sequences such as <i lang="ain-Latn">… hine, orano …</i>
		'and then, after that …' <Ref k="nakagawa2024" p="516" /> ‹consensus›.
	</p>

	<p>
		The bound clause-linkers and the switch-reference-like behaviour of
		<A w="hine" /> versus <A w="akusu" /> are treated in
		<Xr ch="clause-linkage-overview" /> and <Xr ch="hine-akusu-switch-reference" />;
		the sequential particle <A w="wa" /> as a clause-chaining device in its own right is in
		<Xr ch="sequential-wa-and-clause-chaining" />. The present chapter covers only the
		free, clause-initial layer, grouping its members by discourse function: the sequential–
		temporal pair <A w="orano" /> and <A w="rapokke" />; the scene-shift adverb
		<A w="tane" />; the anaphoric-resumptive <A w="newaanpe" />; the NP coordinators
		<A w="newa" /> and <A w="usa" />; and the demonstrative-adjacent forms
		<A w="nea" /> and <A w="tap" />.
	</p>
</S>

<S t="orano and orowano — the sequential connective" id="orano">
	<p>
		<A w="orano" /> (variant forms <A w="orowano" />, <A w="ora" />, <A w="oraun" />) is
		the default narrative sequencer, glossed 'and then, after that'
		<Ref k="nakagawa2024" p="516" />. Its etymology is transparent: the relational-locative
		noun <A w="or" gl="side, place, vicinity" /> combined with the ablative source particle
		<A w="wano" gl="from, away from" /> yields <i lang="ain-Latn">orowano</i> 'from that
		place/point onwards', which has grammaticalized to a purely sequential reading. The
		syncopated form <i lang="ain-Latn">orano</i> is the most frequent in the aligned
		corpus (rank 114; 1,927 tokens across Hokkaido and Sakhalin data); further reductions
		to <i lang="ain-Latn">ora</i> and <i lang="ain-Latn">oraun</i> are dialectally attested
		<Ref k="nakagawa2024" p="516" /> ‹corpus-confirmed›.
	</p>

	<p>
		In both narrative and conversational discourse, <A w="orano" /> can open a new clause
		after a completed prior clause, or stack immediately after a 接続助詞 such as
		<A w="akusu" /> 'when/after'. The following example shows the latter stacking pattern:
	</p>

	<Ex
		m="sekor ku=hawean akusu orano"
		g="QUOT 1SG.S=say COND then"
		tr="…after I said that, and then, …"
		cite="ilcaa1976"
		dial="SAR"
		place="川上まつ子, isoytak 独話2 (AA研アイヌ語資料 音声資料); aa-irc/004#133"
		note="orano follows the conditional–temporal particle akusu directly, at the boundary of a completed quotative frame and the next episode. The Saru narrator's spoken monologue shows this stacking as a routine narrative device."
	/>

	<p>
		The combination of <A w="orano" /> with the resumptive form <A w="newaanpe" /> (§
		below) recurs as a narrative construction: a completed event is nominalized and then
		resumed by the anaphoric demonstrative, providing both temporal succession and referent
		continuity within a single discourse move
		<Ref k="nakagawa2024" p="516" />. The function of such sequences in maintaining
		episode cohesion is developed further in <Xr ch="tail-head-linkage-and-narrative-cohesion" />.
	</p>
</S>

<S t="rapokke and rapoketa — simultaneous temporal" id="rapokke">
	<p>
		<A w="rapokke" /> and <A w="rapoketa" /> mark temporal overlap or parallelism between
		two narrative threads rather than strict sequentiality, and are glossed 'meanwhile, at
		that time, at that juncture' <Ref k="nakagawa2024" p="516–517" />. Both derive
		transparently from the locative noun <A w="rapok" gl="that moment, that juncture" />
		extended by a locative or temporal suffix, following the same etymological pattern as
		<A w="orano" /> from <A w="or" />. Their frequency in the aligned corpus is
		considerably lower than that of <A w="orano" />, and they appear primarily in narrative
		prose rather than conversation. Nakagawa gives no dialect restriction for these forms.
	</p>
</S>

<S t="tane — temporal adverb and scene-shift marker" id="tane">
	<p>
		<A w="tane" /> is the general temporal adverb 'now'; its discourse-connective use
		emerges when it opens a new clause after a completed prior episode. In this position —
		particularly after a clause nominalized by <A w="p" gl="fact, NMLZ" /> or after an
		episode-closing evidential frame — <A w="tane" /> signals a shift in scene, time
		reference, or central participant, functioning as a narrative reset
		<Ref k="nakagawa2024" p="516" />. There is no
		separate discourse-particle lexeme: it is the ordinary temporal adverb 'now' that
		acquires connective force from its position and the completion of the preceding unit.
		The corpus rank (54; 4,272 tokens) therefore conflates the temporal-adverb and
		scene-shift uses ‹speculative›; per-function counts would require disambiguation
		across individual texts.
	</p>

	<Ex
		m="tane k=arpa"
		g="now 1SG.S=go"
		tr="Now — after that — I set off."
		constructed
		note="Constructed minimal form. In attested narrative, tane typically follows a clause-final nominalizer p or a perfective auxiliary a closing an episode, and the shift in time reference is anchored to the new episode that follows."
	/>

	<p>
		The interaction of <A w="tane" /> with the aspectual and TAM apparatus of the verb it
		introduces belongs to <Xr ch="tenselessness-and-time-reference" />;
		its role in signalling participant change across episodes is taken up in
		<Xr ch="reference-tracking" /> and <Xr ch="narrative-tam-evidential-patterning-by-genre" />.
	</p>
</S>

<S t="newaanpe — the resumptive anaphoric connective" id="newaanpe">
	<p>
		<A w="newaanpe" /> is the form most distinctively positioned at the clause boundary
		as a cohesive device, yet it is best analysed as an anaphoric demonstrative NP rather
		than a pure conjunction. Its composition is transparent:
		<A w="nea" gl="that (anaphoric demonstrative)" /> + <A w="an" gl="exist, be present" />
		+ <A w="pe" gl="thing, fact" /> = 'that existing thing/fact'. The whole form occupies
		nominal position, refers anaphorically to a proposition or entity just established in
		the prior clause, and introduces the clause that predicates something of it — yielding
		a resumptive-cohesive function glossed 'and as for that / and as for that (fact)'
		<Ref k="nakagawa2024" p="516" />. Its connective feel is structural (clause-boundary
		position + invariably anaphoric reference) rather than intrinsic to its lexical class.
	</p>

	<p>
		Corpus rank 1,349 (84 tokens) reflects the more specialized, narrative-internal role
		of <A w="newaanpe" /> compared with <A w="orano" />. The form has low homonymy —
		unlike <A w="tane" /> or <A w="tap" /> — so the count is a relatively clean estimate
		of its frequency ‹corpus-confirmed›.
	</p>

	<Ex
		m="honkor hi e-yay-oyamokte newaanpe"
		g="become.pregnant NMLZ APPL-REFL-be.surprised ANAPH"
		tr="She was surprised herself that she had become pregnant, and as for that, …"
		cite="ilcaa1976"
		dial="SAR"
		place="川上まつ子, uwepeker 民話20 (AA研アイヌ語資料 音声資料); aa-irc/030#82"
		note="newaanpe resumes the nominalized proposition (honkor hi 'the fact of becoming pregnant') as the topic NP of the next clause. The APPL prefix e- on oyamokte marks that the surprise is directed at the preceding fact; the REFL yay- marks the subject as co-referential with the surprisee."
	/>

	<p>
		The boundary between 'resumptive demonstrative NP' and 'discourse connective proper'
		is not drawn in the available sources; the form participates in both the demonstrative
		system (<Xr ch="demonstratives-anaphora-definiteness" />) and in discourse-structural
		patterns that overlap with topic marking (<Xr ch="topic-marking-anakne" />). Whether
		to treat it as a connective or as an instance of an anaphoric topic-NP in clause-initial
		position is a matter of descriptive standpoint rather than an empirical question the
		present data resolve ‹speculative›.
	</p>
</S>

<S t="NP coordinators — newa and usa" id="np-coordinators">
	<p>
		Two additional forms operate at the level of nominal coordination rather than at the
		clause boundary. <A w="newa" /> 'and' joins two NPs into a single conjunctive NP and
		marks the first member; it does not appear after the last member of the conjunction
		<Ref k="nakagawa2024" p="322" />. A sharp regional restriction applies: the Saru and
		Chitose dialects use bare NP juxtaposition for nominal coordination and lack
		<A w="newa" /> in this function <Ref k="nakagawa2024" p="322" />. Community discussion
		corroborates the restriction independently — <A w="newa" /> as an explicit 'and'-coordinator
		is little used in modern Ainu and its distribution is dialectally conditioned
		<Ref k="aynucorporadiscord" /> (nukopoli, 2023-12-12) ‹corpus-suggested›.
	</p>

	<p>
		A proposed morphological decomposition <i lang="ain-Latn">ne + wa</i> — with
		<A w="ne" gl="be (COP)" /> and <A w="wa" gl="CONJ (sequential clause-linker)" /> —
		has been noted in community discussion as consistent with the connective meaning 'it
		being so, and …' <Ref k="aynucorporadiscord" /> (aomidori_cs_, 2023-12-12). This
		decomposition is an informal proposal unsupported by a published etymological study
		‹contested›.
	</p>

	<Ex
		m="seta newa cikap ci=e"
		g="dog and bird 1PL.EXCL.A=eat"
		tr="We ate dog and bird."
		constructed
		note="newa marks the first NP (seta 'dog'); the second conjunct (cikap 'bird') follows directly. Saru and Chitose speakers would use bare juxtaposition (seta cikap ci=e) in the same context."
	/>

	<p>
		<A w="usa" /> 'and, as well as, both … and …' serves as the Tokachi/Kushiro counterpart
		for listing. Unlike <A w="newa" />, which marks only the first member, <A w="usa" /> can
		appear after each coordinated element, and it extends to verbal predicates as well as
		nominal ones; in western Hokkaido the same form functions as an adverb rather than a
		particle <Ref k="nakagawa2024" p="324" />.
	</p>

	<Ex
		m="iku usa ipe usa ki"
		g="drink and eat and do"
		tr="Both drink and eat."
		cite="nakagawa2024:324"
		dial="TOK"
		note="Each listed verbal predicate receives usa; the construction is closed by the light verb ki 'do'. In western Hokkaido (including Saru/Chitose), usa does not function as a coordination particle."
	/>
</S>

<S t="nea and tap — demonstrative-adjacent forms" id="demonstrative-adjacent">
	<p>
		The anaphoric determiner <A w="nea" /> 'that (aforementioned)' is not a connective in
		its own right but the demonstrative stem that underlies both <A w="newaanpe" /> (§ above)
		and topic-resumption constructions where an anaphoric NP headed by <A w="nea" /> opens
		the next clause. The demonstrative system and the proximal/distal/anaphoric paradigm are
		in <Xr ch="demonstratives-anaphora-definiteness" />.
	</p>

	<p>
		<A w="tap" /> and <A w="tapne" /> present a dual picture. As temporal-emphatic adverbs
		they carry the senses 'just now', 'thus', 'precisely this/that', positioning the clause
		in the immediate narrative moment or resuming a prior referent with deictic force
		<Ref k="nakagawa2024" p="516" />. However, the phonological form
		<A w="tap" /> also functions as an emphatic particle (係助詞 in the kakari-musubi
		framework, §2.2c of the factbase) with the sense 'こそ/まさに', and corpus counts
		(rank 143; 1,330 tokens) conflate the two uses. The boundary between the clause-initial
		emphatic adverb and the emphatic particle is not settled in the read sources ‹speculative›.
		The particle use is treated in <Xr ch="cleft-nominalization-focus" />. Cross-reference
		also <Xr ch="demonstratives-anaphora-definiteness" /> for the demonstrative continuum
		within which both <A w="nea" /> and <A w="tap" /> sit.
	</p>
</S>

