	<p>
		The foundational work in the modern Japanese descriptive tradition is <Ref k="kindaichi1936" />,
		a collaborative outline grammar by Kindaichi Kyōsuke and Chiri Mashiho. It introduced the
		numbered person-form system and identified the auxiliary verb paradigm and the evidential
		nominalizers, establishing the analytical categories that all subsequent Japanese-language
		description built on. Its Hokkaido dialect coverage is uneven by contemporary standards, and
		many of its morphological analyses have been revised since, but it remains the indispensable
		point of entry into the classical tradition. Chiri's separate grammar of the Sakhalin
		dialect <Ref k="chiri1942" /> falls outside this grammar's primary scope; it is cited when
		the Hokkaido–Sakhalin structural comparison in <Xr ch="sakhalin-and-kuril-ainu-contrast" />
		requires a Sakhalin-side primary source.
	</p>
</S>

<S t="Modern reference grammars" id="modern-reference-grammars">
	<p>
		<Ref k="refsing1986" /> is the only pre-2020 monograph grammar dedicated to a single
		Hokkaido variety: the Shizunai dialect of the Hidaka region (SHI). It covers morphology and
		syntax in full paradigm tables and remains an important comparator for the Saru baseline
		used in this grammar, given that Shizunai belongs to the southwest Hokkaido group and the
		correspondences to Saru are close. For features where Shizunai and Saru diverge, dialect
		tags signal which variety a cited form comes from; the systematics of the southwest group
		are discussed in <Xr ch="dialect-sample-and-corpus-method" />.
	</p>

	<p>
		<Ref k="shibatani1990" /> includes an Ainu chapter in his typological survey of the
		languages of Japan. The coverage is sketch-level, but its framing in terms of polysynthesis,
		noun incorporation, and alignment introduced Hokkaido Ainu to a wide comparative-linguistics
		audience. It is cited here when a brief typologically-oriented reference is needed for a
		structural property treated in detail in <Xr ch="typological-profile" />.
	</p>

	<p>
		<Ref k="sato2008" /> is the most comprehensive Japanese-language descriptive grammar
		available for the Saru–Chitose core. The treatment spans phonology, the full verbal
		template, the person-affix system, aspect, evidentiality, nominalization, and clause
		linkage, with extensive paradigm tables and elicited examples. It is held locally and
		constitutes one of the two primary structural references throughout this grammar; its
		analyses of the auxiliary verb system and person forms are the starting point for the
		corresponding chapters.
	</p>

	<p>
		<Ref k="bugaeva2012" /> provides a compact English sketch of southern Hokkaido Ainu,
		covering the Saru–Chitose varieties. As the most accessible single English-language
		overview of the core dialect group, it is cited when a concise English-language reference
		is needed alongside the more detailed Japanese treatments.
	</p>

	<p>
		<Ref k="bugaeva2022" /> is a multi-author handbook in the De Gruyter Mouton series on the
		Japanese language and linguistics (HJLL 12). Its chapters, each by a specialist on a
		specific grammatical domain, offer the broadest cross-dialect coverage of any single
		reference work currently available. This grammar cites Handbook chapters under their
		respective author keys throughout the content chapters; where a Handbook treatment and the
		analyses of Satō or Nakagawa diverge, the disagreement is stated explicitly.
	</p>

	<p>
		<Ref k="nakagawa2024" /> is the most recent and most comprehensive single-author grammar
		of the Ainu language. It covers the full range of Hokkaido dialects with attention to
		micro-variation, incorporates data from large annotated corpora, and provides substantial
		sections on syntax and discourse that earlier grammars left largely undescribed. It is held
		locally and serves as the principal structural reference for this grammar, particularly for
		the phonology, the person-marking system, the auxiliary and evidential constructions, and
		the formal-noun nominalization paradigm. Where the present grammar departs from Nakagawa's
		analysis, the departure is stated and the reasoning given.
	</p>

	<p>
		<Ref k="tamura2000" /> is an English-language grammar sketch of Saru Ainu, adapted from
		Tamura's 1988 Japanese encyclopedia article. It is not held locally and carries the
		<em>reported</em> status explained in the section below. Structural claims that rely on it
		alone are cross-checked against Satō and Nakagawa; the Saru dictionary
		<Ref k="tamura1996" /> (see below) provides the primary-data complement.
	</p>
</S>

<S t="Dictionaries and lexical resources" id="dictionaries">
	<p>
		<Ref k="tamura1996" /> is the comprehensive Ainu–Japanese dictionary of the Saru dialect.
		Beyond word-forms and definitions, it records grammatical category membership, possessed
		forms, and aspect alternations for individual lexical items. In this grammar it functions as
		a primary data source for Saru morphological evidence — affiliative forms, valency-class
		membership, paradigm alternants — that goes beyond what the grammar texts directly supply.
		Although not held locally in the project corpus, individual entries are cited throughout the
		content chapters when lexical-paradigmatic evidence is needed.
	</p>

	<p>
		<Ref k="nakagawa1995" /> and <Ref k="kayano1996" /> cover the Chitose and Saru dialects
		respectively. Both carry the <em>reported</em> status in this grammar; they are accessible
		through the db.aynu.org catalogue, and their evidence is cross-checked against
		<Ref k="tamura1996" /> and the spoken corpus where possible. Together, the three dictionaries
		supply the lexical–paradigmatic backbone for the Saru–Chitose core that the structural
		chapters draw on when individual morpheme forms are at issue.
	</p>

	<p>
		<Ref k="hattori1964" /> is the comparative dialect dictionary, covering nineteen
		localities across Hokkaido and Sakhalin with a 200-item basic-vocabulary list per site.
		Its word-form data underpin all subsequent Hokkaido dialect classification, and it is cited
		in the dialectological chapters as a reported source. The dialect-classification literature
		built on it is treated in <Xr ch="hokkaido-lexical-dialectology-and-the-dialect-atlas" />.
	</p>

	<p>
		Chiri Mashiho's multi-volume classified dictionary <Ref k="chiri1953" />, arranged by
		semantic field (plants, animals, humans), is the standard reference for cultural-semantic
		vocabulary and has been cited extensively in toponymic and ethnobotanical research. It is
		not held locally and is cited here as a reported source; structural claims resting on it
		alone are flagged accordingly in the relevant content chapters.
	</p>
</S>

<S t="The philological record" id="philological-record">
	<p>
		Batchelor's Ainu–English–Japanese dictionary, first published in 1905 and revised through
		four editions to a fourth in 1938 <Ref k="batchelor1905" />; <Ref k="batchelor1938" />,
		includes a grammar sketch of the Ainu of the northwestern Hokkaido region as it was spoken
		in the late nineteenth and early twentieth centuries. Both editions are cited in this grammar
		with the <em>philological witness</em> role and the <em>reported</em> status: Batchelor's
		transcription conventions and analytical categories diverge substantially from those of
		the mid-century Japanese tradition, and his structural claims require verification against
		the Saru–Chitose baseline before they can bear analytic weight. The phonological
		transcription system he used is discussed in <Xr ch="latin-phonemic-transcription" />.
	</p>

	<p>
		The Kaga-ke monjo (加賀家文書), a collection of Edo-period manuscript records preserved
		at the National Ainu Museum and other repositories, contains Ainu-language material —
		glossaries, word lists, and transcribed phrases — predating systematic grammatical
		description. <Ref k="fukazawa2017" /> provides a philological study of this collection,
		and this grammar accesses Edo-period lexical and phonological evidence through that work.
		The Sino-Ainu glosses of the illustrated document known as the
		<span lang="ja">蝦夷島奇觀</span> (Ezoshima kikan) are studied by
		<Ref k="yasuoka2023" />, whose linguistic analysis is the entry point for that early
		attestation. Neither document furnishes sufficient structural data to affect grammatical
		analyses directly; both are relevant to the historical phonology of
		<Xr ch="proto-ainu-segmental-reconstruction" />, to which the interested reader is
		directed for their philological use.
	</p>
</S>

<S t="Source gaps and the reported-evidence convention" id="source-gaps">
	<p>
		Several works that descriptive practice requires citing are not directly consulted in
		this grammar: the full text is either unavailable locally or was not read in preparation
		for the chapter in question. These sources carry the <em>reported</em> marker in the
		auto-generated references list and are flagged with a badge wherever they appear. The
		convention is defined in <Xr ch="glossing-abbreviations-and-citation" />: claims that
		rest solely on a reported source are not treated as independently verified, and where a
		structural analysis depends on such a claim, an independent supporting source is required
		or the uncertainty is stated explicitly.
	</p>

	<p>
		Among the grammars, <Ref k="tamura2000" /> is the most significant reported source:
		its analytic coverage of Saru is cited throughout the secondary literature, but the full
		text is not held in the project corpus, and its grammatical claims are consistently
		cross-checked against <Ref k="sato2008" /> and <Ref k="nakagawa2024" />. Among the
		Sakhalin-side sources, <Ref k="dalcorso2021" /> — a reedition of Murasaki's West
		Sakhalin materials with grammatical notes — is cited as reported; its evidence enters this
		grammar only where the Sakhalin contrast in <Xr ch="sakhalin-and-kuril-ainu-contrast" />
		requires a philologically worked Sakhalin source.
	</p>

	<p>
		The transcription systems, citation formats, and abbreviation conventions used throughout
