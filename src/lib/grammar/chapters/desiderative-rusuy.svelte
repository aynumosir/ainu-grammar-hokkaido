<S t="The auxiliary and its form" id="form">
	<p>
		The postverbal auxiliary <A w="rusuy" gl="want to" /> follows the lexical verb directly, with no
		intervening conjunction. This places it in the auxiliary (<em>A</em>) frame that
		<Xr ch="tam-overview">the TAM overview</Xr> distinguishes from the pair-verb frame on the basis
		of person marking: in <i lang="ain-Latn">V rusuy</i>, only the lexical verb <i lang="ain-Latn"
			>V</i
		> carries person agreement; <i lang="ain-Latn">rusuy</i> is morphologically invariant regardless
		of the number or person of the experiencer
		<Ref k="nakagawa2024" p="306, 312–313" />; <Ref k="bugaeva2012" p="493" />.
	</p>

	<table>
		<thead>
			<tr>
				<th>frame type</th>
				<th>shape</th>
				<th>person marking</th>
				<th>conjunction</th>
			</tr>
		</thead>
		<tbody>
			<tr>
				<td>auxiliary (A)</td>
				<td><i lang="ain-Latn">V rusuy</i></td>
				<td>on <i lang="ain-Latn">V</i> only; <i lang="ain-Latn">rusuy</i> is invariant</td>
				<td>none (immediate adjacency)</td>
			</tr>
		</tbody>
	</table>

	<p>
		Nakagawa groups <i lang="ain-Latn">rusuy</i> in 表74 alongside
		<i lang="ain-Latn">a</i>, <i lang="ain-Latn">aan</i>, <i lang="ain-Latn">nankor</i>,
		<i lang="ain-Latn">easirki</i>, and several further items as 本来的な助動詞 (proper auxiliaries of
		the Saru dialect). The defining property of all members of that table is the absence of an
		independent verbal use — with one exception: <i lang="ain-Latn">rusuy</i> alone retains a use
		as a full verb alongside its auxiliary function
		<Ref k="nakagawa2024" p="306" />. That exceptional status is discussed in the section below.
		Bugaeva classifies <i lang="ain-Latn">rusuy</i> as a desiderative modal without further
		qualification <Ref k="bugaeva2012" p="493" />. Tamura's 1960 study of Saru auxiliaries
		describes the form in its original Japanese-language analytical context
		<Ref k="tamura1960" />.
	</p>

	<Ex
		m="inkar=an rusuy"
		g="look=4.S DES"
		tr="One wants to look; one has a wish to see."
		cite="nakagawa2024:312"
		dial="SAR"
		place="Nakagawa exx. 473–476 (cited form)"
		note="Fourth-person (indefinite) subject: the person-agreement suffix =an attaches to the main verb inkar, and rusuy is bare. The fourth person here expresses a generic or narrator's first-person referent in the narrative context."
	/>

	<p>
		The construction appears in Saru narrative, conversational, and formal texts alike, and the
		accessible sources document no restriction to a particular speech level.
	</p>
</S>

<S t="Fusion and lexicalization" id="fusion">
	<p>
		<i lang="ain-Latn">rusuy</i> exhibits a strong tendency to fuse phonologically with the
		preceding verb stem, producing single lexicalized units. Two forms are explicitly documented for
		Saru: <i lang="ain-Latn">arparusuy</i> 'want to go' (from <A w="arpa" gl="go (SG)" /> +
		<i lang="ain-Latn">rusuy</i>) and <i lang="ain-Latn">iperusuy</i> 'want to eat a meal' (from
		<A w="ipe" gl="eat a meal" /> + <i lang="ain-Latn">rusuy</i>)
		<Ref k="nakagawa2024" p="312–313" />. The morphological closeness parallels the lexical
		incorporation of an action-verb root into the abilitative <i lang="ain-Latn">easkay</i>, which
		yields fused forms such as <i lang="ain-Latn">sinotca-easkay</i> 'be able to compose songs'
		<Ref k="nakagawa2024" p="315" />. Both auxiliaries therefore sit at an intermediate point on
		the continuum from syntactic auxiliary to derivational suffix, and the desiderative fusions
		represent points closer to the lexicalized end of that cline ‹corpus-suggested›.
	</p>
</S>

<S t="Argument structure and the experiencer" id="argument-structure">
	<p>
		The person who wants must be the same individual who would perform the action: in
		<i lang="ain-Latn">V rusuy</i>, the experiencer of the desire and the would-be agent or subject
		of <i lang="ain-Latn">V</i> are necessarily coreferential. No separate morphological device
		marks this relation; it follows from the single person-agreement slot on
		<i lang="ain-Latn">V</i>. For a transitive lexical verb, agent and object agreement appear in
		the normal position on <i lang="ain-Latn">V</i>:
	</p>

	<Ex
		m="ku=e rusuy"
		g="1SG.A=eat DES"
		tr="I want to eat (it)."
		cite="ijas2023"
		constructed
		note="Ijäs's constructed illustration. The first-person agent ku= attaches to the transitive verb e 'eat'; rusuy is bare. The unspecified patient is understood from context. This is the simplest transitive desiderative form."
	/>

	<p>
		Ainu has no infinitive: the language does not permit a non-finite verbal complement. Where many
		languages use a to-infinitive ('I want <em>to go</em>'), Ainu requires a finite auxiliary
		construction such as <i lang="ain-Latn">V rusuy</i> or, in other complementation contexts, a
		nominalization with the formal nouns <i lang="ain-Latn">pe</i>, <i lang="ain-Latn">hi</i>, or
		<i lang="ain-Latn">kuni</i>. This follows from the head-marking architecture of the verb, under
		which every finite predicate carries a person index. The desiderative construction handles one of
		the core functions that infinitival complements serve in other languages
		<Ref k="ijas2023" />. Community discussion has made the same observation: nukopoli
		<Ref k="aynucorporadiscord" /> (nukopoli, aynu-corpora Discord 2024-11-25) notes that
		<i lang="ain-Latn">ku=e rusuy</i>, <i lang="ain-Latn">ku=e easkay</i>, and the
		<i lang="ain-Latn">kuni</i> nominalization divide the work that a single to-infinitive carries
		in English — a lead consistent with the typological profile of head-marking,
		nominalization-dependent languages ‹proposed, nukopoli, aynu-corpora Discord 2024›.
	</p>
</S>

<S t="Rusuy as full verb: the dual status" id="verbal-status">
	<p>
		Nakagawa's explicit exception note — that <i lang="ain-Latn">rusuy</i> is the only member of
		表74 to retain an independent verbal use — implies a structural distinction between two modes of
		use <Ref k="nakagawa2024" p="306" />:
	</p>

	<table>
		<thead>
			<tr>
				<th>mode</th>
				<th>shape</th>
				<th>person marking</th>
				<th>meaning</th>
			</tr>
		</thead>
		<tbody>
			<tr>
				<td>auxiliary</td>
				<td><i lang="ain-Latn">V rusuy</i></td>
				<td>on <i lang="ain-Latn">V</i>; <i lang="ain-Latn">rusuy</i> invariant</td>
				<td>want to perform action <i lang="ain-Latn">V</i></td>
			</tr>
			<tr>
				<td>full verb</td>
				<td><i lang="ain-Latn">rusuy</i> alone or with object</td>
				<td>on <i lang="ain-Latn">rusuy</i> itself</td>
				<td>want (a thing or state of affairs)</td>
			</tr>
		</tbody>
	</table>

	<p>
		The diagnostic between the two modes is position and the locus of person agreement. When
		<i lang="ain-Latn">rusuy</i> follows a person-marked lexical verb and is itself bare, the
		auxiliary reading holds. When <i lang="ain-Latn">rusuy</i> receives its own person prefix
		without a preceding person-marked verb, it functions as the clause's main predicate. The exact
		frequency and distribution of the full-verb use are not documented with corpus examples in the
		accessible sources; the distinction rests on Nakagawa's descriptive note ‹corpus-suggested›. The
		abilitative <i lang="ain-Latn">easkay</i> offers a partial parallel in that it too appears both
		as an auxiliary following a person-marked verb and as a predicate in its own right; unlike
		<i lang="ain-Latn">rusuy</i>, however, it belongs to 表75 (the transitive-verb auxiliary group)
		and is classified as a transitive verb throughout, making its dual status structurally different
		<Ref k="nakagawa2024" p="314–315" />. The <i lang="ain-Latn">rusuy</i> case is therefore the
		only instance in 表74 of a proper auxiliary with documented verbal currency.
	</p>
</S>

<S t="Possession desire: kor rusuy" id="kor-rusuy">
	<p>
		When the object of desire is a thing rather than an action, the standard strategy uses the
		transitive possessive verb <A w="kor" gl="have, possess, hold" /> as the lexical
		<i lang="ain-Latn">V</i>. The possessed entity — the thing wanted — appears as the object of
		<i lang="ain-Latn">kor</i>, and <i lang="ain-Latn">rusuy</i> attaches in its normal auxiliary
		position:
	</p>

	<Ex
		m="ku=kor rusuy"
		g="1SG.A=have DES"
		tr="I want it."
		constructed
		note="A nominal object precedes kor in the full clause: e.g. cep ku=kor rusuy 'I want a/the fish'. rusuy is in its auxiliary function; kor takes the normal person prefix."
	/>

	<p>
		<i lang="ain-Latn">kor rusuy</i> is thus a sub-case of <i lang="ain-Latn">V rusuy</i> in which
		<i lang="ain-Latn">V</i> is the possessive verb <i lang="ain-Latn">kor</i> 'have'. The semantic
		contrast it encodes — thing-desire versus action-desire — is crosslinguistically common in
		languages that use a single desiderative auxiliary:
