<S t="The applicative-desiderative" id="applicative-desiderative">
	<p>
		The applicative prefix <A w="e-" gl="applicative" /> (treated fully in <Xr ch="applicative-e" />)
		introduces a new patient, goal, or stimulus argument to an otherwise intransitive verb. When that
		transitivized verb precedes <i lang="ain-Latn">rusuy</i>, the result is an
		applicative-desiderative expressing desire directed toward an explicit referent:
	</p>

	<Ex
		m="ku=e-arpa rusuy"
		g="1SG.A=APPL-go DES"
		tr="I want to go to/for it."
		constructed
		note="The applicative prefix e- introduces a goal or beneficiary argument to the intransitive motion verb arpa 'go (SG)'. Person agreement (ku= 1SG.A) attaches at the outer edge of the complex stem; rusuy is bare. The person-marking pattern is identical to the plain V rusuy frame."
	/>

	<p>
		The construction is the regular intersection of the desiderative auxiliary frame with the
		applicative system. <i lang="ain-Latn">rusuy</i> attaches in its normal postverbal position,
		person marking follows the same rule (on the lexical verb only), and the semantic contribution
		of <i lang="ain-Latn">e-</i> is governed by the applicative semantics of
		<Xr ch="applicative-e" />. The accessible sources do not provide corpus examples specifically
		for the <i lang="ain-Latn">e-V rusuy</i> combination; the account here rests on the
		independently documented behaviour of both components ‹corpus-suggested›. Where the lexical verb
		is already transitive — as in <i lang="ain-Latn">ku=e rusuy</i> 'I want to eat it' — the
		applicative prefix is not needed, and the object agreement appears on the transitive stem in the
		normal way.
	</p>
</S>

<S t="Negation and scope" id="scope">
	<p>
		Negation of the desiderative complex places <i lang="ain-Latn">somo</i> before the lexical verb
		and its person agreement, bringing the whole <i lang="ain-Latn">V rusuy</i> predicate into the
		scope of negation:
	</p>

	<Ex
		m="somo ku=nukar rusuy"
		g="NEG 1SG.A=see DES"
		tr="I do not want to see (it)."
		constructed
		note="somo negates the V rusuy predicate as a unit. This is the expected scope position for sentential negation in the auxiliary frame: somo sits inside any outer evidential frame and takes the modal complex as its target."
	/>

	<p>
		The placement parallels the general negation pattern for the auxiliary frame: negation in Ainu
		is clause-internal and sits inside any outer evidential projection
		<Ref k="nakagawa2024" p="393" />. Within the desiderative predicate itself, the nesting order
		follows the auxiliary stacking constraint: aspect auxiliaries attach to the lexical verb, and
		<i lang="ain-Latn">rusuy</i> scopes over the resulting aspectual complex. An evidential frame
		such as <i lang="ain-Latn">ruwe ne</i> then closes the whole expression — the ordering
		<i lang="ain-Latn">V rusuy ruwe ne</i> 'it is a recognized fact that one wants to V' is
		expected and the reverse <i lang="ain-Latn">*ruwe ne V rusuy</i> would be ill-formed, in line
		with the general constraint that the evidential takes widest scope among the clause-internal
		operators <Ref k="nakagawa2024" p="393" />. The full modal field in which
		<i lang="ain-Latn">rusuy</i> sits — alongside the epistemic <i lang="ain-Latn">nankor</i> and
		the prospective <i lang="ain-Latn">kusu ne</i> — is treated in
		<Xr ch="epistemic-irrealis-modality" />, and the deontic space adjacent to the desiderative in
		<Xr ch="deontic-necessity" />.
	</p>
</S>

<S t="Semantic range: community observations" id="semantics">
	<p>
		The core value of <i lang="ain-Latn">rusuy</i> is desiderative: it predicates a desire on the
		part of the grammatical subject to perform the action of the preceding verb. In practice, and
		particularly with verbs of bodily state or emotional reaction, the meaning can shade into an
		imminent-event reading. The community member nukopoli has proposed a comparison with Chinese
		<span lang="zh">要</span> (yào), which covers both 'want to' and 'be about to', citing
		<i lang="ain-Latn">mokor rusuy</i> 'want to sleep / be getting sleepy' and
		<i lang="ain-Latn">cis rusuy</i> 'want to cry / be about to cry' as illustrative cases
		<Ref k="aynucorporadiscord" /> (nukopoli, aynu-corpora Discord 2024-11-29). The same observation
		raises a possible morphological link between <i lang="ain-Latn">rusuy</i> and
		<i lang="ain-Latn">rawe</i>, but no independent phonological or semantic evidence supports this
		etymology in the accessible sources ‹speculative›. The near-future shading is best understood as
		a pragmatic inference from the desiderative meaning rather than a separate grammaticalized
		prospective value; the grammaticalized prospective function in Ainu is carried by
		<i lang="ain-Latn">kusu ne</i> and related forms, treated in
		<Xr ch="epistemic-irrealis-modality" />.
	</p>
</S>
