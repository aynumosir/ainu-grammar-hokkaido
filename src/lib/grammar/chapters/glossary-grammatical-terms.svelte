<S t="Terms" id="terms">
	<dl>
		<dt id="affiliative-form"><strong>affiliative form</strong></dt>
		<dd>
			The possessed or combining shape of a noun, as distinct from its citation shape (see
			<strong>concept form</strong>). The affiliative suffix — realized as
			<i lang="ain-Latn">-he</i>, <i lang="ain-Latn">-i</i>, <i lang="ain-Latn">-e</i>, or
			other allomorphs depending on the stem — attaches to produce the possessed noun used
			inside possessive phrases, nominalized constructions, and evidential frames:
			<i lang="ain-Latn">ru-we</i> 'the trace of —', <i lang="ain-Latn">hum-i</i>
			'the sound/feeling of —'. The same morphological category underlies the grammar's
			glossing abbreviation <span class="sc">poss</span>
			<Ref k="nakagawa2024" p="55–61" />; <Ref k="sato2008" />.
			See <Xr ch="concept-form-affiliative-form" /> and
			<Xr ch="affiliative-suffix-morphophonology" />.
		</dd>

		<dt id="agent"><strong>agent (A)</strong></dt>
		<dd>
			In the S/A/O notation used throughout this grammar, <em>A</em> designates the subject
			of a transitive verb — the argument that in a canonical action event controls the
			action and triggers the A-position person-index morpheme. Following
			<Ref k="foleyvanvalin1984" paren />. See <Xr ch="person-marking-architecture" />.
		</dd>

		<dt id="alignment"><strong>alignment (morphological)</strong></dt>
		<dd>
			The pattern by which person-marking morphology groups or distinguishes the three core
			argument roles S, A, and O. Hokkaido Ainu displays mixed alignment: the first
			singular is nominative–accusative (S=A≠O), the second and third persons are neutral
			(S=A=O), and the first-plural-exclusive and fourth-person rows are tripartite (S≠A≠O)
			‹contested›. <Ref k="bugaeva2012" p="472, 498" paren /> characterizes the overall
			system as basically tripartite; <Ref k="dalcorso2025align" paren /> argues for a
			mixed nominative–neutral–inverse account of the Sakhalin paradigm. See
			<Xr ch="person-marking-architecture" />, <Xr ch="alignment-split-intransitive" />,
			and <Xr ch="alignment-hierarchical-and-the-debate" />.
		</dd>

		<dt id="antipassive"><strong>antipassive</strong></dt>
		<dd>
			A valency-reducing operation that detransitivizes a transitive verb, suppressing or
			demoting the object; the verb then takes intransitive S-position morphology. In
			Hokkaido Ainu the prefix <A w="i-" gl="something/someone" /> (glossed
			<span class="sc">antip</span>) is the primary antipassive device. The prefix is
			homophonous with but distinct from the fourth-person object
			<i lang="ain-Latn">i=</i> <Ref k="bugaeva2021antip" />; <Ref k="nam2021" />.
			See <Xr ch="antipassive-detransitive-i" />.
		</dd>

		<dt id="applicative"><strong>applicative</strong></dt>
		<dd>
			A valency-increasing morphological operation that promotes an oblique argument
			(beneficiary, goal, instrument, comitative, or location) to direct object status,
			making it indexable on the verb. Hokkaido Ainu has three productive applicative
			prefixes: <A w="e-" gl="on/at/with" /> (spatial/instrumental),
			<A w="ko-" gl="to/for" /> (goal/benefactive), and
			<A w="o-" gl="in/among/from" /> (spatial immersion/motion-through)
			<Ref k="bugaeva2006" />; <Ref k="bugaeva2010" />; <Ref k="peterson2007" paren />.
			See <Xr ch="applicative-system-overview" />, <Xr ch="applicative-e" />,
			<Xr ch="applicative-ko" />, and <Xr ch="applicative-o" />.
		</dd>

		<dt id="aspect"><strong>aspect</strong></dt>
		<dd>
			Grammatical encoding of the internal temporal structure of an event — whether it is
			presented as a bounded whole (perfective) or as an ongoing process (imperfective).
			Hokkaido Ainu lacks an obligatory tense category (see <strong>tenselessness</strong>)
			but marks aspect periphrastically through auxiliary constructions: the anterior/perfective
			auxiliary <i lang="ain-Latn">a</i>, existential-frame aspectuals
			(<i lang="ain-Latn">kor an</i>, <i lang="ain-Latn">wa an</i>), and the mirative
			auxiliaries <i lang="ain-Latn">aan</i>/<i lang="ain-Latn">anan</i>
			<Ref k="comrie1976" paren />; <Ref k="takahashi2022" />.
			See <Xr ch="tam-overview" /> and <Xr ch="phasal-iterative-habitual-aspect" />.
		</dd>

		<dt id="bound-noun"><strong>bound noun / obligatorily possessed noun</strong></dt>
		<dd>
			A noun that cannot appear in its bare citation form without a possessor. Body-part,
			kinship, part-whole, and relational nouns form this class; they require a
			personal-prefix possessor or a nominal possessor in the affiliative form
			<Ref k="nakagawa2024" />; <Ref k="sato2008" />.
			See <Xr ch="nominal-subclasses-and-bound-nouns" />.
		</dd>

		<dt id="causative"><strong>causative</strong></dt>
		<dd>
			A valency-increasing operation that adds a causer argument to the structure of a base
			predicate. Hokkaido Ainu causative morphology centers on the suffix
			<i lang="ain-Latn">-re</i> (with dialectal allomorphs including
			<i lang="ain-Latn">-e</i> and <i lang="ain-Latn">-te</i>) on intransitive bases,
			with additional forms in Horobetsu
			<Ref k="nakagawa2024" />; <Ref k="sato2008" />.
			See <Xr ch="causative-morphological-re-e-te" />.
		</dd>

		<dt id="clitic"><strong>clitic</strong></dt>
		<dd>
			A morpheme that is phonologically dependent on a host word but syntactically freer
			than a canonical affix; written in this grammar with the boundary symbol
			<i lang="ain-Latn">=</i>. The personal-index morphemes of Hokkaido Ainu range from
			fuller prefixes (e.g. <i lang="ain-Latn">ku=</i>, <i lang="ain-Latn">en=</i>) to
			more clitic-like items (e.g. <i lang="ain-Latn">=an</i>,
			<i lang="ain-Latn">=as</i>); their precise morphological status is disputed
			‹contested› <Ref k="bugaeva2012" p="472–473" />; <Ref k="nakagawa2024" p="49, 183" />;
			<Ref k="sakaguchi2024" p="ix, 77–85" />.
			See <Xr ch="personal-affix-template-and-ordering" />.
		</dd>

		<dt id="clusivity"><strong>clusivity</strong></dt>
		<dd>
			The grammatical distinction within first-person plural between <em>inclusive</em>
			reference (speaker plus addressee) and <em>exclusive</em> reference (speaker and
			others, excluding the addressee). Hokkaido Ainu has a dedicated exclusive
			first-plural series (<i lang="ain-Latn">ci=</i>/<i lang="ain-Latn">=as</i>/
			<i lang="ain-Latn">un=</i>), while inclusive 'we' falls within the fourth-person
			forms <Ref k="nakagawa2024" p="163–164" />; <Ref k="bugaeva2012" p="472, 475" />.
			See <Xr ch="first-person-plural-and-clusivity" /> and
			<Xr ch="indefinite-fourth-person-paradigm" />.
		</dd>

		<dt id="concept-form"><strong>concept form</strong></dt>
		<dd>
			The citation or free shape of a noun, as distinct from its affiliative (possessed)
			shape (see <strong>affiliative form</strong>). Some stems show different shapes in
			these two environments: the evidential formal nouns appear in their concept form
			(<i lang="ain-Latn">ru</i>, <i lang="ain-Latn">sir</i>,
			<i lang="ain-Latn">haw</i>, <i lang="ain-Latn">hum</i>) in certain Tokachi
			constructions, and in their affiliative form
			(<i lang="ain-Latn">ru-we</i>, <i lang="ain-Latn">sir-i</i>, etc.) in the standard
			evidential frame <Ref k="nakagawa2024" p="55–62" />; <Ref k="takahashi2013" />.
			See <Xr ch="concept-form-affiliative-form" />.
		</dd>

		<dt id="copula"><strong>copula</strong></dt>
		<dd>
			A predicating element that links a subject to a non-verbal predicate (nominal,
			stative, or spatial). The Hokkaido Ainu copula <A w="ne" gl="be, be equal to" />
			predicates nominal and property complements; it also hosts the direct evidential
			constructions (<i lang="ain-Latn">ruwe ne</i>, <i lang="ain-Latn">siri ne</i>, etc.)
			and takes intransitive S-position person marking
			<Ref k="nakagawa2024" p="224–232" />; <Ref k="sato2008" />.
			See <Xr ch="copula-ne-predicate-nominals" />.
		</dd>

		<dt id="desiderative"><strong>desiderative</strong></dt>
		<dd>
			A modal category expressing the subject's desire or wish to perform or experience an
			action. The Hokkaido Ainu desiderative is formed with the auxiliary
			<A w="rusuy" gl="want to" />, which follows a nominalized or subordinated form of
			the predicate <Ref k="nakagawa2024" />.
			See <Xr ch="desiderative-rusuy" />.
		</dd>

		<dt id="ditransitive"><strong>ditransitive</strong></dt>
		<dd>
			A verb requiring three arguments: an agent-like subject, a recipient-like primary
			object, and a theme-like secondary object. Hokkaido Ainu caps person indexes at two
			per verb; with three-place predicates such as <A w="kore" gl="give" />, the object
			index targets the human recipient while the theme surfaces as an unindexed noun phrase
			<Ref k="sato2023b" />; <Ref k="nakagawa2024" p="363–365" />.
			See <Xr ch="valency-voice-overview" /> and <Xr ch="object-indexing-monotransitive" />.
		</dd>

		<dt id="evidential"><strong>evidential / evidentiality</strong></dt>
		<dd>
			A grammatical category encoding the speaker's source of information for the
			proposition expressed. Hokkaido Ainu marks evidentiality analytically: a finite clause
			is nominalized by a bound <em>formal noun</em> derived from a sensory noun, and the
			result is predicated with the copula or an existential verb. Four formal nouns build
			the core paradigm — <i lang="ain-Latn">ruwe</i> (inferential/direct knowledge),
			<i lang="ain-Latn">siri</i> (situational/visual), <i lang="ain-Latn">hawe</i>
			(reportative), <i lang="ain-Latn">humi</i> (sensory) — realizing the C1 type in the
			typology of <Ref k="aikhenvald2004" p="63–65" paren />
			<Ref k="dalcorso2018" />; <Ref k="nakagawa2024" p="258, 390–396" />.
			See <Xr ch="evidential-schema-overview" />.
		</dd>

		<dt id="existential-predicate"><strong>existential predicate</strong></dt>
		<dd>
			A predicate asserting the existence or presence of an entity or state of affairs.
			Hokkaido Ainu uses <A w="an" gl="exist, be (singular)" /> and
			<A w="oka" gl="exist (plural)" /> as the primary existential predicates; both also
			serve as host predicates in indirect evidential frames
			(<i lang="ain-Latn">siri an</i>, <i lang="ain-Latn">hawe as</i>) and in continuative
			aspectual periphrases <Ref k="nakagawa2024" />; <Ref k="sato2008" />.
			See <Xr ch="existential-locational-an-oka" />.
		</dd>

		<dt id="formal-noun"><strong>formal noun / defective noun</strong></dt>
		<dd>
			A member of the closed set of bound nouns that function as clause-nominalizing heads
			in evidential, complementation, and relative-clause constructions:
			<i lang="ain-Latn">ruwe</i>, <i lang="ain-Latn">siri</i>,
			<i lang="ain-Latn">hawe</i>, <i lang="ain-Latn">humi</i>,
			<i lang="ain-Latn">hi</i>, <i lang="ain-Latn">pe</i>/<i lang="ain-Latn">p</i>, and
			<i lang="ain-Latn">kur</i>. They require an adnominal modifier (the nominalized
			clause) and cannot take independent demonstratives or adjectival modifiers of their
			own, distinguishing them from ordinary lexical nouns
			<Ref k="nakagawa2024" p="385–396" />; <Ref k="sato2008" p="131–137" />.
			See <Xr ch="formal-defective-nouns" />.
		</dd>

		<dt id="fourth-person"><strong>fourth person (4)</strong></dt>
		<dd>
			The form-set — <i lang="ain-Latn">a=</i>/<i lang="ain-Latn">an=</i> (A),
			<i lang="ain-Latn">=an</i> (S), <i lang="ain-Latn">i=</i> (O) — that covers the
			referential ground outside persons 1–3. Its range includes inclusive first plural,
			indefinite/agent-defocusing, honorific second-person address, narrative first person,
			and logophoric reference inside quotations. Terminology is unsettled: Nakagawa labels
			the set 'fourth person', Tamura divides it into eleven separate persons, Bugaeva
			analyses the quotation use as logophoric, and Satō treats the forms as polysemous
			‹contested› <Ref k="nakagawa2024" p="164, 174–179" />;
			<Ref k="tamura1972" p="18–20" />; <Ref k="bugaeva2008" p="40–44" />;
			<Ref k="sato2008" p="203–208" />.
			See <Xr ch="indefinite-fourth-person-paradigm" /> and
			<Xr ch="fourth-person-honorific" />.
		</dd>

		<dt id="grammaticalization"><strong>grammaticalization</strong></dt>
		<dd>
			The diachronic process by which a lexical or less-grammatical item develops into a
			more grammatical one, typically with phonological reduction, increasing obligatoriness,
			and narrowed distribution. The Hokkaido Ainu evidential system illustrates partial
			grammaticalization: the sensory nouns <i lang="ain-Latn">ru</i>,
			<i lang="ain-Latn">sir</i>, <i lang="ain-Latn">haw</i>,
			<i lang="ain-Latn">hum</i> have moved toward clause-nominalizers and evidential heads
			while retaining traces of their lexical origin, with the degree of grammaticalization
			varying across Hokkaido dialects <Ref k="hoppertraugott2003" paren />;
			<Ref k="dalcorso2018" p="24–25" />; <Ref k="takahashi2018" />.
			See <Xr ch="evidential-schema-overview" /> and
			<Xr ch="internal-reconstruction-and-grammaticalization-pathways" />.
		</dd>

		<dt id="head-marking"><strong>head-marking</strong></dt>
		<dd>
			A typological property in which grammatical relations between a head and its
			dependents are indexed on the head rather than (or in addition to) on the dependents.
			Hokkaido Ainu is a canonical head-marking language: the verb bears person indexes for
			its subject and object, while nominal arguments carry no case suffixes
			<Ref k="nichols1986" paren />; <Ref k="sato2025a" p="361" />.
			See <Xr ch="person-marking-architecture" />.
		</dd>

		<dt id="inferential"><strong>inferential (evidential)</strong></dt>
		<dd>
			An evidential value signaling that the speaker's information comes from reasoning,
			trace evidence, or knowledge already processed and assimilated, rather than from
			direct perception or hearsay. The Hokkaido Ainu formal noun
			<i lang="ain-Latn">ruwe</i> (from <A w="ru" gl="track, path" />) heads the
			inferential/direct-knowledge evidential <i lang="ain-Latn">ruwe ne</i>, the most
			frequent and most grammaticalized member of the four-term system
			<Ref k="dalcorso2018" p="305, 327" />; <Ref k="nakagawa2024" p="392" />.
			See <Xr ch="ruwe-ne-inferential" /> and <Xr ch="evidential-schema-overview" />.
		</dd>

		<dt id="logophoric"><strong>logophoric reference</strong></dt>
		<dd>
			A form of pronominal reference that tracks the self-referent of a quoted or embedded
			speaker within reported speech, distinct from both ordinary third person and the
			matrix-clause first person. In Hokkaido Ainu the fourth-person prefix
			<i lang="ain-Latn">a=</i>/<i lang="ain-Latn">an=</i> and object
			<i lang="ain-Latn">i=</i> serve logophoric reference inside quoted speech,
			identifying the embedded speaker/object independently of the matrix participant set
			<Ref k="bugaeva2008" />; <Ref k="nikitina2021" />.
			See <Xr ch="logophoric-narration-and-reported-discourse" /> and
			<Xr ch="fourth-person-honorific" />.
		</dd>

		<dt id="mirativity"><strong>mirativity</strong></dt>
		<dd>
			A grammatical or pragmatic category encoding the speaker's surprise or the
			counter-expectational character of the event asserted. In Hokkaido Ainu, mirative
			meaning is contributed chiefly by the perfectve auxiliaries
			<i lang="ain-Latn">aan</i> and <i lang="ain-Latn">anan</i>; the evidential
			<i lang="ain-Latn">ruwe ne</i> may co-occur with these forms but does not itself
			lexicalize surprise ‹contested› <Ref k="dalcorso2018" p="336" />;
			<Ref k="hirosawa2026" p="90" />.
			See <Xr ch="mirativity-scope-grammaticalization" />.
		</dd>

		<dt id="nonconfigurationality"><strong>nonconfigurationality / pronominal-argument hypothesis</strong></dt>
		<dd>
			Two related analytical positions on languages with relatively free constituent order
			and systematic argument omission. <em>Nonconfigurationality</em> holds that such
			languages lack the structural positions that configure subjects and objects in
			languages like English. The <em>pronominal-argument hypothesis</em> proposes that the
			obligatory person indexes on the verb are the true syntactic arguments, with overt
			nominal phrases functioning as discourse-level adjuncts rather than core arguments.
			Whether either analysis applies to Hokkaido Ainu is debated ‹contested›.
			See <Xr ch="nonconfigurationality-pronominal-argument" /> and
			<Xr ch="constituent-order-head-final" />.
		</dd>

		<dt id="nominalization"><strong>nominalization</strong></dt>
		<dd>
			The morphological or syntactic conversion of a verb or predication into a nominal
			constituent. Hokkaido Ainu nominalizes extensively: the suffixes
			<i lang="ain-Latn">-p</i>/<i lang="ain-Latn">-pe</i> (thing/patient nominalizer) and
			<i lang="ain-Latn">-i</i>/<i lang="ain-Latn">-hi</i> (fact/manner nominalizer) form
			derived nouns; formal-noun heads (<i lang="ain-Latn">ruwe</i>,
			<i lang="ain-Latn">hi</i>, <i lang="ain-Latn">pe</i>) nominalize entire clauses in
			evidential, complementation, and relative-clause constructions
			<Ref k="nakagawa2024" p="383–396" />; <Ref k="sato2008" p="119–140" />.
			See <Xr ch="nominalization-p-pe" /> and
			<Xr ch="lexical-head-formal-noun-nominalization" />.
		</dd>

		<dt id="noun-incorporation"><strong>noun incorporation</strong></dt>
		<dd>
			A morphological process in which a noun — typically representing an object, oblique,
			or subject participant — is compounded into the verb stem, forming a single
			polymorphemic verbal word. Hokkaido Ainu is one of the typologically well-documented
			cases, with object, oblique, and subject incorporation all attested; incorporated
			nouns interact with applicative morphology and relativization
			<Ref k="mithun1984" paren />; <Ref k="shibatani1988" />; <Ref k="satoni2022" />.
			See <Xr ch="noun-incorporation-object" />,
			<Xr ch="noun-incorporation-subject-possessor-stranding" />, and
			<Xr ch="noun-incorporation-oblique-polysynthesis-debate" />.
		</dd>

		<dt id="number"><strong>number (verbal and nominal)</strong></dt>
		<dd>
			The grammatical category encoding the count of individuals in a referent set. Hokkaido
			Ainu verb number is marked through suppletive stem alternations
			(e.g. <i lang="ain-Latn">ek</i>/<i lang="ain-Latn">arki</i> 'come') and through the
			suffix <i lang="ain-Latn">-pa</i>. Nominal number is <em>transnumeral</em>: see the
			entry for <strong>transnumerality</strong>
			<Ref k="corbett2000" paren />; <Ref k="nakagawa2024" p="141–159" />.
			See <Xr ch="suppletive-verbal-number" /> and
			<Xr ch="nominal-number-utar-transnumerality" />.
		</dd>

		<dt id="object"><strong>object (O)</strong></dt>
		<dd>
			In the S/A/O notation, <em>O</em> designates the patient-like argument of a transitive
			verb — the argument indexed by the object-position morphemes
			(e.g. <i lang="ain-Latn">en=</i>, <i lang="ain-Latn">i=</i>,
			<i lang="ain-Latn">un=</i>). Following <Ref k="foleyvanvalin1984" paren />.
			See <Xr ch="person-marking-architecture" /> and
			<Xr ch="object-indexing-monotransitive" />.
		</dd>

		<dt id="oblique"><strong>oblique</strong></dt>
		<dd>
			An argument or adjunct that is not indexed on the verb as a core subject or object —
			typically a locative, source, goal, instrument, or comitative participant expressed
			by a postpositional phrase. The applicative prefixes promote obliques to indexable
			object status (see <strong>applicative</strong>). See
			<Xr ch="postpositions-local-case-and-motion-events" /> and
			<Xr ch="applicative-system-overview" />.
		</dd>

		<dt id="perfective"><strong>perfective</strong></dt>
		<dd>
			An aspect value that presents an event as a completed, bounded whole, typically
			locating it in the speaker's anterior domain. The Hokkaido Ainu anterior/perfective
			auxiliary <i lang="ain-Latn">a</i> attaches after the verb (and after evidential
			frames) and contributes both boundedness and anteriority; it is the source of
			mirative readings in certain constructions
			<Ref k="nakagawa2024" p="289–295" />; <Ref k="takahashi2022" />.
			See <Xr ch="perfective-anterior-a" /> and <Xr ch="tam-overview" />.
		</dd>

		<dt id="person"><strong>person (grammatical)</strong></dt>
		<dd>
			A deictic category encoding the role of the referent in the speech act: first
			(speaker), second (addressee), third (other), and fourth (the residual set covering
			inclusive, indefinite, honorific, and logophoric). Hokkaido Ainu formally distinguishes
			four persons, each with singular and plural, through proclitic and enclitic indexes on
			the verb <Ref k="nakagawa2024" p="163–179" />; <Ref k="tamura1972" />.
			See <Xr ch="person-marking-architecture" />.
		</dd>

		<dt id="pitch-accent"><strong>pitch accent</strong></dt>
		<dd>
			A prosodic system in which a single high-pitch position (or a H→L fall) within the
			prosodic word is linguistically distinctive, as opposed to a full lexical-tone system
			(where every syllable carries a tonal specification) or a simple stress system.
			Hokkaido Ainu has a pitch-accent system with largely predictable high-pitch placement
			governed by syllable weight and position; the language also divides into accented and
			accentless dialect classes <Ref k="shibatani1990" p="52–58" />;
			<Ref k="nakagawa2024" p="33–51" />. See <Xr ch="pitch-accent-placement-rule" /> and
			<Xr ch="mora-syllable-accent-vs-tone-analysis" />.
		</dd>

		<dt id="polysynthesis"><strong>polysynthesis</strong></dt>
		<dd>
			A typological property of languages in which words — particularly verbs — routinely
			incorporate multiple participant-tracking and relational morphemes, producing complex
			words that translate as full clauses in analytic languages. The extent to which
			Hokkaido Ainu counts as polysynthetic in the technical sense, versus merely
			morphologically complex, is debated in the noun-incorporation literature ‹contested›
			<Ref k="shibatani1988" />; <Ref k="mithun1984" paren />.
			See <Xr ch="noun-incorporation-oblique-polysynthesis-debate" />.
		</dd>

		<dt id="portmanteau"><strong>portmanteau</strong></dt>
		<dd>
			A single morpheme that simultaneously realizes two or more grammatical values that
			would otherwise require separate morphemes. In Hokkaido Ainu,
			<i lang="ain-Latn">eci=</i> is the portmanteau for first-person A acting on a
			second-person O (covering 1SG→2SG, 1SG→2PL, 1PL.EXCL→2SG, and 1PL.EXCL→2PL),
			replacing the separate first- and second-person indexes that other combinations use
			<Ref k="tamura1972" p="27–28" />; <Ref k="nakagawa2024" p="170, 173" />.
			See <Xr ch="eci-portmanteau-and-second-plural" />.
		</dd>

		<dt id="possession"><strong>possession / possessive</strong></dt>
		<dd>
			The grammatical encoding of a part-whole, kinship, or owner–property relation between
			two nominals. Hokkaido Ainu marks possession on the possessed noun via the affiliative
			suffix and/or a coreferential personal-prefix possessor; certain nouns are
			obligatorily possessed (see <strong>bound noun</strong>)
			<Ref k="nakagawa2024" p="55–77" />; <Ref k="sato2008" />.
			See <Xr ch="affiliative-suffix-morphophonology" /> and
			<Xr ch="adnominal-possession-double-marking" />.
		</dd>

		<dt id="reciprocal"><strong>reciprocal</strong></dt>
		<dd>
			An argument-structure operation marking mutual action: each member of a plural set
			simultaneously acts on and is acted upon by the others. Hokkaido Ainu uses the prefix
			<A w="u-" gl="reciprocal prefix" /> for this function
			<Ref k="nakagawa2024" p="204–209" />; <Ref k="sato2008" />.
			See <Xr ch="reciprocal-u" />.
		</dd>

		<dt id="reflexive"><strong>reflexive</strong></dt>
		<dd>
			An argument-structure operation in which the subject and object of a transitive verb
			are co-referential — the agent acts on itself or on something within its own domain.
			Hokkaido Ainu employs two reflexive prefixes: <A w="yay-" gl="reflexive prefix" />
			(general action reflexive) and <A w="si-" gl="reflexive/possessive prefix" />
			(possessive/inner-domain reflexive), with partly overlapping but distinct distributions
			<Ref k="sato2007" />; <Ref k="kobayashi2010" />.
			See <Xr ch="reflexive-yay" /> and <Xr ch="reflexive-possessive-si" />.
		</dd>

		<dt id="relative-clause"><strong>relative clause</strong></dt>
		<dd>
			A nominally modifying clause that restricts or characterizes the head noun. Hokkaido
			Ainu uses a prenominal gap strategy: the relative clause precedes the head noun and
			contains a positional gap rather than a resumptive pronoun; the verb retains its full
			person marking inside the clause. The strategy covers subjects and objects with equal
			ease, placing Ainu high on the Keenan–Comrie accessibility hierarchy
			<Ref k="keenancomrie1977" paren />; <Ref k="nakagawa2024" p="370–383" />.
			See <Xr ch="prenominal-gap-relative-clauses" /> and
			<Xr ch="headless-internally-headed-noun-modifying-clauses" />.
		</dd>

		<dt id="reportative"><strong>reportative (evidential)</strong></dt>
		<dd>
			An evidential value signaling that the speaker's information derives from verbal
			report or hearsay rather than personal observation or inference. The Hokkaido Ainu
			reportative formal noun is <i lang="ain-Latn">hawe</i> (from
			<A w="haw" gl="voice" />); the construction <i lang="ain-Latn">hawe ne</i> predicates
			the clause content as something the speaker has heard reported
			<Ref k="dalcorso2018" />; <Ref k="nakagawa2024" p="393–394" />.
			See <Xr ch="hawe-ne-reportative" />.
		</dd>

		<dt id="s-a-o"><strong>S, A, O (argument roles)</strong></dt>
		<dd>
			The three core argument roles tracked by morphological alignment systems in this
			grammar. <em>S</em> is the sole argument of an intransitive verb; <em>A</em> is the
			agent-like argument of a transitive verb; <em>O</em> is the patient-like argument of a
			transitive verb. The notation follows <Ref k="foleyvanvalin1984" paren /> and the
			Leipzig Glossing Rules. See <Xr ch="person-marking-architecture" />.
		</dd>

		<dt id="sensory-evidential"><strong>sensory evidential</strong></dt>
		<dd>
			An evidential encoding information received through non-visual sensation: sound, touch,
			smell, or an internal bodily feeling. The Hokkaido Ainu sensory formal noun is
			<i lang="ain-Latn">humi</i> (from <A w="hum" gl="sound, feeling" />); the
			construction <i lang="ain-Latn">humi ne</i> marks non-visual direct evidentiality
			<Ref k="dalcorso2018" />; <Ref k="nakagawa2024" p="394" />.
			See <Xr ch="humi-ne-sensory" />.
		</dd>

		<dt id="situational-evidential"><strong>situational evidential</strong></dt>
		<dd>
			An evidential encoding information derived from direct visual observation of an
			accessible, ongoing situation. The Hokkaido Ainu situational formal noun is
			<i lang="ain-Latn">siri</i> (from <A w="sir" gl="appearance, land" />); the
			construction <i lang="ain-Latn">siri ne</i> marks direct visual evidentiality.
			In Tokachi the evidential is further restricted to stage-level (transient)
			predications, with individual-level properties taking the inferential form instead
			<Ref k="takahashi2013" />; <Ref k="dalcorso2018" />.
			See <Xr ch="siri-ne-situational" />.
		</dd>

		<dt id="split-intransitivity"><strong>split intransitivity</strong></dt>
		<dd>
			A typological pattern in which intransitive subjects divide into two sub-classes —
			agentive (<em>S<sub>A</sub></em>) and patientive (<em>S<sub>O</sub></em>) — each
			aligned differently with the transitive argument roles. In Hokkaido Ainu the
			person-agreement paradigm does not itself split the intransitive subject by agentivity;
			the relevant split surfaces in verb classes, noun incorporation, and valency
			alternations <Ref k="nakagawa2024" p="126–127" />; <Ref k="stassen1997" paren />.
			See <Xr ch="alignment-split-intransitive" />.
		</dd>

		<dt id="switch-reference"><strong>switch-reference</strong></dt>
		<dd>
			A clause-linking mechanism that marks whether the subject of a subordinate clause is
			the same participant (SS) or a different participant (DS) from the subject of an
			adjacent or matrix clause. In Hokkaido Ainu the clause-linkers
			<A w="hine" gl="conjunctive/sequential particle" /> and
			<A w="akusu" gl="consequential/switch particle" /> encode a related opposition;
			whether this constitutes switch-reference in the strict typological sense is debated
			‹contested› <Ref k="nakagawa2024" p="425–440" />; <Ref k="sato2008" p="247–256" />.
			See <Xr ch="hine-akusu-switch-reference" />.
		</dd>

		<dt id="tam"><strong>TAM (tense–aspect–mood)</strong></dt>
		<dd>
			The cluster of grammatical categories encoding temporal location (tense), internal
			event structure (aspect), and speaker stance toward the event (mood/modality).
			Hokkaido Ainu lacks an obligatory tense but marks aspect and modality periphrastically
			through auxiliary and particle constructions
			<Ref k="comrie1976" paren />; <Ref k="nakagawa2024" p="284–320" />.
			See <Xr ch="tam-overview" />.
		</dd>

		<dt id="telicity"><strong>telicity</strong></dt>
		<dd>
			A lexical-semantic property distinguishing predicates that include a natural endpoint
			(telic: an activity with a bounded goal, e.g. 'write a letter') from those that do
			not (atelic: unbounded activities, e.g. 'run'). Telicity interacts with the
			distribution of aspect auxiliaries and with some valency alternations in Hokkaido Ainu
			<Ref k="comrie1976" paren />; <Ref k="takahashi2022" />.
			See <Xr ch="tam-overview" />.
		</dd>

		<dt id="tenselessness"><strong>tenselessness</strong></dt>
		<dd>
			The absence of an obligatory grammatical tense category — no morpheme or construction
			that compulsorily locates an event as past, present, or future relative to speech
			time. Hokkaido Ainu expresses temporal reference through adverbs, aspect auxiliaries,
			and context, rather than through a dedicated tense paradigm
			<Ref k="nakagawa2024" />; <Ref k="sato2008" />.
			See <Xr ch="tenselessness-and-time-reference" />.
		</dd>

		<dt id="transnumerality"><strong>transnumerality</strong></dt>
		<dd>
			A property of nominal number systems in which the unmarked noun form is unspecified
			for the singular/plural distinction, able to refer to one or more individuals without
			grammatical marking of that contrast. Hokkaido Ainu nouns are transnumeral; the only
			dedicated nominal plural element is the associative
			<A w="utar" gl="group, people, companions" />, which marks a social collective rather
			than a pure plurality <Ref k="corbett2000" paren />;
			<Ref k="nakagawa2024" p="78–82" />.
			See <Xr ch="nominal-number-utar-transnumerality" />.
		</dd>

		<dt id="tripartite-alignment"><strong>tripartite alignment</strong></dt>
		<dd>
			A morphological alignment pattern in which all three core argument roles — S, A, and O
			— are marked by formally distinct, non-overlapping morphological exponents. Two rows of
			the Hokkaido Ainu person paradigm display this pattern: the first-plural-exclusive
			(<i lang="ain-Latn">ci=</i>/<i lang="ain-Latn">=as</i>/
			<i lang="ain-Latn">un=</i>) and the fourth person
			(<i lang="ain-Latn">a=</i>/<i lang="ain-Latn">=an</i>/
			<i lang="ain-Latn">i=</i>) <Ref k="bugaeva2012" p="472" />;
			<Ref k="nakagawa2024" p="163–174" />.
			See <Xr ch="person-marking-architecture" /> and
			<Xr ch="alignment-hierarchical-and-the-debate" />.
		</dd>

		<dt id="valency"><strong>valency</strong></dt>
		<dd>
			The number and kind of arguments a predicate requires or licenses. Intransitive
			predicates have valency 1 (one S argument), transitives have valency 2 (A + O), and
			ditransitives have valency 3. Hokkaido Ainu has a rich set of valency-changing
			operations: causatives and applicatives increase valency; antipassives and
			middle/anticausatives decrease it; noun incorporation can further reduce the
			effective valency of the verbal complex <Ref k="bugaevakobayashi2022" />;
			<Ref k="nakagawa2024" p="196–228" />.
			See <Xr ch="valency-voice-overview" />.
		</dd>

		<dt id="verb-template"><strong>verb template</strong></dt>
		<dd>
			The fixed hierarchical ordering of morphological positions within the Hokkaido Ainu
			verb word. From outermost to innermost: personal-index prefixes (A/O) → applicative,
			causative, antipassive, reflexive, and reciprocal prefixes → the stem → stem-forming
			plural suffix (<i lang="ain-Latn">-pa</i>) → personal-index suffixes
			(<i lang="ain-Latn">=an</i>, <i lang="ain-Latn">=as</i> for intransitive S)
			<Ref k="nakagawa2024" p="183, 196–199" />; <Ref k="tamura1972" p="35" />.
			See <Xr ch="verb-word-template" /> and
			<Xr ch="personal-affix-template-and-ordering" />.
		</dd>
	</dl>
