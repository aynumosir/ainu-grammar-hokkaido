<S t="Inclusive and impersonal reference" id="inclusive-impersonal">
	<p>
		The inclusive value fills the gap left by the first plural exclusive. The set
		<i lang="ain-Latn">ci=</i> / <i lang="ain-Latn">=as</i> / <i lang="ain-Latn">un=</i>
		excludes the addressee; addressee-inclusive 'we' therefore requires the fourth-person row
		<Ref k="nakagawa2024" p="163–164, 174" />; <Ref k="tamura1972" p="18–19" />; <Ref
			k="bugaeva2012"
			p="472, 475"
		/>. Tokachi preserves the same clusivity split, and Takahashi observes that the
		inclusive/fourth-person series triggers plural verb forms even where the referent would
		otherwise be singular <Ref k="takahashi2015" p="206–209" />.
	</p>

	<p>
		In its impersonal or generic value, <i lang="ain-Latn">a=</i> indexes an unspecified human
		agent on a transitive predicate. The patient remains in the O role; the verb retains
		transitive valence; an explicit agent-like participant may appear in a source phrase headed by
		<i lang="ain-Latn">or wa</i> 'from'. Japanese translations routinely render the construction
		with a passive, but Ainu has no subject-promoting passive morphology: the agent does not
		receive subject marking, and the patient is not promoted to a nominative argument <Ref
			k="tamura1972"
			p="34"
		/>; <Ref k="sato2008" p="203–205" />; <Ref k="nakagawa2024" p="176–177" />.
	</p>

	<Ex
		m="wen kamuy a=maketa wa a=rayke ru-we ne"
		g="bad god 4.A=defeat CONJ 4.A=kill track-POSS COP"
		tr="The evil kamuy was defeated and killed."
		cite="nakagawa2024:174"
		dial="CHI"
		place="Nakagawa ex. 145"
		note="The patient wen kamuy stays in the object role across two transitive predicates; a= supplies an unspecified human agent."
	/>

	<Ex
		m="kamuy or wa a=epunkine wa kamuy or wa a=kasuy wa"
		g="god place from 4.A=protect CONJ god place from 4.A=help CONJ"
		tr="being protected by the gods and helped by the gods, …"
		cite="nakagawa2024:177"
		dial="CHI"
		place="Nakagawa ex. 146"
		note="The source phrase kamuy or wa names the agent-like participant; the verbs carry fourth-person A and the patient is not promoted to grammatical subject."
	/>

	<p>
		Ijäs's learner grammar illustrates the same generic-human value, glossing
		<i lang="ain-Latn">a=nu</i> as 'people hear (it)' in a lesson on perceptible sounds <Ref
			k="ijas2023"
		/>. Her notation uses a hyphen where this chapter writes <i lang="ain-Latn">=</i>; that
		reflects a pedagogical convention carrying no claim about clitic or affix status.
	</p>

	<Ex
		m="maw a=nu"
		g="breeze 4.A=hear"
		tr="One hears the breeze."
		cite="ijas2023"
		constructed
		dial="HK"
		note="Constructed from Ijäs Lesson 7, where a=nu illustrates the generic-human A value; maw 'breeze' fills the object slot."
	/>

	<p>
		The generic-agent prefix combines with the nominalizer
		<i lang="ain-Latn">p</i> ~ <i lang="ain-Latn">pe</i> to yield participant nominalizations of
		the type <i lang="ain-Latn">a=e-p</i> 'food' (literally 'what people eat'). The following
		example shows <i lang="ain-Latn">a=e p</i> in a headless relative construction:
	</p>

	<Ex
		m="tapan kina a=e p ne toan kina somo a=e p ne"
		g="this plant 4.A=eat NMLZ COP that plant NEG 4.A=eat NMLZ COP"
		tr="This plant is one that people eat; that plant is one that people do not eat."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker folktale 14 [aa-irc/020#27]"
		note="The head p is the thing eaten; a= supplies the unspecified human eater."
	/>

	<p>
		The morphology and arity diagnostics of <i lang="ain-Latn">p</i> ~
		<i lang="ain-Latn">pe</i> are developed in <Xr ch="nominalization-p-pe" />.
	</p>
</S>

<S t="The fourth-person object i= and the detransitive prefix i-" id="object-i">
	<p>
		The object index <i lang="ain-Latn">i=</i> belongs to the same paradigmatic row as
		<i lang="ain-Latn">a=</i> and <i lang="ain-Latn">=an</i>. In narrative and address it indexes
		the fourth-person argument as patient, recipient, or undergoer — the narrator's 'me' or the
		honored person as object <Ref k="nakagawa2024" p="169" />.
	</p>

	<Ex
		m="i=erampokiwen i=rewsire wa i=kore"
		g="4.O=pity 4.O=lodge CONJ 4.O=give"
		tr="Take pity on me and give me lodging."
		cite="nakagawa2024:169"
		dial="SAR"
		place="Nakagawa ex. 127"
		note="The repeated i= is fourth-person O, indexing the narrator/protagonist as object and recipient across three transitive predicates."
	/>

	<Ex
		m="a=rehe Ikuresuye sekor a=i=reko"
		g="4.POSS=name Ikuresuye QUOT 4.A=4.O=name"
		tr="I was named Ikuresuye."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker folktale 7 [aa-irc/005#1]"
		note="The naming formula combines fourth-person possessive a= on rehe 'name', fourth-person A on the predicate, and fourth-person O i= for the narrator/protagonist."
	/>

	<p>
		The prefix <i lang="ain-Latn">i=</i> is phonologically identical to the detransitivising
		prefix <i lang="ain-Latn">i-</i> 'something/someone', which incorporates a generic object and
		reduces the verb's transitivity. The two are notated differently — <i lang="ain-Latn">=</i>
		for the person index, <i lang="ain-Latn">-</i> for the derivational prefix — and they differ
		syntactically: the person index occupies the O slot of an already-transitive stem, while the
		antipassive derives an intransitive stem from a transitive base. Tamura already keeps the two
		apart on functional grounds; their diachronic relationship is disputed. Older accounts derive
		the detransitive prefix from the fourth-person object; Bugaeva argues in the opposite
		direction, connecting the antipassive to O-incorporation of a generic noun; Nam proposes a
		generic-noun source <i lang="ain-Latn">*hi/*ihi</i> 'thing, place' for the detransitive
		prefix directly <Ref k="tamura1972" p="35" />; <Ref k="bugaeva2021antip" p="235–239" />; <Ref
			k="nam2021"
			p="62–74"
		/> ‹contested›. The detransitive prefix and its morphology are treated in
		<Xr ch="antipassive-detransitive-i" />.
	</p>
</S>

<S t="Honorific, narrative, and logophoric uses" id="honorific-narrative">
	<p>
		Honorific use applies the fourth-person row to an esteemed addressee. Nakagawa describes
		women addressing adult men and humans addressing kamuy in prayer; Tamura documents the same
		politeness value for Saru; Satō groups it with the literary-person system of oral performance
		<Ref k="nakagawa2024" p="175–176" />; <Ref k="tamura1972" p="28" />; <Ref
			k="sato2008"
			p="259–262"
		/>. The verb takes a plural stem even for a single referent, a number agreement conditioned
		by register rather than by the count of persons.
	</p>

	<Ex
		m="hunak wa arki=an ru-we an"
		g="where from come.PL=4.S track-POSS exist"
		tr="Where have you come from?"
		cite="nakagawa2024:175"
		dial="CHI"
		place="Nakagawa ex. 140, woman addressing an adult man"
		note="A single addressee is indexed by =an on the plural motion stem arki; the plural stem reflects honorific register."
	/>

	<p>
		In oral narrative the protagonist's first person is expressed by the same row. In Saru and
		Chitose a singular narrator may appear with singular verb stems and the pronoun
		<i lang="ain-Latn">asinuma</i>; Horobetsu and Shizunai display opposite mismatches between
		verb number and the available pronouns <Ref k="nakagawa2024" p="174–179, 254–256" />. Nakagawa
		treats the narrative use as the indefinite person applied to an unanchored referent; Satō
		argues the literary self-reference derives from a first-person strategy and cannot be
		collapsed into the indefinite; Bugaeva, finding comparable uses in non-folklore Asahikawa
		texts, removes the dependence on an elegant register entirely <Ref k="sato2004" p="179–184"
		/>; <Ref k="sato2008" p="259–262" />; <Ref k="bugaeva2008" p="37–44" /> ‹contested›.
	</p>

	<Ex
		m="ratcitara hosipi=an yakka a=uni un tunasno hosipi=an pe ne kusu"
		g="slowly return=4.S although 4.POSS=home to quickly return=4.S NMLZ COP because"
		tr="Although I returned slowly, because I was able to return quickly to my home, …"
		cite="nakagawa2024:165"
		dial="SAR"
		place="Nakagawa ex. 109"
		note="A singular narrator uses =an on both intransitive predicates and the possessive prefix a= on uni 'home'; the singular stem hosipi confirms singular-narrator morphology in Saru."
	/>

	<p>
		The fifth use — the quoted speaker's self-reference inside a reported-speech clause — is
		analysed by Bugaeva as logophoric: the fourth-person row tracks the discourse-source of the
		embedded proposition, functioning as a participant-tracking device independent of literary
		genre <Ref k="bugaeva2008" p="37–44" />; <Ref k="tamura1972" p="19–20, 22–23" />; <Ref
			k="nakagawa2024"
			p="179"
		/>. The social and discourse dimensions of the honorific and logophoric values are developed
		in <Xr ch="fourth-person-honorific" /> and <Xr ch="logophoric-narration-and-reported-discourse" />.
	</p>
</S>

<S t="Terminology and comparative note" id="terminology">
	<p>
		The competing labels encode different analytical choices. Nakagawa's "fourth person" positions
		the row as a formal residual class within a four-person paradigm, gathering everything that
		persons one to three cannot express <Ref k="nakagawa2024" p="163–164, 174" />. Tamura's Saru
		analysis distributes the same forms across the inclusive, two quotative persons, the honorific
		second, and the indefinite, treating the labels as functionally distinct values rather than a
		single form-class <Ref k="tamura1972" p="18–20, 28, 34, 39" />. Bugaeva's "indefinite (IND)"
		foregrounds the generic and inclusive core; the quotation use is relabelled logophoric, a
		move supported by its occurrence outside the narrative register <Ref k="bugaeva2012"
			p="474–476"
		/>; <Ref k="bugaeva2008" p="40–43" />. Takahashi's "inclusive person" gives the Tokachi
		system a unified participant label, arguing that the inclusive function is primary <Ref
			k="takahashi2015"
			p="205–210"
		/> ‹contested›.
	</p>

	<p>
		Dal Corso applies the label "fourth person" to the cognate Sakhalin AN-series by word form,
		while his diachronic reanalysis — which rejects the tripartite component for Sakhalin and
		treats the cognate system as mixed nominative–neutral–inverse — is explicitly cautioned as
		inapplicable to Hokkaido <Ref k="dalcorso2025align" p="187–188, 200, 213" />; <Ref
			k="sakaguchi2024"
			p="26–27, 162–163"
		/> ‹contested›. For Hokkaido, the tripartite characterization of
		<i lang="ain-Latn">a=</i> / <i lang="ain-Latn">=an</i> / <i lang="ain-Latn">i=</i> —
		formalized in Bugaeva's mixed-alignment account and adopted in the architecture chapter —
		remains the standard analysis <Ref k="bugaeva2012" p="472" />.
	</p>
</S>
