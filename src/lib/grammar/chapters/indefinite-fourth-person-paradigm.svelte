<S t="The formal paradigm" id="formal-set">
	<p>
		Three verbal indexes make up the Hokkaido Ainu form-set called the <em>fourth person</em> by
		Nakagawa, the <em>indefinite</em> by Bugaeva, and the <em>inclusive person</em> by Takahashi:
		<i lang="ain-Latn">a=</i> or dialectally <i lang="ain-Latn">an=</i> as the subject of a
		transitive verb (A role), <i lang="ain-Latn">=an</i> as the subject of an intransitive verb
		(S), and <i lang="ain-Latn">i=</i> as the object (O). The full S/A/O architecture of all four
		persons is laid out in <Xr ch="person-marking-architecture" />; this chapter covers the
		internal paradigm and the referential values licensed by the row.
	</p>

	<table>
		<thead>
			<tr>
				<th>role</th>
				<th>Hokkaido form (Saru/Chitose baseline)</th>
				<th>position</th>
				<th>principal values</th>
			</tr>
		</thead>
		<tbody>
			<tr>
				<td>A: subject of a transitive verb</td>
				<td><i lang="ain-Latn">a=</i>, dialectally <i lang="ain-Latn">an=</i></td>
				<td>prefix, before the transitive stem</td>
				<td>
					inclusive or hortative agent; generic/impersonal human agent; honorific addressee;
					narrative protagonist; possessor in fourth-person nominal forms
				</td>
			</tr>
			<tr>
				<td>S: subject of an intransitive verb</td>
				<td><i lang="ain-Latn">=an</i></td>
				<td>suffix, after the intransitive stem</td>
				<td>
					inclusive or hortative subject; honorific addressee; narrative or logophoric subject
				</td>
			</tr>
			<tr>
				<td>O: object of a transitive verb</td>
				<td><i lang="ain-Latn">i=</i></td>
				<td>prefix, before the transitive stem</td>
				<td>
					fourth-person patient, recipient, or undergoer; narrator's 'me/us'; honored or inclusive
					object
				</td>
			</tr>
		</tbody>
	</table>

	<p>
		The row is parallel in structure to the first plural exclusive
		<i lang="ain-Latn">ci=</i> / <i lang="ain-Latn">=as</i> / <i lang="ain-Latn">un=</i>: both
		keep distinct forms for all three roles. The S/A distinction is expressed by the side of
		attachment — a transitivity-conditioned position switch absent from the first singular and the
		second person. That asymmetry is the morphological basis on which Bugaeva calls these two rows
		tripartite <Ref k="bugaeva2012" p="471–472" />; <Ref k="bugaeva2008" p="32" />; <Ref
			k="nakagawa2024"
			p="165–169, 174"
		/>.
	</p>

	<Ex
		m="eani hem paye=an ro"
		g="you also go.PL=4.S HORT"
		tr="Let you and me go too!"
		cite="nakagawa2024:174"
		dial="CHI"
		place="Nakagawa ex. 138"
		note="The addressee-inclusive hortative uses the plural stem paye with =an following as S."
	/>
</S>

<S t="Side-encoding and allomorphy" id="side-encoding">
	<p>
		The subject index selects its position by transitivity. With an intransitive predicate, the
		fourth-person subject appears as the suffix <i lang="ain-Latn">=an</i>; with a transitive
		predicate, the same referent is indexed by the prefix <i lang="ain-Latn">a=</i>. The
		following sentence contains both on adjacent predicates of different transitivity <Ref
			k="nakagawa2024"
			p="166–167"
		/>; <Ref k="bugaeva2012" p="471–472" />.
	</p>

	<Ex
		m="rus patek a=mi wa hotke=an pa"
		g="fur only 4.A=wear CONJ sleep=4.S PL"
		tr="We wore only fur and slept."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker folktale 7 [aa-irc/005#157]"
		note="The transitive mi 'wear' takes the prefix a=; the intransitive hotke 'sleep' takes the suffix =an. The plural auxiliary pa follows outside the person index."
	/>

	<p>
		In Saru and the dialects to the west, <i lang="ain-Latn">a=</i> is the regular
		transitive-subject form. Elsewhere the longer allomorph <i lang="ain-Latn">an=</i> appears in
		varying distributions: Tokachi uses <i lang="ain-Latn">an=</i> before vowel-initial stems
		without first-syllable accent and <i lang="ain-Latn">a=</i> elsewhere; Ishikari and Samani
		favour <i lang="ain-Latn">an=</i>; Shiranuka mostly has <i lang="ain-Latn">an=</i> with
		<i lang="ain-Latn">a=</i> chiefly before <A w="ye" gl="say" />; Shizunai shows no such
		split. Even Saru, Chitose, and Horobetsu preserve <i lang="ain-Latn">an=</i> in verse
		formulae, and Nakagawa regards <i lang="ain-Latn">an=</i> as historically prior <Ref
			k="nakagawa2024"
			p="167–168"
		/> ‹speculative›. The Hokkaido-wide quantitative distribution of the two allomorphs is
		incompletely documented ‹corpus-suggested›.
	</p>

	<p>
		The clitic-or-affix status of <i lang="ain-Latn">=an</i> and <i lang="ain-Latn">a=</i> is
		contested: Bugaeva treats <i lang="ain-Latn">=an</i> as word-like and
		<i lang="ain-Latn">a=</i> as clitic-like on the basis of prosodic independence and
		separability; Nakagawa writes all person indexes with <i lang="ain-Latn">=</i> as a
		convenient notation and separately documents the high autonomy of <i lang="ain-Latn">=an</i>;
		Sakaguchi's formal clitic criterion — developed on Sakhalin evidence — converges on the same
		autonomy ranking for the fourth-person S form <Ref k="bugaeva2012" p="472–473" />; <Ref
			k="nakagawa2024"
			p="49, 183"
		/>; <Ref k="sakaguchi2024" p="ix, 62–64, 77–85, 163" /> ‹contested›. Ordering and wordhood
		diagnostics are treated in <Xr ch="personal-affix-template-and-ordering" />.
	</p>

	<p>
		The plural auxiliary <i lang="ain-Latn">pa</i> of Saru, Chitose, and Ishikari positions
		itself outside the person index (stem<i lang="ain-Latn">=an pa</i>), while the stem-forming
		plural suffix <i lang="ain-Latn">-pa</i> sits inside (stem<i lang="ain-Latn">-pa=an</i>).
		Nakagawa's minimal pair from Saru makes the contrast explicit <Ref
			k="nakagawa2024"
			p="158"
		/>; <Ref k="sakaguchi2024" p="12" />:
	</p>

	<Ex
		m="nenpoka iki=an pa wa siknu=an wa"
		g="somehow do=4.S PL CONJ survive=4.S CONJ"
		tr="Somehow we managed and survived, and …"
		cite="nakagawa2024:158"
		dial="SAR"
		place="Nakagawa ex. 87"
		note="The plural auxiliary pa follows iki=an, external to the fourth-person S suffix =an."
	/>

	<Ex
		m="irwak sinne sorekusu iki-pa=an"
		g="sibling like because do-PL=4.S"
		tr="because we were like siblings …"
		cite="nakagawa2024:158"
		dial="SAR"
		place="Nakagawa ex. 88"
		note="The stem-forming suffix -pa precedes =an; the ordering is reversed relative to the auxiliary pa in the preceding example."
	/>
</S>

<S t="Referential range: five uses" id="functions">
	<p>
		Nakagawa lists five referential values for the same formal row — addressee-inclusive first
		plural, honorific second person, indefinite or impersonal human agent, the narrator or
		protagonist of oral narrative, and the quoted speaker inside reported speech — stressing that
		no single meaning covers all five <Ref k="nakagawa2024" p="174–179" />. Tamura distributes
		the same Saru forms across several distinct persons in her eleven-person system; Bugaeva uses
		the cover label "indefinite" and reanalyses the quotation use as logophoric; Satō treats the
		forms as polysemous; Takahashi's Tokachi account adopts "inclusive person", arguing that the
		inclusive value is primary and the other uses follow from it <Ref k="tamura1972"
			p="18–20, 28, 34, 39"
		/>; <Ref k="bugaeva2012" p="474–476" />; <Ref k="bugaeva2008" p="40–43" />; <Ref
			k="sato2008"
			p="203–208, 259–262"
		/>; <Ref k="takahashi2015" p="205–210" /> ‹contested›.
	</p>

