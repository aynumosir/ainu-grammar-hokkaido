<S t="Orthographic and typological implications" id="orthographic-typological">
	<p>
		For Hokkaido, the orthographic consequences of the debuccalization asymmetry are minimal:
		HA retains original coda consonants on the surface, and the citation forms write exactly
		what is heard — <i lang="ain-Latn">tek</i>, <i lang="ain-Latn">sik</i>,
		<i lang="ain-Latn">cup</i>, <i lang="ain-Latn">mat</i>. The apparent
		<i lang="ain-Latn">h</i> that appears medially in affiliative forms is always the
		onset of the suffix <i lang="ain-Latn">-hi</i> and is morphologically transparent.
	</p>
	<p>
		For Sakhalin orthography the picture is more consequential. Writing the citation form as
		<i lang="ain-Latn">mah</i>, <i lang="ain-Latn">cuh</i>, <i lang="ain-Latn">teh</i>
		follows the phonetic surface but conceals the base consonant that the affiliative restores
		and the Hokkaido cognate corroborates. Thegodofneet (2025, <Ref k="aynucorporadiscord" />)
		proposed writing the underlying <i lang="ain-Latn">p/t/k</i> values directly and reading
		them as <i lang="ain-Latn">h</i> by convention — a strategy with analogues in French and
		Korean orthographic traditions, which preserve historically silent consonants as relics
		of earlier forms. Aldehyde8908 (2025, <Ref k="aynucorporadiscord" />) countered that some
		‹SA› subdialects genuinely surface with <i lang="ain-Latn">p/t/k</i> (Taraika,
		Tarantomari), so writing the surface form is the accurate representation of those dialects,
		while the uniform <i lang="ain-Latn">h</i> convention would misrepresent them ‹disputed›.
		The dispute reflects the genuine ‹SA›-internal variation noted above and has no consensus
		resolution in the read sources. Ijäs <Ref k="ijas2023" /> addresses the SA–HA affiliative
		alternation in pedagogical terms, making the base-consonant restoration accessible to
		learners encountering ‹SA› citation forms for the first time.
	</p>
	<p>
		Typologically, the ‹SA› debuccalization belongs to a widespread class of coda-weakening
		processes in which place distinctions are neutralised toward a laryngeal default. The
		resulting system — a single coda <i lang="ain-Latn">h</i> for historically distinct
		<i lang="ain-Latn">p, t, k</i> — would be irrecoverable from ‹SA› alone; the Hokkaido
		cognates and the ‹SA› affiliative together supply the two independent verification routes
		that Alonso de la Fuente <Ref k="alonso2022" p="§1" /> calls a showcase of internal
		reconstruction for an isolate. The genetic position of Ainu — an isolate for which no
		comparative method in the strict sense applies — makes this type of internal, morphologically
		mediated evidence especially load-bearing; for the macro-comparative context see
		<Xr ch="genetic-position-macro-comparison" />.
	</p>
</S>
