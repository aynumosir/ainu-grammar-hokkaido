	<p>
		The <i lang="ain-Latn">mat / mah / mac-ihi</i> triplet is the strongest case for internal
		reconstruction via the affiliative. The ‹SA›RA surface form <i lang="ain-Latn">mah</i> would
		predict an affiliative *<i lang="ain-Latn">mah-ihi</i>, but the attested form is
		<i lang="ain-Latn">mac-ihi</i>, with an affricate <i lang="ain-Latn">c</i> absent from the
		citation form of both dialects <Ref k="alonso2022" p="§1" />;
		<Ref k="tangiku2022" p="§2.3, §5" />. The affricate arises from the regular
		<i lang="ain-Latn">t → c</i> alternation before front <i lang="ain-Latn">i</i> that applies
		at the morpheme boundary when the ‹SA› uniform affiliative suffix <i lang="ain-Latn">-ihi</i>
		is appended: the underlying <i lang="ain-Latn">*t</i> surfaces as
		<i lang="ain-Latn">c</i> before the connecting <i lang="ain-Latn">-i-</i>. The affiliative
		therefore functions as a transparent window onto the base consonant that debuccalization
		hides on the citation form. In HA, where the affiliative connecting vowel matches the base's
		own final vowel (<i lang="ain-Latn">a</i> for <i lang="ain-Latn">mat</i>), the same
		alternation does not arise: HA <i lang="ain-Latn">mat-aha</i> keeps
		<i lang="ain-Latn">t</i>.
	</p>
	<p>
		The practical consequence — noted by community member nukopoli (2023,
		<Ref k="aynucorporadiscord" />) ‹corpus-confirmed› — is that ‹SA› concept-form
		etymology is often unrecoverable from the citation form alone: a surface
		<i lang="ain-Latn">-h</i> coda could descend from any of
		<i lang="ain-Latn">p, t, k</i>, or (via the secondary rule) any front-vowel-final stem whose
		coda lenited to <i lang="ain-Latn">h</i> then shifted to <i lang="ain-Latn">s</i>. The HA
		cognate or the ‹SA› affiliative — often both — supply the disambiguation.
	</p>
	<p>
		The phonemic status of ‹SA› coda <i lang="ain-Latn">h</i> is itself debated.
		Alonso de la Fuente <Ref k="alonso2022" p="§1" /> transcribes it as a velar fricative /x/,
		reflecting its realization as [ax], [iɕ], [uf] conditioned by the preceding vowel. Tangiku
		<Ref k="tangiku2022" p="§2.3" /> and community member thegodofneet (2025,
		<Ref k="aynucorporadiscord" />) treat it as a glottal or pharyngeal breath [h], citing the
		<Ref k="hattori1964" /> dialect dictionary's description that only breath, not modal voicing,
		exits, and raising the further question of whether the coda segment and the word-initial
		onset <i lang="ain-Latn">h</i> are the same phoneme ‹contested›. The two transcriptions
		have no consequence for the diachronic correspondences laid out here, since both agree on
		the underlying source consonants; but they diverge in the phonological analysis of
		contemporary ‹SA› Ainu and its segmental inventory. The broader consonant inventory
		discussion is in <Xr ch="consonant-inventory" />.
	</p>
	<p>
		‹SA›-internal variation further limits how far back the debuccalization can be projected.
		The Taraika (east coast) and Tarantomari dialects preserve
		<i lang="ain-Latn">p / t / k</i> in coda position where the main ‹SA› dialects have
		<i lang="ain-Latn">h</i>, and Esutoru distinguishes <i lang="ain-Latn">-is</i> from
		<i lang="ain-Latn">-ih</i> <Ref k="tangiku2022" p="§2.3" />. Debuccalization thus
		developed within the Sakhalin branch rather than in the common ancestor, and the HA forms
		represent the conservative retention of whatever coda consonants Proto-Ainu had.
	</p>
</S>

<S t="The Sakhalin vowel-length reflex and proto-Ainu prosody" id="length-reflex-prosody">
	<p>
		Alongside the consonant correspondences, Hokkaido and Sakhalin diverge in their prosodic
		systems. Sakhalin Ainu carries phonemic vowel length in open stem syllables
		(<i lang="ain-Latn">a aa e ee i ii o oo u uu</i>); Hokkaido Ainu has a pitch accent and no
		contrastive length <Ref k="tangiku2022" p="§2.1" />; <Ref k="alonso2022" p="§2.3" />.
		Hattori (1967, via <Ref k="alonso2022" p="§2.3" />; <Ref k="tangiku2022" p="§2.1" />)
		first stated the systematic correspondence: a ‹SA› long vowel in an open stem syllable aligns
		with a HA high-pitch syllable in the same position. A closed-syllable constraint qualifies
		the generalization: where the stem syllable carries a coda consonant (retained in HA,
		debuccalized to <i lang="ain-Latn">h</i> in ‹SA›), the ‹SA› vowel is short while HA
		retains the high pitch on that syllable <Ref k="alonso2022" p="§2.3" />.
	</p>

