<S t="Debuccalization in Sakhalin and the affiliative evidence" id="debuccalization-affiliative">
	<p>
		In Sakhalin Ainu a systematic coda-weakening rule applies: stop and fricative codas
		debuccalize to <i lang="ain-Latn">h</i> (<i lang="ain-Latn">p, t, k → h</i>), nasal
		<i lang="ain-Latn">m</i> merges with <i lang="ain-Latn">n</i>, and coda
		<i lang="ain-Latn">r</i> is followed by a vowel that opens the syllable
		<Ref k="tangiku2022" p="§2.3" />; <Ref k="alonso2022" p="§1" />. A secondary rule then
		shifts the resulting coda <i lang="ain-Latn">h</i> to <i lang="ain-Latn">s</i> after a front
		vowel: HA <i lang="ain-Latn">sik</i> → <i lang="ain-Latn">*sih</i> →
		<i lang="ain-Latn">sis</i> (‹SA›RA). Community members nukopoli and aldehyde8908
		independently documented these alternations in the aynu-corpora Discord —
		<i lang="ain-Latn">kor → koh</i> (nukopoli, 2024) and the
		<i lang="ain-Latn">sik / sis</i> split (aldehyde8908, 2025) <Ref k="aynucorporadiscord" />
		— both fully corroborated by <Ref k="tangiku2022" p="§2.3" /> ‹corpus-confirmed›.
	</p>

