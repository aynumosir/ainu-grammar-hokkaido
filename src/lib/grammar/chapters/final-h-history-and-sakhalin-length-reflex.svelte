<S t="The h phoneme and the coda question in Hokkaido" id="h-coda-question">
	<p>
		Hokkaido Ainu <i lang="ain-Latn">h</i> is an onset segment. It occurs word-initially —
		<i lang="ain-Latn">hapo</i> 'mother', <i lang="ain-Latn">huci</i> 'grandmother' — and as the
		opening consonant of the <Xr ch="affiliative-suffix-morphophonology">affiliative suffix</Xr>
		<i lang="ain-Latn">-hi/-he/-hu/-ha</i>. It does not occur as a syllable coda
		<Ref k="tamura1996" />. The descriptive gap between the two scripts — Hokkaido retaining
		original coda consonants, Sakhalin replacing them with
		<i lang="ain-Latn">h</i> — makes the phoneme appear to play an outsized role in cross-dialect
		description, yet for Hokkaido the question is straightforward: the
		<i lang="ain-Latn">h</i> visible inside forms such as <i lang="ain-Latn">tek-ehe</i> 'hand
		(of someone)' or <i lang="ain-Latn">sik-ihi</i> 'eye (of someone)' is suffix-initial, not
		base-final. The base consonants — <i lang="ain-Latn">k</i> in <i lang="ain-Latn">tek</i>,
		<i lang="ain-Latn">k</i> in <i lang="ain-Latn">sik</i> — surface unchanged across all
		Hokkaido morphological environments ‹consensus›.
	</p>
	<p>
		The analysis that makes this transparent is Janhunen's (2020, via
		<Ref k="alonso2022" p="§2.1" />) account of the affiliative
		<i lang="ain-Latn">-VhV</i>. The proto-Ainu noun base was disyllabic —
		<i lang="ain-Latn">*tek.e</i> 'hand', <i lang="ain-Latn">*sik.i</i> 'eye' — with a final
		vowel subsequently lost in the citation form. When the noun
		<i lang="ain-Latn">hi</i> 'thing, place, time' fused with the base, the lost base-vowel was
		restored before the suffix-initial <i lang="ain-Latn">h</i>: <i lang="ain-Latn">*tek.e</i>
		→ <i lang="ain-Latn">tek-ehe</i>, <i lang="ain-Latn">*sik.i</i>
		→ <i lang="ain-Latn">sik-ihi</i>. The <i lang="ain-Latn">h</i> in the affiliative is
		therefore the onset of <i lang="ain-Latn">hi</i>, and the suffix vowel harmonises with the
		base's final vowel: <i lang="ain-Latn">-ehe</i> (base <i lang="ain-Latn">e</i>),
		<i lang="ain-Latn">-ihi</i> (base <i lang="ain-Latn">i</i>),
		<i lang="ain-Latn">-uhu</i> (base <i lang="ain-Latn">u</i>),
		<i lang="ain-Latn">-aha</i> (base <i lang="ain-Latn">a</i>). The full morphology belongs in
		<Xr ch="concept-form-affiliative-form" /> and
		<Xr ch="affiliative-suffix-morphophonology" />; the point here is that HA morphology
		provides no surface evidence for an underlying word-final /h/ on noun stems ‹consensus›.
	</p>
	<p>
		A separate <i lang="ain-Latn">h</i>-related phenomenon in Hokkaido — the prothetic
		<i lang="ain-Latn">h</i>- of the Bihoro dialect (<i lang="ain-Latn">haspa</i> 'deaf' :
		<i lang="ain-Latn">aspa</i>; <i lang="ain-Latn">ham</i> 'nail' :
		<i lang="ain-Latn">am</i>) — has been analysed as an independent Bihoro innovation driven
		by disambiguation of initial-stressed forms, not as an archaism or an echo of any coda laryngeal
		<Ref k="alonso2022" p="§2.2.5" paren />. It is discussed in the broader consonant inventory
		in <Xr ch="consonant-inventory" />.
	</p>

