	<p>
		The <i lang="ain-Latn">súpki / suhki</i> pair illustrates the closed-syllable constraint
		directly. The compound <i lang="ain-Latn">*sup+ki</i> 'reed' keeps a closed first syllable
		regardless of dialect: in HA, <i lang="ain-Latn">p</i> closes the syllable and the pitch
		falls on <i lang="ain-Latn">súp-</i>; in ‹SA›, <i lang="ain-Latn">p → h</i> closes the
		syllable differently but the syllable remains closed, so the vowel is short. The resulting
		‹SA› form <i lang="ain-Latn">suhki</i> shows a short vowel in the first syllable and a coda
		<i lang="ain-Latn">h</i>, while HA <i lang="ain-Latn">súpki</i> shows high pitch and the
		original coda <i lang="ain-Latn">p</i>.
	</p>
	<p>
		The diachronic interpretation of the length–pitch correspondence is the most contested point
		in proto-Ainu suprasegmental reconstruction. Three positions are current in the literature,
		surveyed comprehensively in <Ref k="alonso2022" p="§2.3" /> and addressed in detail in
		<Ref k="itabashi2001" />:
	</p>
	<ul>
		<li>
			<strong>(a) Length-first (Hattori 1967; De Boer 2010, both via
				<Ref k="alonso2022" p="§2.3" />)</strong>: Proto-Ainu had contrastive vowel length and
			no pitch accent; Hokkaido acquired its pitch system through prolonged contact with
			Japanese, while Sakhalin preserved the older length contrast.
		</li>
		<li>
			<strong>(b) Both length and pitch (Vovin 1993, via <Ref k="alonso2022" p="§2.3" />;
				<Ref k="itabashi2001" />)</strong>: Proto-Ainu carried both phonemic length and pitch
			simultaneously; the Sakhalin branch lost pitch under Amur–Sakhalin contact pressure,
			retaining length; Hokkaido retained pitch and neutralised length.
		</li>
		<li>
			<strong>(c) Pitch-first (Satō 2015–2016, via <Ref k="alonso2022" p="§2.3" />)</strong>:
			Proto-Ainu had pitch accent only; ‹SA› length is a phonetically conditioned phenomenon
			restricted to open syllables and represents a Sakhalin-internal development — possibly
			compensatory lengthening following coda weakening — rather than an inherited contrast.
		</li>
	</ul>
	<p>
		Alonso de la Fuente's own treatment is internally divided: §2.3 endorses view (c) as "most
		economical", while §2.4 states that "Hattori was right when he suggested Proto-Ainu had only
		vowel length, and no pitch accent; pitch accent may be the result of language contact after
		Japanese influence" — a restatement of view (a)
		<Ref k="alonso2022" p="§2.3, §2.4" /> ‹contested›. The two sections advocate opposite
		positions without reconciliation; neither can be taken as Alonso's settled conclusion.
		Itabashi <Ref k="itabashi2001" /> provides the dedicated historical treatment of the
		SA-length ↔ HA-pitch diachronic relation; its arguments should be weighed before any
		definitive account of PA prosody is committed to, and the fuller treatment belongs in
		<Xr ch="mora-syllable-accent-vs-tone-analysis" />.
	</p>
	<p>
		A trochaic-to-iambic shift hypothesis (Satō 2015–2016; Shiraishi 2017, both via
		<Ref k="alonso2022" p="§2.3" />) adds a further layer: if Proto-Ainu or Common Ainu was
		trochaic and the pitch accent spread rightward, the resulting iambic system would leave
		certain archaisms — ‹SA› <i lang="ain-Latn">noociw</i> : HA
		<i lang="ain-Latn">nocíw</i>, ‹SA› <i lang="ain-Latn">niisew</i> : HA
		<i lang="ain-Latn">niséw</i> (length ↔ low pitch in HA) — as the recoverable fingerprints
		of the older system. Community member nukopoli (2023, <Ref k="aynucorporadiscord" />) noted
		the Hattori/Satō dispute directly, identifying the competing claims from within the
		community's reading of the literature ‹contested›. Antitwilight (2024,
		<Ref k="aynucorporadiscord" />) takes a position against Satō's posited `*H` segment and
		prefers Janhunen's vowel-dropping account (see also §1 on the affiliative), arguing that
		introducing an unattested consonant for the purpose of accounting for exceptional accent is
		less economical than assuming base-vowel loss ‹contested›.
	</p>
	<p>
		The pitch-accent system of Hokkaido, including the placement rules and the dialect-internal
		variation among standard-accent, irregular, and unaccented dialects, is treated in full in
		<Xr ch="pitch-accent-placement-rule" /> and <Xr ch="mora-syllable-accent-vs-tone-analysis" />.
