	/>
</S>

<S t="Sub-genre types and typical themes" id="sub-genres">
	<p>
		Endō identifies four sub-categories within the <i lang="ain-Latn">uwepeker</i> heading
		<Ref k="endo2022" />:
	</p>

	<ol>
		<li>
			<strong>Human prose tales</strong> (the default type). The protagonist is an unnamed
			human — "the chief of ___", "a poor young man" — and the narration is in the first
			person with a retrospective identification at the close. The most pervasive narrative
			shape is the impoverished-hero tale: a young man in reduced circumstances acquires
			power or a spouse through courage, divine favour, or correct conduct; the arc broadly
			rewards good behaviour and punishes bad.
		</li>
		<li>
			<strong>Kamuy prose tales</strong> (<i lang="ain-Latn">kamuy uwepeker</i>). A deity
			narrates in the first person, aligning these tales with the world of divine epics
			despite their prose format; the language may reach into the elevated register
			<Ref k="endo2022" />.
		</li>
		<li>
			<strong>Japanese-borrowed tales</strong>. Adapted Japanese narratives told in the
			third person — "In a certain place there lived a young lord…" — with an Ainu-inflected
			worldview.
		</li>
		<li>
			<strong>Pananpe–Penanpe tales</strong>. A dyadic genre built around the archetypal
			pair <A w="pananpe" gl="downstream-dweller" /> ('downstream-dweller',
			<i lang="ain-Latn">pana-n-pe</i>) and
			<A w="penanpe" gl="upstream-dweller" /> ('upstream-dweller',
			<i lang="ain-Latn">pena-n-pe</i>). Variant forms
			<i lang="ain-Latn">panakuru</i> / <i lang="ain-Latn">penakuru</i>, with
			<i lang="ain-Latn">kur</i> 'person' rather than <i lang="ain-Latn">pe</i> 'thing,
			person', also occur. These tales are narrated in the third person, open with a
			formulaic pair-announcement, and are regarded as a children's genre
			<Ref k="endo2022" />.
		</li>
	</ol>

	<p>
		The Pananpe–Penanpe opening is among the most stable formulae in the repertoire. Both
		characters are introduced with the existential verb
		<A w="an" gl="exist, be" /> in succession, joined by a conjunctive clause. The inferential
		evidential <i lang="ain-Latn">ruwe ne</i> predicated over the whole opening grounds the
		state of affairs as established narrative fact (on this function see
		<Xr ch="ruwe-ne-inferential" />):
	</p>

	<Ex
		m="pananpe an penanpe an hine sir-an pe ne ru-we ne hike"
		g="Pananpe exist Penanpe exist CONJ appearance-exist thing COP track-POSS COP although"
		tr="There were Pananpe and Penanpe, and…"
		cite="hirosawa2026:75"
		dial="SAR"
		place="Wateke (田村すゞ子 audio materials 05-02, 'Folktale 1': 4)"
		note="Standard Pananpe–Penanpe opening. The third-person existential frame (no fourth-person a= prefix) contrasts with the first-person fourth-person narration of human prose tales. Hirosawa cites the underlying Tamura audio materials; ruwe ne predicates the simultaneous existence of the two characters as established narrative fact."
	/>
</S>

<S t="Opening and closing formulae" id="formulae">
	<p>
		Human prose tales open with a scene-setting passage establishing the protagonist's
		household and livelihood: an enumeration of family members, the father's hunting
		activities, the household's prosperity or poverty. This is a learned formula, not an
		improvised description, and it recurs across narrators and dialect areas in broadly
		stable shape <Ref k="endo2022" />.
	</p>
	<p>
		The closing formula works in the opposite direction. The narrator, who has spoken
		throughout as the protagonist "I", reveals the protagonist's identity at the tale's end
		through a retrospective disclosure. The pattern takes the shape of a quotative wrap:
		the protagonist's name or description appears inside a
		<A w="sekor" gl="quotative: saying, thus" /> frame, which is closed by a verb of
		telling — typically <A w="isoytak" gl="narrate a tale" /> — with explicit person
		marking. The formula <i lang="ain-Latn">… sekor … isoytak</i> ('…so [so-and-so]
		told the story') simultaneously attributes the performance to its narrator and
		discloses the fourth-person protagonist's identity
		<Ref k="endo2022" />.
		The grammar of <i lang="ain-Latn">sekor</i> and the interaction of the closing frame
		with the logophoric fourth person are treated in
		<Xr ch="quotative-complementation-sekor" /> and
		<Xr ch="logophoric-narration-and-reported-discourse" />.
	</p>
	<Ex
		m="a=ne ruwe ne sekor amameponcikappo isoytak"
		g="4=COP track-POSS COP QUOT little.sparrow tell.tale.PFV"
		tr="[…and so] I was [one of them] — thus Little Sparrow told the tale."
		cite="ilcaa1976"
		dial="SAR"
		place="川上まつ子 (Kawakami Matsuko), uwepeker '民話15' (ILCAA Ainu materials 022:109)"
		note="Canonical closing formula. The fourth-person copula a=ne (narrator-as-protagonist 'I am') is wrapped in the quotative sekor, and the verb of narration isoytak then names the fourth-person protagonist amameponcikappo ('Little Sparrow'), disclosing the identity of the first-person narrator. ruwe ne predicates the whole disclosure as established fact."
	/>
</S>

<S t="Person of narration: the fourth-person narrator-protagonist" id="fourth-person-narrator">
	<p>
		The default prose tale is narrated in the first person from the protagonist's viewpoint,
		but the person marking on verbs is the <em>fourth person</em> —
		<i lang="ain-Latn">a=</i> (transitive subject and possessor) and
		<i lang="ain-Latn">=an</i> (intransitive subject) — not the everyday first-person
		singular <i lang="ain-Latn">ku=</i>. This fourth-person-as-narrative-'I' convention
		holds across all three major Saru/Chitose narrative genres and is the defining feature
		of Ainu oral narrative person-marking ‹consensus›
		<Ref k="sato2008" p="260–261" />; <Ref k="endo2022" />; <Ref k="sato2004" />.
	</p>

	<Ex
		m="a=poho a=kore"
		g="4.POSS=child 4.A=give"
		tr="I gave my child away."
		cite="ilcaa1976"
		dial="SAR"
		place="川上まつ子 (Kawakamimatsuko), ILCAA Ainu materials (AA研アイヌ語資料)"
		note="Fragment from a human prose tale. The fourth-person prefix a= marks both the possessive relation ('my child') and the transitive subject ('I gave'). Everyday first-person narration would require ku= for both roles; the prose-tale convention substitutes a= throughout."
	/>

	<p>
		Within the prose tale, the same fourth-person forms also serve as a
		<em>deferential honorific</em> when the narrator-protagonist refers to a revered figure —
		an elder chief, a deity encountered in the story world, or a powerful adversary. Here
		<i lang="ain-Latn">a=</i> encodes not narrative self-reference but respectful distance
		<Ref k="sato2008" p="261" />; <Ref k="bugaeva2008" p="40–43" />.
		Both functions, the logophoric and the honorific, are realised by the same set of
		forms and are distinguished by context. The full paradigm and its referential range
		are set out in <Xr ch="indefinite-fourth-person-paradigm" /> and
		<Xr ch="fourth-person-honorific" />; the narrator-as-protagonist analysis and its
		relation to logophoricity are treated in
		<Xr ch="logophoric-narration-and-reported-discourse" />.
	</p>
	<p>
		Why <i lang="ain-Latn">a=</i> rather than <i lang="ain-Latn">ku=</i>? Two competing
		accounts circulate in the literature ‹contested›. The standard explanation traces the
		convention to a "shamaness-oracle" origin: the narrative voice is a possessing spirit
		speaking through the teller, making everyday first-person marking inappropriate. Satō
		argues instead for an <em>indirection principle</em>
		(<span lang="ja">間接性の原理</span>): the genre originated as a god's telling inside
		a dream addressed to a living human, and since direct god-to-human address is taboo,
		the non-everyday person marking adds the necessary indirectness
		<Ref k="sato2008" p="259–261" />.
		Bugaeva and Nikitina reanalyse the narrator-protagonist using the framework of
		logophoricity: the "I" of the tale is a logophoric controller, the narration is
		direct-style throughout, and the fourth person is the Ainu mechanism for maintaining
		logophoric reference across the embedded narrative
		<Ref k="bugaeva2008" p="40–43" />; <Ref k="nikitina2021" />.
		Whether Ainu oral narrative therefore qualifies as genuine "first-person literature"
		(<span lang="ja">一人称体</span>) in the typological sense remains an open question
		<Ref k="sato2004" />; <Ref k="endo2022" /> ‹contested›.
		The person-marking architecture that makes all these functions possible is surveyed in
		<Xr ch="person-marking-architecture" />.
	</p>
	<p>
		One feature distinguishes prose-tale person-marking from that of the sung genres: the
		<em>number-agreement profile</em>. In the elevated register of heroic and divine epics,
		<i lang="ain-Latn">=an</i> appears freely with a syntactically singular verb
		(<i lang="ain-Latn">ek-an</i> 'I come', singular verb stem), overriding the everyday
		restriction that bars <i lang="ain-Latn">=an</i> from singular stems in Saru/Chitose.
		Prose <i lang="ain-Latn">uwepeker</i> does not generally exploit this override; its
		number agreement follows the everyday pattern ‹corpus-suggested›
		<Ref k="sato2008" p="263" />.
	</p>
</S>

<S t="The quotative frame and participant tracking" id="quotative-frame">
	<p>
		Because the prose tale maintains direct-voice narration throughout, and because embedded
		dialogue is a constant feature of the genre, the quotative complementizer
		<i lang="ain-Latn">sekor</i> is dense. Speech and thought reports are framed with
		<i lang="ain-Latn">sekor</i> followed by one of a small set of reporting verbs:
		<i lang="ain-Latn">ye</i> 'say', <i lang="ain-Latn">hawki</i> 'recite/say', or
		<i lang="ain-Latn">isoytak</i> 'narrate'. The same frame reappears at the close of
		the whole performance in the retrospective identification formula discussed above
		<Ref k="endo2022" />.
		The construction grammar of <i lang="ain-Latn">sekor</i>, its position with respect
		to tense and aspect, and the distinction between direct and indirect-style reporting
		in prose narrative are treated in <Xr ch="quotative-complementation-sekor" />.
	</p>
	<p>
		Participant tracking across extended prose narration — keeping the listener oriented
		among multiple characters when several embedded speakers each use "I" — relies on the
		fourth-person / first-person contrast in conjunction with the quotative frame. The
		narrator-protagonist's embedded direct speech remains under the logophoric fourth
		person; secondary characters may receive everyday person marking. The reference-tracking
		apparatus is treated in <Xr ch="reference-tracking" />.
	</p>
	<p>
		The TAM and evidential profile of prose tales — the predominance of
		<i lang="ain-Latn">ruwe ne</i> as narrative-frame predicate, the aspectual distribution,
		and the sequencing role of conjunctive <i lang="ain-Latn">hine</i> and
		<i lang="ain-Latn">akusu</i> — is the topic of
		<Xr ch="narrative-tam-evidential-patterning-by-genre" />.
	</p>
</S>

<S t="Register: the everyday frame and its limits" id="register">
	<p>
		Human prose tales operate in <A w="yayan-itak" gl="everyday language" />
		(<span lang="ja">口語・日常語</span>), the ordinary register, in contrast to the
		elevated poetic language (<A w="atomte-itak" gl="elegant/decorated language" />,
		<span lang="ja">雅語</span>) of heroic and divine epics
		<Ref k="sato2008" p="259" />; <Ref k="endo2022" />.
		Prose narration therefore avoids the hallmarks of the elevated register: the applicative
		case-marking prefixes (<i lang="ain-Latn">e-</i>, <i lang="ain-Latn">o-</i>,
		<i lang="ain-Latn">ko-</i>) in place of spatial particles, the polysynthetic formulaic
		periphrases, and the elegant kin-term vocabulary
		(<i lang="ain-Latn">ona</i> 'father', <i lang="ain-Latn">unu</i> 'mother' as
		literary-register forms, against colloquial dialect variants)
		<Ref k="endo2022" />
		(nukopoli, <Ref k="aynucorporadiscord" /> 2024-06-15).
		The elevated register and its structural diagnostics are surveyed in
		<Xr ch="poetic-archaic-elevated-register" />.
	</p>
	<p>
		The boundary is not absolute. Kamuy prose tales (<i lang="ain-Latn">kamuy uwepeker</i>),
		in which a deity narrates, may draw on the elevated register, aligning their language
		with that of divine epics despite their prose format
		<Ref k="endo2022" />.
		Narrators with a broad stylistic range may also import elevated vocabulary or formulae
		into human prose tales, and the register of any given performance reflects the
		individual narrator's competence and choices. The elegant/everyday register boundary
		is a continuum keyed to verse-vs-prose and improvisation-vs-fixedness rather than a
		clean binary ‹contested›
		<Ref k="sato2008" p="259" />.
	</p>
	<p>
		The prosodic correlate of the everyday register is a characteristic intonation pattern.
		Tamura notes that <i lang="ain-Latn">uwepeker</i> narration is unidirectional — the
		teller speaks to an audience rather than seeking a response — and the voice falls to a
		flat, low-variation melodic contour across sentences, in contrast to the rising contour
		of conversational speech where the speaker solicits a reaction
		<Ref k="tamura1996" />;
		the community discussion in the aynu-corpora Discord (nukopoli, 2024-11-16) confirms
		this datum from Tamura's account
		<Ref k="aynucorporadiscord" />.
		Within the Saru basin, upper-Saru (mid-river) narrators tend toward a monotone delivery
		while lower-Saru (downriver) narrators show more melodic undulation; this microvariation
		does not affect the structural grammar of the genre.
	</p>
</S>
