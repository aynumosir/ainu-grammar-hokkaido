	<p>
		The sakehe refrain and metre of the sung genres are treated in
		<Xr ch="sakehe-refrain-and-sung-verse-structure" />.
		An additional prose sub-form, <A w="irupaye" gl="prose rendering of a yukar" />, carries
		heroic-epic content in prose delivery, enabling women narrators to perform yukar material
		without contravening the convention that verse <i lang="ain-Latn">yukar</i> belongs to
		men <Ref k="sato2008" p="259" />. The gendered allocation of genres is discussed in
		<Xr ch="gendered-register-speech" />.
	</p>
	<p>
		Speakers themselves characterise the prose tale as serious, factual narration rather than
		fiction. Osuka Rieko of Shiraoi insists on the genre's truth-status, contrasting it with
		fabricated talk:
	</p>
