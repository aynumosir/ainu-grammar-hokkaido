<S t="The prose tale in the genre system" id="genre-placement">
	<p>
		Hokkaido Ainu oral narrative divides into three major genres: the heroic epic, the divine
		epic, and the prose folktale. The prose folktale is designated
		<A w="uwepeker" gl="prose folktale" /> in the Saru and Chitose dialects of southwestern
		Hokkaido and across much of southern Hidaka; the eastern dialects — Ishikari, Teshio,
		Kitami, Kushiro, Tokachi, and eastern Hidaka east of Shizunai — use
		<i lang="ain-Latn">tuytak</i> for the same genre. The two terms are geographically
		distributed, not functionally distinct ‹consensus›
		<Ref k="endo2022" />; <Ref k="sato2008" p="259" />.
		(Confusingly, <i lang="ain-Latn">tuytak</i> designates the divine epic in Samani; the
		term's referent is therefore dialect-specific — see <Xr ch="oral-literature-genre-taxonomy" />.)
	</p>
	<p>
		What distinguishes the prose tale from the two sung genres is the complete absence of
		three formal features: a fixed melody, a cyclically recurring refrain
		(<A w="sakehe" gl="refrain" />), and the rhythm-stick (<A w="repni" gl="rhythm-stick" />)
		that performers and listeners use to keep time during heroic-epic recitation. The narrator
		speaks in an ordinary, near-conversational voice. The following table summarises the main
		parameters across the three major narrative genres, following
		<Ref k="sato2008" p="259" paren /> and <Ref k="endo2022" paren />:
	</p>

