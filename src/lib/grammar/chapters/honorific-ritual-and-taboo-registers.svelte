		The everyday second-person pronouns <i lang="ain-Latn">eani</i> and
		<i lang="ain-Latn">ecioka(y)</i> share morphological origin with the second-person verbal
		affixes. The honorific alternatives <i lang="ain-Latn">asinuma</i> and
		<i lang="ain-Latn">aoka(y)</i> are drawn from the fourth-person paradigm and function as
		free pronouns in deferential speech — between a junior and a senior speaker, or between a
		human worshipper and a divine interlocutor <Ref k="sato2008" p="261" />. The structural
		convergence of the honorific second-person, the indefinite agent-defocusing, and the
		oral-literary hero-narrator on a single morphological paradigm is the subject of
		<Xr ch="fourth-person-honorific" />; for the distribution by genre see
		<Xr ch="logophoric-narration-and-reported-discourse" />.
	</p>
	<p>
		The permissive–sociative causative <i lang="ain-Latn">-yar</i> functions additionally as a
		register device in formal speech: the causee, when it is the deferential addressee, may be
		left implicit rather than named directly, heightening the indirection appropriate to the
		social relationship <Ref k="sato2008" p="261" />. This strategy is treated in
		<Xr ch="permissive-sociative-causative-yar" />.
	</p>
