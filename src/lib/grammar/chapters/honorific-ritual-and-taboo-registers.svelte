<S t="Elegant kinship terms as register diagnostics" id="elegant-kin-terms">
	<p>
		A small set of kinship terms participates in the everyday/elegant distinction as straightforward
		register alternants. The terms <A w="ona" gl="father (literary/elegant)" /> and
		<A w="unu" gl="mother (literary/elegant)" /> are the literary or elegant (文語的雅語) kin
		terms for 'father' and 'mother', attested in verse and formal contexts; the everyday
		equivalents <A w="haca" gl="father (colloquial)" /> and
		<A w="hapo" gl="mother / father (colloquial, dialect-variable)" /> vary more strongly by
		dialect and belong primarily to everyday speech
		<Ref k="endo2022" />; <Ref k="aynucorporadiscord" /> (nukopoli, 2026-06-15,
		asserted). The possessed forms <A w="onaha" gl="someone's father" /> and
		<A w="unuhu" gl="someone's mother" /> are the grammatically standard affiliative kin-noun
		forms; the collective compounds <A w="onautari" gl="fathers / parents" /> and
		<A w="unuutari" gl="mothers / parents" /> each carry the extended meaning 'parents (both)'
		alongside the primary 'fathers' and 'mothers' readings <Ref k="aynucorporadiscord" />
		(nukopoli, 2026-06-15, asserted) ‹corpus-suggested›.
	</p>
	<p>
		The colloquial term <A w="hapo" /> shows conspicuous dialect variation: it means 'mother' in
		most Hokkaido varieties but 'father' in Chitose and Horobetsu, while in the Kuril varieties it
		denotes 'elder sibling' <Ref k="aynucorporadiscord" /> (thegodofneet, 2026-06-08, asserted,
		citing Chiri Mashiho's comparative data). This lexical instability of the colloquial terms
		against the relative stability of the elegant pair <i lang="ain-Latn">ona</i>/<i lang="ain-Latn">unu</i>
		across verse traditions provides a further diagnostic value for the register distinction: the
		literary terms are fixed precisely where the colloquial terms shift. The possessive morphology
		and the full kinship-possession system are treated in <Xr ch="kinship-honorific-possession" />.
	</p>
</S>

<S t="Prayer language: genre-conditioned lexical substitution" id="prayer-register">
	<p>
		The most extensively documented register-substitution phenomenon in Hokkaido Ainu is the
		genre-conditioned alternation of fire-deity epithets across the performance contexts of divine
		epic and prayer. In kamuy yukar (divine epic), the fire deity is addressed as
		<A w="ape huci" gl="fire grandmother" />, the kin-term-based epithet shared with everyday
		speech. In formal prayer (<i lang="ain-Latn">kamuy nomi</i> /
		<i lang="ain-Latn">inonno itak</i>), the same deity is addressed by the relational epithet
		<A w="i-resu huci" gl="grandmother who raised me" /> or
		<A w="i-resu kamuy" gl="deity who raised me" />, and the prayer text of 鍋沢モトゾウ
		(Motoanrek, Monbetsu) preserves an additional prayer-only form,
		<A w="mosir kor huci" gl="grandmother who rules the land" />, described in
		<Ref k="endo2022" /> as "not found in any other genre". Conversely, the modifier
		<A w="kamuy" gl="splendid / divine (intensifier in heroic epic)" /> functioning as an
		intensifier glossed 'splendid' occurs in the heroic epic but is absent from prayer
		<Ref k="endo2022" />. The full inventory of substitution pairs for this deity is:
