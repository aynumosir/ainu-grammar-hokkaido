	<p>
		The relational epithet <i lang="ain-Latn">i-resu huci</i> encodes the deity-to-worshipper
		bond explicitly: the prefix <i lang="ain-Latn">i-</i> (antipassive / indefinite-object
		marker) indexes 'me' as the implicit object, so the form is lexically 'the grandmother who
		raised [me]', positioning the prayer as a speech-act by a person acknowledging filial
		dependence on the deity. Using the everyday <i lang="ain-Latn">ape huci</i> in formal prayer
		would constitute a register mismatch; the substitution is not stylistic variation but a
		categorical genre requirement <Ref k="endo2022" />.
	</p>
	<p>
		Prayer language is morphologically marked beyond the lexical substitutions. The
		<i lang="ain-Latn">inonno itak</i> register is applicative-heavy, encoding the case frame of
		the verb by applicative prefixes (<i lang="ain-Latn">e-</i>, <i lang="ain-Latn">o-</i>,
		<i lang="ain-Latn">ko-</i>) rather than by case particles — a hallmark of
		<i lang="ain-Latn">atomte-itak</i> generally, but especially concentrated in prayer
		<Ref k="sato2008" p="263" />. The prayer text cited in <Ref k="endo2022" /> (§2.3)
		preserves doubled applicative prefixes on a single verb:
