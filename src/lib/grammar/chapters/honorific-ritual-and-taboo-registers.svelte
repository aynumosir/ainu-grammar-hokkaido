<S t="The indirection principle: taboo on direct deity–human speech" id="indirection">
	<p>
		<Ref k="sato2008" p="259–261" /> proposes that the non-everyday person marking in Ainu oral
		literature — the use of fourth-person <i lang="ain-Latn">a=</i>/<i lang="ain-Latn">=an</i>
		where everyday first-person would use <i lang="ain-Latn">ku=</i> — is rooted in a cultural
		taboo: a deity or spirit (<i lang="ain-Latn">kamuy</i>) addressing a living human directly
		transgresses a boundary, and the non-everyday person marking softens that transgression by
		increasing indirectness. Satō labels this the <em>indirection principle</em>
		(間接性の原理), contrasting it with the standard "shamaness-oracle origin"
		(巫女の託宣起源) theory and presenting it explicitly as a hypothesis rather than an
		established result ‹speculative›. For the heroic and divine epic genres that most visibly
		deploy this device, see <Xr ch="heroic-and-divine-verse" />.
	</p>
	<p>
		Whether or not Satō's diachronic hypothesis is accepted, the synchronic functional overlap
		between the fourth person's honorific use (deferential address to human superiors) and its
		oral-literary use (narration by divine or supernatural heroes) is structurally coherent:
		both exploit the same fourth-person paradigm to mediate the speech-act through a form not
		claimed by the ordinary grammatical first person of the interaction. The prayer epithet
		alternation described above operates on the same logic at the lexical level: the worshipper
		who addresses the fire deity as <i lang="ain-Latn">i-resu huci</i> rather than
		<i lang="ain-Latn">ape huci</i> likewise routes the address through a relational frame
		encoding the deity's prior act toward the worshipper, rather than naming the deity in a
		neutral noun <Ref k="endo2022" />.
	</p>
</S>

<S t="Gendered ritual speech-acts" id="gendered-ritual">
	<p>
		Ritual speech-acts are differentiated by the performer's sex in ways that partly overlap with,
		and partly extend beyond, the narrator-gender allocation for oral-literary genres (treated
		in <Xr ch="gendered-register-speech" />). Two complementary deferential-acknowledgment acts
		are documented for the Saru basin: women utter
		<A w="hinna" gl="thank you, how kind (women's word)" /> (lower-Saru variant:
		<i lang="ain-Latn">hunna</i>; degree form <A w="hioy'oy" gl="light thanks / how nice" />
		&lt; <i lang="ain-Latn">hinna hinna</i>) while performing <A w="raymik" gl="stroke below the nose with the index finger (gesture of receiving)" />,
		a gesture of acceptance offered when receiving food or a gift; men instead perform
		<A w="onkami" gl="bow with both hands together (gesture of reverence)" />. The formal
		general thanks-form <A w="iyayiraykere" gl="formal expression of gratitude" /> is
		unmarked for gender <Ref k="aynucorporadiscord" /> (nukopoli, 2024-12-26, asserted).
	</p>

