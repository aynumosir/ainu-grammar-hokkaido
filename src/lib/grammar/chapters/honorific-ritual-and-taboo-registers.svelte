<S t="Overview: a three-axis register system" id="overview">
	<p>
		Hokkaido Ainu speech is sociolinguistically stratified along three partially independent axes.
		The <em>poetic/everyday</em> axis — the contrast between <A w="atomte-itak" gl="decorated/ornamented language" /> (雅語, poetic–elegant register) and
		<A w="yayan-itak" gl="ordinary language" /> (日常語/口語) — is the broadest division, shared
		across verse and prayer genres; its archaic morphology and formulaic vocabulary are treated in
		<Xr ch="poetic-archaic-elevated-register" />, and the genre system that controls which register
		is appropriate is described in <Xr ch="oral-literature-genre-taxonomy" />. The
		<em>honorific/deferential</em> axis, realized through the fourth-person address forms and the
		associated pronoun set, is analysed formally in <Xr ch="fourth-person-honorific" /> and
		<Xr ch="kinship-honorific-possession" />. The <em>ritual-substitution and taboo</em> axis,
		in which specific vocabulary items are replaced or avoided in ceremonial, deity-address,
		or kinship contexts, is the focus of the present chapter, set against the sociolinguistic
		conditioning shared by all three axes.
	</p>
	<p>
		The three axes do not constitute a clean binary of "high" versus "low" language.
		<Ref k="nakagawa2001b" /> (via <Ref k="endo2022" paren />) argues that elegant and everyday
		Ainu occupy positions on a continuum keyed to verse-vs-prose and
		fixedness-vs-improvisation, and proposes that a classification by genre-type —
		yukar-specific phrases versus prayer-specific phrases — may be more analytically effective
		than the elegant/everyday dichotomy alone ‹contested›. The substitution phenomena treated
		here are social rather than metrical: the determining factor is the relationship between
		speaker and addressee — between worshipper and deity, between kin-avoidance pairs, between
		the hunter and the spirit of the quarry.
	</p>
</S>

<S t="The honorific address system: fourth-person forms and the pronoun set" id="honorific-fourth-person">
	<p>
		The deferential or honorific address register routes second-person reference through the
		fourth-person prefix <i lang="ain-Latn">a=</i> and suffix <i lang="ain-Latn">=an</i> rather
		than the everyday second-person affixes, producing a construction where
		'you did X' or 'you are Y' is morphologically encoded as if the agent were fourth-person
		<Ref k="sato2008" p="261" />; <Ref k="bugaeva2012" p="474–476" />. The same prefix-set
		indexes the indefinite agent-defocusing function in everyday speech and the first-person
		hero-narrator in oral literature; its deferential reading arises specifically in contexts where
		the referent is identifiable and the social relationship calls for deference — address to an
		elder, to a chief (<i lang="ain-Latn">nispa</i>), or to a deity (see
		<Xr ch="fourth-person-honorific" /> and <Xr ch="person-marking-architecture" />).
	</p>
