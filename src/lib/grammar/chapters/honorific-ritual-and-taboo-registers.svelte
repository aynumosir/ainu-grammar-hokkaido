<S t="Taboo vocabulary: attested existence and the source gap" id="taboo-vocabulary">
	<p>
		The existence of a special taboo vocabulary in Hokkaido Ainu — restricted word-forms for
		hunting, bear-ceremony, and kinship-avoidance contexts — is attested independently in several
		sources. <Ref k="tamura2000" /> catalogues "special terminology including taboo and prayer
		words" as a vocabulary class of the language. The 1795 Edo-period record
		<em>Ezoki</em> is reported in <Ref k="bugaeva2022" /> as describing "a kind of secret
		language" observed among Ainu speakers — an early external testimony to a restricted
		speech variety.
	</p>
	<p>
		The primary monographic treatment of this vocabulary is Hattori (1957),
		"Special language of the older generations among the Ainu" (nenchōshasō tokushugo,
		年長者層特殊語), which was not held or consulted in the preparation of this grammar. No
		systematic hunting-substitute or in-law name-avoidance word list was recoverable from the
		read grammars or the aligned corpus ‹speculative›. What the read sources make available is
		the <em>mechanism</em> — lexical substitution by a genre- or context-conditioned alternate
		form — and several attested examples from the prayer and epic registers, as described above.
		The hunting and bear-ceremony vocabulary, and the kinship-avoidance pairs, remain a gap
		pending access to Hattori (1957) and to the taboo-marked entries in
		<Ref k="tamura1996" />.
	</p>
	<p>
		Structurally, the attested substitution cases share a common property: the replacement is a
		different lexeme or an elaborated periphrastic construction, not a phonological modification
		of the everyday form. This is illustrated by the elegant-register periphrases documented
		in <Ref k="sato2008" p="264" />: everyday <A w="soyne" gl="go out" /> is replaced in the
		elegant register by an elaborate circumlocution; everyday <A w="kamuy" gl="god" /> is
		replaced by <i lang="ain-Latn">kamuy ne manu p</i> 'what is said to be a god'. In each
		case the substitute form encodes a relational or evaluative frame around the concept rather
		than naming it directly — a structural parallel to the fire-deity epithet alternation and,
		plausibly, to the bear-ceremony and hunting-taboo substitutions whose inventory remains
		undocumented in the consulted sources. For the broader system of elegant periphrasis and
		archaic morphology, see <Xr ch="poetic-archaic-elevated-register" />; for the genre
		contexts in which these registers are activated, see
		<Xr ch="sakehe-refrain-and-sung-verse-structure" />.
	</p>
</S>
