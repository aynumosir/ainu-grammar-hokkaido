<S t="The indirect counterpart: humi as" id="humi-as">
	<p>
		The indirect sensory evidential <i lang="ain-Latn">humi as</i> replaces the copula
		<i lang="ain-Latn">ne</i> with the posture verb <A w="as" gl="stand" />, shifting the
		evidential reading from direct to more distanced sensory access: the speaker has sensory
		grounds but presents them as slightly removed or inferential
		<Ref k="dalcorso2018" p="306, 328" />. Dal Corso glosses it
		<span class="sc">ind.flt</span> (indirect evidential, feeling). The opposition copula
		<i lang="ain-Latn">ne</i> (internal, direct) vs posture/existential verb (external,
		indirect) runs across all four formal nouns and is set out in
		<Xr ch="evidential-schema-overview" s="eight-form" />.
	</p>

	<p>
		A functional asymmetry distinguishes <i lang="ain-Latn">humi as</i> from its verbal-source
		counterpart. <i lang="ain-Latn">hawe as</i> has largely specialized as a reportative —
		encoding hearsay when the source is absent and quotation when it is overt
		<Ref k="dalcorso2018" p="328–330" />. <i lang="ain-Latn">humi as</i> retains its sensory
		basis throughout: it is consistently inferential from a non-verbal sensory impression and
		is never reportative <Ref k="dalcorso2018" p="328" /> ‹corpus-confirmed›. As a lexical
		predicate, <i lang="ain-Latn">humas</i> (<i lang="ain-Latn">hum-as</i>) means 'to make a
		sound, to resound' <Ref k="dalcorso2018" p="318" />. The physical-sound use is the most
		frequent in the corpus:
	</p>

	<Ex
		m="aynu ek hum-i as"
		g="person come sound-POSS stand"
		tr="The sound of someone arriving was heard."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker 'Folktale 8 (Part 2)' (Saru); AA研 aa-irc/007, sent. 200"
		note="The posture verb as replaces the copula ne: the narrator infers the person's arrival from an auditory impression rather than direct sight. The indirect humi as contrasts here with the direct humi ne that would encode the arrival as directly sensed."
	/>
</S>

<S t="Double evidentiality with humi" id="double-evid">
	<p>
		Like the other formal nouns, <i lang="ain-Latn">humi</i> enters double-evidential
		constructions in which two evidentials stack within a single clause
		<Ref k="dalcorso2018" p="331–333" />. The combination <i lang="ain-Latn">humi ne humi as</i>
		stacks the direct and indirect sensory evidentials on the same non-visual channel:
	</p>

	<Ex
		m="poro cep a=ne hum-i ne hum-i as orano a=yupi-hi os ma=an wa"
		g="big fish 4.A=COP sound-POSS COP sound-POSS stand after.that 4=elder.brother-POSS follow swim=4.S CONJ"
		tr="It felt as though I had become a great fish, and then I swam after my elder brother."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker 'Folktale 11' (Saru); AA研 aa-irc/013, sent. 32"
		note="Dal Corso 2018: 333 (ex. 275 context). The direct humi ne presents the bodily transformation as directly felt; the immediately following humi as then hedges it as also inferential, conveying that the same sensation was simultaneously vivid and uncertain. Both evidentials draw on the non-verbal sensory channel."
	/>

	<p>
		This pattern differs from the double-evidential constructions in
		<Xr ch="ruwe-ne-inferential" s="mirativity" />, where
		<i lang="ain-Latn">ruwe ne</i> ratifies an indirect form from a distinct channel as
		established knowledge. In <i lang="ain-Latn">humi ne + humi as</i>, the same sensory
		channel provides both layers; the two evidentials together convey a closely felt percept
		reported with residual uncertainty <Ref k="dalcorso2018" p="331–333" />.
	</p>
</S>

<S t="Scope, mirativity, and the admirative tail" id="scope">
	<p>
		The constituent nominalized by <i lang="ain-Latn">humi</i> is a full finite clause
		carrying its own person marking, aspect, and negation, over which the evidential then
		scopes. The scope properties are shared across all four formal nouns and are set out in
		<Xr ch="evidential-schema-overview" s="scope-tam" />; nothing in the structure of
		<i lang="ain-Latn">humi ne</i> alters that general picture.
	</p>

	<p>
		Mirativity is carried in Hokkaido Ainu by the auxiliaries <i lang="ain-Latn">aan</i> /
		<i lang="ain-Latn">awan</i> and by <i lang="ain-Latn">anan</i>, not by the formal-noun
		evidentials themselves <Ref k="hirosawa2026" p="90" />;
		<Ref k="dalcorso2018" p="336" />. Chiri's freezing example above,
		<i lang="ain-Latn">rupus humi ne awan</i>, shows the admirative tail
		<i lang="ain-Latn">awan</i> occupying the post-evidential slot: the surprised-retrospective
		reading comes from <i lang="ain-Latn">awan</i>, not from
		<i lang="ain-Latn">humi ne</i> itself. The full treatment of mirativity and its interaction
		with the evidential system is in <Xr ch="mirativity-scope-grammaticalization" />.
	</p>
</S>

<S t="Diachrony and dialect variation" id="diachrony">
	<p>
		The grammaticalization path parallels that of the three sibling formal nouns. The noun
		<A w="hum" gl="sound, feeling" /> grammaticalized first into the possessed nominal
		<i lang="ain-Latn">hum-i</i>, and from there into a clause-final formal noun whose
		implicit possessor is the nominalized event rather than a personal possessor
		<Ref k="tamura1996" p="s.v. hum" />; <Ref k="hirosawa2026" p="65–66" />. The overall
		cline — sensory noun &gt; possessed formal noun &gt; evidential — is shared by
		<i lang="ain-Latn">sir</i>, <i lang="ain-Latn">haw</i>, and <i lang="ain-Latn">ru</i> and
		places the Ainu system within the nominalization-based evidential strategy discussed by
		<Ref k="dalcorso2018" p="24–25" />; the path is set out for the full paradigm in
		<Xr ch="evidential-schema-overview" s="grammaticalization" />. Dal Corso's account of the
		degree of grammaticalization applies to <i lang="ain-Latn">humi</i> as to the others:
		partial, with residual nominal behaviour visible in the possessive blocking
		<Ref k="dalcorso2018" p="24–25" />.
	</p>

	<p>
		Dialect variation in the realization of <i lang="ain-Latn">humi</i> follows the
		system-wide patterns:
	</p>

	<ul>
		<li>
			<strong>Saru, Chitose, Biratori</strong> — the core of Dal Corso's data — use the
			possessed form <i lang="ain-Latn">humi</i> throughout, with the direct/indirect split
			<i lang="ain-Latn">humi ne</i> / <i lang="ain-Latn">humi as</i>
			<Ref k="dalcorso2018" p="304–337" />.
		</li>
		<li>
			<strong>Tokachi</strong> (and frequently Ishikari) close the clause with the bare concept
			form <i lang="ain-Latn">hum</i> rather than the possessed
			<i lang="ain-Latn">humi</i>; the declarative frame attested by Takahashi appears as
			<i lang="ain-Latn">… ran hum ne awan</i>
			<Ref k="takahashi2013" p="129–131" />; <Ref k="nakagawa2024" p="110" />.
		</li>
		<li>
			<strong>Horobetsu</strong> grammaticalizes the possessed-form nominalizers a step further
