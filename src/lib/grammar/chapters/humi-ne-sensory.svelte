	<p>
		Touch and temperature are attested from the early grammatical literature. Chiri's canonical
		example, cited through Hirosawa, encodes the tactile state of a surface frozen solid:
	</p>

	<Ex
		m="rupus hum-i ne awan"
		g="freeze.solid sound-POSS COP MIR"
		tr="It had completely frozen solid."
		cite="hirosawa2026:66"
		dial="HK"
		note="Chiri 1936: 192, cited via Hirosawa 2026: 66. A bodily-tactile state (freezing solid) is encoded with humi ne; the admirative tail awan carries the surprised-retrospective value. The source noun hum 'feeling' grounds the tactile domain in the same formal noun that covers sound."
	/>

	<p>
		Internal emotional states use the same construction. In the following example, the narrator
		perceives anger welling up from within — an internal bodily impression with no external
		auditory source:
	</p>

	<Ex
		m="a=ruska hum-i ne pekor yay-nu=an"
		g="4.A=be.angry sound-POSS COP like REFL-hear=4.S"
		tr="It felt as though anger was welling up."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker 'Folktale 13' (Saru); AA研 aa-irc/016, sent. 122"
		note="The fourth-person prefix a= marks the agent in the narrative frame. The periphrasis humi ne pekor yay-nu=an 'it felt as though' is a common collocation combining the sensory evidential with the simulative pekor and the reflexive-perception verb yaynu; see the section on frames for related variants."
	/>

	<p>
		A common periphrasis in the corpus combines <i lang="ain-Latn">humi ne</i> with a
		simulative particle and a mental-state verb —
		<i lang="ain-Latn">humi ne kunak ku=ramu</i> 'I thought it felt like …' or
		<i lang="ain-Latn">humi ne pekor yaynu=an</i> 'it seemed as though …' — embedding the
		sensory evidential as a complement to a perception or cognition verb. The following example
		uses the <i lang="ain-Latn">kunak</i> variant:
	</p>

	<Ex
		ain="húmas humi ne kunak ku=ramu kor ku=soyne a p"
		m="hum-as hum-i ne kunak ku=ramu kor ku=soyne a p"
		g="sound-stand sound-POSS COP COMP 1SG.A=think while 1SG.S=go.outside PFV NMLZ"
		tr="Thinking that it sounded like there was a noise, I went outside — but (nothing was there)."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker 'Folktale 8 (Part 1)' (Saru); AA研 aa-irc/006, sent. 358"
		note="The contracted form húmas (= hum-as 'a sound arises') provides the sensory stimulus; humi ne encodes the speaker's non-visual sensory access to that sound; kunak introduces the complement of the cognition verb ramu 'think/feel'. The nominalized tail p sets the clause as backgrounded against a disappointed expectation."
	/>

	<p>
		The boundary with <i lang="ain-Latn">siri ne</i> is explicit in the definition:
		<i lang="ain-Latn">siri</i> encodes a directly visible situation or appearance, while
		<i lang="ain-Latn">humi</i> covers any percept not accessed through sight. The boundary
		with <i lang="ain-Latn">hawe ne</i> runs along the verbal/non-verbal divide:
		<i lang="ain-Latn">hawe</i> (from <A w="haw" gl="voice" />) encodes information obtained
		from words — spoken, reported, or overheard — whereas <i lang="ain-Latn">humi</i> covers
		physical sounds and bodily perceptions that are not themselves linguistic utterances
		<Ref k="tamura1996" p="s.v. haw, hum" />.
	</p>
</S>

