	<p>
		The bare exclamatory <i lang="ain-Latn">humi!</i> is the most distinctive member. It
		packages an immediate sensory reaction — most frequently gustatory or thermal — as its own
		utterance, without the copula:
	</p>

	<Ex
		ain="usey ku=ku wa, keraan humi!"
		m="usey ku=ku wa keraan hum-i"
		g="tea 1SG.A=drink CONJ be.delicious sound-POSS"
		tr="I drank tea — how delicious!"
		cite="nakagawa2024:396"
		dial="CHI"
		place="Nakagawa ex. 793, Chitose"
		note="The bare formal noun humi functions as an exclamative main clause, without the copula ne. The gustatory evaluation triggers the non-visual sensory evidential; the insubordinated frame conveys immediate spontaneous reaction."
	/>

	<p>
		Negation follows the pattern shared by all four evidentials: the proposition is denied within
		the embedded clause (the pattern <i lang="ain-Latn">…somo ki humi ne</i> 'there is no
		feeling that [one does …]'), with <i lang="ain-Latn">humi ne</i> remaining outside the
		scope of negation. The fixed expression <i lang="ain-Latn">humi ka isam</i> expresses the
		complete absence of the relevant non-visual percept. The interaction of the evidential with
		the wider TAM and negation apparatus is treated in
