<S t="Place in the evidential paradigm" id="paradigm">
	<p>
		Among the four direct evidential forms of Hokkaido Ainu,
		<i lang="ain-Latn">humi ne</i> marks that the speaker's information derives from a
		<em>non-visual sensory</em> source: a physical sound, a smell or taste, a touch or bodily
		sensation, or an internal impression that is neither visible nor verbally reported. The
		construction — a nominalized clause headed by a
		<Xr ch="formal-defective-nouns">formal noun</Xr> and closed by the copula
		<i lang="ain-Latn">ne</i> — is common to all four terms and is described in
		<Xr ch="evidential-schema-overview" />. The other three direct forms are treated in
		<Xr ch="ruwe-ne-inferential">the inferential <i lang="ain-Latn">ruwe ne</i></Xr>,
		<Xr ch="siri-ne-situational">the situational <i lang="ain-Latn">siri ne</i></Xr>, and
		<Xr ch="hawe-ne-reportative">the reportative <i lang="ain-Latn">hawe ne</i></Xr>.
	</p>

	<p>
		Dal Corso's morphosyntactic account arranges the four direct forms along a source-reliability
		scale: <i lang="ain-Latn">ruwe ne</i> &gt; <i lang="ain-Latn">siri ne</i> &gt;
		<i lang="ain-Latn">humi ne</i> &gt; <i lang="ain-Latn">hawe ne</i>. Source reliability in
		this account designates the <em>psychophysical channel of acquisition</em>, not a degree of
		epistemic certainty: a speaker using <i lang="ain-Latn">humi ne</i> specifies non-visual
		sensory access, not a lower confidence in the proposition
		<Ref k="dalcorso2018" p="304, 312–313, 339" />. The visual/non-visual macrodivision groups
		<i lang="ain-Latn">ruwe ne</i> and <i lang="ain-Latn">siri ne</i> on the visual side and
		<i lang="ain-Latn">humi ne</i> and <i lang="ain-Latn">hawe ne</i> on the non-visual
		<Ref k="dalcorso2018" p="312" />. Mapped onto the typology of
		<Ref k="aikhenvald2004" p="63–65" paren />, the system is C1-type (visual, non-visual
		sensory, inferred, reported) <Ref k="hirosawa2026" p="64" />.
	</p>

	<p>
		<i lang="ain-Latn">humi ne</i> is among the less frequent members of the paradigm. In Dal
		Corso's Hokkaido reference texts it supplies 45 of 2232 evidential tokens (2.02%), with its
		indirect counterpart <i lang="ain-Latn">humi as</i> adding a further 37 (1.66%)
		<Ref k="dalcorso2018" p="72" />. Both counts place the sensory evidential as the third-most
		frequent direct form and second-to-last in the full eight-form system ‹corpus-confirmed›.
	</p>
</S>

<S t="Morphology: humi is hum-i, 'the sound/feeling of —'" id="morphology">
	<p>
		The formal noun <i lang="ain-Latn">humi</i> is the
		<Xr ch="concept-form-affiliative-form">possessed (affiliative) form</Xr> of the noun
		<A w="hum" gl="sound, feeling" />. Tamura's Saru dictionary records
		<i lang="ain-Latn">hum</i> (ordinary possessed form <i lang="ain-Latn">humi(hi)</i>) with
		the primary senses 音・感じ 'sound; feeling/sensation'
		<Ref k="tamura1996" p="s.v. hum, humi(hi)" />. The same source noun underlies the lexical
		predicate <i lang="ain-Latn">humas</i> (<i lang="ain-Latn">hum-as</i> 'to sound, there is
		a noise'), where <A w="as" gl="stand" /> is the posture-verb element that also appears in
		<i lang="ain-Latn">hawe as</i> and in other transient-state predicates such as
		<i lang="ain-Latn">apto as</i> 'it rains' and <i lang="ain-Latn">rera as</i> 'the wind
		blows' <Ref k="dalcorso2018" p="318, 329" />.
	</p>

	<p>
		As a formal noun in the evidential function, <i lang="ain-Latn">humi</i> blocks the
		additional possessive ending that the full lexical possessed form may carry: the regular
		possessed noun is <i lang="ain-Latn">humi(hi)</i>, but evidential
		<i lang="ain-Latn">*humihi</i> is ungrammatical — the same blocking that disallows
		<i lang="ain-Latn">*ruwehe</i> for the inferential
		<Ref k="tamura1996" p="s.v. humi, ruwe" />; <Ref k="nakagawa2024" p="258" />. This
		restriction, shared by all four formal nouns and discussed in
		<Xr ch="formal-defective-nouns" />, is a diagnostic of partial grammaticalization: the
		form has shed ordinary head-noun syntax without yet becoming a bound morpheme
		<Ref k="dalcorso2018" p="24–25" /> ‹consensus›. The standard gloss is
		<strong>sound/feeling-<span class="sc">poss</span></strong>: the morpheme is
		<i lang="ain-Latn">hum-i</i> (base plus possessive suffix), whose implicit possessor is
		the nominalized clause.
	</p>

	<p>
		The scope of <i lang="ain-Latn">hum</i> sets it apart from the adjacent source noun
		<A w="haw" gl="voice" />. Tamura is explicit: inanimate and non-vocal sounds — a drum,
		ambient noise, a sound with no identified speaker — take <i lang="ain-Latn">hum</i>, while
		human and animal voice takes <i lang="ain-Latn">haw</i>
		<Ref k="tamura1996" p="s.v. haw" />. This lexical distinction is grammaticalized in the
		evidential pair: <i lang="ain-Latn">humi ne</i> for non-verbal sensory access,
		<i lang="ain-Latn">hawe ne</i> for verbal report and hearsay. The construction is a
		copular predication over a nominalized clause — <i lang="ain-Latn">[clause] humi</i> 'the
		sound/feeling that [clause]', closed by the copula
		<i lang="ain-Latn">ne</i> (treated in <Xr ch="copula-ne-predicate-nominals" />).
	</p>
</S>

<S t="Meaning: the non-visual sensory domain" id="semantics">
	<p>
		The traditional label for this form is Kindaichi's
		<span lang="ja">感説法</span> 'feeling-mood', characterizing it as expressing 'it feels
		like … / it seems like …' in the modality of uncertain sensory judgment
		<Ref k="kindaichi1936" />; <Ref k="hirosawa2026" p="65–66" />. The modern evidential
		framing retains the source semantics. Tamura specifies <i lang="ain-Latn">humi ne</i> as
		appropriate for hearing, touch, and bodily or emotional feeling
		<Ref k="hirosawa2026" p="67" />. Nakagawa's Chitose dictionary extends the domain to
		音・匂い・味・体感・直感 — sounds, smells, tastes, bodily sensations, and intuitions
		obtained through any means except sight and verbal report
		<Ref k="nakagawa1995" />; <Ref k="hirosawa2026" p="68, 71" />. His 2024 grammar gives the
		standard definition: <i lang="ain-Latn">humi ne</i> marks that the speaker's judgment rests
		on sensation other than sight and language
		<Ref k="nakagawa2024" p="392" />. Satō's question paradigm assigns 感覚・物音 'sensation,
		physical sound' to the <i lang="ain-Latn">humi</i> domain
		<Ref k="sato2008" p="135" />.
	</p>

	<p>
		Dal Corso glosses the form <span class="sc">dir.flt</span> (direct evidential, feeling) and
		lists the covered channels as hearing, smell, touch, taste, and an internal 'sixth sense'
		<Ref k="dalcorso2018" p="306" />. He documents a recurring sensor-stimulus mismatch: the
		evidential derives historically from the noun meaning 'sound, feeling', yet it licenses
		gustatory and other non-auditory stimuli
		<Ref k="dalcorso2018" p="307, 311" />. The following example shows a gustatory stimulus
		under the sound-derived evidential — the embedded clause concerns being given food to eat,
		not hearing a sound:
	</p>

	<Ex
		m="usa sisakpe a=i=y-e-re hum-i ne ya"
		g="various delicious.things 4.A=4.O=EP-eat-CAUS sound-POSS COP Q"
		tr="Whether I was given all sorts of delicious things to eat…"
		cite="dalcorso2018:306"
		dial="SAR"
		place="Wateke (Saru), Tamura audio materials; Dal Corso siglum TMA: 42"
		note="Dal Corso ex. 242 (DIR.FLT). A gustatory stimulus (being given food) appears under the sound/feeling-derived humi ne, illustrating the sensor-stimulus mismatch discussed at pp. 307, 311. The verb complex a=i=y-e-re is a causative benefactive ('caused to eat for 4O'); ya is a question/dubitative particle."
	/>

	<p>
		The extension from auditory to other senses is not unexpected: the source noun
		<A w="hum" gl="sound, feeling" /> already covers both auditory and somatic senses
		lexically, and as it grammaticalized into a formal noun its scope broadened across all
		perceptual channels not taken by <i lang="ain-Latn">siri</i> (sight) or
		<i lang="ain-Latn">hawe</i> (verbal report). Chiri noted the expansion from sound to
		taste, colour, and smell in the earliest grammatical account
		<Ref k="hirosawa2026" p="66" />. The following table shows the attested domains:
	</p>

