<S t="Prayer speech in the genre system" id="overview">
	<p>
		Prayer speech — <A w="inonno itak" gl="prayer words" /> or <A w="kamuy nomi" gl="divine worship /
		prayer ceremony" /> — belongs to the <em>chant</em> (唱え言) macro-category of Ainu oral
		performance, whose distinguishing feature is that the speaker's will is directed toward a
		non-present audience: deities, spirits, the dead, or distant humans
		<Ref k="endo2022" p="§2.3" />. The chant category is set out alongside the narrative and song
		categories in <Xr ch="oral-literature-genre-taxonomy" />; the present chapter treats the
		grammatical and register properties of prayer speech specifically.
	</p>

	<p>
		Within the chant sub-system, prayer (<A w="kamuy nomi itak" gl="divine-worship words" />) is
		distinguished from incantation by improvisation: a prayer is composed fresh each time, to the
		worshipper's own melody, whereas an incantation is memorised word-for-word and shared with a
		fixed community tune <Ref k="endo2022" p="§2.3" />. Prayer is further distinguished from the
		speech-act of public negotiation (<A w="caranke" gl="formal dispute / ceremonial debate" />) by
		its divine addressee. The genre boundaries are set out in the table below.
	</p>

