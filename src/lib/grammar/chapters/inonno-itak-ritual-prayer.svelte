<S t="Performative context: kamuy-nomi and the ceremony" id="performative">
	<p>
		A <A w="kamuy nomi" /> is an address to the divine world conducted at the altar
		(<A w="nusa" gl="sacred stick altar" />) or the hearth. The fire deity (<A w="ape huci" gl="fire
		grandmother" />) is invoked first in every prayer, as the goddess who relays human words to the
		gods; all subsequent divine recipients are addressed through her
		<Ref k="endo2022" p="§2.3" />. Prayer is structurally a two-stage act of communication:
		worshipper → fire deity → target deity. The
		right to lead prayer is restricted to adult men ‹consensus›; women's
		formal expression of gratitude takes the verbal gesture of <A w="hinna" gl="thanks (women)" />
		accompanied by <A w="raymik" gl="stroking below the nose with the index finger" />, while men
		perform <A w="onkami" gl="bow with both hands" /> (nukopoli, aynu-corpora Discord, 2024-12-26,
		asserted; <Ref k="aynucorporadiscord" />).
	</p>

	<p>
		The material offerings that accompany prayer — <A w="inaw" gl="wooden prayer-stick (shaved wood
		offering)" />, fermented millet wine (<A w="sake" />), and dumplings (<A w="sito" />) — are
		enumerated in narrative prose when speakers describe the ceremony from outside it. In one Saru
		uwepeker, the narrator 川上まつ子 lists the offerings: <i lang="ain-Latn">kamuynomi atpa ne,
		a=nomi wa inaw ne ciki sake ne ciki sito ne ciki pirka uske</i> 'at the start of the
		kamuy-nomi, [we] worship and (offer) fine things — inaw, sake, dumplings…'
		<Ref k="ilcaa1976" /> (川上まつ子, AA研 uwepeker 12, Saru). The disjunctive
		<i lang="ain-Latn">X ne ciki</i> 'whether it be X' is an enumerating formula in elevated
		narrative, not specific to prayer speech itself — see
		<Xr ch="oral-literature-genre-taxonomy">the genre taxonomy chapter</Xr>.
	</p>

	<p>
		The ceremonies associated with prayer speech include the seasonal worship calendars
		(<A w="Asirpanomi" gl="New Year's prayer" />, <A w="Cinomisirkamuynomi" gl="mid-year prayer" />,
		<A w="Asircepnomi" gl="first-salmon prayer" />; nukopoli, aynu-corpora Discord, 2024-03-18,
		asserted; <Ref k="aynucorporadiscord" />) and the bear-sending ceremony. The verb forms for the
		sending ceremonies are semantically distinct: <A w="omante" gl="send home (the bear cub)" /> /
		<A w="hopunire" gl="send to the mountain (wild-caught animals)" /> /
		<A w="iwakte" gl="send off (the dog)" /> (nukopoli, aynu-corpora Discord, 2024-11-17, asserted,
		citing 欠ヶ端和也 2016; <Ref k="aynucorporadiscord" />). The prayer speech that accompanies these
		ceremonies follows the same grammatical register as all kamuy-nomi, regardless of the ceremony
		type.
	</p>
</S>

<S t="The register of prayer: atomte-itak" id="register">
	<p>
		Prayer speech belongs to the elevated register <A w="atomte itak" gl="ornamented/poetic
		language" /> (雅語), which is distinguished from everyday speech (<A w="yayan itak" gl="ordinary
		language" />) by a set of grammatical and lexical properties described in
		<Xr ch="poetic-archaic-elevated-register" />. Kindaichi's ranking of genres by elegant-language
		density — greeting &lt; prayer &lt; incantation &lt; <A w="caranke" /> &lt;
		<A w="yukar" /> — places prayer above everyday greeting speech but below the heaviest
		verse genres <Ref k="endo2022" p="§3" />. In Nakagawa's reformulation, elegant and everyday
		language are not a binary but stages on a verse/improvisation continuum, and the prayer
		sub-register is sufficiently distinct from the yukar sub-register that Nakagawa proposes
		a prayer-word vs. yukar-word classification as more analytically useful than the
		simple elegant/everyday dichotomy <Ref k="endo2022" p="§3.4" /> ‹contested›.
	</p>

	<p>
		Four defining properties of <A w="atomte itak" /> (as formulated in Nakagawa 2001a[1987], via
		<Ref k="endo2022" p="§3" />) apply to prayer speech: (1) verse structure built on
		five- or four-syllable line units; (2) formulaic phrases deployed to meet the syllable count;
		(3) morphologically more complex and polysynthetic forms than their everyday equivalents; and
		(4) a distinct, genre-conditioned vocabulary. Properties (3) and (4) are examined in
		§§ <a href="#applicative">applicative morphology</a> and
		<a href="#vocabulary">prayer vocabulary</a> below; property (2) in
		<Xr ch="parallelism-couplets-and-word-pairs" />.
	</p>
</S>

<S t="Grammatical hallmarks of prayer speech" id="grammar">
	<S t="Person marking: first-singular ku- in prayer" id="person-marking">
		<p>
			The most striking person-marking contrast between prayer speech and narrative oral literature is
			that prayer uses the <strong>first-person singular</strong> prefix <A w="ku=" gl="1SG agent" />
			(and suffix <A w="=an" gl="1SG intransitive subject" /> in singular intransitives) where
			verse narrative uses the <strong>fourth person</strong> <A w="a=" gl="4th-person agent" />.
			In narrative genres — heroic epic, divine epic, and prose tale — the protagonist narrates in a
			non-everyday first person realized by the fourth-person affixes
			<A w="a=" /> / <A w="=an" />, which indexes the narrative voice with a degree of
			indirection compatible with the "dream-origin" hypothesis for oral literature
			<Ref k="sato2008" p="259–261" />; the full account of this fourth-person narrative function
			is given in <Xr ch="logophoric-narration-and-reported-discourse" /> and
			<Xr ch="fourth-person-honorific" />.
		</p>

		<p>
			Prayer speech addresses the deities directly in the worshipper's own voice, and uses
			<A w="ku=" /> accordingly. An attested prayer text from the Monbetsu region (鍋澤モトゾウ
			/Motoanrek, narrator; cited via <Ref k="endo2022" p="§2.3" />) shows the forms
			<i lang="ain-Latn">ku-e-ko-pase-…</i> and <i lang="ain-Latn">k=e-ko-onkami</i>
			'I reverently worship [you]' — both built on the <strong>first-person singular</strong>
			prefix <A w="ku-" /> / <A w="k-" /> (reduced allomorph before a vowel-initial stem),
			not the fourth person. The contrast with narrative is thus: fourth person in narrative
			oral literature, first singular in prayer ‹corpus-suggested›. See
			<Xr ch="indefinite-fourth-person-paradigm" /> and
			<Xr ch="person-marking-architecture" /> for the person paradigm.
		</p>

		<Ex
			m="ku=onkami ruwe tapan na"
			g="1SG.A=worship track-POSS DEM.PROX FIN"
			tr="I offer worship (this is a fact)."
			constructed
			note="Constructed to illustrate the 1SG prefix in prayer speech; the ruwe tapan na frame marks formal/ceremonial assertion — see §ruwe-tapan below."
		/>
	</S>

	<S t="Applicative case-marking" id="applicative">
		<p>
			Prayer belongs to the elevated register's most characteristic morphological pattern:
			locative and goal relations are expressed by applicative prefixes
			(<A w="e-" />, <A w="o-" />, <A w="ko-" />) rather than by postpositional case particles
			<Ref k="sato2008" p="263" />; <Ref k="endo2022" p="§3.4" />. The applicative system is
			described in <Xr ch="poetic-archaic-elevated-register" />; what is distinctive in prayer
			is the density of <strong>stacked applicatives</strong> — two or more prefixes on the same
			verb, encoding simultaneously the goal deity and the manner of approach. The Monbetsu prayer
			text cited above shows <i lang="ain-Latn">k=e-ko-onkami</i>: the goal applicative
			<A w="ko-" gl="goal/recipient applicative" /> marks the deity as the target of worship, and
			the additional <A w="e-" gl="path/domain applicative" /> encodes the dimension of address
			<Ref k="endo2022" p="§2.3" />.
		</p>

		<p>
			The contrast between everyday and elevated locative marking is illustrated in
			<Ref k="sato2008" p="263" />: the everyday form uses the particle
			<A w="ta" gl="locative case particle" /> — <i lang="ain-Latn">tanto Ukurmem ta
			ku-sirepa</i> 'I arrived at Ukurmem today' — whereas the elevated form marks the same
			relation with the goal applicative <A w="ko-" />:
		</p>

		<Ex
			m="kamuy menoko uni-hi tane anakne a=ko-sirepa na"
			g="god woman house-POSS now TOP 4.A=APPL-arrive FIN"
			tr="Now I arrived at the divine woman's house."
			cite="sato2008:263"
			dial="CHI"
			note="Satō 2008 ex. 20 (Chitose): elegant-register example contrasting with the colloquial particle-marked form. The goal applicative ko- incorporates the locative relation that would be marked by ta in everyday speech. Here the fourth-person a= marks the (narrative) first person, illustrating the register's applicative morphology in a verse context; in prayer proper, ku- would replace a= (see §person-marking)."
		/>

		<p>
			In prayer speech the same applicative morphology applies, but with the first-singular
			prefix. The pattern <i lang="ain-Latn">k=e-ko-</i> combines two applicative prefixes:
			<A w="ko-" /> for the divine recipient and <A w="e-" /> for the domain of address.
			Multiple applicative stacking is an established property of the elevated register and is
			treated typologically in <Xr ch="poetic-archaic-elevated-register" />.
		</p>
	</S>

	<S t="Prayer-specific vocabulary and honorific epithets" id="vocabulary">
		<p>
			The lexical register of prayer is genre-conditioned in a stronger sense than the
			elevated/everyday dichotomy alone would predict: certain epithets and noun phrases
			occur <em>only in prayer</em> and are not found in everyday speech or in other verse
			genres. Endō identifies this as a property that motivates treating prayer vocabulary as a
			distinct sub-class of the elevated register rather than simply a variety of the yukar
			lexicon <Ref k="endo2022" p="§3.4" />.
		</p>

		<p>
			The fire deity provides the clearest documented case. In everyday speech and in the
			divine epic (<A w="kamuy yukar" />), the fire deity is called
			<A w="ape huci" gl="fire grandmother" />. In prayer, two distinct epithets replace this:
		</p>

