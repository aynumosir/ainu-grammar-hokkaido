		<p>
			The epithet <A w="mosir kor huci" /> 'grandmother who rules the land' was documented in
			the prayer text of 鍋澤モトゾウ (Motoanrek, Monbetsu; cited via <Ref k="endo2022" p="§2.3" />)
			and is described by Endō as "not found in any other genre". The epithet
			<A w="i-resu huci" /> (where <A w="i-resu-" gl="raise me; my raiser" /> = 1.O-raise =
			'the one who raised me') or its kamuy-variant
			<A w="i-resu kamuy" /> references the fire deity's role as the deity who reared and
			protects the community, a relationship that everyday <A w="ape huci" /> does not encode
			<Ref k="endo2022" p="§3.4" />. Endō (2009, cited via <Ref k="endo2022" p="§3.4" />)
			further notes that the modifier <A w="kamuy" gl="god; splendid (modifier)" /> with the
			meaning 'splendid' or honorific intensifier occurs in heroic epics but not in prayer, a
			further sub-register distinction within elevated speech ‹corpus-suggested›.
		</p>

		<p>
			The elegant kin terms documented in the lexicon also belong to this prayer-adjacent register:
			<A w="ona" gl="father (literary/formal)" /> and <A w="unu" gl="mother (literary/formal)" />
			are the elevated equivalents of the colloquial
			<A w="haca" gl="father (colloquial)" /> / <A w="hapo" gl="mother (colloquial)" /> (nukopoli,
			aynu-corpora Discord, 2026-06-15, asserted; <Ref k="aynucorporadiscord" />). Their
			attestation in prayer contexts has not been independently verified from prayer texts, but
			the community observation is consistent with the elegant-register lexical substitution
			described in <Ref k="endo2022" p="§3" /> ‹corpus-suggested›.
		</p>
	</S>

	<S t="The ruwe tapan ceremonial closure" id="ruwe-tapan">
		<p>
			Prayer utterances, and formal speech-acts that invoke prayer register, frequently close with
			the frame <i lang="ain-Latn">ruwe tapan na</i>, a ceremonial assertion tail. In
			<Ref k="tamura1996" /> the frame is marked as a register-specific feature of formal/
			ceremonial speech; it appears in <Ref k="endo2022" p="§2.3" /> prayer contexts and is
			also documented in other elevated speech (the greeting and negotiation genres). The frame
			combines the evidential formal noun <A w="ruwe" gl="track, fact (evidential nominalization)" />
			— the same element treated as an inferential evidential in
			<Xr ch="ruwe-ne-inferential" /> — with the proximate demonstrative
			<A w="tapan" gl="this (proximate)" /> in place of the copula <A w="ne" />. The effect is
			to assert the clause content as an already-established fact that the addressee can witness
			directly; in prayer speech, the deity is the witness
			<Ref k="endo2022" p="§2.3" />; <Ref k="tamura1996" p="s.v. ruwe tapan" />.
		</p>

		<Ex
			m="sonno yayrayke kewtum ku=kor wa ku=onkami ru-we tapan na"
			g="truly be.grateful heart 1SG.A=have CONJ 1SG.A=worship track-POSS DEM.PROX FIN"
			tr="Truly with a grateful heart I offer worship — this is a fact."
			ain="sonno yayrayke kewtum ku=kor wa ku=onkamuy ruwe tapan na"
			cite="ainukoraci1991"
			dial="HK"
			place="Ōno Tetsuhito, Ainu Times issue 41 (Samani)"
			note="The source text has ku=onkamuy, a form with final -y not found in Saru/Chitose documentation of onkami; it may reflect Samani dialect phonology or a different stem shape. Morphemic line normalised to onkami; the ain line gives the source form. ruwe tapan na is the ceremonial assertion frame."
		/>

		<p>
			The full evidential system built on formal nouns is set out in
			<Xr ch="evidential-schema-overview" />. What the prayer use adds is that
			<i lang="ain-Latn">ruwe tapan</i> (versus the more common <i lang="ain-Latn">ruwe ne</i>) is
			register-marked: it is a ceremonial variant found in formal genres and not in everyday
			assertion. Whether the selection of <A w="tapan" /> 'this' over <A w="ne" /> (copula)
			encodes a special speech-act value — presenting the prayer content as observable to the
			divine audience — or whether it is simply a lexically frozen formulaic closure is not
			settled by the available sources ‹speculative›.
		</p>
	</S>

	<S t="Number agreement in the elevated register" id="number">
		<p>
			A diagnostic of elevated speech reported for both Saru and Chitose is the
			<strong>number-agreement override</strong>: in everyday speech, the suffix
			<A w="=an" /> combines only with a plural verb form (e.g., <A w="arki=an" gl="come.PL=4.S" />
			'we come'), never with a singular stem (<i lang="ain-Latn">*ek=an</i> would be
			ungrammatical in conversation). In the elevated register, <A w="=an" /> may appear on a
			singular verb form — <i lang="ain-Latn">ek=an</i> 'I come (sg.)' — to index the
			first-person narrative voice of the verse persona
			<Ref k="sato2008" p="263" />; <Ref k="endo2022" p="§3.4" />. As prayer speech uses the
			first-singular <A w="ku=" /> rather than the fourth-person <A w="a=" />, the
			number-agreement override in its narrative form does not apply within prayer itself; but
			the wider pattern of agreement relaxation is part of the atomte-itak grammar that prayer
			shares with yukar and kamuy yukar.
		</p>
	</S>
</S>

<S t="Prayer vs. incantation: fixed and improvised chant" id="incantation">
	<p>
		The prayer/incantation contrast rests on a single axis: prayer is improvised ("not memorised
		like scripture; spun out by improvisation, different each time", Nakagawa 2016: 16, cited via
		<Ref k="endo2022" p="§2.3" />), while incantation is word-for-word fixed and communally shared.
		Both use melody; both address deities; both belong to the chant category. Grammatically, the
		shared elevated-register morphology (applicatives, archaic vocabulary, number override) applies
		to both, since both are composed in <A w="atomte itak" />. The improvised character of prayer
		means that no canonical prayer text has the status of a fixed literary work in the way that a
		kamuy yukar or yukar text does: prayers are attested only as specimens preserved in recordings
		or transcriptions, not as memorized texts reproduced with authority by named successors
		<Ref k="endo2022" p="§2.3" />.
	</p>

	<p>
		At the same time, prayer is heavily formulaic at the level of the phrase and the opening
		sequence (address to the fire deity; enumeration of offerings), even though the full utterance
		is improvised. This is the pattern Nakagawa identifies for the elevated register generally:
		formulae — fixed verse phrases that are learned and re-deployed — are the unit of memorisation,
		not the text <Ref k="endo2022" p="§3" />. The formulaic patterns in prayer overlap with those
		in yukar and kamuy yukar (couplet structures, <i lang="ain-Latn">tu ~ / re ~</i>
		'two ~ / three ~' intensification doublets, applicative periphrases) and are described in
		<Xr ch="parallelism-couplets-and-word-pairs" />.
	</p>
</S>

<S t="Attestation and source coverage" id="attestation">
	<p>
		Actual prayer texts preserved in audio or transcription are sparser in the accessible
		Hokkaido corpus than narrative texts, for reasons Endō notes: prayer is a performative act
		tied to ceremony, not a memorised genre reproduced for entertainment or transmission
		<Ref k="endo2022" p="§2.3" />. The Hokkaido corpus (209,878 sentences) contains
		metalinguistic mentions of prayer words embedded in narrative (uwepeker, kamuy yukar) rather
		than extended stretches of prayer speech itself. The primary documented prayer text accessible
		for grammatical analysis is the Monbetsu (Biratori) example attributed to 鍋澤モトゾウ
		(Motoanrek), cited and analysed by <Ref k="endo2022" p="§2.3" />; further attested prayer
		language appears in the Biratori oral-literature archives
		<Ref k="biratori1969" /> and in the ILCAA Ainu language materials
		<Ref k="ilcaa1976" /> through prosodic and formulaic phrases embedded in narrators' own
		metalinguistic commentary.
	</p>

	<p>
		The renewal of prayer speech in the revitalisation era has produced written specimens — the
		<Ref k="ainukoraci1991" /> periodical includes self-composed prayer formulas by speakers
		including 大野徹人 (Samani) — but these are modern compositions; they attest living competence
		in the prayer register rather than traditional continuous transmission. The evidential
		tail <i lang="ain-Latn">ruwe tapan na</i> glossed above (§<a href="#ruwe-tapan">ruwe tapan</a>)
		comes from such a modern specimen. The distinction between documented traditional prayer texts
		and revitalisation-era compositions is maintained throughout; the register properties described
		in this chapter are drawn from traditional materials unless stated otherwise.
	</p>

	<p>
		The grammar of the prayer register intersects with several other chapters of Part XIX and
		Part X: the elevated applicative apparatus is treated comparatively across genres in
		<Xr ch="poetic-archaic-elevated-register" />; the honorific and logophoric uses of the fourth
		person (relevant to how deity address and narrative voice interact) are analysed in
		<Xr ch="fourth-person-honorific" />; the genre-conditioned distribution of the evidential
		system (including <i lang="ain-Latn">ruwe tapan</i>) appears in
		<Xr ch="narrative-tam-evidential-patterning-by-genre" />; and the full honorific and taboo
		register (including hunting-taboo vocabulary and in-law speech, which shares some properties
		with prayer-register avoidance strategies) is in
		<Xr ch="honorific-ritual-and-taboo-registers" />.
	</p>
</S>
