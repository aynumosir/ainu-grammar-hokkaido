<S t="Community leads and learner-grammar material" id="community-leads">
	<p>
		Two bodies of material outside the formal research literature bear on specific domains. The
		aynu-corpora Discord community has accumulated analytical discussion of Ainu grammar across
		several thousand archived messages, including morpheme identifications, pattern observations,
		and occasional structural hypotheses <Ref k="aynucorporadiscord" />. These contributions are
		treated as leads: points of departure for corpus investigation and source-checking. Where a
		community observation has been verified against the scholarly literature or the corpus it is
		integrated under the appropriate evidence grade and cited with the contributor's handle. Where
		it has not been verified it is not included.
	</p>
	<p>
		Ijäs (2023) <Ref k="ijas2023" /> provides an accessible English-language learner grammar of
		Ainu, available as an online guide. Its structural analyses are cited where they present a
		reading of a construction not covered in the research literature, and graded accordingly. It is
		also cited for its treatment of revival-register conventions. Being addressed primarily to
		learners rather than to the documentary record, its analyses of the teaching norm and of the
		historical oral corpus are distinguished when citing it.
	</p>
</S>

<S t="How to use this grammar" id="how-to-use">
	<p>
		The grammar can be read linearly — beginning with <Xr ch="typological-profile" /> for a
		structural orientation, then moving through the sources and conventions chapters of Part II
		before entering the substantive description — or consulted chapter by chapter for specific
		phenomena. Chapter numbering is sequential and book-wide, but cross-references are always given
		by chapter title and slug, so individual chapters can be read independently without knowing
		their sequence number.
	</p>
	<p>
		The interlinear glossing conventions — the four-line format, morpheme-boundary symbols, and the
		Ainu-specific gloss inventory — are documented in <Xr ch="glossing-abbreviations-and-citation" />;
		the full abbreviation list is in <Xr ch="abbreviations-glossing-symbols" />. The Latin
		phonemic transcription used throughout the grammar is explained in
		<Xr ch="latin-phonemic-transcription" />. The oral-literature and spoken-language data sources
		that supply most of the attested examples are surveyed in <Xr ch="oral-literature-and-spoken-corpora" />.
	</p>
	<p>
		The optional formal-analysis sections in the five theory-magnet domains are clearly delimited
		within each chapter. Readers who want the descriptive account without the formal apparatus can
		skip those sections; cross-references from the formal sections into the descriptive content are
		one-directional, so the descriptive text stands without them. The bibliography at the close of
		each chapter is generated automatically from the citations that appear in it and lists only
		works actually cited in that chapter.
	</p>
</S>
