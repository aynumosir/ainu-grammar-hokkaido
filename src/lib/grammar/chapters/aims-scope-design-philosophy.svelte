		morphological architecture that all subsequent grammars build on <Ref k="kindaichi1936" />.
		Refsing provides the most systematic morphosyntactic account in English of any single Hokkaido
		dialect <Ref k="refsing1986" />. Shibatani places Ainu within a typological account of the
		languages of Japan <Ref k="shibatani1990" />. Tamura's Saru dictionary and grammar monograph
		remain the central data source for that dialect <Ref k="tamura1996" />; <Ref k="tamura2000" />.
		Satō's grammar gives the most thorough treatment of the Chitose verb system and Hokkaido
		morphosyntax in the modern Japanese-language literature <Ref k="sato2008" />. The Bugaeva
		Handbook supplies extensive English coverage across topics from phonetics to oral literature and
		from Hokkaido to Sakhalin <Ref k="bugaeva2022" />. Nakagawa's grammar brings the description
		of Hokkaido Ainu to its greatest single-volume comprehensiveness <Ref k="nakagawa2024" />.
	</p>
	<p>
		The grammar works from all of these, citing each directly. What the present description adds is
		a corpus-quantified layer — systematic frequency counts for morphological cells, evidential
		paradigm members, alignment environments, and sandhi rates — alongside cross-dialect integration
		extending beyond the Saru–Chitose pair that dominates the modern literature, and an explicit
		typological scoring at both the language level and within each domain. Where prior analyses
		disagree — on the number of formal persons, on the morphological alignment of the paradigm, on
		the word-class status of property concepts, on the syntax of the evidential constructions — the
		disagreements are stated explicitly and graded.
	</p>
