<S t="Aims and intended readership" id="aims">
	<p>
		This grammar provides a comprehensive English-language reference description of Hokkaido Ainu,
		the primary indigenous language of Hokkaido, Japan. The object of description is the language's
		phonological, morphological, morphosyntactic, and discourse structure as documented across the
		Hokkaido dialect cluster — principally Saru, Chitose, Shizunai, Tokachi, Horobetsu, Mukawa, and
		Ishikari — drawing on the full range of prior scholarship from Kindaichi and Chiri
		<Ref k="kindaichi1936" /> through the modern reference grammars of Tamura
		<Ref k="tamura1996" />; <Ref k="tamura2000" />, Refsing <Ref k="refsing1986" />, Shibatani
		<Ref k="shibatani1990" />, Satō <Ref k="sato2008" />, Bugaeva <Ref k="bugaeva2022" />, and
		Nakagawa <Ref k="nakagawa2024" />.
	</p>
	<p>
		The intended readership is broad but assumes linguistic training. The grammar is addressed to
		professional linguists working on polysynthesis, evidentiality, applicatives, alignment, or the
		typology of head-marking languages; graduate students and advanced undergraduates in descriptive
		and typological linguistics; Ainu studies researchers whose primary language of scholarship is
		English or Japanese; documentation workers engaged with Ainu language materials; and community
		members who approach the language through formal description. Readers without prior knowledge of
		Ainu will find a structural overview in <Xr ch="typological-profile" /> and the historical and
		cultural setting in <Xr ch="ainu-people-homeland-history" />. Those already familiar with the
		Japanese-language descriptive tradition may proceed directly to whichever domain interests them.
	</p>
</S>

<S t="What a 'comprehensive reference grammar' commits to" id="comprehensiveness">
	<p>
		A reference grammar is comprehensive in both depth and breadth: it covers every major
		grammatical domain, provides attested examples for every claim it makes, states where the
		literature disagrees and how seriously, and offers a quantitative profile of the phenomena it
		describes wherever the data permit. The benchmark for breadth here is Nakagawa (2024)
		<Ref k="nakagawa2024" />, which organizes its description across 28 top-level chapters —
		the most extensive single-authored description of Hokkaido Ainu yet published. The present
		grammar targets roughly double that topic coverage, distributing 176 leaf chapters across 23
		structural parts. The organizing sequence is the standard structural-descriptive one:
		phonetics and phonology → morphophonology → word classes and nominal morphology → possession →
		pronouns and demonstratives → personal affixes → verbal morphology → valency and voice → TAM →
		evidentiality and modality → negation → basic clause structure → complex sentences and clause
		linkage → discourse and information structure, bracketed by front matter, sources and
		conventions, and back matter (diachrony, dialectology, contact, and texts).
	</p>
	<p>
		Comprehensiveness means integrating all prior descriptions rather than supplanting them. Every
		chapter cites the relevant treatments from the existing scholarship and states where they agree,
		where they diverge, and what the grammar takes from each. The grammar claims comprehensiveness
		of coverage and integration; it does not claim priority over any individual analysis.
	</p>
</S>

<S t="Scope" id="scope">
	<S t="Hokkaido Ainu as the object of description" id="hokkaido-focus">
		<p>
			Hokkaido Ainu is the primary object of description. It commands the great majority of the
			accumulated corpus and the bulk of the modern descriptive literature, and its several dialects
			differ enough to make dialect-internal comparison informative without requiring a separate
			comparative grammar for each. The principal dialects drawn on are Saru (the most copiously
			documented), Chitose, Shizunai, Tokachi, Horobetsu, Mukawa, and Ishikari. Finer locality
			distinctions — Nibutani within Saru, Biratori, Honbetsu within Tokachi — are recorded in the
			<code>place</code> field of each example rather than as separate dialect labels.
		</p>
		<p>
			Sakhalin Ainu (also referred to as Enciw) and Kuril Ainu appear as external contrast material
			throughout the grammar, presented in dedicated contrast boxes with their dialect tags
			(<code>SA</code>, <code>KU</code>) whenever they illuminate a specifically Hokkaido structural
			choice. Several features of Hokkaido Ainu — the pitch-accent system, the four-term
			evidential paradigm, the system of applicative preverbs, noun incorporation as a productive
			syntactic operation, and the elaborate oral-literature register — have no direct counterpart
			or show divergent organization in the Sakhalin variety; these receive expanded first-class
			treatment, with Sakhalin drawn into comparison where the contrast is instructive. The
			sociolinguistic situation, including the present state of the revitalization movement, is
			treated in <Xr ch="sociolinguistic-situation-revitalization" />.
		</p>
		<p>
			The modern revitalization register — the standardized orthography and coined vocabulary
			current among new-speaker communities — is described as a synchronic variety alongside the
			documented oral tradition, with coinages and standardized forms explicitly flagged when they
			differ from documented historical usage. The grammar's corpus frequency counts draw on
			historical texts and conversation recordings rather than revival-norm materials, and the
			distinction is maintained throughout.
		</p>
	</S>

	<S t="Grammar, not dictionary" id="grammar-only">
		<p>
			This is a grammar. Lexical material appears only as grammatical evidence: in examples, in
			paradigm cells, and in class diagnostics. The grammar does not define senses of individual
			lexemes beyond what is needed to gloss an example, and it links out to the available
			dictionary resources rather than reproducing or paraphrasing their content. The dictionaries
			and lexicographic records that underpin the grammar are surveyed in
			<Xr ch="written-sources-grammars-dictionaries" />; the ethnographic and cultural context is
			treated in <Xr ch="ainu-people-homeland-history" />.
		</p>
	</S>
</S>

<S t="The four standing commitments" id="method">
	<p>
		Four commitments run through every chapter. They are not segregated layers but part of how each
		phenomenon is described from the outset.
	</p>

	<S t="Corpus quantification" id="corpus-quantification">
		<p>
			Every frequency claim in the grammar is backed by counts from a large aligned corpus of
			Hokkaido Ainu oral-literature and conversation data that includes material from the NINJAL
			Corpus of Ainu oral literature <Ref k="ninjal2003" /> and the Glossed Audio Corpus of
			Ainu Folklore <Ref k="glossedcorpus2016" />. The corpus is treated as a register archive:
			genre labels (uwepeker 'prose tale', yukar 'human epic', kamuy yukar 'divine epic', ritual
			prayer, conversational recordings) permit per-genre frequency breakdowns. This genre-level
			detail bears directly on several claims about TAM, evidentiality, and information-structural
			phenomena whose distribution is register-conditioned. No predecessor grammar reports
			systematic corpus frequencies for Hokkaido Ainu. The sampling methodology, dialect weighting,
			and the conventions for citing figures are set out in full in
			<Xr ch="dialect-sample-and-corpus-method" />.
		</p>
	</S>

	<S t="Typological framing" id="typological-framing">
		<p>
			Each grammatical domain is placed against a named cross-linguistic framework. At the level of
			the language as a whole, the grammar produces a feature profile comparable to what WALS or
			Grambank would supply for a set of typological parameters: overall structural type
			(polysynthetic, head-marking, head-final, postpositional, modifier-before-head),
			morphological alignment, valency-changing morphology, evidential paradigm type, number
			marking, and numeral base. Within individual domains, standard typological reference works
			supply the comparative scaffolding — the frameworks cited range from general morphosyntactic
			typology to the specific literatures on alignment, evidentiality, applicatives,
			nominalization, and noun incorporation. The purpose is to supply the comparative context that
			makes descriptive choices legible to readers approaching from general linguistics.
			<Xr ch="typological-profile" /> provides a condensed structural thumbnail of the whole
			language that serves as an orientation map into the granular chapters.
		</p>
	</S>

	<S t="Diachronic integration" id="diachrony">
		<p>
			Proto-Ainu reconstruction and grammaticalization pathways are embedded in the chapters
			treating the relevant synchronic phenomena rather than quarantined in a separate historical
			chapter. Where a diachronic source is established — the derivation of the evidential formal
			nouns from possessed sensory nouns, the grammaticalization of postpositions from relational
			nouns, the Sakhalin vowel-length correspondence to Hokkaido pitch accent — it is stated in
			the synchronic chapter, with forward reference to the diachrony and contact parts of the
			grammar (Parts XX–XXI). The genetic position of Ainu as a language isolate and the history
			of its internal classification are treated in <Xr ch="genetic-position-macro-comparison" />;
			the periodized history of its scholarly description is in
			<Xr ch="history-of-description-research" />.
		</p>
	</S>

	<S t="The optional formal-analysis layer" id="formal-layer">
		<p>
			Five domains of Hokkaido Ainu grammar have attracted sustained formal theoretical attention:
			alignment and the personal-affix system (see <Xr ch="alignment-split-intransitive" /> and
			<Xr ch="alignment-hierarchical-and-the-debate" />), the applicative preverb system
			(<Xr ch="applicative-system-overview" />), noun incorporation
			(<Xr ch="noun-incorporation-object" />), the nominalization-plus-copula evidential paradigm
			(<Xr ch="evidential-schema-overview" />), and clause linkage and switch-reference-like
			phenomena (<Xr ch="clause-linkage-overview" />). For these five domains the grammar provides
			an optional formal-analysis layer — explicit theoretical framing beyond the descriptive
			synthesis — clearly sectioned so that readers interested only in the description can skip it
			without losing continuity. Original re-analysis is confined to these five domains; in every
			other chapter the grammar reports, synthesizes, and triangulates the existing literature.
		</p>
	</S>
</S>

<S t="The evidence-grading system" id="evidence-grades">
	<p>
		Every nontrivial or novel claim in the grammar carries an explicit evidence-grade label. Six
		grades are in use. <em>Consensus</em> marks claims on which all major sources agree and which
		are not in dispute. <em>Contested</em> marks genuine disagreements among sources: the competing
		positions are stated, and none is declared settled. <em>Corpus-confirmed</em> marks claims
		checked against the corpus and borne out by the data. <em>Corpus-suggested</em> marks claims
		the corpus data point toward but do not conclusively establish. <em>Speculative</em> marks
		plausible but unverified hypotheses, typically diachronic ones. <em>Original-needs-review</em>
		marks new analyses proposed by the present grammar; this grade is restricted to the five formal
		domains listed in the previous section and is flagged for editorial review.
	</p>
	<p>
		Claims left ungraded are treated as consensus. A grade is placed at the end of the clause or
		sentence it scopes, written as a small-caps label in guillemets:
		‹contested›, ‹corpus-confirmed›, and so on. This is a working apparatus, not a
		rhetorical device: a contested grade commits the grammar to stating the actual positions,
		and a corpus-confirmed grade commits it to showing the relevant counts.
	</p>
</S>

