			<i lang="ain-Latn">akusu</i> while verse texts heavily favour
			<i lang="ain-Latn">awa</i>; Horobetsu and Ishikari use <i lang="ain-Latn">awa</i>
			regardless of register; Shizunai and Tokachi use <i lang="ain-Latn">akusu</i>
			<Ref k="nakagawa2024" p="345" />. Satō's characterization of <i lang="ain-Latn">awa</i>
			as the oral-literature (口承文芸) variant aligns with the verse skew in Saru
