<S t="The two particles and the question they raise" id="overview">
	<p>
		Two conjunctive particles anchor this chapter. <A w="hine" gl="and then (neutral chainer)" />
		closes a finite clause and places it before the next in temporal order, without committing to
		any causal, manner, or integrative relation between the two events.
		<A w="akusu" gl="and then (discovery)" /> closes a realized first event and opens the clause
		in which a consequence is encountered or witnessed. Both are bound, post-predicate particles —
		not verbal suffixes, not converbs — and both join fully finite clauses that carry their own
		person indexing, polarity, aspect, and any evidential tail, in the pattern established for the
		whole inventory in <Xr ch="clause-linkage-overview" />. The typological question they raise —
		whether their distribution tracks same-subject versus different-subject reference in the manner
		of switch-reference morphology <Ref k="foleyvanvalin1984" paren /> — runs through the
		descriptive literature on Ainu clause-linking. The evidence supports a discourse-semantic
		account rather than a grammaticalized subject-tracking one: neither particle carries a
		same-subject or different-subject feature, and the observed correlations with subject continuity
		follow from the particles' discourse-semantic roles rather than from an independent
		reference-tracking function.
	</p>
</S>

<S t="hine: neutral temporal enumeration" id="hine">
	<p>
		<A w="hine" /> places two events in temporal sequence and takes no stance on how they are
		related. Nakagawa's characterization is precise: <i lang="ain-Latn">hine</i> "lists events in
		time order" without any commitment to causal, manner, or purpose connection
		<Ref k="nakagawa2024" p="338" />. The bond is therefore looser than
		<A w="wa" gl="and (integrative chainer)" />, which fuses two acts into one coherent event —
		presenting the first as the method, result-state, or cause of the second — and looser than
		<A w="akusu" />, which signals a discovered or witnessed consequence
		<Ref k="nakagawa2024" p="338–339" />; <Ref k="sato2008" p="44" />; <Ref
			k="refsing1986"
			p="242"
		/>.
	</p>

	<p>
		In Saru and Chitose prose, <i lang="ain-Latn">hine</i> is the dominant chainer for the stock
		opening that establishes the narrator's household before the story begins. The formula
		<i lang="ain-Latn">… an hine oka=an</i> — one or more existence clauses strung together and
		closed by the plural existential 'we lived on' — recurs with minimal variation across the
		genre. The pattern is so regular that Refsing identifies <i lang="ain-Latn">hine</i> as more
		frequent in folk tales than in colloquial speech <Ref k="refsing1986" p="242" />:
	</p>

	<Ex
		m="a=onaha an a=unuhu an hine oka=an"
		g="4.POSS=father exist 4.POSS=mother exist CONJ exist.PL=4.S"
		tr="My father was there, my mother was there, and we lived on."
		cite="nakagawa2024:338"
		dial="SAR"
		note="Nakagawa ex. 601; a stock prose-tale opening. The three items — father, mother, living — are listed in order with hine as a pure temporal enumerator, no causal claim made."
	/>

	<p>
		Corpus texts show the same frame pivoting into the episode proper via the formula
		<i lang="ain-Latn">oka=an pe ne hike</i> 'and we lived on, and …', where the formal-noun
		connective <A w="hike" gl="and (loose link)" /> opens the narrative event:
	</p>

	<Ex
		m="rikun kanto ta a=saha i=resu hine oka=an pe ne hike"
		g="above sky LOC 4.POSS=elder.sister 4.O=raise CONJ exist.PL=4.S NMLZ COP CONJ"
		tr="My elder sister in the sky raised me, and we lived on, and …"
		cite="ilcaa1976"
		dial="SAR"
		place="川上まつ子「uwepeker 民話15」, AA研アイヌ語資料, aa-irc/022#0"
		note="The hine chain closes on oka=an pe ne hike, the standard narrative-pivot formula. The formal-noun pe and the loose-link hike then open the episode proper."
	/>

	<S t="Etymology, phonology, and dialect distribution" id="hine-form">
		<p>
			The particle derives from the nominalizer <A w="hi" gl="the fact that; the thing that" />
			plus the copula <A w="ne" gl="be, is" />: <i lang="ain-Latn">hi + ne</i> &gt;
			<i lang="ain-Latn">hine</i> (Tamura 1972b: 158, via <Ref k="refsing1986" p="243" />;
			Kindaichi 1960: 193, via <Ref k="refsing1986" p="243" />). The source material is reached
			here through intermediaries; the etymology is reported, not independently verified. It aligns
			with a wider pattern in which nominalization-plus-copula material grammaticalizes into
			clause-linkers, visible also in the evidential system (see <Xr ch="ruwe-ne-inferential" />).
		</p>

		<p>
			Phonologically, word-initial <i lang="ain-Latn">h</i> is regularly dropped in normal-speed
			speech, and the resulting vowel <i lang="ain-Latn">i</i> fuses with a preceding final
			consonant: <i lang="ain-Latn">ek hine</i> 'came-and' → [ekine];
			<i lang="ain-Latn">a=ne hine</i> → [aneine]. Ishikari, where initial
			<i lang="ain-Latn">h</i> is generally absent, writes the form
			<i lang="ain-Latn">ine</i>. In rapid speech the vowel itself may elide to bare [ne]
			<Ref k="nakagawa2024" p="338" />; <Ref k="refsing1986" p="243" />.
		</p>

		<p>
			Dialect distribution is uneven. Tokachi uses <i lang="ain-Latn">hinne</i> (e.g. Honbetsu)
			and <i lang="ain-Latn">hine</i> in alternation; Shiranuka and Kushiro write
			<i lang="ain-Latn">ine</i>; Honbetsu lacks both forms entirely, the chaining function
			falling to other resources. In Shiranuka, frequency is lower than
			<i lang="ain-Latn">wa</i> and <i lang="ain-Latn">tek</i> combined
			<Ref k="nakagawa2024" p="338" />. The whole-corpus count of approximately 14,368 tokens —
			the highest-frequency temporal-sequence particle with few competing homonyms — is a usable
			signal of the particle's prominence, with the usual caveat that Hokkaido and Sakhalin
			varieties are not separated in the aggregated count ‹corpus-suggested›.
		</p>
	</S>
</S>

<S t="akusu and awa: sequential discovery" id="akusu-awa">
	<p>
		<A w="akusu" /> and <A w="awa" /> belong to Nakagawa's group-② 'unfolding event' (事態の展開)
		connectives, alongside <A w="hike" /> and
		<A w="ayne" gl="and finally (after a protracted action)" />
		<Ref k="nakagawa2024" p="345–347" />. Their core function is to present a completed first
		event followed by a second event that comes to the participant's attention — "the anterior
		event actually happened, and then coincidentally (偶発的に) the posterior event occurred"
		<Ref k="sato2009" p="52" />. Satō classes the construction as a factual condition
		(事実的条件): the protasis is realized rather than hypothetical, and the apodosis reports the
		encountered consequence <Ref k="sato2008" p="47" />; <Ref k="sato2009" p="52" />.
	</p>

	<p>
		The discovery flavor of <i lang="ain-Latn">akusu</i> — English 'whereupon', 'and then (I
		found/saw that)' — is not a causal claim. Nakagawa makes this explicit: the causal connection
		between E1 and E2 is irrelevant to the particle; what it marks is that E2 comes to the
		attention of a participant after E1 is complete, whatever the causal structure may be
		<Ref k="nakagawa2024" p="345" />:
	</p>

	<Ex
		m="nea pon yuk a=rayke akusu"
		g="that small deer 4.A=kill CONJ"
		tr="After killing that small deer, …"
		cite="ilcaa1976"
		dial="SAR"
		place="川上まつ子「isoytak 独話1」, AA研アイヌ語資料, aa-irc/003#73"
		note="The fourth-person agent a= completes the killing; akusu opens the clause in which the narrator discovers or experiences what follows. The continuation is not preserved in the banked fragment."
	/>

	<p>
		<i lang="ain-Latn">Akusu</i> attaches after a clause-final evidential tail — confirming the
		ordering [VP]–evidential–connective established for the system as a whole. When the first
		clause closes on <i lang="ain-Latn">ruwe ne</i>, the anterior event is presented as an
		established fact before the discovery linker opens the next clause:
	</p>

	<Ex
		m="kanpi ku=sanke ru-we ne akusu itasa kanpi ek ru-we ne"
		g="letter 1SG.A=send.out track-POSS COP CONJ in.return letter come track-POSS COP"
		tr="I had sent a letter, and in return a letter came."
		cite="sato2008:47; sato2009:52"
		dial="CHI"
		note="Satō ex. 16 / Satō 2009 ex. 32. The discovery linker attaches outside the ruwe ne evidential tail. The cross-linguistic asymmetry with Japanese — factual と/たら cannot follow preceding のだ, but Ainu akusu can follow ruwe ne — is discussed in §5."
	/>

