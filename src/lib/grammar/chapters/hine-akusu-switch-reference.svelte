		<p>
			The etymology of <i lang="ain-Latn">akusu</i> as <i lang="ain-Latn">a</i> (perfective
			auxiliary) + <i lang="ain-Latn">kusu</i> (consequential) is supported by Refsing and is
			consistent with the particle's semantics — a completed event (<i lang="ain-Latn">a</i>:
			'situation done and closed') from which a consequence follows
			(<i lang="ain-Latn">kusu</i>) <Ref k="refsing1986" p="248" />. The second morpheme of
			<i lang="ain-Latn">awa</i> has not been identified with confidence
			<Ref k="refsing1986" p="248" />. Shizunai uses the phonologically reduced form
			<i lang="ain-Latn">akus</i> (word-final vowel devoicing and deletion)
			<Ref k="refsing1986" p="248" />.
		</p>

		<p>
			Whole-corpus word-form counts register approximately 3,861 tokens for
			<i lang="ain-Latn">akusu</i> and 2,071 for <i lang="ain-Latn">awa</i>. Both are among the
			cleaner particle counts with few homonyms, and the ratio is consistent with a mixed-register
			corpus containing proportionally more prose than verse ‹corpus-suggested›. Per-function
			counts that would separate Hokkaido from Sakhalin are not available from the current search
			tools.
		</p>
	</S>
</S>

<S t="The switch-reference question" id="switch-reference">
	<p>
		Switch-reference — dedicated morphological marking on clause-linkers that co-indexes subject
		continuity between clauses <Ref k="foleyvanvalin1984" paren /> — is absent from Hokkaido
		Ainu. The language has no converbs, no non-finite verb forms, and no SS/DS feature on any
		conjunctive particle <Ref k="bugaeva2022" />; <Ref k="nakagawa2024" p="514" />. What can be
		asked is whether the choice among particles statistically or semantically correlates with
		subject continuity, and whether any such correlation is grammaticalized or follows from the
		particles' discourse-semantic content.
	</p>

	<S t="Statistical tendencies and their source" id="ss-tendency">
		<p>
			The one quantified figure in the literature is Refsing's Shizunai count: approximately
			ninety per cent of the predicate pairs joined by <i lang="ain-Latn">wa</i> share a subject
			<Ref k="refsing1986" p="240" />. No comparable count exists for
			<i lang="ain-Latn">hine</i> or <i lang="ain-Latn">akusu</i> in any Hokkaido variety; the
			quantitative base is a single informant's Shizunai material (factbase §6, gap 6). The
			ninety-per-cent figure is consistent with a preference but demonstrably not with a
			constraint: Satō shows that <i lang="ain-Latn">wa</i> tolerates different-subject junctions
			without any change in form, as in the child-throws-stone / barrel-falls example where the
			thrower and the barrel are different participants — a junction Japanese
			<span lang="ja">〜て</span> cannot make <Ref k="sato2008" p="45–46" />. No primary grammar
			states a same-subject requirement for <i lang="ain-Latn">hine</i>, and its neutral-listing
			semantics entails no such restriction <Ref k="nakagawa2024" p="338" />;
			<Ref k="refsing1986" p="242" />.
		</p>

		<p>
			The simultaneity particle <A w="kor" gl="while (simultaneous)" /> provides a direct test:
			it takes different-subject complements without restriction, as in Saru
			<i lang="ain-Latn">ku=horipi kor en=nukar</i> 'she looked at me while I danced'
			<Ref k="ijas2023" />, corroborated by an oral-literature example in Satō
			<Ref k="sato2008" p="49–50" />. Shizunai <i lang="ain-Latn">kane</i> "generally" takes
			same-subject complements but exceptions are attested <Ref k="refsing1986" p="250" />. Across
			the particle inventory, any same-subject preference is a graded tendency, not a binary SS/DS
			grammatical feature.
		</p>

		<p>
			Ijäs offers the pedagogical heuristic that negated clauses take
			<i lang="ain-Latn">no</i> when the two predicates share a subject and
			<i lang="ain-Latn">wa</i> when they do not <Ref k="ijas2023" />. Nakagawa and Satō derive
			the <i lang="ain-Latn">no</i>/<i lang="ain-Latn">wa</i> choice from the aspect and polarity
			of the first predicate — stative and negated action predicates take
			<i lang="ain-Latn">no</i> — rather than from subject identity
			<Ref k="nakagawa2024" p="340–341" />; <Ref k="sato2008" p="45–47" />. The Ijäs framing is
			noted because it has entered pedagogical circulation ‹contested›; the primary descriptive
			sources do not support it.
		</p>
	</S>

	<S t="Expectedness and discourse stance" id="discourse-account">
		<p>
			Nakagawa's and Satō's analyses locate the <i lang="ain-Latn">hine</i> /
			<i lang="ain-Latn">akusu</i> contrast in event structure and narrative stance, not in
			subject continuity. The neutral temporal enumerator <i lang="ain-Latn">hine</i> naturally
			chains a single protagonist's sequential acts — producing a predominance of same-subject
			pairs without encoding subject identity. The discovery connective
			<i lang="ain-Latn">akusu</i> marks that the narrator encounters or witnesses the second
			event after the first is complete, a framing that often foregrounds a new or unexpected
			participant and thereby produces an apparent different-subject tendency. The observed SS/DS
			distributions are statistical consequences of discourse function, not evidence for a
			grammaticalized opposition <Ref k="nakagawa2024" p="338, 345" />;
			<Ref k="sato2008" p="44, 47" /> ‹consensus for the basic semantic descriptions›.
		</p>
	</S>

	<S t="The narrative boundary device" id="discourse-boundary">
		<p>
			The nearest structural analogue to a switch-reference signal is discourse-organizational
			rather than morphological. The Handbook's descriptive grammar notes a tendency for the
			sequence of the perfective auxiliary <A w="a" gl="perfective (situation complete)" />
			followed by a connective such as <A w="korka" gl="but, although" />,
			<A w="wa" />, or the formal-noun connective <A w="p" gl="thing (formal noun as connective)" />
			to fall at transitions of scene, topic, or acting participant in narrative
			<Ref k="bugaeva2022" /> ‹speculative›. The perfective marks the anterior situation as
			completed and cut off from the present; an adverb of time typically follows to open the new
			episode. The printed location of this observation has not been confirmed against the
			Handbook print edition (factbase §6, gap 5), so the claim is treated as speculative.
		</p>

		<p>
			The device is a clause-chaining boundary convention. No morpheme on the connective particle
			encodes subject identity; the signal comes from the co-occurrence of the perfective auxiliary
			with a particular linker at a discourse transition. Subject identity across a chain is
			tracked by the obligatory person affixes on each finite predicate, the mechanism drawn
			together in <Xr ch="reference-tracking" />.
		</p>
	</S>

