<S t="The evidential–connective interface" id="evidential-interface">
	<p>
		Conjunctive particles attach outside the clause-final evidential frame. The ordering
		[VP]–evidential–connective, established for the system as a whole in
		<Xr ch="clause-linkage-overview" />, means that <i lang="ain-Latn">akusu</i> can follow a
		<i lang="ain-Latn">ruwe ne</i> tail, yielding <i lang="ain-Latn">… ruwe ne akusu …</i>. The
		evidential sits above clause-internal aspect but below the clause-linker, so
		<i lang="ain-Latn">kusu ne ruwe ne</i> is grammatical and
		<i lang="ain-Latn">*ruwe ne kusu ne</i> is not <Ref k="nakagawa2024" p="393" />.
	</p>

	<p>
		Satō identifies a typologically notable property of this interface. Japanese factual conditional
		<span lang="ja">と</span> / <span lang="ja">たら</span> cannot follow a preceding
		<span lang="ja">のだ</span>, yet Ainu places no such constraint on the
		<i lang="ain-Latn">ruwe ne akusu</i> sequence: the first event is presented as
		knowledge-established fact and the discovery linker then opens the encountered consequence
		<Ref k="sato2009" p="52–53" />. The construction presents the two layers as separately
		stackable because they occupy distinct positions in the clausal hierarchy.
	</p>

	<p>
		A member of the aynu-corpora Discord community corroborates both the frequency and the
		discourse function of this sequence. nukopoli (aynu-corpora Discord, 2024) observes that
		<i lang="ain-Latn">ruwe ne</i> is associated with scene transition and summary framing in
		prose narrative, and that <i lang="ain-Latn">ruwe ne akusu</i> functions as a compound of
		that discourse anchoring with the discovery connective
		<Ref k="aynucorporadiscord" /> ‹asserted; corroborated by <Ref k="sato2009" p="52" />›. The
		observation is consistent with the evidential+linker ordering described by Nakagawa
		<Ref k="nakagawa2024" p="393" /> and with the <i lang="ain-Latn">ruwe ne</i> system set out
		in <Xr ch="ruwe-ne-inferential" />.
	</p>
</S>

<S t="Dialect edges" id="dialect">
	<p>
		The particles described above rest primarily on Saru and Chitose attestation. Several
		dialect-specific details bear on the comparison. Shizunai uses
		<i lang="ain-Latn">akus</i> (the word-final vowel devoiced and deleted) for the same discovery
		function <Ref k="refsing1986" p="248" />. Horobetsu and Ishikari favour
		<i lang="ain-Latn">awa</i> regardless of register, while prose-oriented Shizunai and Tokachi
		use <i lang="ain-Latn">akusu</i> as the default <Ref k="nakagawa2024" p="345" />. The
		category is stable across the dialect continuum; phonological shape and genre weighting shift,
		not the underlying semantic function.
	</p>

	<p>
		Tokachi uses <i lang="ain-Latn">hinne</i> alongside <i lang="ain-Latn">hine</i> for neutral
		chaining; Honbetsu Tokachi shows neither form. These micro-variations caution against
		projecting Saru frequency data onto the dialect cluster as a whole
		<Ref k="nakagawa2024" p="338" />. The full topology of the conjunctive-particle system across
		Hokkaido dialects is surveyed in <Xr ch="clause-linkage-overview" />; the conditional and
		concessive members of the system are treated in
		<Xr ch="conditional-temporal-concessive-clauses" />.
	</p>
</S>

