		The plural <i lang="ain-Latn">rok-oka</i> is the strongest internal argument that
		<i lang="ain-Latn">aan</i> is the auxiliary <i lang="ain-Latn">a</i> plus existential
		<i lang="ain-Latn">an</i>, since it pairs the plural of <i lang="ain-Latn">a</i> with the plural
		of <i lang="ain-Latn">an</i>. The singular surface forms vary by locality —
		<i lang="ain-Latn">anan</i> in Chitose, <i lang="ain-Latn">awan</i> in Horobetsu and Tokachi,
		<i lang="ain-Latn">haw'an</i> in Shizunai — and Yoshikawa grants that some invite alternative
		segmentations, so the unified <i lang="ain-Latn">a + an</i> etymology rests on the plural rather
		than on every singular form <Ref k="nakagawa2024" p="307–308" />; <Ref k="yoshikawa2022" /> ‹contested›.
