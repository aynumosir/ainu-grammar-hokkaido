	<p>
		The progressive <i lang="ain-Latn">kor an</i> joins a lexical verb, the simultaneous conjunctive
		<i lang="ain-Latn">kor</i> 'while', and the existential <i lang="ain-Latn">an</i> (plural
		<i lang="ain-Latn">oka</i>). With action verbs it marks an action in progress; with change verbs
		it marks the process leading into the change; and it covers habitual and repeated situations as
		well <Ref k="nakagawa2024" p="440–441" />; <Ref k="sato2008" p="196–197" />;
		<Ref k="bugaeva2012" p="494" />. Because the construction is a clause chain, the lexical verb and
		<i lang="ain-Latn">an</i> share the subject index:
	</p>

	<Ex
		m="kesto kesto iki=an kor an=an"
		g="every.day every.day do=4.S while exist=4.S"
		tr="I went on living, doing this day after day."
