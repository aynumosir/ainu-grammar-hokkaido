		note="Both iki and the existential an carry the fourth-person =an, the diagnostic that kor an is a chain of two finite clauses; the value here is habitual."
	/>

	<p>
		The resultative <i lang="ain-Latn">wa an</i> joins a lexical verb, the sequential conjunctive
		<i lang="ain-Latn">wa</i>, and the existential verb, and denotes the state that exists after a
		change <Ref k="nakagawa2024" p="439–444" />; <Ref k="sato2008" p="197–199" />;
		<Ref k="bugaeva2012" p="494–495" />. Satō draws the selectional line sharply: an action verb
		leaves no direct result for <i lang="ain-Latn">wa an</i> to predicate, so action perfects fall to
		the auxiliary <i lang="ain-Latn">a</i> instead, and only change and stative verbs enter
		<i lang="ain-Latn">wa an</i> <Ref k="sato2006" p="63" />; <Ref k="sato2008" p="198" />.
	</p>

	<Ex
