<S t="Tenselessness and the periphrastic architecture" id="architecture">
	<p>
		Hokkaido Ainu has no grammatical tense. Past, present, and future reference are never read off
		the shape of the finite verb; the categories the verb does engage are aspect, mood, modality,
		and evidentiality, and even these are optional rather than obligatory inflections
		<Ref k="nakagawa2024" p="434" />; <Ref k="bugaeva2012" p="491, 493" />; <Ref k="takahashi2022" />.
		Takahashi states the point for the modern reference literature: aspect and evidentiality are not
		obligatory grammatical categories in Ainu, and the perfective/imperfective opposition is not
		fixed either lexically or grammatically <Ref k="takahashi2022" />; <Ref k="yoshikawa2022" />. The
		grammatical work that other languages assign to tense and bound aspect is carried here by
		<em>periphrastic complex predicates</em>: a second finite verb, an existential verb, or the
		copula follows the lexical verb and supplies the temporal, aspectual, or modal value. In the
		terms of aspectual typology and the grammaticalization of tense–aspect–mood, the system uses
		optional analytic operators where an inflecting language would use bound morphology
		<Ref k="comrie1976" paren />; <Ref k="bybee1994" paren />. The time-reference system itself is
		treated in <Xr ch="tenselessness-and-time-reference" />; this chapter maps the forms that supply
		aspectual, modal, and illocutionary values and routes each to its detail chapter.
	</p>

	<p>
		Whether a tenseless predicate has a preferred temporal translation was debated through the
		twentieth century. Kindaichi held that Ainu makes no temporal distinction yet renders everything
		as past, the moment of speech being too fleeting to fix; Chiri narrowed the past reading to
		non-stative predicates, leaving statives to translate as a Japanese
		<span lang="ja">-te iru</span> present; and Refsing argued that a language without the category of
		tense does not order events on a linear timeline at all, so that no past translation is more
		correct than a present one <Ref k="chiri1942" p="96–97" />; <Ref k="refsing1986" p="§14.9.1" />
		‹contested›. The descriptive consensus that displaced these positions is the one stated above:
		there are no pure tense markers, and a bare finite verb takes whatever English tense its event
		type and discourse setting require <Ref k="bugaeva2012" p="491, 493" />.
	</p>

	<p>
		Three constructional frames carry almost all of this load, distinguished by their shape and by
		which verb hosts the person index. The distinction is diagnostic: in the auxiliary frame only the
		lexical verb is indexed, whereas in the pair-verb frame both predicates can be, because each
		remains a finite verb in its own right <Ref k="bugaeva2012" p="492–495" />;
		<Ref k="nakagawa2024" p="306, 439–443" />.
	</p>

