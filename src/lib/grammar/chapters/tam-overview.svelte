		note="wa an presents the standing as the state resulting from the posture-change verb as 'stand'."
	/>

	<p>
		Perception and cognition verbs such as <i lang="ain-Latn">nukar</i> 'see' and
		<i lang="ain-Latn">eramuan</i> 'understand' take either frame, and how the contrast should be
		stated is unsettled. Nakagawa's 1981 account separates <i lang="ain-Latn">nukar kor an</i> from
		<i lang="ain-Latn">nukar wa an</i> by active observation against passive coming-into-view; Satō
		rejects activeness as primary and derives the contrast from continuation of action against
		continuation of result, treating any activeness as a contextual by-product. Nakagawa concedes the
		matter needs further discussion <Ref k="nakagawa2024" p="445–446" />; <Ref k="sato2006" p="65" />
		‹contested›. A dialect-specific restriction compounds the picture: in Tokachi,
		<i lang="ain-Latn">kor an</i> does not co-occur with a first-person subject, the progressive of a
		first-person subject using <i lang="ain-Latn">kan an</i> instead; no equivalent is reported for
		the southern dialects <Ref k="takahashi2022" /> ‹contested›.
	</p>

	<p>
		Several dialectal continuatives occupy the same field. Saru and Chitose
		<i lang="ain-Latn">kane an</i> adds a noteworthy-situation nuance, while Shizunai and Samani use
		it as a plain merger of <i lang="ain-Latn">kor an</i> and <i lang="ain-Latn">wa an</i>; Tokachi
		and Ishikari have <i lang="ain-Latn">kan an</i>, and Tokachi <i lang="ain-Latn">tek an</i>
		<Ref k="nakagawa2024" p="446–449" />; <Ref k="sato2006" p="84–85" />. Yoshikawa groups these
		with <i lang="ain-Latn">wa an</i> and <i lang="ain-Latn">kor an</i> as existential aspectual
		forms — forms that may express aspectual meanings without being grammatical aspect
		<Ref k="yoshikawa2022" />. The full treatment of the progressive and resultative belongs to
		<Xr ch="existential-aspect-an-oka" />, the appearance-based and merged continuatives to
		<Xr ch="continuative-siri-siran-kane-an" />, and the iterative and habitual periphery to
		<Xr ch="phasal-iterative-habitual-aspect" />.
	</p>
</S>

<S t="Completion, anteriority, and the admirative" id="anteriority">
	<p>
		The auxiliary <i lang="ain-Latn">a</i> (plural <i lang="ain-Latn">rok</i>), grammaticalized from
		the verb <i lang="ain-Latn">a</i>/<i lang="ain-Latn">rok</i> 'sit', is the most disputed member
		of the system, and the disagreement is substantive rather than terminological. The Kindaichi–Tamura
		tradition reads it as perfective or past; Bugaeva and Satō read an actional perfect that, on
		Satō's account, cannot combine with a negative and so leaves the stative perfect to
		<i lang="ain-Latn">wa an</i>; Nakagawa, following Yoshikawa, denies it is tense or aspect at all,
		taking it to mark an event set in contrast to a following one; and Yoshikawa's handbook chapter
		analyses it as perfective with a direct-evidential, witnessed-reminiscence component
		<Ref k="bugaeva2012" p="493" />; <Ref k="sato2006" p="63" />; <Ref k="nakagawa2024" p="306–307" />;
		<Ref k="yoshikawa2022" /> ‹contested›.
	</p>

