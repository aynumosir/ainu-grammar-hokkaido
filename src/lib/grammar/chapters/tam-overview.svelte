<S t="Boundaries with evidentiality, negation, and clause linkage" id="interfaces">
	<p>
		TAM and evidentiality are adjacent systems because several aspect forms carry evidential or
		mirative values. The perfective <i lang="ain-Latn">a</i> has been analysed as direct evidential,
		<i lang="ain-Latn">aan</i> as indirect-evidential and mirative, and the situational
		<i lang="ain-Latn">siri</i> frame anchors a visible ongoing event in present time
		<Ref k="yoshikawa2022" />; <Ref k="nakagawa2024" p="435" />. The formal-noun evidential system —
		<i lang="ain-Latn">ruwe ne</i>, <i lang="ain-Latn">siri ne</i>, <i lang="ain-Latn">hawe ne</i>,
		<i lang="ain-Latn">humi ne</i> — is treated in <Xr ch="evidential-schema-overview" />, with the
		central member in <Xr ch="ruwe-ne-inferential">the inferential <i lang="ain-Latn">ruwe ne</i></Xr>.
	</p>

	<p>
		Scope ordering keeps the two systems distinct. The evidential predicate sits above clause-internal
		aspect and modality but below the clause linkers, so <i lang="ain-Latn">kusu ne ruwe ne</i> is
		well formed while <i lang="ain-Latn">*ruwe ne kusu ne</i> is not, and negation goes inside the
		frame, as in <i lang="ain-Latn">… somo ki ruwe ne</i> 'it is a fact that one does not …'
		<Ref k="nakagawa2024" p="393" />. In the ability domain negation is lexicalized as
		<i lang="ain-Latn">eaykap</i> or <i lang="ain-Latn">koyaykus</i>, and prohibition uses the
		<i lang="ain-Latn">iteki</i> adverbs, so both belong to the negation system as well as to mood.
	</p>

	<p>
		Clause linkage supplies the connective material of the periphrastic frames. The sequential
		<i lang="ain-Latn">wa</i> that chains events appears in <i lang="ain-Latn">wa an</i>,
		<i lang="ain-Latn">wa isam</i>, and <i lang="ain-Latn">wa okere</i>, and the simultaneous
		<i lang="ain-Latn">kor</i> of adverbial clauses appears in <i lang="ain-Latn">kor an</i>. When
		the second predicate keeps its person marking and verbal properties, the construction retains the
		shape of a linked predication, which is why the existential-aspect frames stand between aspect and
		clause chaining. The linking grammar these forms recruit is set out in
		<Xr ch="sequential-wa-and-clause-chaining">sequential <i lang="ain-Latn">wa</i></Xr> and
		<Xr ch="simultaneous-kor-clauses">simultaneous <i lang="ain-Latn">kor</i></Xr>.
	</p>
</S>
