	<p>
		Epistemic <i lang="ain-Latn">nankor</i> marks probability or conjecture, and with a second-person
		subject and final <i lang="ain-Latn">na</i> it softens into expectation or command
		<Ref k="nakagawa2024" p="311" />. Ijäs gives the same range — 'must have' on a past event,
