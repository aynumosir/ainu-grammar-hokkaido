<S t="Directive and hortative mood" id="directive">
	<p>
		The direct imperative to a singular addressee is the bare verb with no second-person subject
		prefix; object prefixes may remain, and adding a second-person subject affix is ungrammatical.
		A plural or polite addressee takes final <i lang="ain-Latn">yan</i>, number-sensitive verbs
		using the plural stem, and the softener <i lang="ain-Latn">hani</i> or a benefactive
		<i lang="ain-Latn">wa kore</i> 'do for (me)' turns the command into a request
		<Ref k="nakagawa2024" p="421–423" />. Ijäs's command lesson matches these points and warns
		explicitly against the second-person subject affix <Ref k="ijas2023" /> ‹consensus›.
	</p>

	<Ex
		m="hetak pusu wa inkar"
		g="come.on dig CONJ look"
		tr="Come on, dig it up and look!"
		cite="nakagawa2024:421"
		dial="CHI"
		place="Nakagawa ex. 911"
		note="The singular imperative is a chain of bare verbs; no second-person subject prefix appears."
	/>

	<p>
		Prohibition is formed by a preverbal prohibitive adverb, most often Saru and Chitose
		<i lang="ain-Latn">iteki</i>, with dialect variants <i lang="ain-Latn">itekke</i>,
		<i lang="ain-Latn">etekke</i>, and <i lang="ain-Latn">ecikki</i>. The adverb precedes both
		imperatives and indirect command forms such as <i lang="ain-Latn">kusu ne na</i> and
		<i lang="ain-Latn">nankor na</i> <Ref k="nakagawa2024" p="424–426" />.
	</p>

