	<p>
		One principle organizes the pair-verb frame: a verb phrase has exactly one person-bearing main
		verb. In <i lang="ain-Latn">ku=tere wa k=an</i> 'I am waiting', the
		<Xr ch="existential-locational-an-oka">existential verb</Xr> <i lang="ain-Latn">an</i> 'exist'
		carries the same <i lang="ain-Latn">ku=</i> as the lexical <i lang="ain-Latn">tere</i> 'wait',
		so the string is two clauses linked by <i lang="ain-Latn">wa</i> rather than a single inflected
		predicate. Nakagawa accordingly treats <i lang="ain-Latn">kor an</i> and
		<i lang="ain-Latn">wa an</i> not as aspect proper but as forms that express aspect-like meaning,
		and Yoshikawa develops the same point under the cover term "existential aspectual forms": the
		existential <i lang="ain-Latn">an</i> here is less grammaticalized than Japanese
		<span lang="ja">iru</span>/<span lang="ja">aru</span> and keeps its concrete existential sense
		<Ref k="nakagawa2024" p="442" />; <Ref k="yoshikawa2022" /> ‹contested›.
	</p>

	<p>
		The lexical material of these frames comes from the suppletive singular/plural verb pairs that
		Ijäs's Lesson 9 catalogues as special verb forms — <i lang="ain-Latn">a</i>/<i lang="ain-Latn">rok</i>
		'sit', <i lang="ain-Latn">an</i>/<i lang="ain-Latn">oka</i> 'exist',
		<i lang="ain-Latn">ek</i>/<i lang="ain-Latn">arki</i> 'come',
		<i lang="ain-Latn">arpa</i>/<i lang="ain-Latn">paye</i> 'go'
		<Ref k="ijas2023" />; <Ref k="nakagawa2024" p="142–151" />. Two of these pairs feed the TAM
		system directly: <i lang="ain-Latn">a</i>/<i lang="ain-Latn">rok</i> grammaticalizes into the
		perfective/anterior auxiliary, and <i lang="ain-Latn">an</i>/<i lang="ain-Latn">oka</i> into the
		existential-aspect frames, the number of the auxiliary tracking the number of the subject
		<Ref k="nakagawa2024" p="306–308, 439–443" />; <Ref k="bugaeva2012" p="471, 493–495" /> ‹consensus›.
	</p>
</S>

<S t="Time reference without tense" id="time-reference">
	<p>
		Temporal location is recovered from four overt resources. Time adverbs such as
		<i lang="ain-Latn">numan</i> 'yesterday' and <i lang="ain-Latn">nisatta</i> 'tomorrow' place the
		event directly; prospective and necessity forms such as <i lang="ain-Latn">kusu ne</i>,
		<i lang="ain-Latn">oasi</i> 'surely will', and <i lang="ain-Latn">etokus</i> 'be about to' yield
		future readings; the situational evidential <i lang="ain-Latn">siri</i> anchors a visible
		ongoing scene in the present; and completion forms such as <i lang="ain-Latn">a</i>,
		<i lang="ain-Latn">nisa</i>, and <i lang="ain-Latn">wa isam</i> support past translation
		<Ref k="nakagawa2024" p="434–436" />. Each yields a temporal reading only relative to the
		moment of utterance, and context can override the default.
	</p>

