		m="sine ancikar ne wa ek=an kusu ne na"
		g="one night COP CONJ come=4.S PURP COP FIN"
		tr="After one night, I will come."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker 10 [aa-irc/010#200]"
		note="kusu ne supplies the scheduled-future reading; ek is the singular 'come' stem and =an the fourth person used as the narrative first person."
	/>

	<p>
		The same prospective frame surfaces in the grammars as intention, scheduled future, and, with a
		second-person subject, near-command. Ijäs glosses it 'be going to, intend to, plan to', matching
		Nakagawa and Satō, but her pedagogical orthography writes <i lang="ain-Latn">kusune</i> as one
		word; the analysis followed here is the conjunction <i lang="ain-Latn">kusu</i> plus the copula
		<i lang="ain-Latn">ne</i>, with the one-word spelling kept as a teaching convention and carrying
		no separate morphological claim <Ref k="nakagawa2024" p="435" />; <Ref k="sato2008" p="85–86" />;
		<Ref k="ijas2023" />.
	</p>
</S>

<S t="Lexical aspect and the existential-aspect core" id="existential-aspect">
	<p>
		Before any periphrasis applies, each verb carries an inherent aspectual character that conditions
		which frame it enters. Nakagawa divides the lexicon into action, change, and stative verbs, a
		division that aligns with the actional classes Takahashi and Yoshikawa use; derivational
		Aktionsart suffixes on (often onomatopoeic) roots, such as <i lang="ain-Latn">-kosanu</i> 'happen
		once, briefly' or <i lang="ain-Latn">-natara</i> 'persist', add a lexically restricted second
		layer <Ref k="nakagawa2024" p="204–208, 438" />; <Ref k="takahashi2022" />; <Ref k="yoshikawa2022" />.
		A stative verb can also be read as the change into its state, so the same lexeme may shift class:
		<i lang="ain-Latn">poro</i> is 'be big' or 'become big', and the choice of aspect frame turns on
		that reading <Ref k="nakagawa2024" p="438" />.
	</p>

