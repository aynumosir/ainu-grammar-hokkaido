	<p>
		The admirative <i lang="ain-Latn">aan</i> (plural <i lang="ain-Latn">rokoka</i>) forms a tidy
		paradigm against <i lang="ain-Latn">a</i>. It signals that, at some reference point, the speaker
		recognizes after the fact what an earlier situation was, often with surprise; Yoshikawa analyses
		it as perfect plus indirect evidentiality, with mirativity as an extension, where
		<i lang="ain-Latn">a</i> is perfective plus direct evidentiality, the two echoing the
		direct/indirect past of Classical Japanese <span lang="ja">ki</span> and
		<span lang="ja">keri</span> <Ref k="yoshikawa2022" />; <Ref k="bugaeva2012" p="493" />.
	</p>

