		Completion has a wider periphery than <i lang="ain-Latn">a</i>. The recent perfect
		<i lang="ain-Latn">nisa</i> marks an event just finished, and the completive pair verbs
		<i lang="ain-Latn">wa isam</i> and <i lang="ain-Latn">wa okere</i> carry the lexical traces of
		<i lang="ain-Latn">isam</i> 'be absent' and <i lang="ain-Latn">okere</i> 'finish', patterning
