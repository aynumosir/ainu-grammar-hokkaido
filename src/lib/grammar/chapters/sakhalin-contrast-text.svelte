<S t="Source, speakers, and orthographic conventions" id="source">
	<p>
		The sentences glossed in this chapter come from <Ref k="murasaki2025" />, Murasaki
		Kyōko's revised introductory textbook of West-Sakhalin (Enciw) Ainu. The primary data
		were elicited at Tokoro on the Sea of Okhotsk coast, chiefly from Fujiyama Haru
		(c. 1895–1974) and Asai Take (c. 1916–1994), the two principal consultants in Murasaki's
		fieldwork programme spanning 1960–1994. A small number of illustrations also draw on
		Nagara Iso (Shiretoko/Chirijiri). The dialect represented is the western coastal
		(Raichiishka/Laichiśka) variety; minor contrasts with the slightly more southerly Maoka
		variety and the northeastern Taraika variety are noted where they bear on the comparisons
		below <Ref k="tangiku2022" p="ch. 9" />.
	</p>
	<p>
		The orthography follows Hattori's phonemic transcription
		<Ref k="hattori1964" paren />, as adopted by Murasaki. Long vowels are written double
		(<i lang="ain-Latn">aa ee oo</i> etc.); every vowel-initial syllable carries the glottal
		onset written <i lang="ain-Latn">ʼ</i>; the six permissible coda consonants are
		<i lang="ain-Latn">-h -m -n -s -w -y</i>. A person prefix is separated from its verb
		stem by <i lang="ain-Latn">=</i>. Piłsudski's 1912 Sakhalin corpus
		<Ref k="pilsudski1912" />, the earliest substantial Enciw text collection, uses a
		different Romanization; forms cited from that source elsewhere in this grammar have been
		converted to the Hattori convention following <Ref k="dalcorso2024" />. No Cyrillic
		script is reproduced. The broader typological and diachronic profile of Sakhalin Ainu
		relative to Hokkaido is developed in <Xr ch="sakhalin-and-kuril-ainu-contrast" />.
	</p>
</S>

