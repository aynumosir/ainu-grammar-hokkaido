	<p>
		Five sentences drawn from Murasaki's lesson dialogues are glossed below. Together they
		sample existential predication, attributive and locative structure, equational copula,
		analytic negation, suppletive verbal number, and the first-person object prefix. Each
		sentence was produced by Fujiyama Haru or Asai Take in elicitation sessions conducted
		by Murasaki <Ref k="murasaki2025" />.
	</p>

	<Ex
		m="teeta 'itanki 'an"
		g="here bowl exist"
		tr="There is a bowl here."
		cite="murasaki2025:16"
		dial="SA"
		place="Raichiishka; speaker Fujiyama Haru"
		note="SA teeta 'here' carries the phonemic long vowel ee; the Saru cognate locative is ta (short). 'an is the singular existential; the suppletive plural form 'okay appears in (2)."
		id="ex-teeta"
	/>

	<Ex
		m="tara too kaa-ta cikah renkayne 'okay"
		g="DEM lake above-LOC bird many exist.PL"
		tr="There are many birds on that lake."
		cite="murasaki2025:16"
		dial="SA"
		place="Raichiishka; speaker Fujiyama Haru"
		note="'okay is the suppletive plural existential (singular 'an). kaa is the position noun 'above (in contact)' in the elderly register, taking the locative suffix -ta. SA enforces strict grammatical number agreement; the singular 'an would be ungrammatical with a plural subject here."
		id="ex-okay"
