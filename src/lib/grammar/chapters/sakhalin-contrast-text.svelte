<S t="Phonological contrasts: vowel length and the final-h reflex" id="phonology">
	<p>
		The most immediately audible distinction between SA and Hokkaido (HK) Ainu is prosodic.
		Where Saru and other HK varieties carry a lexically contrastive pitch accent on stems,
		West-Sakhalin has phonemic vowel quantity — long vowels (written double) versus short
		vowels — with no accentual contrast. The two systems correspond diachronically: what HK
		encodes as a high pitch on a short vowel, SA encodes as length on that vowel
		<Ref k="itabashi2001" />; <Ref k="tangiku2022" p="ch. 9" />. A selection of cognate
		pairs from Murasaki's textbook illustrates the pattern <Ref k="murasaki2025" p="5" />:
