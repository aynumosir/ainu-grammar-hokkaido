<S t="Morphosyntactic contrasts" id="morphosyntax">
	<S t="Analytic negation: hannehka versus somo" id="negation-contrast">
		<p>
			Example (3) illustrates the SA negative copula construction. The sequence
			<i lang="ain-Latn">ka hannehka (nee)</i> negates an equational proposition:
			<i lang="ain-Latn">ka</i> is the focus particle, and <i lang="ain-Latn">hannehka</i>
			is the negative predicate, historically the copula root with the negative prefix
			<i lang="ain-Latn">han-</i> prefixed <Ref k="murasaki2025" p="11, 15" />. The same
			prefix attaches directly to verb stems for verbal negation:
			<i lang="ain-Latn">hannehka ku='oman</i> 'I did not go'. The copula
			<i lang="ain-Latn">nee</i> following the negative is frequently omitted in
			conversational speech.
		</p>
		<p>
			HK Saru negates an equational predicate with the independent negative particle
			<i lang="ain-Latn">somo</i> before the copula, yielding <i lang="ain-Latn">somo ne</i>
			(NEG COP). The HK <i lang="ain-Latn">somo</i> particle and its distribution across
			existential, copular, and verbal contexts are described in
			<Xr ch="negation-somo-standard-clausal" />. Dal Corso's study of SA negation
			<Ref k="dalcorso2025neg" /> treats the structural history of the SA
			<i lang="ain-Latn">han-</i> series and its divergence from the Hokkaido system
			in detail ‹contested›.
		</p>
	</S>

	<S t="Person and number marking" id="person-number-contrast">
		<p>
			The SA person-prefix inventory parallels HK in its core forms — first-person singular
			<i lang="ain-Latn">ku=</i> (A/S), first-person singular object
			<i lang="ain-Latn">'en=</i> — but diverges in what it assigns to the paradigm cell
			that HK labels 'fourth person' or 'indefinite'. In SA the morpheme
			<i lang="ain-Latn">'an-/='an</i> functions as first-person plural in the speech of
			younger speakers, and as first-person singular in the elderly register, the inverse of
			its value in HK <Ref k="murasaki2025" p="84–85" />; <Ref k="sakaguchi2024" />. SA
			accordingly has no productive indefinite/fourth-person paradigm of the HK type; the HK
			system is described in <Xr ch="indefinite-fourth-person-paradigm" /> and
			<Xr ch="person-marking-architecture" />.
		</p>
		<p>
			Example (4) shows the SA third-person plural subject suffix
			<i lang="ain-Latn">=hci</i> cliticizing to the verb:
			<i lang="ain-Latn">'ariki=hci</i> 'they came'. HK grammars report no cognate suffix
			in this structural position; Dal Corso's alignment study <Ref k="dalcorso2025align" />
			traces the SA morpheme to a grammaticalized pronoun and argues that the SA system
			encodes a richer nominal-tracking morphology than any Hokkaido variety. Sakaguchi
			<Ref k="sakaguchi2024" /> provides the most detailed morphological analysis of the
			SA person–number paradigm to date ‹speculative›.
		</p>
	</S>

	<S t="Suppletive verbal number" id="verbal-number-contrast">
		<p>
			SA and HK share suppletive verbal number — distinct verb stems indexed to the number of
			the core argument — but enforce it differently. In SA, grammatical number agreement is
			strict: a plural subject requires the plural verb form, with no tolerance for the
			singular at low counts that is documented in Hokkaido <Ref k="murasaki2025" p="84–85" />.
			The existential opposition in examples (1) and (2) — singular
			<i lang="ain-Latn">'an</i> versus plural <i lang="ain-Latn">'okay</i> — is cognate
			with HK <i lang="ain-Latn">an</i> versus <i lang="ain-Latn">oka=an</i>
			<Ref k="tangiku2022" p="ch. 9" />. Similarly,
			<i lang="ain-Latn">'eh</i> (singular 'come') and
			<i lang="ain-Latn">'ariki</i> (plural 'come') in example (4) are cognate with
			HK <i lang="ain-Latn">ek / arki</i>. The final long vowel in SA
			<i lang="ain-Latn">'ariki</i> versus the short vowel in HK
			<i lang="ain-Latn">arki</i> is consistent with the length-accent correspondence
			<Ref k="itabashi2001" /> noted in the preceding section. The HK suppletive verbal
			number system is described in <Xr ch="suppletive-verbal-number" /> ‹consensus›.
		</p>
	</S>
</S>
