<S t="Forms and dialect distribution" id="forms">
	<p>
		The prohibitive adverb has several Hokkaido shapes. Nakagawa reports
		<i lang="ain-Latn">iteki</i> for Saru, Chitose, Horobetsu, and Abuta;
		<i lang="ain-Latn">itekke</i> for Shizunai, Samani, and Tokachi; <i lang="ain-Latn">etekke</i>
		for Shiranuka, Bihoro, and Tokachi; and <i lang="ain-Latn">eciki</i> or
		<i lang="ain-Latn">ecikki</i> for Ishikari <Ref k="nakagawa2024" p="424" />. The forms differ
		regionally and carry no reported difference in meaning; Tokachi appears for both
		<i lang="ain-Latn">itekke</i> and <i lang="ain-Latn">etekke</i> in the source. The distribution
		rests on one witness form per locality and is best read as indicative rather than as a closed
		conditioning map ‹consensus›.
	</p>

