<S t="Position and the imperative verb" id="position">
	<p>
		Nakagawa states the positional rule more loosely for the prohibitive than for
		<i lang="ain-Latn">somo</i>: <i lang="ain-Latn">iteki</i> and its counterparts may stand anywhere
		before the verb, whereas <i lang="ain-Latn">somo</i> is in principle directly before the verb form
		<Ref k="nakagawa2024" p="424" />; <Ref k="sato2008" p="124" />. The latitude follows from
		function. Standard negation supplies polarity to a finite verb; the prohibitive adverb contributes
		directive force to the predicate phrase as a whole, and so occupies its left edge.
	</p>

	<p>
		In the bare prohibitive the addressee is the understood S or A argument and the verb carries no
		subject index. An object may still be indexed on the verb, as in <i lang="ain-Latn">iteki e yan</i>
		‘do not eat it’, where the directive subject is unexpressed and the third-person object is borne by
		the verb <Ref k="bugaeva2012" p="497" />. The person-marking facts thus run parallel to the
		affirmative imperative.
	</p>

	<Ex
		m="iteki mokor"
		g="PROH sleep"
		tr="Do not sleep."
		cite="kanazawa1898"
		dial="SAR"
		place="Kanazawa 1898; NINJAL topical dictionary, ‘Sleeping and waking’"
		note="The bluntest form: prohibitive adverb plus a bare, person-unmarked verb."
	/>

	<p>
		The prohibitive shares the politeness scale of the affirmative imperative. A plain verb is the
		most direct command; <i lang="ain-Latn">iteki</i> followed by <i lang="ain-Latn">yan</i> with a
		plural verb stem addresses a plural or respected hearer; <i lang="ain-Latn">iteki</i> with
		<i lang="ain-Latn">hani</i> softens the directive <Ref k="nakagawa2024" p="424" />; <Ref
			k="bugaeva2012"
			p="497"
		/>; <Ref k="ijas2023" />.
	</p>

	<Ex
		m="iteki neno irara yan"
		g="PROH like.that play.tricks POL"
		tr="Do not play such tricks."
		cite="nakagawa2024:424"
		dial="HOR"
		place="Nakagawa ex. 925, after Chiri 1937a:102"
		note="The final yan carries the polite or plural value that also marks affirmative directives."
	/>

	<Ex
		m="ponno ka iteki ekimatek yan"
		g="a.little even PROH be.startled POL"
		tr="Please do not be startled in the least."
		cite="kanazawa1898"
		dial="SAR"
		place="Kanazawa 1898; NINJAL topical dictionary, ‘Emotions’"
		note="The polarity item ponno ka ‘even a little’ precedes the prohibitive and is licensed within its scope."
	/>
</S>

<S t="Person-marked prohibition and the no-linker" id="no-linker">
	<p>
		Beyond the bare imperative stem, <i lang="ain-Latn">iteki</i> takes a verb that keeps its person
		affix. This is regular when a prohibited predicate is linked by <i lang="ain-Latn">no</i>
		‘without V-ing’ to a following directive, so that the prohibitive adverb scopes over a fully
		person-marked clause <Ref k="nakagawa2024" p="425" />.
	</p>

	<Ex
		m="iteki e=esina no e=ye p ne na"
		g="PROH 2SG.A=hide CONJ 2SG.A=say NMLZ COP FIN"
		tr="Say it without hiding it."
		cite="nakagawa2024:425"
		dial="SAR"
		place="Nakagawa ex. 935"
		note="Both verbs keep second-person subject indexing. The no-linked first predicate gives the prohibited manner; the p ne na frame delivers the positive directive."
	/>

	<Ex
		m="iteki i=tura no"
		g="PROH 4.O=accompany CONJ"
		tr="Do not follow me."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, ILCAA Ainu materials, uwepeker 9"
		note="The indefinite-person object i= references the narrator; the no-clause links the prohibited action to the following discourse."
	/>

	<p>
		The <i lang="ain-Latn">no</i> linkage ties the prohibitive to the asymmetry of negated
		subordinate clauses. Takahashi shows for Tokachi that a negated temporal or manner clause selects
		<i lang="ain-Latn">no</i> and blocks the successive <i lang="ain-Latn">wa</i>, because a negated
		action is construed as the static state of the action not taking place <Ref
			k="takahashi2016"
			p="75–77"
		/>. Nakagawa gives the same conditioning from the affirmative side: <i lang="ain-Latn">no</i>
		links a preceding phrase that is stative, negative in meaning, or an action verb accompanied by a
		negative element <Ref k="nakagawa2024" p="340" />. A prohibitive-headed clause is one such
		environment; the symmetry of main-clause negation and the subordinate asymmetry are set out in
		<Xr ch="negation-somo-standard-clausal" />.
	</p>

	<p>
		Ijäs formulates the classroom rule as ‘do Y without doing X’: the prohibited predicate is linked
		with <i lang="ain-Latn">no</i> and the following predicate carries the positive directive <Ref
			k="ijas2023"
		/>. Her further suggestion, that a <i lang="ain-Latn">wa</i> linker would let the prohibition
		continue across the next predicate, is offered without a supporting Hokkaido example and is left
		open ‹speculative›.
	</p>
</S>

<S t="Directive frames after iteki" id="frames">
	<p>
		<i lang="ain-Latn">Iteki</i> feeds the same clause-final directive frames as the affirmative
		command. Nakagawa lists <i lang="ain-Latn">iteki … yak pirka na</i>,
		<i lang="ain-Latn">iteki … kusu ne na</i>, <i lang="ain-Latn">iteki … p ne na</i>, and
		<i lang="ain-Latn">iteki … nankor na</i> <Ref k="nakagawa2024" p="425" />. Negative directive
		force is therefore compatible with conditional, intentive, nominalized, and modalized packaging.
	</p>

