<S t="Place in the directive system" id="system">
	<p>
		The prohibitive is the negative member of the directive system. A command not to act is formed
		with a prohibitive adverb in front of the predicate, most often <A w="iteki" gl="don't" /> in the
		Saru and Chitose dialects, answering to an English negative imperative. Nakagawa groups
		<i lang="ain-Latn">iteki</i> and its dialect counterparts as <span lang="ja">禁止の語句</span>
		‘prohibition words’, and Bugaeva glosses the construction as <span class="sc">proh</span> before
		the verb <Ref k="nakagawa2024" p="424" />; <Ref k="bugaeva2012" p="497" />. In the aligned
		Hokkaido corpus <i lang="ain-Latn">iteki</i> is the frequent, default form, with 1278 tokens
		‹corpus-confirmed›.
	</p>

	<p>
		The propositional negator <A w="somo" gl="not" /> denies a declarative clause; the prohibitive
		adverb withdraws a directive addressed to the hearer. In the typology of standard negation, which
		is defined over declarative verbal main clauses, the prohibitive falls outside the core and into
		the separate domain of the negative imperative <Ref k="miestamo2005" paren />. The two systems
		are formally distinct and can co-occur (below).
	</p>

