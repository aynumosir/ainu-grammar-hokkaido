	<p>
		The conditional element <i lang="ain-Latn">yak</i> underlies two adjacent constructions that must
		be kept apart. In the directive frame <i lang="ain-Latn">iteki … yak pirka na</i> the conditional
		<i lang="ain-Latn">yak</i> ‘if’ combines with <i lang="ain-Latn">pirka</i> ‘be good’ to deliver a
		command. The concessive <i lang="ain-Latn">yakka</i> ‘even if’ instead builds the permission frame
		<i lang="ain-Latn">yakka pirka</i> ‘it is all right even if’, and a permission is negated by the
		propositional <i lang="ain-Latn">somo</i>, giving ‘you need not V’ rather than a command. The
		division follows the boundary between propositional negation and negative directive force; the
		illustrating sentence is constructed in Ijäs’s pedagogical text <Ref k="ijas2023" />.
	</p>

	<Ex
		m="somo e=e yakka pirka"
		g="NEG 2SG.A=eat even.if good"
		tr="You need not eat."
		cite="ijas2023"
		constructed
		dial="HK"
		note="Ijäs Lesson 10. The permission frame yakka pirka is negated with somo, not with iteki; the result is a release from necessity, not a command."
	/>
</S>

<S t="Co-occurrence with somo" id="somo">
	<p>
		<i lang="ain-Latn">Iteki</i> and <i lang="ain-Latn">somo</i> are not in complementary
		distribution. Nakagawa’s <i lang="ain-Latn">iteki pasirota somo eci=ki nankor na</i> carries both
		the prohibitive adverb and the propositional negator in one clause <Ref k="nakagawa2024" p="425" />.
		The prohibitive directs the addressee away from an action; the periphrastic
		<i lang="ain-Latn">somo ki</i> adds clausal negation of the embedded proposition. Each layer
		contributes its own value, which is why <i lang="ain-Latn">iteki</i> cannot be reduced to an
		allomorph of the standard negator.
	</p>

	<Ex
		m="iteki pasirota somo eci=ki nankor na"
		g="PROH scold NEG 2PL.A=do probably FIN"
		tr="You are certainly not to scold (him)."
		cite="nakagawa2024:425"
		dial="SAR"
		place="Nakagawa ex. 932"
		note="The notional verb pasirota ‘scold, revile’ is negated through the somo ki periphrasis, and eci=ki hosts the second-person plural index; iteki frames the whole as a directive, with the modal nankor na giving the emphatic ‘certainly not’."
	/>
</S>

<S t="Apprehensive warnings" id="apprehensive">
	<p>
		Hokkaido Ainu has a set of warning adverbs adjacent to the prohibitive:
		<i lang="ain-Latn">ika</i>, <i lang="ain-Latn">ikiya</i>, <i lang="ain-Latn">ikia(n)kunak</i>,
		<i lang="ain-Latn">inkian</i>, and the elevated <i lang="ain-Latn">ikineypeka</i> <Ref
			k="nakagawa2024"
			p="425"
		/>. They express a cautionary ‘lest, be careful not to’ value, and the verb after them takes
		ordinary subject person marking, which separates the construction from the bare imperative even
		where the discourse force approaches a prohibition.
	</p>

	<Ex
		m="ikiya e=hacir na"
		g="ADM 2SG.S=fall FIN"
		tr="Be careful not to fall."
		cite="tamura2000:177"
		dial="SAR"
		note="Reproduced in Ijäs Lesson 10. The second-person subject is indexed on the verb e=hacir, unlike the unindexed verb of a bare prohibitive."
	/>

	<p>
		The apprehensive warns that an unwanted event may occur; the prohibitive commands the addressee
		not to act. Where <i lang="ain-Latn">iteki mokor</i> says ‘do not sleep’, an
		<i lang="ain-Latn">ikiya</i> clause with a person-marked predicate cautions against an outcome
		outside the addressee’s direct control <Ref k="nakagawa2024" p="425" />.
	</p>
</S>
