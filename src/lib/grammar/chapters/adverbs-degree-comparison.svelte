<S t="Postpositional adverbs" id="postpositional-adverbs">
	<p>
		Postpositional adverbs (後置副詞) take a nominal antecedent — typically the
		immediately preceding NP — and carry the objective person prefix when the
		antecedent is pronominal <Ref k="nakagawa2024" p="280" />. Two diagnostics
		distinguish them from case postpositions: they can stand independently at the head
		of a clause, and they do not require a <em>〈place〉</em>-marked antecedent,
		unlike the locative case particles
		<A w="ta" gl="LOC (at, in)" />, <A w="wa" gl="ABL (from)" />, and
		<A w="peka" gl="PERL (through)" />
		<Ref k="nakagawa2024" p="281–282" />. Compare:
		<i lang="ain-Latn">ruyka kari</i> 'across the bridge' (postpositional adverb
		<A w="kari" gl="through, across" /> with a bare noun antecedent; no
		<em>〈place〉</em> required) vs
		<i lang="ain-Latn">riyopa or peka</i> (the perlative case particle
		<A w="peka" gl="PERL (through)" /> requires the <em>〈place〉</em>-marked
		locative noun <A w="or" gl="inside, place of" />)
		<Ref k="nakagawa2024" p="281–282" />.
	</p>
