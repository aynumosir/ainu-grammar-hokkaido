	/>

	<p>
		The negators <A w="somo" gl="not" /> and <A w="iteki" gl="do not" /> differ
		syntactically. <A w="somo" gl="not" /> is the standard clausal negator (treated in
		<Xr ch="negation-somo-standard-clausal" />); <A w="iteki" gl="do not" /> operates
		in the prohibitive domain covered by <Xr ch="negation-prohibitive-iteki" />. A
		community-observed distinction further separates the adverb
		<A w="ikiya" gl="be careful not to, lest" />, which licenses normal person marking
		on its complement clause (<i lang="ain-Latn">ikiya seta e=na</i> 'be careful not
		to bite the dog'), from <A w="iteki" gl="do not" />, which interacts differently
		with the following verbal complex and its person-prefix slot (nukopoli,
		aynu-corpora Discord 2025; <Ref k="aynucorporadiscord" />) ‹contested›.
	</p>
	<p>
		The adverb <A w="patek" gl="only, just" /> provides an instructive contrast
		with Japanese <span lang="ja">だけ</span>: <A w="patek" gl="only" /> patterns as
		a pure adverb and cannot head a nominal phrase, whereas Japanese
		<span lang="ja">だけ</span> is a grammaticalized noun. Community corpus analysis
		records the traditional order <i lang="ain-Latn">ani patek</i> 'with only this'
		in classical texts (one token against zero of the reversed
		<i lang="ain-Latn">patek ani</i>), with near-equal counts of both orders in
		modern learner material; the reversal is attributed to nominal influence from
		Japanese (nukopoli, aynu-corpora Discord 2024;
		<Ref k="aynucorporadiscord" />) ‹corpus-suggested›.
	</p>
</S>

<S t="Deverbal adverbs: the -no adverbializer and bare conversion" id="deverbal-adverbs">
	<p>
		The suffix <A w="-no" gl="adverbializer" /> attaches to intransitive verbs —
		most productively to property verbs — to derive manner and degree adverbs:
		<A w="pirka" gl="be good" /> → <i lang="ain-Latn">pirka-no</i> 'well, nicely';
		<A w="moyre" gl="be gentle, be slow" /> → <i lang="ain-Latn">moyre-no</i>
		'gently, slowly'; <A w="tunas" gl="be quick" /> →
		<i lang="ain-Latn">tunas-no</i> 'quickly'. The resulting forms are adverbs with
		arity zero and no person affix. Ijäs introduces the most frequent outputs in her
		pedagogical grammar <Ref k="ijas2023" />; the phonological details of the suffix,
		the relationship to the variant <A w="-nu" gl="adverbializer variant" />, and the
		depictive and resultative readings of -no adverbials belong to
		<Xr ch="adverbializer-no" />. The direct consequence for degree is that any
		property verb can furnish a degree adverb without the property-word becoming a
		distinct category:
	</p>

	<Ex
		m="tunas-no pirka"
		g="be.quick-ADV be.good"
		tr="Get well quickly!"
		ain="tunasno pirka"
		cite="shibatani1990:19"
		dial="HK"
		note="Shibatani ex. 13 area. The -no adverbial tunas-no 'quickly' modifies the inchoative imperative pirka 'become well/healthy'. Property verbs serve directly as imperatives, exploiting the stative-to-inchoative polysemy common to Ainu intransitive verbs."
	/>

	<p>
		A second group of intransitive verbs converts to adverbs without any suffix. The
		most frequently attested members of this bare-conversion pattern are
		<A w="tunas" gl="be quick; quickly" />,
		<A w="moyre" gl="be gentle; gently" />,
		<A w="hanke" gl="be near; nearby" />, and
		<A w="tuyma" gl="be far; far away" />
		<Ref k="nakagawa2024" p="273" />. Bare and <i lang="ain-Latn">-no</i> forms often
		co-occur in the same dialect without semantic contrast; the choice may reflect
		prosodic context or register ‹corpus-suggested›.
	</p>
	<p>
		Transitive verb conversion produces a third group, whose members surface mainly as
		postpositional adverbs (e.g., <A w="ani" gl="with, by means of" />,
		<A w="tura(no)" gl="together with" />) and are documented in the following section
		<Ref k="nakagawa2024" p="280–281" />. Directional adverbs derived from locative
		roots are treated immediately below.
	</p>

