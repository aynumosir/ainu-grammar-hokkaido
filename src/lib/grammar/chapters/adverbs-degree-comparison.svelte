<S t="Degree and intensification" id="degree-intensification">
	<p>
		Degree in Hokkaido Ainu is expressed through lexical degree adverbs, through
		property verbs themselves (which predicate directly as intransitives), and in some
		contexts through reduplication. The lexical degree adverbs
		<A w="sonno" gl="truly, really" /> and <A w="earkinne" gl="very, extremely" />
		precede the modified predicate exactly as manner adverbs do:
		<i lang="ain-Latn">sonno poro</i> 'truly big / really large',
		<i lang="ain-Latn">earkinne pirka</i> 'very beautiful'
		<Ref k="nakagawa2024" p="273" />.
	</p>
	<p>
		At the lower end of degree, <A w="pon" gl="be small" /> — a stative property verb
		meaning 'be small' — is used prenominally (<i lang="ain-Latn">pon hekaci</i>
		'small child', <Ref k="nakagawa2022pos" p="475" />) and, in its adverbial
		bare-conversion use, as a diminutive degree modifier meaning 'a little, slightly'
		‹corpus-suggested›. Its logical counterpart in the degree domain is
		<A w="poro" gl="be big, be great" />, which in composition and derivation conveys
		augmentative force; neither is a bound degree morpheme.
	</p>
	<p>
		Reduplication of property verbs and ideophonic forms yields intensified or
		expressive degree readings — the patterns are analysed together with the broader
		reduplication system in <Xr ch="reduplication-phonology-rule-interaction" />.
	</p>
	<p>
		The superlative degree adverb <A w="iyotta" gl="most of all, above all" />
		immediately precedes the modified property predicate:
		<i lang="ain-Latn">iyotta tanne</i> 'the longest',
		<i lang="ain-Latn">iyotta pirka</i> 'the most beautiful'
		<Ref k="tamura1996" p="s.v. iyotta" />. No comparative morpheme competes for this
		slot, so <A w="iyotta" /> bears the superlative function without formal ambiguity
		with a comparative form.
	</p>
</S>

<S t="Comparison: the akkari strategy and the absence of a morphological comparative" id="comparison">
	<p>
		Hokkaido Ainu has no dedicated comparative morpheme — no suffix or particle whose
		sole function is to inflect a property predicate for comparison. The analysis
		follows directly from the no-adjective-class thesis: because property-concept words
		are intransitive verbs, they carry no adjectival inflection and therefore no
		comparative inflection
		<Ref k="nakagawa2024" p="118" />; <Ref k="nakagawa2022pos" p="474" />. In
		Stassen's cross-linguistic survey of comparison strategies
		<Ref k="stassen1997" paren />, this places Ainu in the large type that expresses
		comparison via a separate particle or postpositional element rather than by
		morphological marking on the predicate.
	</p>
	<p>
		The primary comparison construction uses the postpositional adverb
		<A w="akkari" gl="more than, exceeding" /> (表71 above) as the standard-of-comparison
		marker. The standard NP immediately precedes
		<A w="akkari" gl="more than" />; when the standard is pronominal, the objective
		prefix attaches to <A w="akkari" gl="more than" /> in the usual postpositional
		pattern:
	</p>

	<Ex
		m="tan cep en=akkari poro"
		g="this fish 1SG.O=more.than be.big"
		tr="This fish is bigger than me."
		constructed
		note="Illustrates the akkari comparison construction: [subject-NP] [standard-O=akkari] [property-verb]. The standard 'me' is encoded by the 1SG.O prefix en= on akkari rather than by a postposition or case particle on a separate NP. With a noun standard: tan cep seta akkari poro 'This fish is bigger than a dog', with seta 'dog' as bare antecedent without any marking."
	/>

	<p>
		<A w="akkari" gl="more than" /> carries the full comparative meaning; the property
		verb itself (<A w="poro" gl="be big" />, <A w="tanne" gl="be long" />, etc.) is
		unchanged from its predicative form. Comparison is thus encoded entirely in the
		postpositional layer rather than in predicate morphology.
	</p>
	<p>
		For equative comparison ('as … as'), the postpositional adverbs
		<A w="koraci" gl="like, in the manner of" /> and <A w="neno" gl="like" /> (basic
		type, 表70) serve as the standard marker:
		<i lang="ain-Latn">seta koraci poro</i> 'as big as a dog'
		<Ref k="nakagawa2024" p="280" />. The overlap between the equative 'as … as' and
		the manner 'like' reading of <A w="koraci" gl="like" /> is common in languages
		that use manner adverbs for equative comparison, and has not been distinguished
		as a formal ambiguity in the sources read for this chapter ‹speculative›. Degree
		and extent comparison using
		<A w="pak(no)" gl="until, to the extent that" /> — yielding proportional and
		scalar clauses — is treated in <Xr ch="manner-and-degree-adverbial-clauses" />.
	</p>
	<p>
		Juxtaposition without an explicit standard marker also operates as a comparison
		strategy in discourse contexts where the contrasted entities have been introduced:
		two property predicates in adjacent clauses can be read as comparative without any
