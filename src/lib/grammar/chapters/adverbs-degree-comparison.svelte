		The degree adverbs <A w="sonno" gl="truly" /> and <A w="sino" gl="truly" /> are
		cross-class items: both also function as general prenominals meaning 'real, true'
		when preceding a noun phrase (<i lang="ain-Latn">sonno menoko</i> 'a real woman')
