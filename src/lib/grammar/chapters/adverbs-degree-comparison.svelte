		With a pronominal antecedent, the objective prefix attaches to the postpositional
		adverb: <i lang="ain-Latn">en=akkari</i> 'more than me',
		<i lang="ain-Latn">en=sam</i> 'beside me',
		<i lang="ain-Latn">un=koraci</i> 'like us'. The same
		<i lang="ain-Latn">en=</i> / <i lang="ain-Latn">un=</i> series that marks
		objects of transitive verbs appears here, confirming the objective rather than
		nominative slot <Ref k="nakagawa2024" p="280–281" />. With a noun antecedent the
