<S t="The adverb class" id="adverb-class">
	<p>
		Hokkaido Ainu adverbs modify verb phrases, carry arity zero — they neither serve as
		predicate heads nor fill argument slots — and take no nominative person prefix
		<Ref k="nakagawa2024" p="273" />; <Ref k="nakagawa2022pos" p="473" />. The class
		boundary with prenominals (連体詞) turns on target: adverbs attach to the VP,
		prenominals to the NP, and prenominals additionally bear no person prefix of any
		kind <Ref k="nakagawa2024" p="116" />. Postpositional adverbs form a subclass that
		co-indexes a nominal antecedent; when that antecedent is pronominal the
		postpositional adverb receives an <em>objective</em> person prefix — a morphological
		signature shared with <Xr ch="relational-and-spatial-nouns">locative nouns</Xr> and
		distinct from verbal person inflection <Ref k="nakagawa2024" p="273, 280" />.
	</p>
	<p>
		Because this grammar assigns no adjective class to property-concept words — treating
		them as stative intransitive verbs (see <Xr ch="property-concept-predication" />) —
		the adverb–verb boundary matters directly for the description of manner and degree.
		A property verb such as <A w="pirka" gl="be good, be well" /> predicates with full
		person marking; the deadverbal form <i lang="ain-Latn">pirka-no</i> 'well, nicely'
		derived from it carries arity zero and no person affix. Kindaichi, who did posit an
		adjective class, treated these same forms as adjective-derived adverbials
		<Ref k="kindaichi1936" p="123–124" />; in the modern analysis the derivation is
		uniform verb → adverb <Ref k="nakagawa2022pos" p="474" />.
	</p>
	<p>
		Nakagawa identifies five formation routes for adverbs proper, plus a distinct
		postpositional subclass treated below <Ref k="nakagawa2024" p="273–282" />. The
		five are: (i) basic/lexical adverbs (本来型); (ii) intransitive verb + the suffix
		<A w="-no" gl="adverbializer" />; (iii) bare intransitive conversion; (iv)
		transitive verb conversion; and (v) directional formations built on
		<Xr ch="relational-and-spatial-nouns">locative roots</Xr> with pronominal prefixes
		and directional suffixes. The full account of the <A w="-no" gl="adverbializer" />
		suffix belongs to <Xr ch="adverbializer-no" />; the present chapter surveys its
		output alongside the other types, then the degree and comparison systems.
	</p>
	<p>
		Satō additionally distinguishes a demonstrative adverb subtype (指示副詞) and an
		interrogative subtype (疑問副詞) alongside the basic and postpositional divisions
		<Ref k="sato2008" p="2" />; Nakagawa subsumes both into the basic and prenominal
		classes respectively <Ref k="nakagawa2024" p="273" />. Refsing, working on
		Shizunai, groups adverbs with conjunctions and interjections in a single particle
		category <Ref k="refsing1986" p="74–76" /> ‹contested›. Nakagawa and Satō instead
		distribute conjunctive-meaning items between conjunctive particles (接続助詞) and
		adverbs, leaving no independent conjunction class
		<Ref k="nakagawa2024" p="116–117" />; <Ref k="sato2008" p="3" />.
	</p>
</S>

