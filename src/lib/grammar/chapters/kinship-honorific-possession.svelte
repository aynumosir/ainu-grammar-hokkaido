<S t="Dialect variation in kinship terms" id="dialect-variation">
	<p>
		The kinship lexicon shows some of the widest dialect spread in Hokkaido Ainu, and the
		construction assignments in each dialect interact with local lexical choices
		<Ref k="nakagawa2024" p="223–224" />.
	</p>
	<p>
		<strong>'Father'</strong> is the most variable kin term. The oldest reconstructed form is
		<i lang="ain-Latn">aca</i> (affiliative-construction; current in Sōya and Samani);
		<i lang="ain-Latn">mici</i> (concept-construction) is the baseline for Horobetsu,
		Shizunai, Tokachi, Kushiro, Bihoro, and main Saru; <i lang="ain-Latn">iyapo</i> appears
		in lower Saru; <i lang="ain-Latn">hampe</i> in Ishikari; and <i lang="ain-Latn">hapo</i>
		in Chitose. Where <i lang="ain-Latn">hapo</i> means 'father', 'mother' shifts to
		<i lang="ain-Latn">totto</i> to avoid homonymy with the widespread
		<i lang="ain-Latn">hapo</i> 'mother' <Ref k="nakagawa2024" p="223" />. The community
		member thegodofneet (aynu-corpora Discord, 2026-06-08) cites Chiri's dialect survey for
		the same mapping and notes that <i lang="ain-Latn">hapo</i> extends further to 'elder
		sibling' in Kuril varieties <Ref k="aynucorporadiscord" /> ‹corpus-confirmed›.
	</p>
	<p>
		<strong>Eastern Hokkaido fusion forms.</strong> In Tokachi the compounds
		<i lang="ain-Latn">konnarpe</i> 'aunt' (&lt; <i lang="ain-Latn">kor unarpe</i>),
		<i lang="ain-Latn">koracapo</i> 'uncle', and <i lang="ain-Latn">koruci</i> 'grandmother'
		have grammaticalised to the point where internal structure is opaque; 'so-and-so's aunt'
		then requires <i lang="ain-Latn">kor konnarpe</i>, treating the formerly bimorphemic form
		as a simplex concept-form noun <Ref k="nakagawa2024" p="223, ex.257" /> ‹speculative›.
	</p>
	<p>
		<strong>The younger-sister asymmetry</strong> reflects the bilateral-descent kinship system.
		'Younger brother' is <i lang="ain-Latn">ak</i> across Hokkaido; 'younger sister of a
		sister' is <i lang="ain-Latn">matak</i> (all-Hokkaido, from
		<i lang="ain-Latn">mat-ak</i> 'female younger-sibling'). 'Younger sister of a brother'
		varies: Saru uses <i lang="ain-Latn">matapa</i> (from <i lang="ain-Latn">mat-apa</i>,
		denoting a more distant relation), Chitose uses <i lang="ain-Latn">macirpe</i>, and
		oral-narrative tradition generally uses <i lang="ain-Latn">tures(i)</i>
		<Ref k="nakagawa2024" p="223–224" />.
	</p>
	<p>
		The form <i lang="ain-Latn">onneke(he)</i> 'mother / parent', found in the Tokachi
		(Honbetsu) and Nemuro varieties, and <i lang="ain-Latn">onneike</i> 'parent' in Asahikawa,
		follows the standard type-① affiliative pattern:
		<i lang="ain-Latn">onneke→onnekehe</i>. The community member antitwilight (aynu-corpora
		Discord, 2024-11-15) proposes the etymology <i lang="ain-Latn">onne</i> 'old' +
		<i lang="ain-Latn">-ike</i> 'person', with semantic narrowing from 'the elder [side of the
		household]' to 'parent' and further to 'mother' in some varieties
		<Ref k="aynucorporadiscord" /> ‹speculative›.
	</p>
	<p>
		Across these variants the construction assignment — concept-set or affiliative-set — is
		stable within each dialect: variation affects which lexical item is chosen, not the
		morphosyntactic class it belongs to ‹consensus›.
	</p>
