<S t="Kinship terms and the two possession constructions" id="overview">
	<p>
		Kinship terms in Hokkaido Ainu are <Xr ch="nominal-subclasses-and-bound-nouns">bound
		nouns</Xr>: they do not occur without possessive morphology in ordinary prose. Like body
		parts, they are obligatorily possessed. Unlike body parts, they split between the two
		possession constructions that organise the inalienable–alienable divide: some kin terms
		take the <Xr ch="concept-form-affiliative-form">affiliative form</Xr> with a direct
		person-prefix (<i lang="ain-Latn">ku=onaha</i> 'my father'), while others remain in the
		concept form governed by the transitive verb <A w="kor" gl="have" />
		(<i lang="ain-Latn">ku=kor hapo</i> 'my mother') <Ref k="nakagawa2024" p="221–226" />;
		<Ref k="sato2008" p="151" />. The assignment is lexically fixed and does not vary freely
		within a single dialect.
	</p>
	<p>
		Bugaeva places kinship terms alongside body parts as the core of the
		obligatorily-possessed, inalienably-marked class in Ainu, yet the construction split shows
		that "inalienable" covers two morphosyntactically distinct sub-patterns rather than a single
		uniform category <Ref k="bugaeva2021poss" p="52–54" />. The general morphosyntax of both
		constructions and the affiliative-suffix morphophonology are treated in
		<Xr ch="adnominal-possession-double-marking" /> and
		<Xr ch="affiliative-suffix-morphophonology" />; the present chapter covers what is specific
		to kinship and to the honorific use of the 4th-person possessive.
	</p>
</S>

<S t="The affiliative-construction paradigm" id="two-paradigms">
