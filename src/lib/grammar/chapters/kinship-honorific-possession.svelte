	<p>
		The affiliative-suffix allomorphs follow the same phonological patterns as body-part nouns,
		documented in <Xr ch="affiliative-suffix-morphophonology" />. Vowel-final stems (type ①:
		<i lang="ain-Latn">ona</i>, <i lang="ain-Latn">unu</i>, <i lang="ain-Latn">sa</i>,
		<i lang="ain-Latn">po</i>) take the optional suffix <i lang="ain-Latn">-hV₁</i> for the
		long form — <i lang="ain-Latn">ona→onaha</i>, <i lang="ain-Latn">unu→unuhu</i>. Consonant-final
		stems mostly follow type ③-1 (<i lang="ain-Latn">-i(hi)</i>):
		<i lang="ain-Latn">yup→yupi(hi)</i>, <i lang="ain-Latn">sut→suci(hi)</i>,
		<i lang="ain-Latn">mat→maci(hi)</i>, <i lang="ain-Latn">tures→turesi(hi)</i>. For
		vowel-final stems the short affiliative is formally identical to the concept form; in the
		3rd person a person-prefix is the only overt marker of the possessed reading
		<Ref k="nakagawa2024" p="227" /> ‹consensus›.
	</p>
	<p>
		The two address affiliatives <i lang="ain-Latn">yupo</i> and <i lang="ain-Latn">sapo</i>
		carry an <i lang="ain-Latn">-o</i> ending not predictable from the standard suffix classes.
		Nakagawa lists them as lexically specified address forms without deriving the
		<i lang="ain-Latn">-o</i> morpheme synchronically <Ref k="nakagawa2024" p="225–226" />.
	</p>
</S>

<S t="Address versus reference: the vocative split" id="address-reference">
	<p>
		Seven affiliative-construction terms — <i lang="ain-Latn">ona</i>,
		<i lang="ain-Latn">unu</i>, <i lang="ain-Latn">sut</i>, <i lang="ain-Latn">ekas</i>,
		<i lang="ain-Latn">aca</i>, <i lang="ain-Latn">yup</i>, <i lang="ain-Latn">sa</i> — are
		restricted to third-person reference and narrative use; they cannot be used to address the
		relative directly <Ref k="nakagawa2024" p="224" />. For elder siblings the address forms
		are <i lang="ain-Latn">yupo</i> and <i lang="ain-Latn">sapo</i> with
		<i lang="ain-Latn">ku=</i>; for parents and grandparents the concept-construction terms
		(<i lang="ain-Latn">mici</i>, <i lang="ain-Latn">hapo</i>, <i lang="ain-Latn">ekasi</i>,
		<i lang="ain-Latn">huci</i>) serve as vocatives <Ref k="nakagawa2024" p="225–226" />.
	</p>

	<Ex
		m="ku=sapo he"
		g="1SG.A=elder.sister FIN"
		tr="Sis!"
		cite="nakagawa2024:226"
		dial="CHI"
		note="Nakagawa ex. 263. Chitose direct address to elder sister using the specialised address affiliative sapo. The reference form ku=saha is not used vocatively."
	/>

	<Ex
		m="ku=yupo"
		g="1SG.A=elder.brother"
		tr="Elder brother!"
		cite="nakagawa2024:226"
		dial="HK"
		note="Nakagawa ex. 264. Direct address to elder brother; the reference affiliative ku=yupihi is not used as a vocative."
	/>

	<p>
		Satō confirms <i lang="ain-Latn">ku=sapo</i> and <i lang="ain-Latn">ku=yupo</i> as the
		securely attested direct-address (対称詞) forms; Nakagawa additionally reports
		<i lang="ain-Latn">ku=mataki</i> and <i lang="ain-Latn">ku=matkarku</i> as vocatives to
		younger kin, but their distribution is less fully documented
		<Ref k="sato2008" p="151" />; <Ref k="nakagawa2024" p="226, ex.265" /> ‹contested›.
	</p>
	<p>
		The split is grammatical, not a register choice: the construction assigned to a kin noun
		determines what address form is even possible <Ref k="nakagawa2024" p="228" />. Narrative
		formulae use the reference forms in story-openings, where the logophoric narrator lists
		family members:
	</p>

	<Ex
		m="a=ona-ha an a=unu-hu an hine oka=an"
		g="4.A=father-POSS exist 4.A=mother-POSS exist CONJ exist.PL=4.S"
		tr="My father was there, my mother was there, and we lived."
		cite="nakagawa2024:225"
		dial="SAR"
		note="Nakagawa ex. 261. Standard Saru story-opening formula; the 4th-person A-prefix a= functions as the logophoric narrator's first person. Both ona→onaha and unu→unuhu are affiliative-construction terms; the concept-form terms mici/hapo do not appear in this referential slot."
	/>
</S>

<S t="Obligatory possession, the 4th-person default, and the kin-group noun utar" id="obligatory-possession">
	<p>
		Whether a kin term belongs to the concept-construction or the affiliative-construction set,
		it does not appear bare in prose; every token carries possessive morphology. When no
		discourse-salient possessor is active — in riddles, generic statements, or elicitation —
		the 4th-person A-prefix <i lang="ain-Latn">a=</i> supplies a default unspecified-possessor
		value. Bugaeva reports that kin and body-part forms elicited in isolation are supplied by
		speakers with a 3rd- or 4th-person index and the possessive suffix, never as a bare stem
		<Ref k="bugaeva2021poss" p="53" />.
	</p>
	<p>
		The general kin-group noun <A w="utar" gl="family, companions, kin-group members" /> takes
		the affiliative form <i lang="ain-Latn">utari(hi)</i> —
		<i lang="ain-Latn">k=utari</i> 'my household',
		<i lang="ain-Latn">a=utari</i> 'one's family (narrator's)' — and is among the
		higher-frequency kinship-domain tokens in the corpus
		<Ref k="nakagawa2024" p="251–252" />. The community member thegodofneet (aynu-corpora
		Discord, 2025-09-17) observes that the affiliative naturally narrows to a possessed reading
		because a kin-group not belonging to anyone is conceptually difficult to express; this
		parallels the bound-noun tendency across kin vocabulary
		<Ref k="aynucorporadiscord" /> ‹contested›. Community member nukopoli (aynu-corpora
		Discord, 2026-06-15) further notes that the compounds
		<i lang="ain-Latn">onautari</i> and <i lang="ain-Latn">unuutari</i>, literally 'the
		father-group' and 'the mother-group', can designate 'parents' collectively
		<Ref k="aynucorporadiscord" /> ‹contested›.
	</p>
</S>

<S t="Honorific possession via the 4th person" id="honorific-possession">
	<p>
		The 4th-person prefix <i lang="ain-Latn">a=</i> carries three uses that converge on kin
		affiliatives: (i) the unspecified-possessor default described above; (ii) the logophoric
		narrator first person in oral narrative; and (iii) an honorific use, where
		the speaker marks an esteemed third-party possessor — a deity (<i lang="ain-Latn">kamuy</i>),
		a divine ancestor, or a high-status figure — with <i lang="ain-Latn">a=</i> rather than
		the zero 3rd-person form, treating the referent as too elevated for the ordinary 3rd-person
		default <Ref k="nakagawa2024" p="174–179" />; <Ref k="tamura2000" paren />. The three
		values are formally identical; context, genre, and the referent's status together determine
		the reading. The honorific-possession use is most consistent in kamuy yukar and ritual-register
		texts, where divine possessors regularly appear in kinship constructions with
		<i lang="ain-Latn">a=</i>. This is one reason the frequency of
		<i lang="ain-Latn">a=</i> with kin affiliatives in the oral-literature corpus is
		conspicuously higher than pure third-person reference would produce alone. The full treatment
		of the 4th-person paradigm and its logophoric and honorific functions is in
		<Xr ch="indefinite-fourth-person-paradigm" /> and <Xr ch="fourth-person-honorific" />;
		the register dimension belongs to <Xr ch="honorific-ritual-and-taboo-registers" />.
	</p>
	<p>
		The following example from the ILCAA Shizunai corpus shows the logophoric-narrator value
		of <i lang="ain-Latn">a=</i>/<i lang="ain-Latn">an=</i> with stacked kinship affiliatives
		and a possessed voice noun in a single clause:
	</p>

	<Ex
		m="nekon ne wa a=yupi-hi an=sa-ha haw-ehe ka isam ruwe ne"
		g="how COP CONJ 4.A=elder.brother-POSS 4.A=elder.sister-POSS voice-POSS also not.exist track-POSS COP"
		tr="How is it that my elder brother's and my elder sister's voice is not heard?"
		cite="ilcaa1976"
		dial="SHI"
		place="Oda Steno, ILCAA Ainu materials, Shizunai [aa-irc/017#52]; source: 織田ステノ, uwepeker 'Moyuk (zenhan)'"
		note="Three affiliative nouns — a=yupihi (elder brother), an=saha (elder sister; an= is the Shizunai dialectal variant of the 4th-person A-prefix a=), and hawehe (voice, long affiliative of haw 'voice') — stacked in one clause. The 4th-person prefixes function as logophoric narrator first person. The embedded ruwe ne closes the indirect-question frame."
	/>
</S>

<S t="Predicative possession with kinship affiliatives" id="predicative-kin">
	<p>
		Kinship affiliatives combine with the existential verbs <A w="an" gl="exist (sg)" /> and
		<A w="okay" gl="exist (pl)" /> to form predicative-possession clauses of the BE-type,
		restricted to body-part and kin nouns <Ref k="huber2025" p="247, 251" />. The construction
		is the standard schema for story-opening family inventories:
	</p>

	<Ex
		m="a=yup-i an a=sa-ha an"
		g="4.A=elder.brother-POSS exist 4.A=elder.sister-POSS exist"
		tr="I have an elder brother and I have an elder sister."
		cite="huber2025:251"
		dial="HK"
		note="Huber (2025) ex. 16; BE-type predicative possession with kinship affiliatives. The 4th-person a= is the logophoric narrator first person."
	/>

	<p>
		The contrast between the two possession constructions can appear within a single clause when
		a body part (affiliative) and a concept-construction kin term (<i lang="ain-Latn">kor</i>)
		co-occur:
	</p>

	<Ex
		m="ku=teke-he piro hi ta ku=kor totto noya ham uk wa"
		g="1SG.A=hand-POSS be.injured NMLZ LOC 1SG.A=have mother mugwort leaf take CONJ"
		tr="When my hand was injured, my mother took mugwort leaves and …"
		cite="ainukoraci1991"
		dial="CHI"
		place="Yokoyama Hiroyuki, Ainu Times no. 024/1 #20; source: 横山裕之, アイヌタイムズ (沙流・千歳)"
		note="ku=tekehe is affiliative-construction body-part possession; ku=kor totto is concept-construction kin possession. In this Chitose-influenced idiolect hapo means 'father', so totto 'breast' has been recruited as 'mother' and takes the kor construction."
	/>

	<p>
		The BE-type is unavailable for alienable nouns; its restriction to the inalienable domain
		is argued in <Ref k="huber2025" p="251–253" />. The full typology of predicative-possession
		clauses is in <Xr ch="predicative-possession-clauses" />.
	</p>
</S>

