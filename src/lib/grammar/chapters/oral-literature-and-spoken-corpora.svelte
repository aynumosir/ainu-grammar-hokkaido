<S t="Oral literature as the primary documentary resource" id="overview">
	<p>
		The grammar of Hokkaido Ainu is documented overwhelmingly through its oral-literary and
		recorded spoken tradition. Written records from before the twentieth century are sparse and
		linguistically unreliable; the descriptive grammars and dictionaries treated in
		<Xr ch="written-sources-grammars-dictionaries" /> rest in turn on recordings made between the
		1920s and the 1990s from a contracting community of native speakers. The corpus underlying this
		grammar — approximately 209,878 aligned sentences across 45,225 distinct token types — consists
		almost entirely of transcribed and annotated performances of traditional oral genres: heroic epics
		(<A w="yukar" gl="heroic epic" />/<A w="sakorpe" gl="heroic epic (E Hokkaido)" />), divine epics
		(<A w="kamuyyukar" gl="divine epic" />/<A w="oyna" />), prose folktales
		(<A w="uwepeker" gl="prose folktale" />/<A w="tuytak" gl="prose folktale (E Hokkaido)" />),
		ritual prayer (<A w="inonno itak" gl="prayer words" />/<A w="kamuynomi" gl="deity worship" />),
		and a smaller body of composed prose (the <i lang="ain-Latn">Ainu Times</i> newsletter and
		comparable texts). For the genre system and its grammatical correlates see
		<Xr ch="oral-literature-genre-taxonomy" />; for poetic register,
		<Xr ch="poetic-archaic-elevated-register" />; for corpus method and dialect sampling,
		<Xr ch="dialect-sample-and-corpus-method" />. <Ref k="bugaeva2012" p="§15.4.4.1.4" />
	</p>
</S>

<S t="The recording and publication tradition" id="tradition">
	<S t="Chiri Yukie and the pre-war foundations" id="chiri-yukie">
		<p>
			The earliest published Ainu-language oral-literature collection is
			<Ref k="chiri1923" /> — seventeen Iburi (Horobetsu) divine epics with Ainu text and Japanese
			translation, compiled by Chiri Yukie 知里幸惠 (1903–1922) in the final years of her life.
			Linguistically, the collection is anchored in the Horobetsu idiolect of its compiler, who
			drew on repertoires transmitted within her family. Its hero-narrators are indexed throughout
			by the exclusive first-person plural prefix <i lang="ain-Latn">ci=</i> and suffix
			<i lang="ain-Latn">=as</i> deployed as a singular narrator-voice — a convention
			characteristic of Iburi divine epics and distinct from the Saru and Chitose practice of
			using the fourth-person <i lang="ain-Latn">a=</i>/<i lang="ain-Latn">=an</i> in the same
			role <Ref k="sato2008" p="261" />; <Ref k="endo2022" />. This person-marking contrast and
			its grammatical motivation are taken up in
			<Xr ch="indefinite-fourth-person-paradigm" /> and
			<Xr ch="fourth-person-honorific" />.
		</p>
		<p>
			Kindaichi Kyōsuke's parallel scholarly activity in the 1930s–1940s — editing transcriptions
			made by Kubodera Itsuhiko from speakers across Hokkaido — produced the main pre-war
			inventory of heroic-epic texts <Ref k="kindaichi1931" paren />; <Ref k="kindaichi1933" paren
			/>. Kindaichi's yukar grammar established the five-syllable line as the canonical
			metrical unit <Ref k="kindaichi1931" p="165" />, a description subsequently refined by
			Okuda's two-metre analysis distinguishing syllable-count from accent-oriented verse
			<Ref k="okuda2022" />; the metrical system is treated in
			<Xr ch="sakehe-refrain-and-sung-verse-structure" />. The pre-war editions are read here
			through Endō and Okuda and are cited as reported.
		</p>
	</S>

	<S t="Post-war field documentation" id="postwar">
		<p>
			The bulk of the aligned corpus derives from recordings made in the three decades after World
			War II. The most extensively analysed set is the
			<strong>Biratori Town oral-literature archive</strong>
			<Ref k="biratori1969" />, held by the National Ainu Museum (国立アイヌ民族博物館) and
			available online. Principal narrators include 鍋澤ねぷき (Nabesawa Nepuki), 貝澤とぅるしの
			(Kaizawa Torusino), and 川上まつ子 (Kawakami Matsuko), all from Biratori in the Saru basin.
			The archive covers kamuy yukar, menoko yukar (female heroic epic), and uwepeker; it supplies
			many of the attested examples cited throughout this grammar.
		</p>
		<p>
			The <strong>ILCAA Ainu materials</strong>
			<Ref k="ilcaa1976" /> — recordings made at the Tokyo University of Foreign Studies Research
			Institute for Languages and Cultures of Asia and Africa, 1976–1984 — cover Saru and
			Shizunai narrators performing uwepeker, kamuy yukar, and prayer texts. The primary narrators
			are 川上まつ子 (Saru) and 織田ステノ (Shizunai); their performances are available with
			transcription, Japanese translation, and audio at ainugo.aa-ken.jp. Tamura Suzuko's
			contemporaneous field recordings in Biratori (principally 平賀サダモ and others) underlie
			her Saru dictionary <Ref k="tamura1996" /> and the folktale and lyric texts from which Dal
			Corso and Hirosawa draw evidential examples <Ref k="dalcorso2018" paren />;
			<Ref k="hirosawa2026" paren />.
		</p>
		<p>
			An independent line of post-war documentation covers Iburi and neighbouring dialects. Satō's
			study of an Abuta (虻田) yukar narrated by 遠島タンケ extends coverage east of Horobetsu and
			establishes that yukar grammar — aspect marking, the prospective
			<i lang="ain-Latn">anki</i>, comparative
			<A w="kane" gl="as if, to the extent that" />, and fourth-person indexing — is continuous
			with the prose grammar of the same dialects <Ref k="sato2009yukar" />. Nakagawa's Chitose
			dictionary <Ref k="nakagawa1995" paren /> provides a parallel primary reference for the
			genre terms, the sakehe system, and ritual vocabulary in the Chitose dialect.
		</p>
	</S>

	<S t="Continuing documentation: text series, corpora, and modern prose" id="recent">
		<p>
			Nakagawa Hiroshi's 24-volume annotated text series <Ref k="nakagawatexts2000" /> (2000–2025)
			presents Chitose-dialect kamuy yukar and uwepeker narrated principally by 白沢ナベ
			(Shirasawa Nabe), with interlinear glossing and Japanese commentary. This is the most
			extensively annotated single-narrator Hokkaido corpus available in print. Its Chitose data
			complement the Saru-centred Biratori and ILCAA collections and support dialect comparisons
			for person marking, aspect, and evidential distribution across the corpus.
		</p>
		<p>
			The online <strong>NINJAL Ainu oral-literature corpus</strong>
			<Ref k="ninjal2003" /> aggregates recordings with audio across dialects (primarily Saru and
			Chitose, with additional localities). The <strong>Chiba University materials</strong>
			<Ref k="chiba2015" /> add Horobetsu and related dialect texts. The
			<strong>Glossed Audio Corpus</strong>
			<Ref k="glossedcorpus2016" /> provides interlinearly glossed Chitose and Saru folktale texts
			online.
		</p>
		<p>
			The <strong>Ainu Times</strong> (<i lang="ain-Latn">アイヌタイムズ</i>) newsletter
			<Ref k="ainukoraci1991" />, published from 1991, supplies the main body of composed Ainu
			prose from after the period of intensive oral recording. Contributors include speakers from
			Saru, Chitose, and Samani; genres include first-person prose narration, prayer texts, and
			songs. These texts differ from the transcribed oral record in being written compositions;
			their register draws on the traditional prayer-language inventory
			(see <Xr ch="honorific-ritual-and-taboo-registers" />) and on the poetic-elegant
			conventions of <Xr ch="poetic-archaic-elevated-register" />, but their grammatical profile
			is shaped partly by literacy practices rather than performance conventions.
		</p>
	</S>
</S>

