<S t="Genre-conditioned grammar and the corpus as evidence" id="grammar-and-genre">
	<p>
		The collections differ not only in dialect but in the grammatical constructions they make
		available. Verse genres (yukar, kamuy yukar) deploy the elegant poetic register
		(<i lang="ain-Latn">atomte-itak</i>), featuring applicative prefixes in place of case
		particles, expanded polysynthetic verb forms, and a distinct lexicon; they also show the
		fourth-person prefix <i lang="ain-Latn">a=</i>/<i lang="ain-Latn">=an</i> as the
		hero-narrator's voice in Saru and Chitose texts (and <i lang="ain-Latn">ci=</i>/<i
			lang="ain-Latn">=as</i> in Iburi, per Chiri 1923), a number-agreement override under which
		<i lang="ain-Latn">ek-an</i> 'I come.SG' is grammatical where everyday speech requires
		<i lang="ain-Latn">k-ek</i>, and formulaic couplet phrasing throughout
		<Ref k="sato2008" p="259–264" />. Prose folktales use a more conversational register with
		dense quotative marking and a characteristically one-directional, low-variation intonation
		that Tamura distinguishes from the rising pattern of conversational interaction
		<Ref k="tamura1996" paren /> (nukopoli, aynu-corpora Discord 2024;
		<Ref k="aynucorporadiscord" />). These differences are the empirical basis for
		<Xr ch="narrative-tam-evidential-patterning-by-genre" />,
		<Xr ch="logophoric-narration-and-reported-discourse" />, and
		<Xr ch="reference-tracking" />.
	</p>

	<p>
		The contrast between verse and prose registers can be heard in native metalinguistic
		commentary within the corpus. 川上まつ子 describes the menoko yukar genre explicitly in terms
		of the sakehe (refrain): performing with the refrain marks the piece as menoko yukar, while
		omitting it and delivering the same content in prose style makes it an uwepeker
		<Ref k="ilcaa1976" paren /> — a speaker's formulation that identifies the sakehe as the
		primary formal diagnostic. The sakehe system and its verse structure are analysed in
		<Xr ch="sakehe-refrain-and-sung-verse-structure" />; rhetorical couplets and formulaic
		phrases in <Xr ch="parallelism-couplets-and-word-pairs" />.
	</p>

	<Ex
		m="sine kamuyyukar ku=ye so"
		g="one kamuy-yukar 1SG.A=tell FIN"
		tr="Let me tell one kamuy yukar."
		cite="sato2008"
		dial="CHI"
		place="Chitose; Satō 2008 ch. 21 reading exercise"
		note="A native performance frame uttered before beginning a kamuy yukar. The first-person ku= (not the narrative fourth-person a=) appears here because this is conversational framing around the narration rather than inside it — illustrating the boundary between the everyday first person and the hero-narrator's fourth-person voice."
	/>

	<p>
		The evidential framing that pervades the corpus differs by genre:
		<i lang="ain-Latn">ruwe ne</i> marks the narrator's asserted knowledge of story-world events
		in prose tales, while the formulaic ceremonial close
		<i lang="ain-Latn">ruwe tapan na</i> appears in prayer and ritual contexts
		<Ref k="dalcorso2018" p="331" />. For the evidential system see
		<Xr ch="evidential-schema-overview" /> and <Xr ch="ruwe-ne-inferential" />.
	</p>

	<p>
		That the <i lang="ain-Latn">ruwe ne</i> close is not confined to traditional story-world
		narration can be seen in composed first-person prose from the revival: a Saru–Chitose
		speaker reporting on a conference talk in the <i lang="ain-Latn">Ainu Times</i> closes the
		whole clause chain with <i lang="ain-Latn">ku=ye ruwe ne</i>:
	</p>

	<Ex
		m="oro ta k=arpa wa, “Kokusai-gengo-nen” oruspe ku=ye ruwe ne."
		g="there LOC 1SG=go CONJ “international-language-year” talk 1SG=say PERS COP"
		tr="I went there and spoke about the International Year of Languages."
		cite="ainukoraci1991"
		dial="SAR"
		place="Ainu Times 47 (contributor 横山裕之); Saru–Chitose composed prose"
		note="First-person narration (k= 'I go', ku=ye 'I say/tell') about the speaker's own act, yet closed with ruwe ne. Here the evidential does not mark hearsay — the speaker recounts a personal experience — but presents the event as a narrated, publicly framed account rather than a bare assertion; the personal-knowledge evidential reading (PERS) follows from the first-person subject, as analysed in the chapters on the evidential system."
	/>
</S>

<S t="Dialect and genre coverage" id="coverage">
	<p>
		The aligned corpus is heavily weighted toward the Saru and Shizunai dialects, which together
		account for roughly 121,000 of the 209,878 sentences; Chitose adds approximately 4,400
		(plus 5,753 classified as Saru–Chitose combined), Mukawa 13,424, and Horobetsu 3,098.
		Tokachi contributes 1,550. Northern and eastern Hokkaido — the localities of the
		<i lang="ain-Latn">sakorpe</i> heroic epic and the distinct eastern song traditions — are
		represented thinly <Ref k="endo2022" />. This geographic bias reflects the concentration of
		twentieth-century fieldwork in the Saru basin; comparable systematic recording of eastern and
		northern varieties did not occur at comparable depth.
	</p>

	<p>
		Genre coverage is similarly uneven. The kamuy yukar is well documented across multiple
		dialects (Saru, Chitose, Iburi); the uwepeker is well represented in Saru and Chitose;
		prayer texts are attested in Saru (川上まつ子, ILCAA), Shizunai (織田ステノ), and revival
		prose (Ainu Times). The caranke (rhetorical negotiation-speech), which Kindaichi ranks
		immediately below yukar for elegant-register density
		<Ref k="kindaichi1935" p="382" />, is identified in the scholarly literature but does not
		appear as a genre tag in the aligned corpus. Songs (<i lang="ain-Latn">upopo</i>,
		<i lang="ain-Latn">yaysama</i>) are attested but their short lyric texts contribute
		comparatively little to the grammatical evidence base for the phenomena analysed in this
		grammar.
	</p>
</S>

<S t="Philological constraints" id="philological">
	<p>
		Several features of the record limit what the corpus can establish. The transcription history
		is uneven: Chiri Yukie's 1923 edition and the pre-war Kindaichi-era texts were produced
		before modern phonemic conventions; their representations of coda consonants, the glottal
		stop, and pitch accent vary from the orthographic norms used in this grammar (see the
		orthography chapters). The ILCAA and Biratori recordings, made with audio and transcribed
		against the audible record, are phonologically more reliable but reflect the idiolects of a
		small number of elderly Saru-basin speakers.
	</p>

	<p>
		The aligned corpus is not genre-tagged as a queryable facet. Genre must be inferred from
		document titles (e.g. "uwepeker 民話8", "kamuy yukar フム パㇰパㇰ") rather than a
		controlled metadata field. Per-genre frequency counts — for instance, the density of
		quotative <i lang="ain-Latn">sekor</i> in yukar versus uwepeker — are not directly
		retrievable from the database; only whole-corpus token counts are available, and these
		include metalinguistic uses of genre terms alongside performance tokens.
	</p>

	<p>
		Certain corpus documents present genre-boundary ambiguities that resist the standard
		taxonomy. One ILCAA text narrated by 織田ステノ — a kamuy yukar titled "怠け者の子供" —
		combines a long sakehe with a short narrative body and closes as a first-person human
		self-narration (<i lang="ain-Latn">yayeisoytak</i>), making it not straightforwardly
		assignable to the kamuy yukar or yukar categories ‹contested› (gengojiro, aynu-corpora
		Discord 2025-12-12; <Ref k="aynucorporadiscord" />). Genre fuzziness of this kind is
		consistent with Endō's observation that regional repertoires vary and that speaker practice
		does not always track scholarly category boundaries <Ref k="endo2022" />.
	</p>

	<p>
		A further philological note concerns Chiri Yukie's <em>Ainu Shin'yōshū</em>. The kamuy
		yukar section headings appear in full capitals in the original edition (e.g.
		<em>AEKIRUSHI KAMUY</em>). The title element <em>AEKIRUSHI</em> has been proposed, after
		Katayama Ryūmine 2003, to parse as
		<i lang="ain-Latn">a=e-kir-usi</i> 'the place one has memory of' rather than as a form of
		<i lang="ain-Latn">ekiru</i> 'turn back' ‹contested› (0penke, aynu-corpora Discord
		2024-03-19; <Ref k="aynucorporadiscord" />). The analysis is morphologically plausible but
		remains community-proposed and has not been confirmed in independent philological
		commentary. It is noted here without grammatical commitment.
	</p>

	<p>
		The grammatical features conditioned by genre and register — fourth-person narration and its
		logophoric function, TAM and evidential framing in verse versus prose, elegant-register
		morphology — are analysed in <Xr ch="logophoric-narration-and-reported-discourse" />,
		<Xr ch="reference-tracking" />, <Xr ch="person-marking-architecture" />, and
		<Xr ch="narrative-tam-evidential-patterning-by-genre" />. Verse structure and the sakehe
		refrain belong to <Xr ch="sakehe-refrain-and-sung-verse-structure" /> and
		<Xr ch="parallelism-couplets-and-word-pairs" />; the honorific and ritual registers of
		prayer texts to <Xr ch="honorific-ritual-and-taboo-registers" />.
	</p>
</S>
