	<p>
		The long Sakhalin vowel occupies the syllable that carries initial accent in the Hokkaido
		exception word; the short Sakhalin vowel corresponds to the Hokkaido rule-derived form.
		This correspondence underpins the reconstruction of a proto-system in which these syllables
		were prosodically marked — by vowel quantity, retained in Sakhalin and restructured as
		pitch in Hokkaido <Ref k="nakagawa2024" p="27, 51" />; <Ref k="itabashi2001" />. The
		direction of that change and whether the ancestral contrast was quantity-based or
		pitch-based remain disputed ‹contested› (<Ref k="nakagawafukazawa2022" p="§3.1" />). The
		SA–HA length–pitch correspondence independently confirms that the three minimal pairs above
		represent genuine lexical contrasts.
	</p>
