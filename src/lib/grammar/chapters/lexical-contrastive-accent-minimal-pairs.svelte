<S t="Functional load of the accent contrast" id="functional-load">
	<p>
		The contrastive yield of the accent system is limited. The placement rules account for the
		large majority of the lexicon; the exception list is closed and memorizable rather than an
		open accidental class. The three pairs documented above appear to constitute the
		best-established minimal/near-minimal set in the literature; <Ref k="nakagawa2024" p="51" />
		acknowledges the list to be incomplete, which implies further near-minimal contrasts in
		principle — any exception word whose segmental shape matches a rule-derived word — but such
		additional pairs are not systematically tabulated in the available sources. Lexical
		disambiguation relies primarily on morphosyntactic context, and the segmental form alone
		resolves ambiguity in most encounters.
	</p>

	<p>
		The typological character that emerges is a pitch-accent system with a predominantly
		predictable accent assignment and a limited lexically stored residue. The formal typology
		of rising versus falling nuclei and the mora-syllable question are treated in
		<Xr ch="mora-syllable-accent-vs-tone-analysis" />.
	</p>

	<p>
		The contrastive relevance of accent varies across dialects. In Shizunai, Urakawa, Samani,
		Bihoro, Kushiro, and Shiranuka, pitch placement is not phonemically distinctive
		<Ref k="nakagawafukazawa2022" p="§3.1" />; <Ref k="nakagawa2024" p="49" />, and the three
		contrasts above are neutralized. Whether Shizunai should be classified alongside the accent
		dialects — the position of <Ref k="refsing1986" p="73" /> — or treated as accentless,
		as <Ref k="nakagawa2024" p="49–50" /> argues on the basis of variable archival
		transcriptions, has not been settled ‹contested›. The practical consequence is that the
		minimal pairs in this chapter are attributes of the Saru-type accent system and cannot be
		assumed for the accentless eastern and southeastern varieties. The dialect typology is
		addressed in <Xr ch="hokkaido-phonological-microvariation" />.
	</p>

	<p>
		The Yakumo and Oshamambe varieties retain pitch distinctiveness but apply a rightward-shifted
		rule: when both σ1 and σ2 are open, accent falls on σ3 rather than σ2
		<Ref k="nakagawafukazawa2022" p="§3.1" />; <Ref k="nakagawa2024" p="50–51" />.
		Forms that contrast in Saru by the exception-versus-rule distinction may therefore
		pattern differently in Yakumo, since their accent is calculated from a shifted baseline —
		Saru <i lang="ain-Latn">notákam</i> corresponds to Yakumo <i lang="ain-Latn">notakám</i>,
		and Saru <i lang="ain-Latn">sinúye</i> to Yakumo <i lang="ain-Latn">sinuyé</i>
		(<Ref k="nakagawa2024" p="50–51" />). Whether the Yakumo rule interacts with the exception
		list in the same way as the Saru rule — and so whether the same minimal pairs surface in
		Yakumo — is not addressed in the available sources. The Yakumo system is described in
		<Xr ch="pitch-accent-placement-rule" />.
	</p>
</S>

<S t="Accent of loanwords and reduplicated forms" id="loans-reduplications">
	<p>
		Loanwords are adapted to the Hokkaido Ainu (C)V(C) syllable canon — consonant-cluster
		repair, epenthesis, and syllable-boundary insertion — before entering the phonological
		system, as described in <Xr ch="syllable-template" />. Once adapted, they are in
		principle subject to the same placement rules as native words. The Japanese borrowing
		<i lang="ain-Latn">tampaku</i> 'tobacco' has its initial syllable closed by coda
		<i lang="ain-Latn">-m</i> and would receive σ1 accent by 原則1
		(<i lang="ain-Latn">támpaku</i>); the Russian-origin form
		<i lang="ain-Latn">potoloko</i> 'ceiling' begins with open σ1 and would receive σ2
		accent by 原則2 (<i lang="ain-Latn">potóloko</i>). Neither
		<Ref k="nakagawa2024" /> nor <Ref k="shiraishi2022" /> states a loan-specific accent
		rule, and the application of the placement rules to adapted loanforms is an inference
		from the general description of the system rather than a directly documented observation.
		Whether any loanword has joined the exception list with lexically stored non-rule accent
		is unattested in the available sources.
	</p>

	<p>
		A community discussion observed a potential tension with the compound
		<i lang="ain-Latn">kamuynomi</i> 'prayer ceremony'
		(<i lang="ain-Latn">kamúy</i> + <i lang="ain-Latn">nomí</i> →
		<i lang="ain-Latn">kamúynomi</i> by first-element priority,
		<Ref k="nakagawa2024" p="54" />). The Ainu accent on the first element is expected; the
		observation concerned the different prosodic placement that this term acquires when it is
		borrowed into Japanese-language usage ‹proposed›
		(<Ref k="aynucorporadiscord" />; palestine_2026, 2024-08-12). That is a property of the
		borrowing in its receiving language. Within Ainu, <i lang="ain-Latn">kamuynomi</i> follows
		the first-element-priority pattern of all compounds, detailed in
		<Xr ch="accent-in-compounds-and-affixation" />.
	</p>

	<p>
		For reduplicated forms, direct evidence from phonetically transcribed accent-dialect texts
		is sparse. A community observation reports that <i lang="ain-Latn">somósomo</i> — the
		reduplicated form of <i lang="ain-Latn">somó</i> 'even, also' — functions as a single
		prosodic word accented on σ2, matching the base, and that the two-word string
		<i lang="ain-Latn">ka somó ki</i> 'even … too' assigns accent on the base word
		independently ‹speculative› (<Ref k="aynucorporadiscord" />; nukopoli, 2024-12-09). The
		pattern is consistent with first-element priority — in full reduplication the first copy
		retains its accent and the echo copy is de-accented — but primary-source confirmation from
		the phonetically transcribed descriptive literature is lacking. The prosodic-word domain and
		its role in accent assignment are treated in <Xr ch="accent-in-compounds-and-affixation" />.
	</p>
</S>
