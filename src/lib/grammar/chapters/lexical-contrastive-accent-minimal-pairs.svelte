<S t="Predictable and lexically specified accent" id="predictable-lexical">
	<p>
		The placement rules of the accent dialects — if σ1 is closed (CVC), σ1 bears high pitch;
		if σ1 is open (CV), σ2 bears high pitch — assign accent to the large majority of the
		Hokkaido Ainu lexicon <Ref k="nakagawa2024" p="50" />. Shiraishi terms this the iambic (LH)
		pattern: words with a heavy (closed) first syllable are trochaic in accent, and words with a
		light (open) first syllable are iambic <Ref k="shiraishi2022" p="§5.1" />. The full statement
		of these rules and their interaction with syllable weight is the subject of
		<Xr ch="pitch-accent-placement-rule" />.
	</p>

	<p>
		The rules leave a residue of words with open σ1 that are accented on σ1 against the
		prediction. These carry lexically stored accent and must be individually memorized.
		<Ref k="nakagawa2024" p="51" /> lists roughly twenty such items:
		<i lang="ain-Latn">cápe</i> 'cat', <i lang="ain-Latn">húci</i> 'grandmother',
		<i lang="ain-Latn">húra</i> 'smell', <i lang="ain-Latn">húre</i> 'red',
		<i lang="ain-Latn">káni</i> 'I (emphatic)', <i lang="ain-Latn">kéra</i> 'taste',
		<i lang="ain-Latn">míci</i> 'father', <i lang="ain-Latn">mína</i> 'laugh',
		<i lang="ain-Latn">nína</i> 'gather firewood', <i lang="ain-Latn">nísap</i> 'suddenly',
		<i lang="ain-Latn">núman</i> 'yesterday', <i lang="ain-Latn">páse</i> 'heavy',
		<i lang="ain-Latn">sápo</i> 'elder sister', <i lang="ain-Latn">sések</i> 'hot',
		<i lang="ain-Latn">síno</i> 'really', <i lang="ain-Latn">sísam</i> 'Japanese person',
		<i lang="ain-Latn">túki</i> 'cup', <i lang="ain-Latn">túnas</i> 'fast',
		<i lang="ain-Latn">úsey</i> 'hot water', <i lang="ain-Latn">yúpo</i> 'elder brother',
		and further items beyond the list given there. Every word in the accent dialects is either
		rule-assigned or lexically specified; there is no third status.
	</p>

	<p>
		The person-marking prefixes <i lang="ain-Latn">ku=</i>, <i lang="ain-Latn">e=</i>, and
		<i lang="ain-Latn">ci=</i> are incorporated into the prosodic word and can shift accent
		relative to its base; <i lang="ain-Latn">a=</i> and <i lang="ain-Latn">eci=</i> do not
		trigger a shift. Compounds suppress the accent of all but the initial element. These
		morphological and compositional effects on accent placement are treated in
		<Xr ch="accent-in-compounds-and-affixation" /> and, for the person-affix interaction,
		in <Xr ch="person-marking-architecture" />.
	</p>
</S>

