	<p>
		In each pair, form A carries lexical exception accent on σ1 and form B is rule-derived
		with σ2 accent. The critical phonetic property is the <em>rising nucleus</em>
		(上り核アクセント): what is distinctive is where the pitch ↗ rises, not where it
		subsequently falls. A word accented on σ1 begins with high pitch and may remain high
		throughout with no change of meaning, because the system encodes the <em>onset</em> of the
		high span. A word accented on σ2 requires a ↗ rise at σ2; pronouncing it with an
		σ1-initial high instead misidentifies it as the σ1-accented form
		<Ref k="nakagawa2024" p="48–49" />. This stands in contrast with Standard Japanese, whose
		falling nucleus (下り核アクセント) marks the syllable <em>after</em> which pitch drops
		<Ref k="shiraishi2022" p="§5" />. A community observation from the Saru dialect captures
		the asymmetry: the pronouns <i lang="ain-Latn">ku↗ani</i> and
		<i lang="ain-Latn">ku↗a↘ni</i> are both acceptable, but
		<i lang="ain-Latn">ku↘ani</i> (Japanese-style falling nucleus) is perceived as deviant —
		the rise position, not the fall, is what matters ‹corpus-confirmed›
		(<Ref k="aynucorporadiscord" />; nukopoli, 2024-12-09).
	</p>

