<S t="The independent pronouns and their place in the grammar" id="overview">
	<p>
		Hokkaido Ainu marks person obligatorily on the finite verb; the independent personal pronouns
		are additive elements, not the primary vehicle of argument tracking. Nakagawa assigns them
		<em>arity 0</em> — they behave like adverbial adjuncts, are never obligatory, and cannot be
		incorporated into a verb stem, unlike other noun phrases of arity −1
		<Ref k="nakagawa2024" p="250, 262–263" />. Kindaichi and Chiri captured the same point by
		calling the pronoun a 贅語 'pleonasm', comparing
		<i lang="ain-Latn">kuani ku=eraman</i> 'I know' to Latin <i>ego scio</i> and French
		<i>moi je sais</i>: the verbal index alone suffices, and the pronoun doubles it for emphasis
		<Ref k="kindaichi1936" p="53" />. Tamura's Saru description confirms the pattern —
		pronouns function syntactically like adverbs, appearing only when a specific pragmatic
		condition is met <Ref k="tamura1971" p="3–5" />. The practical result is that pronouns appear
		far less freely than in Japanese, and third-person forms are particularly rare: where Japanese
		uses 彼/彼女, Hokkaido Ainu overwhelmingly prefers demonstrative phrases
		(<i lang="ain-Latn">toan kur</i> 'that person', <i lang="ain-Latn">nea menoko</i> 'that
		woman') <Ref k="nakagawa2024" p="250, 256" />.
	</p>
	<p>
		The sole pronoun that is dialect-invariant across all of Hokkaido is 2SG
		<i lang="ain-Latn">eani</i>. All other forms show at least some regional variation; the
		central division separates Saru and Chitose (1SG <i lang="ain-Latn">kani</i>) from the rest
		of Hokkaido (1SG <i lang="ain-Latn">kuani</i>), and the plural and fourth-person rows diverge
		considerably across zones <Ref k="nakagawa2024" p="250–251" />. The verbal affixes these
		pronouns reduplicate are described in <Xr ch="person-marking-architecture" />.
	</p>
	<p>
		<em>Reflexive marking</em> in Hokkaido Ainu is realized verbally, not by dedicated reflexive
		pronouns. The prefixes <i lang="ain-Latn">yay-</i> and <i lang="ain-Latn">si-</i>, which
		redirect an action onto the subject or its possessor, are covered in
		<Xr ch="reflexive-yay" /> and <Xr ch="reflexive-possessive-si" />. The emphatic 'I myself /
		you yourself' function that some languages express with a distinct intensive-pronoun series
		is realized in HA by the independent pronoun plus a focus or topic particle — the same
		pronouns described here, in their focus-hosting function.
	</p>
</S>

<S t="Internal structure: the nominalised existential" id="composition">
	<p>
		The personal pronouns are not primitive forms. Tamura's foundational analysis derives each
		one by attaching a person index to one of two existential roots — singular
		<i lang="ain-Latn">an</i> 'exist (SG)' or plural <i lang="ain-Latn">oka(y)</i>
		'exist (PL)' — and then nominalizing the result with the
		<Xr ch="concept-form-affiliative-form">possessed (affiliative) form</Xr> suffix, whose base
		is the formal noun <A w="hi" gl="thing, fact (formal noun)" />
		<Ref k="tamura1971" p="3–5" />; <Ref k="nakagawa2024" p="253" />. The formula is:
		<strong>person-index + existential root + <i lang="ain-Latn">hi</i></strong>.
	</p>
	<p>
		A diagnostic point secures this analysis. The plural pronouns use the
		<em>transitive-subject / possessive</em> person prefixes (<i lang="ain-Latn">ci=</i>,
		<i lang="ain-Latn">a(n)=</i>, <i lang="ain-Latn">eci=</i>) rather than the
		intransitive-S suffixes (<i lang="ain-Latn">=as</i>, <i lang="ain-Latn">=an</i>) that a
		finite plural-existential verb would carry. This shows that the base is a <em>noun</em>
		<i lang="ain-Latn">oka(y)+hi</i> 'the (plural) being', which is possessed, not a finite
		verb <i lang="ain-Latn">oka(y)</i> that is person-inflected
		<Ref k="nakagawa2024" p="253" />; <Ref k="kindaichi1936" p="53" />.
	</p>
	<p>
		The formal noun <i lang="ain-Latn">hi</i> surfaces in the singular pronouns but is lost in
		the plurals. The singulars preserve it: <i lang="ain-Latn">ku=an-hi → kani</i> (with
		contraction), <i lang="ain-Latn">e=an-hi → eani</i>,
		<i lang="ain-Latn">an-hi → anihi</i>. In the plural series the sequence
		<i lang="ain-Latn">okay-hi</i> would produce an illicit syllable contact; <i lang="ain-Latn">hi</i> is
		suppressed, and the final <i lang="ain-Latn">-y</i> of <i lang="ain-Latn">okay</i> further
		drops in Saru, yielding <i lang="ain-Latn">oka</i> <Ref k="nakagawa2024" p="253" />.
	</p>

