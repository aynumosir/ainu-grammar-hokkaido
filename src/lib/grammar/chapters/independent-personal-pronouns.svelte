<S t="Two further formation types" id="subtypes">
	<p>
		Beyond the existential-base type, two additional strategies contribute to the plural and
		third/fourth-person rows <Ref k="nakagawa2024" p="254–255" />.
	</p>
	<p>
		<strong>Type II — person-prefix + <i lang="ain-Latn">utari</i>:</strong> Several dialects
		form plural pronouns with the
		<Xr ch="nominal-number-utar-transnumerality">associative-plural noun</Xr>
		<A w="utar" gl="relatives, kin group" /> (possessed form <i lang="ain-Latn">utari</i>):
		Tokachi <i lang="ain-Latn">ciutari</i> (1PL), <i lang="ain-Latn">eciutari</i> (2PL),
		<i lang="ain-Latn">anutari</i> (4PL). These forms retain the lexical sense of
		<i lang="ain-Latn">utari</i> 'kin, associates' and stand "halfway between a pronoun and a
		common noun" <Ref k="nakagawa2024" p="254" />. The Saru/Chitose sequences
		<i lang="ain-Latn">k=utari</i>, <i lang="ain-Latn">a=utari</i> similarly mean 'my / their
		family' with a third-person (non-inclusive) verb, and only Tokachi
		<i lang="ain-Latn">anutari</i> — which triggers a fourth-person (inclusive) verb — functions
		as a full plural pronoun 'we (including you)' <Ref k="nakagawa2024" p="254" />.
	</p>
	<p>
		<strong>Type III — <i lang="ain-Latn">sinuma</i> base:</strong> The 3SG form
		<i lang="ain-Latn">sinuma</i> and 4SG <i lang="ain-Latn">asinuma</i> are built on a
		distinct base whose internal structure is unanalysed synchronically. Kindaichi posited a
		literary 2SG form <i lang="ain-Latn">esinuma</i> by analogy, but Nakagawa found no attested
		example and treats it as a phantom form ‹speculative›
		<Ref k="nakagawa2024" p="255" />.
	</p>
</S>

<S t="Functions: emphasis, contrast, and focus hosting" id="functions">
	<p>
		Nakagawa identifies three conditions that trigger an independent pronoun where the verbal
		index alone would otherwise suffice <Ref k="nakagawa2024" p="255–256" />. A neutral
		predication ('I am here', 'he came') carries no pronoun; a self-introduction such as
		<i lang="ain-Latn">*asinuma anak … a=ne</i> 'I am —' would be almost never said without
		special emphasis <Ref k="nakagawa2024" p="255" />.
	</p>

	<S t="Emphasis" id="fn-emphasis">
		<p>
			When a participant is singled out from alternatives ('I and not someone else will do it'),
			the pronoun precedes the verb and co-indexes the verbal prefix. In the following Chitose
			example, <i lang="ain-Latn">kani</i> adds the emphatic reading on top of the
			already-complete verbal index <i lang="ain-Latn">k=</i>:
		</p>

		<Ex
			m="kani k=uomare kusu ne na"
			g="1sg 1SG.A=clean.up PURP COP FIN"
			tr="(I) will clean up, so…"
			cite="nakagawa2024:255"
			dial="CHI"
			note="Nakagawa ex. 321. The verbal index k= (reduced form of ku= before a vowel) already marks the 1SG agent; kani adds the emphatic 'I (not someone else)'. The clause continues; kusu ne na closes the coordinating frame."
		/>

		<p>
			Kindaichi and Chiri's classical demonstration of the same construction places it
			explicitly in the register of Latin and French emphasis:
		</p>

		<Ex
			m="kuani ku=eraman"
			g="1sg 1SG.A=know"
			tr="I know."
			cite="kindaichi1936:53"
			dial="SAR"
			note="Kindaichi & Chiri §78. The full form kuani rather than contracted Saru kani reflects the older or literary register of the source. ku= is the complete 1SG.A index; kuani is strictly additive."
		/>
	</S>

	<S t="Contrast" id="fn-contrast">
		<p>
			When two participants are explicitly opposed in consecutive clauses — 'you do X; I do Y'
			— each is marked by the independent pronoun plus the topic marker
			<i lang="ain-Latn">anak=ne</i>. The following Saru dialogue from the Akor Itak teaching
			material pairs <i lang="ain-Latn">eani anakne</i> (addressee) against
			<i lang="ain-Latn">kani anakne</i> (speaker):
		</p>

		<Ex
			m="eani anak=ne onne kur neno e=yaynu hi"
			g="2sg TOP old man like 2SG.A=think NMLZ"
			tr="Do you think like an old man?"
			cite="utari1994"
			dial="SAR"
			place="Akor Itak §4 [akor-itak/004#6], Saru"
			note="The following sentence in the source — kani anakne mokor akkari sus kusu pet or un k=arpa rusuy 'I'd rather go swimming in the river' — completes the contrast pair. Both pronouns carry anak=ne to mark the opposed topics. The pragmatics of topic fronting are developed in the chapter on topic-marking-anakne."
		/>
	</S>

	<S t="Focus-particle hosting" id="fn-focus">
		<p>
			Focus and scalar particles such as <i lang="ain-Latn">ka</i> 'even/also',
			<i lang="ain-Latn">anakne</i> (topic-focus), and <i lang="ain-Latn">hem</i> must attach
			to a noun phrase; they cannot host on a verbal index.
			<i lang="ain-Latn">*ku= anakne</i> and <i lang="ain-Latn">*ku= ka</i> are
			ungrammatical. An independent pronoun is therefore obligatory whenever such a particle
			scopes over a participant <Ref k="nakagawa2024" p="256" />. In the following Saru example,
			<i lang="ain-Latn">asinuma</i> hosts the scalar particle <i lang="ain-Latn">ka</i>:
		</p>

		<Ex
			m="asinuma ka i=nukar"
			g="4sg FOC 4.O=look.at"
			tr="…(they) looked at me too."
			cite="nakagawa2024:256"
			dial="SAR"
			note="Nakagawa ex. 324, Saru. The fourth-person object i= indexes the narrator as the entity looked at. The scalar particle ka 'even/also' attaches to the pronoun asinuma; without the pronoun there is no NP host for ka. The full range of fourth-person object i= uses is treated in indefinite-fourth-person-paradigm."
		/>

		<p>
			The same constraint governs topic fronting: <i lang="ain-Latn">kani anakne …</i> is the
			canonical pattern, impossible as <i lang="ain-Latn">*ku= anakne</i>. The broader
			implications for argument structure and pro-drop in HA are taken up in
			<Xr ch="nonconfigurationality-pronominal-argument" />, and the particle paradigm in
			<Xr ch="topic-marking-anakne" />.
		</p>
	</S>
</S>

<S t="The fourth-person pronoun and the singular/plural asymmetry" id="fourth-person">
	<p>
		The fourth-person row is where the dialect paradigm shows its sharpest internal
		contradictions, providing, in Nakagawa's words, "an important hint for the historical origin
		of the fourth-person singular" that remains unresolved ‹contested›
		<Ref k="nakagawa2024" p="251–252" />.
	</p>
	<p>
		In Saru and Chitose both the pronoun and the verb distinguish singular from plural: pronoun
		<i lang="ain-Latn">asinuma</i> (SG) vs <i lang="ain-Latn">aoka</i> (PL), paralleled by
		distinct singular and plural verbal forms. Shizunai makes the opposite move: the pronoun
		preserves a singular/plural distinction (<i lang="ain-Latn">anani</i> SG vs
		<i lang="ain-Latn">anoka</i> PL), but the <em>verb</em> is invariably plural regardless
		of which pronoun is used — the pronoun-level contrast survives without a verbal counterpart.
		Horobetsu shows the mirror image: the verb retains a singular/plural opposition, but the
		only available fourth-person pronoun is the plural-base
		<i lang="ain-Latn">aokay</i>, used even for a single narrator
		<Ref k="nakagawa2024" p="252" />. These "opposite twists" are left as an open diachronic
		question by Nakagawa.
	</p>
	<p>
		One community-proposed reconstruction treats the 4th-person prefix as
		<i lang="ain-Latn">a-</i>/<i lang="ain-Latn">=an &lt; *an-</i>, with Shizunai
		<i lang="ain-Latn">anokay</i> preserving the older form: the nasal-drop that produced
		Saru/western <i lang="ain-Latn">a=</i> vs Tokachi/Ishikari <i lang="ain-Latn">an=</i>
		would be blocked at pronoun level because the result would create hiatus
		<Ref k="aynucorporadiscord" /> (.k1270, aynu-corpora Discord, 2024-03-22; ‹speculative›,
		attributed to Nakagawa). The connection between this prefix form and the broader morphological
		alignment of the fourth person is discussed in
		<Xr ch="person-marking-architecture" /> and <Xr ch="indefinite-fourth-person-paradigm" />.
	</p>
	<p>
		In oral narrative, the fourth-person pronoun <i lang="ain-Latn">asinuma</i> functions
		routinely as the first-person narrative self, alternating with or supplementing the verbal
		index <i lang="ain-Latn">ku=</i>/<i lang="ain-Latn">k=</i>. Corpus evidence places
		<i lang="ain-Latn">asinuma</i> at 669 tokens, predominantly in Saru narrative texts. The
		fourth-person pronoun appears in this function with topic-focus particles — the same
		focus-hosting role described above:
	</p>

	<Ex
		ain="asinuma anakne iwor so ka a=ehoyupu wa"
		m="asinuma anak=ne iwor so ka a=e-hoyupu wa"
		g="4sg TOP hunting.ground summit over 4.A=APPL-fly.about CONJ"
		tr="As for myself, I flew about over the hunting ground…"
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker 民話8 (その1), ILCAA Ainu materials (沙流) [aa-irc/006#52]"
		note="The fourth-person pronoun asinuma serves as the first-person narrative self throughout this text. The topic marker anak=ne cannot attach to a verbal affix and requires the free pronoun as host. The ka here is the postposition 'over/on top of' (path marker with the applicative e-), not the scalar focus particle."
	/>

	<p>
		The honorific and logophoric uses of the fourth person — including the use of a fourth-person
		verb for a single esteemed addressee — are treated in <Xr ch="fourth-person-honorific" />.
		The full debate over terminology (fourth person, indefinite, inclusive) and the referential
		range of the <i lang="ain-Latn">a=</i>/<i lang="ain-Latn">=an</i>/<i lang="ain-Latn">i=</i>
		affix set is carried in <Xr ch="indefinite-fourth-person-paradigm" />.
	</p>
</S>
