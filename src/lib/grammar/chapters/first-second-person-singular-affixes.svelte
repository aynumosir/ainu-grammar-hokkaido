<S t="Forms and alignment" id="forms">
	<p>
		In the Hokkaido Ainu person paradigm, the first- and second-person singular cells are
		distinguished by whether the language maintains or collapses the S/A/O contrast within the row.
		First singular keeps two distinct forms: <i lang="ain-Latn">ku=</i> for the single argument of
		an intransitive verb (S) and for the transitive subject (A), and <i lang="ain-Latn">en=</i>
		for the object (O). Second singular has one form, <i lang="ain-Latn">e=</i>, in all three
		roles. In alignment terms, the 1SG row is nominative–accusative (S=A≠O) and the 2SG row is
		neutral (S=A=O)
		<Ref k="nakagawa2024" p="165–169" />; <Ref k="bugaeva2012" p="471–472" />; <Ref
			k="sato2008"
			p="31–32"
		/>; <Ref k="tamura1972" p="27" />; <Ref k="bugaeva2008" p="32" />. The architecture of the
		complete paradigm — including the side-encoding of the first plural exclusive and the fourth
		person, the two-index cap on the verb, and the boundary-notation convention — is set out in
		<Xr ch="person-marking-architecture" />. The present chapter treats the 1SG and 2SG rows in
		detail.
	</p>

