	<p>
		The free pronouns <i lang="ain-Latn">kani</i> and <i lang="ain-Latn">eani</i> are optional
		emphatic or contrastive expressions; ordinary finite clauses carry the person information on
		the verb alone. Their etymology — <i lang="ain-Latn">kani</i> from the 1SG prefix plus an
		existential base; <i lang="ain-Latn">eani</i> invariant across the surveyed Hokkaido dialects
		— and their distribution relative to the verbal index are treated in
		<Xr ch="independent-personal-pronouns" />
		<Ref k="tamura1971" p="3–5" />; <Ref k="nakagawa2024" p="250–253" />.
	</p>
</S>

<S t="First-person singular subject: ku= and k=" id="ku">
	<p>
		The prefix <i lang="ain-Latn">ku=</i> indexes 1SG in both the intransitive-subject and the
		transitive-subject roles; the two uses share a single form. This identity of S and A, set
		against the distinct object <i lang="ain-Latn">en=</i>, is the formal basis for describing the
		1SG row as nominative–accusative
		<Ref k="nakagawa2024" p="166–169" />; <Ref k="bugaeva2012" p="472" />.
	</p>

	<Ex
		m="orwano ku=cise or ta ku=an kor"
		g="then 1SG.POSS=house place LOC 1SG.S=exist while"
		tr="Then, while I was in my house, …"
		cite="nakagawa2024:164"
		dial="ISH"
		place="Nakagawa ex. 104, after Sunazawa Kura 1983: 75"
		note="The intransitive existential ku=an indexes 1SG.S. The nominal ku=cise carries the homophonous first-person prefix in the possessive domain (glossed POSS here); nominal possession belongs to the chapter on the possessed form."
	/>

	<p>
		In Saru, Chitose, and Mukawa, <i lang="ain-Latn">ku=</i> reduces to <i lang="ain-Latn">k=</i>
		before stems beginning in <i lang="ain-Latn">a</i>, <i lang="ain-Latn">u</i>,
		<i lang="ain-Latn">e</i>, or <i lang="ain-Latn">o</i>:
		<i lang="ain-Latn">ek</i> 'come' yields <i lang="ain-Latn">k=ek</i>, and
		<i lang="ain-Latn">eramuan</i> 'understand' and <i lang="ain-Latn">eunkeray</i> 'receive'
		appear as <i lang="ain-Latn">k=eramuan</i> and <i lang="ain-Latn">k=eunkeray</i>. Nakagawa
		writes the Hidaka-western and Chitose convention as <i lang="ain-Latn">k(u)=</i>, recording
		the same alternation; the notation reflects phonological context, not two morphologically
		separate forms
		<Ref k="nakagawa2024" p="49, 166" />; <Ref k="bugaeva2012" p="467–468" />.
	</p>

	<Ex
		m="aynu itak k=eramuan wa kusu icen k=eunkeray wa pirka pirka"
		g="Ainu language 1SG.A=understand CONJ because money 1SG.A=receive CONJ good good"
		tr="Because I had learnt Ainu, I was able to receive money, which was very good."
		cite="nakagawa2024:167"
		dial="SAR"
		place="Nakagawa ex. 111"
		note="Both transitive verbs show k=, the reduced allomorph of 1SG.A ku= before the vowel-initial stems eramuan and eunkeray."
	/>

	<p>
		Bugaeva classifies <i lang="ain-Latn">ku=</i> as a full prefix: it is inseparable from the
		stem and shifts the accent kernel, properties that set it apart from the more autonomous
		intransitive-subject suffixes <i lang="ain-Latn">=an</i> and <i lang="ain-Latn">=as</i>, which
		can be separated from the verb by intervening words and form their own prosodic units
		<Ref k="bugaeva2012" p="472–473" />. Nakagawa writes all personal indexes with
		<i lang="ain-Latn">=</i> as a practical orthographic convention, noting the symbol carries no
		claim about clitic status <Ref k="nakagawa2024" p="49" />. Satō and Tamura write these forms
		with a hyphen; the learner grammar of Ijäs does likewise, stating the hyphen is a teaching
		convention rather than a pronunciation symbol
		<Ref k="ijas2023" />. Sakaguchi's explicit clitic/affix criteria — developed primarily for
		Sakhalin material, and applied to Hokkaido only by extension — place root-bound personal
		indexes in the prefixal class and rank <i lang="ain-Latn">=an</i>/<i lang="ain-Latn">=as</i>
		as the most autonomous subject markers; on these diagnostics, <i lang="ain-Latn">ku=</i>
		would belong to the fully prefixal end
		<Ref k="sakaguchi2024" p="ix, 77–85, 163" /> ‹contested›. The ordering diagnostics are
		developed in <Xr ch="personal-affix-template-and-ordering" />.
	</p>

	<p>
		Ijäs introduces <i lang="ain-Latn">ku=</i> early as the obligatory subject marker on both
		transitive and intransitive verbs, presenting it before the optional independent pronoun and
		treating the pronoun as emphatic or clarifying rather than grammatically required
		<Ref k="ijas2023" />. This is consistent with the head-marking account of the Ainu verb given
		by Satō <Ref k="sato2025a" p="361" /> and with the typological framework of
		<Ref k="nichols1986" paren />. An idiosyncratic note in Ijäs's phonetic guidance specifies
		that initial <i lang="ain-Latn">i</i> retains the fuller form and that careful Saru speech may
		realise the <i lang="ain-Latn">u</i> vowel before other vowels; these are L2-oriented
		observations about pronunciation and do not affect the morphological alternation
		<Ref k="ijas2023" />.
	</p>

	<Ex
		m="ku=hosipi"
		g="1SG.S=return"
		tr="I go back."
		cite="ijas2023"
		constructed
		dial="SAR"
		note="Constructed following Ijäs Lesson 2, which uses this verb type to introduce 1SG.S on an intransitive. Ijäs writes ku-hosipi."
	/>
</S>

<S t="First-person singular object: en=" id="en">
	<p>
		The object counterpart of 1SG is <i lang="ain-Latn">en=</i>. It indexes a first-person
		singular participant in the O role: the patient of a monotransitive verb or, with three-place
		verbs such as <i lang="ain-Latn">kore</i> 'give' and <i lang="ain-Latn">epakasnu</i>
		'teach', the human recipient rather than the theme
		<Ref k="nakagawa2024" p="169" />; <Ref k="sato2008" p="147" />; <Ref k="sato2023b" p="47" />;
		<Ref k="sato2025a" p="368–371" />. The secundative object-indexing pattern — recipient indexed,
		theme left as a bare noun phrase — is developed in <Xr ch="object-indexing-monotransitive" />.
	</p>

	<Ex
		m="en=epunkine wa en=kore yan"
		g="1SG.O=protect CONJ 1SG.O=give IMP"
		tr="Please protect me."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, menokoyukar 'Kamuy yukar 4' [aa-irc/011#5]"
		note="The imperative force supplies a second-person subject; both instances of en= mark the 1SG participant as the object/recipient."
	/>

	<Ex
		m="nep kameaske ene ne yakkay en=kasuy kun pe an cik pirka hike"
		g="what monster thus COP even 1SG.O=help should NMLZ exist if good although"
		tr="It would be good if any monster were to help me."
		cite="nakagawa2024:169"
		dial="TOK"
		place="Nakagawa ex. 122, after Takahashi 2007: 63"
		note="The prefix en= indexes the speaker as the object of kasuy 'help'; the agent is left to context."
	/>

	<p>
		A syncretism affects <i lang="ain-Latn">en=</i> in the Saru and Chitose combination tables.
		Across most Hokkaido dialects, a second-person subject acting on a 1SG object surfaces as a
		second-person A exponent followed by <i lang="ain-Latn">en=</i> — <i lang="ain-Latn"
			>e=en=</i
		> for 2SG→1SG or <i lang="ain-Latn">eci=en=</i> for 2PL→1SG. In Saru and Chitose, the
		expected second-person A exponent is absent, and bare <i lang="ain-Latn">en=</i> covers the
		2SG→1SG cell; Chitose extends the same bare form to 2PL→1SG. The bare
		<i lang="ain-Latn">en=</i> form is therefore shared between the 3→1SG and 2→1SG cells in
		these dialects, and the subject is recovered from discourse or from an overt pronoun
		<Ref k="nakagawa2024" p="170, 173" />; <Ref k="sato2008" p="146–147" /> ‹contested›. The
		full dialect-by-dialect combination table, and the parallel 1→2 portmanteau
		<i lang="ain-Latn">eci=</i>, are treated in <Xr ch="eci-portmanteau-and-second-plural" />.
	</p>

	<p>
		Ijäs presents the bare-<i lang="ain-Latn">en=</i> situation to learners as a predictable rule:
		when the 1SG participant is in O position and the subject is second or third person, the verb
		shows only <i lang="ain-Latn">en=</i>, with the subject inferred from context
		<Ref k="ijas2023" />. The constructed example below illustrates the Saru/Chitose 2SG→1SG
		reading.
	</p>

	<Ex
		m="topenpe en=kore"
		g="sweets 1SG.O=give"
		tr="You gave me sweets."
		cite="ijas2023"
		constructed
		dial="SAR"
		note="Constructed following Ijäs. The translation supplies a 2SG subject from context; in Saru/Chitose the verb shows only en=, which covers both 3→1SG and 2→1SG readings. The phenomenon is attested in Nakagawa 2024: 170, 173 and Satō 2008: 146–147."
	/>

	<p>
		In the literary and oral-narrative register, the 2→1SG configuration maps to
		<i lang="ain-Latn">i=</i>, the fourth-person object index, with the subject again unexpressed
		<Ref k="sato2008" p="261" />. That use belongs to the functional range of the fourth person
		and is treated in <Xr ch="indefinite-fourth-person-paradigm" />.
	</p>
</S>

<S t="Second-person singular: e=" id="e">
	<p>
		The 2SG index <i lang="ain-Latn">e=</i> is neutral across argument roles: one form marks
		2SG.S, 2SG.A, and 2SG.O alike. This pattern is shared by the 2PL form
		<i lang="ain-Latn">eci=</i>, so neither the singular nor the plural second-person index
		distinguishes subject from object
		<Ref k="tamura1972" p="27" />; <Ref k="sato2008" p="32" />; <Ref k="bugaeva2012" p="472" />.
		On an intransitive verb, <i lang="ain-Latn">e=</i> marks 2SG.S; on a transitive verb with a
		third-person counterpart, <i lang="ain-Latn">e=</i> may mark either 2SG.A or 2SG.O, and the
		role is supplied by the co-occurring indexes and by discourse.
	</p>

	<Ex
		m="sinen ne e=oman wa e=ray yakun"
		g="alone COP 2SG.S=go CONJ 2SG.S=die if"
		tr="If you go alone and die, …"
		cite="nakagawa2024:165"
		dial="SHI"
		place="Nakagawa ex. 106"
		note="Both intransitive verbs carry e= as 2SG.S; no object is present."
	/>

	<Ex
		m="kunneywa wakka e=ku"
		g="morning water 2SG.A=drink"
		tr="You drink water in the morning."
		cite="ijas2023"
		constructed
		dial="SAR"
		note="Constructed following Ijäs Lesson 3. The prefix e= indexes a 2SG transitive subject; the third-person object wakka receives no index."
	/>

	<p>
		The role ambiguity of <i lang="ain-Latn">e=</i> on a transitive verb is inherent to the
		neutral row. In a clause such as <i lang="ain-Latn">kanpisos e=kore</i>, the prefix may signal
		either 2SG.A (the addressee gives the book to someone) or 2SG.O (someone gives the book to
		the addressee), depending on which participant is third person; the gloss assigned in
		interlinear analysis follows what the discourse establishes. Ijäs flags this ambiguity
		explicitly in her fifth lesson as a diagnostic for learners
		<Ref k="ijas2023" />.
	</p>

	<p>
		When a 1SG or 1PL.EXCL subject acts on a second-person object, the portmanteau
		<i lang="ain-Latn">eci=</i> replaces any sequence of separate first- and second-person
		indexes. All four 1→2 combinations — 1SG→2SG, 1SG→2PL, 1PL.EXCL→2SG, 1PL.EXCL→2PL — surface
		as <i lang="ain-Latn">eci=</i>, so that forms such as <i lang="ain-Latn">*ku=e=</i> and
		<i lang="ain-Latn">*ci=e=</i> do not occur
		<Ref k="tamura1972" p="27–28" />; <Ref k="bugaeva2008" p="38" />; <Ref
			k="bugaeva2012"
			p="474"
		/>; <Ref k="nakagawa2024" p="170, 173" />. The portmanteau's double role — 1→2 combination
		exponent and 2PL index for S, A, and O — is treated in
		<Xr ch="eci-portmanteau-and-second-plural" />.
	</p>

	<Ex
		m="nep ka eci=kasuy pe an ya"
		g="something even 1SG.A>2SG.O=help NMLZ exist Q"
		tr="Is there anything I can help you with?"
		cite="nakagawa2024:173"
		dial="SAR"
		place="Nakagawa ex. 128, after Honda 2001: 44"
		note="The single exponent eci= encodes the 1SG.A→2SG.O combination; no separate ku= or e= appears."
	/>

	<p>
		Second-person neutrality is the consensus descriptive position for Hokkaido Ainu
		<Ref k="tamura1972" p="27" />; <Ref k="sato2008" p="32" />; <Ref k="bugaeva2012" p="472" />.
		Its diachronic status is more open: most treatments assume an earlier S/A≠O distinction was
		lost, while Satō's reported 2024 position treats neutrality as original — a strategy of
		empathy or deferred agency never preceded by a subject/object split
		<Ref k="dalcorso2025align" p="194–195" /> ‹contested›. The alignment-level debate and the
		competing system-wide accounts are taken up in
		<Xr ch="alignment-hierarchical-and-the-debate" />.
	</p>
</S>
