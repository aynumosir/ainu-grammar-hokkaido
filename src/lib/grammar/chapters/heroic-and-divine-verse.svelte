<S t="The sung verse genres at a glance" id="overview">
	<p>
		Among Hokkaido Ainu narrative genres, two are sung verse: the <em>heroic epic</em>
		(<i lang="ain-Latn">yukar</i> / <i lang="ain-Latn">sakorpe</i>) and the
		<em>divine epic</em> (<i lang="ain-Latn">kamuy yukar</i> / <i lang="ain-Latn">oyna</i>). Both
		are performed in the <Xr ch="poetic-archaic-elevated-register">elevated register</Xr>
		(<i lang="ain-Latn">atomte-itak</i>), both narrate their story in the first person
		from the protagonist's viewpoint, and both draw on a dense stock of formulaic couplets and
		parallelism. They diverge in several formal parameters that are diagnostically useful
		<Ref k="sato2008" p="259" />; <Ref k="endo2022" p="§2.4.2–2.4.3" />:
