<S t="The heroic epic: yukar and sakorpe" id="yukar-heroic-epic">
	<p>
		The <i lang="ain-Latn">yukar</i> is the longest and most prestigious genre in
		Hokkaido Ainu oral literature. The name itself encodes performance: Kayano glosses it as
		'to imitate / mimic' — the narrator heard the story (perhaps in a dream) and now
		re-tells it <Ref k="kayano1996" p="s.v. yukar" />. In south-west Hokkaido the Saru and Chitose
		term <i lang="ain-Latn">yukar</i> is standard; eastern varieties use
		<i lang="ain-Latn">sakorpe</i> (lit. 'the thing with <i lang="ain-Latn">sakor</i>') in Tokachi
		<Ref k="endo2022" p="§2.4.3" />. The Samani term <i lang="ain-Latn">yayerap</i> and the
		prose-content alternative <i lang="ain-Latn">irupaye</i> (yukar material recited without melody)
		mark additional regional and performative variation.
	</p>
	<p>
		A <i lang="ain-Latn">yukar</i> performance is sung to a melody that belongs to the
		narrator rather than to the story: each performer re-melodizes a borrowed narrative, and the
		tune may shift mid-performance <Ref k="endo2022" p="§2.4.3" />. The narrator and listeners
		keep time by beating the hearth-rim with a stick,
		<i lang="ain-Latn">repni</i> ('rhythm-stick'), the act called
		<A w="rep" gl="keep time" /> <Ref k="sato2008" p="259" />. Listeners may add a chorus (<i lang="ain-Latn">hetce</i>).
		There is no fixed <i lang="ain-Latn">sakehe</i> refrain, which is the single clearest
		formal contrast with the divine epic. Satō notes an opening formula
		<i lang="ain-Latn">eyororpe</i> that may repeat at the very start of a performance and
		reads it as a possible vestige of a refrain that the heroic epic once shared with the divine
		epic ‹speculative› <Ref k="sato2008" p="259" />.
	</p>
	<p>
		The protagonist is a parentless superhuman youth — in Saru texts typically
		<i lang="ain-Latn">Poy-ya-un-pe</i> or <i lang="ain-Latn">Pon Sinutapka-un-kur</i>;
		in eastern Hokkaido <i lang="ain-Latn">Pon Otasam/Otasut-un-kur</i>. The hero flies,
		dives, and travels underground; battles are not governed by a good-vs-evil worldview
		but by heroic competition <Ref k="endo2022" p="§2.4.3" />.
		Female protagonists appear in the <i lang="ain-Latn">menoko yukar</i> subgenre, which
		keeps the heroic format but centres on marriage rather than battle.
		The term <A w="rametok" gl="bravery; brave person; hero" /> (<i lang="ain-Latn">ram-etok</i>
		'spirit-tip') stands alone for the hero-concept; a second term
		<i lang="ain-Latn">cipapa</i> appears in Chiri Mashiho's <i>Shinyōshū</i> annotations
		but is not documented in other dictionaries
		<Ref k="aynucorporadiscord" /> (gengojiro, 2024-02-13, <code>asserted</code>) ‹speculative›.
	</p>
	<p>
		The yukar is traditionally narrated by men; women tell the prose equivalent
		<i lang="ain-Latn">irupaye</i> instead
		<Ref k="sato2008" p="259" />. Modern revitalization practices have
		women reciting verse <i lang="ain-Latn">yukar</i>, knowingly departing from the traditional
		allocation <Ref k="endo2022" p="§2.4.3" />. The gendered narrator convention connects to the
		broader pattern of genre allocation by sex treated in
		<Xr ch="gendered-register-speech" />.
	</p>
</S>

<S t="The divine epic: kamuy yukar, oyna, and Chiri Yukie" id="kamuy-yukar-divine-epic">
	<p>
		In the divine epic the narrator speaks in the voice of a deity — a
		<i lang="ain-Latn">kamuy</i>: an animal, plant, fire, water, weather phenomenon, or
		object — telling its own story. The Saru/Chitose term is
		<i lang="ain-Latn">kamuy yukar</i>; Tokachi uses <i lang="ain-Latn">oyna</i>
		for the same genre <Ref k="endo2022" p="§2.4.2" />. The term
		<i lang="ain-Latn">oyna</i> is, however, ambiguous across the dialect area:
		in coastal Hidaka and Iburi it labels a distinct genre whose protagonist is the
		culture-hero Oynakamuy (Aynurakkur / Okikurmi) and which uses no
		<i lang="ain-Latn">sakehe</i>, while in Tokachi and Sakhalin it covers the divine
		epic proper <Ref k="endo2022" p="§2.4.4" />;
		<Ref k="nakagawa1995" p="s.v. oyna" />. At Saru, the term is absent from Hiraga
		Sadamo's (平賀サダモ) usage — she subsumed what Kindaichi and Kubodera classified as
		sacred biographies under her <i lang="ain-Latn">yukar</i> label, a dialectally
		conditioned genre-boundary that resists a single universal taxonomy
		<Ref k="tamura1996" p="s.v. oyna" /> ‹contested›.
	</p>
	<p>
		In contrast to the heroic epic, each <i lang="ain-Latn">kamuy yukar</i> is sung to a
		<em>fixed melody</em> that belongs to the story itself, not to the narrator
		<Ref k="sato2008" p="259" />. Performances are shorter (minutes to roughly an hour)
		and are associated traditionally with women narrators, though men may also
		perform them <Ref k="endo2022" p="§2.4.2" />.
	</p>
	<p>
		The canonical published collection is Chiri Yukie's
		<i lang="ja">アイヌ神謡集</i> (<i>Ainu Shin'yōshū</i>, 1923)
		<Ref k="chiri1923" />, thirteen Horobetsu/Iburi
		<i lang="ain-Latn">kamuy yukar</i> with parallel Japanese translation. Compiled by
		Yukie Chiri (知里幸恵, 1903–1922), it remains the most widely read primary text of
		Ainu oral literature and provides the main evidence for the Iburi convention
		of using the exclusive first-plural <i lang="ain-Latn">ci=</i> / <i lang="ain-Latn">=as</i>
		as a singular deity-narrator marker (see the section on fourth-person narration, below).
		The chapter title formulae in this collection — for example
		<i lang="ain-Latn">AEKIRUSHI</i> — have been analysed as
		<i lang="ain-Latn">a=e-kir-usi</i> 'the place one has memory of' (after 片山龍峯 2003)
		rather than as a reflex of <i lang="ain-Latn">ekiru</i> 'turn back'
		<Ref k="aynucorporadiscord" /> (0penke, 2024-03-19, <code>proposed</code>) ‹contested›.
	</p>
</S>

<S t="The sakehe refrain" id="sakehe">
	<p>
		The <i lang="ain-Latn">sakehe</i> (variant <i lang="ain-Latn">saha</i>, Chiri 1955)
		is the defining feature of the <i lang="ain-Latn">kamuy yukar</i>: a fixed, often
		semantically opaque refrain inserted into the narration. It is fixed per story —
		the same <i lang="ain-Latn">kamuy yukar</i> story-type may carry a different
		<i lang="ain-Latn">sakehe</i> in different localities
		<Ref k="endo2022" p="§2.4.2" />.
		See <Xr ch="sakehe-refrain-and-sung-verse-structure" /> for verse-structural detail; the
		present section concerns the grammatical and philological facts.
	</p>
	<p>
		The word <i lang="ain-Latn">sakehe</i> is the
		<Xr ch="concept-form-affiliative-form">possessed form</Xr> of
		<A w="sake" gl="wine, sake" />: <i lang="ain-Latn">sake-he</i>,
		'the sake of (something)'. Tamura's Saru dictionary distinguishes this
		accented form (<i lang="ain-Latn">sákehe</i> 'melody; refrain') from the
		unaccented noun for 'sake-rice-wine' (also <i lang="ain-Latn">sake</i>)
		<Ref k="tamura1996" p="s.v. sakehe" />. The formal identity of the two is reflected
		in line-internal puns where both readings are live simultaneously.
	</p>
	<p>
		The <i lang="ain-Latn">sakehe</i> is inserted every line or every few lines,
		depending on the story; the insertion pattern is fixed for a given text but not
		uniform across the genre <Ref k="endo2022" p="§2.4.2" />. The content ranges from
		the transparently onomatopoeic — the cuckoo-deity's sakehe
		<i lang="ain-Latn">han kakkok kakkok</i> imitates its own call
		<Ref k="endo2022" p="§2.4.2" /> — to the semantically opaque.
		The function of the sakehe is debated: Nakagawa (2001b, via
		<Ref k="endo2022" p="§2.4.2" />) holds that it originally indexed the protagonist,
		but many sakehe have no recoverable referential link; and the same story-type
		does not carry the same sakehe across localities ‹contested›.
	</p>

