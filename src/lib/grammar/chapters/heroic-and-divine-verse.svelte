	<p>
		Okuda further reports that the same reciters' lyric poems are overwhelmingly 5-syllable,
		confirming that the two systems are not speaker-dependent but genre- and
		rhythm-dependent <Ref k="okuda2022" p="§3.2" />.
		He claims no other language is documented to use two such metre types by one
		reciter <Ref k="okuda2022" p="§5.1" />. A further hypothesis holds that the
		syllable-count system entered Ainu via contact with the Japanese 7-5 mora metre,
		with accent-orientation as the native baseline — but Okuda notes there is no direct
		evidence for this at present <Ref k="okuda2022" p="§5.2" /> ‹speculative›.
	</p>
	<p>
		On the question of rhyme and alliteration: Philippi (1979, via
		<Ref k="okuda2022" p="§2.2" />) held that rhyme in Ainu verse is 'quite accidental'
		and alliteration 'sporadic'. Tangiku (2020), as reported from community discussion,
		argues that an 18th-century yukar text (ルウェサニウンクㇾ) shows deliberate head-rhyme,
		end-rhyme, and imperfect rhyme
		<Ref k="aynucorporadiscord" /> (nukopoli, 2023-12-13, <code>asserted</code>, citing Tangiku 2020)
		‹contested›. The question of whether Ainu verse rhyme is a real compositional device
		or an artefact of a small phoneme inventory remains open.
	</p>
	<p>
		To hit the target syllable count, poets employ a recognized set of techniques:
		formulaic verse phrases, syllable-appropriate word choice, presence/absence of
		final particles, insertion of the dummy verb <A w="ki" gl="do" /> between verb
		and following element, uncontracted <i lang="ain-Latn">ku-</i> person marker,
		a line-initial prefixed <i lang="ain-Latn">u</i>, and the suppression of vowel
		elision that occurs freely in prose speech
		<Ref k="okuda2022" p="§2.1" />; <Ref k="sato2008" p="264" />.
	</p>
</S>

<S t="Fourth-person narration: the hero or deity as speaker" id="fourth-person-narration-and-person">
	<p>
		All sung verse genres, and prose narrative as well, are told in the first person from the
		protagonist's point of view throughout, with a closing
		identification formula revealing who 'I' was
		<Ref k="endo2022" p="§2.4" />;
		<Ref k="sato2008" p="259–261" />. The 'first person' is, however, expressed not
		by everyday <i lang="ain-Latn">ku=</i> (1SG) but by the
		<Xr ch="indefinite-fourth-person-paradigm">fourth-person</Xr> set:
		transitive-subject <i lang="ain-Latn">a=</i>, intransitive-subject
		<i lang="ain-Latn">=an</i>, object <i lang="ain-Latn">i=</i>
		(Saru / Chitose baseline) <Ref k="sato2008" p="260–261" />;
		<Ref k="bugaeva2008" p="40–44" />. The typological status of this convention — whether
		it represents genuine first-person narration throughout or a logophoric construction
		framing the whole text as a reported speech act — is unsettled
		<Ref k="bugaeva2008" p="40–44" />; <Ref k="sato2004" /> ‹contested›.
		The logophoric analysis is developed in <Xr ch="logophoric-narration-and-reported-discourse" />.
	</p>
	<p>
		Satō (2008: 259–261) offers a pragmatic motivation he calls the
		<em>indirection principle</em> (間接性の原理): oral literature originated as
		a deity's narration inside a dream, and the taboo on a deity addressing a living
		human directly motivates a non-everyday first-person marker that increases
		indirectness <Ref k="sato2008" p="259–261" />. He presents this explicitly as a
		hypothesis of his own, dissenting from the standard 'shamaness-oracle origin' theory
		(巫女の託宣起源説) ‹contested›. The discourse–narration connection is taken up in
		<Xr ch="narrative-tam-evidential-patterning-by-genre" />.
	</p>

	<S t="Saru and Chitose: a= / =an" id="saru-chitose-person">
		<p>
			In Saru and Chitose yukar and kamuy yukar the hero or deity is indexed throughout
			by the fourth-person prefixes <i lang="ain-Latn">a=</i> (transitive subject)
			and <i lang="ain-Latn">=an</i> (intransitive subject), identical in form to the
			everyday indefinite and inclusive uses of the fourth person
			<Ref k="sato2008" p="260" />. Corpus attestation of the narrative
			<i lang="ain-Latn">a=</i> comes predominantly from prose tale (uwepeker) texts
			<Ref k="ilcaa1976" />, where the same convention applies. The Abuta
			(虻田, Iburi) yukar narrated by Tōshima Tanke shows the same set:
			<i lang="ain-Latn">rek kor okay</i> 'is crying' (durative; l.46),
			<i lang="ain-Latn">paye=an anki</i> 'I am about to go' (prospective; l.113)
			<Ref k="sato2009yukar" p="§2.2.1" />. The verb <i lang="ain-Latn">sakehawki</i>
			'to sing/recite (a yukar)' appears in the same text, an Iburi-specific term
			<Ref k="sato2009yukar" p="§2.2.1" />.
		</p>
	</S>

	<S t="Iburi: ci= / =as as singular deity marker" id="iburi-person">
		<p>
			In Iburi <i lang="ain-Latn">kamuy yukar</i> — above all in Chiri Yukie's
			<i lang="ja">アイヌ神謡集</i> (Horobetsu, 1923) — the deity-narrator uses the
			exclusive first-plural <i lang="ain-Latn">ci=</i> / <i lang="ain-Latn">=as</i>
			consistently as a <em>singular</em> first person
			<Ref k="sato2008" p="261" />; <Ref k="chiri1923" />.
			In Chitose, <i lang="ain-Latn">ci=</i> / <i lang="ain-Latn">=as</i> may appear
			at the opening and then revert to <i lang="ain-Latn">a=</i> / <i lang="ain-Latn">=an</i>
