	<p>
		The Biratori corpus preserves the narrator 貝澤とぅるしの (Kaizawa Turushino) explicitly
		labelling the phenomenon: after reciting the thunder-deity's refrain
		<i lang="ain-Latn">hum pakpak</i>, she glosses it
		<i lang="ja">humihiruy だわ。sakehe.</i> 'it's a loud sound — [that's] the sakehe'
		<Ref k="biratori1969" />. This is one of the clearest native metalinguistic testimonies
		on the genre term itself.
	</p>
	<p>
		Similarly, the mid-Saru narrator 川上まつ子 (Kawakami Matsuko, ILCAA corpus)
		describes how the <i lang="ain-Latn">menoko yukar</i> ('female heroic epic') differs
		from the prose alternative she uses: one is told <em>with</em> the
		<i lang="ain-Latn">sakehe</i> (<i lang="ain-Latn">sakekoye</i>), the other
		as an <i lang="ain-Latn">uwepeker</i> (prose tale), and the version with
		<i lang="ain-Latn">sakehe</i> runs long while the prose version is short
		<Ref k="ilcaa1976" />.
	</p>
</S>

