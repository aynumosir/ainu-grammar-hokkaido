	<S t="Number-agreement override in the verse register" id="number-override">
		<p>
			Everyday Saru/Chitose allows the intransitive suffix <i lang="ain-Latn">=an</i>
			only with plural verb stems: <i lang="ain-Latn">arki=an</i> 'we come (PL)', never
			*<i lang="ain-Latn">ek=an</i> (the singular stem <i lang="ain-Latn">ek</i>
			'come.SG' does not take <i lang="ain-Latn">=an</i> in prose). In the
			elevated verse register, <i lang="ain-Latn">=an</i> does combine with singular
			stems to mark the hero-narrator as fourth person
			<Ref k="sato2008" p="263" />:
		</p>

		<Ex
			m="k=ek"
			g="1SG.S=come.SG"
			tr="I come."
			constructed
			note="Everyday Saru/Chitose: 1SG subject on the singular suppletive stem ek."
		/>

		<Ex
			m="ek=an"
			g="come.SG=4.S"
			tr="I come. (verse; hero-narrator)"
			constructed
			note="Verse register (atomte-itak): the fourth-person intransitive suffix =an appears with the singular stem ek. In prose =an is restricted to plural stems (arki=an 'we come.PL'); the singular + =an combination is a diagnostic of the verse register (Satō 2008: 263)."
		/>

		<p>
			This override extends to non-Saru/Chitose dialects which in prose also attach
			<i lang="ain-Latn">=an</i> only to plurals; the singular-stem combination is
			a verse-register diagnostic across southern Hokkaido
			<Ref k="sato2008" p="263" />.
		</p>
	</S>
