<S t="Grammatical hallmarks of the verse register" id="verse-register-grammar">
	<p>
		Both genres use the <Xr ch="poetic-archaic-elevated-register">elevated register</Xr>
		(<i lang="ain-Latn">atomte-itak</i>, lit. 'decorated language'), which differs from
		everyday speech (<i lang="ain-Latn">yayan-itak</i>) along four grammatical
		parameters identified by Satō <Ref k="sato2008" p="263–264" />:
	</p>

	<S t="Applicative case-marking" id="applicative-case">
		<p>
			Everyday Ainu marks locatives and goals by free particles
			(<i lang="ain-Latn">ta</i> for locative, <i lang="ain-Latn">un</i> for
			goal, etc.). The verse register replaces these with applicative verbal
			prefixes (<i lang="ain-Latn">e-</i>, <i lang="ain-Latn">o-</i>,
			<i lang="ain-Latn">ko-</i>), producing longer, morphologically heavier
			verb forms <Ref k="sato2008" p="263" />. The contrast is attested in
			the Chitose data Satō provides:
		</p>

		<Ex
			m="tanto Ukurmem ta ku=sirepa"
			g="today Ukurmem LOC 1SG.A=arrive"
			tr="Today I arrived at Ukurmem."
			cite="sato2008:263"
			dial="CHI"
			note="Everyday (yayan-itak): the locative goal is marked by the free particle ta."
		/>

		<Ex
			m="kamuy menoko uni-hi tane anakne a=ko-sirepa na"
			g="divine woman house-POSS today TOP 4.A=APPL-arrive FIN"
			tr="Now I arrived at the goddess's house."
			cite="sato2008:263"
			dial="CHI"
			note="Verse register (atomte-itak): the goal is marked by the applicative prefix ko- on the verb, in place of a particle. The fourth-person a= indexes the hero-narrator. This is Satō's illustrative contrast ex. 20."
		/>

		<p>
			The applicative system itself is described in <Xr ch="applicative-system-overview" />,
			and its interaction with the verse register in
			<Xr ch="poetic-archaic-elevated-register" />.
		</p>
	</S>

	<S t="Longer, more polysynthetic words" id="polysynthesis-verse">
		<p>
			Verse register words are structurally heavier than their everyday counterparts
			through elaborated prefixation and the use of periphrastic expressions where
			prose would use a single stem. Satō's examples include
			<i lang="ain-Latn">soyna-sanma o-si-ray-e</i> (lit. 'let oneself be pushed out')
			for everyday <i lang="ain-Latn">soyne</i> 'go out', and
			<i lang="ain-Latn">kamuy ne manu p</i> (lit. 'what is said to be a god') for
			<i lang="ain-Latn">kamuy</i> 'god/deity'
			<Ref k="sato2008" p="264" />. The periphrastic tendency also serves the syllable-count
			metre (§ above).
		</p>
	</S>

	<S t="Distinct lexicon" id="verse-lexicon">
		<p>
			The verse register maintains a vocabulary stratum not used in everyday speech.
			Genre-specific terms include the elegant kin terms
			<A w="ona" gl="father (elegant)" /> and <A w="unu" gl="mother (elegant)" />,
			contrasting with the colloquial and regionally variable
			<i lang="ain-Latn">haca</i>/<i lang="ain-Latn">hapo</i>
			<Ref k="aynucorporadiscord" /> (nukopoli, 2026-06-15, <code>asserted</code>;
			consistent with the distinct elegant lexicon described in
			<Ref k="endo2022" p="§3" />).
			The modifier <i lang="ain-Latn">kamuy</i> meaning 'splendid' (as opposed to
			its deity meaning) is attested in heroic epic but not in prayer — an
			instance of genre-internal lexical differentiation within the elevated
			register itself <Ref k="endo2022" p="§3.4" />.
			This register lexicon is catalogued in
			<Xr ch="poetic-archaic-elevated-register" />.
		</p>
	</S>
</S>

<S t="Parallelism and formulaic phrasing" id="parallelism-formulae">
	<p>
		Couplets and parallel lines are the backbone rhetorical device of both sung genres
		<Ref k="sato2008" p="264" />; <Ref k="endo2022" p="§4" />. Lines do not need to
		rhyme or metrically match to count as a couplet — the requirement is semantic
		or structural correspondence only. The most productive template is
		<i lang="ain-Latn">tu … / re …</i> 'two … / three …', which does not mean an
		exact count but a rhetorical plural ('many'):
		<i lang="ain-Latn">otu maw-kur-puni kane / ore maw-kur-puni kane</i>
		'raising two winds / raising three winds' (= many winds)
		<Ref k="sato2008" p="264" />. Formulaic descriptive phrases are memorized
		in bulk and redeployed across texts, serving syllable-count requirements while
		conveying conventional praise or scene-setting; for example,
		<i lang="ain-Latn">tuk-no pinni si-kopa-yar / aynu pito</i>
		'a fine person like a grown Manchurian-ash tree'
		(containing <i lang="ain-Latn">si-kopa-yar</i> 'like', not used in everyday
		speech) <Ref k="endo2022" p="§4" />.
		The full couplet and word-pair inventory is treated in
		<Xr ch="parallelism-couplets-and-word-pairs" />.
	</p>
	<p>
		Reference-tracking across the long stretches of yukar narration relies on the
		same fourth-person set throughout, with quotative
		<i lang="ain-Latn">sekor</i> + speech verb (<i lang="ain-Latn">ye</i>,
		<i lang="ain-Latn">hawki</i>, <i lang="ain-Latn">isoytak</i>) closing and
		re-opening direct-speech frames. The density of <i lang="ain-Latn">sekor</i>
		in verse is addressed under
		<Xr ch="reference-tracking" /> and
		<Xr ch="quotative-complementation-sekor" />;
		the reportative tail <i lang="ain-Latn">hawe ne</i> / <i lang="ain-Latn">hawe as</i>
		as a narration-closure device is treated in
		<Xr ch="hawe-ne-reportative" />.
	</p>
</S>

<S t="Genre-boundary debates" id="debates">
	<p>
		Several questions about the verse genres remain open in the literature:
	</p>
	<p>
		<strong>Is Ainu oral literature genuine first-person literature?</strong>
		The uniform use of the fourth-person set throughout all three narrative genres is
		typologically unusual. Whether it constitutes true first-person (direct-style)
		narration or a logophoric construction that frames the entire text as reported
		speech is unsettled
		<Ref k="endo2022" p="§2.4" />; <Ref k="bugaeva2008" p="40–44" /> ‹contested›.
	</p>
	<p>
		<strong>Genre-boundary cases.</strong>
		The aynu-corpora community has identified 織田ステノ's
		<i lang="ain-Latn">kamuy yukar</i> '怠け者の子供' as resistant to genre
		classification: it has a long sakehe (*manayta sanke tar panke tar kocupu*) but
		a short body and ends as a <i lang="ain-Latn">yayeisoytak</i> (self-narration)
		by a human boy — 'neither kamuy yukar nor yukar, and (having a melody) not prose'
		<Ref k="aynucorporadiscord" /> (gengojiro, 2025-12-12, <code>proposed</code>)
		‹contested›. This is consistent with Endō's own caveat that regional genre
		inventories do not map cleanly onto any single scheme
		<Ref k="endo2022" p="§2.5" />.
	</p>
	<p>
		<strong>The residual refrain.</strong>
		Satō's reading of the opening <i lang="ain-Latn">eyororpe</i> as a vestige of a
		lost yukar refrain is an inference without independent textual support
		<Ref k="sato2008" p="259" /> ‹speculative›.
	</p>
	<p>
		<strong>Two metres or one?</strong>
		The long-standing syllable-count description (Kindaichi 1931; Tamura 1973, via
		<Ref k="okuda2022" p="§2.1" />) and Okuda's accent-orientation metre are treated
		as genre- and rhythm-differentiated by Okuda himself, but the relationship
		between them — whether one is derivative of the other or they are independent —
		is not settled ‹contested›
		<Ref k="okuda2022" p="§5" />.
	</p>
</S>
