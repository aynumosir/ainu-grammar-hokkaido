	<p>
		A structural point underlies the table. Proposing a genetic relationship for an
		isolate places the entire burden of proof on the proposer, because the null
		hypothesis — independent origin — accounts for all of the data without additional
		apparatus. Lexical resemblances between any two languages occur by chance at a
		predictable rate; only systematic sound-to-sound correspondences across a
		significant portion of core vocabulary, independent of areal diffusion, constitutes
		evidence for common descent <Ref k="alonso2022" p="§1" />. No proposal for Ainu
		has satisfied this standard.
	</p>
</S>

<S t="Old Japanese loanwords into Ainu" id="oj-to-ainu">
	<p>
		Contact between Ainu and Japonic is the best-documented diachronic layer in
		Ainu's external history, and the directionality is not in dispute for the early
		stratum. A set of Ainu items preserve phonological features of Old Japanese or
		Pre-Old-Japanese that had disappeared from Japanese before the Nara period,
		placing the borrowing events "around the middle of the first millennium CE"
		<Ref k="janhunen2022" p="§3c" />. The diagnostics are the retention of a labial
		stop where later Japanese shows a fricative (Pre-OJ <i>*p-</i> → later Japanese
		<i>/h-/</i>), and the preservation of sequences that subsequently monophthongized.
		The regularly cited items are:
	</p>
	<ul>
		<li>
			<A w="sippo" gl="salt" /> — preserves Pre-OJ <i>*siopo</i> before the labial
			lenition that yields Modern Japanese <span lang="ja">塩</span> <i>shio</i>
			<Ref k="janhunen2022" p="§3c" />.
		</li>
		<li>
			<A w="pasuy" gl="prayer stick, ritual chopstick" /> — compared with OJ
			<i>hashi</i> 'chopstick'; Ainu retains the original stop <i>p-</i> lost
			in Japanese <Ref k="janhunen2022" p="§3c" />.
		</li>
		<li>
			<A w="kamuy" gl="deity, spirit" /> — an early cultural borrowing whose
			deep distribution across all Ainu lects confirms a pre-branching
			age <Ref k="janhunen2022" p="§3c" />.
		</li>
	</ul>
