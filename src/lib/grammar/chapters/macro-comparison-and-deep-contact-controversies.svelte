<S t="The isolate baseline" id="isolate-baseline">
	<p>
		Ainu has no confirmed genetic relative. Every proposal to link it to another
		language family has been examined and set aside; Janhunen's verdict — that Ainu
		"does not show unambiguous indications of being associated with any other language
		family" — expresses the current consensus ‹consensus›
		<Ref k="janhunen2022" p="§2" />; <Ref k="alonso2022" p="§1" />; <Ref k="vovin1993" paren />.
		Within its own lineage Ainu constitutes a small family — sometimes called Ainuic
		or Kurilic — of three closely related varieties: Hokkaido, Sakhalin, and the
		Northern Kuriles (<Xr ch="sakhalin-and-kuril-ainu-contrast" />). The question
		addressed here is not the isolate status itself, which is settled, but two
		connected and genuinely open questions: the nature and direction of contact between
		Ainu and neighbouring languages, especially Japonic; and whether any proposed
		deep-time comparison or areal alignment survives methodological scrutiny. The
		isolate status and its evidential basis are set out in
		<Xr ch="genetic-position-macro-comparison" />; the ethnolinguistic context
		in <Xr ch="ainu-people-homeland-history" />.
	</p>
	<p>
		For an isolate the comparative method is unavailable: there is no sister language
		against which to project correspondences back into a proto-system. Three analytic
		tools remain — internal reconstruction, contact-induced-change analysis, and
		typology — with the important qualification that typological reasoning carries
		no probatory weight for genetic questions and must be used "with utmost caution"
		<Ref k="alonso2022" p="§1" />. The methodologically recoverable depth is also
		shallow: internal reconstruction reaches approximately 1,000 years without
		unsupported extrapolation, terminating at a Common Ainu stage no earlier than the
		thirteenth-century CE branching that separated the Sakhalin varieties from
		Hokkaido <Ref k="alonso2022" p="§1, §4" />. The reconstruction of Proto-Ainu and
		Common Ainu is treated in <Xr ch="proto-ainu-segmental-reconstruction" /> and
		<Xr ch="internal-reconstruction-and-grammaticalization-pathways" />.
	</p>
</S>

