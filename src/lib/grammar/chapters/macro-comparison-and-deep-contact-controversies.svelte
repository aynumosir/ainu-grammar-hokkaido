<S t="The reverse direction: Ainu elements in early Japonic" id="ainu-to-oj">
	<p>
		The claim that Ainu was once spoken more broadly across the Japanese archipelago
		and left loanwords in Old Japanese is the most contested diachronic proposal
		currently appearing in peer-reviewed Ainu scholarship. Vovin (2022) argues that
		Ainu extended through Kantō and Tōhoku before the Yayoi expansion, and that the
		Man'yōshū — particularly East Old Japanese (EOJ) in books 14 and 20 and West Old
		Japanese (WOJ) in books 17 and 19 — contains loans from this substrate ‹contested›
		<Ref k="vovin2022" p="§§1–6" />.
	</p>
	<p>
		Among the specific etymologies Vovin proposes:
	</p>
	<ul>
		<li>
			EOJ <i>kuta</i> 'house' ← Ainu <A w="kotan" gl="settlement" />
			(village→house shift parallel to Latin <i>vicus/villa</i>)
			<Ref k="vovin2022" p="§3" />.
		</li>
		<li>
			WOJ <i>porö</i> 'big' ← Ainu <A w="poro" gl="big, large" />
			<Ref k="vovin2022" p="§4" />.
		</li>
		<li>
			OJ <i>iruka</i> 'dolphin' ← Ainu <i lang="ain-Latn">rika</i>
			'whale-fat' <Ref k="vovin2022" p="§2" />.
		</li>
		<li>
			OJ <i>kanimba</i> 'cherry' ← Ainu
			<i lang="ain-Latn">karinpa</i> 'cherry'
			<Ref k="vovin2022" p="§5" />.
		</li>
		<li>
			Place names in Man'yōshū: <i>Kanipa</i> ← Ainu
			<i lang="ain-Latn">ka-ne-pa</i> 'upper bank';
			<i>Ikuri</i> ← Ainu <i lang="ain-Latn">ikure</i>
			<Ref k="vovin2022" p="§3" />.
		</li>
	</ul>
	<p>
		The loan-phonology proposed is systematic: Ainu geminate clusters simplify
		across the borrowing boundary (Ainu <i lang="ain-Latn">-kk-</i> → OJ
		<i>-k-</i>); Ainu word-final consonants are lost in EOJ surface forms; and the
		Proto-Japonic vowel shifts <i>*o &gt; u</i> and <i>*e &gt; i</i> in EOJ
		environments are said to reflect the Ainu source shapes
		<Ref k="vovin2022" p="§2" />.
	</p>
	<p>
		The argument has not been independently corroborated in either Old Japanese
		philology or Ainu linguistics. The structural problem is that short monosyllabic
		resemblances — like WOJ <i>porö</i> ~ Ainu <A w="poro" /> — fall within the
		range expected by chance between unrelated languages, and the required semantic
		shifts must be stipulated rather than derived from cross-linguistically attested
		patterns. Janhunen (2022) characterises the shared features under the more general
		rubric of "Jōmonization of Japanese" — Ainu-type languages influencing Japonic
		typologically — without endorsing individual lexical etymologies of this kind
		<Ref k="janhunen2022" p="§3c" />. The substrate hypothesis and the
		lexical-etymology program address different phenomena and should be kept
		analytically separate: the former has structural support; the latter remains
		unconfirmed ‹contested›.
	</p>
</S>

<S t="The Jōmon-substrate thesis and Ainu ethnogenesis" id="jomon-substrate">
	<p>
		Independent of the lexical-etymology debate, Janhunen (2022) develops a substrate
		account of Japonic that is better evidenced and more broadly accepted. The Japonic
		family arrived on the archipelago with the Yayoi agricultural expansion from the
		Korean Peninsula, roughly between 900 BCE and 500 CE, and displaced the earlier
		"Jōmonic" linguistic communities whose long prior presence shaped Japonic phonology,
		morphology, and vocabulary in ways that remain incompletely disentangled ‹contested›
		<Ref k="janhunen2022" p="§3a–3c" />. On this account, Ainu is the sole surviving
		Jōmon language — its survival "an accident" in the northeast while the Japonic
		expansion replaced the other Jōmon languages of the archipelago
		<Ref k="janhunen2022" p="§3a" />.
	</p>
	<p>
		The ethnolinguistic implication is that the Ainu homeland was not Hokkaido but
		Honshu. The present Hokkaido distribution results from northward movement: the
		Satsumon culture (c. 700–1200 CE) carried the Ainu linguistic ancestors northward,
		reaching northern Hokkaido around 1000 CE, southern Sakhalin around 1300 CE, and
		the Kuriles around 1500–1600 CE <Ref k="janhunen2022" p="§3a–3b" />. The
		<i lang="ja">Emishi</i> (<span lang="ja">蝦夷</span>) of northern Honshu are most
		plausibly treated as a proto-historical complex partly ancestral to the Ainu
		<Ref k="janhunen2022" p="§3a" />. The Okhotsk culture (c. 600–1600 CE), whose
		language Janhunen associates with early Ghilyak/Amuric, was absorbed by the
		Satsumon population in northern Hokkaido and Sakhalin, contributing a borrowing
		residue treated in <Xr ch="northern-contact-nivkh-tungusic-manchu" />.
	</p>
	<p>
		Community discussion connects the OJ ethnonym <i lang="ja">emi₁si</i> with the
		Sakhalin Ainu ethnonym <i lang="ain-Latn">enciw</i>. Antitwilight (aynu-corpora
		Discord, 2024-12-23) proposes that OJ <i lang="ja">emi₁si</i> borrows from a
		pre-Proto-Ainu form <i lang="ain-Latn">*æmi-ti</i>, with the segmentation
		<i lang="ain-Latn">*ti</i> = first-person plural, regularly giving
		&gt; <i lang="ain-Latn">enciw</i> <Ref k="aynucorporadiscord" /> ‹speculative›.
		This partially converges with Janhunen's (2022 §4) note that
		<i lang="ain-Latn">enciw</i> ~ Emishi/Ebisu/Ezo
		<i lang="ain-Latn">*emisV~*emiciV</i> (citing Kindaichi 1960), though the
		specific morpheme segmentation proposed by the community goes beyond the
		published source and cannot be verified against it.
	</p>
	<p>
		The word <A w="tunakay" gl="reindeer" /> — which entered Japanese as
		<i lang="ja">tonakai</i> — is a probable loan from a pre-Proto-Ghilyak source
		into Ainu, illustrating contact at the Okhotsk cultural interface ‹contested›
		<Ref k="janhunen2022" p="§3c" />.
	</p>
</S>

<S t="Toponymic substrate on Honshu" id="toponymic-substrate">
	<p>
		A Honshu Ainu homeland should have left a toponymic residue; place-name
		substrates are well attested where a speech community has been displaced but not
		entirely erased. Two bodies of candidate evidence circulate in the literature:
		analysis of Tōhoku place names with elements cognate with productive Ainu
		geographical vocabulary, and Vovin's (2022 §3) identifications of Ainu-origin
		proper names in the Man'yōshū (§4 above).
	</p>
	<p>
		Ainu geographical vocabulary is morphologically productive and well-documented
		in Hokkaido: <A w="nay" gl="stream, small river" />, <A w="pet" gl="river" />,
		<A w="nupuri" gl="mountain" />, <A w="wakka" gl="fresh water" />, and
		<A w="or" gl="place, location" /> appear in hundreds of Hokkaido place names.
		The community has noted specific Tōhoku cases: nukopoli (aynu-corpora Discord,
		2026-01-22, proposed) analyses the Iwate surname and toponym
		<i lang="ja">乙茂内</i> (<i>Otomonai</i>) as Ainu
		<i lang="ain-Latn">o-tum-nay</i> 'head-middle-stream', citing the concentration
		of <i lang="ja">内</i> (<i>-nai</i>) toponyms in the surrounding area as
		convergent evidence <Ref k="aynucorporadiscord" /> ‹speculative›.
		The morphological segmentation is plausible against known Ainu geographical
		morphology, but confirmation requires independent phonological correspondence
		evidence that is not yet available.
	</p>
	<p>
		A systematic survey of Tōhoku toponymy against the Ainu geographical lexicon
		remains a research gap. The existing evidence — from the matagi hunter-gatherer
		vocabulary of Tōhoku, from the Emishi cultural zone extending considerably south
		of modern Hokkaido, and from the individual proposals above — is suggestive but
		not yet probative ‹speculative›.
	</p>
</S>

<S t="Separating contact, inheritance, and chance" id="contact-vs-inheritance">
	<p>
		The central methodological problem for all the proposals above is the triage
		between three origins for any given resemblance: inheritance from a common
		ancestor; borrowing through contact; and chance. For an isolate, the inheritance
		route is ruled out by definition for resemblances with established families.
		The issue is therefore always whether a similarity is contact-induced or
		accidental.
	</p>
	<p>
		The standard diagnostics applied in the Ainu contact literature
		<Ref k="shiraishitangiku2022" p="§3" /> are:
	</p>
	<ul>
		<li>
			<strong>Direction of spread:</strong> does the item appear only in lects
			geographically adjacent to the supposed source, or is it pan-Ainu? A
			pan-Ainu distribution implies a pre-branching (Common Ainu or Proto-Ainu)
			age, excluding recent contact.
		</li>
		<li>
			<strong>Morphological analyzability:</strong> inherited vocabulary tends to
			be internally structured; loans tend to be opaque. <A w="sippo" gl="salt" />
			carries no recoverable Ainu morpheme; <A w="cep" gl="fish" />
			(&lt; <i lang="ain-Latn">ci=e-p</i>) is fully transparent — see
			<Xr ch="internal-reconstruction-and-grammaticalization-pathways" />.
		</li>
		<li>
			<strong>Loan phonology:</strong> for a directed borrowing, the donor-language
			form and the recipient-language form must be connected by phonologically
			plausible, preferably recurrent, processes. Ad hoc adjustments stipulated
			for a single item weaken the case considerably.
		</li>
		<li>
			<strong>Systematic correspondence:</strong> a genetic or deep-contact
			relationship requires recurrent sound-to-sound correspondences across a
			significant portion of core vocabulary, not sporadic resemblances.
		</li>
	</ul>
	<p>
		By these criteria the early Japanese→Ainu stratum (§3) meets most diagnostics
		and is well established ‹consensus›. Vovin's reverse-direction program (§4)
		proposes systematic loan-phonology but produces thin correspondence sets and
		semantically variable links, and has not been replicated ‹contested›.
		The Jōmon-substrate thesis (§5) operates at a depth that forecloses direct
		lexical testing; it is evaluated primarily through population history and
		typological alignment and is best treated as a working hypothesis rather than
		a demonstrated result ‹contested›.
	</p>
	<p>
		Two community discussions illustrate the methodological divide. The proposal that
		Ainu <i lang="ain-Latn">si=</i> (reflexive prefix, treated in
		<Xr ch="person-marking-architecture" />) is cognate with Korean and Old Japanese
		reflexive pronouns — aomidori_cs_, aynu-corpora Discord, 2023-12-08
		<Ref k="aynucorporadiscord" /> — invokes forms short enough to match by chance
		and a semantic field (self-reference) common enough to arise independently;
		without a systematic correspondence framework the proposal cannot advance beyond
		conjecture ‹speculative›. The set of proposed Korean-origin vocabulary items
		(<i lang="ain-Latn">casi</i>, <i lang="ain-Latn">notak</i>,
		<i lang="ain-Latn">mose</i>, <i lang="ain-Latn">nay</i>,
		<i lang="ain-Latn">toy</i> — antitwilight, aynu-corpora Discord, 2024-11-15,
		proposed <Ref k="aynucorporadiscord" />) faces the same difficulty:
		<i lang="ain-Latn">casi</i> 'fortification' in particular has a plausible
		Ainu-internal structure and is not compelled to be a contact loan ‹contested›.
		Both sets are the kind of etymologies that Alonso (2022 §1) and Janhunen
		(2022 §2) explicitly bracket as undemonstrable under the current methodological
		standard for Ainu.
	</p>
</S>

<S t="The Amur / North Pacific areal layer" id="amur-areal">
	<p>
		At the typological level Ainu shares features with North Pacific Rim languages —
		polysynthetic morphology, head-marking, verb-final word order, extensive
		nominalization-based clause linkage — that place it loosely within a broad
		Northeast Asian typological zone, though Janhunen (2022 §3c) notes that Ainu
		"stands apart" even within that grouping. The typological characterisation is
		descriptive, not a genetic or even a firm areal claim.
	</p>
	<p>
		The contact-driven Amur areal layer has a real lexical residue, but its
		Hokkaido signature is thin. The primary northern-contact zone was Sakhalin,
		where Ainu, Nivkh, Uilta, and Manchu (via the Santan trade) met; the lexical
		traffic is documented in <Xr ch="northern-contact-nivkh-tungusic-manchu" />.
		The items that reached Hokkaido Ainu directly — <A w="kaya" gl="sail" />
		(&lt; Nivkh, with the 'fish-skin garment' sense retained in Sakhalin but reduced to
		'sail' in Hokkaido), <A w="sintoko" gl="cask" /> (morphologically opaque; possible
		ultimate Eurasian Wanderwort), <A w="san" gl="offering shelf" />
		(&lt; Nivkh <i>cʰaŋ</i>, Uilta <i>saan</i>) — are documented borrowings,
		not indicators of genetic relationship
		<Ref k="shiraishitangiku2022" p="§3" />.
		Whether the Sakhalin contact situation constituted a true <em>Sprachbund</em>
		is left open by the most recent survey <Ref k="shiraishitangiku2022" p="§1" />.
	</p>
	<p>
		Alonso de la Fuente (2022 §2.4) observes that the reconstructed Proto-Ainu
		consonant inventory looks typologically like a mixed Southwest/Northeast Eurasian
		language that may have acquired some Southeast Asian-type features through contact:
		the merger of <i lang="ain-Latn">*l</i> and <i lang="ain-Latn">*ɾ</i>, the
		single fricative <i lang="ain-Latn">/s/</i>, and the bare stop series
		<i lang="ain-Latn">/p t k/</i> are consistent with this areal profile. The
		observation is typological inference, not a contact-language argument, and it
