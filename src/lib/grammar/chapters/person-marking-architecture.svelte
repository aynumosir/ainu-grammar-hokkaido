<S t="Side-encoding: prefixes for A, suffixes for S" id="side-encoding">
	<p>
		The architectural asymmetry of the paradigm is the side of the verb on which the subject index
		appears. First singular and second person are prefixal, or zero, in both subject roles. First
		plural exclusive and the fourth person switch sides by transitivity: <i lang="ain-Latn">ci=</i>
		and <i lang="ain-Latn">a=</i> stand before a transitive stem as A, while
		<i lang="ain-Latn">=as</i> and <i lang="ain-Latn">=an</i> follow an intransitive stem as S. This
		side-encoding is the morphological basis on which Bugaeva calls these two rows tripartite <Ref
			k="nakagawa2024"
			p="166–167"
		/>; <Ref k="bugaeva2012" p="472" />; <Ref k="bugaeva2008" p="32" />.
	</p>

