<S t="Number and the fourth person" id="number-fourth">
	<p>
		The person paradigm intersects with number at two layers. The first plural row is exclusive:
		<i lang="ain-Latn">ci=</i>, <i lang="ain-Latn">=as</i>, and <i lang="ain-Latn">un=</i> exclude
		the addressee, while addressee-inclusive ‘we’ belongs to the fourth person. Tokachi confirms the
		same division, an exclusive first-plural series beside an inclusive fourth-person series whose
		forms take a plural verb even when the referent is single <Ref k="nakagawa2024" p="163–164, 174" />;
		<Ref k="tamura1972" p="18–19" />; <Ref k="bugaeva2012" p="472, 475" />; <Ref
			k="takahashi2015"
			p="206–209"
		/>. The exclusive paradigm is developed in <Xr ch="first-person-plural-and-clusivity" />.
	</p>

	<p>
		Verb number is its own system. Hokkaido uses suppletive singular/plural stems —
		<i lang="ain-Latn">ek</i> / <i lang="ain-Latn">arki</i> ‘come’,
		<i lang="ain-Latn">arpa</i> / <i lang="ain-Latn">paye</i> ‘go’ — alongside a root-attaching
		plural suffix <i lang="ain-Latn">-pa</i> and, in Saru, Chitose, and Ishikari, an outer plural
		<i lang="ain-Latn">pa</i> that sits outside the person index. The minimal pair
		<i lang="ain-Latn">iki=an pa</i> and <i lang="ain-Latn">iki-pa=an</i> places the outer
		<i lang="ain-Latn">pa</i> after the fourth-person suffix and the stem-forming suffix
		<i lang="ain-Latn">-pa</i> before it <Ref k="nakagawa2024" p="141–159" />; <Ref
			k="bugaeva2012"
			p="471, 481"
		/>; <Ref k="sakaguchi2024" p="12" />. The Sakhalin verbal plural <i lang="ain-Latn">=hci</i> and
		its relatives attach outside the person-inflected verb; their classification as number rather than
		third-person marking is a point on which Nakagawa and Sakaguchi converge against earlier
		descriptions <Ref k="nakagawa2024" p="151–155, 178" />; <Ref k="sakaguchi2024" p="77–83, 163" />;
		<Ref k="baek2021" p="73" />. Person–number agreement is taken up in <Xr
			ch="person-number-interactions"
		/>.
	</p>

	<p>
		Several fourth-person uses select plural morphology for a single referent. Honorific address to
		one esteemed person and the inclusive ‘you and I’ take the fourth-person form with a plural stem,
		as does much narrative first-person use outside Saru and Chitose <Ref
			k="nakagawa2024"
			p="160–161, 174–179"
		/>; <Ref k="tamura1972" p="28, 34" />.
	</p>

	<Ex
		m="hunak wa arki=an ru-we an"
		g="where from come.PL=4.S track-POSS exist"
		tr="Where have you come from?"
		cite="nakagawa2024:177"
		dial="CHI"
		place="Nakagawa ex. 140, woman to an adult man"
		note="The honorific addressee is a single person, yet the fourth-person S suffix =an appears on the plural stem arki."
	/>

	<p>
		The whole semantic range of the fourth person — inclusive first plural, honorific second person,
		impersonal agent-defocusing, narrative first person, and logophoric reference inside a quotation
		— rests on this one form-set. The old account of an “elevated” literary person distinct from an
		everyday one is contested: Nakagawa assigns the narrative use to viewpoint and text type, Satō
		derives it from a first-person-plural strategy, and Bugaeva analyses the quotation use as
		logophoric and finds it outside folklore as well <Ref k="nakagawa2024" p="174–179, 600–604" />;
		<Ref k="sato2004" p="179–184" />; <Ref k="sato2008" p="259–262" />; <Ref
			k="bugaeva2008"
			p="40–44"
		/> ‹contested›. The fourth-person object <i lang="ain-Latn">i=</i> must be kept apart from the
		homophonous detransitivising prefix <i lang="ain-Latn">i-</i> ‘something’, whose origin is itself
		debated <Ref k="nam2021" p="62–74" />; the detransitiviser belongs to the valency system and is
		treated under <Xr ch="antipassive-detransitive-i">the antipassive</Xr>.
	</p>
</S>

<S t="Mixed alignment and the verb template" id="alignment-template">
	<p>
		Read person by person, the system is mixed in morphological alignment: 1SG is
		nominative–accusative with S=A≠O, the second and third persons are neutral with S=A=O, and the
		first plural exclusive and the fourth person are tripartite with S≠A≠O. Bugaeva’s analysis of
		Hokkaido Ainu rests directly on the S/A/O paradigm and on the side-encoding of
		<i lang="ain-Latn">ci=</i>/<i lang="ain-Latn">=as</i> and
		<i lang="ain-Latn">a=</i>/<i lang="ain-Latn">=an</i>, and she characterises the language as mixed
		but basically tripartite <Ref k="bugaeva2012" p="472, 498" />; <Ref k="bugaeva2008" p="32" />.
	</p>

	<p>
		The person indexes do not split the intransitive subject by agentivity: agentive and patientive
		intransitives take the same S set once valence is fixed, so the agreement paradigm is not
		split-intransitive in the morphological sense <Ref k="nakagawa2024" p="126–127" />. The split
		that the literature pursues under that name surfaces in verb classes, noun incorporation, and
		valency-changing morphology, and is treated in <Xr ch="alignment-split-intransitive" />. The
		competing system-wide analyses — tripartite, hierarchical, inverse — are sharpened by Sakhalin
		work: Dal Corso rejects a tripartite component for Sakhalin in favour of a mixed
		nominative–neutral–inverse account, while warning that the diachrony may not carry over to
		Hokkaido <Ref k="dalcorso2025align" p="187–188, 200, 213" /> ‹contested›. The Hokkaido paradigm
		above supplies the empirical base for <Xr ch="alignment-hierarchical-and-the-debate" />.
	</p>

	<p>
		The personal indexes occupy the outer argument-marking layer of the verb. Subject and object
		indexes precede the stem in transitives, while the intransitive S forms
		<i lang="ain-Latn">=as</i> and <i lang="ain-Latn">=an</i> follow it; derivational material —
		applicatives, causatives, the reflexive, the reciprocal, the antipassive
		<i lang="ain-Latn">i-</i>, and the stem-forming plural <i lang="ain-Latn">-pa</i> — sits closer
		to the root, and a reflexive or reciprocal fills the object slot rather than co-occurring with an
		object index for the same participant <Ref k="tamura1972" p="35" />; <Ref
			k="nakagawa2024"
			p="183, 196–199"
		/>; <Ref k="sato2023b" p="40" />. The full ordering of these positions is the subject of <Xr
			ch="personal-affix-template-and-ordering"
		/>.
	</p>
</S>
