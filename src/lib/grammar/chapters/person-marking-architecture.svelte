	<p>
		The object counterparts of these two sets, <i lang="ain-Latn">un=</i> and
		<i lang="ain-Latn">i=</i>, are prefixal, so each set keeps its three roles formally distinct in
		both shape and position. The fourth-person object <i lang="ain-Latn">i=</i> indexes, among other
		referents, the narrator’s own in oral narrative <Ref k="nakagawa2024" p="169" />:
	</p>

