<S t="The four formal persons" id="four-persons">
	<p>
		Hokkaido Ainu marks person on the finite verb. An intransitive predicate indexes its single
		argument; a transitive predicate indexes the combination of its subject and its object.
		Independent pronouns — <i lang="ain-Latn">kani</i> ‘I’, <i lang="ain-Latn">eani</i> ‘you’,
		<i lang="ain-Latn">coka</i> ‘we (excluding you)’, <i lang="ain-Latn">asinuma</i> ‘the
		fourth-person self’ — are available for emphasis or contrast and are used more sparingly than
		their Japanese counterparts, while the verbal index carries the ordinary work of tracking
		arguments <Ref k="nakagawa2024" p="163–165, 169" />; <Ref k="tamura1972" p="17" />. Satō
		describes the pattern as head-marking in the sense of <Ref k="nichols1986" paren />: the verb,
		the head of its clause, bears the central information about which participants fill its valence
		frame, and a transitive verb may host both a subject index and an object index <Ref
			k="sato2025a"
			p="361"
		/>.
	</p>

	<Ex
		m="orwano ku=cise or ta ku=an kor"
		g="then 1SG.POSS=house place LOC 1SG.S=exist while"
		tr="Then, while I was in my house, …"
		cite="nakagawa2024:167"
		dial="ISH"
		place="Nakagawa ex. 104, after Sunazawa Kura 1983: 75"
		note="No pronoun appears; the intransitive existential ku=an indexes its 1SG.S subject. The possessor ku=cise carries the homophonous first-person prefix in the nominal domain."
	/>

	<p>
		Nakagawa recognises four formal persons, numbered 1 to 4, each with a singular and a plural, and
		treats the numerals as form-classes detached from any fixed speaker-or-addressee value; the
		mapping between form and reference is many-to-one <Ref k="nakagawa2024" p="163" />. The fourth
		person is the limiting case: it is a single form-set into which everything that persons 1 to 3
		cannot express is gathered, so that the referential range of those three is narrowed in turn
		<Ref k="nakagawa2024" p="164, 174" />.
	</p>

	<p>
		Terminology for this set is unsettled. Nakagawa calls it the fourth person; Tamura divides the
		same Saru forms into separate persons — the inclusive, the quotative, the second-person
		honorific, and the indefinite — counting eleven persons in all; Bugaeva uses “indefinite” and
		reanalyses the quotation use as logophoric; Satō treats the forms as polysemous; and Takahashi,
		working on Tokachi, adopts “inclusive person” as the label that unifies the otherwise disparate
		uses <Ref k="nakagawa2024" p="174–179" />; <Ref k="tamura1972" p="18–20, 39" />; <Ref
			k="bugaeva2012"
			p="474–476"
		/>; <Ref k="bugaeva2008" p="40–43" />; <Ref k="sato2008" p="203–208" />; <Ref
			k="takahashi2015"
			p="205–210"
		/> ‹contested›. The form-label “fourth” or “indefinite” is used here; its referential functions
		are set out in <Xr ch="indefinite-fourth-person-paradigm">the indefinite/fourth-person chapter</Xr>
		and <Xr ch="fourth-person-honorific">the honorific and logophoric chapter</Xr>.
	</p>
</S>

<S t="The S/A/O paradigm" id="sao-paradigm">
	<p>
		The person indexes divide into three role sets. <em>S</em> is the sole argument of an
		intransitive verb, <em>A</em> the subject of a transitive verb, and <em>O</em> its object.
		Nakagawa presents these as separate intransitive-subject, transitive-subject, and object tables,
		and Bugaeva’s southern Hokkaido table gives the same Saru–Chitose paradigm in S/A/O terms <Ref
			k="nakagawa2024"
			p="165–169"
		/>; <Ref k="bugaeva2012" p="471" />. The three-way distinction is load-bearing: two persons keep
		distinct forms for all three roles, one opposes an S=A pair to O, and two collapse the roles
		entirely. The rows below give the architectural cell values for the standard Saru–Chitose
		baseline; allomorphy, dialect microvariation, and the person-combination rules belong to the
		chapters that follow.
	</p>

