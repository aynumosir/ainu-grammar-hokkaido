	/>

	<p>
		The choice of <i lang="ain-Latn">a=</i> over <i lang="ain-Latn">an=</i> for the fourth-person A
		is itself dialectal: Saru and the western dialects use <i lang="ain-Latn">a=</i>, while Tokachi,
		Ishikari, Shiranuka, and Samani favour <i lang="ain-Latn">an=</i> under conditions that vary by
		locality, and Nakagawa takes <i lang="ain-Latn">an=</i> to be the older form <Ref
			k="nakagawa2024"
			p="167–168"
		/>.
	</p>

	<p>
		The boundary symbols used throughout the grammar follow a working convention: <i
			lang="ain-Latn">=</i
		> writes the personal-index boundary and <i lang="ain-Latn">-</i> the boundary of derivational and
		number morphology. The convention is deliberately neutral, because the sources disagree about the
		morphological status of the individual indexes. Bugaeva, on prosodic and separability grounds,
		distinguishes word-like <i lang="ain-Latn">=an</i> and <i lang="ain-Latn">=as</i>, clitic-like
		<i lang="ain-Latn">a=</i> and <i lang="ain-Latn">eci=</i>, and fuller prefixes such as
		<i lang="ain-Latn">ku=</i>, <i lang="ain-Latn">en=</i>, and <i lang="ain-Latn">i=</i> that shift
		the accent kernel; Nakagawa writes the whole set with <i lang="ain-Latn">=</i> for convenience
		yet flags the unusual autonomy of <i lang="ain-Latn">=an</i>, which can be separated from its verb;
		Sakaguchi’s explicit clitic/affix criteria, developed for Sakhalin material, likewise rank
		fourth-person <i lang="ain-Latn">=an</i> as the most autonomous subject marker <Ref
			k="bugaeva2012"
			p="472–473"
		/>; <Ref k="nakagawa2024" p="49, 183" />; <Ref k="sakaguchi2024" p="ix, 77–85, 163" />
		‹contested›. The ordering and boundary diagnostics are pursued in <Xr
			ch="personal-affix-template-and-ordering"
		/>.
	</p>
</S>

<S t="Syncretism and portmanteau cells" id="syncretism">
	<p>
		Second person collapses the role distinction: <i lang="ain-Latn">e=</i> marks 2SG as A, S, and
		O, and <i lang="ain-Latn">eci=</i> does the same for 2PL. The same <i lang="ain-Latn">eci=</i>
		additionally serves as the portmanteau exponent for a first-person A acting on a second-person O,
		covering 1SG→2SG, 1SG→2PL, 1PL.EXCL→2SG, and 1PL.EXCL→2PL with a single prefix in place of a
		sequence of first- and second-person indexes <Ref k="tamura1972" p="27–28" />; <Ref
			k="bugaeva2008"
			p="38"
		/>; <Ref k="bugaeva2012" p="474" />; <Ref k="nakagawa2024" p="170, 173" />. The surrounding
		clause resolves the resulting ambiguity, and the cell is developed in <Xr
			ch="eci-portmanteau-and-second-plural"
		/>.
	</p>

	<Ex
		m="nep ka eci=kasuy pe an ya"
		g="something even 1SG.A>2SG.O=help NMLZ exist Q"
		tr="Is there anything I can help you with?"
		cite="nakagawa2024:174"
		dial="SAR"
		place="Nakagawa ex. 128, after Honda 2001: 44"
		note="The single exponent eci= encodes the 1SG.A→2SG.O combination; no separate ku= or e= appears."
	/>

	<p>
		The mirror configuration is less regular. Most dialectal paradigms express 2→1 with a
		second-person A index followed by the first-person object form, but Saru and Chitose drop the
		expected second-person subject and use bare <i lang="ain-Latn">en=</i> for 2SG→1SG, Chitose
		extending the bare form to 2PL→1SG as well <Ref k="nakagawa2024" p="170, 173" />; <Ref
			k="sato2008"
			p="146–147"
		/>.
	</p>

	<p>
		A verb admits at most one subject index and one object index, and no third argument is ever
		indexed. The cap shapes ditransitive marking. With verbs such as <i lang="ain-Latn">kore</i>
		‘give’ the object index falls on the human recipient while the theme stays a bare noun phrase,
		a secundative pattern that Satō takes as further evidence for a zero third-person affix; Nakagawa
		describes the same three-place verbs through valence, with two bare object phrases and neither
		obligatorily indexed when both are third person <Ref k="sato2008" p="147" />; <Ref
			k="sato2023b"
			p="39, 47"
		/>; <Ref k="sato2025a" p="368–371" />; <Ref k="nakagawa2024" p="363–365" /> ‹contested›. Object
		indexing and the three-place verbs are treated in <Xr ch="object-indexing-monotransitive" />.
	</p>

