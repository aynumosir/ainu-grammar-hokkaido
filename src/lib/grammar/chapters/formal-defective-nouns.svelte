<S t="Overview and terminology" id="overview">
	<p>
		Among the four noun subclasses of Hokkaido Ainu, the <em>formal noun</em> (形式名詞
		<em>keishiki meishi</em>) occupies the lowest position in the independence hierarchy: none
		of its members can stand alone as a predicate argument. Each must be preceded by a verb
		phrase, a prenominal, or — in one exceptional case — a bare noun, from which it draws its
		referential content <Ref k="nakagawa2024" p="257" />; <Ref k="nakagawa2022pos" p="480–482" />.
		The diagnostic that separates formal nouns from the three other subclasses (common, locative,
		and pronominal nouns) is set out in <Xr ch="word-class-inventory-and-diagnostics" />.
		Nakagawa also calls them <em>noun particles</em> (名助詞 <em>meijoshi</em>), reflecting
		their particle-like attachment to the preceding element
		<Ref k="nakagawa2024" p="257, 295" />.
	</p>
	<p>
		The terminology used for this class is unsettled ‹contested›.
		<Ref k="sato2008" p="174–178" /> divides what Nakagawa treats as a single class into two:
		<em>nominalizers</em> (名詞化辞 <em>meishikaji</em>) head content clauses with full
		argument structure, while <em>formal nouns</em> proper head relative clauses from which
		one argument is missing. Both are distinct noun subclasses in his account. Nakagawa keeps
		the single class label and instead distinguishes the <em>role</em> a formal noun plays
		clause by clause: as <em>relativizer</em> it heads a clause with a missing argument; as
		<em>complementizer</em> (補文標識 <em>hōbun hyōshiki</em>) it heads a clause with full
		argument structure, the diagnostic being arity — a clause summing to arity zero takes a
		complementizer, one with a net argument gap heads a relative construction
		<Ref k="nakagawa2024" p="503–504" />. <Ref k="refsing1986" p="259–264" /> uses
		<em>nominalizer</em> more broadly still, grouping items that other analyses class as
		mood-morphemes, complementizers, and relative-clause heads within a four-type
		nominalization typology ‹contested›. The full accounts of relativization and
		complementation are in <Xr ch="nominalization-p-pe" /> and
		<Xr ch="lexical-head-formal-noun-nominalization" />; the evidential constructions are in
		<Xr ch="evidential-schema-overview" />.
	</p>
</S>

