<S t="Shared diagnostics" id="diagnostics">
	<p>
		Four properties distinguish formal nouns from the other noun subclasses as a group
		<Ref k="nakagawa2022pos" p="480–482" />.
	</p>
	<p>
		<strong>Obligatory modification.</strong> A formal noun cannot constitute an NP on its own.
		<i lang="ain-Latn">*pe</i>, <i lang="ain-Latn">*hi</i>, and
		<i lang="ain-Latn">*kur</i> are not grammatical arguments in isolation. This places formal
		nouns at the foot of the independence hierarchy set out in Nakagawa's Table 48 — below
		the locative noun's basic form, which at least requires only an immediately preceding NP.
	</p>
	<p>
		<strong>No self-relativization.</strong> A formal noun cannot serve as the head of a
		separate modifying clause in the ordinary sense:
		<i lang="ain-Latn">*pirka ruwe</i> 'a good track (as NP)' and
		<i lang="ain-Latn">*pon kur</i> 'a small person (as NP)' are ungrammatical as true
		relativizations, where an external clause predicates something of the formal noun itself
		<Ref k="nakagawa2022pos" p="480" />.
	</p>
	<p>
		<strong>No free attributive modifier.</strong> When a prenominal demonstrative precedes a
		formal noun, the result is not a compositional NP but a fused pronoun-like form:
		<i lang="ain-Latn">tan</i> + <i lang="ain-Latn">pe</i> →
		<i lang="ain-Latn">tanpe</i> 'this thing',
		<i lang="ain-Latn">toan</i> + <i lang="ain-Latn">pe</i> →
		<i lang="ain-Latn">toanpe</i> 'that thing' <Ref k="nakagawa2024" p="287" />.
	</p>
	<p>
		<strong>Phonological fusion.</strong> <i lang="ain-Latn">p ~ pe</i> and
		<i lang="ain-Latn">hi</i> fuse prosodically with the preceding element, behaving
		synchronically like suffixes, even though each nominalizes the entire preceding VP rather
		than only the adjacent word <Ref k="nakagawa2022pos" p="481" />.
	</p>
	<p>
		<strong>Distribution asymmetry within the class.</strong> The antecedent constraint is
		not uniform: <i lang="ain-Latn">hi</i> requires a VP antecedent;
		<i lang="ain-Latn">p ~ pe</i> takes a VP or a prenominal but not a bare NP;
		<i lang="ain-Latn">utar</i> exceptionally accepts a bare NP as well
		(<i lang="ain-Latn">cikappo utar</i> 'small birds') <Ref k="nakagawa2024" p="258" />;
		<Ref k="nakagawa2022pos" p="482" />.
	</p>
</S>

<S t="The p ~ pe nominalizer" id="p-pe">
	<p>
		The nominalizer <A w="p" gl="thing, one" /> ~ <A w="pe" gl="thing, one" /> is the most
		productive member of the set. It attaches to any VP and yields either a relative-clause
		head (entity reading, with a missing argument) or a content complement (propositional
		reading, full argument structure) <Ref k="sato2008" p="174–178" />;
		<Ref k="nakagawa2024" p="503–506" />. The allomorph is phonologically conditioned:
		<i lang="ain-Latn">p</i> follows a vowel-final VP; <i lang="ain-Latn">pe</i> follows a
		consonant-final VP <Ref k="sato2008" p="177" />.
	</p>

	<Ex
		m="huci na ipe yakne pirka p"
		g="grandmother FIN eat COND be.good NMLZ"
		tr="Grandmother, it would be good if you ate."
		cite="sato2008:177"
		dial="SAR"
		note="p after the vowel-final predicate pirka 'be good'. The clause ipe yakne pirka 'if one eats, (it) is good' is nominalised as a content complement."
	/>

	<Ex
		m="ratcitara rok=an pe ne na"
		g="quietly sit=4.S NMLZ COP FIN"
		tr="One should sit quietly."
		cite="sato2008:177"
		dial="SAR"
		note="pe after the consonant-final clitic =an (4th-person intransitive). The nominalised clause is predicated with the copula ne."
	/>

	<p>
		As a relative-clause head, <i lang="ain-Latn">p ~ pe</i> nominalizes a VP with a missing
		argument:
	</p>

	<Ex
		m="kani ku=respa p anakne"
		g="1SG 1SG.A=raise NMLZ TOP"
		tr="as for the ones I raised …"
		cite="nakagawa2024:214"
		dial="SAR"
		note="Nakagawa ex. 230. p heads a relative clause with a missing object (the raised entities); anakne marks the nominalized clause as a contrastive topic."
	/>

	<p>
		A productive instrumental pattern uses <i lang="ain-Latn">p ~ pe</i> to nominalize an
		applicative VP: <i lang="ain-Latn">ani X p</i> 'the thing with which one does X', as in
		<i lang="ain-Latn">ani e=kanpinuye p</i> 'pencil' (literally 'the thing with which you
		write') and <i lang="ain-Latn">ani humi a=aste p</i> 'musical instrument'
		(nukopoli, aynu-corpora Discord, 2026-05-29) <Ref k="aynucorporadiscord" />. The
		instrumental <i lang="ain-Latn">ani</i> fills the applicative argument slot while
		<i lang="ain-Latn">p</i> nominalizes the whole VP ‹corpus-confirmed›.
	</p>
	<p>
		When a prenominal demonstrative precedes <i lang="ain-Latn">pe</i>, the result is a
		demonstrative pronoun: <i lang="ain-Latn">tanpe</i> 'this (thing)',
		<i lang="ain-Latn">neap</i> 'that previously mentioned thing', and the rest of the series
		treated in <Xr ch="demonstratives-anaphora-definiteness" />
		<Ref k="nakagawa2024" p="287" />. The full syntax of <i lang="ain-Latn">p ~ pe</i> —
		its arity-based relativization/complementation split and clause-linker interactions — is
		in <Xr ch="nominalization-p-pe" />.
	</p>
</S>

<S t="hi and the other general formal nouns" id="hi">
	<p>
		The formal noun <A w="hi" gl="thing, matter, time, place" /> accepts only a VP antecedent
		and characteristically nominalizes the state of affairs described by the clause —
		the content-complement reading — without a missing argument
		<Ref k="nakagawa2022pos" p="482" />.
	</p>

	<Ex
		m="sirkunne hi ka ku=eramuskari-no ku=mokor wa ku=an"
		g="become.dark NMLZ even 1SG.A=not.know-ADV 1SG.S=sleep CONJ 1SG.S=exist"
		tr="I slept on without even knowing that it had grown dark."
		cite="sato2008:175"
		dial="SAR"
		note="Satō §22 ex. 1. hi nominalises the clause sirkunne 'it became dark'; the resulting NP is the complement of eramuskari 'not know'. The antecedent clause has no missing argument, confirming the content-complement function."
	/>

	<p>
		<i lang="ain-Latn">hi</i> also supplies the morphological base of several personal
		pronouns. The first-person singular <i lang="ain-Latn">kani</i> derives from
		<i lang="ain-Latn">ku=an-hi</i> 'the thing/fact of my existing', reduced by regular
		vowel contraction; <i lang="ain-Latn">c(i)oka(y)</i> 'we (exclusive)' from
		<i lang="ain-Latn">ci=oka(y)</i> + reduction; and <i lang="ain-Latn">asinuma</i> from
		<i lang="ain-Latn">a=</i> + <i lang="ain-Latn">sinuma</i>
		<Ref k="nakagawa2024" p="253" />; <Ref k="kindaichi1936" />. The full pronoun paradigm
		is in <Xr ch="independent-personal-pronouns" />.
	</p>
	<p>
		<i lang="ain-Latn">hike</i> 'the one (that …)' is an anaphoric relative head derived from
		<i lang="ain-Latn">hi</i> by the concretizing suffix <i lang="ain-Latn">-ke</i> — the same
		element that yields <i lang="ain-Latn">arke</i> 'half' and <i lang="ain-Latn">sake</i>
		'refrain-melody' from their bases <Ref k="nakagawa2024" p="247–248" />. The suffix
		<i lang="ain-Latn">-ike</i> that appears in lexicalized items such as
		<i lang="ain-Latn">onneike</i> 'parent/elder' (&lt; <i lang="ain-Latn">onne</i> 'old' +
		<i lang="ain-Latn">-ike</i> 'person/entity', per Chiri's gloss; noted by antitwilight,
		aynu-corpora Discord, 2024-11-15) <Ref k="aynucorporadiscord" /> may represent the same
		element in a lexicalized compound; the synchronic productivity of bare
		<i lang="ain-Latn">-ike</i> as a nominalizing suffix is a proposed analysis ‹contested›.
	</p>
	<p>
		<i lang="ain-Latn">katu</i> 'state, manner' nominalizes the manner or condition described
		by a clause; it appears in the frame <i lang="ain-Latn">… katu ene an hi</i> 'such is the
		state of affairs' as a manner complementizer <Ref k="nakagawa2024" p="504" />.
		<i lang="ain-Latn">uske</i> 'place' and <i lang="ain-Latn">usi(ke)</i> 'time/place' head
		NPs denoting the location or occasion of an activity:
	</p>

	<Ex
		m="nanu memke usi ku=oman wa ek ruwe ne"
		g="face shave place 1SG.S=go CONJ come.back track-POSS COP"
		tr="I went to the place where one shaves — a barber's."
		cite="nakagawa2024:258"
		dial="HK"
		note="Nakagawa ex. 330 (Kushiro dialect, citing Yaegashi 2001: 13). usi nominalises the VP nanu memke 'shave one's face', producing an NP denoting a place; the evidential ruwe ne closes the matrix clause."
	/>

	<p>
		The Tokachi/Ishikari form <i lang="ain-Latn">ihi</i> is a regionally restricted variant
		confined in attested data to the fixed frame <i lang="ain-Latn">ene an ihi</i> 'such a
		thing' <Ref k="nakagawa2024" p="257" />.
	</p>
	<p>
		Nakagawa's diachronic hypothesis — that <i lang="ain-Latn">hi</i> is the historical source
		of the affiliative suffix <i lang="ain-Latn">-i</i> (as in
		<i lang="ain-Latn">sik-i(-hi)</i> 'its eye') — is discussed under
		<Xr ch="affiliative-suffix-morphophonology" /> ‹speculative›
		<Ref k="nakagawa2022pos" p="493" />.
	</p>
</S>

<S t="kur and kuni: person and purpose nominals" id="kur-kuni">
	<p>
		The formal noun <A w="kur" gl="person" /> heads NPs denoting a person or agent. As a
		relativizer it takes a VP antecedent. As a class-term head it combines with the adnominal
		<i lang="ain-Latn">un</i> to form demonyms:
		<i lang="ain-Latn">Sar un kur</i> 'a Saru person',
		<i lang="ain-Latn">Iyoci un kur</i> 'a Yoichi person'
		<Ref k="nakagawa2024" p="234" />. In the Hokkaido corpus
		<i lang="ain-Latn">kur</i> ranks at position 98 (2,461 tokens), consistent with its dual
		role as agent-denoting nominalizer in narrative and as a class-term head.
	</p>
	<p>
		The formal noun <A w="kuni" gl="(in order to …; that …)" /> is the purpose and intention
		complementizer. It follows a finite clause and establishes its content as a goal or the
		object of a mental-state verb: with the transitive verb <i lang="ain-Latn">ramu</i>
		'think/believe', the correct embedding is <i lang="ain-Latn">kuni a=ramu</i> 'think that
		…', contrasting with the quotative frame <i lang="ain-Latn">sekor a=ye</i> 'say that …',
		which selects a directly reported assertion rather than a propositional attitude
		(nukopoli, aynu-corpora Discord, 2024-11-09) <Ref k="aynucorporadiscord" />
		‹corpus-confirmed›. The related <A w="kunak" gl="(lest …; whether …)" /> carries a
		negative-purpose or indirect-question reading.
		<Ref k="sato2008" p="175" /> includes both in the nominalizer list alongside
		<i lang="ain-Latn">hi</i> and <i lang="ain-Latn">p ~ pe</i>. Their full syntax —
		interaction with purpose adverbials, desiderative and abilitative predicates, and control
		— is in <Xr ch="kuni-complementizer-purpose-complements" />.
	</p>
</S>

<S t="The sensory-evidential quartet" id="evidential-quartet">
	<p>
		<i lang="ain-Latn">ruwe</i>, <i lang="ain-Latn">siri</i>,
		<i lang="ain-Latn">hawe</i>, and <i lang="ain-Latn">humi</i> are the affiliative forms
		of four sensory nouns and constitute the formal-noun stratum of the evidential system
		<Ref k="nakagawa2024" p="258" />; <Ref k="tamura1996" p="s.v. ruwe, siri" />. Each
		requires a finite VP antecedent, none stands alone as a predicate argument, and each is
		followed by a predicate — typically the copula <i lang="ain-Latn">ne</i> — that closes the
		evidential construction.
	</p>
	<p>
		A morphological point distinguishes evidential use from the full lexical affiliative: the
		lexical form may carry an additional extension
		(<i lang="ain-Latn">ruwehe</i>, <i lang="ain-Latn">humihi</i>), whereas the evidential
		formal noun blocks it — <i lang="ain-Latn">*ruwehe ne</i> is ungrammatical as the
		inferential evidential <Ref k="tamura1996" p="s.v. ruwe" />.
	</p>
	<p>
		Dialect variation affects the morphological shape: southern Hokkaido (Saru, Chitose) uses
		the possessed forms <i lang="ain-Latn">ruwe / siri / hawe / humi</i>, while Tokachi and
		Ishikari often use the bare concept forms <i lang="ain-Latn">ru / sir / haw / hum</i> in
		the same evidential construction <Ref k="nakagawa2024" p="390" />. Both shapes head the
		same frame.
	</p>
	<p>
		The semantic distinctions among the four — the source-type hierarchy, the indirect evidential
		frames (<i lang="ain-Latn">siri an</i>, <i lang="ain-Latn">hawe as</i>), and the scope of
		the evidential over the embedded TAM system — are treated in the dedicated chapters:
		<Xr ch="ruwe-ne-inferential" />, <Xr ch="siri-ne-situational" />,
		<Xr ch="hawe-ne-reportative" />, and <Xr ch="humi-ne-sensory" />. The architecture
		common to all four is in <Xr ch="evidential-schema-overview" />.
	</p>
</S>

<S t="Grammaticalization: from lexical noun to formal noun" id="diachrony">
	<p>
		The sensory-evidential set makes the grammaticalization cline transparent. The lexical noun
		<A w="ru" gl="track, path" /> denotes a physical trace; its affiliative form
		<i lang="ain-Latn">ruwe</i> 'the trace of —' extends to any trace including an epistemic
		one; clause-final <i lang="ain-Latn">[clause] ruwe ne</i> then asserts the clause content
		as established fact <Ref k="tamura1996" p="s.v. ruwe" />;
		<Ref k="nakagawa2024" p="258, 392–396" />. The parallel paths for
		<i lang="ain-Latn">sir</i>, <i lang="ain-Latn">haw</i>, and <i lang="ain-Latn">hum</i>
		are transparent from the inventory table. The result is only partial grammaticalization:
		the lexical source remains recoverable and the choice of post-nominal predicate
		(<i lang="ain-Latn">ne</i> vs <i lang="ain-Latn">an</i>) still matters, placing these
		forms at an intermediate point on the cline <Ref k="nakagawa2024" p="393" />.
	</p>
	<p>
		The non-evidential formal nouns stand at a later stage.
		<i lang="ain-Latn">hi</i> has no independent lexical noun use in modern Hokkaido Ainu; its
		original sense survives only in the pronoun-formation structures described above and,
		by Nakagawa's hypothesis, in the affiliative suffix <i lang="ain-Latn">-i</i>.
		<i lang="ain-Latn">p ~ pe</i> is likewise fully bound; its affiliative-form origin in the
		monosyllabic open-syllable class
		(cf. <i lang="ain-Latn">ru</i> → <i lang="ain-Latn">ruwe(he)</i>,
		<i lang="ain-Latn">ci</i> → <i lang="ain-Latn">ciye(he)</i>) is preserved in the
		paradigm of monosyllabic nouns but no longer lexically active
