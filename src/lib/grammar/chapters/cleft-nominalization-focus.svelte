<S t="Overview" id="overview">
	<p>
		Hokkaido Ainu has no morphological focus affix and no dedicated cleft particle. The work of
		marking focus, asserting propositions, and identifying referents falls to the same
		nominalization-plus-copula architecture that underlies the evidential system. A finite clause
		is nominalized by a light formal noun — <i lang="ain-Latn">p</i>/<i lang="ain-Latn">pe</i>,
		<i lang="ain-Latn">hi</i>, or one of the four evidential heads <i lang="ain-Latn">ruwe</i>,
		<i lang="ain-Latn">hawe</i>, <i lang="ain-Latn">siri</i>, <i lang="ain-Latn">humi</i> —
		and the result is predicated with the copula <i lang="ain-Latn">ne</i>
		<Ref k="nakagawa2024" p="390–392, 503–509" />; <Ref k="sato2008" p="134–135, 174–180" />.
		The copula predicates the nominalized proposition as an asserted or identified fact, and
		the choice of formal noun and of the post-nominal predicate (<i lang="ain-Latn">ne</i>,
		<i lang="ain-Latn">an</i>, or bare) encodes illocutionary force — declarative, content
		question, or polar question (§5 below).
	</p>
	<p>
		This chapter treats the information-structural function of three overlapping constructions:
		(a) the <i lang="ain-Latn">p</i>/<i lang="ain-Latn">pe</i> and
		<i lang="ain-Latn">hi</i> nominalization cleft, which throws assertive or identificational
		focus onto a participant or proposition; (b) the naming cleft
		<i lang="ain-Latn">sekor a=ye p/hawe/katu ne</i>, which identifies or labels a referent;
		and (c) the evidential frame as an assertion-focus device, with cross-references to the
		evidential chapters. Focus particles (<A w="patek" gl="only" />,
		<A w="ka" gl="also, even" />) and the kakari-musubi emphatic concord are surveyed in §7
		with a pointer to <Xr ch="topic-marking-anakne" />, where their full treatment appears.
		The structural mechanics of <i lang="ain-Latn">p</i>/<i lang="ain-Latn">hi</i> as
		nominalizers are in <Xr ch="nominalization-p-pe" /> and
		<Xr ch="formal-defective-nouns" />; the copula is treated in
		<Xr ch="copula-ne-predicate-nominals" />.
	</p>
</S>

<S t="The p/pe and hi nominalization cleft" id="p-hi-cleft">
	<p>
		The minimal frame is a person-marked VP followed by the nominalizer
		<A w="p" gl="thing, one, fact (nominalizer)" /> (after a vowel) or
		<A w="pe" gl="thing, one, fact (nominalizer)" /> (after a consonant), and then the
		copula <A w="ne" gl="be, copula" />. The nominalizer has the arity of a formal noun
		(−1 on Nakagawa's arity scale), so it saturates a gap left by one participant in the
		embedded clause — producing a relative-clause reading ('the one who …', 'the thing that
		…') — or nominalizes a whole saturated clause — producing a complement reading ('the
		fact that …') <Ref k="nakagawa2024" p="261, 503–509" />; <Ref k="sato2008" p="174–180" />.
		When the result is predicated with <i lang="ain-Latn">ne</i>, the copular sentence frames
		the nominalized content as an assertable fact and throws focus onto either the predicate
		nominal or the proposition as a whole <Ref k="sato2008" p="174–177" />.
	</p>
	<p>
		Ijäs's lesson on adnominal clauses presents the same pre-head gap strategy
		without treating the copular focus function explicitly
		<Ref k="ijas2023" paren />. The arity principle that distinguishes relative-clause from
		complement uses is stated precisely only in <Ref k="nakagawa2024" p="503–507" />
		and <Ref k="sato2008" p="174–175" />, and is the lens through which the focus function
		is understood here.
	</p>

	<S t="Argument focus and predicate focus" id="argument-predicate-focus">
		<p>
			Two focus readings arise depending on whether a participant gap remains in the embedded VP.
			In the <em>argument-focus</em> reading the nominalizer heads a relative clause: the embedded
			VP lacks one of its required participants, and the focused element — supplied as the
			predicate nominal of the copula — identifies that participant. A minimal constructed
			illustration:
		</p>

		<Ex
			m="kani ku=e pe ne"
			g="1SG 1SG.A=eat NMLZ COP"
			tr="I am the one who ate it."
			constructed
			dial="SAR"
			note="The agent of ku=e 'I eat' provides the focused argument: the question implied is 'who ate it?' and kani 'I' is the copular predicate supplying the answer. The nominalizer pe leaves the VP with an unfilled object slot (arity +1 after the agent is indexed by ku=), creating the relative-clause gap that the focused predicate nominal fills."
		/>

		<p>
			In the <em>predicate-focus</em> reading the embedded VP is fully saturated — the fourth
			person <i lang="ain-Latn">a=</i> indexes the generic agent — and the nominalizer
			nominalizes the whole proposition; the predicate nominal then supplies a property or
			category asserted of the nominalized event:
		</p>

		<Ex
			m="cep a=e pe ne"
			g="fish 4.A=eat NMLZ COP"
			tr="What one eats is fish."
			constructed
			dial="SAR"
			note="Here the embedded clause a=e 'one eats (it)' is transitively saturated by the fourth-person agent a= and an implicit third-person object. The nominalizer pe then heads the NP 'the thing one eats', which is predicated as identical to cep 'fish': predicate-nominal focus. The same structure allows a proposition to be asserted as established fact when no explicit contrastive focus is intended."
		/>

		<p>
			The abstract nominalizer <A w="hi" gl="fact, place, time (nominalizer)" /> works in the
			same slot but is more abstract: Satō glosses it as 'こと / の / 時' and notes that it
			"has almost no concrete meaning and nominalizes the whole clause"
			<Ref k="sato2008" p="174, 169" />. The distinction between
			<i lang="ain-Latn">p</i>/<i lang="ain-Latn">pe</i> and
			<i lang="ain-Latn">hi</i> is one of concreteness: <i lang="ain-Latn">p</i>/<i
				lang="ain-Latn">pe</i
			> identifies a person, thing, or event; <i lang="ain-Latn">hi</i> identifies a fact,
			time, or circumstance. Both combine with <i lang="ain-Latn">ne</i> for declarative force
			and with <i lang="ain-Latn">an</i> for content-question force (§5).
		</p>

		<p>
			When the topical item is itself a nominalized clause, the topic particle
			<A w="anak" gl="as for, topic" /> can mark it, producing a construction that combines
			topic and focus in a single sentence. Satō's example of contrastive
			<i lang="ain-Latn">anak</i> illustrates this interaction:
		</p>

		<Ex
			m="ku=nu hi anak k=eramuan pe ne korka tasa itak anak k=eranpewtek"
			g="1SG.A=hear NMLZ TOP 1SG.A=understand NMLZ COP CONJ reply speech TOP 1SG.A=not.know"
			tr="The question I heard I understand, but how to reply I do not know."
			cite="sato2008:62"
			dial="SAR"
			note="Both topic-marked NPs are nominalized clauses: hi nominalizes the VP ku=nu 'I heard (it)' and pe ne predicates the resulting proposition as understood fact. The second clause, tasa itak anak k=eranpewtek, contrasts it with the topic 'the reply', which lacks an explicit pe ne because the bare VP k=eranpewtek closes the sentence. The two anak forms are contrastive, not merely thematic; see topic-marking-anakne for the anak system."
		/>
	</S>
</S>

<S t="The naming cleft: sekor a=ye … ne" id="naming-cleft">
	<p>
		The naming cleft identifies a referent by the name or label applied to it. Its standard
		formula is <i lang="ain-Latn">[label] sekor a=ye [formal-noun] ne</i>, 'the [word/fact]
		of calling [something] [label] is'. The quotative complementizer
		<A w="sekor" gl="quotative" /> introduces the quoted name (see
		<Xr ch="quotative-complementation-sekor" />), and a formal noun — most commonly
		<i lang="ain-Latn">p</i>/<i lang="ain-Latn">pe</i>, or the voice-noun
		<i lang="ain-Latn">hawe</i> 'word of', or the manner-noun
		<i lang="ain-Latn">katu</i> 'way of calling' — nominalizes the whole
		<i lang="ain-Latn">sekor a=ye</i> clause. The copula
		<i lang="ain-Latn">ne</i> then predicates it:
	</p>

	<Ex
		m="tap a=ye katu Piratur sekor a=ye hawe ne"
		g="now 4.A=say manner-POSS Piratur QUOT 4.A=say voice-POSS COP"
		tr="The current way of calling it is called Piratur."
		cite="nakagawa2024:393"
		dial="SAR"
		place="Nakagawa ex. 778, after Kawamura 1985: 30"
		note="The formal noun katu 'manner of' (affiliative of kat) nominalizes the VP tap a=ye 'one currently says/calls it', forming the topic 'the current manner of calling it'. The naming cleft Piratur sekor a=ye hawe ne then predicates that the word (hawe, affiliative of haw 'voice') of saying it is 'Piratur'. The choice of hawe over p highlights the auditory/linguistic character of the name."
	/>

	<p>
		The variant with <i lang="ain-Latn">p</i> produces the most common naming formula —
		<i lang="ain-Latn">[name] sekor a=ye p ne</i> — which the community has discussed for its
		valency structure: the nominalizer <i lang="ain-Latn">p</i> here is
		analysed as <em>zero-valent</em> (not filling an object slot in
		<i lang="ain-Latn">a=ye</i>), because the transitive verb
		<i lang="ain-Latn">ye</i> (+2) is already saturated by the fourth-person agent
		<i lang="ain-Latn">a=</i> (−1) and the implicit zero third-person object (−1), leaving
		<i lang="ain-Latn">p</i> to nominalize the whole proposition rather than fill a
		participant gap <Ref k="aynucorporadiscord" /> (nukopoli, 2024-11-09) ‹contested›.
		This analysis aligns with Nakagawa's arity account: the embedded clause is saturated
		(arity 0), so <i lang="ain-Latn">p</i> functions as a complement nominalizer rather than
		a relative-clause head <Ref k="nakagawa2024" p="507–509" />.
	</p>

	<p>
		The logophoric narration that pervades yukar oral literature produces naming-cleft structures
		in contexts where the fourth-person <i lang="ain-Latn">a=</i> references the narrator
		voicing a protagonist (see <Xr ch="indefinite-fourth-person-paradigm" /> and
		<Xr ch="logophoric-narration-and-reported-discourse" />). In such passages
		<i lang="ain-Latn">sekor a=ye p ne</i> and its variant with
		<i lang="ain-Latn">hawe</i> are a stock device for supplying proper names and category
		labels within the narrative <Ref k="nakagawa2024" p="392" />.
	</p>
</S>

<S t="True cleft, pseudocleft, or nominal predication?" id="cleft-analysis">
	<p>
		Whether <i lang="ain-Latn">[VP] p/hi ne</i> should be called a cleft, a pseudocleft, or
		a nominal predication depends on two analytical choices that remain unresolved in the
		literature: the structural status of the copula <i lang="ain-Latn">ne</i>, and the
		source-language content of "cleft" as a cross-linguistic category.
	</p>
	<p>
		The <em>nominal-predication</em> position, implicit in Nakagawa's arity account, treats
		<i lang="ain-Latn">[VP] p ne</i> as a copular sentence with the nominalized NP
		<i lang="ain-Latn">[VP] p</i> as one argument and the predicate nominal as the other.
		Nothing biclausal is required; the embedded VP is a finite clause that happens to be
		nominalized, and <i lang="ain-Latn">ne</i> is the ordinary copula
		<Ref k="nakagawa2024" p="503–509" />. On this reading the construction is formally
		parallel to any other copular sentence — 'the thing one eats is fish' is no different in
		type from 'this thing is a fish' — and calling it a cleft imports a typological category
		from languages with a structurally distinct cleft construction.
	</p>
	<p>
		The <em>biclausal cleft</em> position holds that the embedded clause bears the full
		person and TAM marking of a main clause, and the copula is a full predicate verb; the
		resulting structure is therefore biclausal — a main copular clause whose subject is
		a nominalized subordinate clause. This is the reading Shibatani adopts when he calls
		<i lang="ain-Latn">-p(e)</i> and <i lang="ain-Latn">-i</i> "nominationalizers" and
		treats the construction as "appositive nominalization"
		<Ref k="shibatani1990" p="83–84" />. The embedded clause retains full finiteness (person
		marking, number-sensitive verb choice, aspect, negation), which Bugaeva treats as the
		defining typological feature of Ainu nominalization: because there is no finite/non-finite
		morphological contrast, every embedded VP looks like a main clause, and nominalization is
		always formally overt, never zero <Ref k="bugaeva2016" p="100, 222–225" />.
	</p>
	<p>
		Neither analysis settles whether these are "true" clefts in the narrow sense of a
		bi-clausal structure with an empty-headed relative clause as one component. In many
		typological frameworks a cleft requires a relativized pivot; in
		<i lang="ain-Latn">[VP] p ne</i> the nominalizer <i lang="ain-Latn">p</i> may head
		a relative clause (when the embedded VP is unsaturated) or a complement clause (when
		it is saturated). On Nakagawa's arity principle, the choice is grammatically determined
		case by case, so some tokens of the surface pattern are structurally clefts and others
		are nominal predications ‹contested›. Satō's diagnostic — a nominalizer nominalizes a
		complete clause; a formal noun heads a relative clause with a recoverable antecedent —
		attempts to sort the cases but concedes the boundary is blurry
		<Ref k="sato2008" p="174–175" />.
	</p>
	<p>
		Whether or not the label "cleft" applies, the IS function is consistent: the copular frame
		<i lang="ain-Latn">[nominalized-clause] ne</i> focuses the predicate nominal and
		foregrounds the nominalized content as a recoverable, asserted fact ‹consensus›. The
		debate over the structural label does not affect the description of that function. The
		copula <i lang="ain-Latn">ne</i> and the question of its verbal status are treated in
		<Xr ch="copula-ne-predicate-nominals" />; the analysis of the embedded clause's
		finiteness and constituency bears on the unresolved questions there.
	</p>
</S>

<S t="Illocutionary force: the ne/an/∅ alternation" id="ne-an-alternation">
	<p>
		The tightest structural fact about the formal-noun system is that the copula slot
		following the formal noun determines the illocutionary type of the whole clause. Satō
		and Nakagawa document a four-way paradigm shared by all formal nouns
		<Ref k="sato2008" p="136–137" />; <Ref k="nakagawa2024" p="392–396" />:
	</p>

	<table>
		<thead>
			<tr>
				<th>sentence type</th>
				<th>formal-noun tail</th>
				<th>example</th>
			</tr>
		</thead>
		<tbody>
			<tr>
				<td>declarative (asserted fact)</td>
				<td>NMLZ + <i lang="ain-Latn">ne</i></td>
				<td>
					<i lang="ain-Latn">sirpirka ruwe ne</i> 'it is that the weather is fine'
				</td>
			</tr>
			<tr>
				<td>content (wh) question</td>
				<td>NMLZ + <i lang="ain-Latn">an</i></td>
				<td>
					<i lang="ain-Latn">ruwe an hi an</i> 'what is the reason that …?'
				</td>
			</tr>
			<tr>
				<td>polar (yes/no) question</td>
				<td>NMLZ bare (copula omitted)</td>
				<td>
					<i lang="ain-Latn">e=eraman hawe?</i> 'did you understand?'
				</td>
			</tr>
			<tr>
				<td>affirmative answer</td>
				<td>NMLZ + <i lang="ain-Latn">un</i></td>
				<td>
					<i lang="ain-Latn">ku=nu ruwe un</i> 'yes, I heard it'
				</td>
			</tr>
		</tbody>
	</table>

	<p>
		Satō's motivation for the three-way split is precise: the declarative
		<i lang="ain-Latn">ne</i> predicates the subject's <em>nature</em> or identity; the
		content-question <i lang="ain-Latn">an</i> asserts that the described entity
		<em>exists</em> while its nature is the open question; and when even existence is at
		issue, the copula drops entirely for the polar question
		<Ref k="sato2008" p="136–137" />. The content-question frame obligatorily uses
		the existential <i lang="ain-Latn">an</i>: the frame
		<i lang="ain-Latn">*ruwe ne</i> with a wh-word in the same clause is ill-formed; the
		wh-word forces the switch to <i lang="ain-Latn">an</i>. This is a rare case in Ainu
		where the choice of formal-noun copula is not freely pragmatic but grammatically
		determined by the presence of an interrogative pro-form (see
		<Xr ch="interrogative-strategies-question-particles" />).
	</p>

	<Ex
		m="hemanta ne kusu ene siran hi an"
		g="why COP because thus appear NMLZ exist"
		tr="Why has the situation come to be like this?"
		cite="nakagawa2024:411"
		dial="SAR"
		place="Nakagawa ex. 856"
		note="The wh-word hemanta ('why/for what reason') forces the existential an in place of the copula ne after the formal noun hi. The structure hemanta ne kusu is 'why/for what reason', and ene siran 'appearing thus' is the embedded VP. Content questions with a formal-noun tail systematically use NMLZ an (not NMLZ ne)."
	/>

	<p>
		The same paradigm extends to <i lang="ain-Latn">p</i>/<i lang="ain-Latn">pe</i>:
		a content question uses <i lang="ain-Latn">p an</i> (with a wh-word) and the polar
		question uses bare <i lang="ain-Latn">p</i> or <i lang="ain-Latn">pe</i>, while
		<i lang="ain-Latn">pe ne ya?</i> with the question final particle
		<i lang="ain-Latn">ya</i> is also attested for content questions
		<Ref k="nakagawa2024" p="409–410" />. The full range of interrogative strategies,
		including the polar-question particle
		<A w="he" gl="question particle (focus-scoping)" /> and its interaction with the bare
		formal-noun frame, is in <Xr ch="interrogative-strategies-question-particles" />.
	</p>
</S>

<S t="The evidential frame as focus and assertion" id="evidential-focus">
	<p>
		The four evidential formal nouns <i lang="ain-Latn">ruwe</i>,
		<i lang="ain-Latn">hawe</i>, <i lang="ain-Latn">siri</i>, and
		<i lang="ain-Latn">humi</i> build exactly the same nominalization-plus-copula frame,
		and they carry a corresponding IS contribution: they frame the embedded proposition as
		established, speaker-endorsed fact. Nakagawa explicitly treats these formal nouns as
		complementizers of the same type as <i lang="ain-Latn">p</i>/<i lang="ain-Latn">hi</i>
		<Ref k="nakagawa2024" p="508" />.
	</p>
	<p>
		The IS function of each evidential frame differs by the nature of the speaker's epistemic
		access. <i lang="ain-Latn">ruwe ne</i>, the most common and most grammaticalized of the
		four (comprising roughly 86% of evidential tokens in the Hokkaido corpus;
		<Xr ch="ruwe-ne-inferential" />), presents the content as a known or inferred fact —
		'it is the trace/fact that …' — and serves as the workhorse of narrative assertion and
		scene-boundary marking. Community discussion confirms that
		<i lang="ain-Latn">ruwe ne</i> does scene-shift and summary (総括) work, is particularly
		frequent in folktales and legends, and is used notably more by male speakers in oral
		literature than by female <Ref k="aynucorporadiscord" /> (nukopoli, 2024-11-09)
		‹corpus-suggested›. Hirosawa connects this to its information-structural role as a
		marker of discourse-new content within the speaker's territory of information
		<Ref k="hirosawa2026" p="75–81" />.
	</p>
	<p>
		The full semantics, the male-register skew (a register fact of the evidential system,
		distinct from the gendered sentence-final particles treated in
		<Xr ch="gendered-register-speech" />; compare <Xr ch="sentence-final-particles-illocutionary-force" />),
		and the interaction with mirativity are described in the evidential chapters:
		<Xr ch="evidential-schema-overview" />, <Xr ch="ruwe-ne-inferential" />,
		<Xr ch="hawe-ne-reportative" />, <Xr ch="siri-ne-situational" />,
		<Xr ch="humi-ne-sensory" />.
	</p>
	<p>
		One cross-cutting point that bears on this chapter: the ordering constraint that places
		the evidential frame below connectives and final particles
		(<i lang="ain-Latn">kusu ne ruwe ne</i> ✓, <i lang="ain-Latn">*ruwe ne kusu ne</i> ✗)
		<Ref k="nakagawa2024" p="393" /> is a syntactic reflex of the same nominalization
		architecture — the evidential NP is itself an argument position in the clause-linkage
		hierarchy, not a free adjunct. Community discussion confirms this ordering
		<Ref k="aynucorporadiscord" /> (nukopoli, 2024-11-09) ‹corpus-confirmed›.
	</p>
</S>

<S t="Focus particles and kakari-musubi concord" id="focus-particles-concord">
	<p>
		Hokkaido Ainu marks delimiting and scalar focus with a class of adverbial particles
		(副助詞) that add interpretive nuance to a following verb phrase, noun, or adverb
		without changing its grammatical category
		<Ref k="nakagawa2024" p="317–330" />; <Ref k="sato2008" p="63–70" />;
		<Ref k="fukuda1961" />. The core focus particles —
		<A w="patek" gl="only, nothing but" />, <A w="ka" gl="also, even" />,
		<A w="hem" gl="also (additive)" /> — are described with their full distributional and
		discourse properties in <Xr ch="topic-marking-anakne" />. The present section notes only
		the points where they interact with the nominalization-based focus system.
	</p>
	<p>
		<i lang="ain-Latn">patek</i> 'only/nothing but' marks exhaustive focus. It attaches
		after a VP with the support of dummy <i lang="ain-Latn">ki</i> 'do' to re-verbalize
		the construction for downstream connectives
		<Ref k="nakagawa2024" p="317, 327–328" />; <Ref k="fukuda1961" />.
		The community notes that <i lang="ain-Latn">patek</i> is an adverb and cannot itself
		be nominalized as 'the only one of —' (unlike Japanese だけ, which derives from a noun);
		the rising modern pattern <i lang="ain-Latn">patek ani</i> instead of traditional
		<i lang="ain-Latn">ani patek</i> is attributed to Japanese-contact influence
		<Ref k="aynucorporadiscord" /> (nukopoli, 2024-12-16) ‹contested›.
	</p>
	<p>
		Closer to a grammaticalized focus-concord is the <em>kakari-musubi</em> (係り結び)
		system first identified by Chiri and described for Saru by Fukuda
		<Ref k="fukuda1961" />: a small set of emphatic particles
		concords with a specific sentence-final particle at the clause end. Nakagawa and Satō
		document the two productive Saru pairings
		<Ref k="nakagawa2024" p="330" />; <Ref k="sato2008" p="73" />:
	</p>

	<table>
		<thead>
			<tr>
				<th>emphatic particle</th>
				<th>gloss</th>
				<th>concordant SFP</th>
				<th>reading</th>
			</tr>
		</thead>
		<tbody>
			<tr>
				<td><i lang="ain-Latn">ta</i></td>
				<td>'こそ' (emotional emphasis)</td>
				<td><i lang="ain-Latn">oka</i> (optional)</td>
				<td>counterfactual wish '〜だったらなあ'</td>
			</tr>
			<tr>
				<td><i lang="ain-Latn">tas(i)</i></td>
				<td>'まさに〜こそ' (assured emphasis)</td>
				<td><i lang="ain-Latn">nek</i> (optional)</td>
				<td>emphatic assertion '〜のだよ'</td>
			</tr>
		</tbody>
	</table>

	<p>
		The closing particle is optional in Saru — "not necessarily present"
		<Ref k="fukuda1961" /> — which complicates calling this a true productive
		focus-concord on the model of Japanese kakari-musubi. Whether the HA system constitutes
		a grammaticalized construction or a set of frozen collocations is open
