<S t="Place in the evidential paradigm" id="paradigm">
	<p>
		<i lang="ain-Latn">hawe ne</i> is the reportative member of the Hokkaido Ainu evidential
		paradigm. Like its three siblings it consists of a
		<Xr ch="formal-defective-nouns">formal noun</Xr> in the
		<Xr ch="concept-form-affiliative-form">possessed form</Xr> followed by the copula
		<A w="ne" gl="be, copula" />: where <i lang="ain-Latn">ruwe ne</i> signals a fact
		recognized through knowledge or reasoning, <i lang="ain-Latn">siri ne</i> a directly
		visible situation, and <i lang="ain-Latn">humi ne</i> a non-visual sensation,
		<i lang="ain-Latn">hawe ne</i> signals that the information was acquired verbally —
		through hearing someone speak, receiving a report, or encountering hearsay. The
		architecture common to all four is set out in <Xr ch="evidential-schema-overview" />; the
		other three evidentials are treated in
		<Xr ch="ruwe-ne-inferential">the inferential <i lang="ain-Latn">ruwe ne</i></Xr>,
		<Xr ch="siri-ne-situational">the situational <i lang="ain-Latn">siri ne</i></Xr>, and
		<Xr ch="humi-ne-sensory">the sensory <i lang="ain-Latn">humi ne</i></Xr>.
	</p>
	<p>
		In Dal Corso's eight-form analysis, <i lang="ain-Latn">hawe ne</i> is the direct
		evidential of hearing (DIR.HRN), the lowest-reliability direct form in the paradigm; its
		indirect counterpart is <i lang="ain-Latn">hawe as</i> (IND.HRN), treated in
		§<i lang="ain-Latn">hawe as</i> below <Ref k="dalcorso2018" p="304–306, 337" />. In Dal
		Corso's reference texts, <i lang="ain-Latn">hawe ne</i> accounts for 298 of 2232
		evidential tokens (13.35%), and <i lang="ain-Latn">hawe as</i> for a further 94 (4.21%),
		making the two <i lang="ain-Latn">hawe</i>-forms together the second most frequent pair
		after <i lang="ain-Latn">ruwe ne</i> <Ref k="dalcorso2018" p="72" />.
	</p>
</S>

<S t="Morphology: hawe is haw-e, 'the voice/word of —'" id="morphology">
	<p>
		The source noun is <A w="haw" gl="voice" />. Tamura's Saru dictionary defines
		<i lang="ain-Latn">haw</i> as '(人間・動物の)声、(バイオリンなどの)音' — the voice of a human or
		animal and the sound of certain instruments; general inanimate sounds such as a drum are
		expressed instead with the separate noun <A w="hum" gl="sound, feeling" />
		<Ref k="tamura1996" p="s.v. haw, hawe" />. The ordinary possessed form is
		<i lang="ain-Latn">hawehe</i> 'the voice of —'. The evidential formal noun is
		<i lang="ain-Latn">hawe</i> = <i lang="ain-Latn">haw-e</i>, the voice noun plus its
		possessive suffix, but like the other formal nouns of the evidential paradigm it blocks
		the additional possessive extension: the form with a further
		<i lang="ain-Latn">-he</i> is ungrammatical as the evidential
		(<i lang="ain-Latn">*hawehe</i>) <Ref k="tamura1996" p="s.v. hawe" />;
		<Ref k="nakagawa2024" p="258" />. Glossing it as <em>voice-POSS</em> tracks this
		morphological origin.
	</p>
	<p>
		The lexical noun <A w="haw" gl="voice" /> remains productive alongside the formal noun.
		The community notes the prohibitive construction <i lang="ain-Latn">iteki e=hawe aste</i>
		'do not raise your voice' — where <i lang="ain-Latn">e=hawe</i> is the second-person
		possessed form of <i lang="ain-Latn">haw</i> and <A w="aste" gl="raise, cause to stand" />
		is the lexical verb — as evidence that the person-marked lexical noun is still active,
		distinct from the evidential formal noun which takes no person prefix
		<Ref k="aynucorporadiscord" /> (nukopoli, aynu-corpora Discord, 2025-12-03) ‹contested›.
		A related observation: <i lang="ain-Latn">sekor haw=an</i> '(one) says thus' and
		<i lang="ain-Latn">sekor isoytak</i> '(one) narrates thus' are both attested as
		quotative expressions, differing in whether a general act of speaking or more formal
		narrating is meant <Ref k="aynucorporadiscord" /> (thegodofneet, aynu-corpora Discord,
		2025-06-03) ‹corpus-suggested›. In <i lang="ain-Latn">sekor haw=an</i> the verb
		<i lang="ain-Latn">haw=an</i> is the lexical 'to speak / give voice', not the formal
		noun; the two functions of the root coexist.
	</p>
	<p>
		Chiri described the diachronic shift as running from 'there is a voice/rumor of X'
		(<span lang="ja">「云々の声がある」「云々の噂がある」</span>) to the hearsay assertive
		<Ref k="kindaichi1936" /> (cited via <Ref k="hirosawa2026" p="66" />), following the
		same cline documented for all four evidentials: sensory noun &gt; possessed formal noun
		&gt; clause-final evidential <Ref k="takahashi2018" p="111" />.
	</p>
</S>

<S t="Meaning: verbal source and reportative function" id="semantics">
	<p>
		Nakagawa defines <i lang="ain-Latn">hawe ne</i> as indicating that the speaker judges
		from information received verbally (<span lang="ja">言葉で聞いた情報から判断していること</span>)
		<Ref k="nakagawa2024" p="392" />. Three overlapping functions are documented in the
		sources.
	</p>
	<p>
		The core use is the <em>direct report</em>: the speaker heard a specific person say
		something, and <i lang="ain-Latn">hawe ne</i> encodes that hearing as the information
		source. Tamura describes this as '誰かが言ったことに基づいて述べる; 他人の発言の紹介、自分の発言の
		さらなる説明' — presenting what someone said, or elaborating on one's own earlier statement
		<Ref k="tamura1996" p="s.v. hawe" /> (cited via <Ref k="hirosawa2026" p="67" />).
	</p>
	<p>
		The second function is <em>hearsay</em>: the information reaches the speaker through
		general social knowledge or rumor rather than from a named interlocutor. The example in
		Hirosawa (2026: 71, ex. 3), where a narrator attributes a plague to a divine figure's
		anger on the basis of circulating report rather than personal reasoning, illustrates
		this function; the example is drawn from Tamura 1988 audio materials (narrator Wateke,
		Saru) <Ref k="hirosawa2026" p="71" />.
	</p>
	<p>
		Third, Nakagawa notes for Chitose that <i lang="ain-Latn">hawe ne</i> may mark the
		speaker's <em>own</em> statement, as in the framing expression
		<i lang="ain-Latn">hawe ne yakun</i> 'if that is the case / given those words'
		<Ref k="nakagawa1995" /> (cited via <Ref k="hirosawa2026" p="68" />) ‹corpus-suggested›.
		In this use, the form makes explicit the verbal nature of what was just said.
	</p>
	<p>
		Dal Corso places <i lang="ain-Latn">hawe ne</i> at the lowest-reliability position of
		the four direct evidentials, reflecting the sense hierarchy
		sight&nbsp;&gt;&nbsp;touch/taste/smell&nbsp;&gt;&nbsp;hearing. Source reliability in his
		framework is a property of the acquisition channel, not a claim about epistemic certainty
		— <i lang="ain-Latn">hawe ne</i> specifies how the information was obtained without
		committing the speaker to a degree of confidence in its truth
		<Ref k="dalcorso2018" p="305–306, 327, 339" /> ‹consensus›.
	</p>
	<p>
		Hirosawa (2026: 81) observes that near-identical sentences can alternate
		<i lang="ain-Latn">hawe ne</i> and <i lang="ain-Latn">ruwe ne</i> — one version
		presenting the proposition as heard speech, the other as assimilated knowledge — without
		a sharp semantic break <Ref k="hirosawa2026" p="81" />; <Ref k="dalcorso2018" p="306, 339" />.
		The boundary between the two forms is gradient rather than categorical ‹contested›.
	</p>
</S>

<S t="The sekor … hawe ne quotative frame" id="quotative-frame">
	<p>
		The most prominent environment for <i lang="ain-Latn">hawe ne</i> in the Saru and
		Chitose corpus is the quotative frame built with the complementizer
		<A w="sekor" gl="(quotative)" />. A clause of quoted content is followed by
		<i lang="ain-Latn">sekor</i> and a speaking verb, with <i lang="ain-Latn">hawe ne</i>
		at the clause edge:
	</p>
	<p>
		<i lang="ain-Latn">[quoted content] sekor [verb of saying] hawe ne</i>
	</p>
	<p>
		<i lang="ain-Latn">sekor</i> marks the boundary of the quoted content; the speaking
		verb specifies the act of utterance; <i lang="ain-Latn">hawe ne</i> then asserts that
		the speaker's information derives from hearing. The full analysis of
		<i lang="ain-Latn">sekor</i> and direct speech framing is in
		<Xr ch="quotative-complementation-sekor" />. The use of reported-speech frames in oral
		literature is described further in <Xr ch="logophoric-narration-and-reported-discourse" />.
	</p>

	<Ex
		m="tap a=ye katu Piratur sekor a=ye haw-e ne"
		g="thus 4.A=say manner Biratori QUOT 4.A=say voice-POSS COP"
		tr="The way one says it is 'Biratori' (so they call it)."
		cite="nakagawa2024:393"
		dial="SAR"
		place="Nakagawa ex. 778; underlying source Kawamura 1985: 30, Saru"
		note="hawe ne predicates the naming 'Biratori' as verbally transmitted knowledge — the speaker heard this name used, rather than independently assigning it. Compare the ruwe ne version at Hirosawa (2026: 81, ex. 24), where the same naming is presented as assimilated personal knowledge, illustrating the gradient boundary between the two forms."
	/>

	<p>
		The community has proposed that Saru <i lang="ain-Latn">sekor</i> is analytically
		<i lang="ain-Latn">se + kor</i> 'saying while holding', and that
		<i lang="ain-Latn">ari / ani</i> — the equivalent in other dialects — preserves an
		older quotative form, with <i lang="ain-Latn">sekor</i> a Saru-centred innovation
		<Ref k="aynucorporadiscord" /> (nukopoli, aynu-corpora Discord, 2024-11-09; 2024-12-06)
		‹contested›. These etymological questions belong to
		<Xr ch="quotative-complementation-sekor" />; the relevant point here is that the
		<i lang="ain-Latn">sekor … hawe ne</i> combination is the canonical reported-speech
		construction in the Saru/Chitose corpus ‹corpus-confirmed›.
	</p>
</S>

