		As a lexical item, <i lang="ain-Latn">hawe as</i> means 'a voice is heard; someone
		speaks; a sound resounds' — the compositional reading of 'voice stands'
		<Ref k="dalcorso2018" p="318" />. The evidential is an extension of this lexical
		meaning, and in some corpus occurrences the boundary between a literal sound event and
		a reportative attribution is genuinely ambiguous ‹speculative›.
	</p>
</S>

