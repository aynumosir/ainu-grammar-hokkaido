	<p>
		The polar reduction <i lang="ain-Latn">hawe!</i> follows the insubordination pattern
		documented for the other bare-nominalizer forms: a once-dependent nominalized clause
		used as a main clause to express surprise or strong emotion at the reported content
		<Ref k="nakagawa2024" p="395" />; <Ref k="takahashi2013" p="132" />. Interrogative
		frames in the wider system are treated in
		<Xr ch="interrogative-strategies-question-particles" />.
	</p>
