		The <i lang="ain-Latn">hawe ta an?</i> variant with an inserted
		<i lang="ain-Latn">ta</i> is dialectally restricted, attested in Shizunai and
		Horobetsu and apparently absent from Saru/Chitose
		<Ref k="nakagawa2024" p="394" />. Whether <i lang="ain-Latn">ta</i> here is the
		locative/temporal postposition or a separate interrogative element is not settled in
		the accessible sources ‹speculative›.
	</p>
</S>

<S t="Scope over TAM and the embedded clause" id="scope">
	<p>
		The clause dominated by <i lang="ain-Latn">hawe ne</i> is a fully finite predication:
		it carries its own person marking, aspect, and perfective auxiliaries.
		<i lang="ain-Latn">hawe ne</i> scopes over the entire TAM-marked clause, and negation
		belongs inside the evidential (<i lang="ain-Latn">… somo ki hawe ne</i> 'it is said
		that [one] does not …'), not on <i lang="ain-Latn">hawe ne</i> itself
		<Ref k="nakagawa2024" p="393" />. Post-evidential material — the perfective
		<i lang="ain-Latn">a</i>, final particles, conjunctions, and nominalizers — may follow
		<i lang="ain-Latn">hawe ne</i>, exactly as for <i lang="ain-Latn">ruwe ne</i>; the
		evidential sits above clause-internal aspect but below clause-linkers.
	</p>

	<Ex
		m="e=hosipi haw-e ne kusu tane anakne a=eyayramkar pe ne na"
		g="2SG.S=return voice-POSS COP PURP now TOP 4.A=resign NMLZ COP FIN"
		tr="Because you returned (so they/you said), I will now resign myself."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakamimatsuko, uwepeker minwa 14 (second half), ILCAA Ainu materials, aa-irc/021#271"
		note="hawe ne in a causal subordinate clause (hawe ne kusu 'because it is said/heard that …'). The evidential scopes over the embedded proposition; kusu then relates the reported event to the main clause."
	/>

	<p>
		The ordering restriction — <i lang="ain-Latn">hawe ne kusu</i> is grammatical while
		<i lang="ain-Latn">*kusu hawe ne</i> is not in this meaning — confirms that the
		evidential sits at the outer edge of the VP and inside the scope of clause-linkers, the
		same layering found for <i lang="ain-Latn">ruwe ne</i> <Ref k="nakagawa2024" p="393" />
		‹consensus›. What accessible sources do not yet settle is the precise constituency
		status of <i lang="ain-Latn">hawe ne</i> within the nominalized projection ‹speculative›.
	</p>
</S>

<S t="Double evidentiality and narrative register" id="double-evid">
	<p>
		<i lang="ain-Latn">hawe as</i> and <i lang="ain-Latn">hawe ne</i> both enter
		double-evidential constructions with <i lang="ain-Latn">ruwe ne</i>. The most frequent
		pattern is <i lang="ain-Latn">… hawe as ruwe ne</i>, where a character's indirect
		verbal source is layered under the narrator's <i lang="ain-Latn">ruwe ne</i>, which
		presents the event as traditional shared knowledge
		<Ref k="dalcorso2018" p="331–333" />. A second pattern,
		<i lang="ain-Latn">… hawe ne nankor ruwe ne</i>, uses
		<i lang="ain-Latn">ruwe ne</i> as an epistemic reinforcer on a lower-reliability direct
		form <Ref k="dalcorso2018" p="332" />. In both, the outer
		<i lang="ain-Latn">ruwe ne</i> can ratify an already evidentially marked proposition
		as established fact; it behaves as a knowledge operator that scopes over the other
		evidential. The wider picture of evidential stacking is in
		<Xr ch="mirativity-scope-grammaticalization" />.
	</p>

	<Ex
		m="ran haw-as ruw-e ne"
		g="descend voice-stand track-POSS COP"
		tr="She came down (the voice said — and so the narrator presents as traditional knowledge)."
		cite="dalcorso2018:331"
		dial="SAR"
		place="Kayano (Kaizawa Tsurushino, Biratori); Dal Corso siglum KAY; Dal Corso ex. 273"
		note="Double evidential: hawe as encodes the character's indirect verbal source; ruwe ne layers the narrator's framing of the event as story-world fact. The supporting outer form is systematically ruwe ne, never siri ne — a distributional argument for ruwe ne's role as a knowledge-ratifying operator."
	/>

	<p>
		In narrative texts <i lang="ain-Latn">hawe ne</i> marks the narrator's explicit
		attribution of a proposition to a verbal source, contrasting with the narrative default
		<i lang="ain-Latn">ruwe ne</i>, which presents events as traditionally known facts. The
		genre-level distribution of the evidential forms is treated in
		<Xr ch="narrative-tam-evidential-patterning-by-genre" />.
	</p>
</S>

<S t="Diachrony and dialect microvariation" id="diachrony">
	<p>
		The shift from sensory noun to formal noun is transparent and is noted in the earliest
		grammars: <i lang="ain-Latn">haw</i> 'voice' &gt; <i lang="ain-Latn">haw-e</i>
		(possessed) 'the voice of —' &gt; <i lang="ain-Latn">hawe ne</i> 'it is said that …',
		parallel to the evolution of <i lang="ain-Latn">sir</i>, <i lang="ain-Latn">hum</i>,
		and <i lang="ain-Latn">ru</i> <Ref k="tamura1996" p="s.v. haw, hawe" />;
		<Ref k="kindaichi1936" />; <Ref k="takahashi2018" p="111" />. Dal Corso's general
		assessment applies: the grammaticalization is partial — the lexical source noun is still
		visible in the blocking of <i lang="ain-Latn">-he</i>, and the lexical verb
		<i lang="ain-Latn">haw as</i> 'a voice sounds' remains active — so the shift is a
		matter of degree rather than categorical completion <Ref k="dalcorso2018" p="24–25" />
		‹consensus›.
	</p>
	<p>
		Dialect realization of the reportative evidential varies in the same way as the other
		three forms:
	</p>
	<ul>
		<li>
			<strong>Saru, Chitose, Biratori</strong> — the possessed form
			<i lang="ain-Latn">hawe</i> in the full eight-form direct/indirect system
			<Ref k="dalcorso2018" p="304–337" />.
		</li>
		<li>
			<strong>Tokachi (and frequently Ishikari)</strong> — the bare concept form
			<i lang="ain-Latn">haw</i> closes the clause rather than the possessed
			<i lang="ain-Latn">hawe</i>; <i lang="ain-Latn">aw</i> is an attested Tokachi
			allomorph; <i lang="ain-Latn">iruska haw ne</i> 'was angry (reportedly)' exemplifies
			the pattern <Ref k="nakagawa2024" p="110, 390" />; <Ref k="takahashi2013" p="130–131" />.
		</li>
		<li>
			<strong>Horobetsu</strong> — the possessed-form nominalizer
			<i lang="ain-Latn">hawe</i> grammaticalizes a step further into adverbial-clause and
			conjunctive-particle uses not reported for well-described Saru, extending the
			'versatile nominalization' cline <Ref k="takahashi2018" p="107–112" />.
		</li>
	</ul>
	<p>
		The dialect geography of the quotative particle that co-occurs with
		<i lang="ain-Latn">hawe ne</i> mirrors this variation: Saru and Chitose use
		<i lang="ain-Latn">sekor</i> while other dialects use <i lang="ain-Latn">ari / ani</i>,
		a distribution that community discussion has analyzed as a Saru-centred innovation
