		(&lt; Jp <span lang="ja">紙</span>; see <Xr ch="japanese-loanwords-in-ainu" />) that already
		carried an older sense 'written order, official document' in the oral-literature corpus — now
		extended to school and information-technology vocabulary:
		<i lang="ain-Latn">kampi-pu</i> 'library' (paper + storehouse),
		<i lang="ain-Latn">uepeker-kampi</i> 'explanatory leaflet' (storytelling + paper), and the
		school-level set <i lang="ain-Latn">pon kampinukatcise</i> (elementary school),
		<i lang="ain-Latn">haykannu kampinukatcise</i> (middle school),
		<i lang="ain-Latn">poro kampinukatcise</i> (high school)
