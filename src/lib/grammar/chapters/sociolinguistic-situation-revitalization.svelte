	<p>
		Formulaic expressions for marriage congratulations, condolences, and other life-event occasions
		remain without established Ainu forms; Ōno documents the tendency to revert to Japanese at
		such moments <Ref k="ono2022" p="§8.10" />. The general constraint on coinage is that new
		words must conform to Ainu grammar and morphological patterns <Ref k="ono2022" p="§8.10" />.
		The spread of individual coinages remains unmeasured: no corpus of new-speaker production
		exists, and Ijäs explicitly notes that it is unknown how widely specific coinages have been
		adopted <Ref k="ijas2023b" p="§5" />.
	</p>
</S>

<S t="New-speaker and heritage varieties" id="new-speakers">
	<p>
		The transition to all-L2 instruction has structural consequences for the variety being acquired.
		All teaching is now delivered by instructors whose first language is Japanese, a situation Ōno
		identifies as likely to affect phonology, morphology, and discourse organisation in learner
		output <Ref k="ono2022" p="§8.7" />. The direct-method approach developed for Māori (Te
		Ataarangi) has been imported in experimental form to reduce dependence on the
		grammar-and-translation model that had discouraged community members uninterested in formal
		linguistic analysis <Ref k="ono2022" p="§8.7" />. The structural features of new-speaker
		Ainu have not been systematically described in the accessible literature ‹speculative›.
	</p>
	<p>
		Dialect choice is a further pressure. No standard variety exists across Hokkaido, and few
		revival participants see the need for one; Ōno anticipates that more than one regional standard
		may emerge independently <Ref k="ono2022" p="§8.1" />. The practical resolution is for
		learners to revive the dialect of their ancestral village where documentation is available.
		For several communities — Hakodate, Erimo, Rumoi, Otaru — Japanese settlement displaced Ainu
		speech before any systematic linguistic recording took place, making reconstruction there
		"almost impossible" <Ref k="ono2022" p="§8.1" />. Where ancestral-dialect records are absent,
		the best-documented variety, Saru, serves as a fallback; dialect mixing arises when instructors
		have drawn on materials from more than one regional tradition <Ref k="ono2022" p="§8.2" />.
		The overall dialect classification and the structure of the available record are set out in
		<Xr ch="hokkaido-dialect-classification-and-dialectometry" />.
	</p>
	<p>
		The contact history with Japanese that produced the modern loanword stratum is documented in
		<Xr ch="japanese-loanwords-in-ainu" /> and <Xr ch="ainu-loanwords-and-toponymy-in-japanese" />.
		The influx of untranslated Japanese vocabulary into Ainu marks precisely the period of disuse:
		it records when Ainu ceased to be productively applied to new domains of experience. The recovery
		of that productivity — through the systematic neologism efforts described in the preceding section
		and through the reviving speaker community — proceeds without a standard variety as its target.
	</p>
</S>
