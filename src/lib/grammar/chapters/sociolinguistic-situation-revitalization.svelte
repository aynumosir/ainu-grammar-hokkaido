<S t="Historical setting and language shift" id="history">
	<p>
		The language-shift history of Ainu is inseparable from the political transformation of Hokkaido
		after 1868. Under the Matsumae domain system a bilingual interpreter class —
		<A w="tunci" gl="interpreter" /> (&lt; Jp <span lang="ja">通事／通詞</span>) — had mediated
		trade contact, but the Meiji government's annexation of Hokkaido and its enactment of the
		Hokkaido Former Natives Protection Act (<span lang="ja">北海道旧土人保護法</span>) of 1899
		reconfigured the position of Ainu speakers decisively
		<Ref k="ono2022" p="§2" />. The Act registered Ainu as Japanese subjects, abolished traditional
		fishing and deer-hunting rights, and assigned Ainu children to segregated schools
		(<span lang="ja">旧土人学校</span>) where Japanese was the sole medium of instruction. No
		statute explicitly prohibited the Ainu language, but its use in institutional settings was
		discouraged and children were penalised for speaking it, producing a generation-by-generation
		shift to Japanese <Ref k="ono2022" p="§2" />.
	</p>
	<p>
		The last generation with full first-language command was born approximately between 1900 and 1910;
		their children acquired Japanese as the primary language and did not transmit Ainu in the home
		<Ref k="ijas2023" />; <Ref k="ono2022" p="§2" />. A subsequent cohort born in the 1940s and
		1950s, whose parents had already shifted to Japanese, in some cases acquired partial Ainu from
		grandparents while parents worked away from the village; these individuals suspended active use
		on entering the Japanese school system but retained partial receptive and productive competence.
		Members of this cohort — sometimes called "rememberers" — have since been recruited as language
		resources and participants in the revival movement <Ref k="ono2022" p="§3" />.
	</p>
</S>

<S t="The speaker situation" id="speakers">
	<p>
		Reliable statistics on Ainu speakers do not exist. The Hokkaido-resident Ainu population has
		been estimated at approximately 24,000 on the basis of a 2005 survey by the Hokkaido Department
		of Environment and Life (<span lang="ja">北海道環境生活部</span>), but this census identifies
		community members by self-declaration, not language ability <Ref k="sato2012" p="§2" />.
		Before the 2019 Ainu Policy Promotion Act the government did not legally recognise Ainu as
		indigenous people, which meant that self-identifying as an Ainu-language speaker amounted to
		disclosing sensitive personal information subject to discrimination risk; speaker counts were
		therefore practically impossible to collect in any systematic form
		<Ref k="sato2012" p="§2.2" />. The closest available measure in the literature is Ōno's
		assessment that fluent first-language speakers are today "almost nil"
		<Ref k="ono2022" p="§8.10" />.
	</p>
	<p>
		The active revival community consists of L2 learners, heritage speakers of varying competence,
		and a small number of elder rememberers. All instruction is now conducted by instructors who are
		themselves L2 speakers working from recordings and transcriptions of deceased native speakers.
		The dialect situation and the corpus on which this grammar rests are described in
		<Xr ch="hokkaido-dialect-classification-and-dialectometry" /> and
		<Xr ch="dialect-sample-and-corpus-method" />.
	</p>
</S>

<S t="From documentation to revival" id="revival">
	<p>
		An era of documentation preceded the organised revival. The foundational document is
		<Ref k="chiri1923" />, a collection of thirteen <i lang="ain-Latn">kamuy yukar</i> (divine
		epics) compiled by Yukie Chiri (知里幸恵, 1903–1922) of Horobetsu and published the year of
		her death — the first book of Ainu texts compiled and published by an Ainu, written in Roman
		script. Her brother Mashiho Chiri produced the major mid-century dictionaries and lexicological
		studies but framed his programme as recording rather than revival
		<Ref k="ono2022" p="§3" />. The newsletter <i lang="ja">アイヌ・モシリ</i>
		(<i>Aynu-mosir</i>), founded by Tasuke Yamamoto (山本多助) and colleagues, ran eighteen issues
		between 1957 and 1965 in text written almost entirely in Ainu <Ref k="ono2022" p="§3" />.
	</p>
	<p>
		The shift toward active revival is associated principally with two first-language speakers.
		Shigeru Kayano (萱野茂, 1926–2006) of Nibutani (Biratori) collected cultural objects, tools,
		and audio recordings; his 1974 collection of <A w="uepeker" gl="prose folktales" /> was
		printed in katakana so that community members without access to academic Roman-script editions
		could read their own oral tradition. Kayano held that the language had to remain a medium of
		living exchange. He founded the first private Ainu language school at Nibutani in 1983 — after
		official permit applications had been rejected — and delivered the first Diet speech in Ainu in
		1994 <Ref k="ono2022" p="§4.1" />. Tatsujirō Kuzuno (葛野辰次郎, 1910–2002) of Shizunai coined
		the phrase <em>katsu-hozon</em> (<span lang="ja">活保存</span>, 'living preservation') as the
		alternative to <em>shi-hozon</em> (<span lang="ja">死保存</span>, 'posthumous preservation'),
		a formulation that became a slogan of the revival movement <Ref k="ono2022" p="§4.2" />.
	</p>
	<p>
		The Ainu Association of Hokkaido (北海道アイヌ協会; formerly Hokkaido Utari Association) launched
		structured Ainu Language Classes (<span lang="ja">アイヌ語教室</span>) in 1987, beginning at
		Nibutani and Asahikawa and growing to fourteen classes across Hokkaido, subsidised by the
		Hokkaido Board of Education <Ref k="ono2022" p="§4.3" />. The classes functioned as community
		gathering spaces as much as formal instruction. They were restructured in 2010 and abolished in
		2012 <Ref k="ono2022" p="§4.3" />. Parallel programmes operated in the Kantō region: classes
		run by the Kantō Utari-kai from 1989 (with Hiroshi Nakagawa as instructor) and Tokyo Ainu
		Association courses.
	</p>
</S>

<S t="Legislation and institutions" id="legislation">
	<p>
		The 1997 Ainu Cultural Promotion Act (<span lang="ja">アイヌ文化振興法</span>) established the
		Foundation for Research and Promotion of Ainu Culture (FRPAC; <span lang="ja">財団法人アイヌ文化
		振興・研究推進機構</span>), which funded Ainu-language courses, instructor training and dispatch,
		and cultural programming <Ref k="ono2022" p="§6" />. The annual
		<i lang="ain-Latn">Itak-an ro</i> ('Let's speak!') Ainu-language speech contest was inaugurated
		under FRPAC in 1998. In the same year, the Sapporo District Court's Nibutani Dam ruling —
		with Kayano Shigeru and Kaizawa Kōichi as plaintiffs — for the first time judicially recognised
		the Ainu as an indigenous minority under Japanese law
		<Ref k="ono2022" p="§6" />.
	</p>
	<p>
		A 2008 Diet resolution called on the government to recognise the Ainu as indigenous people,
		following Japan's endorsement of the 2007 UN Declaration on the Rights of Indigenous Peoples.
		The 2019 Ainu Policy Promotion Act repealed and superseded the 1997 law and extended for the
		first time formal statutory recognition of the Ainu as indigenous, with provisions for
		cultural preservation, ethnic business support, and education
		<Ref k="ono2022" p="§7" />. Leader-training programmes for Ainu-language instructors had been
		running from Shiraoi since 2008, graduating 23 trainees through 2020, with subsequent cohorts
		at Biratori; the Sapporo University Urespa Club, founded 2010, trains university students in
		Ainu language and cultural skills <Ref k="ono2022" p="§7" />.
	</p>
	<p>
		The institutional centrepiece of the 2019 framework is the National Ainu Museum and Park —
		<i lang="ain-Latn">Upopoy</i> (<i lang="ja">ウポポイ</i>, lit. 'many people singing together')
		— at Shiraoi on the Pacific coast, opened in July 2020 after delay from the COVID-19
		pandemic <Ref k="ono2022" p="§7" />. The Upopoy project required the coining of approximately
		180 new Ainu words for museum facilities and signage (see the neologisms section below).
	</p>
</S>

<S t="Orthographic conventions" id="orthography">
	<p>
		Ainu has no indigenous writing system <Ref k="shibatani1990" p="§1" />; text has been rendered
		in katakana or Roman script since the nineteenth century, and both systems remain in use. Katakana
		is preferred for community publications and public signage but requires a set of more than ten
		specially devised small subscript consonant characters — most prominently small
		<span lang="ja">ㇰ</span> for syllable-coda <i lang="ain-Latn">k</i>, small
		<span lang="ja">ㇷ゚</span> for coda <i lang="ain-Latn">p</i>, and small
		<span lang="ja">ㇳ</span> for coda <i lang="ain-Latn">t</i>. The sequence
		<i lang="ain-Latn">tu</i> requires either the archaic <span lang="ja">ト゚</span> or the
		modern <span lang="ja">トゥ</span>; font and encoding problems have been a recurring obstacle
		in digital contexts <Ref k="ono2022" p="§8.4.1.1" />. Roman script needs no special characters
		and handles digital text more readily, but unfamiliarity among community members who learned to
		read in Japanese has limited its uptake <Ref k="ono2022" p="§8.4.2" />.
	</p>
	<p>
		Several conventions are now standardised. The digraphs <i lang="ain-Latn">ch</i> and
		<i lang="ain-Latn">sh</i>, used in Batchelor and many early transcriptions, have been replaced
		throughout the contemporary literature by <i lang="ain-Latn">c</i> and
		<i lang="ain-Latn">s</i> <Ref k="aynucorporadiscord" /> (nukopoli, aynu-corpora Discord,
		2023-12-08). The convention of marking personal-index clitic boundaries with
		<i lang="ain-Latn">=</i> and derivational boundaries with <i lang="ain-Latn">-</i> originated
		in Nakagawa's 1986 annotated collection and has since become the norm in academic publications,
		including this grammar <Ref k="ono2022" p="§8.4" />; <Ref k="aynucorporadiscord" />
		(nukopoli, aynu-corpora Discord, 2024-11-09).
	</p>
	<p>
		Active debates remain unsettled ‹contested›. The spelling of the pre-labial nasal in Japanese
		loanwords divides an etymological approach — writing <i lang="ain-Latn">kampi</i>
		(&lt; Jp <span lang="ja">紙</span> <i lang="ja">kami</i>) with
		<i lang="ain-Latn">m</i>, preserving the underlying form — from a surface-phonological approach
		that writes <i lang="ain-Latn">kanpi</i> with <i lang="ain-Latn">n</i> because the nasal is
		neutralised before <i lang="ain-Latn">p</i>; academic sources and Ainu Association textbooks
		diverge on this point <Ref k="aynucorporadiscord" /> (nukopoli, aynu-corpora Discord,
		2024-11-16). Word-division is similarly variable: the phrase
		<i lang="ain-Latn">sisam itak</i> ('Japanese-person speech') and the compound
		<i lang="ain-Latn">sisamitak</i> ('the Japanese language, as a named entity') carry different
		denotational scopes, yet editorial practice on whether to write them as one or two tokens
		remains inconsistent <Ref k="aynucorporadiscord" /> (nukopoli, aynu-corpora Discord,
		2024-11-05).
	</p>
</S>

<S t="Lexical modernization and neologisms" id="neologisms">
	<p>
		A century of disuse created a vocabulary gap that native word-formation had no opportunity to
		close. Ōno documents the diagnostic asymmetry: during the Meiji period, while Ainu was still
		in daily use, speakers coined <i lang="ain-Latn">tunas-kampi</i> ('fast paper', i.e. telegram,
		Asahikawa) for a new technology and assembled the transparent compound
		<i lang="ain-Latn">a=e-i-nu-p</i> ('the thing with which one hears', i.e. telephone) from
		native morphological material; by contrast, words for television, computers, and modern
		administrative institutions were simply borrowed from Japanese untranslated, because Ainu was
		no longer anyone's daily language when those things spread
		<Ref k="ono2022" p="§8.9" />. Systematic neologism-coining since the 1980s attempts to reverse
		this. Ijäs identifies five formation strategies from the revival literature
