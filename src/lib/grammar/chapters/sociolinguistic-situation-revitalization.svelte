		object noun incorporation and compounding — is described in
		<Xr ch="noun-incorporation-object" /> and <Xr ch="applicative-system-overview" />.
	</p>
	<p>
		The Upopoy project assembled approximately 180 new words for museum facilities and signage,
		ranging from <i lang="ain-Latn">tumamka-us-pe</i> 'poster' (lit. 'thing that hangs prominently')
		to multi-word institutional names such as
		<i lang="ain-Latn">aynu teetawano ankur kampinuye cise sekor an pe</i>
		('Center for Ainu and Indigenous Studies') <Ref k="ijas2023b" p="§5" />.
	</p>
