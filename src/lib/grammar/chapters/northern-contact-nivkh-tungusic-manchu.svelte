<S t="The northern contact zone" id="overview">
	<p>
		Three genealogically unrelated languages occupied Sakhalin simultaneously and traded across it:
		Ainu (an isolate), Nivkh/Ghilyak (an isolate), and Uilta/Orok, a Southern Tungusic language.
		Manchu-origin goods and vocabulary reached the island via the Santan trade network from the Qing
		frontier; Chinese commodities followed the same route without producing direct Ainu–Chinese
		bilingualism. Whether this configuration constitutes a linguistic area in the strict sense —
		a Sprachbund defined by structural convergence — Shiraishi and Tangiku explicitly leave for
		future research <Ref k="shiraishitangiku2022" p="§1" />. What is documented is directional
		lexical borrowing; the structural case for areal convergence remains under-evidenced.
	</p>
	<p>
		The contact layer as it reaches Hokkaido is thin. Most Nivkh, Tungusic, and Manchu vocabulary
		stayed on Sakhalin, and loanwords travelling in the Ainu-to-continent direction barely reached
		the mainland <Ref k="shiraishitangiku2022" p="§1" />. A small residue —
		<A w="kaya" gl="sail" />, <A w="sintoko" gl="cask" />, <A w="san" gl="offering shelf" />,
		<A w="tonto" gl="tanned hide" /> — passed from the Sakhalin contact zone into Hokkaido Ainu,
		together with the Eurasian Wanderwort <A w="kotan" gl="settlement, village" /> and the
		proposed Tungusic borrowing <A w="inaw" gl="ritual wood-shavings (ināw)" />. Contact
		borrowing is distinct from genetic relatedness: all proposals linking Ainu genetically to Nivkh
		or any other northern family have failed, and the Ainu–Nivkh relationship is one of early
		lexical interaction between two isolates <Ref k="janhunen2022" p="§3c" /> ‹consensus›. The
		genetic question is addressed in <Xr ch="genetic-position-macro-comparison" />.
	</p>
</S>

<S t="Ainu–Nivkh contact: the lexical layer" id="ainu-nivkh-lexical">
	<p>
		Borrowing between Ainu and Nivkh ran in both directions on Sakhalin. Standard directionality
		diagnostics — geographic spread within each family, morphological analysability, sound-change
		plausibility, and dialect correspondences — must be applied case by case
		<Ref k="shiraishitangiku2022" p="§3" />. Multilingualism on the island was not a general
		social condition: before the late nineteenth century it was "extremely limited and localized",
		mediated by individual interpreters and trading contacts rather than widespread bilingualism
		<Ref k="shiraishitangiku2022" p="§2.5" />. The west-coast Sakhalin group called
		<em>Sirunkur</em>, described as "originally Ainu but Nivkh-ized", documents a sociolinguistic
		boundary zone rather than a stable bilingual population
		<Ref k="shiraishitangiku2022" p="§2.2" />.
	</p>
	<p>
		Items that passed from Nivkh into Ainu on Sakhalin include <i lang="ain-Latn">nuso/nuhsu</i>
		'dog sled' (‹SA›; &lt; Nivkh <i lang="ain-Latn">nuci</i>),
		<i lang="ain-Latn">hankata</i> 'birch-bark plate' (&lt; Nivkh
		<i lang="ain-Latn">haŋr̥</i>), and <i lang="ain-Latn">ka</i> 'sable trap' (&lt; Nivkh
		<i lang="ain-Latn">ha</i>). In the reverse direction, Sakhalin Ainu
		<i lang="ain-Latn">tonto</i>, <i lang="ain-Latn">sinduχ</i>,
		<i lang="ain-Latn">upun</i> 'winter storehouse', <i lang="ain-Latn">nikar</i> 'ladder',
		and <i lang="ain-Latn">muks</i> 'swaddling cloth' appear in Nivkh
		<Ref k="shiraishitangiku2022" p="§3.2–3.6" />. Of the Nivkh-into-Ainu items,
		<A w="kaya" gl="sail" /> is the one with a clear Hokkaido reflex: Batchelor's dictionary
		documents <i lang="ain-Latn">kaya</i> in Sakhalin with the meaning 'fish-skin garment', and
		Hokkaido retained the word with the narrower meaning 'sail'
		<Ref k="batchelor1938" p="241" />; <Ref k="shiraishitangiku2022" p="§3.2" />.
	</p>
	<p>
		The early Ainu–Ghilyak contact layer reaches deeper than the Sakhalin trade period. Alonso de
		la Fuente, revising Vovin's Proto-Ainu reconstruction, argues that Ainu-into-Ghilyak loans
		preserve the proto-lateral PA <i lang="ain-Latn">*l-</i> before its merger with
		<i lang="ain-Latn">*r</i> in all Hokkaido reflexes: the Proto-Ghilyak reflex
		<i lang="ain-Latn">*l-</i> corresponding to Vovin's PA <i lang="ain-Latn">*d-</i> makes
		sense if Ainu lent these words to Ghilyak before the lateral–flap merger had run its course
		<Ref k="alonso2022" p="§2.2.1" />. This is a secondary use of contact evidence to
		triangulate segmental reconstruction; the reconstruction itself is treated in
		<Xr ch="proto-ainu-segmental-reconstruction" />.
	</p>
	<p>
		Among the deeper shared items, <i lang="ain-Latn">tunakay</i> 'reindeer' — source of Japanese
		<i lang="ja">トナカイ</i> <i>tonakai</i> (treated in
		<Xr ch="ainu-loanwords-and-toponymy-in-japanese" />) — is linked by Janhunen, following
		Austerlitz (1976), to Pre-Proto-Ghilyak <i lang="ain-Latn">*tola-ŋa.y</i>
		‹contested› <Ref k="janhunen2022" p="§3c" />. The Sakhalin-only parallel
		<i lang="ain-Latn">kaco</i> 'shaman's drum', shared with ‹SA› Ghilyak
		<i lang="ain-Latn">*kaco</i>, points to a similar early contact stratum
		<Ref k="janhunen2022" p="§3c" />. The reindeer was not native to Hokkaido proper;
		<i lang="ain-Latn">tunakay</i> passed through the Sakhalin trade route into Ainu cultural
		vocabulary and then into Japanese.
	</p>
	<p>
		The community note that Sakhalin Ainu <i lang="ain-Latn">nikubun</i> 'a Nivkh person' derives
		from the Sakhalin Nivkh form <i lang="ain-Latn">ɲiɣvŋ</i> — distinct from the Amur-dialect
		form <i lang="ain-Latn">ɲivx</i> — is consistent with the Sakhalin contact setting and is
		corroborated by Shiraishi and Tangiku's discussion of Ainu ethnonyms ‹contested›
		<Ref k="aynucorporadiscord" /> (nukopoli, aynu-corpora Discord, 2024-12-11);
		<Ref k="shiraishitangiku2022" p="§2" />.
	</p>
</S>

<S t="Tungusic, Manchu, and the Santan trade" id="tungusic-manchu-santan">
	<p>
		The Santan trade (<span lang="ja">山丹交易</span>) was the channel through which Qing-dynasty
		Manchu goods reached Sakhalin and, through Sakhalin, the northern coast of Hokkaido. Uilta
		(Orok), a Southern Tungusic language closely related to Evenki, occupied the east and central
		coast of Sakhalin alongside Ainu communities. Direct Ainu–Uilta exchange, Manchu-mediated
		trade contact, and the downstream movement of Chinese goods via the Santan network are
		distinguishable layers even though the documentary record conflates them.
	</p>
	<p>
		The offering shelf <A w="san" gl="offering shelf" /> has cognates in both Nivkh
		(<i lang="ain-Latn">cʰaŋ</i>) and Uilta (<i lang="ain-Latn">saan</i>), making the direction
		of borrowing difficult to establish without further philological evidence
		<Ref k="shiraishitangiku2022" p="§3" /> ‹contested›. The comparison of person and number
		marking across Uilta, Sakhalin Ainu, and Nivkh has been carried out systematically
		<Ref k="baek2021" /> and is addressed in <Xr ch="hokkaido-dialect-classification-and-dialectometry" />.
	</p>
	<p>
		Manchu proper was reached through trade rather than direct contact. The Manchu word
		<i lang="ain-Latn">hasaha</i> 'scissors' entered Nivkh as <i lang="ain-Latn">haza</i>; the
		Hokkaido Ainu word for the same object, <i lang="ain-Latn">hasami</i>, is a Japanese borrowing
		from the same Manchu ultimate source — showing that the same trade good reached Ainu and Nivkh
		through different intermediaries <Ref k="shiraishitangiku2022" p="§3" />.
	</p>
	<p>
		The ritual vocabulary item <A w="inaw" gl="ritual wood-shavings (ināw)" /> has been proposed
		as a Tungusic borrowing by Ikegami (1980), cited by Janhunen ‹speculative›
		<Ref k="janhunen2022" p="§5" />. If correct, <i lang="ain-Latn">ināw</i>/<i lang="ain-Latn"
			>inaw</i
		> — central to Ainu ceremonial life and frequent throughout the corpus — is among the oldest
		identifiable northern borrowings in the Hokkaido lexicon, pre-dating the Sakhalin trading
		system.
	</p>
	<p>
		The Sakhalin Ainu numeral system carries the clearest structural trace of northern contact.
		Hokkaido Ainu counts vigesimally; Sakhalin uses a decimal system above ten, with the word for
		'hundred', <i lang="ain-Latn">tanku</i>, established as a Tungusic borrowing, and the word for
		'ten', <i lang="ain-Latn">kunkutu</i>, proposed on the basis of early records as a loan from
		Itelmen/Kamchadal <Ref k="tangiku2022" p="§6.1" /> ‹contested›. The community analysis of
		<i lang="ain-Latn">kunkutu</i> as an Itelmen item independently converges with Tangiku's
		hedged position
		<Ref k="aynucorporadiscord" /> (antitwilight, aynu-corpora Discord, 2024-11-22).
	</p>
	<p>
		The Sakhalin Ainu vocabulary of neighbouring peoples is richer than its Hokkaido counterpart,
		reflecting the island's position in the Qing-era trade network. Attested ‹SA› forms include
		<i lang="ain-Latn">manciw</i> 'Manchu', <i lang="ain-Latn">canta</i> 'Santan person',
		<i lang="ain-Latn">kakan</i> 'Khan', and <i lang="ain-Latn">nuca</i> 'Russian'; the word
		<i lang="ain-Latn">kawren</i> 'Korean' has been speculatively connected to
		<i lang="ja">高麗</i> <i>Kōrai</i> ‹speculative›
		<Ref k="aynucorporadiscord" /> (thegodofneet, aynu-corpora Discord, 2024-12-10);
		<Ref k="shiraishitangiku2022" p="§2" />. Hokkaido Ainu has no equivalent vocabulary at this
		density: Japanese-mediated contact, treated in <Xr ch="japanese-loanwords-in-ainu" />, was
		the channel through which the wider world reached HA.
	</p>
</S>

