<S t="Structural contact and the Sprachbund question" id="sprachbund">
	<p>
		Whether Sakhalin constituted a Sprachbund has two separable sub-questions: whether Sakhalin
		Ainu shows contact-induced structural changes attributable to Nivkh or Tungusic, and whether
		the three languages converged as an areal unit. Shiraishi and Tangiku leave the Sprachbund
		label for future research <Ref k="shiraishitangiku2022" p="§1" />; on current evidence,
		lexical exchange is real and directional, while the structural case is thin.
	</p>
	<p>
		Person and number marking across Uilta, Sakhalin Ainu, and Nivkh has been compared directly
		<Ref k="baek2021" />. All three languages mark third person with zero or near-zero morphology,
		and all three attach plural affixes at the outer edge of the person-marked verbal complex.
		Whether these parallel patterns reflect contact, shared typological pressures on head-marking
		systems, or independent development cannot be resolved from the existing data ‹speculative›.
	</p>
	<p>
		The phonological innovations distinguishing Sakhalin from Hokkaido Ainu — final-consonant
		debuccalization, the vowel-length system — are internal to Ainuic and independent of northern
		contact; their diachrony is treated in <Xr ch="proto-ainu-segmental-reconstruction" />. The
		claim in one view of the prosodic debate that Hokkaido pitch accent arose through contact with
		Japanese concerns the HA end, not Sakhalin <Ref k="alonso2022" p="§2.3–2.4" /> ‹contested›;
		that debate is reviewed there and in <Xr ch="internal-reconstruction-and-grammaticalization-pathways" />.
	</p>
</S>

<S t="Evidence base and its limits" id="evidence-limits">
	<p>
		The primary source for northern-contact data is Shiraishi and Tangiku's survey
		<Ref k="shiraishitangiku2022" />, which draws on Piłsudski's early-twentieth-century Sakhalin
		corpus <Ref k="pilsudski1912" />, post-war word-list records, and secondary comparative Nivkh
		and Tungusic material. Three limits bound what can be established. First, the documentation of
		Sakhalin contact vocabulary is uneven across the island's dialects and social groups. Second,
		directionality of individual loans is difficult to verify without comprehensive comparative
		Nivkh fieldwork, some of which remains unpublished. Third, individual interpreters working
		between Ainu, Nivkh, and Uilta could have introduced phonologically irregular intermediary
		forms that complicate the etymological record
		<Ref k="shiraishitangiku2022" p="§2.5" />.
	</p>
	<p>
		For Hokkaido, the corpus gives little direct traction on the northern loanword layer. Items
		such as <i lang="ain-Latn">kaya</i>, <i lang="ain-Latn">sintoko</i>, and
		<i lang="ain-Latn">san</i> are cultural vocabulary with low narrative frequency; only
		<A w="kotan" gl="settlement" /> is a corpus staple. Dictionary evidence —
		<Ref k="batchelor1938" /> and <Ref k="tamura1996" /> — together with the secondary literature
		remain the principal witnesses for most items. Japanese-mediated contact, which produced a
		larger and better-attested Hokkaido loanword layer, is treated in
		<Xr ch="japanese-loanwords-in-ainu" />. The Ainu contribution to Japanese in the reverse
		direction, including <i lang="ain-Latn">tonto</i> → Japanese <i lang="ja">todo</i> 'sea
		lion' and <i lang="ain-Latn">tunakay</i> → <i lang="ja">tonakai</i> 'reindeer', is treated
		in <Xr ch="ainu-loanwords-and-toponymy-in-japanese" />. Macro-comparison proposals that would
		extend the lexical Ainu–Nivkh relationship into a genetic one are catalogued and critiqued in
		<Xr ch="macro-comparison-and-deep-contact-controversies" />.
	</p>
</S>
