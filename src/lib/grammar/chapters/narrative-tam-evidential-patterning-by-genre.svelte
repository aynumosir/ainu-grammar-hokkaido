		m="yaykeste=an wa tan uske ta ek=an wa an=an ruwe ne korka"
		g="wander.about=4.S CONJ DEM place LOC come=4.S CONJ exist=4.S track-POSS COP but"
		tr="I wandered about and came to this place and have been living here, but …"
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker 10"
		note="The narrative 'I' is indexed by the fourth-person =an throughout: ek=an pairs the singular verb ek 'come' with the 4th-person ending, and an=an ruwe ne closes the clause as narrative-default. The everyday 1SG ku= never appears; the protagonist speaks of herself in the non-everyday logophoric frame."
	/>
	<p>
		The narrative closes with a retrospective identification formula. The fourth-person
		"I" who has narrated throughout is revealed at the end: the narrator supplies the
		protagonist's name or nature with <i lang="ain-Latn">sekor</i> followed by a speech verb
		(<i lang="ain-Latn">isoytak</i> 'narrate', <i lang="ain-Latn">ye</i> 'say',
		<i lang="ain-Latn">hawki</i> 'recite/utter'). At that moment the narration shifts from
		fourth-person to everyday first person as the reciter resumes her or his own voice
		<Ref k="endo2022" p="§2.4" />; <Ref k="sato2008" p="259" />.
	</p>
</S>

<S t="Quotative density and the reportative frame" id="quotative-reportative">
	<p>
		Because narrative runs in direct speech of the protagonist throughout, the quotative
		<A w="sekor" gl="(quotative particle)" /> is dense at clause boundaries: every speech act,
		thought, or action declared by the hero-narrator is formally attributed to that voice by
		<i lang="ain-Latn">sekor</i> + a speech or cognitive verb <Ref k="endo2022" p="§2.4" />.
		The closing identification formula (<i lang="ain-Latn">sekor isoytak</i> 'so X narrated';
		<i lang="ain-Latn">sekor ye</i> 'so X said') terminates the logophoric frame.
		<i lang="ain-Latn">isoytak</i> and <i lang="ain-Latn">ye</i> carry different semantics:
		<i lang="ain-Latn">isoytak</i> means specifically 'narrate/tell a story', while
		<i lang="ain-Latn">ye</i> is the general speech verb; both are attested at the close of
		<i lang="ain-Latn">uwepeker</i> (nukopoli and thegodofneet, <Ref k="aynucorporadiscord" />,
		2025-06-03, <code>asserted</code>).
	</p>
	<p>
		Within the narrator's frame, characters' utterances and attributed knowledge are marked
		by the reportative evidential <i lang="ain-Latn">hawe ne</i> (direct reportative, voice-POSS +
		copula) or <i lang="ain-Latn">hawe as</i> (indirect reportative, voice-POSS + existential).
		<i lang="ain-Latn">hawe as</i> typically frames a character's state as known by hearsay
		or mediated inference rather than direct witness <Ref k="dalcorso2018" p="298, 331–333" />;
		<Ref k="hirosawa2026" p="64" />. The full morphological analysis is in
		<Xr ch="hawe-ne-reportative" />.
	</p>
	<p>
		Double evidentiality is a distinctive feature of narrative: a character's hearsay frame
		<i lang="ain-Latn">hawe as</i> is embedded inside the narrator's assertive
		<i lang="ain-Latn">ruwe ne</i>, yielding sequences of the type
		<i lang="ain-Latn">… hawe as ruwe ne</i> 'it is established fact that (it was said that
		…)'. Dal Corso identifies this as the pattern in which the narrator ratifies a character's
		indirect source as shared narrative knowledge — the operator <i lang="ain-Latn">ruwe ne</i>
		scoping over the already-evidentially-marked inner proposition
		<Ref k="dalcorso2018" p="331–333" /> ‹corpus-confirmed›.
	</p>

	<Ex
		m="... haw-e as ru-we ne"
		g="... voice-POSS exist track-POSS COP"
		tr="… (so it was said) — established fact."
		constructed
		note="Schematic illustration of the double-evidentiality pattern hawe as ruwe ne described in Dal Corso (2018: 331–333). The inner reportative hawe as (voice-POSS + existential) is embedded under the outer inferential ruwe ne (track-POSS + copula), which presents the whole proposition as narrator-level established fact."
	/>

	<p>
		In contrast, the low-reliability indirect frame <i lang="ain-Latn">hawe ne nankor ruwe ne</i>
		('it is fact that (it seems to be said that …)') reinforces an uncertain report by wrapping
		it in the direct evidential, marking that the narrator vouches for the report even if its
		original source is uncertain <Ref k="dalcorso2018" p="332–333" />.
	</p>
</S>

