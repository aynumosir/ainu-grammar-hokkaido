	/>

	<p>
		The opening frame of prose tales characteristically sets the scene with
		<i lang="ain-Latn">ruwe ne</i>, sometimes followed by <i lang="ain-Latn">hike</i>
		'although; and yet' to signal the adversative turn that opens the main narrative sequence
		<Ref k="hirosawa2026" p="75" />. Within the episode, <i lang="ain-Latn">ruwe ne</i> marks
		the close of each foregrounded event unit; the narrative proceeds by chaining these
		clause-final assertions with <i lang="ain-Latn">akusu</i> or
		<i lang="ain-Latn">hine</i> into a sequenced storyline. The perfective
		<i lang="ain-Latn">a</i> may follow <i lang="ain-Latn">ruwe ne</i> in the post-evidential
		slot to flag a temporally anterior proposition before the next episode opens, as in
		<i lang="ain-Latn">… ruwe ne a p</i> 'and (having done) that …' <Ref k="dalcorso2018"
			p="196"
		/>.
	</p>
</S>

<S t="Tenseless narration and aspectual foregrounding" id="tenseless-foregrounding">
	<p>
		Hokkaido Ainu has no tense morphology. Temporal sequencing in narrative rests instead on
		the conjunction-aspect apparatus treated in <Xr ch="tam-overview" /> and
		<Xr ch="existential-aspect-an-oka" />. In practice, the distinction between storyline
		(foreground) and setting (background) falls out from the aspect constructions selected and
		the connective that links clauses.
	</p>
	<p>
		Foreground events — the sequential actions that advance the plot — are typically expressed
		with a bare, uninflected verb form closing the clause, chained into sequences by
		<i lang="ain-Latn">hine</i>, <i lang="ain-Latn">wa</i>, or <i lang="ain-Latn">akusu</i>,
		and closed at episode boundaries with <i lang="ain-Latn">ruwe ne</i>
		<Ref k="sato2008" p="259" />; <Ref k="takahashi2022" />. Background material — ongoing
		states, framing circumstances, simultaneous action — is expressed with durative or
		resultative aspect constructions: <i lang="ain-Latn">kor okay</i> (have + exist.PL, durative),
		<i lang="ain-Latn">wa okay</i> or <i lang="ain-Latn">wa an</i> (resultative continuative),
		and <i lang="ain-Latn">kor an</i> (possessive existential, stative frame).
	</p>
	<p>
		Satō's analysis of the Abuta/Iburi heroic epic text (narrator 遠島タンケ) shows the same
		aspect inventory in verse as in prose, confirming that the foreground/background distinction
		is not genre-specific grammar but the same aspectual system deployed under metrical
		constraints <Ref k="sato2009yukar" p="§2.2.1" />. The durative
		<i lang="ain-Latn">kor okay</i> frames backgrounded action:
	</p>

	<Ex
		m="rek kor okay"
		g="cry while exist.PL"
		tr="(she) was crying"
		cite="sato2009yukar:§2.2.1"
		dial="HOR"
		place="Abuta/Iburi yukar, narrator 遠島タンケ (Tōshima Tanke), l. 46; Satō 2009: §2.2.1"
		note="Cited from Satō's aspectual analysis of the Abuta yukar text. kor okay encodes a durative background state — the weeping frames the main-line action, not as a storyline event."
	/>

	<p>
		The resultative <i lang="ain-Latn">wa okay</i> similarly backgrounds a settled outcome:
		<i lang="ain-Latn">rok wa okay</i> 'is sitting (has sat down)' (l. 192),
		<i lang="ain-Latn">yaykoan wa an</i> 'living alone (has settled alone)' (l. 481)
		<Ref k="sato2009yukar" p="§2.2.1" />. The pattern is: background clauses use existential-aspect
		constructions and are embedded under the ongoing narrative frame; storyline clauses use bare
		verbs (or the prospective <i lang="ain-Latn">anki</i> 'about to') closing with
		<i lang="ain-Latn">ruwe ne</i> when they constitute an asserted episode-boundary fact.
	</p>
	<p>
		The prospective auxiliary <i lang="ain-Latn">anki</i> also appears in the Abuta yukar to
		mark an imminent action — <i lang="ain-Latn">paye-an anki</i> 'I am about to go' (l. 113)
		— representing the hero's stated intention within direct speech, which is the immediate
		context for all narrative action in these verse genres
		<Ref k="sato2009yukar" p="§2.2.1" />.
	</p>
</S>

<S t="Fourth-person narration: genre-universal and regionally split" id="fourth-person-narration">
	<p>
		All three major narrative genres share a defining grammatical feature: the protagonist is
		indexed not by the everyday first person <i lang="ain-Latn">ku=</i>/<i
			lang="ain-Latn">=an</i
		> but by the fourth person <i lang="ain-Latn">a=</i>/<i lang="ain-Latn">=an</i> (Saru and
		Chitose) <Ref k="sato2008" p="259–261" />; <Ref k="endo2022" p="§2.4" />. The form is
		the same prefix set that encodes indefinite reference, second-person honorific address, and
		inclusive first plural in other contexts; in narrative it indexes the protagonist as
		logophoric — cast as the direct speech of the hero-narrator — rather than as the actual
		speaker <Ref k="bugaeva2008" p="31–40" />; <Ref k="nikitina2021" />. See
		<Xr ch="fourth-person-honorific" /> and
		<Xr ch="logophoric-narration-and-reported-discourse" /> for the paradigm and its
		syntactic analysis.
	</p>
	<p>
		Satō connects the non-everyday person marking to what he calls the indirection principle:
		the genre convention that the narrative voice is not the actual reciter's but a god's or
		spirit's (possibly transmitted in a dream), and the non-everyday marker increases the
		indirectness of that address and softens the implied taboo on a deity speaking directly to a
		living human <Ref k="sato2008" p="259–261" />; <Ref k="sato2004" />. This remains Satō's own
		hypothesis and stands as an alternative to the older "shamaness-oracle origin" account
		‹contested›.
	</p>
	<p>
		The choice of marker shows a regional split without altering the narrative function.
		Saru/Chitose prose tales and heroic epics use <i lang="ain-Latn">a=</i>/<i
			lang="ain-Latn">=an</i
		>. Iburi kamuy yukar — canonically the collection compiled by Chiri Yukie
		(<i lang="ain-Latn">Ainu Shin'yōshū</i>, 1923, Horobetsu) — uses the exclusive first
		plural <i lang="ain-Latn">ci=</i>/<i lang="ain-Latn">=as</i> as the singular hero-narrator
		marker consistently throughout <Ref k="sato2008" p="261" />; <Ref k="chiri1923" />. In
		Chitose <i lang="ain-Latn">ci=</i>/<i lang="ain-Latn">=as</i> may appear only at the
		opening before reverting to <i lang="ain-Latn">a=</i>/<i lang="ain-Latn">=an</i>
		<Ref k="sato2008" p="261" />.
	</p>
	<p>
		A second genre-diagnostic agreement property follows from the use of <i lang="ain-Latn"
			>a=</i
		>/<i lang="ain-Latn">=an</i>. In everyday Saru/Chitose <i lang="ain-Latn">=an</i> never
		combines with a singular verb: the singular form <i lang="ain-Latn">ek</i> 'come' takes
		<i lang="ain-Latn">ku=</i>/<i lang="ain-Latn">=en</i> (1SG), while
		<i lang="ain-Latn">arki=an</i> 'we come' (plural + fourth) is well-formed. In the
		<em>atomte-itak</em> (elegant) register of verse,
		<i lang="ain-Latn">ek-an</i> 'I come.SG' is grammatical and serves as the hero's first
		person, overriding the number-agreement constraint <Ref k="sato2008" p="263" />. This
		number-agreement override is a diagnostic of verse language and does not appear in prose
		<Ref k="sato2008" p="263" />. See <Xr ch="person-marking-architecture" /> for the paradigm.
	</p>
