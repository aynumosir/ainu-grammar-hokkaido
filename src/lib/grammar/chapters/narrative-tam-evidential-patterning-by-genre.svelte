<S t="Elegant register, applicative morphology, and poetic verse language" id="elegant-register-verse">
	<p>
		Verse genres (kamuy yukar, yukar, prayer) require the <em>atomte-itak</em> (elegant/poetic
		register), which is distinguished from everyday <em>yayan-itak</em> by four properties:
		development in 5-syllable verse units, dense use of formulaic phrases to meet syllable
		count, longer polysynthetic word-forms than colloquial usage, and a distinct poetic lexicon
		<Ref k="endo2022" p="§3" />; <Ref k="sato2008" p="263–264" />. Register is a continuum
		keyed to verse-vs-prose and improvisation-vs-fixedness, not a binary: Nakagawa argues that
		elegant and everyday mark stages on this cline, with prayer phrases and yukar phrases
		constituting sub-variants of the elegant pole rather than a unified "poetic register"
		distinct from prayer <Ref k="nakagawa2001b" p="5" /> ‹contested›. See
		<Xr ch="poetic-archaic-elevated-register" />.
	</p>
	<p>
		Grammatically, the most diagnostic elegant-register feature is the applicative case-marking
		system: locative, directional, and other oblique relations that prose encodes with postpositional
		particles are encoded in verse by the prefixes <i lang="ain-Latn">e-</i>,
		<i lang="ain-Latn">o-</i>, and <i lang="ain-Latn">ko-</i> applied to the verb
		<Ref k="sato2008" p="263" />. The choice between prefix and particle is therefore a
		genre diagnostic — prefix = verse; particle = prose — and applies equally across kamuy yukar,
		yukar, and prayer. Prayer is additionally characterised by
		<i lang="ain-Latn">e-ko-</i> stacks: the Monbetsu prayer text cited by Endō shows
		<i lang="ain-Latn">ku-e-ko-pase-</i> and <i lang="ain-Latn">k-e-ko-onkami</i> '(I)
		respectfully worship', layering applicative prefixes in a way unattested in conversational
		register <Ref k="endo2022" p="§2.3, §3.4" />. The applicative system is treated in the
		valency chapters; its genre-distribution is noted here as a cross-cutting marker of elegant
		language.
	</p>
	<p>
		The comparative / likening particle <i lang="ain-Latn">kane</i> takes a poetic value in
		verse that extends beyond its everyday circumstantial use. In the Abuta yukar,
		<i lang="ain-Latn">kane</i> introduces similes of the type
		<i lang="ain-Latn">urar ruyka a-turi kane</i> 'stretching the mist as if a bridge' (l. 136)
		and <i lang="ain-Latn">kamuy takaramono esik kane</i> 'as if filled with divine treasure'
		(l. 182) — a comparative use that Satō treats as a yukar-specific extension of the
		circumstantial particle <Ref k="sato2009yukar" p="§2.2.1" /> ‹corpus-suggested›.
	</p>
</S>

<S t="Prose intonation and genre-conditioned phonetics" id="prose-intonation">
	<p>
		A phonetic correlate of genre is the sentence intonation contour. In conversational
		register, utterances addressed to an interlocutor tend toward rising intonation, marking
		expectation of a response. Folktale narration (<i lang="ain-Latn">uwepeker</i>) is
		one-directional and shows a flat, low-variation melodic line — a register correlate of the
		absent interlocutor and the unidirectional narrative address. Within the Saru basin itself
		there is geographic variation: upper-Saru (middle-river) narrators are more monotone,
		lower-Saru narrators more melodically undulating (nukopoli, <Ref k="aynucorporadiscord" />,
		2024-11-16, <code>asserted</code>, citing <Ref k="tamura1996" />). The verse genres, sung
		to fixed or narrator-specific melodies, impose melody over phonetic intonation; prose prayer
		is improvised with a personal melody but is not strictly syllable-metered
		<Ref k="endo2022" p="§2.3" />.
	</p>
</S>

<S t="Debates and limitations" id="debates-gaps">
	<p>
		Three issues remain contested or under-documented. First, the semantic status of
		<i lang="ain-Latn">ruwe ne</i> in narrative — whether it is an evidential that happens to
		dominate narrative or a discourse-boundary marker that has been reanalysed from evidential
		source — is unresolved in the literature: Dal Corso's source-reliability account and
		Hirosawa's territory-of-information account both handle the distribution but make different
		predictions at the discourse level <Ref k="dalcorso2018" p="335–337" />;
		<Ref k="hirosawa2026" p="89" /> ‹contested›. This question cannot be settled here; it is
		flagged in <Xr ch="ruwe-ne-inferential" />.
	</p>
	<p>
		Second, per-genre corpus frequencies of the evidential and TAM constructions are not
		directly queryable from the available corpus, which is not genre-tagged as a facet.
		The whole-corpus token counts reported above (<i lang="ain-Latn">ruwe ne</i> 16,350;
		<i lang="ain-Latn">hawe ne</i> 796, etc.) include both narrative and non-narrative
		contexts, and the 86% share of <i lang="ain-Latn">ruwe ne</i> among evidential forms is an
		upper bound that folds in homophonous lexical uses of the noun <i lang="ain-Latn">ruwe</i>.
		Genre-internal counts — confirming, for example, that prayer shows lower
		<i lang="ain-Latn">ruwe ne</i> density than uwepeker — remain to be computed on a
		genre-tagged subcorpus ‹speculative›.
	</p>
	<p>
		Third, the grammar of eastern Hokkaido verse (<i lang="ain-Latn">sakorpe</i>, Tokachi and
		Kushiro) is documented from few texts. The Sakaguchi 2018 Tokachi sakorpe (narrator
		山本多助) and the Nakagawa white-heron text series (2000–2025) contain the relevant
		material but have not been fully analysed for evidential and TAM distribution
		<Ref k="endo2022" p="§5" />; <Ref k="nakagawatexts2000" />. Whether the Tokachi
		bare-concept-form preference (<i lang="ain-Latn">ru</i>/<i lang="ain-Latn">sir</i>/<i
			lang="ain-Latn">haw</i
		> rather than possessed <i lang="ain-Latn">ruwe</i>/<i lang="ain-Latn">siri</i>/<i
			lang="ain-Latn">hawe</i
		>) found in Takahashi's elicited data also holds in
		<i lang="ain-Latn">sakorpe</i> verse is unknown <Ref k="takahashi2013" p="129–131" />
		‹speculative›.
	</p>
</S>
