<S t="Overview: genre conditions clause-final choice" id="overview">
	<p>
		The Hokkaido Ainu evidential system — four formal nouns (<i lang="ain-Latn">ruwe</i>,
		<i lang="ain-Latn">hawe</i>, <i lang="ain-Latn">siri</i>, <i lang="ain-Latn">humi</i>)
		closing nominalized clauses with the copula or an existential — does not distribute evenly
		across connected discourse. Prose folktale (<i lang="ain-Latn">uwepeker</i>), divine epic
		(<i lang="ain-Latn">kamuy yukar</i>), heroic epic (<i lang="ain-Latn">yukar</i>/
		<i lang="ain-Latn">sakorpe</i>), and ritual prayer each impose a characteristic profile on
		the clause-final slot: which evidential nominal dominates the main-clause boundary, how
		dense quotative embedding is, and which register-specific frames appear or are blocked.
		<Ref k="dalcorso2018" p="331" />; <Ref k="endo2022" p="§2.4" />.
	</p>
	<p>
		The constructions themselves are treated in the evidential chapters: the semantics and
		morphosyntax of <i lang="ain-Latn">ruwe ne</i> in <Xr ch="ruwe-ne-inferential" />, the
		reportative frame <i lang="ain-Latn">hawe ne</i>/<i lang="ain-Latn">hawe as</i> in
		<Xr ch="hawe-ne-reportative" />, and the full four-term paradigm in
		<Xr ch="evidential-schema-overview" />. Fourth-person narrator marking and its logophoric
		analysis are treated in <Xr ch="fourth-person-honorific" /> and
		<Xr ch="logophoric-narration-and-reported-discourse" />. The genre taxonomy and verse
		structure are in <Xr ch="oral-literature-genre-taxonomy" />,
		<Xr ch="heroic-and-divine-verse" />, and <Xr ch="sakehe-refrain-and-sung-verse-structure" />.
		The present chapter maps those systems onto genre-by-genre distribution.
	</p>
</S>

<S t="ruwe ne as the narrative clause-final default" id="ruwe-ne-narrative-default">
	<p>
		Across all three narrative genres — <i lang="ain-Latn">uwepeker</i>,
		<i lang="ain-Latn">kamuy yukar</i>, and <i lang="ain-Latn">yukar</i> —
		<i lang="ain-Latn">ruwe ne</i> is the unmarked main-clause clause-final. Dal Corso's
		Hokkaido reference texts yield 1,508 tokens of <i lang="ain-Latn">ruwe ne</i> out of 2,232
		evidential tokens (roughly two-thirds), with the form dominating wherever the narrator presents
		story-world events as established fact <Ref k="dalcorso2018" p="72, 331" />. In the present
		Hokkaido corpus the skew is steeper still: 16,350 tokens of <i lang="ain-Latn">ruwe ne</i>
		beside 796 <i lang="ain-Latn">hawe ne</i>, 607 <i lang="ain-Latn">siri ne</i>, and 200
		<i lang="ain-Latn">humi ne</i> ‹corpus-confirmed›. Community discussion corroborates the
		distributional pattern: <i lang="ain-Latn">ruwe ne</i> is especially frequent in folktale
		and legend narration and collocates with the narrative connective <i lang="ain-Latn">akusu</i>
		'thereupon' to move the storyline forward (nukopoli, <Ref k="aynucorporadiscord" />,
		2024-11-09, <code>asserted</code>).
	</p>
	<p>
		Two accounts of this dominance are available and they partially conflict. Hirosawa argues that
		<i lang="ain-Latn">ruwe ne</i> marks discourse-new content that the speaker claims as
		personal knowledge — the form presents a proposition as belonging to the speaker's
		information territory, which is exactly what a narrator does when introducing each new
		story-world event <Ref k="hirosawa2026" p="75–81" />. Dal Corso, treating the same form as
		a direct evidential of reasoning and knowledge, notes separately that it serves as "the
		default predicate of the narrative frame-text" because the narrator presents traditional
		narrative content as shared, knowable fact rather than as ongoing perception or hearsay
		<Ref k="dalcorso2018" p="331" />. Yoshikawa analyses <i lang="ain-Latn">ruwe ne</i> as the
		semantically unmarked member of the evidential series, functioning like Japanese
		<span lang="ja">のだ</span>/<span lang="ja">んです</span> to mark episode boundaries and
		scene transitions <Ref k="yoshikawa2020" /> ‹contested›. The three accounts share the
		structural observation — <i lang="ain-Latn">ruwe ne</i> is the default at main-clause
		boundaries in narrative — while differing on its semantic driver; the debate is treated in
		<Xr ch="ruwe-ne-inferential" />.
	</p>

