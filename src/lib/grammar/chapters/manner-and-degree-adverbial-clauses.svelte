<S t="Scope of this chapter" id="scope">
	<p>
		The conjunctive-particle inventory of Hokkaido Ainu (surveyed in
		<Xr ch="clause-linkage-overview" />) contains three sets of items whose semantics cluster
		around manner, state, and degree rather than temporal sequence, conditionality, or causation. This
		chapter treats all three: the conjunctive particle <A w="no" gl="(state/manner linker)" /> in
		its stative and negated-manner uses; the group&nbsp;⑦ simile and evidential-inference particles
		<A w="pekor" gl="as if (counterfactual)" />, <A w="apekor" gl="as if (strong)" />,
		<A w="noyne" gl="apparently (inference)" />, and
		<A w="kotom" gl="it seems (literary)" />; and degree and extent clauses built around the
		postposition <A w="pakno" gl="up to, to the extent of" />. The chapter is bounded on two
		sides: the manner-adverb-deriving suffix <i lang="ain-Latn">-no</i> that attaches to stative
		verb roots to form adverbs such as
		<A w="pirkano" gl="well, in a good way" /> belongs to the word-formation domain and is treated
		in <Xr ch="adverbializer-no" />; the conditional and concessive particles, though partially
		manner-like in their semantics, are documented in
		<Xr ch="conditional-temporal-concessive-clauses" />. <Ref k="nakagawa2024" p="§9" />; <Ref k="sato2008" p="§1.7" />
	</p>
</S>

<S t="The no particle: stative and negated-manner clauses" id="no-particle">
	<p>
		The conjunctive particle <i lang="ain-Latn">no</i> occupies the same post-predicate slot as
		<i lang="ain-Latn">wa</i> and <i lang="ain-Latn">hine</i> (see
		<Xr ch="sequential-wa-and-clause-chaining" />), but its distribution is conditioned by the
		aspectual and polar character of the clause it closes rather than by temporal semantics. Nakagawa
		states the conditioning directly: <i lang="ain-Latn">no</i> appears when the preceding
		predicate is either stative, expressing a persistent condition, or an action verb under negation
		<Ref k="nakagawa2024" p="340" />. Satō identifies the high-frequency pattern as the 'without
		doing X' reading after negation: in <i lang="ain-Latn">somo … no …</i> constructions the choice
		of <i lang="ain-Latn">no</i> over <i lang="ain-Latn">wa</i> or
		<i lang="ain-Latn">kor</i> is near-obligatory <Ref k="sato2008" p="45–46" />. Refsing reports
		the same distributional pattern for Shizunai, where <i lang="ain-Latn">no</i> on a stative
		predicate yields an adverbial-adjunct reading 'in the state of X' <Ref
			k="refsing1986"
			p="241"
		/>.
	</p>

	<Ex
		m="a=onaha somo mokor no kamuyoritak kor an"
		g="4.POSS=father NEG sleep CONJ pray CONJ exist"
		tr="My father, without sleeping, was praying to the gods."
		cite="nakagawa2024:340"
		dial="SAR"
		place="Nakagawa ex. 614"
		note="no follows the negated predicate somo mokor 'does not sleep'; the main-clause progressive kamuyoritak kor an 'was praying' is expressed with the simultaneity particle kor plus the existential an."
	/>

	<Ex
		m="somo ipe no arpa=an"
		g="NEG eat CONJ go=4.S"
		tr="Without eating, she went."
		constructed
		note="The canonical somo V no construction: no is the required linker after a negated action predicate. Compare the unpreferred *somo ipe wa, which is disfavoured precisely because the preceding predicate is negated."
	/>

	<p>
		The particle <i lang="ain-Latn">no</i> is segmentally identical to the manner-adverb suffix
		<i lang="ain-Latn">-no</i> that derives adverbs from stative verb roots (see
		<Xr ch="adverbializer-no" />), and the two overlap where the host is a stative property verb.
		The string <i lang="ain-Latn">pirka no arpa</i> 'went well' admits either the particle analysis
		— a stative-predicate clause <i lang="ain-Latn">[pirka]</i> followed by the connective — or the
		suffix analysis, where <i lang="ain-Latn">pirkano</i> 'well' is a derived adverb modifying the
		motion verb. Nakagawa notes that the particle analysis is more transparent for productive
		negation patterns such as <i lang="ain-Latn">k=eyam no</i> 'without hesitating', but that
		frozen adverbs such as <i lang="ain-Latn">poronno</i> 'a lot' — from
		<A w="poron" gl="be big, be much" />, which does not mean 'big-ly' in compositional terms —
		resist it; he concludes that a sharp line cannot be drawn between the particle and the suffix
		<Ref k="nakagawa2024" p="340–341" />. Ijäs presents <i lang="ain-Latn">no</i> for the learner
		alongside the claim that it tends to mark a same-subject context while
		<i lang="ain-Latn">wa</i> marks a different-subject one; that framing is Ijäs's own and is not
		supported by the primary grammars, which derive the distribution from polarity and aspect
		‹contested› <Ref k="ijas2023" />. That debate is adjudicated in
		<Xr ch="clause-linkage-overview">the overview chapter</Xr>.
	</p>
</S>

<S t="Simile and evidential inference: pekor, apekor, noyne, and kotom" id="simile-inference">
	<p>
		Nakagawa <Ref k="nakagawa2024" p="353–355" /> places a cluster of four particles in group&nbsp;⑦
		of his semantic taxonomy, labelled 推量・様態 'conjecture/manner'. All four follow a finite
		predicate in the standard post-predicate position, but they encode the speaker's epistemic stance
		toward the preceding clause — either as a comparison against something the speaker knows to be
		false (simile) or as an inference drawn from available evidence. The distinction matters for
		translation and for analysis: <i lang="ain-Latn">pekor</i> likens the main-clause state of
		affairs to a counterfactual standard, while <i lang="ain-Latn">noyne</i> and
		<i lang="ain-Latn">kotom</i> present a genuine inference the speaker holds about what is
		actually the case <Ref k="sato2008" p="55–56" />. These particles overlap with the broader
		evidential system; their relation to the nominalizing evidentials is taken up in
		<Xr ch="evidential-schema-overview" /> and <Xr ch="ruwe-ne-inferential" />.
	</p>

