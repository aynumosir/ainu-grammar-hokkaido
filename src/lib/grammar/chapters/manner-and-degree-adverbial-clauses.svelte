	<p>
		<strong>The pekor cluster.</strong> <i lang="ain-Latn">Pekor</i> and its dialectal allomorphs
		<i lang="ain-Latn">pokor</i> and <i lang="ain-Latn">pokon</i> liken the main-clause situation
		to the preceding clause while presupposing that the preceding clause is false or
		counter-to-fact from the speaker's viewpoint <Ref k="nakagawa2024" p="354" />. The intensified
		<i lang="ain-Latn">apekor</i> (allomorphs <i lang="ain-Latn">apokon</i>) strengthens this to
		utter impossibility: Nakagawa's illustration is a figure in narrative so large that two or three
		men stacked on one another would match the comparison, a physically impossible standard <Ref
			k="nakagawa2024"
			p="354"
		/>. A segmentally related postpositional pair —
		<i lang="ain-Latn">nepkor</i> / <i lang="ain-Latn">nepkon</i> 'like (a noun)' — takes a
		nominal host and is distinct from the clause-final particle both in its host category and in
		its position <Ref k="nakagawa2024" p="354" />.
	</p>

	<Ex
		m="nep ka e=nukar pekor e=yki"
		g="something EMPH 2SG.A=see CONJ 2SG.S=act"
		tr="You are acting as if you can see something."
		cite="sato2008:56"
		dial="SAR"
		note="pekor follows the finite clause nep ka e=nukar 'you see something' and connects to the main clause e=yki 'you behave/act'. The presupposition is that nothing is actually visible — the comparison is counterfactual. Satō (2008: 56) uses this example to contrast pekor with noyne: noyne would instead report a genuine inference from evidence."
	/>

	<p>
		<strong>Noyne and kotom.</strong> Where <i lang="ain-Latn">pekor</i> introduces a counterfactual
		standard, <i lang="ain-Latn">noyne</i> and <i lang="ain-Latn">kotom</i> present what the
		speaker genuinely infers. <i lang="ain-Latn">Noyne</i> is the everyday-speech variant, attested
		in Saru, Chitose, Horobetsu, and Shizunai; <i lang="ain-Latn">kotom(no)</i> carries a literary
		register and is commonly found with judgment verbs such as
		<i lang="ain-Latn">a=esanniyo</i> 'I judged/surmised' <Ref k="nakagawa2024" p="354–355" />.
		Recurring collocation frames for <i lang="ain-Latn">noyne</i> include
		<i lang="ain-Latn">noyne ramu / yaynu</i> 'think that',
		<i lang="ain-Latn">noyne humas</i> 'feel as if', and
		<i lang="ain-Latn">noyne siran</i> 'look as if' <Ref k="nakagawa2024" p="355" />.
	</p>

	<Ex
		m="tane tamanko kor noyne okokokse hawe as"
		g="now egg have INFR cluck voice-POSS exist.PL"
		tr="Apparently having just laid an egg, it is clucking kok-kok."
		cite="nakagawa2024:355"
		dial="CHI"
		place="Nakagawa ex. 687"
		note="The subject (a hen) is implicit. kor is the transitive verb 'have'; noyne is the inferential clause-linker that marks the entire first clause [tane tamanko kor] 'now having an egg' as inferred rather than directly witnessed. okokokse is onomatopoeic 'kok-kok' used as a sound verb; hawe as 'there is the voice/sound of' closes the main clause."
	/>

	<p>
		Both <i lang="ain-Latn">noyne</i> and <i lang="ain-Latn">kotom</i> sit at the boundary of the
		evidential system in <Xr ch="evidential-schema-overview" />. Their collocational frames —
		<i lang="ain-Latn">noyne siran</i> 'look as if', <i lang="ain-Latn">noyne humas</i> 'feel as
		if' — parallel the situational evidential <i lang="ain-Latn">siri ne</i> (visible scenes) and
		the non-visual <i lang="ain-Latn">humi ne</i> (sensations), but the two systems differ
		structurally: <i lang="ain-Latn">noyne</i> and <i lang="ain-Latn">kotom</i> are conjunctive
		particles and do not take the copula <i lang="ain-Latn">ne</i> ‹consensus›.
	</p>

	<p>
		Refsing's Shizunai grammar classifies <i lang="ain-Latn">pokon</i> 'pretence' and
		<i lang="ain-Latn">kotom</i> 'seeming' together as "nominalizers of appearance" in her §14.12
		conjunctionalization taxonomy, and lists alongside them a "comparative" formal noun
		<i lang="ain-Latn">koraci</i> <Ref k="refsing1986" p="259" />. These subclass labels are
		specific to Refsing's Shizunai analysis and are not foregrounded in Saru or Chitose
		descriptions; the extent to which <i lang="ain-Latn">koraci</i> is productive in other Hokkaido
		dialects is not established in the accessible sources ‹speculative›.
	</p>
</S>

<S t="Degree and extent: pakno and comparison frames" id="degree-pakno">
	<p>
		The postposition <A w="pakno" gl="up to, as far as, to the extent of" /> heads a range of
		extent and degree constructions in Hokkaido Ainu. Its core use is spatial or temporal — 'up to
		the house', 'until now' — but the same form extends to degree: 'to the extent/degree of X'.
		Nakagawa lists <i lang="ain-Latn">pakno</i> in the postposition inventory without assigning it
		a special comparative status; the degree reading follows from the general extent semantics
		applied to a property or state rather than a location or time point
		<Ref k="nakagawa2024" p="504" />.
	</p>

	<p>
		In the clause-linkage domain the most grammatically productive role of
		<i lang="ain-Latn">pakno</i> is in comparison frames. Bugaeva documents a prenominal
		relative-clause strategy in which <i lang="ain-Latn">pakno</i> marks the standard against
		which a property of the head noun is measured. Because direct relativization of a standard of
		comparison — the head noun filling a gap in a comparative clause — is not attested, Ainu
		instead embeds a degree-adjunct clause prenominally: the construction '[to whose extent no rich
		man exists] rich man' places the comparison standard inside the relative clause as a degree
		adjunct rather than gapping the standard as an argument <Ref k="bugaeva2015" p="45, 49" />.
	</p>

	<Ex
		m="a=ona pakno nispa isam nispa ne"
		g="4.POSS=father equal rich.man not.exist rich.man COP"
		tr="My father was a rich man with no equal."
		cite="bugaeva2015:49"
		dial="HK"
		note="pakno marks the degree of the father's wealth as the comparison standard. The prenominal clause [a=ona pakno nispa isam] 'to whose extent/degree no rich man exists' modifies the head nispa 'rich man'. The strategy amounts to a comparative without a dedicated comparative particle: no other rich man exists to the extent/degree of my father. See the full relativization analysis in the prenominal chapter."
	/>

	<p>
		The full relativization mechanism behind this construction is treated in
		<Xr ch="prenominal-gap-relative-clauses" />. Bugaeva identifies the type as an "equivalent of
		the standard-of-comparison relativization" — a degree-adjunct workaround rather than a
		grammaticalized comparative morpheme — and leaves open whether it constitutes a productive
		strategy or a pragmatic repair ‹corpus-suggested› <Ref k="bugaeva2015" p="45" />.
	</p>

	<p>
		A further degree/comparison use surfaces in Satō's description of
		<i lang="ain-Latn">kane</i>. Alongside its core function as a simultaneity particle in
		non-Saru/Chitose dialects (see <Xr ch="simultaneous-kor-clauses" />), Satō records a reading
		glossed '〜ほどに' 'to the degree/extent that' — a degree-comparison value of the same form <Ref
			k="sato2008"
			p="54–55"
		/>. The extent to which this degree use is distinct from the simultaneity value, and how it
		distributes across dialects, is not settled in the available sources ‹speculative›.
	</p>
</S>
