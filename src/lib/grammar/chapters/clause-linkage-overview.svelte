	/>

	<p>
		The particle is looser than a verbal suffix in prosody as well as in structure. Nakagawa,
		reporting Tamura, notes that a speaker may close the first clause with full sentence-final
		intonation, pause, and only then produce the connective and the clause that follows it; on this
		account the connective belongs to neither of the clauses it joins <Ref
			k="nakagawa2024"
			p="514"
		/>. Refsing records the same delayed continuation for Shizunai, where a pause may fall between
		the verb expression and the conjunctionalizer <Ref k="refsing1986" p="238" />. This autonomy
		feeds a genuine disagreement over constituency: Nakagawa declines to posit a clause
		(<span lang="ja">節</span>) unit or a main-versus-subordinate division for Ainu, holding that the
		apparatus cannot be carried over from Japanese or English unchanged, while Refsing and the
		Handbook describe the same surface domain with terms such as conjunctionalizer, adverbial-clause
		marker, and subordinate clause <Ref k="nakagawa2024" p="510, 514" />; <Ref
			k="refsing1986"
			p="238"
		/>; <Ref k="bugaeva2022" /> ‹contested›.
	</p>

	<p>
		Two word-classes do the linking work, and keeping them apart resolves most of the descriptive
		clutter. The post-predicate conjunctive particle (<span lang="ja">接続助詞</span>) is bound to the
		predicate it closes; a clause-initial connective adverb such as <i lang="ain-Latn">orowa(no)</i>
		‘and then’ or <i lang="ain-Latn">rapok(ke)</i> ‘meanwhile’ is free and can stack after a
		conjunctive particle. Refsing makes this the organizing cut of her chapter, between bound
		conjunctionalizers and free conjunctions; Nakagawa keeps only the particles as a
		<span lang="ja">助詞</span> class and folds the free connectives into the adverb phrase <Ref
			k="refsing1986"
			p="238"
		/>; <Ref k="nakagawa2024" p="516" />.
	</p>

