<S t="Finite clauses joined by post-predicate particles" id="finite-linkage">
	<p>
		Hokkaido Ainu builds complex sentences by chaining fully finite clauses. Each linked predicate
		keeps the ordinary verbal morphology of its own clause — person indexing, polarity, plural stem
		choice, aspectual auxiliaries, and any clause-final evidential or nominalizer — and the link
		between clauses is carried by a separate post-predicate particle such as
		<i lang="ain-Latn">wa</i>, <i lang="ain-Latn">hine</i>, <i lang="ain-Latn">akusu</i>,
		<i lang="ain-Latn">kor</i>, <i lang="ain-Latn">kusu</i>, or <i lang="ain-Latn">yakun</i>. There
		is no finite/non-finite split in the verb and no converb series: the Ainu verb does not oppose a
		sentence-final form to a special linking form, so a Japanese rendering in
		<span lang="ja">〜て</span> gives no warrant for a converb analysis <Ref
			k="nakagawa2024"
			p="514"
		/>. The typological profile of the language states the same fact directly — Ainu has no
		participles, converbs, or other standard non-finite forms <Ref k="bugaeva2022" />. The
		connectives are therefore particles that join finite clauses, glossed here as
		<span class="sc">conj</span> and not as verbal suffixes.
	</p>

	<Ex
		m="a=yupi-hi i=resu hine oka=an pe ne hike"
