<S t="The coordination–subordination continuum" id="continuum">
	<p>
		The inventory spans a continuum rather than a clean coordinate/subordinate dichotomy. At one
		edge, <i lang="ain-Latn">hine</i> enumerates events in temporal order and takes no stance on how
		they are related; at the other, the complementizing uses of <i lang="ain-Latn">sekor</i>,
		<i lang="ain-Latn">kuni</i>, <i lang="ain-Latn">kunak</i>, <i lang="ain-Latn">yak</i>, and
		<i lang="ain-Latn">kusu</i> place an entire clause under a verb of speech, thought, or command
		<Ref k="nakagawa2024" p="338, 470–479, 507–513" />. Most linkage falls between the poles: the
		medial clause stays finite and keeps its own argument indexes while the chain is read as one
		larger episode, the configuration that the coordination–subordination typology of <Ref
			k="foleyvanvalin1984"
			paren
		/> treats as cosubordination.
	</p>

	<Ex
		m="micipa ku=kar hine ohaw or k=omare"
		g="honewort 1SG.A=gather CONJ soup place 1SG.A=put.in"
		tr="I gathered honewort and put it into the soup."
		cite="sato2008:44"
		dial="SAR"
		note="A plain hine sequence: two finite predicates, each with first-person A indexing, listed in order with no claim of dependence."
	/>

	<p>
		<i lang="ain-Latn">Wa</i> binds more tightly. In Tamura's characterization, as transmitted by
		Nakagawa, <i lang="ain-Latn">wa</i> gathers two situations into one coherent event; Nakagawa
		divides its readings into result-state, manner or method, and cause-and-effect <Ref
			k="nakagawa2024"
			p="339–342"
		/>. Satō gives the same three-way description and supplies the diagnostic that separates Ainu
		<i lang="ain-Latn">wa</i> from a same-subject converb: the two clauses it joins may have
		different subjects, which a Japanese <span lang="ja">〜て</span> clause cannot <Ref
			k="sato2008"
			p="45–46"
		/>.
	</p>

	<Ex
		m="hekaci suma osura wa ontaro hokus"
