	<p>
		Cause and purpose are centred on <i lang="ain-Latn">kusu</i>. Plain
		<i lang="ain-Latn">kusu</i> gives the purpose of the following event, especially with motion
		verbs, or its reason; <i lang="ain-Latn">wa kusu</i> and <i lang="ain-Latn">hi kusu</i> are
		reason-only forms that present the first event as actual <Ref k="nakagawa2024" p="352–353" />;
		<Ref k="sato2008" p="53–54" />; <Ref k="refsing1986" p="251–252" />. The same segmental material
		enters the intention auxiliary <i lang="ain-Latn">kusu ne</i>, the copula taking no person index,
		so the detail chapter separates clause-linking <i lang="ain-Latn">kusu</i> from future and
		intention syntax <Xr ch="causal-and-purpose-clauses" />.
	</p>

