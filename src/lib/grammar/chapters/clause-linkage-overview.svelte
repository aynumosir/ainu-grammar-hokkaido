<S t="Reference tracking without switch-reference morphology" id="reference">
	<p>
		Hokkaido Ainu tracks participants across a chain through the obligatory person indexes on each
		finite verb and through discourse expectation; it has no same-subject/different-subject converb
		morphology <Ref k="bugaeva2022" />. The connectives carry only statistical tendencies. Refsing
		finds that the predicates joined by <i lang="ain-Latn">wa</i> share a subject in about ninety
		percent of her Shizunai material, yet Satō shows the preference is not a constraint, since
		<i lang="ain-Latn">wa</i> tolerates different subjects <Ref k="refsing1986" p="240" />; <Ref
			k="sato2008"
			p="46"
		/>. The result is a same-subject-preferring chainer with no dedicated morphology.
	</p>

	<p>
		The <i lang="ain-Latn">no</i>/<i lang="ain-Latn">wa</i> choice is derived from the first
		predicate rather than from coreference. Nakagawa and Satō tie <i lang="ain-Latn">no</i> to the
		aspect and polarity of that predicate: stative predicates and negated action predicates take
		<i lang="ain-Latn">no</i>, with the high-frequency reading ‘without doing X’ under negation <Ref
			k="nakagawa2024"
			p="340–341"
		/>; <Ref k="sato2008" p="45–47" />. Ijäs offers the learner's heuristic that
		<i lang="ain-Latn">no</i> marks a same-subject negated clause and <i lang="ain-Latn">wa</i> a
		different-subject one; that framing is hers, and the primary grammars condition the choice on
		polarity and aspect, not on subject identity ‹contested› <Ref k="ijas2023" />.
	</p>

	<Ex
		m="ku=caranke ka somo ki no ku=hosipi"
		g="1SG.S=complain even NEG do CONJ 1SG.S=return"
		tr="I went home without complaining."
		cite="sato2008:44"
		dial="SAR"
		note="No follows a negated action predicate; both clauses share the first-person subject, so the choice of no over wa here tracks polarity, not a change of subject."
	/>

	<p>
		The nearest approach to a switch-reference device is discourse-structural. The Handbook's
		descriptive grammar reports that the sequence of a perfective <i lang="ain-Latn">a</i> auxiliary
		and a connective such as <i lang="ain-Latn">korka</i>, <i lang="ain-Latn">wa</i>, or connective
		<i lang="ain-Latn">p</i> tends to fall where the scene, topic, or actor of a narrative changes,
		the perfective marking a situation as completed and cut off from the present <Ref
			k="bugaeva2022"
		/> ‹speculative›. The exact author and page of that passage have not been confirmed against the
		print edition, and the device is a clause-chaining boundary marker, not grammaticalized
		switch-reference. Whether the sequential connectives <i lang="ain-Latn">hine</i> and
		<i lang="ain-Latn">akusu</i> encode subject continuity in a switch-reference-like opposition or
		instead encode discourse (un)expectedness is the central open question of the domain ‹contested›;
		it is adjudicated against a corpus count of subject continuity in <Xr
			ch="hine-akusu-switch-reference"
		/>, and the reference-tracking toolkit as a whole is drawn together in <Xr
			ch="reference-tracking"
		/>.
	</p>
</S>

<S t="Condition, concession, cause, and the mood of the matrix clause" id="condition-cause">
	<p>
		The conditional system is organized by realis status and, distinctively, by the sentence type of
		the matrix clause. Satō's dedicated study finds that Ainu conditionals are mood-constrained but
		do not privilege the epistemic mood the way Japanese conditionals do: the language has no
		conditional restricted to truth-judgement, yet it has conditionals that require an imperative or
		interrogative matrix <Ref k="sato2009" p="53–54" />. <i lang="ain-Latn">Yakun</i> is the broadest
		member, attested with declarative, interrogative, imperative, and volitional or conjectural
		matrices, and so it bears most of the translator's load across Japanese
		<span lang="ja">レバ</span>, <span lang="ja">タラ</span>, and <span lang="ja">ナラ</span> <Ref
			k="sato2009"
			p="51–53"
		/>; <Ref k="nakagawa2024" p="347" />. <i lang="ain-Latn">Yak</i> attaches to law-like or warning
		premises and clusters with an evaluative <i lang="ain-Latn">pirka</i> ‘good’ or
		<i lang="ain-Latn">wen</i> ‘bad’; <i lang="ain-Latn">ciki</i> and conditional
		<i lang="ain-Latn">kor</i> are the mood-bound members, <i lang="ain-Latn">ciki</i> introducing a
		premise for a command or question and <i lang="ain-Latn">kor</i> the general-causation
		conditional that elsewhere requires a wh-interrogative matrix <Ref k="sato2009" p="50–51" />;
		<Ref k="nakagawa2024" p="349–351" />.
	</p>

	<Ex
		m="e=ype rusuy haw-e ne yakun tan kaboca e"
		g="2SG.S=eat.a.meal DES voice-POSS COP CONJ this pumpkin eat"
		tr="If you are saying that you are hungry, eat this pumpkin."
		cite="sato2009:52"
		dial="CHI"
		place="Satō ex. 28"
		note="The protasis closes on the reportative tail hawe ne before yakun; the matrix is an imperative, one of the several moods yakun freely admits."
	/>

	<p>
		Concession and counter-expectation sit alongside the conditionals. <i lang="ain-Latn">Yakka</i>
		‘even if, even though’ states that the matrix holds whatever the event of the marked clause, and
		in the frames <i lang="ain-Latn">yakka pirka</i> / <i lang="ain-Latn">yakka wen</i> it grants or
		withholds permission; <i lang="ain-Latn">korka</i> ‘but’, transparently <A w="kor" gl="when" />
		plus <A w="ka" gl="even" />, marks counter-expectation <Ref k="nakagawa2024" p="351–352" />;
		<Ref k="sato2008" p="53" />. Ijäs banks the same set for the learner, and her listing
		construction illustrates the concessive cleanly:
	</p>

