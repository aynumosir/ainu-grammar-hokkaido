	/>

	<p>
		<i lang="ain-Latn">Akusu</i> and <i lang="ain-Latn">awa</i> sit at a third point: a realized
		first event is followed by a discovered, witnessed, or newly unfolding second one, with the
		causal connection left open. Nakagawa treats the two as semantically equivalent and conditioned
		by register and dialect — Saru prose favours <i lang="ain-Latn">akusu</i>, Saru verse and
		several other varieties favour <i lang="ain-Latn">awa</i> <Ref k="nakagawa2024" p="345" />. Satō
		describes the domain as factual condition and ties <i lang="ain-Latn">awa</i> to oral literature
		<Ref k="sato2008" p="47–48" />.
	</p>

