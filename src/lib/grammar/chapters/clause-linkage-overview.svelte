	<p>
		Several of the particles are segmentally identical to material from other classes, and the
		grammar resolves the clash by word-class and position rather than by form.
		<i lang="ain-Latn">Wa</i> is the sequential linker, the ablative case particle, and a
		sentence-final emphatic; <i lang="ain-Latn">kor</i> is a linker and the transitive verb ‘have’;
		<i lang="ain-Latn">kusu</i> is a causal/purpose linker, a free ‘therefore’, and part of the
		intention auxiliary <i lang="ain-Latn">kusu ne</i>; <i lang="ain-Latn">no</i> is a linker and
		the adverb-forming suffix <i lang="ain-Latn">-no</i> <Ref k="nakagawa2024" p="340, 353" />; <Ref
			k="ijas2023"
		/>.
	</p>
