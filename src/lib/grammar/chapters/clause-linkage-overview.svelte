<S t="Interfaces with evidentiality, complementation, and reported speech" id="interfaces">
	<p>
		Conjunctive particles attach outside the clause-final evidential frame. A clause may close on a
		nominalizer-plus-copula evidential such as <i lang="ain-Latn">ruwe ne</i> or
		<i lang="ain-Latn">hawe ne</i> and then take a linker, giving attested orders
		<i lang="ain-Latn">ruwe ne akusu</i>, <i lang="ain-Latn">hawe ne yakun</i>, and
		<i lang="ain-Latn">hawe ne kusu</i>; the evidential sits above clause-internal aspect but below
		the connective, so <i lang="ain-Latn">kusu ne ruwe ne</i> is well formed while
		<i lang="ain-Latn">*ruwe ne kusu ne</i> is not <Ref k="nakagawa2024" p="393" />. The general
		order for a linked clause is thus predicate – evidential – connective. The inferential value is
		treated in <Xr ch="ruwe-ne-inferential" /> and the system as a whole in <Xr
			ch="evidential-schema-overview"
		/>. Satō notes one cross-linguistically marked interaction at this seam:
		<i lang="ain-Latn">akusu</i> can follow a <i lang="ain-Latn">ruwe ne</i> tail, where the Japanese
		factual <span lang="ja">と</span>/<span lang="ja">たら</span> rejects a preceding
		<span lang="ja">のだ</span> <Ref k="sato2009" p="52–53" />.
	</p>

	<p>
		Several connectives are formal nouns pressed into linking service, and the same set runs a
		grammaticalization cline. A formal noun can be a relative-clause head that fills an argument gap,
		a complement marker that nominalizes a saturated clause, or a connective that is neither subject
		nor object of the following clause; Nakagawa distinguishes the three by the argument structure
		around the marker, and places connective <i lang="ain-Latn">p</i>, <i lang="ain-Latn">pe</i>,
		and <i lang="ain-Latn">hike</i> ‘but, and’ at the connective end of the cline <Ref
			k="nakagawa2024"
			p="507–516"
		/>. This links clause combining to nominalization, treated in <Xr
			ch="lexical-head-formal-noun-nominalization"
		/> and <Xr ch="nominalization-p-pe" />.
	</p>

	<p>
		<i lang="ain-Latn">Sekor</i> is the bridge into reported speech. In Saru and Chitose it frames a
		direct quote with a verb of speaking or thinking, names entities in the pattern
		<i lang="ain-Latn">X sekor a=ye</i> ‘call it X’, and frames cries and mimetic sound, its history
		transparent in <A w="se" gl="make a sound, say" /> ‘say’ plus <A w="kor" gl="while" /> ‘while’
		<Ref k="nakagawa2024" p="470" />; <Ref k="ijas2023" />. It is also the one connective that
		triggers person reassignment: a third party's quoted first person is expressed by fourth-person
		forms while the narrator's own quoted speech keeps the first person, the mechanism behind the
		fourth-person voice of oral narrative, which Bugaeva reanalyses as logophoricity <Ref
			k="nakagawa2024"
			p="470–471"
		/>; <Ref k="bugaeva2008" p="40–44" />. The interaction belongs with <Xr
			ch="person-marking-architecture"
		/> and is developed in <Xr ch="quotative-complementation-sekor" />.
	</p>

