	<p>
		<i lang="ain-Latn">Tek</i> is the clearest micro-debate. It is not a connective in Saru or
		Chitose, where it is an auxiliary, yet in most other dialects it links clauses with an
		‘immediately upon X’ sense. Nakagawa's synthesis is geographical: western dialects near Saru
		carry the short-duration restriction, while eastern descriptions dispute whether short duration
		belongs to the meaning at all, and one analysis makes its core function parallel listing <Ref
			k="nakagawa2024"
			p="343–344"
		/> ‹contested›. A related open question is whether the bare verb-plus-verb sequences of Tokachi,
		Ishikari, and Shiranuka — where Saru requires an intervening connective — constitute a serial
		verb construction <Ref k="nakagawa2024" p="518–521" /> ‹contested›. Sakhalin supplies a contrast
		at the edge of the system, where <span class="sc">sa</span> <i lang="ain-Latn">kusu</i> covers
		the continuation and result-state range that Hokkaido splits between
		<i lang="ain-Latn">kor</i> and <i lang="ain-Latn">wa</i>, rather than marking cause or purpose
		<Ref k="nakagawa2024" p="344" />.
	</p>
