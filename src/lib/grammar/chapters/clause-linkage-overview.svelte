<S t="Dialect and comparative edges" id="dialect-edges">
	<p>
		The inventory is dialectally uneven, with Saru and Chitose the best-described baseline in
		Nakagawa and Satō and Shizunai a full reference grammar in Refsing. Shizunai realigns two cells:
		<i lang="ain-Latn">kane</i> marks the simultaneity that Saru assigns to
		<i lang="ain-Latn">kor</i>, and <i lang="ain-Latn">konno</i> the conditional/consequential
		function <Ref k="refsing1986" p="248–250" />; <Ref k="nakagawa2024" p="342, 347–348" />.
	</p>

	<Ex
		m="a=kor katkemat utar an=oskur kane kesto an konno oka=an"
