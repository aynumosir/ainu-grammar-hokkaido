<S t="The exclusive first plural paradigm" id="excl-paradigm">
	<p>
		The first-person plural of Hokkaido Ainu is exclusively exclusive: <i lang="ain-Latn">ci=</i>
		as the transitive subject (A), <i lang="ain-Latn">=as</i> as the intransitive subject (S),
		and <i lang="ain-Latn">un=</i> as the object (O) always refer to a group from which the
		addressee is absent. Nakagawa, Tamura, Satō, and Bugaeva converge on this characterisation
		without qualification <Ref k="nakagawa2024" p="163–164" />; <Ref k="tamura1972" p="18–19" />;
		<Ref k="sato2008" p="31–32" />; <Ref k="bugaeva2012" p="471–472" />.
	</p>
	<p>
		The free pronoun for this row is Saru <i lang="ain-Latn">coka</i>, with dialectal counterparts
		Tokachi <i lang="ain-Latn">ciokay</i> and <i lang="ain-Latn">ciutari</i>. Both the Saru and
		Tokachi forms are built on the transitive-subject prefix <i lang="ain-Latn">ci=</i> followed
		by the plural existential <i lang="ain-Latn">oka(y)</i> 'there are', nominalized — so
		<i lang="ain-Latn">coka(y)</i> is, in origin, 'those who exist as
		<i lang="ain-Latn">ci=</i>-ers' <Ref k="tamura1971" p="3–5" />; <Ref
			k="nakagawa2024"
			p="253"
		/>. The three-cell paradigm, set within the full person architecture, is introduced in
		<Xr ch="person-marking-architecture" />.
	</p>
	<p>
		The table below places the exclusive row beside the fourth-person/indefinite row, whose first
		function is the inclusive 'we'. The contrast between the two rows is the structural basis of
		clusivity in Hokkaido Ainu.
	</p>

	<table>
		<thead>
			<tr>
				<th>row</th>
				<th>A: transitive subject</th>
				<th>S: intransitive subject</th>
				<th>O: object</th>
				<th>referential value</th>
			</tr>
		</thead>
		<tbody>
			<tr>
				<td>
					1PL.EXCL — pronoun Saru <i lang="ain-Latn">coka</i>, Tokachi
					<i lang="ain-Latn">ciokay</i>/<i lang="ain-Latn">ciutari</i>
				</td>
				<td><i lang="ain-Latn">ci=</i> (prefix)</td>
				<td><i lang="ain-Latn">=as</i> (suffix)</td>
				<td><i lang="ain-Latn">un=</i> (prefix)</td>
				<td>we, excluding the addressee</td>
			</tr>
			<tr>
				<td>
					4 / indefinite — pronoun Saru <i lang="ain-Latn">aoka</i>, Tokachi
					<i lang="ain-Latn">anokay</i>/<i lang="ain-Latn">anutari</i>
				</td>
				<td>
					<i lang="ain-Latn">a=</i> / dial. <i lang="ain-Latn">an=</i> (prefix)
				</td>
				<td><i lang="ain-Latn">=an</i> (suffix)</td>
				<td><i lang="ain-Latn">i=</i> (prefix)</td>
				<td>inclusive we; honorific you; impersonal one; logophoric narrator</td>
			</tr>
		</tbody>
	</table>

	<p>
		Before a vowel-initial stem, <i lang="ain-Latn">ci=</i> reduces to <i lang="ain-Latn">c=</i>,
		parallel to the reduction of first-singular <i lang="ain-Latn">ku=</i> to
		<i lang="ain-Latn">k=</i> in the same phonological context <Ref k="nakagawa2024" p="49, 166"
		/>; <Ref k="bugaeva2012" p="467–468" />.
	</p>
</S>

<S t="Tripartite patterning: S, A, and O are distinct" id="tripartite">
	<p>
		The exclusive first plural is one of two tripartite rows in the Hokkaido paradigm: all three
		role exponents differ in form and position. <i lang="ain-Latn">ci=</i> precedes a transitive
		stem as A; <i lang="ain-Latn">=as</i> follows an intransitive stem as S; and
		<i lang="ain-Latn">un=</i> again precedes a transitive stem as O. The S/A side-switch —
		prefix for A, suffix for S — is the morphological basis on which Bugaeva assigns this row a
		tripartite classification <Ref k="bugaeva2008" p="32" />; <Ref k="bugaeva2012" p="472" />;
		<Ref k="nakagawa2024" p="166–167" />.
	</p>
	<p>
		The Tokachi example below shows both subject functions within a single clause chain: the
		exclusive group is the A of the transitive verb <i lang="ain-Latn">o</i> 'board' and, in
		the same utterance, the S of the intransitive plural motion verb <i lang="ain-Latn">arki</i>
		'come'.
	</p>

	<Ex
		m="kuruma an wa ci=o tek arki=as"
		g="car exist CONJ 1PL.EXCL.A=board then come.PL=1PL.EXCL.S"
		tr="There was a car, and we boarded it and came."
		cite="takahashi2015:206"
		dial="TOK"
		place="Takahashi ex. 2"
		note="ci= indexes the exclusive group as A of the transitive o; =as indexes the same group as S of the intransitive plural arki."
	/>

	<p>
		The object prefix <i lang="ain-Latn">un=</i> occupies the normal O-prefix slot of the
		transitive verb alongside a distinct subject index. In the Saru example below, the transitive
		agent is an indefinite/impersonal fourth person indexed by <i lang="ain-Latn">a=</i>, while
		the exclusive group stands as object, indexed by <i lang="ain-Latn">un=</i>.
	</p>

	<Ex
		m="sanbyakugojuen a=un=kore hike ka"
		g="350.yen 4.A=1PL.EXCL.O=give although even"
		tr="although we were given 350 yen, …"
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, isoytak monologue 2, AA Institute Ainu materials [aa-irc/004#109]"
		note="The prefix sequence a=un= stacks a fourth-person (indefinite/impersonal) A and a first-plural-exclusive O on a single transitive stem."
	/>

	<p>
		<i lang="ain-Latn">un=</i> is formally separate from the fourth-person object
		<i lang="ain-Latn">i=</i>: the two prefixes belong to different paradigm rows and index
		different participants. Their historical relationship — including whether Hokkaido
		<i lang="ain-Latn">un=</i> and the Sakhalin counterpart <i lang="ain-Latn">in=</i> are
		cognates of the exclusive O or of the indefinite O — is part of the broader alignment debate
		and is pursued in <Xr ch="alignment-hierarchical-and-the-debate" /> ‹contested›.
	</p>
</S>

<S t="Clusivity: the exclusive row and the fourth-person inclusive" id="clusivity">
	<p>
		The semantic domain 'we' is distributed across two formal rows. The exclusive forms
		<i lang="ain-Latn">ci=</i>/<i lang="ain-Latn">=as</i>/<i lang="ain-Latn">un=</i> index a
		speaker-only group; addressee-inclusive 'we' falls to the fourth-person row
		<i lang="ain-Latn">a=</i>/<i lang="ain-Latn">=an</i>/<i lang="ain-Latn">i=</i>. Nakagawa
		identifies inclusive 'we' as the first of five referential values of the fourth person; Tamura
		treats the same morphology as an inclusive first plural in her eleven-person Saru analysis; and
		Bugaeva places the inclusive value at the centre of the indefinite paradigm <Ref
			k="nakagawa2024"
			p="163–164, 174"
		/>; <Ref k="tamura1972" p="18–19, 39" />; <Ref k="bugaeva2012" p="474–476" />; <Ref
			k="bugaeva2008"
			p="40–43"
		/>.
	</p>

	<Ex
		m="eani hem paye=an ro"
		g="2SG also go.PL=4.S HORT"
		tr="Let you and me go too!"
		cite="nakagawa2024:176"
		dial="CHI"
		place="Nakagawa ex. 138"
		note="The hortative construction includes the addressee; the fourth-person S suffix =an appears on the plural stem paye, not the exclusive =as."
	/>

	<p>
		Tamura notes that <i lang="ain-Latn">coka</i> is the genuine plural of
		<i lang="ain-Latn">kani</i> 'I', while <i lang="ain-Latn">aoka</i> would more accurately be
		described as a speaker-plus-addressee category — though she retains the label 'inclusive first
		plural' for it <Ref k="tamura1972" p="19" />. The exclusive row carries no inclusive value in
		any Hokkaido description consulted. The complete morphology and the non-inclusive uses of
		<i lang="ain-Latn">a=</i>/<i lang="ain-Latn">=an</i>/<i lang="ain-Latn">i=</i> belong to
		<Xr ch="indefinite-fourth-person-paradigm" /> and <Xr ch="fourth-person-honorific" />.
	</p>
	<p>
		Terminological disagreement over the fourth-person row does not affect the clusivity fact.
		Nakagawa's 'fourth person' is a formal class label; Tamura distinguishes several persons whose
		morphology is neutralised; Bugaeva's 'indefinite' groups inclusive, honorific, logophoric, and
		impersonal functions; and Takahashi, on Tokachi, adopts 'inclusive person' as a unifying
		functional term, arguing that the participant structure of the inclusive gives a principled
		account of all the disparate uses <Ref k="nakagawa2024" p="174–179" />; <Ref k="tamura1972"
			p="18–20, 28, 34"
		/>; <Ref k="bugaeva2012" p="474–476" />; <Ref k="takahashi2015" p="205–206, 210" />
		‹contested›. Across these analyses, addressee-inclusive 'we' takes the fourth-person forms.
	</p>
</S>

<S t="Tokachi evidence and dialect scope" id="tokachi-scope">
	<p>
		Takahashi's Tokachi description provides the most explicit formulation of the
		exclusive/inclusive opposition for a single Hokkaido dialect. The exclusive side has
		<i lang="ain-Latn">ci-</i>, <i lang="ain-Latn">-as</i>, and <i lang="ain-Latn">un-</i>, with
		free pronouns <i lang="ain-Latn">ciokay</i> and <i lang="ain-Latn">ciutari</i>; the inclusive
		side has <i lang="ain-Latn">a-</i>/<i lang="ain-Latn">an-</i>, <i lang="ain-Latn">-an</i>,
		and <i lang="ain-Latn">i-</i>, with <i lang="ain-Latn">anokay</i> and
		<i lang="ain-Latn">anutari</i> <Ref k="takahashi2015" p="206–207" />. Takahashi's hyphens for
		personal markers are a source-internal convention; they correspond to the
		<i lang="ain-Latn">=</i>-notation for personal indexes used here.
	</p>
	<p>
		Takahashi also notes that the inclusive row selects plural verb morphology even when the
		referent is a single additional person: a sole addressee triggers plural stem agreement on an
		inclusive-marked verb <Ref k="takahashi2015" p="209" />. Nakagawa documents the same pattern
		for the fourth-person honorific and hortative uses — one esteemed individual addressed with a
		plural stem — and for the narrator in most dialects outside Saru and Chitose <Ref
			k="nakagawa2024"
			p="160–161, 174–176"
		/>. The person-number agreement consequences and the conditioning of plural stems are developed
		in <Xr ch="person-number-interactions" /> and <Xr ch="suppletive-verbal-number" />.
	</p>
	<p>
		In oral-narrative texts, the exclusive series serves a genre-specific narrator function. In
		kamuy yukar, the divine protagonist uses <i lang="ain-Latn">ci=</i>/<i lang="ain-Latn"
			>=as</i>/<i lang="ain-Latn">un=</i> as self-reference, distinguishing the kamuy narrator's
		speech from the human first-person voice of uwepeker, which uses the fourth person <Ref
			k="nakagawa2024"
			p="602–604"
		/>. Whether this pattern derives from an original first-person plural strategy or a
		register-internal specialisation is debated <Ref k="sato2004" p="179–184" />; <Ref
			k="sato2008"
			p="259–262"
		/> ‹contested›. These narrative and logophoric dimensions are treated in <Xr
			ch="fourth-person-honorific"
		/> and <Xr ch="logophoric-narration-and-reported-discourse" />.
	</p>
	<p>
		Sakhalin Ainu presents a different configuration. Sakaguchi reports that the
		<i lang="ain-Latn">ci=</i>-series functions as a grammatically plural encoding of the first
		singular, while the fourth-person <i lang="ain-Latn">an=</i>-series covers both first singular
		and first plural without an exclusive/inclusive split of the Hokkaido type <Ref
			k="sakaguchi2024"
			p="26–27, 162–163"
		/> ‹SA›.
	</p>
</S>

<S t="Morphological status of the indexes and notation" id="notation">
	<p>
		The <i lang="ain-Latn">=</i> boundary on personal indexes and the <i lang="ain-Latn">-</i>
		boundary on derivational and number morphology are descriptive conventions, not a uniform
		claim about morphological status. Bugaeva distinguishes three grades for the Hokkaido indexes,
		using prosodic autonomy and separability as diagnostics: <i lang="ain-Latn">=as</i> and
		<i lang="ain-Latn">=an</i> are word-like (they may be separated from the stem by intervening
		material and have autonomous prosodic weight); <i lang="ain-Latn">a=</i> and
		<i lang="ain-Latn">eci=</i> are clitic-like; and <i lang="ain-Latn">ku=</i>,
		<i lang="ain-Latn">ci=</i>, <i lang="ain-Latn">en=</i>, and <i lang="ain-Latn">un=</i> are
		fuller prefixes that shift the accent kernel <Ref k="bugaeva2012" p="472–473" />. Sakaguchi's
		explicit diagnostics, formulated for Sakhalin material, classify <i lang="ain-Latn">-as</i>
		as root-attaching (affixal) and fourth-person <i lang="ain-Latn">=an</i> as autonomous
		(clitic-like) — reversing Bugaeva's ranking for <i lang="ain-Latn">=as</i> — while both
		agree that <i lang="ain-Latn">=an</i> is the most autonomous of the subject indexes <Ref
			k="sakaguchi2024"
			p="ix, 62–64, 77–85, 163"
		/> ‹contested›. Nakagawa writes all person indexes with <i lang="ain-Latn">=</i> as a
		convenience notation, explicitly disavowing any clitic claim <Ref k="nakagawa2024" p="49" />.
		The ordering diagnostics and the clitic/affix boundary question are pursued in <Xr
			ch="personal-affix-template-and-ordering"
		/>.
	</p>
	<p>
		Ijäs's learner grammar writes the exclusive markers as <i lang="ain-Latn">ci-</i>,
		<i lang="ain-Latn">-as</i>, and <i lang="ain-Latn">un-</i>, with hyphens used as a teaching
		convention rather than a phonological boundary symbol <Ref k="ijas2023" />. The constructed
		example below shows the exclusive intransitive-subject marker in a simple locative clause.
	</p>

	<Ex
		m="cise or ta okay=as"
		g="house place LOC exist.PL=1PL.EXCL.S"
		tr="We are at the house."
		constructed
		cite="ijas2023"
		dial="HK"
		note="Constructed after the pattern of Ijäs's learner materials. The exclusive S suffix =as is written -as in that source; okay is the plural existential stem."
	/>
</S>
