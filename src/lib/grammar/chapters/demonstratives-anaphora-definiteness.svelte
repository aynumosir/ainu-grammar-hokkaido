<S t="Overview: a bipartite deixis system" id="overview">
	<p>
		Hokkaido Ainu demonstratives divide into two formally distinct sets. One set —
		the <em>deictic</em> (on-site) forms — points to referents in the physical scene;
		the other — the <em>anaphoric</em> forms — tracks referents established in
		discourse. Japanese uses a single three-way <span lang="ja">この/その/あの</span>
		series for both functions; Hokkaido Ainu keeps the two apart with different lexical
		roots <Ref k="nakagawa2024" p="284" />. Kindaichi and Chiri's classical grammar
		already identified the deictic prenominals as fusions of three locative roots
		(<i lang="ain-Latn">te</i> 'here', <i lang="ain-Latn">ta</i> 'there',
		<i lang="ain-Latn">to</i> 'yonder') with the copula
		<i lang="ain-Latn">an</i>, while the anaphoric
		<i lang="ain-Latn">ne</i>-series points in the discourse stream, not in space
		<Ref k="kindaichi1936" p="57–58" />. Refsing's Shizunai description formalises
		the whole system as a two-dimensional root paradigm
		<Ref k="refsing1986" p="109" />; the typological framing of Diessel's
		demonstrative taxonomy maps the anaphoric forms onto a recognitional–anaphoric
		cluster distinct from both exophoric pointing and grammaticalized definite articles
		<Ref k="diessel1999" paren />.
	</p>

