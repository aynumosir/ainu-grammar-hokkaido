		Minor deictic prenominals in Saru include verse-register
		<i lang="ain-Latn">tanan</i> (pl. <i lang="ain-Latn">tanoka(y)</i>) and
		emphatic <i lang="ain-Latn">tapan</i> (pl. <i lang="ain-Latn">tapoka(y)</i>),
		the latter most frequent in Chitose address contexts
		<Ref k="nakagawa2024" p="284" />. The independent personal pronouns are treated
