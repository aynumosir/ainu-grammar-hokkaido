		<li>
			<strong><i lang="ain-Latn">nea</i> vs. <i lang="ain-Latn">nean</i>.</strong>
			Saru and Chitose use <i lang="ain-Latn">nea</i>; most other Hokkaido
			dialects (Horobetsu, Shizunai, Ishikari) use
			<i lang="ain-Latn">nean</i> (pronoun <i lang="ain-Latn">neanpe</i>), with
			both forms occurring in some Horobetsu and Ishikari speakers
			<Ref k="nakagawa2024" p="285–286" />.
		</li>
		<li>
			<strong><i lang="ain-Latn">ikia</i> / <i lang="ain-Latn">ikiap</i>.</strong>
			Shizunai and the dialects east of it have a second anaphoric prenominal
			<i lang="ain-Latn">ikia</i> (pl. <i lang="ain-Latn">ikirok</i>; pronoun
			<i lang="ain-Latn">ikiap</i>), functionally close to
			<i lang="ain-Latn">nea</i> but appearing to be more restricted in discourse
			scope. It is rare in Saru <Ref k="nakagawa2024" p="285" />.
		</li>
		<li>
			<strong><i lang="ain-Latn">toan</i> vs. <i lang="ain-Latn">toon</i>.</strong>
			Saru favours <i lang="ain-Latn">toan</i>; both forms occur in Chitose;
			Obihiro and Tokachi avoid <i lang="ain-Latn">toan</i>; Ishikari lacks
			<i lang="ain-Latn">toon</i>
			<Ref k="nakagawa2024" p="285" />. Takahashi's Tokachi description documents
			the local demonstrative system and the range of deictic forms available there
			<Ref k="takahashi2011" />.
		</li>
	</ul>
</S>

<S t="Definiteness in an article-less language" id="definiteness">
	<p>
		Hokkaido Ainu has no grammatical article. Neither the deictic nor the anaphoric
		demonstratives are obligatory in contexts where a definite-article language would
		require an article; bare nouns occur freely in argument position without
		referential specification <Ref k="nakagawa2024" p="283" /> ‹consensus›. The
		language encodes identifiability through a cluster of constructions, none of
		which individually constitutes an article system:
	</p>

	<ul>
		<li>
			<strong>Anaphoric demonstrative</strong>
			<i lang="ain-Latn">nea</i> / <i lang="ain-Latn">ne</i> marks a referent
			as established and salient in discourse — the functional counterpart of the
			definite article in post-introduction contexts. Its high corpus frequency
			relative to the deictic forms (§3 above) is consistent with this
			article-like role ‹corpus-suggested›.
		</li>
		<li>
			<strong>Possessive morphology.</strong> The personal possessor prefix
			combined with the <Xr ch="concept-form-affiliative-form">affiliative form</Xr>
			of an inalienable noun singles out the referent as uniquely identifiable
			relative to its possessor. The alienable/inalienable split and the
			semantics of obligatory possession are treated in
			<Xr ch="alienable-inalienable-split" />.
		</li>
		<li>
			<strong>Proper nouns and naming</strong> are inherently referentially unique
			and carry no additional definiteness morphology.
		</li>
		<li>
			<strong>Discourse position.</strong> A sentence-initial topic NP is
			typically presupposed as identifiable; first-mention NPs in non-topic
			position are indefinite by discourse default, without any morphological
			signal.
		</li>
	</ul>

	<p>
		Whether <i lang="ain-Latn">nea</i> should be analysed as a grammaticalized
		definite article is contested ‹contested›. Against the article analysis:
		<i lang="ain-Latn">nea</i> is absent from many clearly definite contexts
		where English or Japanese would require the article; it alternates with a bare
		noun in narrative without apparent semantic contrast; and its distribution is
		better described as <em>anaphoric-recognitional</em> — marking a referent as
		recoverable from discourse — than as an absolute definiteness category
		<Ref k="nakagawa2024" p="284" />. Refsing glosses the Shizunai cognate as
		'the said', which aligns with recognitional use rather than a grammaticalized
		article <Ref k="refsing1986" p="109" />. In Diessel's typology, recognitional
		demonstratives track referents that are identifiable through shared knowledge or
		discourse memory but are not fully grammaticalized articles
		<Ref k="diessel1999" paren />. The related claim — that the
		<Xr ch="concept-form-affiliative-form">affiliative form</Xr> of certain nouns
		carries definiteness-like semantics in post-possessive contexts — is evaluated
		in that chapter. For the clause-level question of how topics and foci interact
		with the demonstrative system, see <Xr ch="noun-phrase-structure" />.
	</p>

	<p>
