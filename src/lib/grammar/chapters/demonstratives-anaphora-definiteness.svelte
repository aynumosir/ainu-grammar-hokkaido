<S t="Deictic prenominals" id="deictic-prenominals">
	<p>
		The etymology of the deictic prenominals is transparent. Kindaichi and Chiri
		derive <i lang="ain-Latn">tan</i> from <i lang="ain-Latn">ta</i> + copula
		<i lang="ain-Latn">an</i>, and <i lang="ain-Latn">toan</i> from
		<i lang="ain-Latn">to</i> + <i lang="ain-Latn">an</i>; the medial
		<i lang="ain-Latn">taan</i> is the long (information-richer) variant of
		<i lang="ain-Latn">tan</i>, and the plurals
		<i lang="ain-Latn">tanokay</i> / <i lang="ain-Latn">tonokay</i> are formed by
		fusing the root with the plural existential <i lang="ain-Latn">okay</i>
		<Ref k="kindaichi1936" p="58" />; <Ref k="nakagawa2024" p="288" />. The
		functional difference between <i lang="ain-Latn">tan</i> and
		<i lang="ain-Latn">taan</i> parallels Japanese
		<span lang="ja">この vs. その</span>: <i lang="ain-Latn">tan</i> is used when
		the referent is immediately at hand, <i lang="ain-Latn">taan</i> when it lies
		within the speaker's perceptual sphere but is not immediately present, and
		<i lang="ain-Latn">toan</i> when it is away from both speaker and addressee
		<Ref k="nakagawa2024" p="283–284" />.
		Community discussion has proposed an iconic account of the vowel gradient:
		longer form encodes more spatial distance-information, front raised vowel signals
		proximity, back lowered vowel signals remoteness ‹speculative›
		<Ref k="aynucorporadiscord" /> (nukopoli, 2025-12-27). The functional
		distribution is uncontroversial <Ref k="nakagawa2024" p="283–284" />; the
		iconicity explanation is unverified.
	</p>

	<Ex
		m="taan cise pirka"
		g="DEM.PROX house be.good"
		tr="That house (in sight, nearby) is good."
		constructed
		dial="SAR"
		note="Illustrative. taan signals the referent is within the speaker's sphere but not immediately at hand, the closest HA equivalent to Japanese sono. Compare tan cise 'this house (right here)' and toan cise 'that house over there'."
	/>

	<p>
		The distal <i lang="ain-Latn">toan</i> is also the principal vehicle for nominal
		third-person reference: Hokkaido Ainu uses
		<i lang="ain-Latn">toan kur</i> 'that person' or
		<i lang="ain-Latn">nea kur</i> 'that person (aforementioned)' where languages
		with grammaticalized third-person pronouns use them, since the third-person
		pronouns of the paradigm treated in <Xr ch="independent-personal-pronouns" />
		are rare in actual discourse <Ref k="nakagawa2024" p="256" />.
	</p>

	<Ex
		m="toan kur hunna an"
		g="DEM.DIST person who exist"
		tr="Who is that person?"
		cite="utari1994"
		dial="SAR"
		note="From アコㇿイタㇰ ch. 2. The question word hunna 'who' is the h-series form of Saru and Chitose; eastern and northern dialects have the n-series instead: toan kur nen ta an (see the chapter on interrogative pro-forms). The toan kur frame illustrates the demonstrative as the carrier of third-person deictic reference."
	/>
</S>

<S t="Anaphoric and recognitional demonstratives" id="anaphoric">
	<p>
		The anaphoric prenominals — <i lang="ain-Latn">ne</i>,
		<i lang="ain-Latn">nea</i>, <i lang="ain-Latn">newaan</i> — are formally
		separate from the deictic set and cannot be used to point at a scene-present
		referent. <i lang="ain-Latn">nea</i> picks up a referent already established
		in the discourse; in a narrative, <i lang="ain-Latn">nea menoko</i> means
		'the woman (who was just spoken of)' and does not admit a deictic
		(scene-pointing) reading <Ref k="nakagawa2024" p="284" />. The weaker
		<i lang="ain-Latn">ne</i> marks a referent as immediately prior in the
		discourse; <i lang="ain-Latn">newaan</i> is more emphatic.
	</p>

	<Ex
		m="arpa akusu nea menoko"
		g="go CONJ DEM.ANAPH woman"
		tr="…when (he) went, that woman (the one already introduced)…"
		cite="ilcaa1976"
		dial="SAR"
		place="川上まつ子 uwepeker 民話 11, AA研アイヌ語資料 [aa-irc/013#94]"
		note="Fragment from a Saru narrative. The form nea picks up a female character introduced earlier; no scene-pointing interpretation is possible in the narrative context. This discourse-tracking use accounts for the form's high corpus frequency."
	/>

	<p>
		Corpus counts confirm the predominance of the anaphoric function in narrative
		text: the surface string <i lang="ain-Latn">nea</i> occurs approximately 1,984
		times in the ainu-corpora aligned set — substantially more than
		<i lang="ain-Latn">toan</i> (c. 735 tokens) and
		<i lang="ain-Latn">tanpe</i> (c. 391 tokens) combined — consistent with its
		role as the primary anaphora-tracking device of narrative ‹corpus-confirmed›
		<Ref k="nakagawa2024" p="287" />.
		(Surface counts conflate any homophonous forms and are upper bounds.)
	</p>

	<p>
		<strong>Third-person reference.</strong> Where Japanese uses
		<span lang="ja">彼 / 彼女</span>, Hokkaido Ainu characteristically uses a
		demonstrative NP — <i lang="ain-Latn">nea menoko</i> 'that woman (referred to)',
		<i lang="ain-Latn">toan kur</i> 'that person (over there)'. The third-person
		and fourth-person pronouns of the independent paradigm occur, but they are
		rare; the demonstrative NP is the default anaphora-tracking strategy in oral
		literature and in narrative prose
		<Ref k="nakagawa2024" p="256" />. The interaction of this strategy with
		pronominal-argument interpretations of the person affixes is considered in
		<Xr ch="nonconfigurationality-pronominal-argument" />.
	</p>

	<p>
		<strong>Dialect variation.</strong>
		<i lang="ain-Latn">nea</i> is pan-Hokkaido; most dialects outside Saru and
		Chitose use <i lang="ain-Latn">nean</i> (pronoun
		<i lang="ain-Latn">neanpe</i>) — attested in Horobetsu, Shizunai, and
		Ishikari, with both forms occurring in some Horobetsu and Ishikari speakers
		<Ref k="nakagawa2024" p="285–286" />. Shizunai and the dialects to the east
		additionally have <i lang="ain-Latn">ikia</i> (pl.
		<i lang="ain-Latn">ikirok</i>; pronoun <i lang="ain-Latn">ikiap</i>),
		a second anaphoric prenominal functionally close to
		<i lang="ain-Latn">nea</i> but more restricted in scope; it is rare in Saru
		<Ref k="nakagawa2024" p="285" />.
		The Ijäs pedagogical grammar covers the core
		<i lang="ain-Latn">nea</i> forms alongside the deictic series
		<Ref k="ijas2023" />.
	</p>
</S>

<S t="Demonstrative pronouns" id="dem-pronouns">
	<p>
		Each prenominal may fuse with the formal noun
		<A w="p" gl="thing (nominalizer)" /> (<i lang="ain-Latn">~pe</i>) to produce a
		demonstrative pronoun referring to a thing rather than modifying a noun head:
		<i lang="ain-Latn">tan-pe</i> 'this (thing)',
		<i lang="ain-Latn">toan-pe</i> 'that (thing)',
		<i lang="ain-Latn">newaan-pe</i> / <i lang="ain-Latn">nea-p</i> 'the
		aforementioned thing', and the eastern
		<i lang="ain-Latn">ikia-p</i> (Shizunai-and-east)
		<Ref k="nakagawa2024" p="287" />. The suffix
		alternation <i lang="ain-Latn">-pe</i> / <i lang="ain-Latn">-p</i> follows
		the phonological environment of the nominalizer, treated in
		<Xr ch="nominalization-p-pe" />.
	</p>

	<Ex
		m="tan-pe anak su sekor a=ye p ne"
		g="DEM.PROX-NMLZ TOP pot QUOT 4.A=say NMLZ COP"
		tr="This is what is called a pot."
		ain="tanpe anak su sekor a=ye p ne"
		cite="nakagawa2024:287"
		dial="SAR"
		note="Nakagawa ex. 389. The demonstrative pronoun tanpe heads the topic NP; the predicate is the quotative frame sekor a=ye p 'what (one) calls', nominalised by p, closed by the copula ne."
	/>

	<p>
		The indefinite pronoun <i lang="ain-Latn">nep</i> 'something / what' — formed
		from the same root <i lang="ain-Latn">ne-</i> plus the nominalizer — is an
		interrogative/indefinite, not a demonstrative. For anaphoric reference to a
		thing, Hokkaido Ainu uses <i lang="ain-Latn">newaanpe</i> or
		<i lang="ain-Latn">neap</i>, not <i lang="ain-Latn">nep</i>
		<Ref k="nakagawa2024" p="287" />. This gap in the anaphoric pronoun paradigm
		is an incidental consequence of <i lang="ain-Latn">nep</i>'s grammaticalization
		as an indefinite/interrogative; the full indefinite and interrogative series are
		treated in <Xr ch="interrogative-pro-forms" />.
	</p>
</S>

<S t="Locative roots and case-marked forms" id="locative-roots">
	<p>
		The three roots <A w="te" gl="here" />, <A w="ta" gl="there" />, and
		<A w="to" gl="yonder" /> function as locative nouns taking case particles
		directly, not as prenominal modifiers. Only <i lang="ain-Latn">ta</i> and
		<i lang="ain-Latn">to</i> enter the prenominal by fusing with the copula
		(<i lang="ain-Latn">ta-an</i> → <i lang="ain-Latn">tan</i>,
		<i lang="ain-Latn">to-an</i> → <i lang="ain-Latn">toan</i>). The proximal
		root <i lang="ain-Latn">te</i> does not produce a prenominal and cannot
		precede a head noun — <i lang="ain-Latn">*te menoko</i> is ungrammatical;
		<i lang="ain-Latn">te</i> only takes case particles
		(<i lang="ain-Latn">te ta</i> 'here', <i lang="ain-Latn">te un</i> 'this way',
		<i lang="ain-Latn">te wano</i> 'from now') <Ref k="nakagawa2024" p="289" />.
	</p>

	<Ex
		m="te ta k=an"
		g="here LOC 1SG.S=exist"
		tr="I am here."
		constructed
		dial="SAR"
		note="Constructed. The proximal locative root te takes the case particle ta. te cannot head an NP (*te menoko 'this woman' is ungrammatical) and never yields a prenominal form."
	/>

	<p>
		Case-marked forms of the deictic prenominals arise by ellipsis of the formal
		noun <i lang="ain-Latn">hi</i> before the case particle:
		<i lang="ain-Latn">taan ta</i> 'here' &lt;
		<i lang="ain-Latn">taan hi ta</i> 'the-place-of-this at';
		<i lang="ain-Latn">toan un</i> 'over there (toward)' &lt;
		<i lang="ain-Latn">toan hi un</i> <Ref k="nakagawa2024" p="288" />.
		These locative uses intersect with the relational-noun system; see
		<Xr ch="relational-and-spatial-nouns" />.
	</p>
</S>

