		Two observations follow from the grid. The anaphoric root
		<i lang="ain-Latn">ne-</i> is formally identical to the interrogative root of
		<i lang="ain-Latn">nep</i> 'what', <i lang="ain-Latn">nen</i> 'who', and
		<i lang="ain-Latn">neyta</i> 'where', and to the copula
		<i lang="ain-Latn">ne</i>. Refsing flags this convergence as possibly more than
		coincidence <Ref k="refsing1986" p="108" /> ‹speculative›. The
		place-row entry for the anaphoric column is empty — a systematic gap: there is
		no form <i lang="ain-Latn">*neta</i> or <i lang="ain-Latn">*neyta</i>
		functioning as 'the aforementioned place'.
	</p>

