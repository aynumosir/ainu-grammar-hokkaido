<S t="The O set in the person paradigm" id="overview">
	<p>
		A monotransitive verb indexes its object with the O column of the personal-index paradigm. In
		the standard Saru–Chitose baseline, that column contains <i lang="ain-Latn">en=</i> for 1SG.O,
		<i lang="ain-Latn">un=</i> for 1PL.EXCL.O, <i lang="ain-Latn">e=</i> for 2SG.O,
		<i lang="ain-Latn">eci=</i> for 2PL.O, a zero exponent for third person, and
		<i lang="ain-Latn">i=</i> for the fourth/indefinite person <Ref k="nakagawa2024" p="169" />;
		<Ref k="bugaeva2012" p="471" />; <Ref k="sato2008" p="31–32" />. The three-set S/A/O paradigm
		and its cell-by-cell alignment are set out in <Xr ch="person-marking-architecture" />; this
		chapter examines each O form in its monotransitive function, its morphological and positional
		properties, and the allomorphic contrasts that arise in specific combinations.
	</p>

