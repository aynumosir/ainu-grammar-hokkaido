	<p>
		A finite verb carries at most one subject index and one object index; a third argument — such
		as the theme in a ditransitive clause — is never additionally person-indexed and surfaces as a
		bare noun phrase <Ref k="sato2023b" p="39, 47" />; <Ref k="sato2025a" p="369–371" />. The
		object index occupies the prefixal argument-marking layer immediately before the lexical stem,
		alongside the transitive subject index; derivational material — applicatives, the causative, the
		antipassive, the reflexive, and the root-attaching plural <i lang="ain-Latn">-pa</i> — sits
		closer to the stem. The full template is treated in <Xr ch="personal-affix-template-and-ordering" />.
	</p>

	<p>
		Ijäs's Lesson 5 introduces the O set through an "object markers" frame: the English object
		pronoun corresponds to a verbal prefix on the Ainu finite verb, so that "me" and "you" map onto
		<i lang="ain-Latn">en=</i> and <i lang="ain-Latn">e=</i> respectively <Ref k="ijas2023" />. Her
		pedagogical notation uses hyphens throughout; the convention in this grammar is that
		<i lang="ain-Latn">=</i> marks the personal-index boundary and <i lang="ain-Latn">-</i> is
		reserved for derivational and stem-number morphology.
	</p>
</S>

<S t="First-person singular object: en=" id="en">
	<p>
		The prefix <i lang="ain-Latn">en=</i> indexes a first-person singular object. Its S/A
		counterpart <i lang="ain-Latn">ku=</i> covers both intransitive subject and transitive subject
		functions; the opposition <i lang="ain-Latn">ku=</i> (S=A) versus <i lang="ain-Latn">en=</i>
		(O) is the one nominative–accusative cell in the Hokkaido Ainu paradigm <Ref
			k="bugaeva2012"
			p="472"
		/>; <Ref k="bugaeva2008" p="32" />; <Ref k="nakagawa2024" p="166–169" />; <Ref
			k="sato2008"
			p="31–32"
		/>. The full 1SG row and the morphological properties of <i lang="ain-Latn">ku=</i> are
		developed in <Xr ch="first-second-person-singular-affixes" />.
	</p>

	<Ex
		m="en=epunkine wa en=kore yan"
		g="1SG.O=protect CONJ 1SG.O=give IMP"
		tr="Please protect me."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, menokoyukar 'Kamuy yukar 4' [aa-irc/011#5]"
		note="An imperative clause with an implicit second-person subject; both instances of en= index the narrator as the object of 'protect' and 'give' respectively."
	/>

	<Ex
		m="nep kameaske ene ne yakkay en=kasuy kun pe an cik pirka hike"
		g="what monster thus COP even 1SG.O=help should NMLZ exist if good although"
		tr="It would be good if any monster were to help me."
		cite="nakagawa2024:169"
		dial="TOK"
		place="Nakagawa ex. 122, after Takahashi 2007: 63"
		note="en= indexes the speaker as the object of kasuy 'help'; the transitive subject is not specified by an overt prefix."
	/>

	<p>
		The combination 2A→1SG.O takes a dialect-specific shape. Most Hokkaido dialects form it with a
		second-person A prefix followed by <i lang="ain-Latn">en=</i>: the expected sequences are
		<i lang="ain-Latn">e=en=</i> (2SG.A→1SG.O) and <i lang="ain-Latn">eci=en=</i>
		(2PL.A→1SG.O). Saru and Chitose drop the second-person A prefix and use bare
		<i lang="ain-Latn">en=</i> for 2SG→1SG; Chitose extends the bare form to 2PL→1SG as well,
		so that the same surface prefix covers both 3→1SG and, in those dialects, 2→1SG — subject
		identification falls to discourse context or overt noun phrases <Ref
			k="nakagawa2024"
			p="170, 173"
		/>; <Ref k="sato2008" p="146–147" /> ‹consensus›.
	</p>

	<p>
		An oral-literary parallel uses <i lang="ain-Latn">i=</i> where the colloquial register uses
		<i lang="ain-Latn">en=</i>: in the elegant register the 2SG subject is again unexpressed, and
		<i lang="ain-Latn">i=</i> appears as the first-person object prefix in the 2SG→1SG
		configuration <Ref k="sato2008" p="261" />. The literary referential range of
		<i lang="ain-Latn">i=</i> is part of the fourth-person narrative system, developed in
		<Xr ch="indefinite-fourth-person-paradigm" />.
	</p>
</S>

<S t="First-person plural exclusive object: un=" id="un">
	<p>
		The prefix <i lang="ain-Latn">un=</i> indexes a first-person plural exclusive object. It
		belongs to the tripartite 1PL.EXCL row alongside <i lang="ain-Latn">ci=</i> (A) and
		<i lang="ain-Latn">=as</i> (S), all three formally distinct <Ref
			k="nakagawa2024"
			p="166–169"
		/>; <Ref k="bugaeva2012" p="472" />; <Ref k="bugaeva2008" p="32" />. The row is exclusive
		throughout: <i lang="ain-Latn">un=</i> marks a speaker-group object that excludes the
		addressee, while addressee-inclusive 'us' is indexed in the object position by the fourth-person
		prefix <i lang="ain-Latn">i=</i>. Tokachi confirms the same exclusive–inclusive split, with
		<i lang="ain-Latn">un=</i> systematically opposed to the fourth-person object <Ref
			k="takahashi2015"
			p="206–209"
		/> ‹consensus›.
	</p>

	<Ex
		m="sanbyakugojuen a=un=kore hike ka"
		g="350.yen 4.A=1PL.EXCL.O=give although even"
		tr="Although they gave us 350 yen, …"
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, isoytak 'Monologue 2' [aa-irc/004#109]"
		note="The prefix a= marks an indefinite fourth-person transitive subject; un= indexes the exclusive first-plural group as the recipient-object of kore 'give'."
	/>

	<p>
		The full exclusive paradigm and the clusivity opposition are developed in
		<Xr ch="first-person-plural-and-clusivity" />.
	</p>
</S>

<S t="Second-person objects: e= and eci=" id="second-person">
	<p>
		Second-person indexes are role-neutral: <i lang="ain-Latn">e=</i> marks 2SG as A, S, or O,
		and <i lang="ain-Latn">eci=</i> does the same for 2PL, with no dedicated object form for
		either <Ref k="tamura1972" p="27" />; <Ref k="sato2008" p="32" />; <Ref
			k="bugaeva2012"
			p="472"
		/>. The role of <i lang="ain-Latn">e=</i> in a transitive clause follows from discourse: a
		form such as <i lang="ain-Latn">e=kore</i> may be read as 2SG.A acting on a zero-indexed
		third-person O, or as a zero-indexed third-person A acting on a 2SG.O, with overt noun phrases
		or context resolving the ambiguity. Ijäs notes this as one of the practically challenging
		features of the O set for learners of Saru <Ref k="ijas2023" />.
	</p>

	<Ex
		m="kanpisos e=kore"
		g="book 2SG.O=give"
		tr="She gave you the book."
		constructed
		cite="ijas2023"
		dial="SAR"
		note="Constructed following Ijäs Lesson 5. With e= read as 2SG.O, the transitive subject is a zero-indexed third person; the same e=kore is equally grammatical with e= as 2SG.A ('you gave the book (to her)')."
	/>

	<p>
		The form <i lang="ain-Latn">eci=</i> additionally serves as the portmanteau exponent for a
		first-person A acting on a second-person O: the four combinations 1SG.A→2SG.O, 1SG.A→2PL.O,
		1PL.EXCL.A→2SG.O, and 1PL.EXCL.A→2PL.O all surface as bare <i lang="ain-Latn">eci=</i>,
		without a separate first-person subject prefix <Ref k="tamura1972" p="27–28" />; <Ref
			k="bugaeva2008"
			p="38"
		/>; <Ref k="bugaeva2012" p="474" />; <Ref k="nakagawa2024" p="170, 173" />. The portmanteau
		and its consequences for the combination paradigm are developed in
		<Xr ch="eci-portmanteau-and-second-plural" />.
	</p>
</S>

<S t="Fourth-person object: i=" id="i">
	<p>
		The prefix <i lang="ain-Latn">i=</i> is the O member of the fourth-person tripartite row,
		alongside <i lang="ain-Latn">a=</i> (or dialectally <i lang="ain-Latn">an=</i>) as A and
		<i lang="ain-Latn">=an</i> as S. Nakagawa lists five referential uses for the form-set —
		inclusive first-plural, second-person honorific, impersonal or agent-defocusing, oral-narrative
		protagonist, and logophoric reference in reported speech; Bugaeva covers the same range under
		"indefinite", reanalysing the quotation use as logophoric; Tamura distributes the functions
		across analytically separate persons <Ref k="nakagawa2024" p="169, 174–179" />; <Ref
			k="bugaeva2012"
			p="474–476"
		/>; <Ref k="tamura1972" p="18–20, 28, 34, 39" /> ‹contested›. The full referential paradigm of
		the form-set is treated in <Xr ch="indefinite-fourth-person-paradigm" />; the concern here is
		<i lang="ain-Latn">i=</i> as the object prefix common to all of those contexts.
	</p>

	<Ex
		m="sunku tono mat i=kaopiwki wa i=kore"
		g="Sunku lord wife 4.O=help CONJ 4.O=give"
		tr="Help me and take care of me, wife of the lord of Sunku."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker 'Folktale 8 (part 1)' [aa-irc/006#243]"
		note="Both i= prefixes index the oral-narrative protagonist as the object; kaopiwki 'help' and kore 'give' are linked by the conjunctive wa. This clause is an address within the narrative."
	/>

	<Ex
		m="a=rehe Ikuresuye sekor a=i=réko"
		g="4.POSS=name Ikuresuye QUOT 4.A=4.O=call"
		tr="I was named Ikuresuye."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker 'Folktale 7' [aa-irc/005#1]"
		note="The possessive a= on the noun rehe 'name' and the transitive-subject a= on the verb réko 'call/name' are independent instances of the fourth-person prefix; i= indexes the oral-narrative protagonist as the object of the naming action."
	/>

	<p>
		The prefix <i lang="ain-Latn">i=</i> is homophonous with the detransitivising prefix
		<i lang="ain-Latn">i-</i>, which derives intransitive predicates from transitives — as in
		<A w="i-ruska" gl="be angry at something unspecified" /> beside transitive
		<A w="ruska" gl="be angry at someone" />. The two items belong to different grammatical layers:
		the object marker <i lang="ain-Latn">i=</i> is a personal index in the paradigm, while the
		detransitivising <i lang="ain-Latn">i-</i> belongs to valency-changing morphology and patterns
		with the reflexive <A w="yay-" /> and the reciprocal <A w="u-" /> <Ref
			k="tamura1972"
			p="35"
		/>; <Ref k="nam2021" p="62–74" />. Their historical relationship is debated: the older
		analysis derives the antipassive from the fourth-person object marker; Bugaeva reverses the
		direction; Nam proposes a common source in the generic noun
		<i lang="ain-Latn">*hi/*ihi</i> 'thing, place, time' ‹contested›. The detransitivising prefix
		is treated in <Xr ch="antipassive-detransitive-i" />.
	</p>
</S>

<S t="Zero third-person object" id="third-zero">
	<p>
		Third-person objects carry no overt index. With a first- or second-person subject, the visible
		personal prefix is the A form alone — <i lang="ain-Latn">ku=</i>, <i lang="ain-Latn">ci=</i>,
		<i lang="ain-Latn">e=</i>, <i lang="ain-Latn">eci=</i>, or <i lang="ain-Latn">a=</i>; with
		third-person subject and object alike, the finite verb carries no overt person prefix <Ref
			k="nakagawa2024"
			p="169"
		/>; <Ref k="bugaeva2012" p="471" />.
	</p>

	<Ex
		m="moyuk ku=nukar"
		g="raccoon.dog 1SG.A=see"
		tr="I saw a raccoon dog."
		constructed
		cite="ijas2023"
		dial="SAR"
		note="Constructed following Ijäs Lesson 5. The third-person object has no overt index; only the 1SG.A prefix ku= appears on the verb."
	/>

	<p>
		Whether the absence of a third-person index reflects a zero affix or the simple absence of any
		third-person exponent is contested. Nakagawa treats third person as lacking an affix; Satō
		argues that a zero third-person affix must be posited to sustain the two-index cap — the
		ungrammaticality of sequences such as <i lang="ain-Latn">*Ø=en=e=kore</i> 'one makes me give
		you' follows if both subject and object slots are already occupied by zero, blocking a third
		index <Ref k="nakagawa2024" p="169" />; <Ref k="sato2023b" p="39" />; <Ref
			k="sato2025a"
			p="369"
		/> ‹contested›. The alignment implications of the zero versus absent-exponent analysis are
		pursued in <Xr ch="alignment-hierarchical-and-the-debate" />.
	</p>
</S>
