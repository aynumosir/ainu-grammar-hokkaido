<S t="siran — the appearance-based continuative" id="siran">
	<p>
		The noun <A w="sir" gl="appearance, visible state, land" /> is the source both of the
		evidential <Xr ch="formal-defective-nouns">formal noun</Xr>
		<i lang="ain-Latn">sir-i</i> and of the existential-aspectual predicate
		<i lang="ain-Latn">sir-an</i> 'appearance-exist'. As the second predicate in a
		pair-verb chain, <i lang="ain-Latn">sir-an</i> replaces the plain existential
		<i lang="ain-Latn">an</i> and adds a perceptual dimension: the state or action named
		by the first verb is not merely ongoing but visibly so, its appearance persisting for
		the observer <Ref k="nakagawa2024" p="434–435" />;
		<Ref k="dalcorso2018" p="304, 326–327" />.
	</p>

	<p>
		Nakagawa frames this function as the third of four temporal-reference strategies
		available in the tenseless system. Where a time adverb places an event on the timeline
		and a prospective form such as <i lang="ain-Latn">kusu ne</i> points to the future,
		clause-final <i lang="ain-Latn">siri</i> anchors an action unfolding visibly in front
		of the speaker — 「現在まさに目の前で行われている行為」 <Ref k="nakagawa2024" p="434–435" />.
		The form is not a tense marker; the present-ongoing temporal reading follows from the
		evidential value, because if the speaker is currently perceiving the event, the event
		is current.
	</p>

	<p>
		Structurally, <i lang="ain-Latn">sir-an</i> occupies the position of the existential
		in the pair-verb frame: a lexical verb is linked to it by a simultaneous conjunction,
		most commonly <i lang="ain-Latn">kor</i>, the same conjunction that builds
		<i lang="ain-Latn">kor an</i>. The resulting contrast is between
		<i lang="ain-Latn">V kor an</i> (action in progress) and
		<i lang="ain-Latn">V kor sir-an</i> (action in progress, visibly apparent). Person
		agreement follows the pair-verb pattern: both the lexical verb and the existential
		component of <i lang="ain-Latn">sir-an</i> carry the subject index
		<Ref k="nakagawa2024" p="442" />.
	</p>

	<Ex
		m="nea kur arpa kor sir-an"
		g="that person go CONJ appearance-exist"
		tr="That person appears to be going."
		constructed
		note="Illustrates the appearance-based continuative: sir-an replaces plain an as the existential predicate, signalling that the ongoing action is perceptible to the observer. Compare nea kur arpa kor an 'that person is going', which carries no evidential colouring."
	/>

	<p>
		In attested usage, <i lang="ain-Latn">sir-an</i> frequently appears embedded before
		the inferential evidential <i lang="ain-Latn">ruwe ne</i>, forming a double-evidential
		stack: the visual state is packaged as an ongoing scene and then reframed as an
		established fact. Dal Corso reads <i lang="ain-Latn">sir-an</i> in this position as
		the indirect visual evidential in his eight-form system, the form that applies when the
		scene is accessible but the speaker treats the evidence as somewhat less immediately
		processed than under the direct-perception
		<i lang="ain-Latn">siri ne</i> <Ref k="dalcorso2018" p="304, 326–327" />.
	</p>

	<Ex
		m="supuya at kor sir-an ru-we ne anan"
		g="smoke rise CONJ appearance-exist track-POSS COP MIR"
		tr="Smoke was rising, it turned out!"
		cite="dalcorso2018:327"
		dial="HK"
		place="Nakagawa text, Dal Corso siglum NGF:176"
		note="Dal Corso ex. 268. The visual-state predicate sir-an is embedded before the inferential ruwe ne, which repackages the proposition as a fact the speaker now recognizes with surprise. The stacking confirms that sir-an and ruwe ne are separate predicates with a fixed scope order: aspect/visual-state inside, inferential outside."
	/>
</S>

<S t="The aspect–evidential boundary" id="aspect-evidential">
	<p>
		The overlap between the continuative reading of <i lang="ain-Latn">siran</i> and the
		evidential functions of <i lang="ain-Latn">siri ne</i> and
		<i lang="ain-Latn">siri an</i> is a recognized analytical difficulty. Nakagawa treats
		the present-ongoing reading under temporal-reference strategy 3 without assigning it to
		the aspect system proper <Ref k="nakagawa2024" p="434–435" />. Yoshikawa, whose
		existential aspectual forms group includes <i lang="ain-Latn">kor an</i>,
		<i lang="ain-Latn">wa an</i>, <i lang="ain-Latn">kane an</i>, and
		<i lang="ain-Latn">hine an</i>, does not list <i lang="ain-Latn">siran</i> among them;
		the progressive reading is a pragmatic inference from the evidential value rather than
		an independent category <Ref k="yoshikawa2022" />. Dal Corso reads
		<i lang="ain-Latn">siri an</i> as one of eight evidential forms — the indirect visual
		counterpart to the direct-perception <i lang="ain-Latn">siri ne</i> — organized by the
		parameters of source reliability and event accessibility
		<Ref k="dalcorso2018" p="304, 313–315" />.
	</p>

	<p>
		These framings are not contradictory; they describe the same construction from different
		angles. The present chapter treats <i lang="ain-Latn">siran</i> /
		<i lang="ain-Latn">siri ne</i> as a form that licenses a continuative reading through
		its evidential value, while locating it primarily in the evidential paradigm. The
		direct-perception evidential <i lang="ain-Latn">siri ne</i> and the indirect visual
		<i lang="ain-Latn">siri an</i> receive their full treatment in
		<Xr ch="siri-ne-situational" />. The stacking pattern
		<i lang="ain-Latn">kor an siri ne</i> — attested in Nakagawa's corpus of Saru Ainu —
		supplies the most transparent diagnostics: the progressive marks the action as in
		progress, the situational evidential marks the speaker as currently witnessing it, and
		the scope order (aspect inside, evidential outside) is fixed
		<Ref k="nakagawa2024" p="393" />. When <i lang="ain-Latn">sir-an</i> fills the
		existential slot itself, the visual dimension is encoded in the existential predicate
		rather than in a separate closing evidential, and the form sits at the intersection of
		the two systems ‹contested›.
	</p>
</S>

<S t="kane an — the merged durative" id="kane-an">
	<p>
		<i lang="ain-Latn">kane an</i> is a pair verb of the same structural type as
		<i lang="ain-Latn">kor an</i> and <i lang="ain-Latn">wa an</i>: a lexical verb is
		linked to the existential <i lang="ain-Latn">an</i> by the simultaneous conjunction
		<i lang="ain-Latn">kane</i>. Its aspectual behaviour differs in that it is unconstrained
		by verb class: it accepts action verbs (progressive reading) and change-of-state verbs
		(resultative reading) equally, merging the two values into a single durative continuative
		<Ref k="nakagawa2024" p="446–447" />; <Ref k="yoshikawa2022" />;
		<Ref k="sato2006" p="84–85" />. Chiri's classic label for it is 持続態 <em>durative</em>,
		set against 進行態 <em>progressive</em> (<i lang="ain-Latn">kor an</i>) and
		完了態 <em>perfective</em> (<i lang="ain-Latn">wa isam</i> /
		<i lang="ain-Latn">wa okere</i>) in his three-way aspect classification
		<Ref k="chiri1942" p="503–504" />; <Ref k="takahashi2022" />.
	</p>

	<p>
		The dialectal picture divides into two patterns. In Saru and Chitose,
		<i lang="ain-Latn">kane an</i> is marked: alongside its durative value it carries a
		nuance of remarkable or unexpected situation. This nuance connects it to the standalone
		auxiliary <i lang="ain-Latn">kane</i> (Nakagawa 2024: 309–310 table 74), which Tamura
		characterizes as marking an unanticipated action or state and Satō as encoding degree
		— a semantic base that grammaticalizes toward simultaneity in some contexts
		<Ref k="nakagawa2024" p="309–310, 446–447" />; <Ref k="takahashi2022" />. In Shizunai
		and Samani, <i lang="ain-Latn">kane an</i> carries no such nuance and functions as the
		plain imperfective, replacing both <i lang="ain-Latn">kor an</i> and
		<i lang="ain-Latn">wa an</i> without selectional constraint on verb class
		<Ref k="nakagawa2024" p="447" />; <Ref k="sato2006" p="84–85" /> ‹contested›.
	</p>

	<p>
		The aspect-neutral character of <i lang="ain-Latn">kane an</i> in Saru is visible
		within a single clause chain: the form can take a resultative reading on one conjunct
		and a progressive reading on the next.
	</p>

	<Ex
		m="asir sapa tuye kane an hine mina kane an wa"
		g="be.new head cut CONJ exist CONJ laugh CONJ exist CONJ"
		tr="having cut the new head and laughing"
		cite="nakagawa2024:446"
		dial="SAR"
		place="Nakagawa ex. 1004"
		note="The first kane an follows the change verb tuye 'cut' with a resultative reading — the new head has been cut and the state persists; the second follows the action verb mina 'laugh' with a progressive reading — laughter is ongoing. The same form covers both, unlike kor an (progressive only) and wa an (resultative only)."
	/>

	<p>
		Person agreement follows the pair-verb pattern: both the lexical verb and
		<i lang="ain-Latn">an</i> carry the subject index, and the plural existential
		<i lang="ain-Latn">oka</i> replaces <i lang="ain-Latn">an</i> with a plural subject
		(<i lang="ain-Latn">kane oka</i>), parallel to <i lang="ain-Latn">kor oka</i> and
		<i lang="ain-Latn">wa oka</i> <Ref k="nakagawa2024" p="441, 446" />.
	</p>
</S>

<S t="hine an, kan an, and tek an — further dialect variants" id="dialect-variants">
	<p>
		<i lang="ain-Latn">hine an</i> uses the sequential-temporal conjunction
		<i lang="ain-Latn">hine</i> in the existential-aspectual construction and functions as
		a resultative like <i lang="ain-Latn">wa an</i>: the conjunction encodes that the
		situation has come to obtain as a result of the lexical verb's action. Yoshikawa lists
		it among the Saru–Chitose existential aspectual forms; Chiri's gloss
		'…<i lang="ain-Latn">hine an</i> = be …-ing' applies the durative label to it
		<Ref k="yoshikawa2022" />; <Ref k="chiri1942" p="503–504" />.
	</p>

	<p>
		<i lang="ain-Latn">kan an</i> is the Tokachi and Ishikari counterpart of Saru–Chitose
		<i lang="ain-Latn">kane an</i>: it merges progressive and resultative values into a
		single durative continuative. The form's aspectual centre of gravity differs between
		the two dialects: Tokachi usage leans progressive, Ishikari usage leans resultative
		<Ref k="nakagawa2024" p="448" />; <Ref k="takahashi2022" />.
	</p>

	<Ex
		m="a=tura wa cepkoyki=an kan an pe a=ki kan oka=an ainne"
		g="4.A=accompany CONJ catch.fish=4.S CONJ exist NMLZ 4.A=do CONJ exist.PL=4.S CONJ"
		tr="while we were fishing together"
		cite="nakagawa2024:448"
		dial="TOK"
		place="Nakagawa ex. 1016, Tokachi"
		note="kan an in Tokachi progressive use: the ongoing collective action of fishing persists as a state. The nominalizer pe packages the first kan an clause; a=ki kan oka=an continues the chain with a plural subject marked by oka=an."
	/>

	<Ex
		m="poro cise as kan an na"
		g="be.big house stand CONJ exist FIN"
		tr="A large house was standing there."
		cite="nakagawa2024:448"
		dial="ISH"
		place="Nakagawa ex. 1019, Ishikari; originally Otsuka 1990: 45"
		note="kan an in Ishikari resultative use: the posture verb as 'stand' enters kan an where Saru would use wa an, presenting the house as being in the resultant state of having stood up."
	/>

	<p>
		<i lang="ain-Latn">tek an</i> (Tokachi) covers the resultative of change verbs,
		corresponding to Saru <i lang="ain-Latn">wa an</i> in selectional behaviour. Nakagawa
		traces a connection to the Saru auxiliary <i lang="ain-Latn">tek</i> 'do briefly /
		suddenly', which shows how the same root grammaticalizes along different paths in
		different dialect communities — as a momentary-action auxiliary in Saru, as a
		resultative pair-verb connector in Tokachi
		<Ref k="nakagawa2024" p="448–449" /> ‹speculative›.
	</p>
</S>

<S t="The Tokachi first-person restriction on kor an" id="tokachi-restriction">
	<p>
		Tokachi presents a distributional constraint on <i lang="ain-Latn">kor an</i> that
		elevates <i lang="ain-Latn">kan an</i> into a distinct functional role. In Tokachi,
		<i lang="ain-Latn">kor an</i> cannot take a first-person subject: where another
		dialect would say <i lang="ain-Latn">ku=nukar kor ku=an</i> 'I am watching', Tokachi
		requires <i lang="ain-Latn">ku=nukar kan ku=an</i> with
		<i lang="ain-Latn">kan an</i>. Takahashi's hypothesis is that
		<i lang="ain-Latn">kor an</i> encodes the speaker's observation of an event, a
		meaning that clashes with a first-person subject who is themselves the agent; the
		construction is therefore confined to second- and third-person subjects in this dialect.
		No equivalent restriction has been documented for Saru, Chitose, or any other Hokkaido
		variety <Ref k="takahashi2022" /> ‹contested›.
	</p>

	<p>
		Takahashi invokes a cross-linguistic parallel with Shuri Ryūkyūan, where the
		progressive equivalent likewise bars first-person agents; the Tokachi restriction is
		thus positioned as an instance of a typologically attested pattern rather than an
		isolated anomaly <Ref k="takahashi2022" />. The further question — whether the
		restriction reflects a residual connection between <i lang="ain-Latn">kor an</i> and
		the visual-evidential domain, such that first-person use is blocked because first
		persons cannot be their own external observers — remains open in the sources ‹speculative›.
	</p>
</S>

<S t="Grammaticalization and the degree of aspect" id="grammaticalization">
	<p>
		All the forms in this chapter share a grammaticalization path: a conjunction
		(<i lang="ain-Latn">kane</i>, <i lang="ain-Latn">hine</i>,
		<i lang="ain-Latn">kan</i>, <i lang="ain-Latn">tek</i>) or a sensory noun
		(<i lang="ain-Latn">sir</i>) links a lexical verb to the existential
		<i lang="ain-Latn">an</i>, and the compound develops an ongoing-state reading from the
		literal meaning 'the state of V-ing exists' <Ref k="bybee1994" paren />. This is the
		same path as <i lang="ain-Latn">kor an</i> and <i lang="ain-Latn">wa an</i> in
		<Xr ch="existential-aspect-an-oka" />; the cross-dialect variation concerns which
		conjunction is recruited and whether the result merges or maintains the progressive /
		resultative distinction.
	</p>

	<p>
		Yoshikawa's assessment is that none of these forms has reached obligatory grammatical
		aspect: the existential <i lang="ain-Latn">an</i> retains semantic content, person
		marking is overt on both predicates, and the construction is optional. They are 'forms
		that may express aspectual meanings but are not grammatical aspectual forms'
		<Ref k="yoshikawa2022" />. The cross-dialectal evidence supports this. The
		Shizunai–Samani <i lang="ain-Latn">kane an</i>, which has lost the
		remarkable-situation nuance retained in Saru and Chitose, appears to be at a more
		advanced stage: the form is more bleached, its selectional freedom more complete.
		Different dialect communities have therefore travelled different distances along a single
		grammaticalization trajectory ‹corpus-suggested›. The phasal, iterative, and habitual
		extensions of the aspect system are treated in
		<Xr ch="phasal-iterative-habitual-aspect" />.
	</p>
</S>
