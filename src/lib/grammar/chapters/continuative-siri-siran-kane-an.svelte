	<p>
		Four constructions complement the progressive <i lang="ain-Latn">kor an</i> and the
		resultative <i lang="ain-Latn">wa an</i> described in
		<Xr ch="existential-aspect-an-oka" />: the appearance-based continuative built on
		<i lang="ain-Latn">siran</i> (<i lang="ain-Latn">sir-an</i>, 'appearance-exist'); the
		merged durative <i lang="ain-Latn">kane an</i>; the resultative variant
		<i lang="ain-Latn">hine an</i>; and the Tokachi–Ishikari pair
		<i lang="ain-Latn">kan an</i> / <i lang="ain-Latn">tek an</i>. Each recruits the
		existential verb <A w="an" gl="exist (singular)" /> as its second predicate.
		Yoshikawa groups all of them with <i lang="ain-Latn">kor an</i> and
		<i lang="ain-Latn">wa an</i> under the cover term 「存在型アスペクト」, existential
		aspectual forms — constructions that may express aspectual meanings without constituting
		a grammatical aspect category in the strict sense <Ref k="yoshikawa2022" />;
		<Ref k="nakagawa2024" p="446–449" />. None of them is distributed uniformly across
		dialects, and the forms centred on the sensory noun
		<A w="sir" gl="appearance, visible state, land" /> straddle the boundary between the
		aspect system and the nominalization-plus-copula evidential paradigm treated in
		<Xr ch="evidential-schema-overview" />.
	</p>

