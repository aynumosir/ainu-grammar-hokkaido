	<S t="Altaic and related proposals" id="altaic">
		<p>
			Proposals linking Ainu to Altaic languages (Turkic, Mongolic, Tungusic) and to the
			"Ural-Altaic" macro-family circulated in comparative philology from the 19th century onward.
			<Ref k="alonso2022" p="§1" /> explicitly sets aside all such proposals — "ignoring all
			previous attempts at seeking relatives" — to work on internal reconstruction alone. The Altaic
			hypothesis itself lacks consensus status, and no systematic cognate set with regular Ainu
			correspondences has been advanced in the peer-reviewed literature ‹speculative›.
		</p>
		<p>
			Community members have proposed individual Mongolic or Korean connections for specific Ainu
			words (antitwilight, aynu-corpora Discord 2024–2025 <Ref k="aynucorporadiscord" />), but
			isolated look-alike pairs — the kind of comparison that motivated the earliest hypotheses —
			are precisely what the comparative method excludes as evidence. Without a systematic
			correspondence set covering basic vocabulary, such proposals cannot be evaluated as genetic
			claims ‹speculative›.
		</p>
	</S>

	<S t="The Austroasiatic problem" id="austroasiatic">
		<p>
			The most consequential internal methodological problem in Proto-Ainu reconstruction arose from
			<Ref k="vovin1993" />'s choice to use Austroasiatic as the typological template for his
			consonant inventory, positing a bloated 12-consonant stop system to explain the distribution
			of the affiliative suffix. All three published reviewers of <Ref k="vovin1993" /> independently
			concluded that this background assumption vitiates the consonant reconstruction: Sidwell
			(1996), Abondolo (1997), and Witczak (1999), as summarised by
			<Ref k="alonso2022" p="§1" />. <Ref k="alonso2022" />'s revised reconstruction abandons the
			Austroasiatic template and proposes a leaner inventory that better matches the Ainu
			phonological profile (see <Xr ch="proto-ainu-segmental-reconstruction" />) ‹speculative›.
		</p>
	</S>

	<S t="Nivkh and the northern contact zone" id="nivkh">
		<p>
			Ainu and Nivkh (Ghilyak) share an extensive zone of contact, centred on Sakhalin, and
			lexical material has moved in both directions. <Ref k="shiraishitangiku2022" p="§3" />
			document items including
			<i lang="ain-Latn">kaya</i> (from Nivkh, 'fish-skin garment, sail'),
			<i lang="ain-Latn">san</i> (from Nivkh <i lang="ain-Latn">cʰaŋ</i>, 'offering shelf'), and
			the shared term <i lang="ain-Latn">tunakay</i> 'reindeer' (→ Japanese
			<i lang="ja">トナカイ</i> <i lang="ja-Latn">tonakai</i>), reconstructed from
			Pre-Proto-Ghilyak <i lang="ain-Latn">*tola-ŋa.y</i>. These are contact items, not
			inherited cognates; Ainu and Nivkh are both independent isolates, and their structural
			differences exclude the hypothesis of common descent ‹consensus›. The
			contact layer is the subject of <Xr ch="northern-contact-nivkh-tungusic-manchu" />.
		</p>
		<p>
			The label "Paleo-Asiatic" (or "Paleosiberian") groups Ainu, Nivkh, Yukaghir, Chukotkan
			languages, and Itelmen on geographic grounds. It is a useful shorthand for geographically
			unaffiliated languages of Siberia and the North Pacific, but carries no implication of common
			ancestry; each member is an independent isolate or small family
			<Ref k="janhunen2022" p="§3c" /> ‹consensus›.
		</p>
	</S>
</S>

<S t="The Japonic question" id="japonic">
	<p>
		The relationship between Ainu and Japonic (the family including Japanese and Ryukyuan) is the
		most actively debated contact question, even though the genetic claim — that the two
		descend from a common ancestor — is rejected by the specialist consensus
		<Ref k="janhunen2022" p="§3c" /> ‹consensus›. Three distinct positions exist in the
		literature, and they need to be held apart.
	</p>
	<p>
		<strong>Japonic as a recent intrusion.</strong> <Ref k="janhunen2022" p="§3a" /> places the
		Japonic languages' arrival in the Japanese archipelago in the Yayoi/Kofun period (roughly
		900 BCE–500 CE), as a migration from the Korean Peninsula that replaced or marginalized
		the Jōmon languages. On this account, Ainu is not related to Japonic but to the substrate
		population that Japonic replaced or absorbed; structural features shared between Ainu and
		Japanese reflect Jōmon-language influence on the incoming Japonic varieties — what
		<Ref k="janhunen2022" p="§3c" /> calls "Jōmonization of Japanese" — rather than common
		descent. This position, sometimes termed the <em>substrate hypothesis</em>, explains the
		typological resemblances without positing genetic relatedness ‹consensus›.
	</p>
	<p>
		<strong>Early Japanese loanwords in Ainu.</strong> A secure stratum of Old Japanese loans
		in Hokkaido Ainu is attested by phonological fossils.
		<i lang="ain-Latn">sippo</i> 'salt' and
		<i lang="ain-Latn">pasuy</i> 'chopstick' preserve Old Japanese features — word-initial
		<i>*p</i> and the diphthongoid <i>*uy</i> — that had already been lost by the time
		Old Japanese sources were compiled, placing the borrowing "around the middle of the first
		millennium CE" <Ref k="janhunen2022" p="§3c" />. The culturally significant term
		<i lang="ain-Latn">kamuy</i> 'deity' also belongs to this early contact stratum.
		Later loans, such as Saru
		<i lang="ain-Latn">cikenki</i> (Standard Japanese <i lang="ja">つけぎ</i>
		<i lang="ja-Latn">tsukegi</i> 'matches') and
		<i lang="ain-Latn">antuki</i> (Standard Japanese <i lang="ja">小豆</i>
		<i lang="ja-Latn">azuki</i>, with prenasalization from the Tōhoku substratum), document
		the continuing contact through the medieval period <Ref k="alonso2022" p="§1" />. These
		loanwords are treated fully in <Xr ch="japanese-loanwords-in-ainu" /> ‹consensus›.
	</p>
	<p>
		<strong>Ainu loans in Old Japanese — Vovin's reverse-direction thesis.</strong>
		<Ref k="vovin2022" p="§§1–6" /> argues the traffic also ran in the other direction: that
		Ainu was once spoken throughout the Japanese archipelago (from the Kantō and Tōhoku regions
		to Kyūshū) and left identifiable loanwords in the earliest Japonic records, notably in
		eastern and western Old Japanese poetry of the <i lang="ja">万葉集</i>
		<i lang="ja-Latn">Man'yōshū</i>. Proposed loans include
		<i lang="ja-Latn">iruka</i> 'dolphin' (&lt; Ainu <i lang="ain-Latn">rika</i> 'whale-fat'),
		<i lang="ja-Latn">kuta</i> 'house' (&lt; <i lang="ain-Latn">kotan</i> 'village, settlement',
		with a household↔village semantic shift parallel to Latin <i lang="la">vicus</i>),
		<i lang="ja-Latn">porö</i> 'large' (&lt; <i lang="ain-Latn">poro</i>), and a set of place
		names in the Kantō and Tōhoku regions. The loan phonology proposed by <Ref k="vovin2022" />
		includes: Ainu <i>*-kk-</i> → OJ <i>*-k-</i>; Ainu medial <i>*-k-</i> (half-voiced)
		→ OJ <i>*-ŋg-</i>; and Ainu final consonants lost before OJ vowels. This is a live claim
		that has attracted attention but has not achieved consensus ‹contested›; it is evaluated
		in full in <Xr ch="ainu-loanwords-and-toponymy-in-japanese" />.
	</p>
</S>

<S t="Ethnogenesis — the Jōmon inheritance and internal classification" id="ethnogenesis">
	<p>
		Ainu's roots lie in the Jōmon period (12,000+ BP), and Ainu is the sole surviving
		representative of the Jōmon language complex, its continuity into the present "an accident"
		of demographic history <Ref k="janhunen2022" p="§3a" />. The Jōmon population spoke a range
		of languages, of which only the Ainu lineage is traceable; the others were absorbed or
		replaced.
	</p>
	<p>
		The geographic picture is counterintuitive: Janhunen argues that Ainu's homeland is
		<em>Honshu, not Hokkaido</em>. The ancestors of the modern Ainu people moved northward,
		associated with the Satsumon material culture (c.700–1200 CE), colonizing Hokkaido (northern
		areas c.1000 CE), southern Sakhalin (c.1300 CE), and the Kuril Islands (c.1500–1600 CE)
		<Ref k="janhunen2022" p="§3a–3b" />. The <em>Emishi</em> (<span lang="ja">蝦夷</span>) of
		northern Honshu — a recurrent presence in early Japanese historical sources — are viewed as a
		"generic proto-historical complex" ancestral in part to the Ainu
		<Ref k="janhunen2022" p="§3a" />. The Sakhalin ethnonym <i lang="ain-Latn">enciw</i>
		(cognate with <i lang="ja">emi₁si</i>, Ebisu, Ezo, <i lang="ain-Latn">*emisV~*emiciV</i>;
		Kindaichi 1960, via <Ref k="janhunen2022" p="§4" />) preserves the Honshu connection at the
		vocabulary level ‹corpus-suggested›.
	</p>
	<p>
		A community member has proposed that the Sarunkur group of the Saru valley represents an
		Aomori-origin migrant population with connections to the Jōmon cylindrical-pottery culture
		(aomidori_cs_, aynu-corpora Discord 2023 <Ref k="aynucorporadiscord" />). The proposal is
		consonant with Janhunen's Honshu-origin thesis, but the specific pottery connection is not
		supported by the archaeological record as presented in the accessible sources ‹speculative›.
	</p>
	<p>
		In the north, the Ainu population on Sakhalin absorbed the <em>Okhotsk culture</em>
		(c.600–1600 CE), whose language may have been an early form of Ghilyak/Amuric
		<Ref k="janhunen2022" p="§3b–3c" />. This contact episode introduced the items documented
		by <Ref k="shiraishitangiku2022" /> and helped shape the morphological distinctiveness of
		the Sakhalin variety. The linguistic legacy of the Okhotsk-culture contact remains
		incompletely disentangled from the Ainu substrate ‹speculative›.
	</p>
	<p>
		The linguistic lineage is, in Janhunen's terms, "the one ethnic marker traceable
		unambiguously back in time" for the Ainu people <Ref k="janhunen2022" p="§4" />. The
		ethnonyms <i lang="ain-Latn">aynu</i> 'human being' (the endonym, of shallow diachronic
		formation) and <i lang="ain-Latn">utari</i> 'people' are internally analysable; the
		exonym <i lang="ain-Latn">kur</i> 'man', whose plural form (via Northern Tungusic <i>-l</i>)
		is the origin of the Russian <i lang="ru-Latn">Kuríly</i> 'Kurils', and the term for the
		Japanese, <i lang="ain-Latn">sisam</i> (reconstructed to Pre-Proto-Ainu
		<i lang="ain-Latn">*sii+sam/o</i>), both point to a period of established ethnic
		differentiation predating the Hokkaido colonization.
	</p>
</S>

<S t="The recoverable contact layer and internal reconstruction" id="contact-reconstruction">
	<p>
		What the available methods do support, for Hokkaido Ainu, is a two-stratum contact
		vocabulary — early Old Japanese loans dated to the first millennium CE (§ above) and a
		thinner Nivkh-mediated layer from the northern contact zone (§<Xr
			ch="northern-contact-nivkh-tungusic-manchu"
		/>) — and a body of internal reconstructions reaching back at most ~1,000 years. Within
		that depth, internal reconstruction has produced robust results. The most celebrated is
		the etymology of <A w="cep" gl="fish" /> in Saru.
	</p>

