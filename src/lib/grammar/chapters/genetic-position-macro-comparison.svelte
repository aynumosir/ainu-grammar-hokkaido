	<p>
		Two Eurasian Wanderwörter appear in the Hokkaido lexicon and locate Ainu in a network of
		long-distance cultural contact.
		<A w="kotan" gl="village, settlement" /> is an Eurasian Wanderwort of continental origin
		<Ref k="janhunen2022" p="§5" />; it is among the most frequent Hokkaido nouns
		(rank 56 in the Hokkaido oral-literature corpus). The term
		<A w="inaw" gl="ritual wood-shavings offering" /> is reconstructed to a Tungusic source
		(Ikegami 1980, via <Ref k="janhunen2022" p="§5" />), pointing to archaic northern contact
		predating the Sakhalin-era Ainu–Nivkh–Uilta interaction. These items signal cultural
		exchange, not genealogical relationship ‹consensus›.
	</p>
	<p>
		The grammaticalization pathways recoverable through internal reconstruction — the possessive
		suffix <i lang="ain-Latn">-hV</i> from the noun <i lang="ain-Latn">hi</i> 'thing', the
		evidential nominalizers <i lang="ain-Latn">ruwe</i>, <i lang="ain-Latn">siri</i>,
		<i lang="ain-Latn">hawe</i>, and <i lang="ain-Latn">humi</i> from possessed sensory
		nouns, and the applicative prefixes <i lang="ain-Latn">e-</i>,
		<i lang="ain-Latn">ko-</i>, and <i lang="ain-Latn">o-</i> from locative/relational
		nouns — are among the best-documented results of Ainu historical linguistics and are treated
		in <Xr ch="internal-reconstruction-and-grammaticalization-pathways" />. As Alonso notes,
		"when it comes to functional words and TAM formatives, the etymology is still transparent"
		<Ref k="alonso2022" p="§3.1" />; it is in the basic vocabulary where external comparison
		would be decisive — and where it has consistently failed to produce the requisite
		correspondence sets.
	</p>
	<p>
		The macro-comparative and deep-contact debates are returned to in full detail in
