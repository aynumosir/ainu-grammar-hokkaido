<S t="Isolate status" id="isolate">
	<p>
		Ainu has no demonstrated genetic relationship to any other language or language family. Every
		attempt to connect it — whether to Japonic, Altaic, Austroasiatic, Austronesian, or Nivkh —
		has failed to produce the regular sound correspondences across a systematic body of cognates
		that the comparative method requires ‹consensus›. The assessment is stated plainly by
		<Ref k="janhunen2022" p="§2" />: Ainu "does not show unambiguous indications of being
		associated with any other language family". <Ref k="alonso2022" p="§1" /> adopts the same
		position and explicitly sets aside all prior affiliation proposals to concentrate on what can
		be recovered by internal analysis alone.
	</p>
	<p>
		Within its own lineage, Ainu is a small family. <Ref k="janhunen2022" p="§2" /> refers to it
		as "Ainuic" (or "Kurilic", from the Russian exonym for the Kuril-speaking population), with
		three member varieties: Hokkaido, Sakhalin, and the Northern Kuriles. The internal comparison
		among these three is the only comparative operation available to Ainu linguistics; it is
		treated in depth in <Xr ch="proto-ainu-segmental-reconstruction" />,
		<Xr ch="final-h-history-and-sakhalin-length-reflex" />, and
		<Xr ch="sakhalin-and-kuril-ainu-contrast" />, and the classification of Hokkaido dialects
		within the Ainuic family is the subject of <Xr ch="hokkaido-dialect-classification-and-dialectometry" />.
		The Sakhalin variety branched off first, approximately 800 years ago; the Kuril variety
		diverged less than 600 years ago, following what <Ref k="janhunen2022" p="§3b" /> calls the
		"Great Migration" of the 13th century ‹consensus›. The deep genealogical history of the
		family, and the ethnogenesis of the Ainu people, are treated in
		<Xr ch="ainu-people-homeland-history" />.
	</p>
	<p>
		Typologically, Ainu is distinct from all its geographic neighbours, including Japanese.
		<Ref k="janhunen2022" p="§3c" /> notes it may be placed under the broad rubric of North
		Pacific Rim typology, but stands apart even within that areal grouping.
	</p>
</S>

<S t="The comparative method and what isolate status entails" id="method">
	<p>
		For a language with no established relatives, three analytical tools are available, in
		decreasing order of probatory force <Ref k="alonso2022" p="§1" />: (i) <em>internal
		reconstruction</em> — working backwards from dialectal and morphophonological alternations
		to recover earlier forms — which <Ref k="alonso2022" p="§1" /> calls "by far the most
		powerful" for Ainu; (ii) <em>contact-induced change analysis</em>, chiefly Ainu interactions
		with Japanese and, on Sakhalin, with Nivkh; and (iii) <em>typological reasoning</em>, to be
		"used with utmost caution" since structural parallels between languages provide heuristics
		for the reconstruction scenarios but carry no genealogical weight.
	</p>
	<p>
		The time depth reachable by internal reconstruction is shallow. Without a comparative method
		drawing on related sister languages, reconstruction "with wild speculation excluded" takes
		Ainu back roughly 1,000 years <Ref k="alonso2022" p="§1" />. The one firm external date is
		the 13th-century separation of Common Ainu into its descendant varieties (§<Xr
			ch="proto-ainu-segmental-reconstruction"
		/>). The Pre-Proto-Ainu and Proto-Ainu stages reconstructed by
		<Ref k="vovin1993" /> and revised by <Ref k="alonso2022" /> rest entirely on internal
		reconstruction, without an external comparative check.
	</p>
	<p>
		The standard for assessing claims of genetic relationship is the presence of <em>regular
		sound correspondences</em> across a sizeable set of basic-vocabulary items — correspondences
		that cannot be explained by borrowing, universal sound-change tendencies, or chance
		resemblance. Typological similarity (shared SOV order, similar phoneme inventories) and
		individual look-alike pairs fall short of this standard, as does the observation that two
		languages share structural features that typologists group together aareally. The macro-comparison
		proposals surveyed below are evaluated against this criterion.
	</p>
</S>

