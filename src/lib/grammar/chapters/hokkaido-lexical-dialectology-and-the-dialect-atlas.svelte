<S t="The eastern–western lexical divide" id="ew-divide">
	<p>
		The most robust lexical signal dividing northeastern from southwestern Hokkaido is a set of
		interrogative and temporal stems. The southwestern dialects (including Saru, Chitose, Horobetsu,
		and Yakumo) use interrogative stems beginning in
		<i lang="ain-Latn">hem-</i> / <i lang="ain-Latn">hen-</i>: 'when' is
		<i lang="ain-Latn">hempara</i> and 'how many' is <i lang="ain-Latn">hempak</i>. The
		northeastern dialects (Asahikawa, Nayoro, Sōya, Obihiro, Kushiro, and Bihoro) use
		<i lang="ain-Latn">ne-</i>-initial forms: <i lang="ain-Latn">nempara</i> and
		<i lang="ain-Latn">nempak</i> (<Ref k="nakagawafukazawa2022" p="§5.1" />). The Saru–Chitose
		group adds its own specific interrogatives: <i lang="ain-Latn">hemanta</i> 'what',
		<i lang="ain-Latn">hunna</i> 'who'.
	</p>
	<p>
		Nakagawa and Fukazawa read the interrogative distribution as a diachronic contrast. The
		Saru–Chitose group preserves two etymologically distinct stems — an interrogative
		<i lang="ain-Latn">hVm-</i> / <i lang="ain-Latn">hVn-</i> base and a separate indefinite
		<i lang="ain-Latn">ne-</i> base — while most northeastern dialects generalized the indefinite
		stem to interrogative function, yielding <i lang="ain-Latn">ne-mpara</i> by analogy with other
		<i lang="ain-Latn">ne-</i> indefinites (<Ref k="nakagawafukazawa2022" p="§5.2" />). On that
		analysis the southwestern and Saru–Chitose patterns are retentions and the northeastern forms
		are innovations ‹contested›. Community discussion at the aynu-corpora Discord independently
		arrived at the same two-stem analysis — interrogative <i lang="ain-Latn">hem</i> /
		<i lang="ain-Latn">hun</i> versus indefinite <i lang="ain-Latn">ne</i> — and further
		segmented <i lang="ain-Latn">hunna</i> as <i lang="ain-Latn">hun</i> + <i lang="ain-Latn"
			>na</i> and <i lang="ain-Latn">hempak</i> as <i lang="ain-Latn">hem</i> +
		<i lang="ain-Latn">pak</i> (<Ref k="aynucorporadiscord" />, nukopoli 2023-12-08, proposed).
		The segmentation is corroborated by the comparative data in Nakagawa and Fukazawa's analysis
		‹corpus-confirmed›.
	</p>
</S>

