<S t="The survey apparatus and its primary data" id="apparatus">
	<p>
		The systematic comparison of lexical variation across Hokkaido Ainu rests on a survey carried
		out by Hattori Shirō and Chiri Mashiho in 1960. Working from a 200-item basic-vocabulary list
		elicited at 19 localities — 13 across Hokkaido and 6 in Sakhalin ‹SA› — together with Torii's
		1903 Kuril ‹KU› materials, they scored each item for cross-dialectal cognacy as positive,
		negative, or partial (<Ref k="nakagawafukazawa2022" p="§2.1" />). The 13 Hokkaido localities
		were Yakumo, Oshamambe, Horobetsu, Biratori, Nukibetsu, Niikappu, Samani, Obihiro, Kushiro,
		Bihoro, Asahikawa, Nayoro, and Sōya. The resulting cognacy percentages supplied the first
		quantified measure of lexical distance between Hokkaido varieties and remain the empirical layer
		beneath every subsequent dialectometric classification.
	</p>
	<p>
		The survey's reach is narrower than the item count suggests. Of the 200 elicited items, only
		91 actually contributed to the cognacy matrix — the remaining 109 showed no cross-dialectal
		stem difference and therefore carried no discriminating information (<Ref
			k="nakagawafukazawa2022"
			p="§2.3"
		/>). Those 91 variable items are the effective data for Hokkaido lexical dialectology: a
		diagnostically rich but typologically restricted window, concentrated on body parts, basic
		environmental vocabulary, and core verbal concepts.
	</p>
	<p>
		Two further resources expand and document the survey results. Hattori's 1964 dialect dictionary
		(<Ref k="hattori1964" />) organized the comparative material across the 19 localities into a
		single reference work, giving paradigmatic forms and attestations for each item. Fukazawa
		(2025) has since brought to light a previously unpublished document: Chiri Mashiho's own
		handwritten and annotated copy of the basic-vocabulary survey tables, preserved at the National
		Ainu Museum (<Ref k="fukazawa2025" />). These tables, covering at minimum the Yakumo,
		Oshamambe, and Horobetsu localities, carry Chiri's marginal variant readings and hesitation
		marks, providing a check on the published cognacy scores and revealing forms that may have been
		suppressed in the final scoring.
	</p>
</S>

<S t="Coverage and documentation gaps" id="coverage">
	<p>
		The geographic distribution of the 13 Hokkaido localities is uneven. The Pacific coast and the
		Ishikari–Hidaka corridor are represented by multiple sites; the Sea-of-Japan coast contributes
		only Yakumo, and the Sea-of-Okhotsk coast only Bihoro. The northernmost point, Sōya, was
		effectively a single speaker — Ben Kashiwagi — so its data reflect an idiolect rather than a
		community sample (<Ref k="nakagawafukazawa2022" p="§2.4" />). Many areas that certainly held
		Ainu-speaking populations into the nineteenth century, particularly along the Sea-of-Japan
		coast, left no post-Meiji lexical record at all ‹corpus-suggested›.
	</p>
	<p>
		Kasuga's 2026 expansion to 75 localities, incorporating place-name evidence and historical
		documents, begins to fill some of these gaps (<Ref k="kasuga2026" />), but systematic
		multi-speaker lexical data from the poorly attested regions remain largely absent. The place-name
		record is geographically broader than the spoken-language survey, though it introduces its own
		complications: toponyms reflect an earlier language state, their orthographic conventions vary
		across sources, and phonological changes may have operated differently on frozen names than on
		the living lexicon ‹speculative›.
	</p>
</S>

