<S t="The par / car 'mouth' isogloss" id="mouth-isogloss">
	<p>
		Among the lexical differences documented by the survey, the word for 'mouth' has attracted the
		most analytical attention. Southwestern Hokkaido dialects use
		<A w="par" gl="mouth" /> and its affiliative form <i lang="ain-Latn">paro</i>;
		northeastern dialects, Sakhalin ‹SA›, and Kuril ‹KU› varieties use
		<A w="car" gl="mouth" /> and <i lang="ain-Latn">caro</i>. The related body-part terms follow
		the same division: 'lip' is <i lang="ain-Latn">papus</i> / <i lang="ain-Latn">capus</i> or
		<i lang="ain-Latn">patoy</i> / <i lang="ain-Latn">catoy</i> depending on region (<Ref
			k="nakagawafukazawa2022"
			p="§3.4"
		/>).
	</p>
	<p>
		The contrast superficially resembles a regular sound correspondence between a southwestern
		/p/-initial and a northeastern /c/-initial dialect group, but it does not behave like one.
		No other set of unrelated Hokkaido words shows the same /p/ ∼ /c/ alternation in any
		systematic pattern; the variation is restricted to the 'mouth' semantic cluster and a small
		number of body-part and motion terms (<i lang="ain-Latn">can</i> / <i lang="ain-Latn">pan</i>
		'thin'; <i lang="ain-Latn">cas</i> / <i lang="ain-Latn">pas</i> 'run'). Nakagawa and Fukazawa
		therefore call it a "pseudo-correspondence": it is geographically distributed like a sound
		change but is not phonologically general (<Ref k="nakagawafukazawa2022" p="§3.4" />).
	</p>
	<p>
		Four analytical positions have been advanced for the origin of the split, all reported here via
		Nakagawa and Fukazawa (<Ref k="nakagawafukazawa2022" p="§3.4" />):
	</p>
	<ul>
		<li>
			<strong>Single-change account</strong> (Kindaichi, Chiri, Naert 1958): a historical change
			<i lang="ain-Latn">*pa &gt; ca</i> propagated through the northeastern region, or both derive
			from a single ancestral form <i lang="ain-Latn">*Xa</i>.
		</li>
		<li>
			<strong>Two-'mouth'-words account</strong> (Kirikae 1994): Proto-Ainu already had two distinct
			words for 'mouth'; their regional distribution is a fossilized retention of this original
			synonymy.
		</li>
		<li>
			<strong>Age-area / ABA account</strong> (Nakagawa 1996):
			<i lang="ain-Latn">car</i> is the older form, preserved at the northeastern periphery and in
			‹SA›/‹KU›, and retained in Saru-region place names (e.g.
			<i lang="ain-Latn">sancaorta</i>); <i lang="ain-Latn">par</i> is a Saru-Hidaka innovation
			that spread through the southwest, eventually producing the compound
			<i lang="ain-Latn">par-unpe</i> 'tongue' (lit. 'that-which-lies-in-the-par-region').
		</li>
		<li>
			<strong>Two-morpheme account</strong> (Fukazawa 2014/2017):
			<i lang="ain-Latn">ca-</i> and <i lang="ain-Latn">pa-</i> were separate morphemes from the
			outset, each appearing across a range of body-part and spatial vocabulary
			(<i lang="ain-Latn">pa-</i> also meaning 'head'), so the alternation reflects a productive
			morphemic distinction rather than a lexical split or sound change.
		</li>
	</ul>
	<p>
		All four accounts rest on the same survey dataset and on place-name evidence with its own
		chronological uncertainties; none can be ruled out on the available materials ‹contested›.
		Place-name counts — 11:0 in favor of <i lang="ain-Latn">par-</i> in the Ishikari basin, 13
		instances of <i lang="ain-Latn">car-</i> in Nemuro and Kushiro combined (Kirikae 1994, reported
		via <Ref k="nakagawafukazawa2022" p="§3.4" />) — are consistent with a sharp geographic
		boundary but do not settle the mechanism. A community discussion at the aynu-corpora Discord
		(nishiyasu 2026-05-07, proposed) independently observed that place-name distributions favor
		<i lang="ain-Latn">car</i> in the northeast and east, strengthening the place-name evidence
		without resolving the analytical question (<Ref k="aynucorporadiscord" />) ‹corpus-suggested›.
	</p>
	<p>
		A further phonological account, not among the four original proposals, reconstructs a
		Proto-Ainu palatalized initial <i lang="ain-Latn">*pʸaɾ</i>, whose northeastern reflex yielded
		/c/ while the southwestern region generalized plain /p/ (reported via
		<Ref k="nakagawafukazawa2022" p="§3.4" />) ‹speculative›. A community member (nukopoli
		2023-12-08) independently proposed a related palatalization pathway via a reconstructed
		intermediate <i lang="ain-Latn">*ti</i> (<Ref k="aynucorporadiscord" />, proposed) ‹speculative›;
		the two mechanisms converge in positing a palatal intermediate but differ in the structural
		details.
	</p>
</S>

<S t="ABA distributions and retention at the periphery" id="aba-patterns">
	<p>
		Several variable items show an ABA distribution: the same form occurs at the geographic
		margins of Hokkaido — both east and west — while a different form occupies the center. Under
		the wave-model reading standard in Japanese dialectology and adopted here by Nakagawa (reported
		via <Ref k="nakagawafukazawa2022" p="§5" />), the peripheral form (A) is typically the older
		retention and the central form (B) is an innovation that has not yet completed its spread to
		the edges.
	</p>
	<p>
		The word for 'tongue' is the primary example. The peripheral dialects — Yakumo in the
		southwest, Kushiro and Bihoro in the east — retain <A w="aw" gl="tongue" />. The central
		dialects, including Saru, Chitose, Horobetsu, and Asahikawa, use
		<i lang="ain-Latn">parunpe</i>, a transparent compound: literally 'that which lies inside the
		<i lang="ain-Latn">par</i> region / the mouth-interior'. The ABA reading takes
		<i lang="ain-Latn">aw</i> as the form reconstructable for pre-innovation Hokkaido and
		<i lang="ain-Latn">parunpe</i> as an innovation coined in the <i lang="ain-Latn">par</i>-using
		southwestern area and spreading outward without displacing <i lang="ain-Latn">aw</i> at the
		farthest edges (<Ref k="nakagawafukazawa2022" p="§5" />) ‹speculative›. That interpretation
		is also consistent with the Nakagawa 1996 age-area account of the
		<i lang="ain-Latn">par</i> / <i lang="ain-Latn">car</i> alternation: if
		<i lang="ain-Latn">par</i> 'mouth' is itself an innovation displacing <i lang="ain-Latn">car</i>,
		then <i lang="ain-Latn">parunpe</i> 'tongue' (<i lang="ain-Latn">par</i> + locative/relational
		element) is a secondary compound built on that innovation and will share the same geographic
		profile — which is exactly what the survey data show ‹speculative›.
	</p>
	<p>
		The 'father' terms offer a second, less clear-cut case. The form
		<i lang="ain-Latn">mici</i> appears along the Pacific Coast arc from the Kuril Islands through
		the eastern coast to Horobetsu, a distribution that cuts across the NE/SW divide. The form
		<i lang="ain-Latn">iyapo</i> is recorded at an isolated inland point near the Saru river mouth.
		Yamada's proposed "Pacific Tribe" — a hypothetical coastal population whose lexical traces are
		visible in forms like <i lang="ain-Latn">mici</i> and in place-name elements
		(<i lang="ain-Latn">koyka</i> / <i lang="ain-Latn">koypok</i>) — is a speculative
		interpretation of this coastal continuity (reported via <Ref k="nakagawafukazawa2022" p="§5" />)
		‹speculative›.
	</p>
</S>

<S t="Semantic field variation beyond the basic list" id="semantic-fields">
	<p>
		The 91-item variation set is limited to high-frequency basic vocabulary. Systematic
		cross-dialectal comparison in other semantic fields — animal and plant names, technological
		vocabulary, ceremony-related terms — is absent from the survey, and the dialect dictionary
		(<Ref k="hattori1964" />) covers the same 19 localities but does not extend systematically
		beyond the survey items. The domain of place names has been used as a partial supplement: because
		toponyms encode older vocabulary and are geographically pinnable, they allow isoglosses to be
		reconstructed for areas with no living-speaker data.
	</p>
	<p>
		Fukazawa's 2025 edition of Chiri Mashiho's annotated survey tables (<Ref k="fukazawa2025" />)
		adds a qualitative dimension to the Yakumo, Oshamambe, and Horobetsu data. Chiri's marginal
		notes reveal the range of variant forms that a skilled lexicographer heard during elicitation
		but collapsed in the final binary cognacy score — information that the published 1960 dataset
		conceals. This document is a significant resource for reassessing the resolution of the survey
		data, though it covers only three southwestern localities and cannot compensate for the absent
		data from underattested regions.
	</p>
	<p>
		One kinship term deserves note as a clear inter-dialectal lexical isogloss outside the
		basic-vocabulary list: the terms for 'father' (see above, §<em>aba-patterns</em>) and the
		broader kinship vocabulary in general have not been systematically mapped across the survey
		localities. Regional variation in kinship terminology is attested in scattered sources but
		lacks a comprehensive comparative treatment comparable to the basic-vocabulary survey ‹corpus-suggested›.
	</p>
</S>

<S t="Saru–Chitose as a lexically distinct group" id="saru-chitose">
	<p>
		Within the southwestern zone, the Saru–Chitose group shows a cluster of lexical and
		morphosyntactic innovations that set it apart from neighboring Horobetsu and Biratori, even
		though basic-vocabulary cognacy alone would place Saru, Chitose, and Horobetsu in a common
		southwestern group. The distinctively Saru–Chitose items include the interrogative forms
		<i lang="ain-Latn">hemanta</i> 'what' and <i lang="ain-Latn">hunna</i> 'who', the existential
		plural <i lang="ain-Latn">oka</i>, the indefinites <i lang="ain-Latn">nep</i> 'something'
		and <i lang="ain-Latn">nen</i> 'someone', and the prefix <i lang="ain-Latn">eci-</i> 'I to
		you (pl)' (<Ref k="nakagawafukazawa2022" p="§5" />).
	</p>
	<p>
		Several of these Saru–Chitose forms recur in ‹SA› Sakhalin: the form
		<i lang="ain-Latn">hemata</i> (the Sakhalin cognate of <i lang="ain-Latn">hemanta</i>) 'what'
		and the prefix <i lang="ain-Latn">eci-</i> appear in both Saru–Chitose and ‹SA›, yet are
		absent from much of northeast Hokkaido. Nakagawa and Fukazawa find this distribution "hard to
		believe [is] independent" and leave it as an unresolved puzzle about the internal phylogeny of
		Ainu (<Ref k="nakagawafukazawa2022" p="§5" />) ‹contested›. One reading is that these forms
		are innovations shared by an ancestral coastal or southern group before the Sakhalin branch
		diverged; another is that they are archaisms preserved separately in Saru–Chitose and ‹SA›
		while the northeast innovated. Neither account has been demonstrated from the available data.
	</p>
</S>

<S t="Lexical data and dialectometric classification" id="dialectometry-bridge">
	<p>
		The cognacy matrix computed from the 91 variable items has been the primary input for the main
		proposals about how Hokkaido dialects group. Asai's 1974 cluster analysis of this matrix
		proposed a partition into northern (Sōya), eastern (Obihiro / Kushiro / Bihoro / Asahikawa /
		Nayoro), central-southern (Yakumo / Oshamambe / Horobetsu / Biratori / Nukibetsu / Niikappu),
		and eastern-Hidaka (Samani) zones for Hokkaido, together with a major split between the
		Hokkaido+Kuril group and Sakhalin (reported via <Ref k="nakagawafukazawa2022" p="§2.2" />; <Ref
			k="ono2020"
			p="§2"
		/>).
	</p>
	<p>
		Ono's 2020 statistical reanalysis demonstrates that the claimed three-way "Major Division" of
		Hokkaido / North Kuril / Sakhalin is not supported when methodologically appropriate algorithms
		are applied to Asai's original data. Asai's linkage methods (single-linkage and complete-linkage
		agglomerative clustering) ignore mid-range cognacy values that are precisely where the evidence
		for sub-groupings lies; spectral clustering over the same matrix yields two groups — Hokkaido
		plus North Kuril vs Sakhalin — without any robust internal three-way split (<Ref
			k="ono2020"
			p="§3–4"
		/>) ‹contested›. Within the Hokkaido+Kuril group, North Kuril clusters with the northeastern
		Hokkaido localities (Samani, Obihiro, Kushiro, Bihoro, Sōya) rather than with the southwestern
		group, while the southwestern and Saru–Chitose varieties form the second sub-cluster. Ono
		also notes that Asai (1974) may never have intended to canonize a tripartite dialect
		classification, yet his result became the standard reference beyond its original purpose.
	</p>
	<p>
		Fukazawa and Ono's 2024 paper takes a complementary methodological direction (<Ref
			k="fukazawaono2024"
		/>). Rather than assigning each locality to a group, it maps the similarity of every Hokkaido
		point to two "boundary" reference dialects — Sōya (the Hokkaido/Sakhalin boundary variety) and
		Asahikawa (the NE/SW boundary variety) — through correspondence analysis combined with
		projection pursuit. This dissolves the unresolvable question of which group a boundary dialect
		belongs to and instead treats geographic gradience as the object of description. The resulting
		picture is one of overlapping similarity fields converging on the boundary localities rather than
		a set of discrete blocs.
	</p>
	<p>
		A persistent tension runs through the classification literature: lexical and morphosyntactic
		isoglosses do not always converge on the same groupings. The clearest case is Horobetsu.
		Basic-vocabulary cognacy places Saru, Chitose, and Horobetsu together in a southwestern cluster;
		person-marking morphology (Horobetsu uses prefix–suffix circumfixes while Saru–Chitose use
		prefix-only marking) places Horobetsu with the geographically distant Ishikari and Tokachi
		varieties (<Ref k="nakagawafukazawa2022" p="§4.1" />) ‹contested›. Lexical distance is the
		broadest signal; finer grammatical features track different, partly independent histories. The
		morphosyntactic dimension is developed in <Xr ch="hokkaido-phonological-microvariation" />, and
		the full statistical classification debate in <Xr ch="hokkaido-dialect-classification-and-dialectometry" />.
	</p>
</S>

<S t="The Sakhalin and Kuril data as comparative evidence" id="sa-ku-comparative">
	<p>
		The 1960 survey included six Sakhalin localities and the Torii 1903 Kuril materials alongside
		the 13 Hokkaido points, giving the lexical comparisons a triangulable external dimension. For
		the present chapter, two findings are directly relevant.
	</p>
	<p>
		First, the form <A w="car" gl="mouth" /> — northeastern in Hokkaido — is the standard word
		for 'mouth' in ‹KU› and ‹SA›, attested in early Kuril sources including Krasheninnikov (1738)
		and Golovnin (1811) and throughout the Sakhalin records (reported via <Ref
			k="nakagawafukazawa2022"
			p="§3.4"
		/>). If the age-area account of the <i lang="ain-Latn">par</i> /
		<i lang="ain-Latn">car</i> split is correct (§<em>mouth-isogloss</em>), the agreement between
		northeastern Hokkaido, ‹SA›, and ‹KU› in using <i lang="ain-Latn">car</i> supports reading that
		form as older, and <i lang="ain-Latn">par</i> as an innovation limited to southwestern Hokkaido
		‹speculative›.
	</p>
	<p>
		Second, Ono's reanalysis of the full dataset, including the Sakhalin and Kuril points, places
		North Kuril lexically closer to northeastern Hokkaido than to southwestern Hokkaido, with
		Sakhalin forming a clearly separated external group (<Ref k="ono2020" p="§3–4" />). This
		topological finding — NE Hokkaido+Kuril as one cluster, SW Hokkaido as a second, Sakhalin as
		the external comparator — is the most statistically defensible summary of the lexical evidence
		as the dataset currently stands ‹contested›. The Sakhalin and Kuril varieties in their full
		phonological and morphosyntactic dimensions are treated in <Xr ch="sakhalin-and-kuril-ainu-contrast" />.
	</p>
