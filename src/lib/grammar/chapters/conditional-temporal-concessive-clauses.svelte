<S t="yakka — concessive and permissive" id="yakka">
	<p>
		<i lang="ain-Latn">Yakka</i> 'even if, even though' is the concessive particle. Corpus
		counts place it at roughly 7,500 tokens in the aligned Hokkaido corpus, the highest among
		the conditional–concessive particles and a signal of its frequency in narrative and daily
		speech alike <Ref k="nakagawa2024" p="352" />. The etymology is transparent: conditional
		<A w="yak" gl="if, when" /> plus scalar <A w="ka" gl="even, also" />, literally 'even if',
		the same analysis given for Shizunai by Refsing <Ref k="refsing1986" p="254" />. Dialect
		variants <i lang="ain-Latn">yakkay</i> (Tokachi, Shiranuka) and
		<i lang="ain-Latn">yakkayki</i> (Kushiro verse) carry an emphatic or rhythmic suffix found
		also on <i lang="ain-Latn">korkay</i> <Ref k="nakagawa2024" p="352" />.
	</p>
	<p>
		Nakagawa distinguishes two readings by the temporal orientation of the protasis. With a
		future or hypothetical protasis, <i lang="ain-Latn">yakka</i> yields a suppositional
		concessive: 'whatever happens with X, Y holds'. With a past protasis the reading shifts
		toward adversative: 'though X occurred, Y is/was so'
		<Ref k="nakagawa2024" p="352" />. The following example shows both
		<i lang="ain-Latn">yakun</i> and <i lang="ain-Latn">yakka</i> in a single utterance:
	</p>

	<Ex
		m="kironnu=an yakun a=i=rayke yakun a=i=rayke yakka pirka"
		g="be.satiated=4.S CONJ 4.A=4.O=kill CONJ 4.A=4.O=kill CONJ be.good"
		tr="If I am full, even if they kill me — let them; it is fine."
		cite="nakagawa2024:347"
		dial="SAR"
		note="Nakagawa ex. 641. The first two clauses deploy yakun for successive hypothetical premises; the third uses yakka with the evaluative pirka in the concessive-permissive reading. The indefinite 4th-person agent a= covers an unspecified actor; i= marks the 4th-person object."
	/>

	<p>
		The idiomatic frames <i lang="ain-Latn">yakka pirka</i> and
		<i lang="ain-Latn">yakka wen</i> are the primary permissive and non-permissive
		constructions of Hokkaido Ainu. <i lang="ain-Latn">Yakka pirka</i> licenses an action for
		the addressee ('you may X'); <i lang="ain-Latn">yakka wen</i> withholds that license
		('you may not X') <Ref k="ijas2023" />; <Ref k="refsing1986" p="254" />. These differ
		structurally and semantically from the obligation frames <i lang="ain-Latn">yak pirka</i>
		/ <i lang="ain-Latn">yak wen</i>, which prescribe or prohibit an action as a habitual norm
		rather than granting or denying a right. The permissive and deontic dimensions of the
		conditional system connect to the broader modal apparatus in
		<Xr ch="epistemic-irrealis-modality" /> and to the imperative constructions of
		<Xr ch="imperative-and-prohibitive" />.
	</p>

	<Ex
		m="e=e yakka pirka"
		g="2SG.A=eat CONJ be.good"
		tr="You may eat it."
		constructed
		cite="ijas2023"
		note="Teaching illustration of the permissive yakka pirka frame, constructed after Ijäs L8 ex. 11-5. The Shizunai parallel e=kor yakka pirka 'you may keep it' (Refsing 1986: 254, ex. 448) confirms the construction across dialects."
	/>

	<p>
		A distinct use builds a parallel-listing construction from the pattern
		<i lang="ain-Latn">[NP] ne yakka [NP] ne yakka …</i>, surveying nominal alternatives under
		the concessive and asserting that the main clause holds regardless of which alternative
		obtains ('whether X or Y, both X and Y')
		<Ref k="nakagawa2024" p="355–356" />.
	</p>
</S>

<S t="korka and hikeka — adversative" id="korka-adversative">
	<p>
		<i lang="ain-Latn">Korka</i> 'but, however' marks the matrix clause as counter to the
		expectation raised by the preceding clause. Corpus frequency of roughly 3,700 tokens
		confirms its steady use across oral literature and modern prose
		<Ref k="nakagawa2024" p="351" />. The etymology is the same construction-type as
		<i lang="ain-Latn">yakka</i>: conditional <A w="kor" gl="when; while" /> plus scalar
		<A w="ka" gl="even, also" /> — literally 'even when' — pointing to an original conditional
		reading that has shifted entirely toward counter-expectation
		<Ref k="nakagawa2024" p="351" />; <Ref k="ijas2023" />. The dialect variant
		<i lang="ain-Latn">korkay</i> (Samani, Tokachi, Shiranuka, Kushiro) carries the same
		emphatic suffix found in <i lang="ain-Latn">yakkay</i>
		<Ref k="nakagawa2024" p="351" />.
	</p>

	<Ex
		m="nay poro p ne korka nay nutap ka hutne uske ne wa an"
		g="river big NMLZ COP CONJ river bend even narrow place COP CONJ exist"
		tr="The river was big, but even the bends had become narrow."
		cite="nakagawa2024:351"
		dial="SAR"
		note="Nakagawa ex. 662. The first clause closes on a formal-noun predicate p ne before korka; the particle spans the expectation set by the size of the river and the counter-result of narrow meanders. The resultative wa an at the end of the matrix presents a state that has come into being."
	/>

	<p>
		<i lang="ain-Latn">Hikeka</i> (Ishikari <i lang="ain-Latn">ikeka</i>; Shiranuka and
		Kushiro <i lang="ain-Latn">ikekay</i>) occupies overlapping semantic space. Some analysts
		classify it under concession (譲歩) rather than adversative (逆接), but its distribution in
		attested texts is largely interchangeable with <i lang="ain-Latn">korka</i>
		<Ref k="nakagawa2024" p="351–352" />; <Ref k="sato2008" p="52" /> ‹contested›.
		Etymologically it is the loose-link formal noun
		<A w="hike" gl="thing, fact (linking noun)" /> plus
		<A w="ka" gl="even, also" />, a formation parallel to the
		<i lang="ain-Latn">kor</i> + <i lang="ain-Latn">ka</i> source of
		<i lang="ain-Latn">korka</i>.
	</p>
</S>

<S t="Interface with the evidential system" id="evidential-interface">
	<p>
		The conditional and concessive particles of this chapter attach outside the clause-final
		evidential frame. The ordering [finite VP] – evidential nominalizer – connective is
		verified: <i lang="ain-Latn">hawe ne yakun</i> 'if (it is reported that) X',
		<i lang="ain-Latn">ruwe ne akusu</i> 'and (as established fact) Y was then discovered',
		and <i lang="ain-Latn">ruwe ne korka</i> 'though X is the case' are all attested
		<Ref k="nakagawa2024" p="393" />. The evidential sits above clause-internal aspect but
		below the connective: <i lang="ain-Latn">kusu ne ruwe ne</i> is well-formed while
		<i lang="ain-Latn">*ruwe ne kusu ne</i> is not, confirming that the connective scopes over
		the evidential <Ref k="nakagawa2024" p="393" />. The evidential paradigm is treated in
		<Xr ch="ruwe-ne-inferential" />.
	</p>
	<p>
		A cross-linguistically notable asymmetry at this seam was identified by Satō: the factual
		conditional <i lang="ain-Latn">akusu</i> can follow a <i lang="ain-Latn">ruwe ne</i>
		tail — the functional equivalent of Japanese のだ — whereas the Japanese factual conditional
		<span lang="ja">と</span> / <span lang="ja">たら</span> rejects a preceding
		<span lang="ja">のだ</span>. Ainu thus allows [clause — established fact —
		discovery-link], while Japanese does not; this difference argues that the interaction of
		evidentiality with clause-linking is organized differently in the two languages
		<Ref k="sato2009" p="52–53" />.
	</p>
