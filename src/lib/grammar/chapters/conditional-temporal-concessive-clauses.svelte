<S t="The conditional, concessive, and adversative subsystem" id="overview">
	<p>
		Hokkaido Ainu marks hypothetical, temporal, and concessive relationships between clauses
		through post-predicate particles attached after the full finite predicate of the antecedent
		clause. Each linked predicate is fully finite — it carries its own person indexing, polarity,
		aspect, and any clause-final evidential — and the particle follows, belonging to neither of
		the two clauses it joins <Ref k="nakagawa2024" p="514" />; <Ref k="refsing1986" p="238" />.
		The principal members of the conditional–concessive subsystem are:
		<i lang="ain-Latn">yakun</i> (hypothetical conditional),
		<A w="yak" gl="if, when (habitual or evaluative)" /> and
		<i lang="ain-Latn">yakne</i> (evaluative and oral-literary conditionals),
		<i lang="ain-Latn">ciki</i> / <i lang="ain-Latn">cik</i> (mood-restricted conditional),
		<i lang="ain-Latn">kor</i> in its conditional use (habitual or temporal),
		<i lang="ain-Latn">yakka</i> (concessive and permissive), and
		<i lang="ain-Latn">korka</i> (adversative).
	</p>
	<p>
		Two dimensions organize the system: the realis status of the antecedent clause, and the
		sentence type the particle requires or permits in the matrix clause. Satō's dedicated study of
		Chitose conditional expressions finds that Ainu conditionals impose no restriction tied to
		epistemic-judgement mood, yet they do impose restrictions linked to directive and interrogative
		illocution — a distributional pattern not predicted by hierarchies of conditionality derived
		from Japanese <Ref k="sato2009" p="53–54" />.
	</p>
	<p>
		These particles are conjunctive particles (<span lang="ja">接続助詞</span>) in Nakagawa's
		classification: post-predicate, bound forms distinct from the free connective adverbs such as
		<i lang="ain-Latn">orowa</i> 'and then' and <i lang="ain-Latn">rapokke</i> 'meanwhile',
		which can stack after them <Ref k="nakagawa2024" p="337, 347–352, 516" />. The architecture
		of the whole clause-linking inventory is set out in <Xr ch="clause-linkage-overview" />; cause
		and purpose particles are treated in <Xr ch="causal-and-purpose-clauses" />, and the
		complementizer uses of <i lang="ain-Latn">kuni</i>, <i lang="ain-Latn">yak</i>, and
		<i lang="ain-Latn">kusu</i> in indirect speech belong to
		<Xr ch="kuni-complementizer-purpose-complements" />.
	</p>
</S>

<S t="yakun — the hypothetical conditional" id="yakun">
	<p>
		<i lang="ain-Latn">Yakun</i> is the broadest and most frequent of the conditional particles,
		attested in Saru and Chitose with declarative, imperative, interrogative, and conjectural
		matrix clauses alike; Satō's study finds no mood restriction on it
		<Ref k="sato2009" p="51–53" />; <Ref k="nakagawa2024" p="347" />. Its semantic scope covers
		the hypothetical one-off conditional: the protasis states a premise and the matrix presents
		its consequence. Japanese distributes comparable functions across three conditional forms
		(レバ, タラ, ナラ); Ainu lays them largely on <i lang="ain-Latn">yakun</i>, so a Japanese
		rendering must disambiguate by context <Ref k="sato2009" p="53" />.
	</p>
	<p>
		The etymology is transparent: conditional <A w="yak" gl="if, when" /> plus the
		allative/attributive postposition <A w="un" gl="to, toward (allative)" />, a segmentation
		reported for Shizunai by Refsing and supported by morpheme decomposition
		<Ref k="refsing1986" p="256" />. The particle can also head a sentence independently, with
		the anaphoric reading 'in that case, then', a free-standing use that marks its partial
		grammaticalization beyond the strictly bound conditional
		<Ref k="nakagawa2024" p="347" />; <Ref k="ijas2023" />.
	</p>
	<p>
		Refsing notes a 'when'-nuance in Shizunai realis contexts, where
		<i lang="ain-Latn">yakun</i> attaches after an event already in progress or completed,
		yielding something closer to 'when X happened, Y followed'
		<Ref k="refsing1986" p="256" /> ‹corpus-suggested›.
	</p>

	<Ex
		m="sirkunne yakun suy ruyanpe an nankor"
		g="get.dark CONJ again storm exist probably"
		tr="If it gets dark, there will probably be another storm."
		cite="sato2008:51; sato2009:51"
		dial="CHI"
		note="yakun with a conjectural main clause (nankor 'probably'); Satō uses this Chitose example to illustrate the absence of mood restrictions on yakun. The particle is compatible with nankor here, with an imperative elsewhere (Satō 2009: 52 ex. 28), and with a declarative in general statements."
	/>

	<p>
		The protasis of a <i lang="ain-Latn">yakun</i> conditional may carry its own evidential tail
		before the particle, since the verified ordering is [VP] – evidential – connective. The
		sequence <i lang="ain-Latn">hawe ne yakun</i> 'if (it is said that) X' presents the protasis
		as reported hearsay before conditionalizing it; <i lang="ain-Latn">ruwe ne yakun</i> presents
		it as established fact <Ref k="nakagawa2024" p="393" />. The interface with the evidential
		system is discussed in the final section of this chapter.
	</p>
</S>

<S t="yak and yakne — habitual and evaluative conditionals" id="yak-yakne">
	<p>
		Where <i lang="ain-Latn">yakun</i> handles one-off hypotheticals,
		<i lang="ain-Latn">yak</i> marks a habitual or law-like premise whose consequence the
		speaker evaluates. Its hallmark is a strong tendency to be followed by the evaluative
		predicates <A w="pirka" gl="be good" /> or <A w="wen" gl="be bad" />, or by a directive
		expression; it carries a warning nuance when the evaluation is negative
		<Ref k="nakagawa2024" p="349" />; <Ref k="refsing1986" p="256–257" />;
		<Ref k="sato2008" p="50–51" />.
	</p>
	<p>
		The idiomatic frame <i lang="ain-Latn">yak pirka</i> 'it would be good if one did X'
		functions as mild advice or prescription ('one should X'); its negative counterpart
		<i lang="ain-Latn">yak wen</i> 'it would be bad if one did X' conveys prohibition or warning
		('one must not X') <Ref k="nakagawa2024" p="349" />. Both frames can be reinforced by
		<i lang="ain-Latn">anak</i> / <i lang="ain-Latn">anakne</i>,
		<i lang="ain-Latn">easir</i> 'only then', or <i lang="ain-Latn">tasi</i>
		<Ref k="refsing1986" p="256–257" />. These obligation frames differ from the permissive
		frames <i lang="ain-Latn">yakka pirka</i> / <i lang="ain-Latn">yakka wen</i> built on
		the concessive particle, treated below.
	</p>

	<Ex
		m="a=e=siknure yak isitoma=an"
		g="4.A=2SG.O=let.live CONJ be.frightening=4.S"
		tr="Keeping you alive is frightening."
		cite="nakagawa2024:349"
		dial="SAR"
		note="Nakagawa ex. 651. yak with an evaluative-stative matrix (isitoma 'be frightening / dangerous'); the indefinite 4th-person agent a= covers an unspecified subject ('one'), and =an marks 4th-person S on the matrix predicate. The Japanese rendering 'お前を生かしておくと恐ろしい' confirms the warning-evaluation reading."
	/>

	<p>
		In the Ishikari dialect the sequence <i lang="ain-Latn">yak easir</i> (conditional
		<i lang="ain-Latn">yak</i> plus <i lang="ain-Latn">easir</i> 'only then') extends the
		evaluative conditional into an explicit obligation construction — 'it is only fitting if
		one does X' — moving from evaluation toward necessity ‹contested›; the analysis is proposed
		in community discussion and awaits corroboration from a primary grammar of Ishikari
		(nukopoli, <Ref k="aynucorporadiscord" /> 2026).
	</p>
	<p>
		<i lang="ain-Latn">Yakne</i> functions similarly to <i lang="ain-Latn">yak</i> but is
		associated with prayer and oral-literary register;
		<i lang="ain-Latn">yakne pirka</i> is extremely rare and the form appears mainly in the
		fixed phrase <i lang="ain-Latn">wa ne yakne</i> (sequential
		<i lang="ain-Latn">wa</i> plus copula <i lang="ain-Latn">ne</i> plus conditional
		<i lang="ain-Latn">yakne</i>), concentrated in kamuy yukar and prayer texts
		<Ref k="nakagawa2024" p="349" />.
	</p>
</S>

<S t="ciki/cik — the mood-restricted conditional" id="ciki">
	<p>
		<i lang="ain-Latn">Ciki</i> (Shizunai, Samani, and Tokachi <i lang="ain-Latn">cik</i>)
		presents a premise for which the speaker issues a command or asks a question; the particle
		consequently requires an imperative or interrogative matrix clause and is ill-formed with a
		declarative <Ref k="sato2009" p="50–51" />; <Ref k="nakagawa2024" p="350" />;
		<Ref k="refsing1986" p="255" />. This places <i lang="ain-Latn">ciki</i> at the opposite
		end of the mood-freedom scale from <i lang="ain-Latn">yakun</i>. The stock formula opening
		instructions and demonstrations illustrates the imperative use:
	</p>

	<Ex
		m="itak=an ciki e=nu katu ene an hi"
		g="speak=4.S CONJ 2SG.A=hear manner thus exist NMLZ"
		tr="As I speak, listen in the following manner."
		cite="nakagawa2024:350"
		dial="SAR"
		note="Nakagawa ex. 655. ciki closes the premise clause (itak=an 'one speaks / as I speak'), and the matrix is a nominalized directive — the standard formula for opening a speech, recipe, or demonstration. The imperative force is embedded in the nominalized ene an hi 'the manner in which (you are to) listen'."
	/>

	<p>
		Refsing characterizes Shizunai <i lang="ain-Latn">cik</i> as carrying an assumption: the
		speaker takes the protasis to be true or imminent and issues a directive or question on
		that basis, distinguishing it from the purely hypothetical
		<i lang="ain-Latn">yakun</i> <Ref k="refsing1986" p="255" />. In the Horobetsu and
		Chitose dialects, <i lang="ain-Latn">ciki</i> overlaps in use with the
		sequential-discovery particle <i lang="ain-Latn">akusu</i> — it can mark factual discovery
		('did X, and then found Y') — a temporal reading absent from the mood-restricted Saru form
		<Ref k="nakagawa2024" p="350–351" /> ‹contested›.
	</p>
	<p>
		The etymology of <i lang="ain-Latn">ciki</i> is not settled by the primary grammars. A
		community-proposed segmentation reads the particle as <i lang="ain-Latn">ci</i> (a
		continuative or plural auxiliary attested in most dialects outside Saru and Chitose) plus
		<i lang="ain-Latn">ki</i> 'do', making the conditional a lexicalized construction; the
		primary sources treat the form as an unanalyzed particle ‹contested›
		(nukopoli, <Ref k="aynucorporadiscord" /> 2024). A diachronic connection between the
		conditional <i lang="ain-Latn">ciki</i> and the homophonous adverb
		<i lang="ain-Latn">ciki</i> 'now' — pointing to a grammaticalization path from 'now (if
		…)' toward the conditional–admonitive — is advanced in community discussion but likewise
		awaits support from primary sources ‹speculative›
		(antitwilight, <Ref k="aynucorporadiscord" /> 2024).
	</p>
</S>

<S t="Conditional kor — temporal and habitual" id="kor-conditional">
	<p>
		The particle <A w="kor" gl="while; when; have" /> carries two distinct conjunctive uses —
		simultaneity ('〜しながら') and a habitual or general-causation conditional ('〜するたびに /
		〜すると') — that are sometimes indistinguishable in context
		<Ref k="nakagawa2024" p="340, 347" />; <Ref k="ijas2023" />. The simultaneity use is
		treated in <Xr ch="simultaneous-kor-clauses" />; the present section concerns the
		conditional reading.
	</p>
	<p>
		In the conditional use, <i lang="ain-Latn">kor</i> encodes general causation or a
		recurrent event: 'whenever X, Y'. This differs from <i lang="ain-Latn">yakun</i> (one-off
		hypothetical) in that the protasis describes something that holds repeatedly or as a
		general law. Satō finds that conditional <i lang="ain-Latn">kor</i> takes a declarative
		matrix in general-causation statements; for a one-off individual event the particle is
		restricted to wh-interrogative matrices <Ref k="sato2009" p="51" />. The Shizunai
		equivalents are <i lang="ain-Latn">konno</i> / <i lang="ain-Latn">koonno</i>, and those of
		Horobetsu, Shiranuka, and Kushiro are <i lang="ain-Latn">ko</i>, all serving the
		conditional function that Saru assigns to <i lang="ain-Latn">kor</i>
		<Ref k="nakagawa2024" p="348" />; <Ref k="refsing1986" p="248–249" />.
	</p>

	<Ex
		m="sironuman kor cikap utar iwak"
		g="get.dark CONJ bird ASS go.home"
		tr="When(ever) it gets dark, the birds go home."
		cite="sato2008:49"
		dial="SAR"
		note="Satō's illustration of the habitual-conditional kor: darkness is a recurrent condition and the birds' return a recurrent response. The associative plural utar after cikap collects the birds as a group. A formal-noun tail pe ne frequently closes the matrix of such clauses to foreground the generalization."
	/>

	<p>
		A formal-noun tail <i lang="ain-Latn">pe ne</i> / <i lang="ain-Latn">p ne</i> frequently
		closes the matrix of a <i lang="ain-Latn">kor</i> conditional, yielding the reading 'it
		is the sort of thing that happens when X' and underlining the habitual or general-truth
		character of the pattern <Ref k="nakagawa2024" p="347" />. This is an instance of the
		nominalizer <i lang="ain-Latn">p</i> functioning as a connective after a saturated clause;
		the formal-noun continuum is treated in <Xr ch="clause-linkage-overview" />.
	</p>
</S>

