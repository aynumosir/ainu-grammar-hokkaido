<S t="The nay ~ pet distinction" id="nay-pet">
	<p>
		The contrast between <A w="nay" gl="stream, tributary" /> and
		<A w="pet" gl="river" /> reflects a live semantic distinction in the Ainu lexicon, not
		merely the presence of two suffix shapes. Tamura (1996, s.v. <i lang="ain-Latn">pet</i>)
		characterizes it from the Saru area: <i lang="ain-Latn">pet</i> designates the
		representative waterway of a locality — the Saru, Mukawa, and Monbetsu rivers all carry
		this label — while a branch stream or tributary is <i lang="ain-Latn">nay</i>. The
		criterion is typicality, not size: even a small stream that defines its valley is
		<i lang="ain-Latn">pet</i>, while a large tributary of a major river remains
		<i lang="ain-Latn">nay</i>. Corpus frequencies reflect this asymmetry:
		<i lang="ain-Latn">pet</i> occurs 1,344 times (rank 140) against
		<i lang="ain-Latn">nay</i>'s 559 (rank 312) in the Hokkaido text corpus, consistent with
		the broader referential range of the former.
	</p>

