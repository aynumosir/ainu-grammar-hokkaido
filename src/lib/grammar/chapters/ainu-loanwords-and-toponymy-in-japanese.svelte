	<p>
		The semantic distinction matters for toponymic analysis: a <i lang="ja">-nai</i> place name
		designates a tributary or smaller mountain stream, while a <i lang="ja">-betsu</i> name
		marks the primary waterway of its valley. A common folk-etymological error equates
		<i lang="ain-Latn">nay</i> with 'pond'; nukopoli (aynu-corpora Discord, 2023-12-11,
		corroborating Tamura 1996) registers this as a mistaken conflation: 'pond' or 'lake' is
		<A w="to" gl="lake, pond" />, a semantically distinct root with its own toponymic reflex
		<Ref k="aynucorporadiscord" /> ‹corpus-confirmed›.
	</p>
</S>

<S t="The locative formatives -us(i) and -oma(i)" id="usi-omai">
	<p>
		Two further formatives encode habitual or permanent location and are productive in
		place-name formation.
	</p>
	<p>
		The verbal element <A w="us" gl="be habitually at, grow at" /> (Tamura 1996, s.v.
		<i lang="ain-Latn">us</i>) means 'be habitually attached to, grow at'; the suffix
		<i lang="ain-Latn">-us(i)</i> nominalizes this to 'place where X habitually grows or is
		found'. The compound <i lang="ain-Latn">ni-kap-us-i</i> — <i lang="ain-Latn">ni</i>
		'tree' + <i lang="ain-Latn">kap</i> 'bark' + <i lang="ain-Latn">-us-i</i> 'habitually-at'
		— yields 'place where bark trees stand', commonly cited as the source of Niikappu 新冠
		(illustrative only; <Ref k="tamura1996" p="s.v. -us(i)" />). The suffix also surfaces in
		Usu 有珠 and several other Hokkaido names.
	</p>
	<p>
		The element <A w="oma" gl="be located in/at" /> 'be located in, be contained in' (Tamura
		1996, s.v. <i lang="ain-Latn">oma</i>; Nakagawa 1995, s.v. <i lang="ain-Latn">oma</i>)
		forms the nominalized <i lang="ain-Latn">oma-i</i> 'place where X is located', the source
		of the Japanese <i lang="ja">-omai</i> / <i lang="ja">-oma</i> endings. In Ainu syntax,
		<i lang="ain-Latn">oma</i> also appears in postpositional constructions expressing
		containment; this overlap between its lexical geographic use and its grammatical locative
		function is discussed in <Xr ch="relational-and-spatial-nouns" />.
	</p>
	<p>
		A further geographic compound illustrates the compounding pattern that underlies Hokkaido
		toponymy:
	</p>

	<Ex
