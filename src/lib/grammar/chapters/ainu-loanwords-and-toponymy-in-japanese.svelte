<S t="Methodological cautions" id="cautions">
	<p>
		Place-name etymology faces several systematic pitfalls that bear on the analysis above.
	</p>
	<p>
		Japanese phonological adaptation erases information present in the Ainu source.
		Coda consonants, the voiced/voiceless distinction absent from Ainu, and syllable-weight
		differences are all reorganized by Japanese phonotactics, as described in
		<Xr ch="syllable-template" />. Rendaku produces <i lang="ain-Latn">pet</i> →
		<i lang="ja">-betsu</i>; recovering the Ainu source requires knowledge of both the Ainu
		phonological inventory and the Japanese adaptation rules. Reading Ainu-derived place names
		through kanji orthography produces folk etymologies — <i lang="ja">稚内</i> as 'young
		interior'? — that are wholly Japanese in structure and irrelevant to the Ainu source.
	</p>
	<p>
		A second difficulty is the limited documentation of Ainu as spoken across many areas. As
		discussed in <Xr ch="sociolinguistic-situation-revitalization" />, recording of Ainu
		varieties was geographically uneven, and for areas with no documented variety —
		Hakodate, Erimo, Rumoi, Otaru — place-name reconstruction by extrapolation from
		better-recorded dialects is at best speculative. The constraint that a proposed source
		compound must be morphologically analysable within Ainu and that phonological
		correspondences must be regular remains the methodological floor for any specific toponym
		claim.
	</p>
	<p>
		A third caution is convergence. Phonologically similar strings may arise from unrelated
		sources, and it is possible that some Ainu geographic terms entered Ainu at an earlier
		period from a continental substrate. Antitwilight (aynu-corpora Discord, 2024-11-15) has
		proposed Korean-origin candidates for several Ainu roots, including
		<i lang="ain-Latn">nay</i>, <i lang="ain-Latn">casi</i>, and others
		<Ref k="aynucorporadiscord" />. This is unverified and has not been tested against the
		comparative literature; it is noted here as an acknowledged edge case, not a claim
		‹speculative›.
	</p>
