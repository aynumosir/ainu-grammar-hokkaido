	<p>
		This compounding pattern — a landscape attribute or associated entity plus a geographic
		generic (<i lang="ain-Latn">nay</i>, <i lang="ain-Latn">pet</i>,
		<i lang="ain-Latn">to</i>, etc.) — is the typical structure of an Ainu place name; the
		phonological adaptation that Japanese applies when borrowing such compounds is treated in
