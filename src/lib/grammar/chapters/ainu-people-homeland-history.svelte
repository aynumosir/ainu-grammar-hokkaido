<S t="The ethnonym Aynu and related designations" id="ethnonym">
	<p>
		The word <i lang="ain-Latn">aynu</i> is a common noun meaning 'person, human being'; it is
		also the ethnic self-designation of the people whose language this grammar describes
		<Ref k="janhunen2022" p="§2.4" />; <Ref k="shibatani1990" p="§1.1" />. In its ethnic use
		the term identifies a person as a member of the Ainu community, as distinct from the gods
		(<i lang="ain-Latn">kamuy</i>), and marks the boundary against neighbouring peoples. Its
		territorial correlate is <i lang="ain-Latn">Ainu Moshir</i>
		(<i lang="ain-Latn">aynu mo-sir</i>) 'Ainu land', the indigenous name for the homeland
		<Ref k="janhunen2022" p="§2.4" />.
	</p>
	<p>
		A second self-referential term, <i lang="ain-Latn">utar</i> 'people', appears in affiliative
		form as <i lang="ain-Latn">utari</i> 'relatives, our people'; it was used as the basis for
		the name of the Hokkaidō Utari Kyōkai (<span lang="ja">北海道ウタリ協会</span>), the main
		Ainu civic organization for much of the twentieth century
		<Ref k="janhunen2022" p="§2.4" />. The Sakhalin Ainu have a distinct self-designation,
		<i lang="ain-Latn">enciw</i>, which their modern descendants in Japan have adopted as a
		distinguishing label
		<Ref k="janhunen2022" p="§2.4" />.
	</p>
	<p>
		The exonym "Ainu" (in older literature also "Aino") is now standard in both Japanese and
		international usage. Through much of the twentieth century the Japanese government applied
		the term <span lang="ja">旧土人</span> (<i>kyū-dojin</i>, 'former aborigines') in official
		documents, most notably as part of the 1899 legislation that governed the Ainu's legal status
		<Ref k="ono2022" p="§2" />. This usage was formally abandoned with the abolition of that
		legislation in 1997. Within the Hokkaido Ainu community, the relative weight of
		"Ainu" versus "Utari" as preferred public designations was contested during the 1980s and
		1990s, and the discussion reflects continuing negotiations over political identity
		‹contested›.
	</p>
</S>

<S t="The Ainu homeland and its historical range" id="homeland">
	<p>
		Hokkaido, the northernmost of Japan's main islands, was the core of the historical Ainu
		territory <Ref k="janhunen2022" p="§2.1" />; <Ref k="shibatani1990" p="§1.1" />. The same
		ethnic population extended across the full chain of the Kuril Islands and across the southern
		half of Sakhalin (Japanese: Karafuto; Ainu: <i lang="ain-Latn">Karapto</i>); a marginal
		Ainu presence on the southernmost tip of Kamchatka is also attested in ethnonymic and
		archaeological sources
		<Ref k="janhunen2022" p="§2.1" />.
	</p>
	<p>
		Evidence for an earlier, more southerly range on Honshu comes principally from toponymy. The
		endings <A w="-nay" gl="river, brook" /> and <A w="-pet" gl="river" />, both characteristic
		of Hokkaido river names, appear in dense clusters across the Tōhoku and Kantō regions,
		pointing to Ainu (or an immediately ancestral variety) having been spoken well south of
		Hokkaido at some period
		<Ref k="shibatani1990" p="§1.1" />; <Ref k="chiri1956" />. Linguistic borrowing evidence —
		potential Ainu-derived words in early Japonic texts — has been advanced as additional support
		for a Honshu range <Ref k="vovin2022" />, though the direction and scale of those loans
		remain a matter of specialist debate ‹contested›. The southern limit of former Ainu territory
		on Honshu cannot be fixed with precision on present evidence ‹contested›. The genetic position
		of the language within this spatial history is treated in
		<Xr ch="genetic-position-macro-comparison" />.
	</p>
	<p>
		Within Hokkaido the Ainu dialect cluster is internally diverse: the dialects of the Saru,
		Chitose, Ishikari, Tokachi, Horobetsu, Shizunai, Asahikawa, and Yakumo areas differ
		phonologically and morphologically in ways that the corpus chapters track in detail. Sakhalin
		Ainu (abbreviated <strong>SA</strong> in this grammar) and the now-extinct Kuril variety
		(<strong>KU</strong>) are cited comparatively; the dialect sample and method are set out in
		<Xr ch="dialect-sample-and-corpus-method" />.
	</p>
</S>

<S t="Subsistence, society, and the documentary record" id="society">
	<p>
		Ainu subsistence in the historical period centred on fishing, hunting, and gathering, with
		strong seasonal and ecological patterning <Ref k="shibatani1990" p="§1.1" />;
		<Ref k="janhunen2022" p="§2.5" />. Settlement followed rivers and coastlines, and the
		vocabulary reflects this orientation: the various stages of the salmon's life cycle are
		distinguished by roughly twenty distinct lexical items, and some fifty words for harbour seals
		and more than twenty for types of whale point to the centrality of sea-mammal hunting in the
		coastal zones <Ref k="shibatani1990" p="§1.1" />. Among land animals, bears held a special
		ceremonial role: the word <A w="kamuy" gl="god, bear" /> serves both as the generic term for
		the divine and as a specific designation for the bear, which was understood as a mountain god
		that brought its meat to the village <Ref k="shibatani1990" p="§1.1" />. Dogs figure
		prominently in subsistence and ceremony alike.
	</p>
	<p>
		The precolonial Ainu maintained a diversified economy that also included small-scale
		horticulture and, via the Santan trade, the exchange of furs, dried fish, and lacquerware
		for metal goods and silk manufactured further inland on the Asian continent
		<Ref k="janhunen2022" p="§2.5" />. Population size is uncertain for the precolonial period;
		a figure close to 25,000, possibly more, has been suggested for Hokkaido alone
		<Ref k="janhunen2022" p="§2.5" />.
	</p>
	<p>
		A rich tradition of oral literature — the heroic epics called <A w="yukar" gl="heroic epic" />,
		the divine epics called <A w="kamuy yukar" gl="divine epic" />, and the prose folktales
		called <A w="uwepeker" gl="prosaic folktale" /> — was the principal medium for transmitting
		cultural knowledge and was performed in a register distinct from everyday speech. The oral
		tradition and its register differences are directly relevant to grammar because the
		kamuy yukar corpus contains the densest concentration of older morphological forms and
		archaic vocabulary. The oral-literature genres and their linguistic properties are treated
		in the corpus chapters; the grammatical differences between the oral-literary and
		colloquial registers appear throughout the descriptive chapters.
	</p>
</S>

<S t="The archaeological background: from Jōmon to the historical Ainu" id="archaeology">
	<p>
		The ancestors of the historical Ainu arrived on Hokkaido in connection with the Satsumon
		culture (c. 700–1200 CE), an archaeological complex that spread northward from Honshu and
		replaced the earlier local Epi-Jōmon traditions in southern Hokkaido
		<Ref k="janhunen2022" p="§3" />. The linguistic lineage that became Ainu has roots in the
		Jōmon period on Honshu; Ainu can therefore be described as a "Jōmonic" language in the
		sense that it likely preserves features that were broadly characteristic of pre-Yayoi Japan
		<Ref k="janhunen2022" p="§3a" />. As a consequence, Hokkaido itself is a secondary
		territory for the Ainu; their earlier homeland was on Honshu, from which the Satsumon
		complex — and the ancestral Ainu language — was displaced northward by the Yayoi and
		subsequent Yamato expansion
		<Ref k="janhunen2022" p="§3" />.
	</p>
	<p>
		The Satsumon expansion on Hokkaido encountered the Okhotsk culture (c. 600–1600 CE), a
		littoral and maritime complex that had spread to northern Hokkaido from Sakhalin
		<Ref k="janhunen2022" p="§3" />. The interaction and gradual absorption of Okhotsk
		populations by the Satsumon bearers contributed both genetic and cultural material — and
		potentially some linguistic features — to the complex that became the historically attested
		Ainu ethnicity. The chronology of the Ainu territorial expansion can be traced approximately
		from this archaeological evidence: northern Hokkaido was under Ainu ethnic domination by
		roughly 1000 CE; southern Sakhalin was reached by roughly 1300 CE; and the Kurile Islands,
		including the Northern Kuriles, were occupied by Ainu speakers as late as c. 1500–1600 CE
		<Ref k="janhunen2022" p="§3" />. All these dates are approximations, and the specific
		identification of the Satsumon bearers as Ainu-speakers rests on an inference from
		archaeological continuity rather than direct linguistic evidence ‹contested›.
	</p>

