<S t="Contact history: the Matsumae domain and Russian expansion" id="early-contact">
	<p>
		Japanese contact with the Ainu of southern Hokkaido predates formal political control by
		centuries. The earliest surviving written records of the language, including the Matsumae no
		koto — Ezo kotoba no koto of c. 1600 with 117 words compiled for use by merchants, come
		from this trade context
		<Ref k="refsing1986" p="17" />. The Matsumae domain (<span lang="ja">松前藩</span>),
		established at the southern tip of Hokkaido under Tokugawa authority, held a monopoly on
		trade with the Ainu through the Edo period; Ainu communities in the interior of the island
		and in the Kurile Islands remained outside this domain's political control
		<Ref k="janhunen2022" p="§2.1" />. Commerce in furs, dried fish, and eagle feathers
		constituted the principal economic link between the Ainu and the Japanese domains. Episodic
		tensions over trade terms escalated into armed conflicts, most notably the Shakushain War of
		1669–1672 and the Menashi-Kunashir uprising of 1789, both of which ended with Ainu defeat
		and tightened Matsumae control ‹contested› (the interpretation of Ainu agency and
		objectives is debated).
	</p>
	<p>
		The broader trade network extending from Sakhalin into the Amur basin and ultimately
		connecting with Qing China — the Santan trade (<span lang="ja">山丹交易</span>) — supplied
		the Ainu with metal goods and silk in exchange for northern products
		<Ref k="janhunen2022" p="§2.1" />. This trade also fostered sustained contact between the
		Sakhalin Ainu and their Nivkh and Uilta neighbours, the linguistic traces of which are
		reviewed in the Handbook's chapter on language contact
		<Ref k="shiraishitangiku2022" />. Russian exploration and colonization of Kamchatka from
		the early eighteenth century brought a second colonial power into contact with the Kurile
		Ainu; Sakhalin was transferred from Manchu to Russian control by the Treaty of Peking (1860),
		and the island's status was subsequently settled between Japan and Russia by the Treaty of
		St. Petersburg (1875), which assigned Sakhalin to Russia and the Kurile Islands to Japan
		<Ref k="janhunen2022" p="§2.1" />.
	</p>
</S>

<S t="Meiji-era annexation and assimilation policies" id="meiji-colonial">
	<p>
		The formal annexation of Hokkaido by the Meiji state and its opening to Japanese
		colonization in 1869–1870 ended the Ainu's position as the dominant population of the
		island within a few decades
		<Ref k="janhunen2022" p="§2.1" />. Japanese settler immigration proceeded rapidly: from
		a figure close to the precolonial Ainu population of roughly 25,000, settlers came to
		outnumber the Ainu by an ever-growing margin
		<Ref k="janhunen2022" p="§2.5" />. The Hokkaido Former Aborigines Protection Act of 1899
		(<span lang="ja">北海道旧土人保護法</span>) placed the Ainu in a defined legal category
		as wards of the state, assigned them small agricultural land plots, and legislated
		their assimilation into Japanese society
		<Ref k="ono2022" p="§2" />. No formal legal prohibition of the Ainu language was enacted,
		but the use of the language was restricted or effectively banned in schools, and children
		were pressured to learn Japanese in place of Ainu; administrative discrimination reinforced
		the suppression of public language use
		<Ref k="ono2022" p="§2" />.
	</p>
	<p>
		The human cost of this period appears in the census figures available, though the figures
		must be read with awareness that assimilation policy itself distorted their collection.
		The 1940 census recorded 16,170 Ainu in Hokkaido; the 1935 census put the Sakhalin Ainu
		population at 1,512; the Kurile Ainu were relocated to Shikotan in 1884, and by 1933 only
		forty-one descendants survived — that variety of Ainu is now extinct
		<Ref k="shibatani1990" p="§1.1" />. After 1945, Sakhalin Ainu communities were evacuated
		to Hokkaido; the last speaker of Sakhalin Ainu died in 1994
		<Ref k="janhunen2022" p="§2.3" />. Intercommunal marriage and the discontinuation of Ainu
		census categories make reliable demographic estimates for the late twentieth century
		difficult; figures reported in earlier surveys suggest a population of roughly 16,000 to
		24,000 persons with Ainu ancestry in Hokkaido, with wide uncertainty depending on
		self-identification criteria ‹contested›. By the second half of the twentieth century
		the language had ceased to be transmitted to children in most Hokkaido communities
		<Ref k="ono2022" p="§2" />. The present sociolinguistic situation and the documentation
		record are taken up in <Xr ch="sociolinguistic-situation-revitalization" />.
	</p>
</S>

<S t="From the 1997 law to the 2019 recognition" id="recognition">
	<p>
		The 1899 act was abolished in 1997, when the Japanese Diet enacted the Ainu Culture
		Promotion Act (<span lang="ja">アイヌ文化の振興並びにアイヌの伝統等に関する知識の普及
		及び啓発に関する法律</span>). This legislation provided institutional funding for
		cultural activities and language classes for the first time, but did not recognize
		the Ainu as an indigenous people with specific rights
		<Ref k="ono2022" p="§6" />. A unanimous resolution by the Japanese parliament in 2008
		recognized the Ainu as the indigenous people of Japan, though without statutory force
		<Ref k="janhunen2022" p="§1" />. The Act on Promoting Measures to Realize a Society
		in Which the Pride of the Ainu People Is Respected, enacted in April 2019, superseded
		the 1997 act: it provides the statutory definition of the Ainu as an indigenous people,
		establishes support mechanisms for cultural and language activities, and authorized the
		establishment of the National Ainu Museum (<i lang="ain-Latn">Upopoy</i>), which opened
		in Shiraoi in 2020
		<Ref k="ono2022" p="§7" />. The linguistic implications of revitalization policy —
		including the standardization pressures that accompany institutionalized language classes
		and the rise of new-speaker varieties — are discussed in
		<Xr ch="sociolinguistic-situation-revitalization" />.
	</p>
	<p>
		The documentary record that this grammar draws on is a product of this entire history: the
		bulk of the attested Hokkaido Ainu material was collected in the early-to-mid twentieth
		century from elderly speakers in communities where the language was already in decline.
		The unevenness of that record — richer for some dialects (Saru, Chitose) and thinner for
		others — directly shapes what can and cannot be described with confidence, and the corpus
		chapters note coverage gaps as they arise. The history of Ainu language description and
		the main grammars and dictionaries consulted in this work are surveyed in
		<Xr ch="history-of-description-research" />; the corpus method and dialect sample
		are set out in <Xr ch="dialect-sample-and-corpus-method" />.
	</p>
</S>
