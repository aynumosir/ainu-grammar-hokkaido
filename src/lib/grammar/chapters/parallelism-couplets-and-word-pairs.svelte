<S t="Overview" id="overview">
	<p>
		Hokkaido Ainu verse genres — the heroic epic (<i lang="ain-Latn">yukar</i> /
		<i lang="ain-Latn">sakorpe</i>), the divine epic (<i lang="ain-Latn">kamuy yukar</i> /
		<i lang="ain-Latn">oyna</i>), and ritual speech (prayer, <i lang="ain-Latn">caranke</i>)
		— are performed in an elevated register, <i lang="ain-Latn">atomte itak</i> 'decorated
		language', distinct from everyday prose speech (<i lang="ain-Latn">yayan itak</i>)
		<Ref k="endo2022" p="§3" />; <Ref k="kindaichi1935" paren />. The primary rhetorical
		device of this register is parallelism: two or more consecutive verse lines share the
		same syntactic frame while one or more lexical positions are filled by semantically
		related but distinct words <Ref k="endo2022" p="§4" />; <Ref k="sato2008" p="264" />;
		<Ref k="tamura1996" p="xv–xvii" />. Phonological matching plays no constitutive role:
		"for lines to match or rhyme is not required" <Ref k="endo2022" p="§4" />. The device
		is syntactic and lexical, not phonological.
	</p>
	<p>
		Parallelism in Ainu verse is described in the Japanese scholarship as 対句
		(<em>taigu</em>) and treated alongside formulaic phrasing and periphrasis as the core
		of the poetic apparatus <Ref k="sato2008" p="264" />; <Ref k="endo2022" p="§4" />. A
		minimal pair of parallel lines — a couplet — is the basic unit. The device is "pervasive
		in divine and heroic epics" <Ref k="endo2022" p="§4" /> and extends into prayer and
		incantation; it is absent from everyday prose (<i lang="ain-Latn">uwepeker</i> folktale,
		conversation). For the verse genres and their grammatical signatures, see
		<Xr ch="oral-literature-genre-taxonomy" />; for verse metre, the sakehe refrain, and
		sung-verse structure, see <Xr ch="sakehe-refrain-and-sung-verse-structure" />; for
		the full elevated-register morphological and lexical cluster, see
		<Xr ch="poetic-archaic-elevated-register" />.
	</p>
</S>

<S t="The couplet: structure and attested examples" id="couplet-structure">
	<p>
		The canonical couplet pairs two lines that are syntactically identical or near-identical,
		with one lexical position — typically a modifier or head noun — replaced by a
		semantically contrasted or associated word. The simplest attested type fills the
		modifier slot with a spatial antonym pair:
	</p>

	<Ex
		m="hanke nispa"
		g="near chief"
		tr="the near chief"
		ain="hanke nispa"
		cite="endo2022"
		dial="HK"
		note="First line of an attested couplet (Endō 2022: ch.10 §4, ex.18). The parallel second line substitutes tuyma 'far' for hanke 'near' while the head noun nispa 'chief, lord' is constant."
	/>

	<Ex
		m="tuyma nispa"
		g="far chief"
		tr="the far chief"
		ain="tuyma nispa"
		cite="endo2022"
		dial="HK"
		note="Second line. The pair hanke 'near' // tuyma 'far' is itself a fixed word-pair; together the two lines designate the full social field — all chiefs, proximal and distal."
	/>

	<p>
		The same antonymic pair extends to a larger syntactic frame with incorporated nouns and
		causative morphology. Endō 2022: ch.10 §4, ex.19 gives:
		<i lang="ain-Latn">hanke kotan sonko-kama-re / tuyma kotan e-sonko-ciw-re</i>
		— glossable as 'send word to the near village / drive word into the far village'. Here
		both the spatial modifier (<i lang="ain-Latn">hanke</i> vs <i lang="ain-Latn">tuyma</i>)
		and the verb differ, while the nominal skeleton (<i lang="ain-Latn">___ kotan</i>
		'___ village') and the incorporated noun <A w="sonko" gl="word, message" /> are
		constant <Ref k="endo2022" p="§4" />.
	</p>

	<p>
		A couplet may extend to a three-line set (tricola) in which the first two parallel lines
		lead into a shared predicate. A Chitose text gives:
	</p>

	<Ex
		m="tu imeru kur"
		g="two shining person"
		tr="two shining ones"
		ain="tu imeru kur"
		cite="nakagawa1995:45"
		dial="CHI"
		note="First line of a tricola (Nakagawa 1995: 45; cited in Endō 2022: ch.10 §4, ex.16). Describes embroidery beauty. The second parallel line replaces tu 'two' with re 'three'; the third line supplies the predicate kotuytuyke 'crossed over'."
	/>

	<Ex
		m="re imeru kur"
		g="three shining person"
		tr="three shining ones"
		ain="re imeru kur"
		cite="nakagawa1995:45"
		dial="CHI"
		note="Second line. With tu // re functioning as a 'many' doublet rather than an exact count, the pair together means 'many shining ones'. A third line, kotuytuyke 'crossed over', closes the verse unit."
	/>

	<p>
		A carving couplet attested in Endō 2022: ch.10 §4, ex.17 shows synonymic elaboration
		rather than antonymy: <i lang="ain-Latn">tomika nuye / ikorka nuye</i> 'carve the
		sword-sheath / carve the treasure-sword-sheath'. The verb <A w="nuye" gl="carve" /> is
		constant; <A w="tomika" gl="sword-sheath" /> and <A w="ikorka" gl="treasure-sword-sheath" />
		name the same type of decorated sheath at a plain and an elaborated grade
		<Ref k="endo2022" p="§4" />.
	</p>
</S>

<S t="Types of parallelism" id="types">
	<p>
		The sources <Ref k="endo2022" p="§4" /> and <Ref k="sato2008" p="264" /> document
		the following structural types, which may co-occur within a single couplet:
	</p>

	<p>
		<strong>Antonymic parallelism</strong> pairs words that occupy opposite poles of a
		semantic domain — spatial (<A w="hanke" gl="near" /> // <A w="tuyma" gl="far" />),
		quantitative, or social. The two lines collectively cover the full extent of the
		relevant domain.
	</p>

	<p>
		<strong>Synonymic / elaborating parallelism</strong> pairs a plain term with a more
		specific or more elevated one referring to the same referent
		(<i lang="ain-Latn">tomika</i> // <i lang="ain-Latn">ikorka</i>). The second member
		typically adds a qualifier marking prestige or preciousness — a common escalation
		pattern in heroic and divine verse.
	</p>

	<p>
		<strong>Numeral parallelism</strong> deploys the <A w="tu" gl="two" /> //
		<A w="re" gl="three" /> pair as an intensifying doublet meaning 'many' rather than
		encoding an exact count (see §<a href="#tu-re-formula">below</a>). This is the most
		formulaic type and the most pervasive in the corpus.
	</p>

	<p>
		Endō 2022 does not sort these into discrete sub-types, treating all three under the
		single label 対句. The distinction between antonymic and synonymic types is analytical,
		not a native speaker's metalinguistic category <Ref k="endo2022" /> ‹contested›.
	</p>
</S>

<S t="The tu // re formula: numeral parallelism as intensification" id="tu-re-formula">
	<p>
		The numeral doublet <i lang="ain-Latn">tu</i> // <i lang="ain-Latn">re</i> ('two' //
		'three') is a stock parallel pair that functions as a hyperbolic expression of
		multiplicity rather than a precise count. Used across a couplet — as in Satō 2008:
		264: <i lang="ain-Latn">otu maw-kur-puni kane / ore maw-kur-puni kane</i> 'raising
		two winds / raising three winds' — the pair means 'many winds', the adjacent small
		numerals intensifying the predicate by iteration <Ref k="sato2008" p="264" />;
		<Ref k="endo2022" p="§4" />.
	</p>
	<p>
		In that same example, Satō 2008 notes the archaic counting forms
		<i lang="ain-Latn">otu</i> and <i lang="ain-Latn">ore</i> rather than the everyday
		<i lang="ain-Latn">tu</i> and <i lang="ain-Latn">re</i>; the archaic variants are
		themselves markers of the elevated register <Ref k="sato2008" p="264" />. The Chitose
		tricola above (<i lang="ain-Latn">tu imeru kur / re imeru kur</i>) uses the everyday
		numeral forms, showing that the intensifying function of the doublet does not depend on
		the archaic variants — it resides in the adjacent-numeral pattern itself.
	</p>
	<p>
		The <i lang="ain-Latn">tu // re</i> device belongs to the broader strategy of
		hyperbole and escalation that Satō 2008: 264 identifies alongside periphrasis and
		metaphor as the defining rhetorical inventory of <i lang="ain-Latn">atomte itak</i>.
	</p>
</S>

