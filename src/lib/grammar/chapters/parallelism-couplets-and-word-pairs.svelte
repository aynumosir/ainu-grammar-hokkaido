<S t="Formulaic character and mnemonic function" id="formulaic">
	<p>
		Couplets in Ainu verse are typically also <em>formulae</em>: fixed word sequences learned
		as units and redeployed across performances and across reciters, matching the available
		metrical slot <Ref k="endo2022" p="§4" />. The Hokkaido Ainu Center 2000 documentation
		(via Endō 2022: §4) describes these phrases as "learned in bulk" — a reciter acquires a
		stock of ready-made pairs and deploys whichever fits the narrative moment. An illustrative
		stock phrase is <i lang="ain-Latn">tuk-no pinni si-kopa-yar / aynu pito</i> 'a fine
		person like a grown Manchurian-ash tree' (featuring the poetic similative
		<i lang="ain-Latn">si-kopa-yar</i>, absent from everyday speech), which serves as a
		portable hero-praise formula <Ref k="endo2022" p="§4" />.
	</p>
	<p>
		The formulaic character of couplets serves at least three functions in performance.
		First, mnemonic: in yukar performances lasting several hours or multiple nights (see
		<Xr ch="heroic-and-divine-verse" />), prefabricated phrase-pairs reduce the cognitive
		load of real-time composition, allowing the reciter to reproduce long texts with
		exactitude. Second, intensifying: repetition of a semantic content across two adjacent
		syntactic slots (with controlled lexical variation) constitutes verse-internal emphasis;
		the <i lang="ain-Latn">tu // re</i> doublet works by doubling the iteration rather than
		by selecting a single emphatic word. Third, cohesive: paired lines frame episodes and
		signal transitions within the narrative, binding adjacent performance segments through
		structural symmetry <Ref k="endo2022" p="§4" />; <Ref k="sato2008" p="264" />.
	</p>
	<p>
		Nakagawa (via <Ref k="endo2022" p="§3" />) identifies couplets as one of the sub-devices
		enabling "one phrase → multi-phrase expansion": a single semantic content is projected
		across two lines, satisfying the metre. The syllable-count and accent-orientation
		metrical constraints described in <Xr ch="sakehe-refrain-and-sung-verse-structure" />
		motivate this expansion directly — the choice between the <i lang="ain-Latn">tu</i>
		and <i lang="ain-Latn">re</i> variants, or between a short and an elaborated form, can
		resolve a metrical insufficiency ‹corpus-suggested›.
	</p>
</S>

<S t="Register conditioning and co-occurring grammatical features" id="register">
	<p>
		Parallelism and couplets are confined to <i lang="ain-Latn">atomte itak</i> and are
		absent from prose folktale (<i lang="ain-Latn">uwepeker</i>) and everyday conversation.
		The register is not, however, a discrete switch: Nakagawa 2001a[1987]: 222 (via
		<Ref k="endo2022" p="§3" />) treats elegant and everyday language as points on a
		continuum defined by the degree of verse vs prose and of fixed vs improvised delivery;
		couplet density tracks this continuum, reaching its maximum in heroic epic ‹contested›.
		Kindaichi's hierarchy — greeting &lt; prayer &lt; incantation &lt;
		<i lang="ain-Latn">caranke</i> &lt; yukar, each richer in elegant language than the
		last (<Ref k="kindaichi1935" paren />, via <Ref k="endo2022" p="§3" />) — implies a
		comparable gradient of couplet density, but this has not been quantified on a
		genre-tagged corpus ‹speculative›.
	</p>
	<p>
		Parallelism co-occurs with the other grammatical hallmarks of <i lang="ain-Latn">atomte
		itak</i>: applicative case-marking by prefixes <i lang="ain-Latn">e-/o-/ko-</i> in place
		of case particles, longer polysynthetic words, number-agreement override (the singular
		verb with <i lang="ain-Latn">-an</i> for the 1SG hero-narrator), and a distinct elevated
		lexicon <Ref k="sato2008" p="263–264" />; <Ref k="endo2022" p="§3.4" />. The couplet is
		thus one component of a co-occurring cluster, not an isolated device. The full cluster is
		treated in <Xr ch="poetic-archaic-elevated-register" />; the number-agreement override
		belongs to <Xr ch="person-marking-architecture" />.
	</p>
	<p>
		Within verse narration, couplets typically accompany the fourth-person
		(<i lang="ain-Latn">a=/-an</i>) hero-narrator marking that indexes the logophoric
		first person. The interaction of this narrative convention with reported-speech structure
		is described in <Xr ch="logophoric-narration-and-reported-discourse" /> and
		<Xr ch="fourth-person-honorific" />. The evidential frame <i lang="ain-Latn">ruwe ne</i>,
		which predicates narrative events as established facts (see <Xr ch="ruwe-ne-inferential" />
		and <Xr ch="evidential-schema-overview" />), is the unmarked clause-final frame in
		verse narration and thus the primary syntactic environment in which couplets appear;
		the sentence-final particle inventory of verse is treated in
		<Xr ch="sentence-final-particles-illocutionary-force" />.
	</p>
</S>

<S t="Typological note" id="typological">
	<p>
		Paired-line organization of the type found in Ainu verse — a fixed syntactic frame
		with one or more varying lexical slots, without phonological matching as a constitutive
		requirement — is documented in many oral-poetic traditions worldwide. Whether the
		Hokkaido Ainu pattern shares an origin with neighboring North Pacific verse traditions
		or developed independently is an open question: no source read for this grammar
		addresses the diachrony or areality of the couplet device directly, and the primary
		comparative literature on verse parallelism lies outside the sources available here
		‹speculative›. The absence of a phonological requirement, confirmed for Hokkaido Ainu
		by <Ref k="endo2022" p="§4" /> and consistent with the contested evidence on rhyme and
		alliteration reviewed in <Ref k="okuda2022" p="§2.2" />, is in itself typologically
		diagnostic and merits future systematic comparison.
	</p>
</S>
