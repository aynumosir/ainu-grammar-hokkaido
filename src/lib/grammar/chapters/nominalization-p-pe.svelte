	/>
</S>

<S t="Arity and the boundary between the two uses" id="arity">
	<p>
		Nakagawa's valence arithmetic keeps the participant and complementizer uses from collapsing into
		one reading. A formal noun has arity −1, on a par with <i lang="ain-Latn">hi</i> and
		<i lang="ain-Latn">uske</i>; a clause that still requires a participant has positive arity, and
		the formal noun fills the open slot, giving a relative-clause NP; a clause of arity 0 is complete,
		and the formal noun nominalizes the whole of it, giving an event or fact nominal <Ref
			k="nakagawa2024"
			p="261–262, 504, 507"
		/>. The surface string <i lang="ain-Latn">[clause] p/pe</i> is identical in both; the
		grammatical relation differs.
	</p>

