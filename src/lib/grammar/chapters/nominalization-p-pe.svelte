<S t="Analyses and open questions" id="analyses">
	<p>
		The descriptions agree on the substance — <i lang="ain-Latn">p</i> ~ <i lang="ain-Latn">pe</i>
		builds lexical, phrasal, and clausal nominals, heads a generic participant in a relative clause,
		and nominalizes a saturated clause as an event or fact — and differ on the labels. Shibatani and
		Refsing use ‘nominalizer’ broadly; Satō separates the whole-clause nominalizer from the
		relative-clause-heading formal noun; Nakagawa makes the formal noun a word class and the
		complementizer a syntactic role available to some of its members; Bugaeva treats these light
		bound nouns as the generic heads of gapped relative clauses <Ref k="shibatani1990" p="33–34" />;
		<Ref k="refsing1986" p="259–264" />; <Ref k="sato2008" p="174–175" />; <Ref
			k="nakagawa2024"
			p="508"
		/>; <Ref k="bugaeva2015" p="47" /> ‹contested›.
	</p>

	<p>
		The constituency of the nominalized clause plus a following copula — the frame
		<i lang="ain-Latn">[clause] p/pe ne</i> — is the deeper disagreement, and it is the same one that
		runs through the evidential system. One line analyses the structure as biclausal, a formal or
		possessed noun heading the preceding clause and closed by the copula; another holds that the
		erstwhile noun is grammaticalizing toward a monoclausal complex predicate. No source read here
		offers a constituency proof that decides between them <Ref k="tamura2000" p="227" />; <Ref
			k="sato2008"
			p="175"
		/>; <Ref k="bugaeva2016" p="306–315" />; <Ref k="dalcorso2018" p="24–25" /> ‹contested›.
	</p>

	<p>
		Ijäs's observations on relative clauses fit this system, with two cautions. Her report that the
		subject of a ditransitive resists being the head matches Bugaeva's finding that no example of an
		A-of-three-place-verb head is attested; the construction is an attested gap rather than a settled
		ungrammaticality ‹corpus-confirmed›. Her difficulty in finding ablative
		<i lang="ain-Latn">wa(no)</i> ‘from which’ or mutative <i lang="ain-Latn">ne</i> ‘as which’ as
		relative-clause heads is shared by the sources read here, so the forms she suggests for them are
		left aside rather than presented as established ‹corpus-suggested› <Ref k="ijas2023" />; <Ref
			k="bugaeva2015"
			p="48"
		/>.
	</p>

	<p>
		The same nominalizer feeds clause linkage at the far end of the cline: a bleached
		<i lang="ain-Latn">p</i> ~ <i lang="ain-Latn">pe</i> works as an adversative conjunction
		‘though, and yet’, no longer an argument of the following clause, and the Horobetsu nominalizers
		grammaticalize a step further into adverbial-clause markers <Ref k="nakagawa2024" p="515–516" />;
		<Ref k="takahashi2018" p="107–112" />.
	</p>

	<p>
		Token frequency confirms that <i lang="ain-Latn">pe</i> is common but cannot measure this
		construction directly. The corpus word-frequency table returns 13,249 tokens of
		<i lang="ain-Latn">pe</i> (rank 15), a figure that folds together the formal noun, the numeral
		counter <i lang="ain-Latn">-pe</i>, and lexical <i lang="ain-Latn">pe</i> ‘water’, while the
		short allomorph <i lang="ain-Latn">p</i> is obscured by segmentation. A per-function count would
		require construction-level annotation ‹corpus-suggested›.
	</p>
</S>
