	<p>
		The form spans lexical derivation and syntax without a break. Shibatani describes
		<i lang="ain-Latn">-p(e)</i> as deriving a noun for a person or thing characterized by the verb,
		as in <i lang="ain-Latn">pirka-p</i> ‘good thing’ and <i lang="ain-Latn">ray-pe</i> ‘the dead’,
		and as a productive phrasal and clausal nominalizer besides
		<Ref k="shibatani1990" p="33–34" />. In Bugaeva's areal account of nominalization, Hokkaido
		expresses clausal nominalization through a nominalizing word placed after the clause, the type
		<i lang="ain-Latn">[(np…) V p/pe]</i>, a stage from which lexical items such as
		<A w="aep" gl="food" /> have crystallized; the Sakhalin varieties extended the same system to
		possessed verbal forms <Ref k="bugaeva2016" p="243, 385–397" />. Among the formal nouns,
		<i lang="ain-Latn">p</i> ~ <i lang="ain-Latn">pe</i> keeps a ‘thing/person’ profile beside the
		maximally abstract <i lang="ain-Latn">hi</i> and the sensory-evidential heads
		<i lang="ain-Latn">ruwe</i>, <i lang="ain-Latn">siri</i>, <i lang="ain-Latn">hawe</i>, and
		<i lang="ain-Latn">humi</i>, whose inventory and possessed morphology are set out in
		<Xr ch="formal-defective-nouns" /> and whose clause-final evidential use is treated in
		<Xr ch="evidential-schema-overview" />.
	</p>
</S>

<S t="Participant nominalization: the one who, the thing that" id="participant">
	<p>
		In its participant reading, <i lang="ain-Latn">p</i> ~ <i lang="ain-Latn">pe</i> is a generic
		head whose referent is fixed by the argument structure of the clause before it. The clause
		contains a gap, and the role of that gap — agent, patient, or an oblique participant — is
		recovered from the verb's valence and from any postposition left in place
		<Ref k="nakagawa2024" p="512" />; <Ref k="bugaeva2015" p="47" />. This is the headless edge of
		the relative-clause system: where an ordinary relative clause precedes a lexical head noun,
		<i lang="ain-Latn">p</i> ~ <i lang="ain-Latn">pe</i> supplies the head itself. The gap strategy,
		the accessibility hierarchy, and the postposition-retention facts are developed in
		<Xr ch="prenominal-gap-relative-clauses" />, and the place of the headless type within the wider
		typology of noun-modifying clauses in
		<Xr ch="headless-internally-headed-noun-modifying-clauses" />.
	</p>

	<Ex
		m="tapan kina a=e p ne toan kina somo a=e p ne"
		g="this plant 4.A=eat NMLZ COP that plant NEG 4.A=eat NMLZ COP"
		tr="This plant is one that people eat; that plant is one that people do not eat."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker 14 (first half) [aa-irc/020#27]"
		note="The fourth person a= gives a generic human eater; p is the object head of each clause — the thing eaten. The vowel-final e selects the short allomorph -p."
	/>

	<Ex
		m="a=se wa okay pe"
		g="4.A=carry.on.back CONJ exist.PL NMLZ"
		tr="the things one is carrying along"
		cite="nakagawa2024:512"
		dial="HK"
		note="pe is the object of se and the subject of okay; the consonant-final okay selects the allomorph -pe."
	/>

	<p>
		Lexicalized items preserve the syntax at word scale. <A w="aep" gl="food" /> is
		<i lang="ain-Latn">a=e-p</i>, ‘thing one eats’, with the fourth-person transitive prefix and the
		nominalizer <Ref k="bugaeva2016" p="245–251" />. Nakagawa's contrast between the synthesized
		<i lang="ain-Latn">a=e-suke-p</i> ‘kitchen utensil’ and the compound
		<i lang="ain-Latn">sukemakiri</i> ‘kitchen knife’ shows the diagnostic: the relative-clause
		nominalization keeps the applicative <i lang="ain-Latn">e-</i> that licenses the instrument it
		names, whereas a compound leaves the relation between its two nouns grammatically unspecified
		<Ref k="nakagawa2024" p="506–507" />. The lexicalized inventory is treated in
		<Xr ch="lexical-head-formal-noun-nominalization" />.
	</p>

	<Ex
		m="a=e-suke-p"
		g="4.A=APPL-cook-NMLZ"
		tr="kitchen utensil"
		lit="thing one cooks with"
		cite="nakagawa2024:507; kayano1996:281"
		dial="SAR"
		note="The applicative e- licenses the instrumental participant that p nominalizes; the plain compound sukemakiri has no such prefix."
	/>

	<p>
		An oblique participant is nominalized the same way, with the governing postposition retained
		inside the clause so that the relation between head and predicate stays overt. With an
		instrument the postposition <i lang="ain-Latn">ani</i> ‘with’ remains, and
		<i lang="ain-Latn">p</i> names the instrument <Ref k="nakagawa2024" p="504" />; <Ref
			k="bugaeva2015"
			p="47–49"
		/>; <Ref k="keenancomrie1977" paren />.
	</p>

	<Ex
		m="ani usey a=ku p"
		g="INS hot.water 4.A=drink NMLZ"
		tr="a thing to drink hot water with"
		cite="nakagawa2024:504"
		dial="HK"
		note="The instrumental ani stays inside the clause; p is the generic instrument head, the object of ku 'drink'."
	/>

	<p>
		Ijäs teaches the same rule for relative clauses generally: the modifying verb phrase precedes
		the head, there is no relative pronoun, and an oblique head keeps the postposition fronted within
		the clause <Ref k="ijas2023" />. Her examples are constructed teaching material and align with
		Nakagawa, Satō, and Bugaeva.
	</p>

	<Ex
		m="ani cep ku=tuypa makiri"
		g="INS fish 1SG.A=cut knife"
		tr="the knife I cut fish with"
		cite="ijas2023"
		constructed
		dial="HK"
		note="Ijäs Lesson 11. The instrumental ani is fronted and retained; the head here is the common noun makiri, the same gap strategy that p and pe carry without a lexical head."
	/>
</S>

<S t="Event and fact nominalization" id="event-fact">
	<p>
		When the clause before it is already saturated — every argument the verbs require is present —
		<i lang="ain-Latn">p</i> ~ <i lang="ain-Latn">pe</i> nominalizes the event or fact as a whole and
		serves as a complementizer rather than a participant head. Nakagawa treats some formal nouns,
		<i lang="ain-Latn">p</i> ~ <i lang="ain-Latn">pe</i> among them, as filling this complementizing
		role; Satō draws the line explicitly between a nominalizer (<span lang="ja">名詞化辞</span>) that
		turns a whole content clause into a noun phrase and a formal noun that heads a relative clause,
		the first allowing no reconstruction of an underlying sentence <Ref
			k="nakagawa2024"
			p="503–509, 512–513"
		/>; <Ref k="sato2008" p="174–180" />.
	</p>

	<Ex
		m="ne kusu haweoka wa cis pa p ka a=erampewtek"
		g="COP because speak.PL CONJ cry PL NMLZ even 4.A=not.understand"
		tr="I did not even understand why they were crying."
		cite="nakagawa2024:512–513"
		dial="HK"
		note="p is not an argument of speak or cry; the clause haweoka wa cis pa is complete (arity 0). p nominalizes the whole reason clause, which is then the object of a=erampewtek."
	/>

	<Ex
		m="ratcitara rok=an pe ne na"
		g="quietly sit=4.S NMLZ COP FIN"
		tr="One should sit quietly."
		cite="sato2008:177"
		dial="CHI"
		note="pe nominalizes a saturated intransitive; the predication pe ne na carries the deontic 'should' reading of Satō's content-clause use."
	/>

	<p>
		In this use <i lang="ain-Latn">p</i> ~ <i lang="ain-Latn">pe</i> stands beside the other
		complementizing formal nouns — abstract <i lang="ain-Latn">hi</i> and the sensory heads — which
		differ in the construal they impose. The complement-taking predicates and the control and
		raising diagnostics are treated in <Xr ch="nominalized-complements-control-raising" />; the
		future-oriented sequence <i lang="ain-Latn">kuni p</i> in
		<Xr ch="kuni-complementizer-purpose-complements" />; the quotative complementizer
		<i lang="ain-Latn">sekor</i> and the indirect-speech markers in
		<Xr ch="quotative-complementation-sekor" />; and the clause-final evidential reflexes of the
		sensory heads in <Xr ch="ruwe-ne-inferential">the inferential <i lang="ain-Latn">ruwe ne</i></Xr>.
	</p>

	<Ex
		m="a=e=túskote kuni p ne ru-we ne na"
		g="4.A=2SG.O=tie.up PURP NMLZ COP track-POSS COP FIN"
		tr="You will surely be tied up."
		cite="ilcaa1976"
