<S t="Form and place among the formal nouns" id="form">
	<p>
		The nominalizer <i lang="ain-Latn">p</i> ~ <i lang="ain-Latn">pe</i> turns a finite clause into a
		noun phrase. It is a member of the closed class of
		<Xr ch="formal-defective-nouns">formal nouns</Xr> — light, weakly autonomous nouns placed after
		another element to build an NP — and it supplies a generic nominal head meaning, in its concrete
		uses, ‘the one who’ or ‘the thing that’. Hokkaido Ainu has no participial or otherwise
		non-finite verb form for this work: the verb inside the nominalized phrase keeps its ordinary
		person marking, polarity, number, and clause-internal morphology, and subordination is signalled
		by the following formal noun rather than by any change in the verb
		<Ref k="bugaeva2016" p="100, 226–228" />; <Ref k="nakagawa2024" p="503, 512–513" />; <Ref
			k="sato2008"
			p="174–175"
		/>.
	</p>

	<p>
		The allomorphy is phonologically conditioned: the short form <i lang="ain-Latn">-p</i> follows a
		stem-final vowel, and <i lang="ain-Latn">-pe</i> follows a stem-final consonant. The same split
		holds whether the host is a single stative stem (<i lang="ain-Latn">poro-p</i> ‘the big one’,
		<i lang="ain-Latn">pon-pe</i> ‘the small one’) or a full clause (<i lang="ain-Latn">a=e p</i>
		‘what one eats’, <i lang="ain-Latn">okay pe</i> ‘what one carries’)
		<Ref k="sato2008" p="175" />; <Ref k="refsing1986" p="260" />; <Ref k="bugaeva2022" p="ch. 14" />.
		Satō records a sandhi variant <i lang="ain-Latn">pa</i> at the junction of
		<i lang="ain-Latn">p</i> and a following conjunctive <i lang="ain-Latn">wa</i>
		<Ref k="sato2008" p="175" />.
	</p>

