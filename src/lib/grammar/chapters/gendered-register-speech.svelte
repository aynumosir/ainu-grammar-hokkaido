	<p>
		Grammatically, the <i lang="ain-Latn">menoko yukar</i> is not differentiated from the
		male-narrator <i lang="ain-Latn">yukar</i> by any morphological marker of the narrator's sex.
		Both use the fourth-person narrator voice (<i lang="ain-Latn">a=</i>/<i lang="ain-Latn">=an</i>)
		and the elevated register (<A w="atomte-itak" gl="ornamented/elegant language" />) in the
		same way <Ref k="endo2022" />; <Ref k="sato2008" p="259" />. The narrator fourth person and
		its logophoric function in oral epic are described in
		<Xr ch="logophoric-narration-and-reported-discourse" />; the elevated register and its
		grammatical hallmarks in <Xr ch="poetic-archaic-elevated-register" />.
	</p>
</S>

<S t="Gendered ritual speech-acts" id="ritual-speech">
	<p>
		Two sets of thanks-expressions and accompanying gestures are sex-differentiated in
		traditional practice. When receiving food or a gift, women say
		<A w="hinna" gl="thanks/gratitude" /> (lower-Saru <i lang="ain-Latn">hunna</i>) while
		performing <A w="raymik" gl="index-finger nose-stroke gesture" />, stroking below the nose
		with the index finger; men perform <A w="onkami" gl="reverential bow with both hands" />
		without this interjection. A lesser degree of gratitude uses <i lang="ain-Latn">hioy'oy</i>;
		a stronger reduplicated form <i lang="ain-Latn">hinna hinna</i> marks stronger feeling
		(scale: <i lang="ain-Latn">hioy'oy</i> &lt; <i lang="ain-Latn">hinna hinna</i>). Formal
		thanks in ceremonial contexts uses <A w="iyayiraykere" />, applicable by either sex
		<Ref k="aynucorporadiscord" /> (nukopoli, aynu-corpora Discord, 2024-12-26; asserted; consistent
		with Endō's account of gendered oral-literature performance <Ref k="endo2022" />) ‹corpus-suggested›.
	</p>

	<p>
		The greeting <A w="irankarapte" /> was traditionally a man's formula, delivered with
		<i lang="ain-Latn">onkami</i>. In the revitalization period it has come into gender-neutral
		use, recognized by speakers as a shift from traditional practice
		<Ref k="bugaeva2022" />. These gendered formulae belong to the broader system of deferential
		address that includes the honorific fourth-person forms and prayer-specific epithets for the
		deities, treated in <Xr ch="honorific-ritual-and-taboo-registers" /> and
		<Xr ch="fourth-person-honorific" />.
	</p>
</S>

<S t="Sentence-final particles and gendered interactional register" id="sfp-gender">
	<p>
		Whether specific sentence-final particles carry a sex-of-speaker feature in Hokkaido Ainu
		— a structural parallel to the Japanese feminine <span lang="ja">わ</span> versus masculine
		<span lang="ja">ぞ</span>/<span lang="ja">ぜ</span> — cannot be answered from the sources
		read for this chapter. Satō <Ref k="sato2008" />, Endō <Ref k="endo2022" />, and Nakagawa
		<Ref k="nakagawa2024" /> supply no systematic gender-indexed inventory of sentence-final
		particles for Hokkaido Ainu, and a search of the grammar literature yielded no attested
		sex-conditioned particle pair ‹speculative›. For the pedagogical grammar, Ijäs likewise
		addresses Hokkaido speech norms without specifying gendered SFP contrasts
		<Ref k="ijas2023" />.
	</p>

	<p>
		Three primary sources that might bear on this question were not read for this chapter:
		Fukuda's (Tamura's) 1961 description of the Saru SFP system
		<Ref k="fukuda1961" />, Kindaichi's writings on elegant-language and
		genre-specific grammar <Ref k="kindaichi1935" />, and Hattori's 1957 monograph on the
		'special language of the older generations' (<em>nenchōshasō tokushugo</em>), the primary
		source on age- and register-conditioned lexical repertoires. These constitute the main
		empirical gap. Any future study would need to work from conversational recordings, which
		are sparse in the available Hokkaido documentation relative to the predominantly
		oral-literary aligned corpus.
	</p>

	<p>
		The SFP inventory — <A w="na" />, <A w="wa" />, <A w="ne" />, <A w="ya" />,
		<A w="he" /> and their illocutionary values — is treated without sex-of-speaker
		stratification in <Xr ch="sentence-final-particles-illocutionary-force" />. The
		evidential formal nouns that grammaticalize into sentence-final positions
		(<i lang="ain-Latn">ruwe</i>, <i lang="ain-Latn">hawe</i>) are covered in
		<Xr ch="evidential-schema-overview" />.
	</p>
</S>

<S t="Honorific and age-conditioned registers" id="honorific-age">
	<p>
		Age- and status-conditioned register is distinct from sex-of-speaker conditioning and is
		better documented. The fourth-person affix set (<i lang="ain-Latn">a=</i>/<i lang="ain-Latn">=an</i>)
		functions as a second-person honorific in respectful address, drawing on the same forms that
		serve as the narrator's voice in oral epic <Ref k="sato2008" p="261" />. Satō lists the
		elegant-language pronoun set — <i lang="ain-Latn">asinuma</i>, <i lang="ain-Latn">aoka(y)</i>,
		<i lang="ain-Latn">eani</i>, <i lang="ain-Latn">ecioka(y)</i> — as drawn from the
		fourth-person paradigm and used in contexts of respectful address without sex-specificity
		<Ref k="sato2008" p="261" />. The full treatment of these honorific-fourth-person forms is
		in <Xr ch="fourth-person-honorific" />; their intersection with kinship-term morphology in
		<Xr ch="kinship-honorific-possession" />.
	</p>

	<p>
		Kindaichi's genre-density ranking — the density of elegant-language features increases from
		greetings through prayer and incantation to negotiation (<A w="caranke" gl="formal dispute-resolution oratory" />)
		and peaks in heroic verse <Ref k="kindaichi1935" /> (via <Ref k="endo2022" />) — overlaps
		with the narrator-sex allocation above: prayer and heroic verse, the male-allocated genres,
		occupy the high-density end of the scale. The elegant register (<i lang="ain-Latn">atomte
		itak</i>) and its grammatical hallmarks — applicative case-marking by prefix rather than
		particle, number-agreement override, and the distinct poetic lexicon — are described in
		<Xr ch="poetic-archaic-elevated-register" />. The register is not itself sex-indexed: a
		narrator of either sex performing a genre that uses <i lang="ain-Latn">atomte itak</i>
		uses the same elevated morphology ‹consensus›.
	</p>
</S>

<S t="Reliability of the record and open questions" id="record-reliability">
	<p>
		The gendered-register evidence for Hokkaido Ainu stratifies by quality. Genre allocation by
		narrator sex rests on genre tables, fieldwork descriptions, and native metalinguistic
		statements extending from early twentieth-century documentation through Satō's Chitose-based
		grammar <Ref k="sato2008" p="259" /> and Endō's Handbook synthesis <Ref k="endo2022" />; it
		is the most robustly documented layer. The ritual speech-act differentiation
		(<i lang="ain-Latn">hinna</i>/<i lang="ain-Latn">onkami</i>) is attested in community
		discussion consistent with the ethnographic picture but would benefit from independent
		primary documentation. Sentence-final particle gender-indexing remains open and
		unconfirmed ‹speculative›.
	</p>

	<p>
		Two further documentary gaps limit what can be said. First, Hattori 1957 on the elders'
		special register — the key inventory of age- and sex-conditioned non-everyday vocabulary —
		is not held in the project repository and was not read; Fukuda/Tamura 1961
		<Ref k="fukuda1961" /> on Saru SFPs and Kindaichi <Ref k="kindaichi1935" /> on elegant
		language are held but were not read for this chapter. Second, the aligned Hokkaido corpus
		is overwhelmingly oral-literary and genre-coded, so conversational distribution of any
		potential gendered particle cannot be quantified from it directly. Together these gaps mean
		that a strong claim about sex-indexed particles or a full inventory of gendered register
		features in Hokkaido Ainu remains beyond what the present evidence supports.
	</p>

	<p>
		Revitalization-era usage may be leveling some traditional allocations. The
		<i lang="ain-Latn">irankarapte</i> greeting is the clearest documented case; it is
		possible but undocumented that other sex-specific formulae are similarly shifting
		<Ref k="bugaeva2022" /> ‹speculative›. The sociolinguistic context of the revitalization
		is treated in the opening chapter of the grammar.
	</p>
</S>
