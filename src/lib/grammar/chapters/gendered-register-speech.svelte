	<p>
		(After <Ref k="sato2008" p="259" /> and <Ref k="endo2022" />.) The allocation is not
		symmetrically two-way. Several genres are open to performers of either sex; the two genres
		allocated exclusively to men are heroic verse (<i lang="ain-Latn">yukar</i>) and prayer
		(<i lang="ain-Latn">kamuy nomi</i>). Prayer is improvised with a personal melody and
		structurally distinct from the fixed, shared incantation, which any speaker may perform <Ref k="endo2022" />.
	</p>

	<p>
		Genre norms operated as conventions, and their social character is clear from modern practice: Endō notes that modern revitalization
		recordings document women reciting verse <i lang="ain-Latn">yukar</i>; the performers
		themselves recognized this as a departure from tradition <Ref k="endo2022" />. The grammar
		and genre taxonomy of each form, including the narrator-person conventions and the
		<i lang="ain-Latn">sakehe</i> refrain, are treated in <Xr ch="oral-literature-genre-taxonomy" />,
		<Xr ch="sakehe-refrain-and-sung-verse-structure" />, and <Xr ch="heroic-and-divine-verse" />.
	</p>
</S>

<S t="The menoko yukar — women's heroic verse" id="menoko-yukar">
	<p>
		The <A w="menokoyukar" gl="women's heroic verse" /> retains the verse architecture of the
		standard <i lang="ain-Latn">yukar</i> — notably the <A w="sakehe" gl="refrain" /> — while
		replacing the battle theme with marriage. Its female protagonist holds the same superhuman
		capacities as the male hero of the standard epic, including <A w="tusu" gl="shamanistic foresight and revival power" />,
		but the narrative turns on a marriage outcome <Ref k="endo2022" />. The genre is documented
		for mid-Saru (沙流中流); its presence in other Hokkaido regions is not established from the
		sources read for this chapter ‹speculative›.
	</p>

	<p>
		A Saru speaker's metalinguistic testimony illustrates the sakehe distinction. 川上まつ子 described the <i lang="ain-Latn">menoko yukar</i> as a genre one
		properly performs with the <i lang="ain-Latn">sakehe</i>, and noted that she was narrating
		one without it — treating it as an <i lang="ain-Latn">uwepeker</i> — and that with the
		sakehe the performance would be long, whereas without it, short (AA研アイヌ語資料
		「menokoyukar 神謡6（節なし）」, 沙流, aa-irc/019, ll. 0, 66). This metalinguistic testimony
		aligns with Satō's genre-feature table <Ref k="sato2008" p="259" />: the
		<i lang="ain-Latn">sakehe</i> defines the genre boundary, not the thematic content. A
		speaker switching off the refrain performs the same story in a different register.
	</p>

