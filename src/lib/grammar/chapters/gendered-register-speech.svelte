<S t="Genre allocation and narrator sex" id="genre-allocation">
	<p>
		The most thoroughly documented gender dimension of Hokkaido Ainu is the performance norm that
		distributes oral-literary genres by the sex of the traditional narrator. Satō's table for
		Chitose specifies narrator sex as a genre-diagnostic parameter alongside melody,
		<A w="sakehe" gl="refrain" />, and rhythm-tapping <Ref k="sato2008" p="259" />; Endō's
		synthesis confirms the same allocation across the Hidaka–Iburi region and notes that the
		norm applies to both the content and the formal-poetic apparatus of each genre
		<Ref k="endo2022" />.
	</p>

