<S t="The pair-verb frame and the existential auxiliary" id="overview">
	<p>
		Two periphrastic constructions recruit the existential verbs
		<i lang="ain-Latn">an</i> (singular) and <i lang="ain-Latn">oka</i> /
		<i lang="ain-Latn">okay</i> (plural) as the second predicate in a clause chain. The
		simultaneous frame <i lang="ain-Latn">V kor an</i> marks an action or change in progress
		and habitual situations; the sequential frame <i lang="ain-Latn">V wa an</i> marks the
		state that persists after a change has been completed
		<Ref k="nakagawa2024" p="439–442" />; <Ref k="sato2008" p="195–199" />;
		<Ref k="bugaeva2012" p="494–495" />.
	</p>
	<p>
		Both constructions belong to the <Xr ch="tam-overview">pair-verb frame</Xr>: each predicate
		retains its own person marking and its status as a finite verb. This finite independence is
		the diagnostic that distinguishes them from fully grammaticalized aspect suffixes, and it is
		the basis on which Nakagawa treats <i lang="ain-Latn">kor an</i> and
		<i lang="ain-Latn">wa an</i> as forms that express aspect-like meaning rather than as
		grammatical aspect proper <Ref k="nakagawa2024" p="442" />. Yoshikawa develops the same
		point under the cover term <em>existential aspectual forms</em>
		(<span lang="ja">存在型アスペクト形式</span>): <i lang="ain-Latn">an</i> here is less
		grammaticalized than Japanese <span lang="ja">いる</span>/<span lang="ja">ある</span>,
		retaining its concrete existential sense, and the cline from lexical existential to
		aspectual auxiliary has advanced without reaching obligatory inflectional morphology
		<Ref k="yoshikawa2022" />; <Ref k="bybee1994" paren /> ‹contested›.
	</p>
	<p>
		The locational and existential uses of <i lang="ain-Latn">an</i> and
		<i lang="ain-Latn">oka</i> are covered in <Xr ch="existential-locational-an-oka" />. The
		suppletive SG/PL alternation underlying the pair belongs to
		<Xr ch="suppletive-verbal-number" />. The conjunctive particles
		<i lang="ain-Latn">kor</i> 'while' and <i lang="ain-Latn">wa</i> 'and then' are analysed in
		their wider clause-linking roles in <Xr ch="simultaneous-kor-clauses" /> and
		<Xr ch="sequential-wa-and-clause-chaining" />. The merged and extended continuatives
		<i lang="ain-Latn">kane an</i>, <i lang="ain-Latn">kan an</i>,
		<i lang="ain-Latn">tek an</i>, and <i lang="ain-Latn">hine an</i> belong to
		<Xr ch="continuative-siri-siran-kane-an" />, and the actional-perfect auxiliary
		<i lang="ain-Latn">a</i>/<i lang="ain-Latn">rok</i> to <Xr ch="perfective-anterior-a" />.
	</p>
</S>

<S t="The suppletive existential as auxiliary" id="suppletive-auxiliary">
	<p>
		<i lang="ain-Latn">an</i> and <i lang="ain-Latn">oka</i> form one of the suppletive
		singular/plural verb pairs that the Hokkaido verb system maintains: the singular stem is
		<i lang="ain-Latn">an</i>, the plural <i lang="ain-Latn">oka</i> (realized as
		<i lang="ain-Latn">okay</i> in many phonological environments). The two are in strict
		complementary distribution by the number of the controlling argument
		<Ref k="nakagawa2024" p="142–145" />; <Ref k="bugaeva2012" p="471" />;
		<Ref k="ijas2023" /> ‹consensus›.
	</p>
	<p>
		In the aspectual periphrases this suppletion carries over directly: the auxiliary tracks
		the same argument that would trigger the choice between the lexical forms. For
		<i lang="ain-Latn">kor an</i> and the standard intransitive pattern of
		<i lang="ain-Latn">wa an</i>, that argument is the shared subject; in the
		transitive object-agreement pattern of <i lang="ain-Latn">wa an</i>, it is the object.
		The preserved SG/PL alternation is consistent with the partial-grammaticalization
		assessment shared by Nakagawa, Yoshikawa, and Bugaeva: a fully fused aspect suffix
		typically loses lexical alternations of this kind. Ijäs catalogues <i lang="ain-Latn">an</i>/<i lang="ain-Latn">oka</i>
		as one of several suppletive pairs — alongside <i lang="ain-Latn">a</i>/<i lang="ain-Latn">rok</i>
		'sit', <i lang="ain-Latn">ek</i>/<i lang="ain-Latn">arki</i> 'come', and
		<i lang="ain-Latn">arpa</i>/<i lang="ain-Latn">paye</i> 'go' — whose number suppletion
		extends into grammaticalized contexts <Ref k="ijas2023" />;
		<Ref k="nakagawa2024" p="142–151" /> ‹consensus›.
	</p>
</S>

<S t="kor an: progressive, habitual, and inceptive" id="kor-an">
	<p>
		The construction <i lang="ain-Latn">V kor an</i> (plural: <i lang="ain-Latn">V kor oka</i>)
		joins the lexical verb to the simultaneous conjunctive particle <i lang="ain-Latn">kor</i>
		'while' and the existential verb. With <strong>action verbs</strong> — activities without an
		inherent change of state (<i lang="ain-Latn">apkas</i> 'walk', <i lang="ain-Latn">mina</i>
		'laugh', <i lang="ain-Latn">sinot</i> 'play') — the construction marks the action as
		in progress at the reference time. With <strong>change verbs</strong> — predicates whose
		completion involves a change of state or location (<i lang="ain-Latn">as</i> 'stand up',
		<i lang="ain-Latn">hacir</i> 'fall', <i lang="ain-Latn">ek</i> 'come') — it marks the
		process leading toward the change, not the resulting state, which is the domain of
		<i lang="ain-Latn">wa an</i>
		<Ref k="nakagawa2024" p="440–441" />; <Ref k="sato2008" p="196" />;
		<Ref k="bugaeva2012" p="494" />.
	</p>

	<Ex
		m="yukkoterke kor an ayne"
		g="leap.at.deer while exist then"
		tr="while (he) was springing upon the deer —"
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, isoytak monologue 1 [aa-irc/003#64]"
		note="kor an with an action verb (yukkoterke, incorporating the noun yuk 'deer'); the progressive marks the action as in progress at a narrative reference point."
	/>

	<p>
		The construction also covers <strong>habitual and repeated</strong> situations — the type
		that Japanese translates with <span lang="ja">〜している</span> in iterative contexts — and
		with change verbs approaching their culmination, an <strong>inceptive</strong> reading
		('it is getting light'; Saru <i lang="ain-Latn">epuykehe tane sipuske kor oka</i>
		'the buds are now about to open')
		<Ref k="sato2008" p="197" />; <Ref k="nakagawa2024" p="441" />;
		<Ref k="bugaeva2012" p="494" />.
	</p>

	<Ex
		m="sittokap kor pon seta turano sinot=an kor an=an ayne"
		g="noon when small dog with play=4.S while exist=4.S then"
		tr="at noon I would always play with the puppy, and"
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker 9 [aa-irc/009#14]"
		note="Habitual value set by the temporal clause sittokap kor 'whenever it was noon'. Both sinot and the existential an carry the fourth-person suffix =an, the pair-verb signature (§agreement below)."
	/>

	<p>
		When the subject is plural, <i lang="ain-Latn">an</i> is replaced by
		<i lang="ain-Latn">oka</i>. The generic plural marker <i lang="ain-Latn">pa</i> may
		follow the main verb and precede the conjunctive:
	</p>

	<Ex
		m="uenewsar pa kor oka"
		g="chat PL while exist.PL"
		tr="they are chatting"
		cite="nakagawa2024:441"
		dial="SAR"
		note="Plural subject: oka replaces an in the auxiliary slot. This is the predicate of Nakagawa's ex. 983; the full sentence is a=onaha turano uenewsar pa kor oka 'they are chatting with my father'."
	/>

	<S t="Person agreement in kor an" id="kor-an-agreement">
		<p>
			Because <i lang="ain-Latn">kor an</i> is a chain of two finite clauses, the lexical verb
			and the existential each take a person index independently, and both must agree with the
			shared subject. The co-indexation is the clearest diagnostic of the pair-verb structure:
			in the example above, the fourth-person suffix <i lang="ain-Latn">=an</i> appears on
			both <i lang="ain-Latn">sinot</i> and the existential. The same holds with overt
			second-person prefixes:
		</p>

		<Ex
			m="hemanta e=ciskar kor e=an"
			g="what 2SG.S=cry while 2SG.S=exist"
			tr="What are you crying about?"
			cite="nakagawa2024:442"
			dial="SAR"
			note="The second-person prefix e= prefixes both ciskar 'cry' and the existential an; two independently indexed finite verbs joined by kor."
		/>
	</S>

	<S t="The Tokachi first-person restriction" id="tokachi">
		<p>
			In Tokachi, <i lang="ain-Latn">kor an</i> cannot co-occur with a first-person subject.
			The progressive of a first-person event uses <i lang="ain-Latn">kan an</i> instead:
			<i lang="ain-Latn">ku=nukar kan ku=an</i> 'I am watching it' is grammatical where
			<i lang="ain-Latn">*ku=nukar kor ku=an</i> is not. Takahashi proposes that Tokachi
			<i lang="ain-Latn">kor an</i> carries an observational component — presenting an event
			as witnessed by the speaker — that is incompatible with the speaker being the agent
			performing the action <Ref k="takahashi2022" /> ‹contested›. The restriction is
			specific to Tokachi; no equivalent is reported for Saru, Chitose, or the remaining
			Hokkaido varieties. The distribution of <i lang="ain-Latn">kan an</i> is covered in
			<Xr ch="continuative-siri-siran-kane-an" />.
		</p>
	</S>
</S>

<S t="wa an: the resultative" id="wa-an">
	<p>
		The construction <i lang="ain-Latn">V wa an</i> (plural: <i lang="ain-Latn">V wa oka</i>)
		joins the lexical verb to the sequential conjunctive particle <i lang="ain-Latn">wa</i> and
		the existential verb, denoting the state that exists after the completion of a change. The
		canonical environment is a <strong>change verb</strong>: the nearest Japanese equivalents
		are <span lang="ja">〜ている</span> in its result-state reading and
		<span lang="ja">〜てある</span>
		<Ref k="nakagawa2024" p="439" />; <Ref k="sato2008" p="197–198" />;
		<Ref k="bugaeva2012" p="494" />; <Ref k="sato2006" />.
	</p>

	<Ex
		m="ek wa an pe hoppa kor"
		g="come CONJ exist NMLZ send.away CONJ"
		tr="taking away what had arrived, and"
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker 8 (1) [aa-irc/006#146]"
		note="ek wa an 'has come and is now here': the resultative of the motion-change verb ek. The nominalized clause ek wa an pe 'the thing that has arrived' functions as the object of hoppa."
	/>

	<p>
		The motion verb <i lang="ain-Latn">ek</i> provides the clearest minimal pair, because it
		enters both frames with distinct readings:
	</p>

	<Ex
		id="ek-kor-an"
		m="ek kor an"
		g="come while exist"
		tr="is coming / is on the way"
		cite="nakagawa2024:441"
		note="kor an with ek: the change of location is in progress, the arrival not yet complete. Nakagawa ex. 986."
	/>

	<Ex
		id="ek-wa-an"
		m="ek wa an"
		g="come CONJ exist"
		tr="has arrived and is (now) here"
		cite="nakagawa2024:441"
		note="wa an with ek: the motion is complete and the resultant state — being present at the destination — persists. Nakagawa ex. 987."
	/>

	<p>
		<strong>Stative verbs</strong> enter <i lang="ain-Latn">wa an</i> under their inchoative
		reading. The verb <A w="poro" gl="be big; become big" /> 'be big / become big', read as
		'become big', takes <i lang="ain-Latn">wa an</i> to describe the reached state:
		<i lang="ain-Latn">poro wa an</i> 'has grown / is now big'
		<Ref k="nakagawa2024" p="440" />. <strong>Action verbs</strong> — activities without an
		inherent endpoint — are excluded, since they leave no stable result for the existential
		to predicate: <i lang="ain-Latn">*tapkar wa an</i> (from <A w="tapkar" gl="dance" />) and
		<i lang="ain-Latn">*uenewsar wa an</i> are ungrammatical
		<Ref k="nakagawa2024" p="441" />. Their completion falls to the auxiliary
		<i lang="ain-Latn">a</i>, treated in <Xr ch="perfective-anterior-a" />.
	</p>

	<Ex
		m="isepo ray wa an"
		g="hare die CONJ exist"
		tr="The hare is dead."
		constructed
		cite="sato2008:195; bugaeva2012:495"
		note="Satō's constructed diagnostic (Bugaeva ex. 72f). The punctual change verb ray 'die' yields a stable result state; *isepo ray kor an is bad — death is not an ongoing process."
	/>

	<p>
		Satō extends the action-verb exclusion into a complementary-distribution claim: the actional
		perfect — completion of an action, without implying a residual state — belongs exclusively to
		the auxiliary <i lang="ain-Latn">a</i>, while <i lang="ain-Latn">wa an</i> covers only
		stative perfects (a result state persists) <Ref k="sato2006" p="63" />;
		<Ref k="sato2008" p="198" /> ‹corpus-suggested›. Bugaeva concurs that a separate
		auxiliary-based construction handles the actional perfect
		<Ref k="bugaeva2012" p="493, 495" />.
	</p>

	<S t="mokor wa an: sleep as a result-state" id="mokor-wa-an">
		<p>
			Nakagawa draws attention to <i lang="ain-Latn">mokor wa an</i> 'is asleep' as a case
			where Ainu forces a choice that Japanese leaves ambiguous. The verb
			<A w="mokor" gl="sleep, fall asleep" /> is construed as a change of state — the act of
			falling asleep — rather than as an ongoing activity, so the result state (being asleep)
			is what <i lang="ain-Latn">wa an</i> predicates. A progressive reading under
			<i lang="ain-Latn">kor an</i> is unavailable for this verb
			<Ref k="nakagawa2024" p="444" />. Japanese <span lang="ja">眠っている</span> is
			ambiguous between the progressive and the resultative; West Japanese dialects resolve
			the same ambiguity formally, with <span lang="ja">-shiyoru</span> (progressive) and
			<span lang="ja">-shitoru</span> (resultative), paralleling the Ainu distinction.
		</p>

		<Ex
			m="mokor wa an"
			g="sleep CONJ exist"
			tr="is asleep"
			cite="nakagawa2024:444"
			dial="SAR"
			note="Nakagawa ex. 998. Only the resultant-state reading is available: Ainu construes sleep as the result of falling asleep, not as an ongoing activity."
		/>
	</S>

	<S t="Person agreement in wa an" id="wa-an-agreement">
		<p>
			As in <i lang="ain-Latn">kor an</i>, both predicates in the chain carry independent
			person marking. For <i lang="ain-Latn">wa an</i>, however, the controller of the
			existential depends on transitivity and which argument holds the resultant state
			<Ref k="nakagawa2024" p="443" />; <Ref k="sato2008" p="198" />:
		</p>

		<table>
			<thead>
				<tr>
					<th>pattern</th>
					<th>condition</th>
					<th>controller of <i lang="ain-Latn">an</i></th>
					<th>illustrative form</th>
					<th>gloss</th>
				</tr>
			</thead>
			<tbody>
				<tr>
					<td>(a) intransitive</td>
					<td>V intransitive; subject persists in resultant state</td>
					<td>S of V</td>
					<td><i lang="ain-Latn">hotke=an wa an=an</i></td>
					<td>'I am / was lying down'</td>
				</tr>
				<tr>
					<td>(b) transitive, agent</td>
					<td>V transitive; agent = pivot of <i lang="ain-Latn">an</i></td>
					<td>A of V</td>
					<td><i lang="ain-Latn">a=ehotke wa an=an</i></td>
					<td>'I am lying on it'</td>
				</tr>
				<tr>
					<td>(c) transitive, object</td>
					<td>V transitive; object holds resultant state</td>
					<td>O of V</td>
					<td><i lang="ain-Latn">a=o wa an</i></td>
					<td>'(I) have put it in — it is in there'</td>
				</tr>
			</tbody>
		</table>

		<p>
			In (a) and (b), both predicates bear the same person index, exactly as in
			<i lang="ain-Latn">kor an</i>. In (c) — the construction closest to Japanese
			<span lang="ja">〜てある</span> — the agent's action is complete and what persists is
			the object in its new state; the existential <i lang="ain-Latn">an</i> appears bare
			when the object is third-person or inanimate, because no explicit person prefix is
			required <Ref k="nakagawa2024" p="443" />; <Ref k="sato2008" p="198" />;
			<Ref k="bugaeva2012" p="495" />.
		</p>

		<Ex
			m="a=o wa an"
			g="4.A=put.in CONJ exist"
			tr="(I) have put it in — it is in there."
			cite="nakagawa2024:443"
			dial="SAR"
			note="Object-agreement pattern (c): an indexes the object (the thing placed), not the fourth-person agent a=. The agent's action is presupposed; what the construction asserts is the object's resultant state (Nakagawa exx. 996–997)."
		/>
	</S>
</S>

<S t="Perception and cognition verbs" id="perception">
	<p>
		Verbs of perception and cognition — <A w="nukar" gl="see, look at" />,
		<A w="eramuan" gl="understand" />, and <A w="nu" gl="hear" /> — take either construction,
		and the contrast between <i lang="ain-Latn">nukar kor an</i> and
		<i lang="ain-Latn">nukar wa an</i> is the main analytical locus of debate within this domain.
	</p>
	<p>
		Nakagawa's 1981 analysis attributed the distinction to the degree of active involvement of
		the perceiver: <i lang="ain-Latn">nukar kor an</i> is active, deliberate looking, while
		<i lang="ain-Latn">nukar wa an</i> is a passive coming-into-view of a scene
		<Ref k="nakagawa2024" p="445" />. Satō (2006) challenges this characterization. In his
		account the primary contrast is between continuation of the perceiving action and
		continuation of the post-acquisition knowledge state: <i lang="ain-Latn">nukar kor an</i>
		= 'is in the act of looking' and <i lang="ain-Latn">nukar wa an</i> = 'has seen it and
		now holds that knowledge'. Any impression of deliberateness is a contextual by-product of
		the action/result distinction rather than its basis
		<Ref k="sato2006" p="65" />; <Ref k="sato2008" p="199" />. Nakagawa concedes in the 2024
		edition that the question warrants further investigation
		<Ref k="nakagawa2024" p="446" /> ‹contested›.
	</p>
	<p>
		Yoshikawa adds a structural caution: for these verbs, the line between "continuation of an
		aspectual relation" and "a coincidental co-occurrence of two situations" is genuinely unclear,
		and the ambiguity is part of the broader argument that these constructions occupy a gradient
		between clause chaining and grammaticalized aspect
		<Ref k="yoshikawa2022" /> ‹contested›.
	</p>
</S>

<S t="Grammaticalization status and typological framing" id="grammaticalization">
	<p>
		The cross-linguistic grammaticalization of an existential or locative verb into a progressive
		or resultative auxiliary is well-attested typologically
		<Ref k="bybee1994" paren />; <Ref k="comrie1976" paren />. Hokkaido Ainu represents a
		mid-stage on that cline. Three properties mark the constructions as only partially
		grammaticalized ‹consensus›:
	</p>
	<p>
		First, the source verb is morphologically and syntactically present — both predicates remain
		independently finite and indexed. Second, the suppletive number alternation
		<i lang="ain-Latn">an</i>/<i lang="ain-Latn">oka</i> is preserved, a property
		typically lost when aspect auxiliaries fuse into bound morphemes. Third, aspect is not an
		obligatory grammatical category in Ainu: the constructions can be omitted without
		ungrammaticality, unlike the obligatory aspect marking of heavily aspect-marking languages
		<Ref k="takahashi2022" />; <Ref k="yoshikawa2022" />.
	</p>
	<p>
		In Bugaeva's typology of Ainu periphrastic frames, the pair-verb construction is
		distinguished from the auxiliary-verb frame (hosting <i lang="ain-Latn">a</i>,
		<i lang="ain-Latn">nankor</i>, <i lang="ain-Latn">rusuy</i>, and their kin) precisely by
		this double indexation: in the auxiliary frame only the main verb is person-marked, while
		in the pair-verb frame both predicates are <Ref k="bugaeva2012" p="492–495" />.
	</p>
</S>

<S t="Dialect variation and the existential-aspect family" id="dialect">
	<p>
		Several Hokkaido varieties deploy additional forms that overlap with or extend the
		<i lang="ain-Latn">kor an</i>/<i lang="ain-Latn">wa an</i> field. Yoshikawa groups
		<i lang="ain-Latn">kor an</i>, <i lang="ain-Latn">wa an</i>,
		<i lang="ain-Latn">kane an</i>, and <i lang="ain-Latn">hine an</i> under the cover term
		existential aspectual forms, all tracing to the same grammaticalization source in the
		lexical existential <i lang="ain-Latn">an</i>, with individual varieties positioned at
		different points along the cline
		<Ref k="yoshikawa2022" />; <Ref k="yoshikawa2020" />.
	</p>
	<p>
		In Saru and Chitose, <i lang="ain-Latn">kane an</i> covers both progressive and resultative
		readings and adds a noteworthy-situation nuance that the simpler pair lacks; in Shizunai and
		Samani it serves as a plain, unmarked equivalent of either construction. Tokachi and Ishikari
		have <i lang="ain-Latn">kan an</i> — leaning progressive in Tokachi, resultative in
		Ishikari — and Tokachi additionally uses <i lang="ain-Latn">tek an</i> for the resultative
		of change verbs <Ref k="nakagawa2024" p="446–449" />; <Ref k="sato2006" p="84–85" />.
		The full dialect survey belongs to <Xr ch="continuative-siri-siran-kane-an" />.
	</p>
	<p>
		One community observation notes a correspondence between Hokkaido
		<i lang="ain-Latn">wa an</i> and a Sakhalin form <i lang="ain-Latn">hemaka</i> in
		cross-variety word lists (nukopoli, aynu-corpora Discord, December 2024)
		<Ref k="aynucorporadiscord" /> ‹speculative›. The sources consulted for this grammar do not
		address this correspondence, and its functional equivalence requires independent
		verification in the Sakhalin grammatical literature.
	</p>
</S>
