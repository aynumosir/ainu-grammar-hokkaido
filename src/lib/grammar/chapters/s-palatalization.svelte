	<p>
		The phonetic interpretation is straightforward: the labial co-articulation of /w/ suppresses
		the palatal colouring that the coda /s/ otherwise carries. Whether this is best analysed as
		anticipatory labial assimilation blocking palatalization, or as a context-sensitive
		allophone rule that takes precedence over the general coda rule, is not resolved in the
		read sources. The broader sandhi environment in which /s/ participates at word junctures —
		including the /r/+/s/ cluster that produces progressive effects (see
		<Xr ch="coda-r-assimilation-sonorant-sandhi" />) — is treated there.
	</p>
</S>

<S t="Ainu [ɕ] and Japanese /ɕ/: a phonetic note" id="vs-japanese">
	<p>
		The [ɕ] that appears as the coda /s/ allophone after /i/ and as the onset /s/ allophone
		before /i/ is phonetically distinct from the Japanese palato-alveolar [ɕ] of シ. Community
		phonetic analysis characterizes the Ainu surface [ɕ] as having less high-frequency friction
		than its Japanese counterpart, placed somewhat further back in the oral cavity — on the
		boundary between Japanese [ɕ] and an English postalveolar [ʃ] — while never reaching a
		full retroflexed or postalveolar [ʃ]
		<Ref k="aynucorporadiscord" /> (nukopoli, 2023-12-11) ‹speculative›. The analysis
		characterizes it as a laminal sound, with the tongue blade rather than the tip engaged,
		which distinguishes it from the Japanese [ɕ] even within the palato-alveolar region.
	</p>
	<p>
		This distinction has no phonemic status: there is no /ɕ/ phoneme in Hokkaido Ainu separate
		from /s/; the [ɕ] surface form is entirely predictable from the vowel context. The use in
		earlier descriptions of /ʃ/ as a coda symbol (as in Shiraishi's notation
		<i lang="ain-Latn">as</i> [aʃ], and Tamura's representation of coda
		<i lang="ain-Latn">-s</i> after /i/ as シ) reflects the impressionistic transcription of
		the fully palatalized allophone, not a phonemic /ʃ/ distinct from /s/
		<Ref k="shiraishi2022" p="§3" />.
	</p>
</S>

<S t="Speaker and dialect variation" id="speaker-variation">
	<p>
		The degree of coda palatalization shows inter-speaker variation within and across dialects.
		Analysis of NINJAL recordings identifies Hiraga Sadamo (平賀さだも, Saru dialect) as
		producing "pure [ɕ]" at the coda and onset-before-/i/ position, while Mukawa dialect
		recordings from the same archive frequently show a reduced variant [ɕ&#x0331;&#x0325;] — a
		partially devoiced, laminal palato-alveolar — in the same environment
		<Ref k="aynucorporadiscord" /> (nukopoli, 2024-03-24) ‹speculative›. The Mukawa variant
		suggests an interaction between coda /s/ palatalization and the vowel devoicing that
		affects /i/ between voiceless consonants (treated in
		<Xr ch="vowel-inventory" />): when /i/ devoices in the environment of the coda /s/,
		the palatalization feature is retained on the fricative but the overall phonetic output
		moves toward a devoiced palatal fricative [ç] or a devoiced [ɕ&#x0325;].
	</p>
	<p>
		These inter-speaker differences have not been quantified from published acoustic
		measurements in any directly-read source. The phonetic detail is graded accordingly.
		The vowel devoicing pattern that creates the relevant environment in Mukawa is
		independently attested in <Ref k="shiraishi2022" p="§2.1" /> and
		<Ref k="nakagawa2024" p="28" />.
	</p>
</S>

<S t="Progressive palatalization in sirsak" id="progressive-palatalization">
	<p>
		A sporadic case of progressive palatalization has been observed in NINJAL corpus recordings
		of the compound word <i lang="ain-Latn">sirsak</i>
		<Ref k="ninjal2003" />. The underlying form /sir.sak/ has initial /si/ → [ɕi] by the
		regular onset rule, followed by the coda /r/ of <i lang="ain-Latn">sir</i> and then the
		onset /s/ of <i lang="ain-Latn">sak</i>. In the speech of 木村きみ (Kimura Kimi, Saru), the
		coda /r/ after /i/ is palatalized ([ɾ&#x02B2;]), and this palatalization carries forward
		progressively onto the following /s/, shifting it from the expected laminal [s&#x033B;]
		toward a laminal postalveolar [&#x1D98;], as in [ɕiɾ&#x02B2;&#x1D98;ak&#x02DA;]; an
		alternative surface realization is [ɕiɾ&#x02B2;ɕak&#x02DA;]
		<Ref k="aynucorporadiscord" /> (nukopoli, 2026-05-11) ‹speculative›.
	</p>
	<p>
		The speaker 小田イト (Oda Ito, also documented in Saru at the same NINJAL site) does not
		show the same progressive shift: her realization is [ɕiɾ&#x02B2;is&#x033B;ak&#x02DA;],
		with an echo vowel after the /r/ coda (a property of coda /r/ discussed in
		<Xr ch="rhotic-r" />) and a plain laminal [s&#x033B;] — not a palatalized
		[&#x1D98;] or [ɕ] — as the onset before /a/
		<Ref k="aynucorporadiscord" /> (nukopoli, 2026-05-11) ‹speculative›.
	</p>
	<p>
		The contrast between the two speakers illustrates two independent strategies for the
		/ɾ&#x02B2;.s/ juncture: progressive coarticulation of palatality from [ɾ&#x02B2;] onto
		the following /s/ (Kimura), versus insertion of an echo vowel that interposes a vocalic
		buffer between the palatalized rhotic and the onset /s/ (Oda). The observation is
		idiolect-specific in the available NINJAL data, has not been confirmed in other
		compounds or other speakers in any read source, and the IPA transcription reflects
		community analysis rather than published spectral measurement ‹speculative›. The
		broader /r/+/s/ sandhi environment — where a coda /r/ interacts with a following /s/
		onset — is treated in <Xr ch="coda-r-assimilation-sonorant-sandhi" />.
	</p>
</S>
