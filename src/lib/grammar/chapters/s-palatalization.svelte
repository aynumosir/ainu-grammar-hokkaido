<S t="The single fricative and its positional variants" id="overview">
	<p>
		Hokkaido Ainu has a single fricative phoneme, /s/ (see <Xr ch="consonant-inventory" />).
		Its phonetic realization depends on syllabic position. In onset function /s/ appears as a
		laminal sibilant with a degree of palatalization that varies by speaker and vowel context.
		In coda function the realization splits by the preceding vowel: after /i/ the coda /s/
		surfaces as [ɕ], merging perceptually with the initial consonant of Japanese シ; after /a/,
		/e/, /o/, and /u/ it surfaces as a partially palatalized laminal [s&#x02B2;]~[ʃ], intermediate
		between Japanese ス and シ. A third context overrides the coda rule: immediately before the
		glide /w/ at a following syllable or word boundary, the coda /s/ surfaces as a plain [s]
		<Ref k="nakagawa2024" p="27–28" />; <Ref k="shiraishi2022" p="§3" />.
	</p>
	<p>
		Because /s/ is the only fricative in the Hokkaido Ainu phoneme inventory, the alternants
		[s&#x02B2;], [ʃ], [ɕ], and [s] are all allophones of one underlying segment, never
		contrastive with each other. The distribution does, however, produce perceptual near-mergers
		that led early transcribers working within a Japanese kana frame to hear palatal colouring
		in unexpected environments, and it creates systematic difficulty for Japanese-background
		listeners distinguishing morphological structures that differ in syllabic affiliation rather
		than in the consonant itself.
	</p>
</S>

<S t="Onset realization" id="onset-realization">
	<p>
		In onset position /s/ is realized as a laminal alveolar fricative. A community phonetics
		survey characterizes it as [s&#x033B;] — with the blade of the tongue making the primary
		constriction rather than the tip — and notes a perceptible difference from the
		palato-alveolar [ɕ] of Japanese シ even when both are heard as "sh-like"
		<Ref k="aynucorporadiscord" /> (nukopoli, 2023-12-11) ‹speculative›.
	</p>
	<p>
		The palatalization tendency of onset /s/ in older recordings extends beyond the environment
		of a following /i/. Community phonetic analysis of early twentieth-century audio reports
		that the onset /s/ was palatalized in a wider range of vowel environments than is typical
		of the better-documented mid-century speakers
		<Ref k="aynucorporadiscord" /> (nukopoli, 2023-12-05) ‹corpus-suggested›. Shiraishi
		describes Ainu /s/ as "often palatalized"
		<Ref k="shiraishi2022" p="§3" />, citing Kindaichi (1931: 9) and Tamura (1988b: 13) for
		the forms <i lang="ain-Latn">sini</i> [ʃini] and <i lang="ain-Latn">as</i> [aʃ] as
		early printed attestations.
	</p>
	<p>
		Early Japanese transcriptions of Ainu rendered the onset <i lang="ain-Latn">sa, su, se,
		so</i> syllables with the digraphs <span lang="ja">シャ, シュ, シェ, ショ</span> rather than
		<span lang="ja">サ, ス, セ, ソ</span>, reflecting a palatal impression before non-high
		vowels. Tamura (1996) states that in Saru "the fricative is /s/ alone, close to the
		consonant of シ, but with individual variation; <i lang="ain-Latn">sa, se, so, su</i>
		vary between a サ-type and a シャ-type pronunciation, while <i lang="ain-Latn">si</i>
		is invariably シ" (reported via <Ref k="aynucorporadiscord" />, nukopoli, 2023-12-11;
		<Ref k="shiraishi2022" p="§3" /> corroborates the general pattern) ‹speculative›.
	</p>
</S>

<S t="Coda allophony: the /i/-conditioning" id="coda-allophony">
	<p>
		The realization of coda /s/ is conditioned by the vowel of the syllable nucleus it closes.
		When that vowel is /i/, the coda /s/ surfaces as [ɕ]; when it is /a/, /e/, /o/, or /u/,
		the coda surfaces as a partially palatalized [s&#x02B2;]~[ʃ]. The rule as stated in
		<Ref k="nakagawa2024" p="27–28" /> and corroborated by
		<Ref k="shiraishi2022" p="§3" /> can be formulated:
	</p>
	<p>
		/s/ (coda) → [ɕ] / i_ ; → [s&#x02B2;]~[ʃ] / (a, e, o, u)_
	</p>
	<p>
		The following table gives attested examples in word-citation (pause) form. All coda forms
		are taken from <Ref k="nakagawa2024" p="27–28" />; the general rule applies equally to
		coda /s/ after /u/, though no dedicated example is cited there.
	</p>
	<table>
		<thead>
			<tr>
				<th>Vowel before coda /s/</th>
				<th>Phonetic realization</th>
				<th>Example</th>
				<th>Approximate gloss</th>
			</tr>
		</thead>
		<tbody>
			<tr>
				<td>/i_/</td>
				<td>[ɕ]</td>
				<td><i lang="ain-Latn">cis</i> [tʃiɕ]</td>
				<td>'to cry'</td>
			</tr>
			<tr>
				<td>/a_/</td>
				<td>[s&#x02B2;]~[ʃ]</td>
				<td><i lang="ain-Latn">as</i> [as&#x02B2;]~[aʃ]</td>
				<td>'to stand'</td>
			</tr>
			<tr>
				<td>/e_/</td>
				<td>[s&#x02B2;]~[ʃ]</td>
				<td><i lang="ain-Latn">tures</i> [tuɾes&#x02B2;]</td>
				<td>'younger sister'</td>
			</tr>
			<tr>
				<td>/o_/ or /u_/</td>
				<td>[s&#x02B2;]~[ʃ]</td>
				<td>—</td>
				<td>(same rule applies)</td>
			</tr>
		</tbody>
	</table>
	<p>
		The near-merger between coda /s/ after /i/ and onset /s/ before /i/ generates a
		systematic perceptual difficulty. The pair <i lang="ain-Latn">tures</i> 'younger sister'
		— with coda /s/ in the syllable /res/ — and <i lang="ain-Latn">tures-i</i> 'his/her
		younger sister' — in which the same /s/ resyllabifies as the onset of the following
		possessive suffix <i lang="ain-Latn">-i</i>, giving the syllable /si/ [ɕi] — both
		approach [tureɕ] at their right edge as heard by Japanese listeners, even though they
		are structurally distinct <Ref k="nakagawa2024" p="27–28" />. For Ainu speakers the
		difference is in the syllabic affiliation of /s/ (coda vs. onset), recoverable from
		morphological structure. Resyllabification at morpheme boundaries is treated in
		<Xr ch="syllable-template" />; the morphophonology of the possessive suffix is taken
		up in <Xr ch="affiliative-suffix-morphophonology" />.
	</p>
	<p>
		The onset /s/ before /i/ participates in the same [ɕ] realization. The reflexive prefix
		<A w="si-" gl="reflexive" /> surfaces as [ɕi], the same phonetic output as coda /s/
		after /i/. The two environments converge on [ɕ] from different structural positions
		(coda conditioned by preceding /i/, onset conditioned by following /i/) without forming a
		phonemic /ɕ/ distinct from /s/.
	</p>
</S>

<S t="Coda /s/ before /w/: the override constraint" id="sw-constraint">
	<p>
		At a juncture before the labio-velar glide /w/, the coda /s/ surfaces as plain [s] rather
		than the [s&#x02B2;]~[ʃ] expected from the non-/i/ conditioning rule. Tamura (1996)
		describes <i lang="ain-Latn">as</i> 'stand' in isolation as
		<span lang="ja">アㇱ</span> ([aʃ]), but the same verb followed by the conjunctive
		particle <A w="wa" gl="and then; conjunctive" /> becomes <span lang="ja">アㇲワ</span>
		([aswa]), with the fricative shifting to a plain sibilant before the /w/ onset
		<Ref k="aynucorporadiscord" /> (aomidori, 2023-12-11, reporting Tamura 1996);
		see also <Ref k="nakagawa2024" p="27" /> for the coda-/s/ allophony frame within which
		this constraint operates ‹corpus-suggested›.
	</p>

	<Ex
