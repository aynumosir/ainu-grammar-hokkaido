	<p>
		The sung/prose split is not merely a performance fact. Sung verse genres deploy the
		poetic register (<i lang="ain-Latn">atomte itak</i>, 'ornamented language') with its
		distinct morphological and lexical features, while prose (<i lang="ain-Latn">yayan itak</i>,
		'ordinary language') does not — even when the prose narrator's voice rises and falls with
		a narrative lilt <Ref k="endo2022" p="§3" />; <Ref k="sato2008" p="263–264" />.
		The elevated register is described in <Xr ch="poetic-archaic-elevated-register" />; its
		TAM and evidential patterning by genre in <Xr ch="narrative-tam-evidential-patterning-by-genre" />.
		Folktale intonation — one-directional, low-variation melody, with upper-Saru narration more
		monotone than lower-Saru — sits apart from the strict melodic structure of sung verse
		(<Ref k="aynucorporadiscord" />, nukopoli 2024, <i lang="ain-Latn">asserted</i>, after
		<Ref k="tamura1996" />).
	</p>

	<p>
		The verb <A w="sakehawki" gl="sing/recite a yukar" /> (E-Iburi) captures the sung
		dimension lexically; it appears in a Abuta yukar passage meaning 'about to sing a yukar'
		(<i lang="ain-Latn">sakehawki anki</i>), confirming that for at least the eastern Iburi
		dialects, sung performance was lexically distinguished from prose narration
		<Ref k="sato2009yukar" p="§2.2" />.
	</p>
