		<p>
			A further hypothesis, explicit in Okuda and flagged as conjectural, relates the
			syllable-count system to Japanese 7-5 mora metre: the contact with Japanese literary
			tradition could have reinforced or introduced the strict syllable target
			<Ref k="okuda2022" p="§5.2" /> ‹speculative›. No positive evidence is offered and the
			hypothesis rests on structural analogy alone.
		</p>
	</S>
</S>

<S t="Alliteration and rhyme" id="rhyme-alliteration">
	<p>
		Philippi's influential judgment (1979, via <Ref k="okuda2022" p="§2.2" />) was that rhyme
		in Ainu poetry is 'quite accidental' and alliteration 'sporadic' — not prosodically
		functional as in Mongolic or Tungusic traditions. Okuda formalizes the reasoning: given
		five vowels and roughly twelve onset consonants, chance repetition in a corpus of five-syllable
		lines is high, and a sequence counts as technique only when it demonstrably exceeds chance
		and is not forced by morphological reduplication, which is pervasive in Ainu
		<Ref k="okuda2022" p="§2.2" />.
	</p>
	<p>
		A counter-position has been raised in the community literature. Tangiku (2020, 北大言語
		アーカイヴ報告書) argues that an 18th-century Ainu yukar — <i lang="ain-Latn">ルウェサニウンクㇽ</i>
		— uses deliberate head-rhyme, end-rhyme, and imperfect rhyme as compositional devices
		(<Ref k="aynucorporadiscord" />, nukopoli 2023-12-13, <i lang="ain-Latn">asserted</i>,
		after Tangiku 2020). If the Tangiku analysis survives scrutiny, it would demonstrate at
		least one historical period or textual tradition in which rhyme was a conscious structural
		choice rather than an artefact of phonological probability ‹contested›. The Tangiku source
		has not been consulted directly for this grammar; the claim is flagged as needing verification
		against the primary text.
	</p>
</S>

<S t="Fourth-person narration in sung verse" id="fourth-person-sung">
	<p>
		Both yukar and kamuy yukar are narrated in the first person from the protagonist's
		viewpoint. The grammatical marker of this 'first person' is, in Saru and Chitose, the
		<Xr ch="indefinite-fourth-person-paradigm">fourth-person</Xr> prefix <i lang="ain-Latn">a=</i>
		and suffix <i lang="ain-Latn">=an</i>, not the everyday first person <i lang="ain-Latn">ku=</i>
		<Ref k="sato2008" p="259–261" />; <Ref k="endo2022" p="§2.4" />. The choice of the
		fourth-person forms is grammatically productive: in elegant language, <i lang="ain-Latn">=an</i>
		combines with singular verbs (e.g., <i lang="ain-Latn">ek-an</i> 'I come.SG'), a
		combination that everyday Saru disallows (<i lang="ain-Latn">*ek-an</i> is ill-formed
		there but <i lang="ain-Latn">arki-an</i> 'we come.PL' is fine)
		<Ref k="sato2008" p="263" />. This number-agreement override is a diagnostic of the
		elegant register in sung verse — see <Xr ch="poetic-archaic-elevated-register" />.
	</p>

	<p>
		The Iburi dialect tradition, canonically represented by Chiri Yukie's
		<Ref k="chiri1923" />, uses instead the exclusive first-person plural <i lang="ain-Latn">ci=</i>
		/ <i lang="ain-Latn">=as</i> as a singular hero-narrator marker, consistently throughout
		the collection <Ref k="sato2008" p="261" />; <Ref k="sato2004" />. The regional split
		is systematic: <i lang="ain-Latn">a=</i> / <i lang="ain-Latn">=an</i> in Saru and Chitose;
		<i lang="ain-Latn">ci=</i> / <i lang="ain-Latn">=as</i> in Iburi (Horobetsu and related
		traditions) — with Chitose showing an intermediate pattern where <i lang="ain-Latn">ci=</i>
		/ <i lang="ain-Latn">=as</i> may appear in the opening lines before reverting to
		<i lang="ain-Latn">a=</i> / <i lang="ain-Latn">=an</i> <Ref k="sato2008" p="261" />.
	</p>

	<p>
		The motivation for the non-everyday person-marking in oral narrative is discussed under two
		competing accounts: the shamaness-oracle origin theory (standard), in which the verse is
		framed as spirit-speech, and Satō's 'indirection principle' (<span lang="ja">間接性の原理</span>),
		in which the god's first-person speech inside a dream required indirectness to soften the
		taboo on direct divine address <Ref k="sato2008" p="259–261" /> ‹contested›. The
		logophoric and direct-speech dimensions of the fourth person as used in narration are
		treated in <Xr ch="logophoric-narration-and-reported-discourse" /> and
		<Xr ch="fourth-person-honorific" />; reference tracking across verse episodes in
		<Xr ch="reference-tracking" />; the full paradigm in <Xr ch="person-marking-architecture" />.
	</p>

	<p>
		One grammatically significant consequence of sung-verse performance is quotative density.
		Because narration is cast as direct speech and closes with a self-identification formula
		(<i lang="ain-Latn">…sekor … isoytak</i> 'so X told the story'), the quotative particle
		<A w="sekor" gl="quotative" /> with speech verbs (<A w="ye" gl="say" />,
		<A w="hawki" gl="say/recite" />, <A w="isoytak" gl="tell a story" />) clusters heavily
		in verse text-closings. The evidential tail <i lang="ain-Latn">hawe ne</i> / <i lang="ain-Latn">hawe as</i>
		(&lt; <A w="haw" gl="voice" /> + <Xr ch="concept-form-affiliative-form">possessed form</Xr>)
		carries much of the reportative marking in these contexts
		<Ref k="endo2022" p="§2.4" />; the broader evidential system is treated in
		<Xr ch="evidential-schema-overview" /> and <Xr ch="ruwe-ne-inferential" />.
	</p>
</S>
