	<p>
		The story-type and sakehe are demonstrably not in fixed correspondence: the same narrative
		situation (marlin-tuna confrontation) carries sakehe <i lang="ain-Latn">tusunapanu</i>
		in Saru (Biratori), <i lang="ain-Latn">tussay turi tussay matu</i> in Horobetsu, and
		<i lang="ain-Latn">nissō</i> in Chitose (where the protagonist is swapped for the fish
		<i lang="ain-Latn">okina</i>) <Ref k="endo2022" p="§2.4.2" />. Kayano's Saru dictionary
		preserves a long multi-word sakehe (<i lang="ain-Latn">tunoyake renoyake kutunke kamuyke
		kamuy cikappo hum hum hum</i>) as one illustration of the form's capacity for extended,
		rhythmically organized strings <Ref k="kayano1996" p="s.v. sakehe" />.
	</p>
</S>

<S t="Melody: fixed per story in kamuy yukar, personal in yukar" id="melody">
	<p>
		The melodic contrast between kamuy yukar and yukar is the clearest formal divide in the genre
		system. A kamuy yukar text comes with a melody that is bound to that story and is transmitted
		alongside the words and the sakehe; a performer who learns the story learns all three as an
		integrated unit <Ref k="endo2022" p="§2.4.2" />. Prayer (<i lang="ain-Latn">kamuy nomi</i>)
		also has a melodic delivery, but its melody is improvised and personal — different each time
		and not transmitted with the words — a property Nakagawa (via <Ref k="endo2022" p="§2.3" />)
		uses to distinguish prayer from the fixed-word, fixed-melody incantation.
	</p>

	<p>
		Yukar stands apart: each narrator constructs or inherits his own tune for a borrowed story,
		and the melody is liable to change mid-performance <Ref k="endo2022" p="§2.4.3" />. The
		absence of a sakehe in yukar corresponds to this melodic instability: where the kamuy yukar
		uses the repeating sakehe to bind melody, narrative line, and rhythmic unit together, the
		yukar uses rhythm-tapping (<A w="rep" gl="keep time" /> / <A w="repni" gl="rhythm-stick" />
		— 'the act of keeping time', confirmed in the Mukawa-dialect dictionary as 拍子棒
		<Ref k="sato2008" p="259" />) to provide rhythmic scaffolding without a textual refrain.
		Listeners add a sung chorus (<A w="hetce" gl="chorus response" />; corpus count 13) during
		yukar performance, a further rhythmic layer absent from kamuy yukar
		<Ref k="sato2008" p="259" />.
	</p>

	<p>
		Satō notes that an opening formula — the <A w="eyororpe" gl="opening formula (yukar)" /> —
		may recur at the start of a yukar and could represent a vestige of a refrain that yukar
		once possessed <Ref k="sato2008" p="259" /> ‹speculative›. The inference is Satō's own and
		rests on structural analogy with kamuy yukar rather than on textual attestation.
	</p>
</S>

<S t="Function of the sakehe: the protagonist-index debate" id="sakehe-function">
	<p>
		The functional motivation of the sakehe has generated competing accounts. Nakagawa proposes
		that it originally <strong>indicated the protagonist</strong>: the refrain was an
		identifying tag that signalled whose voice or experience was being narrated
		<Ref k="endo2022" p="§2.4.2" />, reporting Nakagawa 2001b: 7 ‹contested›. The
		counter-evidence is substantial: the same story-type does not consistently carry the same
		sakehe across dialects (the marlin-tuna case above), and many sakehe have no recoverable
		semantic or deictic link to their protagonist. The thunder-god's <i lang="ain-Latn">hum
		pakpak</i> can be read as onomatopoeia of thunder's sound (the narrator's own gloss
		supports this), but that is iconic, not indexical, and it is not generalizable to the
		full sakehe inventory <Ref k="endo2022" p="§2.4.2" />.
	</p>

	<p>
		A performer's own metalinguistic statement, recorded in the AA研 archive, puts the
		sakehe at the centre of the sung/prose boundary: the narrator Kawakami Matsuko explains
		that a <i lang="ain-Latn">menoko yukar</i> is performed <i lang="ain">sakekoye</i>
		('told with the refrain'), and that when the refrain is omitted the same material is
		told as <i lang="ain-Latn">uwepeker</i>:
	</p>

