	<p>
		The community discussion archives one live genre-boundary case that bears on the sakehe's
		defining role. A kamuy yukar narrated by Oda Suteno (織田ステノ, 'Lazybones Child') carries
		a long sakehe (<i lang="ain-Latn">manayta sanke tar panke tar kocupu</i>) but an
		unusually short narrative body, and its closing move — the protagonist self-identifying as
		a human boy (<i lang="ain-Latn">sine wen toranne hekaci</i>, 'a single weak miserable
		child') — formally resembles a <i lang="ain-Latn">yayeisoytak</i> (self-narration) rather
		than a divine-epic conclusion (<Ref k="aynucorporadiscord" />, gengojiro 2025-12-12,
		<i lang="ain-Latn">proposed</i>). This text, with melody and a long sakehe but a
		human-protagonist resolution, resists placement in either kamuy yukar or yukar, illustrating
		that the sakehe alone does not uniquely define genre membership ‹contested›.
	</p>

