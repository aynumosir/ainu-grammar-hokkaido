		One additional philological datum: the title-line <i lang="ain-Latn">AEKIRUSHI</i> in Chiri
		Yukie's <Ref k="chiri1923" /> has been proposed to parse as <i lang="ain-Latn">a=e-kir-usi</i>
		'the place one has memory of' (<Ref k="aynucorporadiscord" />, 0penke 2024-03-19,
		<i lang="ain-Latn">proposed</i>, after 片山龍峯 2003) rather than deriving from
		<i lang="ain-Latn">ekiru</i> 'turn back'. If correct, the title-line encodes the hero's
		logophoric self-location, consistent with the divine-epic pattern of first-person
		self-presentation ‹contested›.
	</p>
</S>

<S t="Verse metre: two analytical systems" id="metre">
	<p>
		The metrical organization of HA verse has been described under two competing frameworks.
		The long-standing analysis treats verse as <strong>syllable-count oriented</strong>, with
		five syllables per line as the basic unit. Okuda's more recent work identifies a second
		system, <strong>accent oriented</strong>, in which the first pitch-accent falls in a fixed
		rhythmic position regardless of line length. The two frameworks are not simply rival
		accounts of the same data: Okuda argues they are genre-differentiated and co-active
		within a single performer's repertoire ‹contested›
		<Ref k="okuda2022" p="§2, §3" />.
	</p>

	<S t="Syllable-count orientation" id="syllable-count">
		<p>
			The syllable-count description goes back to Kindaichi (1931: 165, via
			<Ref k="okuda2022" p="§2.1" />) and was adopted by Tamura (1973: 56, via
			<Ref k="okuda2022" p="§2.1" />): the basic line unit is <strong>five
			syllables</strong>, with four to seven as the attested range. The five-syllable
			target is met through a set of compositional techniques catalogued by Tamura and
			Satō <Ref k="sato2008" p="264" />; <Ref k="okuda2022" p="§2.1" />:
		</p>
		<ul>
			<li>use of formulaic verse phrases already calibrated to syllable count;</li>
			<li>choice between synonyms for their syllable count;</li>
			<li>insertion or omission of final particles;</li>
			<li>insertion of the dummy verb <A w="ki" gl="do (light verb)" /> between a verb and its
				following element;</li>
			<li>use of the uncontracted <i lang="ain-Latn">ku-</i> person prefix rather than its
				reduced form;</li>
			<li>prefixing an initial <i lang="ain-Latn">u</i> to a line when reciting;</li>
			<li>splitting a long phrase across two five-syllable lines;</li>
			<li>not eliding vowels that would contract in everyday speech;</li>
			<li>not pronouncing the line-final syllable (a performance strategy, not a morphological
				rule).</li>
		</ul>
		<p>
			These techniques confirm that the syllable target is a compositional constraint on the
			verse line rather than a property of the grammar at large. They are the mechanisms through
			which the sung verse actively shapes morphosyntax: word choice, prefix realization, and
			particle use are all subordinated to the five-syllable frame. This is the most direct
			route by which genre conditions grammatical form in Hokkaido Ainu.
		</p>
	</S>

	<S t="Accent-orientation metre" id="accent-orientation">
		<p>
			Okuda's comparative analysis of divine epics and lyric songs identifies a second system
			that he terms <strong>accent-orientation</strong> (アクセント指向): the first
			pitch-accent of the line falls in the first disyllabic foot, regardless of how many
			syllables the line contains <Ref k="okuda2022" p="§3" />. Under this system, a
			four-syllable line is metrically well-formed as long as the accent is initial; the
			syllable-count system, by contrast, disfavours four-syllable lines.
		</p>

		<p>
			The scansion evidence comes from divine epics recorded by narrators in Monbetsu and
			Chitose. In the divine epic <i lang="ain-Latn">Amamecikappo</i> narrated by Hatozawa
