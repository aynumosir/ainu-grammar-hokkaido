<S t="The sakehe: morphology and the fixed-melody criterion" id="sakehe-form">
	<p>
		The term <A w="sakehe" gl="refrain; the sake of —" /> is morphologically the
		<Xr ch="concept-form-affiliative-form">possessed form</Xr> of <i lang="ain-Latn">sa</i>
		(Japanese Wiktionary: "sa の所属形"). Tamura's Saru dictionary records a prosodic
		distinction: the genre-refrain sense carries initial accent (<i lang="ain-Latn">sákehe</i>)
		while the common-noun sense 'sake (the beverage)' is unaccented, creating near-homophones
		that occasionally coincide within a single verse line (see the <i lang="ain-Latn">hancikiki</i>
		example below) <Ref k="tamura1996" p="s.v. sákehe, sakehe" />.
		The corpus registers fifteen tokens of the form (rank 4,602 among 45,225 distinct tokens),
		spanning both senses; the refrain sense is attested in narrator metalinguistic commentary
		(see example below) ‹corpus-suggested›.
	</p>

	<p>
		In kamuy yukar, the sakehe is a <strong>fixed refrain per story</strong>, inserted into
		the narration — every line or every few lines depending on the text — and held constant
		throughout one performance of that story <Ref k="endo2022" p="§2.4.2" />;
		<Ref k="sato2008" p="259" />. The melody to which the text is sung is equally fixed per
		story (contrast yukar, where each narrator deploys a personal tune that may change
		mid-performance; see the melody section below). Together, the fixed melody and the fixed
		sakehe define the formal identity of a kamuy yukar text: a reciter who knows a story
		knows both its sakehe and its melody as an inseparable package <Ref k="endo2022" p="§2.4.2" />.
	</p>

	<p>
		A canonical instance of sakehe-every-line placement comes from a marlin-tuna kamuy yukar
		narrated by Nabezawa Nepuki (鍋沢ネブキ) of Biratori. The sakehe <i lang="ain-Latn">tusunapanu</i>
		precedes every narrative word-group, producing a strict alternation of refrain and narrative
		that generates the rhythmic frame of the performance
