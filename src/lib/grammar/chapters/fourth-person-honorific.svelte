<S t="The honorific and logophoric functions in the fourth-person system" id="overview">
	<p>
		The honorific and logophoric values of the fourth person belong to the same formal row as its
		inclusive and indefinite uses: the transitive-subject prefix
		<i lang="ain-Latn">a=</i> (dialectally <i lang="ain-Latn">an=</i>), the
		intransitive-subject suffix <i lang="ain-Latn">=an</i>, and the object prefix
		<i lang="ain-Latn">i=</i>. The S/A/O architecture of this tripartite row and its
		agent-defocusing function are set out in <Xr ch="person-marking-architecture" /> and
		<Xr ch="indefinite-fourth-person-paradigm" />. The present chapter treats two discourse-anchored
		functions: respectful address to a socially elevated second person, and self-reference by a
		narrated protagonist or by a speaker whose words are embedded inside a quotation
		<Ref k="nakagawa2024" p="175–179" />; <Ref k="tamura1972" p="19–20, 28" />;
		<Ref k="bugaeva2012" p="475–476" />.
	</p>

