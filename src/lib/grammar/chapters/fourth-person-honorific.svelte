	<p>
		Nakagawa subsumes all these values under a single fourth-person label; Tamura's Saru analysis
		separates the honorific second person and the quotative first person as distinct categories;
		Bugaeva's cover term is indefinite, with the quotation use reanalysed as logophoric; Satō
		discusses the literary and quotative functions under the literary-person problem without positing
		a fourth-person label; Takahashi, working on Tokachi, uses "inclusive person" for the same
		form range
		<Ref k="nakagawa2024" p="174–179" />; <Ref k="tamura1972" p="19–20, 28, 39" />;
