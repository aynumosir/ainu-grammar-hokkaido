<S t="Honorific address to an exalted second person" id="honorific-second">
	<p>
		The fourth-person row indexes a second person of elevated social standing. Nakagawa cites two
		primary contexts: a woman addressing an adult man, and any speaker addressing a kamuy in
		prayer; in both, the verb takes fourth-person morphology and a plural stem even when the
		addressee is singular
		<Ref k="nakagawa2024" p="175–176" />; <Ref k="bugaeva2012" p="475" />. Tamura's Saru account
		places the same frame alongside contexts of esteem toward an adult
		<Ref k="tamura1972" p="28" />.
	</p>

	<Ex
		m="hunak wa arki=an ru-we an"
		g="where from come.PL=4.S track-POSS exist"
		tr="Where have you come from?"
		cite="nakagawa2024:177"
		dial="CHI"
		place="Nakagawa ex. 140; a woman addressing an adult man"
		note="The addressee is a single person; the fourth-person S suffix =an appears on the plural motion stem arki rather than the singular ek. The evidential frame ru-we an closes the question."
	/>

	<p>
		The plural-stem requirement in this single-referent frame is grammatically obligatory for the
		honorific use. The same constraint — one referent triggering plural verbal morphology — operates
		across the honorific, inclusive, and narrative fourth-person functions
		<Ref k="nakagawa2024" p="160–161, 174–176" />. Person–number interactions in the verbal system
		are treated in <Xr ch="person-number-interactions" />. The syntax of prayer address to a kamuy
		and the attendant ritual formulas are developed in <Xr ch="honorific-ritual-and-taboo-registers" />.
	</p>

	<p>
		Ijäs's online pedagogical grammar documents several present-day address strategies for L2
		learners, including plural-stem imperatives with the final particle
		<A w="yan" gl="final particle (request)" /> as a routine polite request, and the use of
		<A w="nispa" gl="gentleman; master" /> and <A w="katkemat" gl="respected woman" /> as address
		terms for non-relatives <Ref k="ijas2023" />. The following constructed example illustrates
		the plural-stem polite imperative, a strategy structurally distinct from the fourth-person S
		suffix <i lang="ain-Latn">=an</i> used in the honorific paradigm:
	</p>

	<Ex
		m="eteun arki yan"
		g="here come.PL FIN"
		tr="Please come here."
		cite="ijas2023"
		constructed
		dial="HK"
		note="Ijäs pedagogical example. The plural stem arki and the final particle yan form a polite request without deploying fourth-person morphology; it is a parallel addressee-politeness resource, not an instance of the honorific paradigm described above. Ijäs further notes that attaching nispa and katkemat to personal names as titles is not uniformly attested in traditional usage; such address conventions belong to contact and revitalization contexts rather than to the inherited verbal honorification system."
	/>
</S>

<S t="The narrator's fourth person in oral literature" id="narrator-person">
	<p>
		In Hokkaido oral narrative, the protagonist's self-reference is expressed by the fourth-person
		row. The verb takes fourth-person morphology, and Japanese translations of the same passages
		render the protagonist with first-person pronouns, because the referent is the narrated self
		<Ref k="nakagawa2024" p="177–179, 562" />. Satō discusses this under the literary-person
		problem; Bugaeva places it alongside the logophoric use
		<Ref k="sato2004" p="179–184" />; <Ref k="sato2008" p="259–262" />;
		<Ref k="bugaeva2008" p="37–44" />. All three grammatical roles are attested. The following Saru
		example shows the intransitive-subject suffix and the possessive prefix together:
	</p>

	<Ex
		m="ratcitara hosipi=an yakka a=uni un tunasno hosipi=an pe ne kusu"
		g="slowly return=4.S though 4.POSS=home to quickly return=4.S NMLZ COP because"
		tr="Although I returned slowly, because I was able to return home quickly, …"
		cite="nakagawa2024:168"
		dial="SAR"
		place="Nakagawa ex. 109; singular Saru narrator"
		note="The intransitive stem hosipi takes the fourth-person S suffix =an; a= functions as the narrator's possessive prefix on uni 'home'. Saru and Chitose allow a singular stem with a singular fourth-person narrator."
	/>

	<p>
		Saru and Chitose permit a singular narrator to appear with a singular verb stem, as in the
		example above. Dialect variation is substantial: in Horobetsu, the verb may carry a singular
		fourth-person index while the only available free pronoun is the plural
		<i lang="ain-Latn">aokay</i>; in Shizunai, the free pronouns distinguish singular
		<i lang="ain-Latn">anani</i> from plural <i lang="ain-Latn">anoka</i>, but the verb is
		consistently plural <Ref k="nakagawa2024" p="174–176, 254–256" /> ‹contested›. These
		opposite mismatches between pronoun number and verbal number across the dialect area indicate
		that the number dimension of the narrator form diverged from a common source.
	</p>

	<p>
		The object prefix <i lang="ain-Latn">i=</i> indexes the narrator as patient or recipient
		alongside <i lang="ain-Latn">a=</i> and <i lang="ain-Latn">=an</i>. A single predicate can
		carry both the agent-defocusing <i lang="ain-Latn">a=</i> and the narrator-object
		<i lang="ain-Latn">i=</i>:
	</p>

	<Ex
		m="a=rehe Ikuresuye sekor a=i=reko"
		g="4.POSS=name Ikuresuye QUOT 4.A=4.O=name"
		tr="I was named Ikuresuye."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker folktale 7 [aa-irc/005#1]"
		note="a= on rehe is the narrator's possessive prefix; on reko it is an indefinite-agent prefix (someone named); i= indexes the narrator as the named object. The co-occurrence of fourth-person A as agent-defocuser and fourth-person O as narrator-object on one predicate yields a passive-like Japanese reading (私はイクレスイェと名付けられた)."
	/>

	<Ex
		m="i=erampokiwen i=rewsire wa i=kore"
		g="4.O=pity 4.O=shelter CONJ 4.O=give"
		tr="Take pity on me and give me lodging."
		cite="nakagawa2024:171"
		dial="SAR"
		place="Nakagawa ex. 127"
		note="The object prefix i= recurs across three transitive predicates, indexing the narrator as the entity pitied, sheltered, and given to. The homophonous detransitivising prefix i- of the antipassive construction is morphologically and functionally distinct."
	/>

	<p>
		The distinction between the fourth-person object prefix <i lang="ain-Latn">i=</i> and the
		antipassive detransitiviser <i lang="ain-Latn">i-</i> 'something' requires attention to
		syntactic context. The antipassive is treated in <Xr ch="antipassive-detransitive-i" />.
	</p>
</S>

<S t="The debate over the literary person" id="literary-debate">
	<p>
		Earlier analyses distinguished a "literary" person (雅語の人称) from an "everyday" person
		(日常語の人称), treating the narrative fourth-person as a register marker. Nakagawa rejects
		this bifurcation: prose tales (uwepeker) use fourth-person marking for the protagonist even
		though their register is conversational, while prayers (inonno itak), though formally elevated,
		use first-person <i lang="ain-Latn">ku=</i> for the praying speaker. The controlling factor
		is discourse role: a character's story-internal self-representation takes the fourth person;
		a speaker's real-world first-person address takes <i lang="ain-Latn">ku=</i>
		<Ref k="nakagawa2024" p="600–602" />.
	</p>

	<p>
		Satō advances three objections to reducing the literary person to the indefinite
		<Ref k="sato2004" p="179–184" />. First, Ainu speakers and translators consistently interpret
		the narrative form with first-person reference. Second, the indefinite in its agent-defocusing
		function is referent-non-specific and incompatible with an overt free pronoun; yet narrators
		use <i lang="ain-Latn">asinuma</i> alongside fourth-person verbal morphology, indicating
		referential specificity. Third, kamuy-yukar texts from Horobetsu use the first-plural
		exclusive row <i lang="ain-Latn">ci=/=as/un=</i> as the divine narrator's person — a fact
		that resists derivation from an indefinite base <Ref k="nakagawa2024" p="602–604" />.
		Nakagawa counters that most texts mix the first-plural exclusive and the fourth person, and
		that the uniformly first-plural presentation of Chiri Yukie's 1923 神謡集 is likely an
		editorial regularisation, since Kanari Matsu's tellings of the same material show mixed usage.
		The divine narrator pattern and its genre context are examined in
		<Xr ch="heroic-and-divine-verse" />.
	</p>

	<p>
		Bugaeva's approach sidesteps the origin question by relocating the quotation-internal use to
		logophoricity and documenting it in non-folklore Asahikawa reported-speech data
		<Ref k="bugaeva2008" p="41–44" />; <Ref k="bugaeva2012" p="475–476" />. Since the form
		appears wherever a discourse source refers to itself — in oral literature and in everyday
		reported speech alike — a register-based account cannot be primary. The three analyses agree
		on the empirical core while diverging on grammatical category and diachronic origin ‹contested›.
	</p>
</S>

<S t="Logophoric reference in reported speech" id="logophoric">
	<p>
		When a third party's speech is embedded after the quotative complementizer
		<A w="sekor" gl="quotative particle" />, that speaker's self-reference appears as fourth-person
		morphology in place of ordinary first-person <i lang="ain-Latn">ku=</i> or
		<i lang="ain-Latn">ci=</i>. Tamura had already treated this as a "quotative first person" in
		Saru; Bugaeva reanalyses it as logophoricity in the typological sense, where a dedicated form
		tracks the self of the source of a reported utterance
		<Ref k="tamura1972" p="19–20, 22–23" />; <Ref k="bugaeva2008" p="37–41" />;
		<Ref k="bugaeva2012" p="475–476" />.
	</p>

	<Ex
		m="a=ye wa anun katkemat a=nure sekor a=ye acapo hawean kor"
		g="4.A=say CONJ other lady 4.A=tell QUOT 4.A=say uncle say while"
		tr='"I will tell it and let another lady hear it," the uncle said.'
		cite="nakagawa2024:180"
		dial="SAR"
		place="Nakagawa ex. 153, after Tamura 1984: 22"
		note="Both predicates of the embedded quotation (a=ye, a=nure) carry fourth-person a= as the reported uncle's self-reference; Nakagawa notes that the actual first-person utterance would have used ku=ye and ku=nure. The narrator's own framing verb a=ye is fourth-person for the separate reason that it is the narrator's story-world first person."
	/>

	<p>
		The logophoric analysis receives support from non-folklore contexts. Bugaeva's Asahikawa data
		show the same pattern in everyday reported speech, severing the connection to elevated register
		<Ref k="bugaeva2008" p="41–44" />. Nikitina and Bugaeva develop the syntactic argument
		further, proposing that logophoric reported speech in Ainu is a structurally distinct category,
		neither indirect speech nor ordinary direct speech <Ref k="nikitina2021" />. The dialectal
		scope remains uneven: Nakagawa confirms the phenomenon for Saru and Chitose, while its
		extension to other dialects is less well documented <Ref k="nakagawa2024" p="179" /> ‹contested›.
	</p>

	<p>
		The free pronoun <i lang="ain-Latn">asinuma</i> (&lt; <i lang="ain-Latn">a=</i> +
		<i lang="ain-Latn">sinuma</i> 'oneself') is the fourth-person singular self-referential
		pronoun. Bugaeva notes that the consultant Ito Oda consistently glossed
		<i lang="ain-Latn">asinuma</i> as Japanese <span lang="ja">自分</span> 'oneself', never as
		<span lang="ja">私</span> 'I', which is consistent with a logophoric interpretation: the
		pronoun tracks the perspective of the contextually salient discourse source rather than fixing
		the speaker of the utterance <Ref k="bugaeva2008" p="41–43" />.
	</p>

	<p>
		The morphology of the logophoric use is identical to that of the honorific and narrative uses:
		<i lang="ain-Latn">a=</i>, <i lang="ain-Latn">=an</i>, and <i lang="ain-Latn">i=</i> serve
		all three. Disambiguation depends on discourse frame — quotative embedding, story narration,
		or respectful address — together with the number of the verb stem and the presence of the
		free pronoun <i lang="ain-Latn">asinuma</i> or its plural counterpart
		<i lang="ain-Latn">aoka</i>. The clusivity contrast between the exclusive
		<i lang="ain-Latn">ci=/=as/un=</i> and the fourth person is developed in
		<Xr ch="first-person-plural-and-clusivity" />. The sekor construction and the syntax of
		quotative complementation are treated in <Xr ch="quotative-complementation-sekor" />. The
		extended analysis of logophoric narration and reported discourse is in
		<Xr ch="logophoric-narration-and-reported-discourse" />.
	</p>
</S>
