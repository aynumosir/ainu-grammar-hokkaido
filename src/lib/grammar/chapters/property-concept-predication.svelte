<S t="Property words as intransitive verbs" id="overview">
	<p>
		Property-concept words — <A w="pirka" gl="be good" />, <A w="poro" gl="be big" />,
		<A w="wen" gl="be bad" />, <A w="tanne" gl="be long, tall" />,
		<A w="pon" gl="be small" />, <A w="hure" gl="be red" />, <A w="pewre" gl="be young" />,
		<A w="rui" gl="be heavy, strong" />, <A w="ri" gl="be high" /> and their class-mates —
		occupy the predicate slot of a simple clause without a copula. The clause
		<i lang="ain-Latn">cape pon</i> 'the cat is small' is structurally parallel to
		<i lang="ain-Latn">cape hoyupu</i> 'the cat ran': in both, a bare intransitive verb is
		the entire predicate <Ref k="nakagawa2024" p="118" />; <Ref k="nakagawa2022pos" p="474–475" />.
		This behavior reflects a categorical fact: Hokkaido Ainu has no dedicated adjective
		word class ‹consensus›. What English and Japanese encode in a distinct adjective category
		is covered in Ainu by a set of intransitive stative verbs. The three-criterion
		argument — morphological identity with intransitive verbs, structural parallelism in the
		prenominal slot, and evidential compatibility — is set out in
		<Xr ch="no-adjective-class-property-verbs" />; the present chapter treats the predication
		patterns that the analysis implies.
	</p>

	<p>
		Because property words are verbs, they take subject-agreement affixes, combine with the
		standard clausal negation morphology, and take the evidential tail
		<i lang="ain-Latn">ruwe ne</i> exactly as any other predicate does
		<Ref k="nakagawa2024" p="118–119" />; <Ref k="sato2008" p="20–21" />.
		A learner who writes <i lang="ain-Latn">*pirka ne</i> for '(it) is good' has imported a
		copula requirement that the grammar does not impose
		<Ref k="nakagawa2024" p="119" />. The copula
		<i lang="ain-Latn">ne</i> is restricted to predicate-nominal clauses (see
		<Xr ch="copula-ne-predicate-nominals" />) and cannot combine with a property verb.
	</p>

	<Ex
		m="opitta poro ru-we ne"
		g="everything be.big track-POSS COP"
		tr="Everything was big."
		cite="ainukoraci1991"
		dial="SAR"
		place="Maruya Kazuko 丸野和子, '「Kuma」turano k=an', アイヌタイムズ 15/1 [ainu-times/015/1#5]"
		note="The property verb poro 'be big' is the sole predicate; the evidential frame ruwe ne closes the clause exactly as it does on action verbs." />
</S>

<S t="Person marking" id="person-marking">
	<p>
		Property verbs take the intransitive subject (<em>S</em>) person affixes — the same set that
		marks the sole argument of action verbs — following the paradigm described in
		<Xr ch="person-marking-architecture" />. The prefix <i lang="ain-Latn">ku=</i> marks
		first-singular S on any intransitive stem, whether it denotes motion, existence, or a
		property:
	</p>

	<Ex
		m="sonno ku=pewre or ta ku=nu yukar ne ru-we ne"
		g="really 1SG.S=be.young place LOC 1SG.A=hear epic COP track-POSS COP"
		tr="It was when I was really young that I heard this epic."
		cite="nakagawa2024:118"
		dial="SAR"
		note="Nakagawa ex. 1 (p. 118). The 1SG.S prefix ku= on pewre 'be young' is identical to its use on ku=an 'I exist' or ku=arpa 'I go'." />

	<p>
		The full intransitive paradigm applies without adjustment. The table below illustrates with
		<A w="pirka" gl="be good" /> (Saru–Chitose); the forms are constructed on the basis of
		<Ref k="nakagawa2024" p="118, 165–167" />:
	</p>

