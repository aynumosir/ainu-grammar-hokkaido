<S t="Degree and intensification" id="degree">
	<p>
		Property verbs carry no inherent comparative or degree morphology. Degree is expressed by
		adverbs that precede the property verb without triggering any change to its form
		<Ref k="nakagawa2024" p="120" />. The most frequently recruited degree adverbs are
		<A w="sonno" gl="really, truly" />, <A w="arikinne" gl="very" />,
		<A w="poronno" gl="greatly, a lot" />, and <A w="earkinne" gl="exceedingly" />.
		The following example shows <i lang="ain-Latn">arikinne</i> scoping over an attributive
		property verb:
	</p>

	<Ex
		m="toan kur arikinne wen pe ne"
		g="that.one person very be.bad thing COP"
		tr="That person is a very bad one."
		cite="sato2008:20"
		dial="SAR"
		note="Satō ex. 4 (p. 20). The degree adverb arikinne modifies the attributive property verb wen; the whole complex wen pe serves as the nominal complement of the copula." />

	<p>
		The prefix <i lang="ain-Latn">si-</i> combines directly with the verb root for intensification:
		<i lang="ain-Latn">si-poro</i> 'really big'
		<Ref k="nakagawa2024" p="120" />.
	</p>

	<p>
		Property verbs also generate manner adverbs for other predicates through the suffix
		<i lang="ain-Latn">-no</i>: <A w="tunas" gl="be fast" /> yields
		<i lang="ain-Latn">tunasno</i> 'fast(ly)', <A w="moyre" gl="be slow" /> yields
		<i lang="ain-Latn">moyreno</i> 'slowly'
		<Ref k="nakagawa2024" p="120" />. The derived adverb quantifies the manner of the event
		described by the main predicate and is not itself a predicate. The comparative construction
		(<i lang="ain-Latn">akkari</i> 'surpassing, more than') and the full adverb system are
		treated in <Xr ch="adverbs-degree-comparison" />; the sources consulted do not provide a
		systematic degree–comparison paradigm for property verbs, and the gap is noted there.
	</p>

	<S t="Property verbs as manner auxiliaries" id="manner-auxiliaries">
		<p>
			A small set of property verbs — including <A w="moyre" gl="be slow" />,
			<A w="tunas" gl="be fast" />, and <A w="hayta" gl="be insufficient" /> — can follow
			a main predicate as a post-verbal auxiliary expressing the pace or adequacy of the
			denoted event <Ref k="nakagawa2024" p="121" />:
		</p>

		<Ex
			m="ek moyre ru-we an na"
			g="come be.slow track-POSS exist FIN"
			tr="It is slow in coming."
			cite="nakagawa2024:121"
			dial="SAR"
			note="Nakagawa §3.4.3 ex. 5. The property verb moyre follows the main predicate ek 'come' and qualifies its pace; the existential an (not the copula) closes the evidential frame, as expected for a content-question-capable sentence-final form." />
	</S>
</S>

<S t="The adjective debate" id="adjective-debate">
	<p>
		The classical treatment in <Ref k="kindaichi1936" p="123–124" /> (§§129, 189–193) recognized
		that property words inflect for person just as verbs do, yet nonetheless posited a separate
		形容詞 'adjective' class defined by its dual attributive–predicative distribution. Kindaichi
		conceded the instability of the boundary directly: "the difference between a verb and an
		adjective is razor-thin" (動詞と形容詞の差は紙一重; §129). His own person-inflection tables
		for 形容詞 reproduce the intransitive S paradigm without modification — the concession is
		already implicit in the paradigm.
	</p>

	<p>
		Chiri's reanalysis (<Ref k="chiri1942" />, summarized in <Ref k="nakagawa2022pos" p="478" />)
		proposed the 用詞 category — a class of stative verbs covering property meanings — and
		collapsed the distinction. All subsequent major grammars of Hokkaido Ainu adopt that
		position: <Ref k="refsing1986" /> classifies property words as a semantically defined subclass
		of verbs without a distinct morphosyntactic status; <Ref k="shibatani1990" p="19" /> notes
		that property words take "the same personal affixes" as intransitive verbs;
		<Ref k="sato2008" p="21" /> places them within his four-verb-type classification as a
		stative subtype; and <Ref k="nakagawa2022pos" p="474–478" /> provides the most detailed
		argument, covering all three diagnostic dimensions ‹consensus›. In terms of the cross-linguistic
		typology of intransitive predication <Ref k="stassen1997" paren />, Hokkaido Ainu is an
		unambiguous case of the verbal-property type: the coding of property meanings through
		intransitive verbs extends without exception to every diagnostic — morphological marking,
		syntactic distribution, TAM compatibility, and negation strategy.
	</p>

	<p>
		One genuinely open question concerns the interaction of property predication with the
		evidential system. Takahashi's study of Tokachi Ainu shows that individual-level (permanent)
		property predicates select the trace nominalizer <i lang="ain-Latn">ru</i> rather than
		the visual nominalizer <i lang="ain-Latn">sir</i>, because the visual form is restricted to
		stage-level (transient) predicates <Ref k="takahashi2013" p="131–133" />. This predication-type
		constraint operates on individual-level vs. stage-level readings across the verb inventory
		and is not specific to property words, but it shows that the ontological character of property
		predicates is grammatically active in Ainu. The interaction of property predicates with the
		evidential paradigm is treated in <Xr ch="ruwe-ne-inferential" /> and
		<Xr ch="evidential-schema-overview" />.
	</p>
</S>
