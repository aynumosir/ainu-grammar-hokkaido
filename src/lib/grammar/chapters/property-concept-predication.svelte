				<td><Ref k="nakagawa2024" p="118" /></td>
			</tr>
			<tr>
				<td><i lang="ain-Latn">rui apto</i></td>
				<td>heavy rain</td>
				<td><i lang="ain-Latn">apto rui</i></td>
				<td>the rain is heavy</td>
				<td><Ref k="kindaichi1936" p="123" />; <Ref k="shibatani1990" p="19" /></td>
			</tr>
		</tbody>
	</table>

	<p>
		The prenominal slot admits person-marked property verbs: <i lang="ain-Latn">ku=pewre or ta</i>
		in the example above reads as 'in the time of my being young', a headless relative clause
		with the property verb predicated of the implicit head
		<Ref k="nakagawa2024" p="118" />. This confirms that the prenominal position is a syntactic
		slot that verbs occupy, not a morphologically distinct adjectival form.
	</p>
</S>

<S t="The stative–inchoative duality" id="stative-inchoative">
	<p>
		Like other Ainu verbs, property-concept words encode both the state ('be X') and its onset
		('become X') without distinct forms; context determines the reading
		<Ref k="nakagawa2024" p="118–119" />; <Ref k="nakagawa2022pos" p="477" />.
		<i lang="ain-Latn">poro</i> means 'be big' in <i lang="ain-Latn">poro cise an</i> 'there is
		a big house' (see <Xr ch="existential-locational-an-oka" />) and 'grow big' in the
		following corpus example:
	</p>

	<Ex
		m="tane pakno e=poro ru-we ne korka"
		g="now till 2SG.S=be.big track-POSS COP but"
		tr="You have grown big, but …"
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko 川上まつ子, uwepeker 民話18, AA研アイヌ語資料 [aa-irc/025#266]"
		note="The 2SG.S prefix e= marks the single argument of poro in the inchoative reading; the evidential ruwe ne predicates the growth as a recognized fact." />

	<p>
		The inchoative reading is contextual, not morphological. It follows that the copula
		<i lang="ain-Latn">ne</i> cannot be inserted to force an inchoative:
		<i lang="ain-Latn">*poro ne</i> for 'become big' is ungrammatical in Hokkaido Ainu
		<Ref k="nakagawa2024" p="119" />. Where the copula does appear after an inchoative-type
		expression (as in <i lang="ain-Latn">nispa ne</i> 'become a chief'), the complement is a
		nominal, not a property verb. The copula's own stative–inchoative duality belongs to the
		predicate-nominal system treated in <Xr ch="copula-ne-predicate-nominals" />.
	</p>

	<p>
		A community discussion noted that both <i lang="ain-Latn">ne an</i> and
		<i lang="ain-Latn">pirka an</i> carry 'become' readings, raising the question whether the
		existential <i lang="ain-Latn">an</i> contributes an inchoative-aspect value in such
		environments
		<Ref k="aynucorporadiscord" /> (nukopoli, aynu-corpora Discord, November 2024) ‹speculative›.
		The aspectual uses of <i lang="ain-Latn">an</i> as a periphrastic auxiliary are set out in
		<Xr ch="existential-aspect-an-oka" />.
	</p>
</S>

<S t="Negation" id="negation">
	<p>
		Property verbs follow the standard clausal negation strategies of intransitive verbs: the
		negator <i lang="ain-Latn">somo</i> precedes the verb
		(<i lang="ain-Latn">somo V</i>), or the frame
		<i lang="ain-Latn">V ka somo ki</i> is used for contrastive or emphatic negation
		<Ref k="nakagawa2024" p="127" />; <Ref k="sato2008" p="21–22" />:
	</p>

	<Ex
		m="merayke=an ka somo ki no"
		g="be.cold=4.S even NEG do CONJ"
		tr="not feeling cold, and …"
		cite="nakagawa2024:127"
		dial="SAR"
		note="Nakagawa ex. 15 (p. 127, 表12). The 4th-person S suffix =an indexes the subject of the experiencer predicate merayke 'feel cold'; ka somo ki frames the negation contrastively." />

	<p>
		These patterns contrast with the dedicated frames required by the copula and the existential
		<Ref k="sato2008" p="22" />; <Ref k="nakagawa2024" p="407, 450" />. The copula requires
		<i lang="ain-Latn">X ka somo ne</i> (the negator placed before
		<i lang="ain-Latn">ne</i> itself), and the existential <i lang="ain-Latn">an</i> recruits
		its suppletive negative <i lang="ain-Latn">isam</i> 'not exist'. The property verb is
		the only non-dynamic predicate type that takes ordinary clausal negation, a direct reflex
		of its status as an ordinary intransitive verb. The full scope of negation across predicate
		types is treated in <Xr ch="negation-somo-standard-clausal" /> and
		<Xr ch="nonverbal-predicate-tam-negation" />.
	</p>

