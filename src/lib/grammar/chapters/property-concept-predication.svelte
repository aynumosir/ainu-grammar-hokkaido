	<p>
		The third-person cell is zero-marked, as for all intransitive verbs; the fourth-person
		(INDEF) uses the enclitic <i lang="ain-Latn">=an</i>. No object affix can appear: property
		verbs are strictly intransitive and take no object slot. A member of the aynu-corpora
		community observed that the zero-marked (3rd-person) form of <i lang="ain-Latn">pirka</i>
		shifts contextually toward 'that's fine / it's OK' when no overt subject is present, while
		person-marked forms like <i lang="ain-Latn">e=pirka</i> read as a direct evaluation of the
		indexed person ('you are beautiful, in good shape')
		<Ref k="aynucorporadiscord" /> (nukopoli, aynu-corpora Discord, November 2024) ‹contested›.
	</p>
</S>

<S t="Attributive and predicative positions" id="attributive-predicative">
	<p>
		Property verbs appear in two syntactic positions: as bare predicates heading the main clause,
		and as prenominal modifiers preceding a noun head. The morphological form is identical in
		both — a fact recognized since <Ref k="kindaichi1936" p="123" /> ‹consensus›. In predicative
		position the property verb alone constitutes the predicate:
	</p>

	<Ex
		m="toan nupuri ri"
		g="that mountain be.high"
		tr="That mountain is high."
		cite="kindaichi1936:123"
		dial="HK"
		note="Kindaichi &amp; Chiri §189B. Predicative property verb with zero third-person subject marking." />

	<p>
		In attributive position the same verb precedes the noun head with no morphological change —
		no suffix, no linker, no case marker. This is structurally parallel to a prenominal relative
		clause in Ainu, where the predicate verb likewise immediately precedes the head noun
		<Ref k="nakagawa2022pos" p="475" />; see <Xr ch="prenominal-gap-relative-clauses" />:
	</p>

	<table>
		<thead>
			<tr>
				<th>attributive (prenominal)</th>
				<th>gloss</th>
				<th>predicative</th>
				<th>gloss</th>
				<th>source</th>
			</tr>
		</thead>
		<tbody>
			<tr>
				<td><i lang="ain-Latn">pon hekaci</i></td>
				<td>small child</td>
				<td><i lang="ain-Latn">hekaci pon</i></td>
				<td>the child is small</td>
				<td><Ref k="kindaichi1936" p="123" /></td>
			</tr>
			<tr>
				<td><i lang="ain-Latn">poro cise</i></td>
				<td>big house</td>
				<td><i lang="ain-Latn">cise poro</i></td>
