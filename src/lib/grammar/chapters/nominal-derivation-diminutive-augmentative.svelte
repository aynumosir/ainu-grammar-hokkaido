<S t="Semantic values: diminutive and affective" id="semantics">
	<p>
		Kindaichi and Chiri describe <A w="-po" /> as a <span lang="ja">指小辞</span>
		meaning 'small thing / dear thing' and trace it etymologically to the free noun
		<A w="po" gl="child, son" /> <Ref k="kindaichi1936" p="25" />. Two semantic values are
		present in the attested data and are recognized by subsequent treatments
		<Ref k="nakagawa2024" p="264–265" />; <Ref k="ijas2023" />.
	</p>
	<p>
		The <em>diminutive</em> value encodes that the referent is smaller than the prototype of its
		class. For <i lang="ain-Latn">ceppo</i> 'small fish' this is size in the physical sense;
		for <i lang="ain-Latn">supuyapo</i> 'faint smoke' it is reduced quantity or intensity
		rather than spatial extent — a scalar application of the suffix to a non-count noun.
		The <em>affective / hypocoristic</em> value encodes endearment or intimacy without implying
		reduced size. Kindaichi's example <i lang="ain-Latn">ku=sampepo</i> 'my dear one' (lit.
		'my little heart') illustrates pure affect: the referent is a person, not a physically small
		object <Ref k="kindaichi1936" p="25" />. The two values are not in complementary
		distribution — the same form carries both potential meanings — and context selects the
		dominant reading.
	</p>
</S>

