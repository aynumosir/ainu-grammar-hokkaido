<S t="The diminutive suffix -po and the absence of an augmentative" id="overview">
	<p>
		The principal derivational suffix operating on Hokkaido Ainu nouns for size and affect is
		<A w="-po" gl="diminutive suffix" />, which attaches to a noun stem to yield a derivative
		meaning 'small X' or 'dear X' <Ref k="nakagawa2024" p="264–265" />;
		<Ref k="kindaichi1936" p="25" />; <Ref k="ijas2023" />. No productive augmentative suffix —
		a suffix encoding 'large X' — is described for Hokkaido Ainu in any of the grammatical
		treatments consulted. Upward size modification relies on the prenominal property verb
		<A w="poro" gl="be big" />, used attributively before a noun without morphological change
		(see <Xr ch="no-adjective-class-property-verbs" />). Morphological encoding of size is
		therefore asymmetric: the diminutive has a suffix, the augmentative does not ‹consensus›.
	</p>
	<p>
		Three features of <A w="-po" /> merit individual treatment: its etymological identity with
		the free noun <A w="po" gl="child, son" />, its lexicalization into a subset of animal
		names where the base is no longer a free morpheme, and a Horobetsu colloquial meaning
		extension to 'deceased (intimate)' not reported for other dialects
		<Ref k="kindaichi1936" p="25" />.
	</p>
</S>

<S t="Morphology" id="morphology">
	<p>
		<A w="-po" /> attaches to the <Xr ch="concept-form-affiliative-form">concept form</Xr> of a
		noun stem. The output is itself a common noun in the concept form and may take the affiliative
		suffix in the normal way <Ref k="nakagawa2024" p="264" />. Nakagawa explicitly excludes the
		suffix from <Xr ch="noun-noun-compounding">compound-noun formation</Xr>: a form such as
		<i lang="ain-Latn">cikappo</i> is a modifier-derived nominal, not a compounding element, and
		the person-affix and affiliative-suffix diagnostics that distinguish compounds from
		modifier–noun sequences classify <A w="-po" /> derivatives as single-word outputs that
		function as ordinary free nouns <Ref k="nakagawa2024" p="265" />. The form is invariant
		across the Hokkaido dialects documented in the sources read here, and the suffix integrates
		phonologically with no reported junctural changes.
	</p>

