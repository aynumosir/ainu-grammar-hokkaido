<S t="Lexicalization in animal names: isepo and the class-term question" id="animal-names">
	<p>
		Several Hokkaido Ainu animal names contain a synchronically opaque
		<i lang="ain-Latn">-po</i> where the base noun is not independently attested as a free
		form. The best-known case is <A w="isepo" gl="rabbit" />. Two analyses have been proposed
		in community scholarship, both identifying <i lang="ain-Latn">-po</i> as the diminutive
		suffix <Ref k="aynucorporadiscord" />.
	</p>
	<p>
		One analysis holds that <i lang="ain-Latn">ise</i> is an onomatopoeia for the rabbit's
		high-pitched call, so <i lang="ain-Latn">isepo</i> originated as 'the small/dear thing
		that goes <i>ise</i>' — an expressive base plus the diminutive (nukopoli, aynu-corpora
		Discord, 2023-12-11; palestine_2026, 2024-08-28) <Ref k="aynucorporadiscord" /> ‹speculative›.
		A related line invokes the productive pattern by which an onomatopoeic base followed by
		<i lang="ain-Latn">-se</i> yields a verb (compare <i lang="ain-Latn">pawse</i>
		'a fox calls', where <i lang="ain-Latn">paw</i> is the cry, and
		<i lang="ain-Latn">rimse</i> 'dance'), and treats <i lang="ain-Latn">ise</i> in
		<i lang="ain-Latn">isepo</i> as the base of such a nominalized verb with
		<i lang="ain-Latn">-po</i> added (palestine_2026, 2024-08-28)
		<Ref k="aynucorporadiscord" /> ‹speculative›. Neither analysis has been confirmed by a read
		primary grammatical source; the status of <i lang="ain-Latn">ise</i> as an independent
		morpheme in contemporary Hokkaido Ainu is unclear ‹speculative›.
	</p>
	<p>
		The broader pattern of class-term residues in animal and plant names — where a frozen
		element meaning 'bird', 'fish', or 'tree' is embedded in a lexicalized form — is treated
		in <Xr ch="noun-noun-compounding" />. The <i lang="ain-Latn">-po</i> pattern is
		structurally distinct: rather than a class-term head retained from a transparent compound,
		it is a derivational suffix that appears to have frozen at an earlier productive stage, once
		the base <i lang="ain-Latn">ise</i> lost independent currency.
	</p>
</S>

<S t="The okkaypo ambiguity: suffix versus noun po 'child'" id="okkaypo">
	<p>
		The suffix <A w="-po" /> is homophonous with the free noun <A w="po" gl="child, son" />,
		and Nakagawa attributes the homophony to a shared etymology
		<Ref k="nakagawa2024" p="265" />. In <i lang="ain-Latn">okkaypo</i> the ambiguity is
		productive and dialect-sensitive. In Saru and Chitose, <i lang="ain-Latn">okkay</i> does
		not occur as a free noun — it appears only inside derivatives — so
		<i lang="ain-Latn">okkaypo</i> parses unambiguously as [bound root
		<i lang="ain-Latn">okkay</i> + DIM] 'young man'. In dialects where
		<i lang="ain-Latn">okkay</i> is a free noun meaning 'man', the form is genuinely
		ambiguous: [<i lang="ain-Latn">okkay</i> + DIM] 'young man' versus
		[<i lang="ain-Latn">okkayo</i> 'male' + <i lang="ain-Latn">po</i> 'child'] = 'son'. The
		unambiguous form for the N+N reading in those dialects is <i lang="ain-Latn">okkayopo</i>
		with the full base <Ref k="nakagawa2024" p="265" />.
	</p>

	<Ex
		m="okkay-po utar"
		g="man-DIM ASS"
		tr="young men (Saru: unambiguously DIM reading)"
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko 川上まつ子, isoytak 独話1, ILCAA Ainu materials [aa-irc/003#26]"
	/>

	<p>
		The free noun <A w="po" gl="child, son" /> has an affiliative form
		<i lang="ain-Latn">poho</i> and enters the kinship paradigm as an inalienably-possessed
		term: <i lang="ain-Latn">po:poho</i> 'child — one's child'
		<Ref k="nakagawa2024" p="222" />. See <Xr ch="kinship-honorific-possession" /> and
		<Xr ch="concept-form-affiliative-form" />. The grammaticalization from free noun to
		suffix plausibly runs through a modifier-like compounding stage ('small/dear child of' →
		'small/dear instance of'), consistent with the shared affective semantics, though no
		read source provides a detailed trajectory ‹speculative›.
	</p>
</S>

<S t="Horobetsu: -po on an intimate as 'deceased'" id="horobetsu">
	<p>
		Kindaichi records a Horobetsu colloquial pattern in which <A w="-po" /> attached to a term
		denoting an intimate acquires a 'deceased' reading:
		<i lang="ain-Latn">ku=akipo</i> 'my late younger brother' and
		<i lang="ain-Latn">ku=kor operpo</i> 'my late daughter'
		<Ref k="kindaichi1936" p="25" />. The semantic path from 'dear' to 'dearly departed' is
		pragmatically transparent — the affective value of the suffix bleeds into memorializing
		reference — but the extension is attested only in Horobetsu colloquial in the sources
		read here, and no modern grammar corroborates it for other Hokkaido dialects. It is
		recorded as a single-source, dialect-specific datum ‹corpus-suggested›.
	</p>
</S>

<S t="Size modification without suffixation: prenominal pon and poro" id="prenominal-size">
	<p>
		The grammatical resources for noun-level size reference extend beyond <A w="-po" />. The
		prenominal property verbs <A w="pon" gl="be small" /> and <A w="poro" gl="be big" />
		modify a following noun in the same position that all attributive stative verbs occupy,
		without morphological change <Ref k="nakagawa2024" p="264" /> (see
		<Xr ch="no-adjective-class-property-verbs" /> and <Xr ch="noun-phrase-structure" />).
	</p>
	<p>
		Three distinctions between the suffixal and prenominal strategies matter grammatically.
		First, <A w="-po" /> derives a new lexical item — a common noun in the concept form —
		while the prenominal is a syntactic modifier that can be independently relativized, negated,
		and causativized: <i lang="ain-Latn">porore</i> 'make big' (causative of the property verb)
		has no morphological parallel on a DIM derivative. Second, the corpus examples above show
		<i lang="ain-Latn">pon</i> stacking freely with DIM derivatives
		(<i lang="ain-Latn">pon cikappo</i>, <i lang="ain-Latn">pon ceppo</i>), confirming that
		the two strategies are grammatically independent. Third, the suffix asymmetry —
		<A w="-po" /> for small, no suffix for large — is not mirrored at the prenominal level,
		where <A w="poro" /> covers large size just as freely as <A w="pon" /> covers small.
		The sources do not explain the origin of this one-sided suffix system ‹consensus›.
	</p>
	<p>
		For the wider system of noun-deriving morphology, including deverbal noun formation and
		the productive nominalizer suffixes, see <Xr ch="deverbal-denominal-noun-derivation" />.
		For verbal number suppletives whose selection interacts with nouns headed by DIM derivatives,
		see <Xr ch="suppletive-verbal-number" />.
	</p>
</S>
