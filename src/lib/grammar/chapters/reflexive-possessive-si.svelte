	<p>
		The direct/indirect analysis (line four) is the framework Nakagawa (2024: 189) and the
		Handbook adopt as the current working account ‹contested›. Satō (2007: ≈31–38) builds it
		from three paradigm cases: <A w="si-nuye" gl="have oneself tattooed" /> 'have oneself
		tattooed' (traditional Ainu tattooing required another person to do the work);
		<A w="si-kasuy-re" gl="have others help oneself" /> 'have others help oneself' (the help
		comes from outside); and <A w="si-ranpewtek-ka" gl="pretend not to know" /> 'pretend not
		to know' (the pretence succeeds only if another observer believes it). In each case the
		subject's reflexive involvement is mediated by another party's action or perception. Satō
		himself qualifies the account as not yet established
		(<span lang="ja">定説ではない</span>) <Ref k="sato2007" />.
	</p>
	<p>
		None of the earlier analyses is fully discredited. The intentionality framing (Kindaichi,
		Tamura) captures the spontaneous reading of <A w="si-pusu" /> but not the pretend-to
		construction, where the subject acts with full intent. The agentive/patientive framing
		(Kirikae 1994: 316, reported by <Ref k="sato2007" />) separates the forms at the
		thematic role level, but feigning sleep — <A w="si-mokor-e" gl="pretend to sleep" /> —
		is agentive and falls outside the patientive characterisation. The argument-structural
		restriction that Kobayashi (2008: 140) derives — <A w="si-" /> occupies Theme or Patient
		but never causee — correctly excludes causee arguments but does not explain the opposition
		between <A w="si-pusu" /> and <A w="yay-pusu" />, where both occupy a Theme role
		<Ref k="kobayashi2010" />.
	</p>
	<p>
		The Handbook extends the empirical base with two additional minimal pairs:
		<A w="yay-tuy-tuy-e" gl="brush dust off oneself" /> versus
		<A w="si-tuy-tuy-e" gl="shake off (while becoming a bird)" /> — where the latter signals a
		shedding of human clothes driven by the subject's transformation into bird form, an outward
		change not self-initiated — and <A w="yay-etaye" gl="draw oneself outward" /> 'draw
		oneself out' versus <A w="si-etaye" gl="withdraw, pull back" /> 'withdraw, pull back in
		response to another's approach' <Ref k="bugaevakobayashi2022" p="§4.2.2" />.
	</p>
	<p>
		A discussion on the aynu-corpora Discord (nukopoli, <Ref k="aynucorporadiscord" />, 2024)
		labels the contrast as anticausative (<A w="si-" />) versus reflexive proper
		(<A w="yay-" />), on the grounds that <A w="si-" /> marks 'something that happens to the
		subject through another force' and <A w="yay-" /> marks 'the subject acting on itself
		directly'. The labelling is consistent with the Satō analysis and invokes an explicit
		anticausative category ‹contested›. A separate thread (aomidori_cs_,
		<Ref k="aynucorporadiscord" />, 2023) proposed that one prefix may originally have encoded
		a voice distinct from reflexive proper, without adducing corroborating evidence
		‹speculative›.
	</p>

