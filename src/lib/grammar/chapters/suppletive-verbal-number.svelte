	<p>
		In Corbett's typology of number the category is verbal number proper: a value on the predicate
		keyed to participant or event plurality, set apart from the agreement number of a nominal
		controller <Ref k="corbett2000" paren />. The pattern is cross-linguistically uncommon, and
		Nakagawa places Ainu among the few languages of northern Eurasia that carry it, with the isolates
		Ket and Burushaski; even within Ainu the opposition reaches a minority of the verbal lexicon
		<Ref k="nakagawa2024" p="141" />; <Ref k="nakagawa2022" /> ‹consensus›. Ijäs's learner grammar
		states the same architecture for the classroom: nouns do not inflect for number, and a subset of
		verbs have distinct singular and plural forms, set out in her Lesson 9 <Ref k="ijas2023" />. Her
		example sentences are constructed teaching material rather than corpus attestations.
	</p>
</S>

<S t="Type A: suppletive stems" id="type-a">
	<p>
		Type A is total stem alternation: the singular and plural members are lexically distinct. The
		class is small and centres on basic existence, posture, and motion verbs, with a handful of
		transitives of handling and affected change <Ref k="nakagawa2024" p="146–147" />;
		<Ref k="sato2008" p="102" />. Dialect membership is part of each entry. Saru, Chitose, Mukawa, and
		Shizunai have <i lang="ain-Latn">oka</i> beside <i lang="ain-Latn">okay</i> elsewhere; Saru and
		Chitose have <i lang="ain-Latn">arpa</i> for 'go' where other dialects often use
		<i lang="ain-Latn">oman</i>; and Tokachi has no singular partner to <i lang="ain-Latn">ronno</i>
		'kill', so it treats that verb as number-invariant <Ref k="nakagawa2024" p="146–147" />.
	</p>

