	<p>
		Type C replaces a stem-final vowel with <i lang="ain-Latn">-pa</i>. It is the most populous class
		and consists mainly of transitives denoting a change of state in the object; many pair with a
		same-root intransitive whose S corresponds to the transitive O <Ref k="nakagawa2024" p="149–151" />;
		<Ref k="nakagawa2022" />. The change-of-object semantics is a strong tendency with lexical
		exceptions: <i lang="ain-Latn">piru</i> / <i lang="ain-Latn">pirpa</i> 'wipe',
		<i lang="ain-Latn">suwe</i> / <i lang="ain-Latn">supa</i> 'boil', and
		<i lang="ain-Latn">ani</i> / <i lang="ain-Latn">anpa</i> 'hold, carry' impose no change on the
		object and have no intransitive partner <Ref k="nakagawa2024" p="149–150" />.
	</p>

