	<p>
		Derived transitives inherit the number relation of the base that supplies their affected
		participant. A transitive built on an intransitive keeps subject-type agreement, as with
		<i lang="ain-Latn">hosippa</i> 'return.PL' (&lt; <i lang="ain-Latn">ho-sipi</i>); a causative in
		<i lang="ain-Latn">-re</i> tracks the causee, which is the original intransitive subject, so
		<i lang="ain-Latn">hopunire</i> is used for sending off one slain bear and
		<i lang="ain-Latn">hopunpare</i> for three wolves <Ref k="nakagawa2024" p="145–146" />. The Type-C
		suffix <i lang="ain-Latn">-pa</i> is therefore a stem-forming, root-bound suffix, the pa1 of the
		Handbook account, and is kept apart from the outer plural clitic pa2 treated next; its place in the
		verb word is set out in <Xr ch="verb-word-template" />, and the derivation in
		<Xr ch="causative-morphological-re-e-te" /> <Ref k="nakagawa2022" />.
	</p>
