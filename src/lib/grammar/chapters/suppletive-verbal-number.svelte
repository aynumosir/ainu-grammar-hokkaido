<S t="The category and its locus" id="category">
	<p>
		A small, high-frequency class of Hokkaido Ainu verbs distinguishes a singular from a plural
		stem. The contrast is carried by the verb: ordinary nouns are number-neutral, and the plurality
		of a participant is read from context, quantification, or human collectives such as
		<A w="utar" gl="people, fellows" />, while the verb itself is the only locus at which number is
		grammaticalized <Ref k="nakagawa2024" p="124–125, 140–141" />; <Ref k="sato2008" p="99–101" />;
		<Ref k="shibatani1990" p="51–54" />; <Ref k="nakagawa2022" />. The nominal side of that division
		belongs with <Xr ch="nominal-number-utar-transnumerality" />, the position of the stem and the
		plural elements within the verb word with <Xr ch="verb-word-template" />, and the consequences
		for the personal indexes with <Xr ch="person-number-interactions" />.
	</p>

	<p>
		Transitivity fixes which argument the plural stem tracks. An intransitive verb indexes the
		number of S, its sole argument, and the relation is close to agreement: a plural subject index
		and a plural subject phrase take the plural stem, a singular one the singular stem. A transitive
		verb indexes the plurality of O, or of the action construed through O, independently of subject
		number; the singular stem is the default and surfaces even with an overtly plural object
		<Ref k="nakagawa2024" p="142–145" />; <Ref k="sato2008" p="101" />. Following Durie, Shibatani
		states the cross-class generalization as selection of the principally affected argument — S of an
		intransitive, O of a transitive — rather than as agreement <Ref k="shibatani1990" p="51–53" />.
		Nakagawa accepts the S/O split but reads the intransitive side as agreement-like and reserves the
		selectional, marked character for the transitive side, so the agreement-versus-selection question
		is best left open <Ref k="nakagawa2024" p="145" /> ‹contested›.
	</p>

