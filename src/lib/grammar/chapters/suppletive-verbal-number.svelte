<S t="Types B and C: segmentable alternations" id="types-b-c">
	<p>
		Two further patterns have a segmentable exponent and a wider, though still lexically bounded,
		membership. Type B alternates a final <i lang="ain-Latn">-n</i> in the singular with
		<i lang="ain-Latn">-p</i> in the plural, and is confined to intransitives of directed motion. The
		matching transitives are derived with singular <i lang="ain-Latn">-ke</i> (or
		<i lang="ain-Latn">-ka</i>) and plural <i lang="ain-Latn">-te</i>, and the number relation runs
		off the moving participant, with <i lang="ain-Latn">rikinka</i> an exception to the
		<i lang="ain-Latn">-ke</i> shape <Ref k="nakagawa2024" p="148–149" />.
	</p>

