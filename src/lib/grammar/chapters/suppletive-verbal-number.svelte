	<p>
		The clitic primarily indexes participant plurality, especially of the agent, which is the device
		behind one cross-linguistically unusual property of Ainu: plural-marked transitives can co-occur
		with a plural transitive subject, as in <i lang="ain-Latn">i=res-pa</i> 'they raised me'
		<Ref k="shibatani1990" p="53" />. The eastern <i lang="ain-Latn">ci</i> behaves the same way and
		gives the inside/outside test directly: in Horobetsu it is a fossilized suffix inside the person
		clitic (<i lang="ain-Latn">iki-ci=as</i>), while in Shizunai it is a productive auxiliary outside
		it (<i lang="ain-Latn">hotke=an ci</i> 'we slept') <Ref k="nakagawa2024" p="154–155" />. The
		interaction of these elements with the second-person-plural index is taken up in
		<Xr ch="eci-portmanteau-and-second-plural" />.
	</p>

	<p>
		The status of pa2 is disputed. Nakagawa and the Handbook treat it, eastern
		<i lang="ain-Latn">ci</i>, and Sakhalin <i lang="ain-Latn">hci</i> as a single clitic class
		distinct from the stem suffix, on the positional evidence above, with Sakaguchi reaching the same
		conclusion for <i lang="ain-Latn">hci</i> <Ref k="nakagawa2024" p="156–159" />;
		<Ref k="nakagawa2022" />. Satō's Chitose data make the clitic analysis less secure there:
		<i lang="ain-Latn">=an pa</i> is rare, a consultant rejected <i lang="ain-Latn">*yaynu-as pa</i>
		in favour of the bound <i lang="ain-Latn">yaynupa-as</i>, and <i lang="ain-Latn">okaypa</i> is
		marginal <Ref k="sato2025b" p="25–26" /> ‹contested›. The descriptive generalization is robust: the
		postverbal marker supplies plurality where the stem does not, and adds emphatic or event-plural
		force where the stem already carries number; Nakagawa reads forms such as
		<i lang="ain-Latn">kuspare</i> and <i lang="ain-Latn">korpare</i> as evidence that pa2 is drifting
		toward affixhood.
	</p>

	<p>
		The Sakhalin marker serves as a contrast. Murasaki analysed <i lang="ain-Latn">-hci</i> as a
		third-person plural affix, but its optionality and its appearance with non-third-person reference
		led Nakagawa to class it with optional plural markers rather than person indexes
		<Ref k="murasaki1979" p="49–52" />; <Ref k="nakagawa2024" p="153–155" />. It can over-mark a stem
		that is already suppletively plural.
	</p>

	<Ex
		m="Orohko-utah ariki=hci"
		g="Orok-ASS come.PL=PL"
		tr="The Oroks came."
		cite="shibatani1990:53"
