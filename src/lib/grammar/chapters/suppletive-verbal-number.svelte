<S t="The postverbal plural clitics" id="plural-clitics">
	<p>
		Plurality is also marked outside the stem. Western Hokkaido — Saru, Chitose, Ishikari — uses the
		postverbal element pa2; eastern Hokkaido uses <i lang="ain-Latn">ci</i>; and Sakhalin has
		<i lang="ain-Latn">=hci</i> / <i lang="ain-Latn">=ahci</i>, the three in rough complementary
		distribution <Ref k="nakagawa2024" p="151–159" />; <Ref k="nakagawa2022" />. Reduplication occupies
		the same descriptive neighbourhood but ordinarily contributes iterative, continuative, or intensive
		lexical aspect, and stays outside the number category proper <Ref k="nakagawa2024" p="151–153" />.
		The diagnostic that separates the root-bound suffix pa1 from the clitic pa2 in Saru is position.
	</p>

