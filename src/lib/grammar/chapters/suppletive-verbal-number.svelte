	/>
</S>

<S t="Mismatches between semantic and stem number" id="mismatches">
	<p>
		Two environments separate semantic number from stem number. A semantically singular referent
		takes the plural stem in the polite imperative: a bare singular stem commands one addressee
		(<i lang="ain-Latn">ek!</i> 'come!'), but the polite imperative in <i lang="ain-Latn">yan</i>
		requires the plural stem even for one addressee, so <i lang="ain-Latn">arki yan</i> is well formed
		and <i lang="ain-Latn">*ek yan</i> is not <Ref k="nakagawa2024" p="160" />. The honorific fourth
		person used for a single respected addressee, and in many dialects a single story protagonist in
		the fourth person, likewise take the plural stem; these are person-system effects, routed to
		<Xr ch="imperative-and-prohibitive" />, <Xr ch="fourth-person-honorific" />, and
		<Xr ch="person-number-interactions" />.
	</p>

	<Ex
		m="hokure hokure ape sam ta arki yan"
		g="come.on come.on fire side LOC come.PL IMP.POL"
		tr="Please come, come near the fire."
		cite="ilcaa1976"
		dial="SAR"
		place="Kawakami Matsuko, uwepeker 18 [aa-irc/025#224]"
		note="The polite imperative yan selects the plural stem arki; the plural morphology belongs to the directive register, not to a plural addressee."
	/>

	<p>
		The reverse mismatch is a semantically plural subject specified by a numeral, which ordinarily
		takes the singular intransitive stem. Tamura's rule of thumb, reported through Nakagawa, is that
		two, three, and sometimes four favour the singular <Ref k="nakagawa2024" p="160–162" />. The
		threshold is not firm: the culturally salient <i lang="ain-Latn">iwan</i> 'six' often takes the
		singular as well, and the quantifier <i lang="ain-Latn">poronno</i> 'many' favours the plural yet
		is recorded with singular predicates by both Nakagawa and Satō, so no predictive rule covers the
		full range of numeral, quantifier, dialect, and stance <Ref k="nakagawa2024" p="161–162" />;
		<Ref k="sato2008" p="101" /> ‹contested›. The quantifier syntax itself belongs with
		<Xr ch="numeral-classifiers-ordinals-and-quantifier-syntax" />.
	</p>

