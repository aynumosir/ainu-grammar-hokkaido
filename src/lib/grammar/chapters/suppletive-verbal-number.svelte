		m="re cise as wa an"
		g="three house stand.SG CONJ exist.SG"
		tr="Three houses are standing."
		cite="nakagawa2024:161"
		dial="SAR"
		place="Nakagawa ex. 89, after Tamura 1988a"
		note="The numeral re 'three' specifies a plural subject, yet both the posture stem as and the existential stem an are singular."
	/>

