	<p>
		The transitive rows are the reason a simple subject-agreement rule fails. A plural object index or
		a plural object phrase does not force the plural stem: Nakagawa's <i lang="ain-Latn">a=eci=rayke</i>
		'I kill you (pl)' keeps singular <i lang="ain-Latn">rayke</i> under an overtly plural object, and
		one narrator tells the same hunting formula with singular <i lang="ain-Latn">rayke</i> and with
		plural <i lang="ain-Latn">ronnu</i> for the same event <Ref k="nakagawa2024" p="143–145" />. Satō
		reaches the same conclusion, that the conditions on the transitive plural stem remain partly
		obscure <Ref k="sato2008" p="101" /> ‹contested›. A few transitive pairs depart from the O-locus:
		<i lang="ain-Latn">eram(u)an</i> / <i lang="ain-Latn">eramuoka</i> 'understand', attested as a
		pair only in Saru, Chitose, and Shizunai, tracks the number of the subject.
	</p>

